    </p>
    <p class="suojin">
      這是客家流行歌手黃連煜，結合貳樓樂團與陳昇的新寶島康樂隊，在11/11（東京）、11/13（東京）、11/14（橫濱）、11/30（臺北）分別舉行四場的亞洲巡迴會。其中第四場11/30在臺北三創生活園區5樓clapper
      studio舉行的臺北場演唱會，是納入臺灣客家音樂節的首場節目。由於黃連煜的客家歌手身份與輩份，長期累積的樂迷，以及陳昇那無拘無束的搖滾樂風，成功帶動了樂迷買票入場聆聽。雖然該演唱會並非完全客家音樂曲目，但也打響了臺灣客家音樂節的首場演出與參與度。
    </p>
    <h4>（二）臺三線客家音樂地圖音樂會</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;演出人員：馬修連恩<br>
    
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時間：2019年12月7日19:30<br>
    
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;地點：苗北藝文中心演藝廳<br>
    
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;入場說明：索票入場
    </p>
    <p class="suojin">
      馬修連恩（Matthew Carl
      Lien）加拿大籍音樂家。以製作大自然為主題的音樂著稱，有「環保音樂家」的美譽。近年來深入原住民及客家文化，其成就備受肯定。2000年受高雄縣政府任命為「原住民文化大使」對於臺灣原住民方面的工作不宜餘力；2002年榮獲加拿大西海岸音樂「育空年度藝人獎」；2007年與謝宇威主持的《台灣客翻天》入圍金鐘獎三項大獎；客家音樂作品《義民塚》獲選為電影《一八九五》的主題曲。曾發行的音樂專輯有：《狼》、《海角一樂園》、《馴鹿宣言》、《美麗新世界》、《水世紀》、《靈光乍現》、《旅客》等。
    </p>
    <p class="suojin">
      馬修連恩是一個對於「文化」很著迷的人，很喜歡接觸不同的文化。過去常深入原住民村落，製作了以原住民為主題的音樂。後來因為好友謝宇威的關係，開始接觸到客家文化，並一起合作創作了一些客家歌曲，後來又因客家委員會前主委的邀請參加了「a-ha
      客家藝術節」，讓他對於客家文化有更深一層的認識。然而在這個過程中他發現到，雖然臺灣有很多的客家音樂家，但每一次參與這類客家音樂活動時，看到的總是幾個熟悉的面孔。所以馬修連恩透過這場<span class="bkt">「臺三線客家音樂地圖音樂會」</span> 到臺三線的北部客屬文化區去發掘那些生活在你我周圍的客家音樂，目的是讓更多人認識這些優秀的客家音樂家，並了解豐富的客家文化。
    </p>
    <p class="suojin">
      在這場「臺三線客家音樂地圖音樂會」中，他將音樂會分成上下半場，前半場帶出了當地的客家歌手如曾雅君、官靈芝、米莎、黃子軒、Yappy與山歌老師賴仁政等人，客家委員會主委李永得也獻聲情義相挺，馬修誇他聲音純樸厚實。個人認為下半場才是《浪漫臺三線－馬修連恩的客家音樂地圖》的精華，透過北部客家聚落的尋幽探察客家人的歌，馬修一首首的呈現自彈自唱一個加拿大人與臺三線客家人的對話。這個節目製作的方向就是外國人眼中的臺三線客家音樂世界。
    </p>
    <h4>（三）2019全國客家合唱比賽</h4>
    <p class="pa4">
      演出人員：合唱類（兒少組、青少組、社會組及樂齡組）、傳統山歌類及現代阿卡貝拉類共計3類6組之合唱團體<br>
      時間：2019年12月7日至8日<br>
      地點：新竹縣政府文化局演藝廳
    </p>
    <p class="suojin">
      為推廣客家語言文化，藉由傳唱客家歌曲，提升客家語言文化與精神之能見度，增進客家族群認同及各族群彼此尊重、欣賞，豐富臺灣多元文化，客家委員會特辦理全國性跨族群的「客家合唱比賽」，分為「合唱類兒少組」、「合唱類青少組」、「合唱類社會組」、「合唱類樂齡組」、「傳統山歌類」及「現代阿卡貝拉類」共計3類6組，比賽將於12月7日至8日在新竹縣政府文化局演藝廳辦理。客家語言復甦與永續發展一直是客委會推動客家文化最重要的任務，藉由跨越年齡、族群的音樂做為文化傳播媒介，透過「2019客家合唱比賽」的音樂感動，建構屬於全民的客家音樂記憶。
    </p>
    <p class="suojin">
      全國客家合唱比賽，雖然不是首次舉行，但分成傳統山歌類、合唱類與阿卡貝拉等3類，卻是2019年之創舉。第1類之傳統山歌類合唱比賽，主要是鼓勵全臺各地有合唱能力之客家歌謠班或客家山歌班，組隊參加比賽。今年共有6隊參加，分別是:全球聲韻歌劇團、竹友混聲合唱團、新竹市客家文化發展協會、大襟客客家文化藝術協會、臺中市客家文化發展協會與高雄市客家好音圓合唱團。比賽結果，其中以竹友混聲合唱團表現最優；第2類之合唱類比賽，是針對登記在案的全國社會組合唱團，或客籍合唱團，以及全國各級學校所屬合唱團參加。第2類又分成合唱類樂齡組（12隊）、合唱類兒少組（13隊）、合唱類社會組（14隊）與合唱類青少組（10隊）。其中又以社會組競爭最為激烈，每團的演唱技巧與合聲演繹，難分軒輊；至於第3類之「現代阿卡貝拉類」，是要鼓勵年輕學生或社會人士，透過Accappella的方式，組隊參與比賽。今年初次舉辦，但仍有12隊青年團隊組隊參與比賽。
    </p>
    <p class="suojin">
      透過今年的客家合唱比賽，我們觀察到客委會對於推廣客家音樂的傳統到流行，以合唱比賽的方式來進行，無疑地擴大了民眾的參與度，因為它的對象不只是客家人，也成功吸引了年輕的非客家籍青年參與其中。透過這場為期2天的各級客家合唱比賽，在客家歌謠的曲目選擇上，我們也觀察到客家山歌班大都選擇傳統客家山歌改編的合唱曲為主；至於非客家社會合唱團，或青少年合唱團，則以客家流行歌曲改編的合唱曲為主。其中又以陳永淘、謝宇威創作的歌曲，被演唱的次數最高。
    </p>
    <p class="suojin">
      此外，在今年度之客家合唱比賽舉行之前，客委會已先行召開了「改編客家合唱曲譜」諮詢會議，委請編曲家重新改編不同曲目的客家合唱曲，並在賽前提供給各級合唱團，做為比賽之參考或選曲之依據。其中又以改編成客家阿卡貝拉曲目，並提供給青年團體。此項創舉，也適時地解決了過去無曲可唱的窘境，同時也開始累積客家合唱或阿卡客家曲目，這對有效推廣客家音樂與傳承客家音樂，有相當大之助益；除了客家合唱曲譜的增訂，客委會也在臺灣的南部與北部，針對合唱團的指揮或指導老師，各辦了2場「客家合唱演唱與詮釋」的研習營，希望能針對客家音樂的核心美學觀，透過指揮老師與指導老師的轉譯，成功而有效地傳達到每位團員的身上。
    </p>
    <h4>（四）「臺灣客家369樂章」全球首演</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時間：2019年12月11日19:30<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;地點：國家音樂廳<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;策劃人/吳榮順<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指揮/林天吉<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作曲/温隆信<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;樂團/NSO國家交響樂團、四爪樂團、美濃客家八音團<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;客家歌手/謝宇威、陳永淘、湯運煥、劉劭希<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;客家合唱團/心築愛樂合唱團、國立客家兒童合唱團<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第一樂章：〈台三客庄老街懷舊之旅〉<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二樂章：〈台九交響曲〉<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第三樂章：〈六堆八音交響協奏曲〉
