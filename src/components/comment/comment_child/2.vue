    <h3>前言</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國立傳統藝術中心臺灣音樂館」基於記錄臺灣音樂史料，從2018年起在臺藝大張儷瓊教授主持下編纂當代藝術音樂年鑑，為歷史作見證，可喜可賀。2019邁入第二個年頭，因為大環境沒有太大的改變，所以保留了部份不變的內容與章節，卻也從各團隊的表現，發現了頗多變遷顯示在數據上。或許這只是巧合，或許這說明了潛藏的活力正逐漸開花結果、綻放光彩。再者，在2018的年鑑，筆者收集了若干相關照片輔助閱讀；而2019的年鑑，筆者試圖透過音樂會海報的呈現，讓讀者一窺各團隊的美學底蘊與風格，期盼經由視覺藝術享受，提升讀者對各團隊的印象，來日蒞臨音樂會現場，落實聽覺藝術的欣賞。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀國內音樂環境，流行（娛樂）音樂充斥蔚為奇觀，更是眾多中下階層官員與民意代表接近地氣的必修課，即使五音不全也樂此不疲。至於古典（嚴肅或藝術）音樂雖然仍然是一般小學的課程之一，可惜歷經藝術課程變革與整合，如今更是形同虛設而無所適從。到底音樂應該歸屬表演藝術與否，或音樂是否應該是獨立而專門的藝術，歐洲與美洲看法有別，雙方的表現與成就自然不同。令人擔心的是如若將音樂劃歸表演藝術，那麼音樂終將淪落為配樂的功能性述求，而失去其抽象而專業藝術的定位，列位諸公可曾深思熟慮過？記得，筆者曾經應邀擔任第二屆台新藝術獎的評審兼表演藝術類召集人，我就明白表示過，如此區分方式，音樂即使再好也無從得獎，或許這就是音樂界的宿命吧。文化建設委員會轉型文化部的多次公聽會或座談會，筆者也曾多次呼籲過，讓音樂維持獨立，奈何人微言輕，幾乎引不起任何漣漪與討論而深感遺憾。而所謂傳統音樂或國樂雖然有別，命運其實相類似，它們與古典音樂和流行音樂相較，顯得非常弱勢，畢竟藝術性與娛樂性迥然不同。它們原本可以相輔相成而相得益彰，卻因為歷史上的忽視與政策上的失衡，導致認知與作法上南轅北轍而莫衷一是。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;老實說，時序進入二十一世紀的臺灣，古典音樂與傳統音樂或國樂的整體表現讓人欣慰，2,300萬人口的蕞爾小島，居然能夠蘊育出如此多樣化的國際級音樂家，他（她）們的天資聰穎加上戮力以赴，應該是最主要的因素。但是，那種個人英雄式的成就，或那出類拔萃的創舉，又能夠持續多久呢﹖不免讓人憂心忡忡。是否我們也應該有個機制洞察機先，雪中送炭適時伸出援手，幫助他（她）們成長茁壯，而不僅只是錦上添花而矣。其實，不管哪行哪業，高度的成就總是伴隨著孤獨與寂寞；任何音樂作品流通了必定俗氣，絲毫沒有流通了又不俗氣的可能。沒有堅強的毅志力與持之以恆的反省與自我修護力，要成為像樣的音樂家是不可能的，真所謂「臺上一分鐘，臺下十年工」，更何況是兼具原創性、前瞻性與專業性作曲家的養成與維繫。
    </p>
    <p class="bui">
      歐洲各國因為有專門演奏當代音樂創作的樂團與合唱團，有歷史悠久專門鼓勵新音樂創作的音樂出版社與現代音樂中心，有健全的音樂著作權保障制度，故資訊取得不虞匱乏，編纂新音樂年鑑，相當容易。舉例來說，筆者曾經合作過的全球三大室內樂團－法國巴黎“Ensemble InterContemporain”、德國法蘭克福“Ensemble Modern”與奧地利維也納“Klangforum”，它們不但擁有頂尖的音樂家，專門演奏新音樂創作，而且足跡遍及全球各大音樂節慶，對於新音樂作品的演出與推廣不遺餘力，功不可沒。再則，歐洲各國都擁有歷史悠久的音樂出版社與現代音樂中心，旗下擁有許多知名作曲家，並認養了不少年輕作曲家，算是「以經典養現代」，使得音樂創作得以延續傳承。此外他們的音樂著作權保障制度，更讓作曲家無後顧之憂，專心從事音樂創作。三方匯聚相關資料，編撰新音樂年鑑自然水到渠成。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1706）</span>
    </p>
    <p class="bui">
      反觀臺灣，我們既無如歐洲各國專門職司當代新音樂演奏的知名團隊，亦無專司現代音樂出版的出版社或現代音樂中心。音樂著作權的維護，也是一波三折，難以為繼，導致音樂年鑑相關資料，散佈在各個不同的機構或團隊，取得更是困難重重。難得的是「國立傳統藝術中心臺灣音樂館」發現了臺灣在逐漸蓬勃發展的音樂活動背後，缺乏完整的記錄。到底臺灣每年有多少音樂創作被首演？每年有多少作品被重新搬上舞臺演奏？又有多少臺灣青年音樂家榮獲國際獎項？或許是這一連串的好奇心與記錄臺灣音樂史的使命感促成了編纂音樂年鑑的動機。值此千載難逢的時刻，筆者謹就個人多年來在國內外參與和觀察音樂活動的經驗，論述臺灣當代音樂的文化現象與生態環境。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1706）</span>
    </p>
    <h3>壹、臺灣當代音樂的文化現象</h3><p></p>
    <h4>一、殖民主義音樂文化景觀：</h4><p></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;正如筆者在《2018臺灣音樂年鑑》中提出的文化觀察，我們應該嚴肅地思考建構自我定位的音樂文化主體性：
    </p>
    <p class="bui">
      臺灣因為曾歷經異族殖民統治，而且自從1895年以降迄今120年餘「全盤西化」的音樂教育，在音樂生態環境上，缺乏自我主體性的認知與作為，充分展現了「後殖民主義」的痕跡。不容諱言，時下許多年輕的臺灣人，可能對歐洲古典音樂，遠比對我們祖先留傳下來的傳統音樂更為嫻熟，他（她）們對於長笛、小提琴與鋼琴等西方樂器，遠比對笛、簫、笙、二胡、琵琶與古箏等傳統樂器來得熟悉而親切。那麼是否意味著，這是個東西混雜的音樂文化生態環境？臺灣人面對殖民主義，似乎只有逆來順受，未曾嚴肅地思考建構自我定位的音樂文化主體性，這抑或正是「殖民文化」的終極面向！？<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1706-1707）</span>
    </p>
    <h4>二、東西雙重傳統音樂文化交織的人文景觀：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣所謂的傳統音樂，有原住民歌謠、閩南歌謠、客家歌謠，與先民從中國大陸帶來，落地生根的「南管音樂」和「北管音樂」，以及17世紀以來，伴隨西方傳教士移植臺灣的歐洲古典音樂。而我們的音樂家，在1945年二戰結束前以留學日本為主，戰後轉向留學西歐；1980年代以迄世紀末逐漸前往美國，或到中國大陸研習傳統樂器演奏；及至21世紀的留學風潮再度偏往歐洲，他（她）們吸收了西方古典音樂與中國傳統音樂的精華，形成了東西並存、新舊並列的獨特音樂生態環境。如今臺灣的音樂家或返國貢獻所學，或客居歐美、亞洲各國從事演奏和教學，或參與各種國際音樂競賽，都有著輝煌的成績與豐碩的成果，讓人刮目相看，這或許正是東西雙重傳統音樂文化交織所產生的必然吧。
    </p>
    <h4>三、建構自我主體性音樂生態新環境：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據筆者在去年曾提出的回顧，在政府長年的提倡和民間協會的推動之下，國內音樂創作領域逐漸建構了富有自我主體性的生態環境，我們的音樂家持續在國際樂壇上展露頭角，在國際樂壇上與歐美音樂家並駕齊驅。
    </p>
    <p class="bui">
      有鑑於全盤西化音樂教育帶來了音樂技藝的精進與自我文化認知的困擾，前輩作曲家許常惠教授於1971年號召籌組「亞洲作曲家聯盟（Asian
      Composers
      League，簡稱ACL或曲盟）」，並於1973年正式成立該聯盟，開啟了亞洲國家（或地區）音樂交流的管道。而政府有鑑於文化業務的重要與龐雜，需要專責機構處理，於1981年成立的「行政院文化建設委員會（簡稱文建會）」，揭櫫「傳統與創新」的宗旨，一方面委託作曲家創作新曲，另一方面甄選優秀的音樂作品，舉辦「文藝季」首演委託創作與甄選作品，拓展國內音樂新環境。筆者也曾經於1985年起，與若干同好積極申請加入「國際現代音樂協會（International
      Society for Contemporary
      Music，該會成立於1923年，簡稱ISCM）」，我們於1989年成為該協會正式會員國，對於臺灣與國際音樂文化交流提供了更為寬廣的管道與機會。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1707）</span>
    </p>
    <p class="bui">
      文建會曾經在1991年委託潘皇龍負責籌備「八O樂展」－國人音樂創作發表會，委託臺灣作曲家以傳統絲竹樂器作為媒介創作現代音樂，製作「傳統與現代的兩極對話」。一方面定期主辦「傳統樂器示範演奏研討會」，讓作曲家認識傳統樂器，並學習傳統音樂語彙，另一方面邀請「台北絲竹室內樂團」與「采風樂坊」分別假臺中、高雄與臺北巡迴演出，打破了國樂界與西樂界老死不相往來的陋習，開啟了兩方相互交流與學習的管道，促進了雙方相互溝通與合作的新契機。文建會也在1990年代，委託筆者分別在美國紐約與法國巴黎舉辦「台灣作曲家樂展」，拓展國際音樂文化交流。1996年「行政院文化基金管理委員會」，變更為「財團法人國家文化藝術基金會」，建立了更為公開而透明的補助審核機制。爾後文建會歷經轉型為文化部，擴大編制與業務。加上「扶植團隊」到「分級獎助績優團隊」的轉變與落實，讓我們的音樂家可以在國際樂壇上揚眉吐氣，與歐美音樂家並駕齊驅絲毫不遜色。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1707）</span>
    </p>
    <p class="bui">
      2011年，臺灣第五度主辦「曲盟大會暨亞太音樂節」，更讓與會各國刮目相看。除了創作上的優勢，還空前地推薦了「國家交響樂團」、「國立臺灣交響樂團」、「台北室內合唱團」與「朱宗慶打擊樂團」等四個團隊為曲盟2011年度傑出演奏團隊，打破了曲盟有史以來單年榮獲最多傑出演奏團隊紀錄。2018臺灣再度成功主辦該音樂盛會，使得臺灣在建構自我主體性音樂生態新環境的認知與作為上逐步趨於明顯。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1708）</span>
    </p>

    <h3>貳、臺灣當代音樂創作的推手：</h3><p></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣當代音樂創作除了需要公資源的投入，亦需要政府與民間的攜手合作。
    </p>
    <p class="bui">
      一個傑出作曲家的養成，需要經過漫長的學習與考驗。一方面要從眾多經典作品中學習、模仿，爾後逐漸在創作中，尋求屬於自己的風格定位；另一方面更需要累積一定份量的曲目，創造屬於個人的品味。它比較像是「引領風潮」，而非「隨波逐流」。如果你（妳）是個天才，不用花太多時間學習，就有傑出的表現自是最好，但是，那畢竟只是鳳毛麟角，可遇而不可求。一個作曲家的養成，除了基礎樂理，還有曲調作法、音樂基礎訓練的養成，歷經和聲學、對位法、曲式學、樂器學的磨練與音樂史的學習，才能進入樂曲分析、管弦樂法（配器學）、風格寫作…等的鍛煉，並增進文史哲學、天文地理…的涉獵與體驗。所以，在作曲家的成長過程中，適時給予鼓勵與協助越益顯得重要。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1708）</span>
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧台灣2019年的國人委託創作與作品首演，它們或是公家機構補助、公設樂團委託，財團法人國家文化藝術基金會贊助、民間樂團委託…等，為數相當可觀。至於漏網之魚在所難免，尚需國家級機構持續追蹤，更需要吾輩共同努力，繼續更上一層樓！
    </p>
    <h4>一、文化部或中央部會補助案：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部雖然將每年分級獎助補助案移交國家文化藝術基金會辦理，但是，依文化部各類補助作業要點，仍然保留了若干特殊性質而不歸屬國家文化藝術基金會工作範疇的補助案或重大藝文活動補助案。如以藝術發展司2020年的獎補助為例，就有如下相關作業要點可供查詢或申請補助︰如文化部文化體驗內容徵選補助作業要點、文化部補助文化團體及個人參與多面向拓展文化交流處理要點、文化部扶植青年藝術發展補助作業要點、文化部臺灣品牌團隊計畫補助作業要點、文化部補助藝術村營運扶植計畫作業要點、文化部媒合演藝團隊進駐演藝場所合作計畫、文化部補助地方扶植傑出演藝團隊計畫作業要點、文化部補助視覺藝術產業辦理或參加國際藝術展會作業要點、文化部視覺藝術類補助作業要點、文化部表演藝術類補助作業要點、文化部地方文化特色及藝文人口培育計畫補助作業要點、文化部選送文化相關人才出國駐村交流計畫、文化部補助國內表演藝術經典作品大陸巡演、科技藝術創作發展補助作業要點…等，因為種類繁多，不再一一贅述。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而其他中央部會，也或多或少有類似文化活動補助案的經費，如教育部、外交部、科技部…等。近年來特別是客家委員會投注在音樂會或委託創作的補助案，更讓人津津樂道，或可為其他部會楷模。
    </p>
    <h4>二、財團法人國家文化藝術基金會補助案：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;財團法人國家文化藝術基金會（簡稱國藝會）有關音樂類的補助案有創作、委託創作、駐團藝術工作者、演出、策展、調查與研究、研討會、研習進修、出版、
      特殊計劃…等受理申請。近年來為了強化音樂創作補助，將「創作」與「委託創作」申請案審查，由每年一期提升為兩期，期許發掘更多的原創音樂作品。2019年「創作」補助楊祖垚、林佳瑩、張譽馨、林芝良、簡宇君、劉韋志、羅仕偉、謝蕙馨、李志純、連憲升、紀柏豪、張玹、黃靖雅、鄭伊里、羅芳偉、朱怡潔、陳樹熙、游昌發等。其他針對各樂團、合唱團的委託創作的補助案，亦不在少數，顯見團隊演奏國人作品，逐漸形成共識。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國藝會為了鼓勵富於「原創性」、「前瞻性」與「專業性」的音樂創作，讓作曲家免於過度遷就「市場經濟」的困擾，而能夠發揮創意創新作品。二十餘年來，國藝會補助無數臺灣青壯作曲家，活躍在世界各大現代音樂節慶上，它的錦上添花或雪中送炭功不可沒。
    </p>
    <h4>三、縣市政府文化局補助案：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;委託創作或演出國人作品，除了國藝會有定期補助外，還有某些縣市政府文化局也有如此規範，或甄選傑出演奏團隊給予贊助。筆者曾經數度參與評審工作，自然以臺北市文化局的表現最為熟悉。如2019年經由各樂團、合唱團委託創作作品，或補助樂團演出作品…等，數量可觀。其他各縣市政府對於國人音樂創作或作品演出補助為數較少，尚待進一步開發。
    </p>
    <h4>四、公設樂團委託創作或首演國人作品：</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的公設樂團，國家級的如「國立臺灣交響樂團」、「國家交響樂團」、「臺灣國樂團」，縣市級的有「臺北市立交響樂團」、「臺北市立國樂團」、「高雄市交響樂團」、「高雄市國樂團」…等。老實說，交響樂團來之於西歐，所以主要的任務似乎都停留在演奏西洋古典音樂的經典曲目，或基於商業票房考量，滿足聽眾耳熟能詳的經典曲目，而鮮少關切當代音樂或國人作品，已經成為慣例。而一般國樂團的成立，係倣效「香港中樂團」、「新加坡華樂團」、「北京民族樂團」或「上海民族樂團」…。往昔演出曲目必須仰賴上列樂團的常態作品，或創作風格深受影響，導致依樣畫葫蘆的窘態不絕於耳。如今隨著技術精進、中西樂界跨越交流與樂團的努力，逐漸開闢新的氣象，拓展了嶄新的局面。所以，如何建構自我經典曲目，打造自我品牌，會是一個值得嚴肅面對與仔細斟酌的課題。
    </p>
    <p class="bui">
      指揮家的養成，泰半出身於歐美音樂學院，耳濡目染自然只求反芻耳熟能詳的經典曲目，鮮少跨足不熟稔的當代音樂或國人作品，而習以為常，或許亦無可厚非。樂團需要接受不同音樂風格的挑戰，指揮家亦然，而聽眾也需要成長。還好，我們也曾經有過若干指揮家或團隊，別具慧眼，或富於使命感的趨使，異軍突起，發掘不少臺灣傑出作曲家與優秀作品，蔚為美談。<span style="font-family:'微軟正黑體'">（潘皇龍，2019，1710）</span>
    </p>
