    <h3>結語：期許與展望<sup>1</sup></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣多元社會結構，創作風格多樣化，有前衛亦不乏非常保守者。有取材自傳統文化，亦不乏深受歐美音樂風格影響者，堪稱欣欣向榮、各顯神通。部份作曲家已經具有國際知名度，而年輕作曲家也接連在國際各大音樂節獲獎，值得嘉勉。而我們的演奏（唱）家、指揮家、樂團、合唱團，雖然學的幾乎都是歐美古典音樂或傳統音樂，卻也有部份菁英可以縱橫天下、足堪告慰。藝術音樂創作年鑑記錄了音樂創作與展演的重要事跡，也為記錄臺灣音樂史提供了寶貴的史料，期盼持之以恆，衷心感恩。茲依個人多年來對臺灣音樂生態環境的參與和觀察，匯集整理建構自我品牌與發掘經典作品，創造多元音樂生態新環境，與拓展國際音樂文化交流重點如下：
    </p>
    <h4>一、建構自我品牌與發掘經典作品：</h4>
    <strong>（一）成立「臺灣音樂中心」，建立「臺灣音樂資料庫」：</strong>
    <p></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;擴大搜集、整理、出版作曲家作品目錄、編制、創作年代、首演紀錄…提供研究、演出與國際交流應用。並積極申請加入「國際音樂資訊中心聯合會」（International Association for Music Information Center，簡稱IAMIC）。
    </p>
    <strong>（二）打造自我品牌與發掘經典作品：</strong>
    <p></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;明訂樂團委託國人創新的比率，並逐年提高。獎勵音樂家、樂團、合唱團，詮釋國人作品，建立自我特性、確立自我品牌，發掘經典曲目，積累團隊獨特風格。
    </p>
    <h4>二、創造多元音樂生態新環境：</h4>
    <span>（一）鼓勵樂團、合唱團常設「駐團作曲家」，充實自我曲目資料庫。</span><br>
    <span>（二）舉辦作曲比賽，並首演入圍作品，提供年輕作曲家嶄露頭角的機會。</span><br>
    <span>（三）呼籲臺灣音樂館、國藝會、國家兩廳院與衛武營積極扮演領頭羊角色，引導各地方展演場所，擴張展演型式與內容。</span><br>
    <span>（四）籲請政府機關釋出少子化後的公共閒置空間，供所在地樂團、合唱團活用，化被動的補助功能為積極的引導策略。</span><br>
    <span>（五）音樂是時間的藝術，也是獨立而專門的藝術。敦請總統府成立國家級「臺灣音樂研究院」或「臺灣藝術研究院」，拓展音樂、藝術學術研究領域，充實「文化立國」精神。</span>
    <p></p>
    <h4>三、拓展國際音樂文化交流：</h4>
    <span>（一）獎助國內團隊與協會積極爭取在國內主辦國際性音樂節，促進國際音樂文化交流。</span><br>
    <span>（二）擴大甄選傑出音樂家、團隊出國展演、或與國外著名團隊聯合演出，或參加國際競賽，充實駐外單位文化交流相關業務。</span><br>
    <span>（三）籲請文化部主導，協調教育部、外交部責成各駐外館所，舉辦「臺灣音樂節」或「臺灣藝術週」，拓展國際音樂文化交流，增進臺灣音樂的國際能見度。</span>
    <p></p>
