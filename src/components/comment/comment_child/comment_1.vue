    <p><strong>二、資料來源</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;資料來源方面，不同子領域除了透過文獻資料，搜尋相關網站訊息之外，傳統音樂部分，包含南管音樂、北管音樂、福佬音樂、客家音樂、原住民音樂、宗教音樂中的佛、釋、道、法、釋奠樂等，因為這些樂種許多相關訊息，並非經由網路流通，更多的是透過口耳相傳與當地人告知，因此在資料來源上，應用較多的田野調查，活動的記錄，也多透過實地訪查觀察而書寫，所獲多為第一手的資料。至於藝術（當代創作）音樂、流行音樂、跨界音樂與其他，更多的是音樂廳與演唱會場的直接拜訪聆賞，以及對於各種比賽、會議、出版等的多方關注，除此之外，親臨現場對於節目演出、會議報告等內容以及觀眾反應的獲取與觀察，則是相關子領域的重要資料來源。
    </p>
    <p><strong>三、人才培育</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑之人才培育數量調查，以大專院校系所為對象，整理各子領域評介專文後發現，各自領域人才培育方式不盡相同。</p>
    <p><strong>（一）傳統音樂中漢族音樂部分</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管音樂與北管音樂有國立臺北藝術大學傳統音樂學系學士班與碩士班，其中又可再細分為南管樂組與北管樂組，來進行人才培育的課程，在校生約有80人。客家音樂方面有國立臺灣戲曲學院，以十二年一貫戲曲人才養成學府，自國小、國中、高職至四技部（四年制技術學院制）一貫教育，培養優質戲曲專業人才。其中戲曲音樂學系、客家戲學系等，皆有開設客家音樂相關課程。至於福佬歌謠之人才培育，皆未列入大專院校的正式課程之中，多屬社團性質，或是以「本土語言課程」之名所安排的相關課程內容。
    </p>
    <p><strong>（二）傳統音樂中原住民音樂與宗教音樂領域</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民音樂與宗教祀典音樂目前臺灣大專院校，未見單一的相關科系。原住民領域以多元族群以及相關知識領域的學習為主，例如東華大學、國立暨南大學、靜宜大學等大學設有相關學院、學程、專班。宗教音樂部分則大多於宗教系所之相關課程中進行人才培育，計有華梵大學、佛光大學、國立政治大學、中原大學、南華大學、真理大學、玄奘大學等7所大專院校，設有佛教學系、宗教學系及宗教研究所等，但並非針對音樂內容。
    </p>
    <p><strong>（三）藝術（當代創作）音樂</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂與西樂人才在大專院校的培育部分，在臺灣有較為完整的教育體系。國樂方面有6所大專院校，分別為國立臺灣藝術大學、中國文化大學、國立臺北藝術大學、國立臺南藝術大學、南華大學及國立臺灣師範大學等，這些學校皆有學士班和碩士班，以及國立臺灣師範大學民族音樂研究所的碩士班，約1100人於相關科系、組別就讀。西樂方面，本年鑑中收錄之相關學校包含國小、國中、高中、大學、研究所等，其中國小68所、國中56所、高中41所、大學72所、研究所17所，超過萬名學子於音樂班、音樂科系、組別就讀。
    </p>
    <p><strong>（四）流行音樂與跨界音樂</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;流行音樂自2013年文化部開始推動設立流行音樂學程等會議後，近十年來各大專院校陸續成立相關學程，從學士、學士後學位學程、碩士班等流行音樂專業，將流行音樂納入系統性的正規教育體系。2022年計有國立臺灣師範大學、國立清華大學、輔仁大學、文化大學、中國科技大學、中原大學、國立臺南藝術大學、臺南應用科技大學、南臺科技大學、臺北城市科技大學、國立中山大學、遠東科技大學、佛光大學、醒吾科技大學等14所大專院校，約1500人於相關科系、組別就讀。跨界音樂則有國立臺灣藝術大學跨域表演藝術研究所，在校生人數約70人，培訓兼具國際宏觀與在地視野的跨域表演藝術研究人才。
    </p>
    <p><strong>四、統計數據</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過13個子領域對於其各自面向的關照，呈現在本年鑑的整體音樂數量上，可看見展演活動非常活躍，總共有6,268件，佔總體調查的57.8%，顯示了音樂中最重要的展示方式。這其中，又以流行音樂的2,057件為最大宗。而藝術（當代創作）音樂中以西方樂隊與樂器，或混合編制形式所展演的國人作品部分有1,356件，傳統音樂中的北管有1,044件，都顯示了音樂廳內與音樂廳外，在2022年都有生氣蓬勃的音樂活動。
    </p>
