  <p><strong>一、未來研究規劃</strong></p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑的最大優點，在於邀請各領域專業人士參與資料蒐集與寫作。不同領域具有一定的專業性，而在此領域努力的學者，為我們留下值得保存的紀錄，讓我們有機會了解不同領域在不同年份下的發展。專家學者不但訊息來源與相關資料蒐集方面，都比非專業人士敏銳，且管道多樣，訊息量充沛。加上對於活動紀錄專業的選擇與描述，都讓一般大眾能藉此有專業觀點的認識。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但國內在專業人才的培訓上並不平均。例如原住民音樂族群眾多，活動頻繁，但因地理位置分布廣泛，不但在蒐集資料上需更多人力的支持，蹲點紀錄也所費不貲。加以原住民研究人才培育不易，一位專家學者必須同時兼顧多個族群，造成願意承擔此領域的專業人才難尋。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑的第二優點，在於其內容完備。從2009年臺灣年鑑開始執行時，僅集中於傳統音樂，到2018年開始含括了藝術（當代創作）、流行與跨界音樂等內容。一般民眾因此能透過一本年鑑，了解當年度臺灣音樂全貌。其中除了在傳統音樂方面，許多訊息不見與網路，需親自蒐集，2018年新加入的領域，其資料雖在網路等相關媒體已相當豐富，但訊息卻是凌亂紛雜。因此專家的整理與歸納，將資料分類置入一定的範疇中，對於一般希望了解當年音樂內容的民眾，提供了系統性認識的可能性。
  </p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但本年鑑有流行音樂子領域訊息，與因為國家對於流行音樂的重視，有例如文化內容策進院所進行的《2021年台灣文化內容產業調查報告III
    流行音樂、廣播產業》中所見到，對於流行音樂產業的調查報告，似乎有所重疊。但文化內容策進院的調查重點，在於流行音樂市場銷售金額狀況、數位串流音樂熱門歌曲狀況、國內外歌手/樂團演出之售票場次，我國廣播產業營業收入趨勢等內容，與本年鑑中所重視的音樂文化生態、活動蹲點觀察等有所不同。再者，不同調查不但常各有所重，專家學者們的觀點也常有差異，因此即使內容範圍看似均針對流行音樂，但所呈現的結果仍會有所不同。
  </p>
  <p><strong>二、研究建議</strong></p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂因其抽象性，是所有藝術中最需具備專業性者，專業人才較其他藝術領域少，但這並不表示此專業應被忽略。況且我國其實已具備許多音樂相關專業人士，故應可打破既有各子領域需統籌較大內容範圍的情形，讓更多新興一代的年輕學者可以其所學專長，共同參與歷史書寫。至於流行音樂部分，雖有其他機構的類似統計數據，但此領域仍不應置於年鑑之外，反之更應與相關單位協調聯手，完備一本音樂年鑑所需具備的不同面向。此外，機構工作有重疊性，多因對於音樂機構的規劃未見統一性，才導致流行音樂子領域被質疑與其他音樂單位所做重疊的問題。應成立層級較高之臺灣音樂中心，統領臺灣音樂各項事務，避免重複，浪費公帑，才為解決此問題根本之道。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;許多事件，如未能在當年立即記錄，事後的追憶將參雜更多主觀色彩，另也讓臺灣音樂史研究所需的第一手資料，落入民間愛樂人的收藏中，之後必須再次從民間愛樂人士手中收購。文化是國家的軟實力，我們音樂界責無旁貸應做之事，年鑑則為其中要務。透過年鑑可了解歷史、盤點事件、認識自我。因此如能持續進行，臺灣音樂史的樣貌將更加清晰。
  </p>
