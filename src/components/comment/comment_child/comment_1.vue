  <h3>肆、未來研究規劃與建議</h3>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;許多事件的發生，在當年多樣紛雜、百物齊陳的情況下，可能並未能立即顯現其特殊性，但在時間長河下，歷史學者的針砭，若干年後若需回頭翻閱，則透過年鑑的記載，當年發生時的資料，早已蒐集齊備。因此年鑑雖然每年看來似乎大同小異，卻保留了當年瞬間所發生事件的第一手紀錄，沒有事後的加油添醋、錦上添花，完全是樸質的呈現了當下的原始樣貌與觀點。
