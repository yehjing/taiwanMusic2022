    <h3>壹、年度社會背景</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年初開始，全球大爆發的嚴重特殊傳染性肺炎（COVID-19），2022年仍繼續肆虐，但在年初之時，本土疫情控制得當。1月1日本土累計確診總人數為17,050人，死亡累積總人數850人，當日本土確診數為0。之後雖有零星確診數，但國民生活基本正常。而4月開始，染病者逐漸增多，先是4月1日本土確診數破百（104人），4月15日破千（1,209人），4月28日破萬（11,353人），累計死亡人數也於5月13日破千（1,009）。截至7月31日止，本土當日確診人數為20,824人，累計死亡人數為8,927人，累計確診總人口數為4,588,185人。<sup>1</sup>
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在這波疫情下，上半年初逐漸回溫的音樂活動，又在4月到6月縮減場次。所幸多數成年人接種了疫苗，患病者多為輕症，政府也適時修訂隔離政策以及規範確診情況。因此雖有4到6月之間，部分藝文活動因演出者確診遭受隔離，而被取消或延期，但有些活動仍照常舉行。除了改以視訊或部分視訊演出，另也有縮減活動規模的形式。為因應疫情，藝文活動形式這幾年正逐漸轉向，此可透過近年各子領域月曆觀察發現。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的下半年度，國際社會間逐漸走向與病毒共存，10月13日起，入境人員免除居家檢疫，12月1日起，更放寬室外免戴口罩，許多從疫情開始被推延的音樂相關活動，都在下半年各展特色，音樂活動逐漸熱絡起來。此情形從每個子領域的活動月曆可看出。
    </p>
