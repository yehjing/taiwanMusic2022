<h4>結語︰音樂創作的三段階層</h4>
<p><strong>（一）「學而用之」：</strong>如海納百川成其大，用以「提升自我」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂創作是個漫長的學習過程，非一觸可及。如果你（妳）是個天才，不用學就能創作，值得恭喜。然而，擺在我們眼前的經典作品有增無減，天才只會越來越少；所以學習音樂理論、分析經典作品，了解前人是如何創作的，便成為提升自我音樂創作的不二法門了。</p>
<p><strong>（二）「習而避之」：</strong>有得亦有捨，去蕪存菁，用以「檢驗自我」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;歷經不斷的「嘗試與錯誤」，檢測音樂創作語彙，把符合自己的留下來，把不適合自己的拋棄掉，如此累積自己的能量，檢驗自己的創作語彙。</p>
<p><strong>（三）「取而化之」：</strong>像吃了豬肉長人肉，用以「超越自我」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當作曲家歷經「學而用之」與「習而避之」的洗禮，他（她）已經具備了足夠的語彙與寬廣的視野，不管是從欣賞音樂或日常生活所接觸到的林林總總，都有可能被消化為己所用，轉化為創作的靈感、語彙，從而超越先前的自我。</p>
<h3>玖、呼籲文化部盡速成立「國立臺灣音樂中心」，確立國家級音樂行政機構</h3>
<p><strong>一、合併「臺灣音樂館」與「國立臺灣交響樂團」為「國立臺灣音樂中心」的利弊得失</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;記得當年鄭麗君部長時代，有關合併「臺灣音樂館」與「國立臺灣交響樂團」的諮詢會議，筆者曾經提供若干看法做為參考。</p>
<p>（一）將「臺灣音樂館」與「國立臺灣交響樂團」合併為「國立臺灣音樂中心」，中心隨即擁有現成三管編制約70人的管弦樂團，與約50人的行政團隊。當然，我們也約略知道行政院組織法有關員額問題，可能是兩單位合併升格最主要的關鍵。以臺灣音樂館4名員額擴張5倍，也只有20人；如若合併國立臺灣交響樂團的約121人，再重新合併相關性質業務，如若擴張四分之一，就有約156人，或擴張五分之一，就有約150人，解決了臺灣音樂館目前員額極度缺乏的困境，可以理解。</p>
<p>（二）兩者功能屬性不同：交響樂團普遍以演奏歐洲經典作品為主，而臺灣音樂館卻是以搜集、典藏、研究、出版、推廣、演出國人作品與促進國際音樂文化交流為主要任務。而且國人作品室內樂的小型編制約佔百分之80以上，管弦樂作品不及百分之20，任務與編制不相符合。交響樂團的演出，由團長與音樂總監統籌，重視群體的表現，建構團隊的精神，不能容許有個別的特色；而室內樂著重於它的細緻性，培養團員默契，充分發揮個別樂器性能，剛好與樂團屬性相反。而且，讓交響樂團經常演出小編制作品，如同用大砲打小鳥，除非交響樂團能夠徹底檢討它的功能定位，方可為之。</p>
<p>（三）臺灣的音樂生態環境非常獨特，我們的音樂教育幾乎全盤西化，造就的人才縱橫天下，與歐美音樂家並駕齊驅絲毫不遜色。但是，我們其實還有傳統音樂/國樂系統的發展也已經創下了顛峰。1991年，那東、西樂界老死不相往來的年代，當時的「行政院文化建設委員會」委託我製作「八0樂展」，我大膽的提出「傳統與現代的兩極對話」，以傳統樂器演奏新音樂的構想獲得首肯，邀請剛成立的「采風樂坊」首演部分國人作品；如今它們已經名震寰宇，獲得亞洲、歐洲與美洲各國的邀約演出。只是畢竟是私人樂團，營運困難重重而倍感艱辛。2021年11月28日，「采風樂坊」以線上視訊方式在義大利「羅馬新音樂節（Nuova Consonanza）」粉墨登場，演出臺灣作曲家新音樂作品，12月12日重播，反應非常好，而且可以說是獨一無二的臺灣音樂新創舉，這一點連中國大陸都難以匹敵。再者，這些年來，我們從傳統樂器演奏當代音樂到東西樂器混合編制作品，也已經在國際間走出了新氣象，將「臺灣音樂館」與「國立臺灣交響樂團」合併升格，是否意味著我們的文化政策，只有站在西方的部分而捨棄的東方的傳統？</p>
<p>（四）順便為年輕音樂家請命：臺灣的音樂家，泰半出身音樂班，歷經國內外音樂系（所）獲得博士學位或碩士學位，他（她）們至少都擁有20年以上的專業經歷；其實他們的造詣與能力普遍都很好，卻苦於找不到專任職缺，而淪落到近乎擺地攤式的南北奔波，賺取微薄生活費用。而且，臺灣從筆者當學生時期只有一個師大，一個藝專，和一個北師音樂科，到目前約略將近30個音樂系，音樂教育已經飽和；尤其自從少子化以來，後段班面臨退場的命運，如今考不上音樂系也很困難了，如何了得？所以如何給這眾多年輕音樂家一線生機，「國立臺灣音樂中心」如果能夠仿傚德國成立Ensemble Modern，法國成立Ensemble InterContemporain（簡稱EIC）職業室內樂團，演奏當代音樂作品；提供新職缺，廣邀年輕好手來助陣，不但活絡了國內音樂環境，也可以在國際間揚眉吐氣。</p>
<p><strong>二、成立「國立臺灣音樂中心」的期許</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如若「國立臺灣音樂中心」的成立，合併「臺灣音樂館」與「國立臺灣交響樂團」，升格為三級單位，沒有更好的對策，我的建議是：</p>
<p>（一）臺灣難得有一個跨越76年的交響樂團，不管將來如何發展，我希望「臺灣交響樂團」的名稱能夠維持下去。</p>
<p>（二）期待樂團除了演奏歐洲經典作品與國人管弦樂作品外，也能夠明文規定，拓展樂團的室內樂編制，組成室內樂團，因應將來合併升格後的主要新任務。</p>
<p>（三）「國立臺灣音樂中心」，在臺北至少也應該有個專屬的專業音樂廳，演出新音樂作品，並成立樂器博物館，收藏臺灣製造與世界各地樂器，嘉惠愛樂人士，並創造音樂人潮帶來錢潮。</p>
<p>（四）「國立臺灣音樂中心」透過橫向聯繫，與「臺灣國樂團」或其他絲竹樂團建構經常性的合作關係，發展東西樂器混合編制作品，呈現臺灣多元社會人文景觀。</p>
<p>（五）「國立臺灣音樂中心」應該積極主動申請加入「國際音樂資訊中心聯盟」（International Association of Music Information Centers，簡稱IAMIC），或其他相關國際組織，拓展國際音樂文化交流。讓臺灣走出去，讓世界走進來。</p>
<h3>結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儘管嚴重特殊傳染性肺炎疫情肆虐，臺灣終究還是比較安全的國家。我們雖然曾經有過約略4個月的實體演出的空窗期，然而各政府機構為了持續運作，與展演團隊協會）為了求生存，卻也發展出嶄新的會議形式與展演新模式。綜觀上列2021年九大事跡，從「學習音樂是漫長而永無止境的旅程」，「臺灣作曲家的養成與音樂創作的傳承」，「音樂是獨立、抽象而專門的藝術」，「臺北公設樂團大事記與2021主事新手」，「推薦富於『挑戰性』、『前瞻性』與『使命感』的年度展演團隊」，「金曲獎及傳藝金曲獎之歷史沿革與獎項」；經由「國際音樂文化交流新型態」，2021「羅馬專題講座/作曲大師班」；到「呼籲文化部盡速成立『國立臺灣音樂中心』，確立國家級音樂行政機構」；顯示臺灣音樂人如何在逆境中，把持音樂文化的能量，勇敢的拓展了嶄新的展演新形式，創造了璀璨的新樂章。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的藝術音樂在逆境中求發展，如同臺灣經濟脈動在疫情的威嚇下，創造了無數「臺灣新奇蹟」。臺灣的「護國神山」群，以優勢的研究、發展潛力，與無以倫比的勤奮工作態度，變成炙手可熱的科技霸主。原本不被看好、不被關懷的臺灣代工業，突然麻雀變鳳凰，躍升為眾星拱月的王國。國際間眾多幾乎不相往來，既無正式邦交關係，亦無多少經貿往來的國家，卻因為臺灣曾經雪中送炭，贈送防疫口罩而投桃報李，捐助臺灣疫苗作為回饋。曾經長期是國際間的棄嬰，剎那間變成人世間的寵兒。再者，臺灣的運輸業，空運也好，海運也罷，客運蕭條瀕臨瓦解；瞬息間，改變經營模式，轉化為貨物運輸，不但解除了電子產品外銷的燃眉之急，更紓解了當前的營運危機，並將危機轉化為契機，創造了空前的業績。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧臺灣藝術音樂2021年的活動，雖然飽受疫情嚴峻的摧殘，數量銳減。不過筆者為「日本作曲家評議會（Japan Federation of Composers）」60週年慶的委託創作《花甲禮讚》木管五重奏曲，與為臺灣「時間藝術工作室」的委託創作《東南西北X》東西樂器混合編制十三重奏，均告如期完成，並完成電子檔製作總譜。為「國立臺灣交響樂團」「臺灣文化協會100週年慶紀念計畫」的委託創作《文化頌》管弦交響協奏曲，電子檔製譜校對與首演，並即將出版發行總譜數位版；而40年前客居西柏林創作的《單簧管四重奏曲》，由「時間藝術工作室」單簧管演奏家王冠傑意外地在「臺灣音樂館」「作曲家潘皇龍簡介與作品目錄」中發現而得以首演。10月份，或許拜疫情趨緩，馬不停蹄地周轉在7部作品的排練、錄影與演出間，可能是空前，也可能是絕後吧？嚴重特殊傳染性肺炎攪亂了「一池春水」，還好總算安然無恙。加上11月底為義大利「羅馬新音樂節」的12小時專題講座與作曲大師班採線上視訊傳輸，與「采風樂坊」線上視訊音樂會，發表3首絲竹編制作品，補足了筆者在南歐諸國發表作品的最後一塊拼圖，算是不幸中之大幸了。人世間，常存善念與感恩，排山倒海而來的危難險惡，終將逢凶化吉、轉危為安。</p>
