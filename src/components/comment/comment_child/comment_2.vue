<h4>六、時間藝術工作室（Time Art Workshop）擴大舉辦作品徵選與讀譜工作坊</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來新崛起的「時間藝術工作室」以其年輕活力的姿態，轉戰全臺灣各地，尤其那嶄新的曲目設計與精湛的詮釋新音樂作品，讓人印象深刻。他們持續2020年「那些好想讓你聽懂的音樂－現代音樂經典音樂會」演出潘皇龍作品《時間與空間》&hellip;等，2021年1月3日假臺南市台江文化中心台江劇場，由張庭碩（小提琴）、王冠傑（單簧管）、張智惠（大提琴）與徐敬彤（鋼琴）演出，作品依序有利蓋蒂《第一號弦樂四重奏》、布列茲《飄移》、潘皇龍《時間與空間》、陳曉勇"EVAPORA"與萊利《C音》等5首。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年1月12日「時間藝術工作室」策劃主辦「貝多芬與我們的時代」，假臺北市國家戲劇院實驗劇場，由吳正宇（長笛）、王冠傑（單簧管）、謝佩穎（擊樂）、林孟君（女高音）、張庭碩（小提琴）與張智惠（大提琴）演出潘皇龍作品《螳螂捕蟬．黃雀在後》。同年10月26日又假「國立臺灣大學」藝文中心雅頌坊與11月30日假臺中「東海大學」音樂系演奏廳，由高承胤、謝介豪、楊雅棻與王冠傑作曲家世界首演潘皇龍作品《單簧管四重奏曲》。遙想1978以迄1982年客居德國柏林時期，以自由作曲家的身份從事音樂創作維生，在創作與作品發表間，穿梭於西歐各國而自得其樂。當初為「荷蘭單簧管四重奏團（Dutch Clarinet Quartet）」創作樂曲，交件後卻因為幾度搬遷以及返國就職，而與樂團失去了聯繫，根本忘記了它的存在。及至近年來活躍的「時間藝術工作室」單簧管演奏家王冠傑來信尋求樂譜，才猛然想起蟄伏抽屜40年的舊作，而得以重見天日。</p>
<p class="bui">時間藝術工作室以推行當代音樂為基，2020年首次舉辦作品徵選與讀譜工作坊，讓演奏家參與作曲家的創作過程，深化從創作至演出的能量與品質。今年暑假，時間藝術學院即將成立，並擴大舉辦作品徵選與讀譜工作坊，邀請青年作曲家參與，不需遠赴國外，也能獲得與專業演奏家們一同創造音樂的經驗與成長&hellip;獲選的創作者在工作坊中，皆能參與從試演、點評、個別課、演奏家排練、作品修改、而至場館演出呈現一系列的過程。<span style="font-family:sans-serif !important;">（時間藝術工作室作品徵選與讀譜工作坊簡章）</span></p>
