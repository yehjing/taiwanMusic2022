<h3>壹、音樂的學習是漫長而永無止境的旅程</h3>
<h4>一、音樂的學習從認識五線譜開始</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣學習音樂的人口非常多，有些人是父母為了彌補自己當年沒有機會學習，或受到樂器公司宣揚「學習音樂的小孩不會變壞」的影響，期許子女將來學有所長，有的是羨慕鄰居或同學，會演奏樂器，致投射到自己或兒女身上，有的是天生喜歡音樂而樂此不疲&hellip;。然而不管是那一種因緣際會，音樂的學習都要從認識五線譜開始，在彈奏鋼琴中學習基礎樂理，培養音感與節奏感，他（她）們甚至於需要犧牲很多跟同伴玩耍的機會，心無旁騖，專心的練習，持之以恆才有可能入門。當音樂的基本能力養成之後，有些人會再選擇管樂器、敲擊樂器、彈撥樂器、弦樂器演奏或聲樂演唱作為精進的方向。</p>
<h4>二、學習樂器演奏或聲樂演唱沒有捷徑</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;學習樂器演奏或聲樂演唱，對於樂譜上的演奏精確度，務必做到百分之一百。如若以一般學校教育的學科考核概念，以60分或70分為及格與否的分界點，是行不通的。因為只有將樂譜上的訊息做到完善無誤，才有可能在詮釋上有所表現，成就屬於自我的演奏（唱）能力，否則只能說是玩票性質，甚至可以說是對作曲家的大不敬。所以，學習樂器演奏或聲樂演唱者從初學到演奏（唱）的過程中沒有捷徑，他們需要經過無數的練習曲與樂曲的磨練，才能提升演奏（唱）的技術與音樂表現的能力，至於將來能否成為音樂演奏（唱）家，堅決的毅力與永恆的信念絕對是成功的主要因素。</p>
<h4>三、音樂的學習是漫長而永無止境的旅程</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在學習樂器演奏或聲樂演唱有成之後，某些人會再轉移到指揮、作曲或音樂學研究，作為人生奮鬥的目標。然而，不管選擇哪一種樂器演奏、演唱、或指揮、或理論與作曲、或音樂學研究，除了一般技術性的養成與音樂素養外，還需具備抽象的語言能力，豐富的人生閱歷，精湛的詮釋能力、與高度的美學涵養，搭配時勢的運轉，方有可能異軍突起，成就一家之言。音樂的學習真是一條漫長而永無止境的旅程，即使有一天功成名就，也還是鬆懈不得。</p>
<h3>貳、臺灣作曲家的養成與音樂創作的傳承</h3>
<h4>一、臺灣作曲家的養成與作品發表</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣作曲家的養成，相對於演奏（唱）家來說，要複雜許多。猶記得有一次，筆者為了某樂團首演管弦樂作品，經樂團安排出席某知名電臺某知名主持人的節目接受訪問，主持人毫不保留地詢問，為何我們沒有個像演奏家一般響叮噹的作曲家？我的回答是：「作曲家的養成教育遠比演奏家來的長久，他（她）們常常是在樂器演奏上學有專精以後，才轉向作曲發展，所以幼稚期很長。首先他（她）們需要精通和聲學、對位法、曲式學、樂器學、樂曲分析、管弦樂法（配器法）&hellip;；從模仿大師經典作品著手，歷經無數的『嘗試與錯誤』，逐漸加以融會貫通，去蕪存菁，才能完成自我風格定位。作曲家在臺灣，沒有太多的機會可以發表作品，他（她）們寫完作品，還需要等待演奏（唱）家或樂團與合唱團的演出，才能將作品呈現在聽眾面前。在作品發表時，作曲家們永遠退居到幕後，把光彩全留給演奏（唱）家們。此外，音樂史也告訴人們，當代最受歡迎、最受推崇的作曲家，往往很少留名青史；反而是那些具備原創性，作品風格不被當代所接受的，生活窮困、潦倒的作曲家們，成就了音樂史上璀璨的樂章。傑出的作曲家們引領了音樂美學，但群眾卻經常辜負了他（她）們的創意，只看到演奏（唱）家的光環。」</p>
<h4>二、臺灣音樂創作的傳承</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣第二代的作曲家，從歐美學成歸來，他（她）們學習與教學的內容是歐美一百年以前的音樂理論。而在這基礎上受教育的第三代作曲家，到歐美學習當代的音樂理論與創作，已經與歐美同步，甚至於屢獲國際作曲獎項，也有些人曾經在歐洲依創作維生，你聽說過嗎？老實說，我們的音樂教育比起鄰近的日本，至少晚了50年才開始。然而，亞洲最重要的國際組織「亞洲作曲家聯盟（Asian Composers League）」之「亞太音樂節」2011年在臺灣主辦時，演奏的品質與演出的數量上，創造了歷史上輝煌的新里程碑。緊接著2012在以色列、2013在新加坡、2014在日本舉辦，各屆的青年作曲競賽，臺灣青年作曲家囊括了連續3屆的首獎，這是空前、想必也會是絕後的創舉，你聽說過嗎？臺灣的音樂創作與展演，已經超越亞洲各國與紐西蘭和澳洲，你知道嗎？</p>
<h4>三、臺灣作曲家擔任國際性作曲比賽評審委員的反思</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者曾經於1997與2001年應邀擔任奧地利「國際莫札特作曲比賽」評審委員，2005年應邀擔任波蘭「陸透斯拉夫斯基（W. Lutoslawski, 1913-1994）國際作曲比賽」評審委員，2010擔任波蘭「譚斯曼（A. Tansmann, 1897-1986）國際作曲比賽」評審委員&hellip;。筆者甚感意外的不是他們提供優渥的獎金，可以讓音樂創作當作職業謀生；也不是因有超過200位作曲家參與競賽，讓評審委員疲於奔命；而是發現波蘭居然擁有7個這樣的國際性作曲比賽，提供波蘭與全球作曲家經常性的觀摩與發表作品的機會，以及拓展國際音樂文化交流的管道，讓人羨慕不已。反觀我們臺灣，除了「國立臺灣交響樂團」每年定期舉辦「青年音樂創作競賽」外，其他諸如「臺灣藝術教育館」或「國際現代音樂協會（International Society for Contemporary Music）臺灣總會（中華民國現代音樂協會）」所承辦的「音樂臺北」作曲競賽，在多年前因為政黨輪替，已經灰飛煙滅、久不復返，非常遺憾。</p>
<h3>參、音樂是獨立、抽象而專門的藝術</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂是獨立、抽象而專門的藝術，它不應該被教育部、文化部歸類為表演藝術，誤導了音樂的本質與格局。</p>
<h4>一、音樂的養成教育，是獨特且無可取代的</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂的養成教育，通常從歌唱或學習鋼琴開始，經由辨識五線譜，熟諳基礎樂理，累積音樂常識，並接受音樂基礎訓練，培養音感與節奏感，歷經3至5年的「入門款」，才能逐漸進入狀況，這個過程是獨特且無可取代的，更不是在一般學校正規教育中所能達標，學子必須額外付出時間與精力努力學習，才能擁有音樂的專業能力。</p>
<h4>二、音樂是一種時間的藝術，稍縱即逝</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂的演出是詮釋，而不是表演。一個好的演奏（唱）家，會忠實呈現作曲家的創作理念，而這創作理念需要仰賴樂譜來傳達，音符的準確度必須是百分之百正確而熟練，才有可能再加入演奏（唱）家個人主觀的美學詮釋內涵，它是一種時間的藝術，稍縱即逝。</p>
<h4>三、音樂與表演藝術的互動關係</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;表演藝術需要音樂，當音樂依附於戲劇或舞蹈，那是配樂可以理解，但這只是音樂呈現的極小部分。或許歌劇的演出，勉強可以歸屬於表演藝術，因為它是音樂與表演藝術的綜合展演。至於其他更多的演出，諸如獨唱（奏）、重唱、合唱、室內樂、管弦樂、清唱劇&hellip;演出，就非表演藝術所能涵蓋了。</p>
<h4>四、第三世界國家對表演藝術的認知</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;或許有人會說：「聯合國教科文組織（UNESCO）」是把音樂劃歸表演藝術的。問題是它係由眾多第三世界國家所把持、所壟斷；即使連諸多歐洲高度文明國家，也只能獨善其身不敢茍同，我們難道也要向第三世界國家看齊、認同嗎？</p>
<h4>五、歐洲音樂暨表演藝術的嚴格區隔</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在歐洲，當一所大學有音樂、戲劇與舞蹈學院時，它的名稱是「音樂暨表演藝術大學」，而非籠統的「表演藝術大學」；顯而易見，音樂與表演藝術互不隸屬。而當大學有音樂、美術、戲劇、舞蹈、電影、建築與多媒體藝術&hellip;等，它的名稱就是「藝術大學」。</p>
<h4>六、當音樂淪落為配樂</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;把音樂劃歸表演藝術，將音樂淪落為配樂，被迫臣服於戲劇或舞蹈的演出，其結果就是犧牲了音樂作為獨立、抽象而專門藝術的可貴。</p>
<h4>七、「台新藝術獎」</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;猶記得筆者曾經於2004年應邀擔任第二屆「台新藝術獎」表演藝術類評審委員兼召集人，評審委員有音樂、戲劇、舞蹈的教授與製作人，我就曾斷言：「<span class="bkt">音樂人遇到這樣的評審團，不可能有機會得獎</span>」。畢竟純音樂要瞬間感動人，絕對不如戲劇或舞蹈來得直接與容易，只有被迫當陪襯、被犧牲了。</p>
<h4>八、消滅音樂最好的途徑</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;消滅音樂最好的途徑就是把音樂劃歸表演藝術，從此讓音樂淪落為配樂，失去了它作為獨立、抽象而專門的藝術之可貴，期盼列位諸君能夠重新省視它的屬性。我們的文化部與教育部長官，或許不理解它的嚴重性；但音樂人若不自知其利弊得失，恐將逐漸喪失臺灣數十年來建構精緻音樂安身立命之所在。</p>


<h3>肆、臺北公設樂團大事記與2021年主事新手</h3>
<h4>一、國家交響樂團（National Symphony Orchestra）</h4>
<p class="bui">「國家交響樂團（NSO）」的前身「聯合實驗管絃樂團」成立於1986年，集合優秀新生代音樂家，以打造頂尖交響樂團為目標；2005年成為國立中正文化中心附設團隊，2014年4月改隸國家表演藝術中心，以「臺灣愛樂」立足國際。經過三十多年耕耘，NSO參與交響樂、室內樂、歌劇、舞蹈、跨界製作，自信、精銳，有文化意識地展現「來自臺灣的聲音」。NSO的歷任音樂總監張大勝、林望傑、簡文彬、呂紹嘉；常任指揮艾科卡（Gerard Akoka）與史耐德（Urs Schneider），藝術顧問暨首席客座指揮根特．赫比希（G&uuml;nther Herbig）共同悉心呵護樂團成長。近十餘年來，NSO銳意求變，大步朝專業、開放、勇於創新的職業樂團發展，成為亞洲地區最具指標性的樂團；2021年8月，德國指揮家準．馬寇爾（Jun M&auml;rkl）接任NSO藝術顧問，期望以樂團獨特的聲音擔任臺灣的文化大使，向全世界表達特有的身份和情感；馬寇爾於2022年1月起擔任國家交響樂團音樂總監。<span style="font-family:sans-serif !important;">（NSO網頁）</span></p>
<h4>二、臺北市立交響樂團（Taipei Symphony Orchestra）</h4>
<p class="bui">「臺北市立交響樂團」創立於1969年。創團超過50年，臺北市立交響樂團從原本只有少數一群音樂家組成的小樂團，成長為超過百位團員的大型樂團。為臺灣的音樂演出史增添全新又輝煌的一章，亦是真正屬於這個世界上最友善的都市獨一無二的西方樂團。從成立之初，臺北市立交響樂團就以高水準的音樂品質聞名，邀請合作的知名音樂家不計其數，包括女高音安娜．涅翠科、安琪拉．蓋兒基爾，小提琴家祖克曼、凡格羅夫，大提琴家馬友友、羅斯托波維奇，指揮家泰密卡諾夫、單簧管演奏家莎賓．梅耶、長號演奏家林柏格等。1979年開辦「臺北市音樂季」，創立臺灣大型音樂季之先河，除了邀請國際知名樂團來臺外，更建立每年進行歌劇製作的悠久傳統，曾演出《丑角》、《茶花女》、《浮士德》、《卡門》、《杜蘭朵》、《波西米亞人》等經典劇碼。2011年10月《阿依達 在臺北》，成為首齣登上臺北小巨蛋的全本歌劇表演，確立臺北市立交響樂團在臺灣歌劇製作上的重要性。除了國內演出，臺北市立交響樂團更沒有缺席於國際舞臺，曾赴奧地利、美國、日本、俄羅斯、法國、西班牙、德國、盧森堡、新加坡、中國、菲律賓等地演出；近年來更參與許多國際藝術節慶及重要活動，如2010年上海世博會、2011年亞洲文化推展聯盟大會閉幕演出、2012年日本金澤、富山及東京熱狂之日音樂節、2014年日本巡迴、2015年法國漢斯夏日漫步音樂節、2016年上海國際藝術節、2017年美國巡迴及2019年中國大陸巡迴，2019年11月應華府表演藝術協會（WPA）邀請於美國華府演出。2019年8月起國際指揮大師伊利亞胡．殷巴爾應邀擔任首席指揮，在全球疫情肆虐的窘況中，不間斷地呈現完美的音樂成果，推動樂團持續成長並向國際發聲，臺北市立交響樂團將成為臺北市最美的聲音，讓全世界聽見TSO！2021年4月宋威德教授借調擔任「臺北市立交響樂團」團長。<span style="font-family:sans-serif !important;">（北市交網頁）</span></p>
<h4>三、臺北市立國樂團（Taipei Chinese Orchestra）</h4>
<p class="bui">「臺北市立國樂團」創立於1979年，是臺灣第一個專業國樂團。隸屬於臺北市政府文化局，並於2001年進駐臺北市中山堂。北市國每年樂季平均辦理超過50場定期音樂會、推廣教育性音樂會與其他型態的演出，並於每年4月至6月舉辦「臺北市傳統藝術季」。成立多年來，北市國同時擔負起臺北市文化大使的角色，曾出訪日本、美國、加拿大、菲律賓、馬來西亞、澳洲、新加坡、英國、德國、法國、奧地利、瑞士、荷蘭、比利時、捷克、中國、香港、澳門等地。自2007年由作曲家鍾耀光擔任團長與指揮家邵恩擔任音樂總監以來，邁入一個新的藝術發展新紀元，以開創性來拓展國樂的定義，樹立獨特的樂團風格與藝術走向。現任北市國團長鄭立彬自（民國）104年5月上任至今，任期即將屆滿6年，5月1日歸建中國文化大學。&hellip;台北市文化局2021年4月28日發布新聞稿，宣布臺北市立國樂團團長遴選結果出爐，由陳鄭港博士接任團長職位，於5月初上任。文化局表示，陳鄭港接任團長後，除將繼續規劃北市國新樂季節目，推出更多元化的精采節目外，也將憑藉多年的職業樂團專業經理人經驗，帶領北市國繼續穩步前進，肩負起國樂發展的重責大任。<span style="font-family:sans-serif !important;">（北市國網頁）</span></p>
<h3>伍、推薦富於「挑戰性」、「前瞻性」與「使命感」的年度展演團隊</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;特殊的年代，獨特的年鑑。筆者捨棄往年流水式的撰述方式，改採推薦演奏技術富於「挑戰性」、曲目設計富於「前瞻性」與長程營運富於「使命感」的展演團隊，作為論述的對象。</p>
<h4>一、國立臺灣交響樂團（Taiwan Symphony Orchestra）「臺灣文化協會成立100週年紀念計畫」</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;很慶幸的在音樂創作的旅途上，筆者擁有無數的貴人相扶持。以知名樂團週年慶的委託創作為例︰「柏林愛樂管弦樂團」為了慶祝該團成立100週年團慶，曾經邀請客居柏林，依賴創作維生的筆者創作《慶典》管弦樂曲。1982年返國後，「國家交響樂團」20週年慶、「臺北市立國樂團」30週年慶、「臺北市立交響樂團」50週年慶，也陸續邀請筆者創作新曲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國立臺灣交響樂團」創立於第二次世界大戰後不久的1945年底，如今屆滿76載，它不僅是臺灣第一個交響樂團，也是亞洲第一個交響樂團。它曾經數度更名，多次變更團址，團員多次換血，如今的團員已經逐漸年輕化，演奏曲目也逐步擴張到各種風格流派，呈現出活躍璀璨的新氣象。「臺灣文化協會」創立於1921年。2021年「國立臺灣交響樂團」劉玄詠團長邀請筆者為「臺灣文化協會成立100週年紀念計畫」創作新曲，促成了《文化頌》管弦交響協奏曲的誕生。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者在管弦樂團制式的編制上做了適度調整，也擴張某些樂器族群，以彰顯《文化頌》管弦交響協奏曲的特色。例如：啟用8支長號與2支低音號，並將它分成3組，分別放在舞臺上與觀眾席的右後側和左後側，創造環繞音樂廳的立體音響，追尋嶄新的音響意境，並挑戰交響曲與協奏曲的新定位，抒發作曲家的創意、也讓人耳目一新。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《文化頌》管弦交響協奏曲，第一樂章：在層出不窮的敲擊聲響中，用原住民、河洛、客家與日本殖民統治者音樂元素的舖陳與衍生的矛盾與衝突，展現蔣渭水創辦人與「臺灣文化協會」的關鍵密碼逐漸顯著且分庭抗衡。第二樂章：用個別樂器的炫技片段，在群組間相互輝映成趣，或異軍突起展現自我，或相得益彰成就群體生態於多重律動音型和層面音響交織中，呈現「臺灣文化協會」歷經多次改造與變革，牽引著臺灣文化啟蒙與民族自決運動的核心價值。第三樂章：深入淺出的陌生化音響處理，擘畫了欣欣向榮的民主自由景觀，裡應外合的支聲異音語彙，凝聚了環環相扣的族群團結共識，古調新韻，幽情雅致。第四樂章：排山倒海而來的殖民統治與高壓迫害，激勵了遍地開花的群情激憤和風起雲湧般的抗爭，以妥協取代對抗，以包容化解仇恨，形塑多元文化生活底蘊。第五樂章：篳路藍縷而孕育眾生，含辛茹苦而啟滌生靈，管弦繚繞而鑼鼓喧天，創造了當前東西兼容並蓄，古今水乳交融的人文景觀，光輝璀璨，傲視寰宇的立體音響意境。回顧臺灣近百年的文化歷史軌跡，反映了知識菁英面臨的路線衝突，歷經激烈而曲折的紛擾與演變，最後戲劇性地糾纏在一起，深深影響著臺灣多元社會的變遷，也微妙地牽引著生命共同體的脈動與命運。《文化頌》管弦交響協奏曲於2021年10月2日在「國立臺灣交響樂團」「臺灣文化協會成立百週年系列」NTSO「文化頌」音樂會中，由范楷西指揮「國臺交」，成功的首演於該團演奏廳，隔日10月3日再度假臺南文化中心演出，獲得極大的好評。該場音樂會，上半場有許雙亮編曲：《臺灣議會設置請願歌》、張菁珊編曲：《音樂百年》與盧亮輝作曲：《西子灣之戀》小提琴協奏曲。《文化頌》管弦交響協奏曲，演奏長度約40分鐘，被安排於下半場。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，「國立臺灣交響樂團」2021也辦理了「4K5G上的福爾摩沙音樂家」計畫案，此計畫以認識臺灣在地音樂家為系列主題，分階段將臺灣各縣市在地音樂家以專題式製作影音節目，於影音平臺網站及YouTube頻道線上欣賞，提供學校師生及一般民眾認識臺灣在地音樂家，以推廣臺灣音樂文化。更值得讚揚的是「國立臺灣交響樂團」每年定期舉辦「青年音樂創作競賽」，催生我國原創音樂的創作，並建置從創作、實踐、出版到行銷的完整產銷平臺，提升臺灣作曲家音樂作品的質與量，成果卓著&hellip;。多年來，曾經獲獎的臺灣傑出青年作曲家，有不少人已經在國內外嶄露頭角，可喜可賀。</p>

