<h4>二、台北室內合唱團（Taipei Chamber Singers）</h4>
<p class="bui">「台北室內合唱團」成立於1992年，以30年演唱經驗，成為亞洲最頂尖的合唱組織。在前任藝術總監暨指揮陳雲紅，及歷任指揮梁秀玲、翁佳芬帶領下，從早期音樂到現代作品，從人聲重唱、室內合唱團到交響合唱團，台北室內合唱團均展現出高度藝術性。2006年匈牙利巴爾托克國際合唱大賽，獲得室內合唱團項目第一名與大賽總冠軍。台北室內合唱團已在全球留下演唱足跡，頻繁的演出行程，包括丹麥、英國、法國、義大利、奧地利、德國、西班牙、匈牙利、斯洛維尼亞、美國、日本、中國、澳門、香港等地。<br>身為合唱音樂先行者，台北室內合唱團自2006年啟動「合唱無設限」計畫，建立現代合唱音樂委託創作與發表平台，歷年來已與臺灣、香港、中國、馬來西亞、美國等地之傑出作曲家，委創超過四十多部作品，風格涵蓋無伴奏人聲、室內樂、電子音樂、現代劇場⋯等，在臺灣、丹麥、日本、中國、香港等地，持續發表深具前瞻眼光的合唱新作，並受邀2007「ISCM&ndash;ACL國際現代音樂節」、2011「ACL亞太音樂節」、2015「ISCM國際音樂節」、2017「上海當代音樂週」等國際現代音樂節中演出。<br>「當代眼光，完美的聲響」，台北室內合唱團立足亞洲，放眼全球舞台，在音樂會演出、現代音樂委創、音樂教育等不同面向，皆以聲音藝術傳遞者為志，打造令人驚異的東方美聲。2021年，TCS最新大碟，全創作專輯《䪩》&mdash;無設限的詩歌紀行，豪取第32屆傳藝金曲獎【最佳演唱：台北室內合唱團】、【最佳專輯製作人：陳雲紅】、【最佳作曲：潘皇龍〈夢遊女〉】、【最佳錄音：楊敏奇】四項大獎，創下金曲獎自2007年增設藝術音樂類以來，專輯獲得最多獎項的歷史紀錄！<span style="font-family:sans-serif !important;">（台北室內合唱團網頁）</span></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;潘皇龍作品《夢遊女》無伴奏混聲八部合唱曲係「台北室內合唱團」委託創作，歷經多次演出，爾後在「國立臺北藝術大學」音樂廳製作錄音發行。歌詞取自陳黎先生的詩作〈夢遊女之歌〉。全曲分成5段：首段（A）透過「韻母」與「複韻」的逐次渲染，牽引出夢境的場景，或形影相隨，或一形數影逐步分流擴散，遊盪在「和諧」－「緊張」－「和諧」的循環中。第二段（B）的樂句歷經分割或片斷堆疊，形成較為不規則的張力與不平衡的動感。第三（C）、四段（D）各以朗誦起，再以稍慢的速度歌唱，塑造戲劇性的張力與對比，彰顯劇情發展在對情人之「愛」與對情敵之「恨」的雙重終極面向。尾段（B+A）短暫倒轉第二段的場景，回復到初始的夢境，卻在情境逆行中隱喻著愛恨糾纏不得平靜而徐緩落幕。本曲曾獲「國家文化藝術基金會」創作補助，第三十二屆「傳藝金曲獎」最佳作曲獎。</p>
<h4>三、采風樂坊（Chai Found Music Workshop）</h4>
<p class="bui">「采風樂坊」是臺灣的傳統絲竹音樂樂團，在1991年由笛子演奏家吳宗憲與二胡演奏家黃正銘成立，主攻胡琴、笛子、琵琶、古箏、揚琴與阮咸等中國傳統絲竹樂器。旗下另有專門培育中國傳統音樂人才的「益生文教基金會」，是2000年由江朝陽居士與采風聯手成立。旗下另有專門出版有聲資料及樂譜的出版社「采風國際文化事業有限公司」。采風樂坊最著名之音樂劇創作，當屬2005年的《十面埋伏》。而在2007年，采風更在國家音樂廳二十週年慶時，以上千人同時拉胡琴，創下「千人胡琴」的世界紀錄。<span style="font-family:sans-serif !important;">（采風樂坊網頁）</span></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年，「采風樂坊」黃正銘團長，委託筆者創作以花為主題的絲竹音樂，正好把我30餘年來在大安宅邸頂樓蒔花植樹的因緣際會略作回溯與盤整，我選擇孤挺花、曇花與玉蘭花等3種頗具代表性的花卉，作為主要敘說對象。它們一方面，陪伴了我在音樂創作上的思維與成長；另一方面，更見證了筆者自歐陸返國後的諸多創作語彙變遷與形式邏輯轉折。並探索從繁華喧鬧的臺北街頭，試圖創造回歸童年與青少年時代樸實幽靜、悠然自得的鄉間景緻。樂曲的進行，刻意不以單一花卉作為對象，而是以整體空中花園作為鋪陳。突顯孤挺花、曇花與玉蘭花等3種花卉在眾多花枝招展間，搭配大自然界的晴雨風寒、鳥叫蟲鳴間，清新脫俗而相映成趣。或輪番上陣、爭奇鬥艷，或清香撲鼻、令人陶醉，或驕豔動人，引人遐想。樂曲的演出長度約15分鐘。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「孤挺花浪漫無瑕」：孤挺花為石蒜科多年生草本球根花卉。或清幽高貴、嫵媚嬌矜，或孤芳自賞、英姿挺拔。它有個可愛的別名叫做「阿瑪黎麗絲」（Amaryllis），這是古羅馬詩人在田園詩中歌詠的牧羊女，透露了浪漫、鄉野的氣息，所以「喋喋不休」也就成為她的標籤了。「曇花一現．瞬間永恆」：曇花屬於仙人掌科柱狀仙人掌亞科的一種植物。所謂「曇花一現」，或曰：「剎那間的美麗」，「一瞬間的永恆」。它選擇在幽靜的夜晚綻放，潔白清香的大花朵，使她在歐美國家贏得了"Queen of the night"的美名，華裔文人雅士則歌詠她為「月下美人」。「玉蘭花堪稱愛意使者」：玉蘭花又稱木筆花、白玉蘭。玉蘭花開，如玉無瑕，純真樸實，它的香氣，堪稱是臺灣人的共同記憶。它表報恩、高貴、端莊、愛意、純潔、有著忠貞不渝愛情的寓意，每逢喜慶吉日，人們常以玉蘭花饋贈，堪稱為「表露愛意」的使者。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年，采風樂坊委託本人創作的《花團錦簇》於1月21日在該團「花雨．花語」音樂會中，假國家演奏廳成功首演。此外，11月中旬采風樂坊應義大利「羅馬新音樂節（Nuova Consonanza）」邀請的國際交流音樂會，因為嚴重特殊傳染性肺炎仍持續發威，展演只得改採線上視訊方式進行，容後國際音樂交流新型態中說明。</p>
