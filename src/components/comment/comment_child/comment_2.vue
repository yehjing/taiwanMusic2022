<h4>四、金曲獎與傳藝金曲獎的評審工作</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者曾經於1994年應邀擔任第五屆評審委員，並膺選總召集人：2019年與2020年再度應邀擔任第三十屆與三十一屆出版類評審委員兼評審委員會總召集人。老實說，傳藝金曲獎的評審工作非常繁複而長久，身為評審委員，尤其是身兼總召集人，要出席金曲獎的評審說明會，前後約2個月在家重覆聆聽/觀賞約200張（套）CD/DVD，與閱讀相關文字說明資料，繳交評審成績，參加複審會議選出各項入圍名單，召集人出席金曲獎記者會公布入圍名單，出席金曲獎檢討會議，出席金曲獎決審會議，議決得獎名單，並出席頒獎典禮，前後持續超過半年的歲月才宣告完成。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當然，1994年參與新聞局主辦的金曲獎已經不復記憶，歷經25年再度應邀擔任評審工作，不管獎項或評審委員的組成方式皆改變了許多。獎項方面逐年略微調整，近年來出版類有13個獎項，戲曲表演類有7個獎項。至於出版類評審委員，會依作曲家、演奏（唱）家、音樂學家與錄音師不同專業領域各若干人，合計12人組成；戲曲表演類評審委員9人，總計評審委員為21人。有關部分獎項，雖然最佳傳統表演藝術影音出版獎劃歸出版類，其實其專業領域屬於戲曲表演類，由戲曲表演類評審委員擔任評審工作，是合理而且必須的，只是因為事涉出版而寄放在出版類。然而自從2021年起開放非實體CD/DVD的數位音樂平臺參與報名，最佳傳統表演藝術影音出版獎應該可以名正言順回歸戲曲表演類；再者，出版類也應該順理成章更名為音樂類，形成音樂類12個獎項，戲曲表演類8個獎項的新格局，持續為音樂界與戲曲表演界發光、發亮。</p>
<h3>柒、國際音樂文化交流新型態</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以下是筆者2021年參與的幾個國際交流活動的紀錄，有在南韓、奧地利、義大利的作品發表會，有在義大利的專題講座/作曲大師班，有在臺灣的學術研討會，皆因疫情嚴峻改採視訊方式進行：</p>
<p><strong>一、南韓「泛音音樂節（The 49</strong><strong>th</strong><strong> Pan Music Festival/ISCM-Korea）」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pan Music Festival（簡稱PMF）係ISCM（International Society for Contemporary Music）大韓民國分會的重大業務，2021年邁入第四十九屆。早年由「德國歌德學院首爾分院」強力贊助，勘稱亞洲最重要、持續推動新音樂演出的音樂節之一。它最主要的推手是「首爾大學」作曲家姜碩熙（Sukhi Kang）教授，筆者「柏林藝術大學」的學長，他曾經在1986年邀請筆者到首爾發表作品；湊巧1987年「荷蘭三重奏團（Het Trio）」應邀演出，安排了拙作《因果三重奏曲》，奠定了爾後合作的管道。當他自首爾大學退休，隨即成立「姜碩熙音樂學院（Kang Sukhi Music Institute）」，也曾經邀請「采風樂坊」到首爾演出，持續推廣現代音樂與國際音樂文化交流。2021年PMF邀請「台北中央C室內樂團」演出作曲家潘皇龍、Youngkwang Yang、Michael Sidrey Timpson、Jacob David Sudol、Jackyang Lim、董昭民等作曲家作品，算是再續前緣。</p>
<p><strong>二、奧地利「薩爾茲堡景觀（Aspekte Salzburg）」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拙作《五行生剋八重奏》，獲選為荷蘭「國際高德阿姆斯新音樂節（International Gaudeamus Music Week）」1980年度代表作，同年9月5日假荷蘭海牙音樂廳，由Klaus Ager指揮「奧地利新音樂室內樂團（Ӧsterreichisches Ensemble f&uuml;r Neue Musik）」首演，極受好評。隨後於1981年6月17日，再由該團在「薩爾茲堡景觀」演出，開啟了雙方爾後多次的互訪與交流活動。1998年，「薩爾茲堡景觀」演出「音樂臺北」作曲比賽得獎作品4首，暨拙作《因果三重奏》（Ursache und Wirkung）與《蝴蝶夢》（Transformation）等合計6首作品。2021年邀請「中央C室內樂團」線上視訊音樂會，演出Chao-Ming Tung："Demolding of Mountain-Shape"（2018）, Klaus Ager："7 Wege in H. "（2018）, Hwang-Long Pan："East and West VIII"（2014）, Michael Mautner："39,4"f&uuml;r Violine und Klavier（1990）等作品。</p>
<p><strong>三、義大利「羅馬新音樂節（58</strong><strong>th </strong><strong>Nuova Consonanza）」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年「羅馬新音樂節」邁進第五十八屆，邀請「采風樂坊」舉行線上視訊音樂會，演出潘皇龍的3首作品，以及由采風樂坊推薦2位臺灣年輕作曲家王樂倫與蔡承哲的作品。並邀請筆者提供以「國立羅馬音樂學院」師生為對象的4場各3小時的專題講座/作曲大師班（"De Musica"）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;身為臺灣作曲家，作品被演出自然以臺灣為主。在亞洲以日本、南韓最多。在歐洲以西歐最多，東歐或南歐次之。筆者在南歐演出的版圖，有法國、西班牙、希臘與克羅埃西亞等國，甚至於跨越地中海到北非的埃及（開羅、亞歷山大港）與中東的沙烏地阿拉伯（吉達）、約旦（安曼）與黎巴嫩（貝魯特）等國，2020年以前，唯獨少了義大利，不無遺憾。2021「采風樂坊」線上視訊音樂會，演出潘皇龍的作品《迷宮．逍遙遊》為5位傳統樂器演奏家，蔡承哲《大黑天》與王樂倫《鳳凰花》的作品，以及潘皇龍的《花團錦簇》為傳統樂器六重奏曲，與《釋．道．儒》5首傳統樂器六重奏曲等5首絲竹作品，補足了筆者在南歐演出的最後一塊版圖。該線上視訊音樂會的影音由義大利「國際羅馬新音樂節（"Nuova Consonanza"）」播放於臺北時間11月29日04:00-05:30/義大利時間11月28日21:00-22:30，12月12日同一時段再度重播。</p>
<p><strong>四、臺灣「南華大學」當代作曲家新創作交流會：「藝術性音樂中前衛與實驗性的創作思維」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年「南華大學」民族音樂學系（所）為了舉辦「當代作曲家新創作交流會」，由召集人George Holloway（中文姓名盧長劍）成立學術委員會，邀請筆者擔任研討會的論文及作品徵選與論文評論委員，並公開向全球徵求作品與稿件兩部分作品，有來自臺灣與全世界各約30篇（部）應徵，結果一共有9篇論文和8部作品入選。因為疫情關係，論文發表部分於7月3日採線上視訊方式進行，音樂會的部分則分別錄製入選之作品，將影音連結發布於網頁，供大家聆聽。</p>
