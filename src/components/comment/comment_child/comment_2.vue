<h4>四、對位室內樂團（Ensemble Counterpoint）</h4>
<p class="bui">「對位室內樂團」擅長以古典音樂為主軸並揉合戲劇元素，既能不失古典音樂的細膩、保有古典音樂的專業、精緻與豐富細膩的情感，並且使古典音樂更貼近人心。而演奏國人的作品，用自己的語言唱自己的歌，亦是對位創團宗旨之一。自團隊成立以來，除演出西方作曲家之作品外，也演奏國人作曲家盧炎、李子聲、周久渝、李元貞、張俊彥、石青如、陳玠如等人的創作。透過多樣元素的結合，使聽眾在聆聽西方音樂時，也能認識國人的優秀作品，並致力於將國人的音樂推至國際舞台。<br>「對位」室內樂團成軍將邁入第十個年頭，多位學成歸國、熱愛音樂的音樂家們，藉由精緻室內樂的推廣，深耕南台灣的音樂環境，進而推廣音樂教育，並將演出的足跡拓展至全台各地。樂團目前為得文化部「演藝團隊分級獎助計畫」團隊之一，連續獲邀參與「高雄春天藝術節」演出，並錄取國立傳統藝術中心「105-106年音樂人才培育-大師門徒與夥伴」計畫甄選，肯定團隊推廣國人作品及精緻音樂活動不遺餘力。<span style="font-family:sans-serif !important;">（對位室內樂團網頁）</span></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年3月7日「對位室內樂團」假高雄市衛武營國家藝術文化中心表演廳主辦「動物派對」從海洋到森林音樂會，演出潘皇龍作品《螳螂捕蟬．黃雀在後》，室內樂成員有男高音林健吉、長笛江品誼、單簧管葉明和、擊樂王盈智、小提琴林佳霖與大提琴黃子維擔綱演出。同年10月17日該團又假高雄市文化中心至善廳主辦「在月光下」－「聽．韻」音樂會，分別由洪菱憶（笙）、王冠傑（單簧管）、趙怡然（琵琶）、陳品均（大提琴）與賴宜絜（古箏）演出潘皇龍作品《東南西北IX》。「對位室內樂團」能夠在高雄異軍突起，總算疏緩了南北不平衡的差距，讓人驚喜。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《東南西北IX》係「國際現代音樂協會台灣總會」2017委託創作，並謹以此紀念恩師尹依桑（Isang YUN, 1917-1995）百歲冥誕。本曲曾獲「國家文化藝術基金會」創作補助，於2017年10月20日假臺北國家演奏廳首演。</p>
