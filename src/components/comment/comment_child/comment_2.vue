<h3>陸、金曲獎及傳藝金曲獎之歷史沿革與獎項</h3>
<h4>一、金曲獎及傳藝金曲獎之歷史沿革</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有關金曲獎及傳藝金曲獎的歷史沿革，國立傳藝中心網頁有如下的整理和回顧：</p>
<p class="bui">1988年：行政院新聞局正式籌辦「金曲獎」，並與唱片業者與音樂界從業人員訂定了《金曲獎獎勵要點》。首屆金曲獎共設11個獎項（最佳年度歌曲獎、最佳作詞人獎、最佳作曲人獎、最佳編曲人獎、最佳單曲歌唱錄影帶影片獎、最佳單曲歌唱錄影帶導演獎、最佳男演唱人獎、最佳女演唱人獎、最佳演唱組獎、新人獎、特別獎）。1989年：第一屆「金曲獎」8月開放給在臺灣地區有居所，並有半年居住事實的中華民國國民和香港居民報名。1990年：10月舉辦的第2屆金曲獎增設專輯、專輯製作人以及錄音獎項，第3屆將演奏專輯獨立設獎，並開始按國語（華語）以及台語等方言分設獎項。<br>1995年：4月，新聞局廣電處在舉辦了6屆金曲獎後，將金曲獎主辦業務移轉至新聞局出版處。1996年：第7屆金曲獎落幕後，新聞局出版處提出音樂獎項合併方案，並比照同年金馬獎邀請華語電影參賽的模式，將金曲獎擴大為所有華語音樂都能參與的跨國性音樂獎項。1997年：第8屆金曲獎起，金鼎獎唱片獎項正式自併入金曲獎，同時金曲獎增設傳統音樂及藝術音樂獎項；同年參賽國籍限制放寬，以增設獨立獎項方式，開放中國大陸及其它國家的華人報名。1998年：第9屆金曲獎進一步取消個人獎項的國籍限制，只要作品於臺灣地區首次發行者均可報名參選，並增設獎金（出版獎獎項獲獎者獎勵新臺幣15萬元、個人獎獎項則獎勵新臺幣10萬元）首度將頒獎典禮改為現場直播的方式播出；該屆金曲獎報名件數首度突破千件，報名件數後續也出現大幅成長。<br>2001年（第12屆）首度將金曲獎頒獎典禮移師高雄舉行，2002年（第13屆）、2005年（第16屆）亦在高雄舉行，但2006年（第17屆）與2007年（第18屆）以後的流行類頒獎典禮固定於2005年底啟用的臺北小巨蛋舉行。<br>2003年：第14屆金曲獎為鼓勵介於古典和流行之間的跨界作品而增設「最佳跨界音樂專輯獎」，並為鼓勵客語及原住民語音樂而將「最佳方言男演唱人」和「最佳方言女演唱人」兩個獎項改為「最佳台語男演唱人」、「最佳台語女演唱人」、「最佳客語演唱人」和「最佳原住民語演唱人」四個獎項。2005年：第16屆金曲獎將流行類演唱專輯獎和新人獎分別按語言劃分為國語、台語、客語和原住民語四個獎項；但新人獎因參選作品較少，隔年第17屆金曲獎便重新合併成單一獎項。2007年：第18屆起將「傳統暨藝術音樂類」與「流行音樂類」獎項分開舉辦頒獎典禮。並增設「評審團獎」以獎勵未能入圍但具有特色的優秀報名作品。2008年：第19屆「傳統暨藝術音樂類」一度因評審反應「最佳民族樂曲專輯獎」、「最佳戲曲曲藝專輯獎」報名數量太少，合併為「最佳傳統音樂專輯獎」，並在個人獎項增設「最佳傳統音樂詮釋獎」。後因曲藝業界創作者反彈聲浪大，認為自己因此無獎可報名，故2009年第20屆又將「最佳民族樂曲專輯獎」、「最佳戲曲曲藝專輯獎」恢復為獨立獎項。2009年：第20屆「傳統暨藝術音樂類」增設「最佳傳統歌樂專輯獎」。<br>2014年：第25屆起，由國立傳統藝術中心統籌承辦「傳統暨藝術音樂類」之獎項評審及頒獎典禮，正式正名為「傳藝金曲獎」。獎項上調整幅度較大，首先將獎項分為「出版類」和「戲曲表演類」兩大類別。在「出版類」增設對音樂出版業界有重要影響力的「最佳錄音獎」，以及獎勵傳統表演藝術界出版品的「最佳傳統表演藝術影音出版獎」；在「戲曲表演類」增設「最佳年度演出獎」、「最佳表演新秀獎」。2016年：第27屆起，戲曲表演類增設「年度最佳演員獎」。2019年：第30屆起，戲曲表演類再增設「最佳劇本獎」及「最佳音樂設計獎」，落實獎項和業界脈動之連結，回應業界之需求。<span style="font-family:sans-serif !important;">（傳藝中心網頁）</span></p>
<h4>二、金曲獎及傳藝金曲獎辦理之重大變革</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;查1989年行政院新聞局辦理第一屆金曲獎，原來純粹為流行音樂而設置，自1997年（第八屆）起，該局金鼎獎唱片獎項正式併入金曲獎，同時金曲獎再增設傳統音樂及藝術音樂獎項。1998年（第九屆）首度將頒獎典禮改為現場直播的方式播出。2007年（第十八屆）起將「傳統暨藝術音樂類」與「流行音樂類」獎項分開舉辦頒獎典禮。2014年（第二十五屆）起，由國立傳統藝術中心統籌承辦「傳統暨藝術音樂類」之獎項評審及頒獎典禮，正式正名為「傳藝金曲獎」，獎項調整幅度較大，分為「出版類」和「戲曲表演類」。2021年（第三十二屆）起，報名資格除了CD/DVD實體出版外，增列數位平臺參賽資格。</p>
<h4>三、傳藝金曲獎的分類與獎項</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳藝金曲獎分類與獎項，以2021年為例，分出版類與戲曲表演類，獎項如下：</p>
<p><strong>（一）出版類：</strong>共13個獎項</p>
<p>1.團體獎：最佳傳統音樂專輯獎、最佳藝術音樂專輯獎、最佳跨界音樂專輯獎、最佳宗教音樂專輯獎、最佳傳統表演藝術影音出版獎。</p>
<p>2.個人獎：最佳作曲獎、最佳作詞獎、最佳編曲獎、最佳專輯製作人獎、最佳演奏獎、最佳演唱獎、最佳錄音獎。</p>
