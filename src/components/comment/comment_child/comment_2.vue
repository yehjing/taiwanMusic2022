<h4>五、台北中央C室內樂團（C-Camerata Taipei）</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北第一個演奏古典和現代經典的中西混編室內樂團，創團音樂會「新經典系列&Iota;－傳承」於2014年4月9日假臺北國家演奏廳隆重推出；曲目煥然一新，讓人刮目相看。計有1.陳曉勇："Duet" for Violin and Zheng（1989），2. Gy&ouml;rgi Ligeti："Etudes" pour Piano，3.陳曉勇："Diary III" for piano，4. Gy&ouml;rgi Ligeti："Sonata" for Cello Solo，5. Henry Cowell：Piano Trio for violin, violoncello and piano，6. John Cage："Variation I"（笛子、琵琶、古箏、電子音樂），7. Helmut Lachenmann："Interieur I" for solo percussionist，8.潘皇龍《東南西北VIII》給中西混合編制室內樂團（委託創作）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《東南西北》系列作品的創作理念，有幾個特色：（一）樂器編制包涵了傳統樂器與西洋樂器。（二）揭示新音列、新調式與新結構創作語法的理論與實踐。（三）從意念的凝聚、樂想的擴充至精神的表徵上，體證古今文化水乳交融、東西音樂兼容並蓄的美感。基於上述理念，筆者選擇了笙、琵琶與古箏作為傳統樂器的代表，與西洋樂器中的單簧管與大提琴結合為五重奏。一方面以遵循各別樂器的特殊性能與歷史傳統為縱軸，另一方面更以新音列、新調式與新結構為橫軸，透過各樂器間的共通性與獨特性之分析比較與整理歸納，試圖反映吾人身處20世紀、21世紀之交的心聲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《東南西北》系列作品係建立在3個互為因果關係的音列基礎上，「音列A」為21弦箏的獨特定弦。「音列B」為由大七度、小七度循序遞減至小二度的音程重疊。「音列C」係以中央C為主軸，順序由小音程至大音程分向兩側排列輪換組合而成。以上3個音列為本系列作品音高上的基礎，因為各音列的某些音有重覆，也有省略，而且並不侷限在一個八度內，以茲展現它的多變性與不可限量性，姑且定名為「新音列、新調式與新結構」，以與吾人所認知的調性音樂音階或非調性音樂音列作區隔。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年2月20日「中央C室內樂團」假「十方樂集」為奧地利「薩爾茲堡景觀（Aspekte Salzburg）」錄製潘皇龍作品《東南西北VIII》，同年3月5日於當地時間19:00視訊傳播。10月9日再由陳泰吉指揮「中央C室內樂團」假「十方樂集」錄音錄影，於11月5日以視訊傳輸韓國"The 49th Pan Music Festival"。</p>
