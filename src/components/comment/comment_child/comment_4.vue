<p><strong>（三）</strong></p>
<span>節目名稱：「跨界．跨年」2021新年音樂會</span><br>
<span>跨界元素：國樂、爵士樂</span><br>
<span>演出單位：鍾耀光、李念澐、楊斯琪爵士五重奏、桃園市國樂團</span><br>
<span>演出時間：2021/1/3</span><br>
<span>演出地點：中壢藝術館音樂廳</span>
<p></p>
<span>演出內容：桃園在地深耕的桃園市國樂團是一個年輕、有活力，立足傳統的同時，更勇於追求創新和跨界製作的國樂團。這場「跨界．跨年」是特別為慶賀2021年到來的新年音樂會，特邀請了臺北市國樂團前團長、也是知名跨界作曲家鍾耀光來創作並指揮，爵士歌手楊斯琪及其爵士團隊來獻唱演出，以及桃市國的古箏首席獨奏家李念澐，共同合作了這場可既跨界且跨文化，雅俗共賞的演出。</span><br><br>
<p><strong>（四）</strong></p>
<span>節目名稱：《聽見．宮崎駿》</span><br>
<span>跨界元素：親子劇、音樂劇場、動畫配樂</span><br>
<span>演出單位：狂美交響管樂團、如果兒童劇團</span><br>
<span>演出時間：2021/1/10</span><br>
<span>演出地點：苗北藝文中心演藝廳</span>
<p></p>
<span>演出內容：「狂美交響管樂團」的成員皆來自國內外高等學府畢業的高學歷青年音樂家，該團以「精緻」、「創新」、「傳承」、以及「和眾」為四節目製作導向。創團以來，「狂美」從演出的內容、形式、到合作的夥伴，皆不將自己設限在「交響樂」的框架中，動畫配樂、親子劇場、百老匯音樂劇、兒童劇、主題式音樂會等，都是「狂美」樂於嘗試的表演。《聽見．宮崎駿》便是「狂美」與「如果兒童劇團」（趙自強領軍）的跨界合作，一起演出宮崎駿動畫中的奇幻故事，演繹久石讓雋永迷人的動聽音樂。</span><br><br>
<p><strong>（五）</strong></p>
<span>節目名稱：朱宗慶打擊樂團35週年－2021擊樂劇場《泥巴》</span><br>
<span>跨界元素：擊樂劇場、臺語文學、新詩、陶瓷工藝、數位藝術</span><br>
<span>演出單位：朱宗慶打擊樂團</span><br>
<span>演出時間：2021/1/16-1/17</span><br>
<span>演出地點：衛武營國家藝術文化中心/音樂廳</span>
<p></p>
<span>演出內容：「朱宗慶打擊樂團」的多元觸角和運作模式，早已是業界公認的模範團隊，我國私立樂團的領導品牌之一，該團對於跨界方面的開發與改革有先見之明，在多年前便以開始進行跨界的創演實踐。《泥巴》是繼《木蘭》之後，另一齣經典擊樂劇場製作。《泥巴》的靈感源自打擊樂與陶瓷工藝的跨界共鳴，以苗栗蘆竹湳在地人文風情為藍本，所有對白語歌詞以臺語文學呈現，配合數位藝術的運用，表現獨特的臺灣擊樂跨界之美。</span><br><br>

<p><strong>（六）</strong></p>
<span>節目名稱：「花雨．花語」</span><br>
<span>跨界元素：花藝、文學、絲竹樂</span><br>
<span>演出單位：采風樂坊</span><br>
<span>演出時間：2021/1/21</span><br>
<span>演出地點：國家兩廳院/演奏廳</span>
<p></p>
<span>演出內容：采風樂坊是把絲竹樂導向「東方器樂劇場」發展的重要推手，從《十面埋伏》、《七太郎與狂狂妹》、《東方傳奇．搖滾國樂》、《無極》、直到《阿銘上菜》等，采風樂坊長期耕耘音樂跨界製作的成績有目共睹。新絲竹計畫「花雨．花語」，是結集花藝、音樂與文學的跨界製作。</span><br><br>
<p><strong>二、2月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比起1月份的演出，2月份的演出相對有限，在跨界音樂的部分僅有少數的作品演出，其中在牯嶺街小劇場演出的《覺醒者Awakening》是以音樂劇場為自我定位，該作品特別處理視覺性和身體性及場域關係，挑戰欣賞者的聆聽慣習，並且打破傳統樂器的演奏方法。此外在多媒的運用上，也挑戰了傳統音樂劇場的單一思想，在廣度與層次的敘事性上，都毫不吝嗇地提供觀演過程的元素，可謂近期跨界音樂相當具實驗性的作品。</p>
<span>節目名稱：多媒介的音樂劇場《覺醒者Awakening》</span><br>
<span>跨界元素：舞蹈、劇場、器樂、聲樂、多媒藝術</span><br>
<span>演出單位：李俐錦、周新純、周寬柔、吳志維</span><br>
<span>演出時間：2021/2/27</span><br>
<span>演出地點：牯嶺街小劇場一樓實驗劇場</span><br><br>
<p>演出內容：除了「音樂劇」（Musical）和歌劇（Opera），這類以故事劇情為基底，搭配器樂、聲樂與舞蹈肢體動作的表演方式外，音樂人常從事的另一種跨界演繹，則是以音樂本身為表現主體的「音樂劇場」（Music Theater）。《覺醒者Awakening》的創作發想便是以「音樂劇場」為定位，讓音樂家、舞者合作，各自打破原生領域的框架，在音樂（作曲者為劉韋志）的引導下場，嘗試視覺性、聽覺性和身體性的交互轉化，以及跨域表演者（音樂家李俐錦、周新純，舞蹈家周寬柔、吳志維）之間的主客易位。此外，對於前衛劇場中所應用的物質性媒介（如器物、道具、身體）與抽象性元素（如噪音、聲響、意境），也在《覺醒者Awakening》中被磨練與融合。</p>
<p><strong>三、3月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3月份出現了兩個與眾不同的跨界音樂作品，其一是在臺灣戲曲中心上演的，由國立臺灣交響樂團與故事工廠合作的原創魔幻音樂劇《妖怪臺灣》，此音樂劇結合了音樂、戲劇及燈光音效等華麗的舞美效果，讓交響樂除了聽覺上的展開之外也在視覺、及敘事性上有了非常豐富與飽滿的歷程。戲劇性始終是音樂創作中一個非常引人入勝的著力點，除了扣人心弦的劇情鋪陳與角色刻畫之外，此曲關係詩樂合一、乃至於抑揚頓挫的唱腔，都能放大音樂的魅力，這也正是文學與音樂的關係從古至今始終是創作者經常關注的議題。2021年3月19日在國家兩廳院的演奏廳，上演了一場以音樂和文學為兩大素材的跨界講座音樂會「音樂與文學系列：吟詩、閱讀與寫作」，由知名的聲樂家、作家和詩人一起合作，如焦元溥、林慈音、翁若珮、林義偉、羅俊穎、許惠品等人以對話、閱讀、朗誦、吟詩等方式，營造出音樂與文學的融合魅力。</p>
<p><strong>（一）</strong></p>
<span>節目名稱：NTSO x故事工廠《妖怪臺灣》原創魔幻音樂劇</span><br>
<span>跨界元素：戲劇、器樂、聲樂</span><br>
<span>演出單位：NTSO國立臺灣交響樂團、故事工廠</span><br>
<span>演出時間：2021/3/12-3/14</span><br>
<span>演出地點：臺灣戲曲中心大表演廳</span><br><br>
<p>演出內容：國內音樂界和戲劇界兩大團隊－國立臺灣交響樂團和故事工廠－攜手合作，以臺灣在地傳奇故事為藍本，創演出結集視覺、聽覺與故事情節的多重藝術饗宴。</p>
<p><strong>（二）</strong></p>
<span>節目名稱：音樂與文學系列－吟詩、閱讀與寫作</span><br>
<span>跨界元素：文學、聲樂、教育</span><br>
