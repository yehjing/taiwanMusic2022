<span>演出時間：2021/3/19</span><br>
<span>演出地點：國家兩廳院演奏廳</span><br><br>
<p>演出內容：音樂與文學在聲樂、歌劇中是密不可分的好夥伴，但如何以音樂為主體來創作出「文學場景」，以呈現出如詩如畫、有文學觸感的音樂，則考驗著作曲家、作詞人與演唱家的功力。這場以文學和聲樂為主題的另類演出，透過解說、演唱與示範，帶領觀眾在與眾不同的文學場景中品嘗聲樂之美，玩味詩詞字裡行間的寓意，窺探文學在音樂中的閱讀與寫作。</p>
<p><strong>四、4月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4月份的跨界音樂比較引人注目的有：琴園國樂團於中山堂中正廳所上演的說書人音樂劇場《風華大稻埕》，該節目串連了在地的戲曲精隨，融合了包括南北管、布袋戲、歌仔戲等的元素，以說書人的方式呈現出臺北城百年風華的樣貌，堪稱一場獨樹一幟的跨界音樂演出。另一場4月在中山堂光復廳的演出則是由鼓魯擊世界打擊樂團所帶來的「無國擊」－關於國樂與世界擊樂，為該打擊樂團在臺灣首場將國樂與世界打擊樂結合的新創作品，許多來自世界各地民族、社會、語言與國樂的語言融合在一起，呈現出跨文化的融合與創新，令人在美學中亦處處發現意外的驚喜與異國情調。該節目廣邀國樂界與世界音樂演奏家一起合奏，包括林雅雪、羅正楎、黃勝宏、金光亮平及林小楓等人。擊樂劇場始終是發揮跨界音樂的極佳類型。另一場與眾不同的跨界音樂演出是在嘉義市文化局廣場所上演的《一擊瞬間》，該節目是由艸創心演擊樂場所帶來的節目，該節目包含音樂、戲劇、雜技、特技以及武術等元素，以喜劇般的劇情和對話，帶動現場的親民形象，是一場展現親和力與民為伍的演出。</p>
<p><strong>（一）</strong></p>
<span>節目名稱：【TCO】藝享系列：風華大稻埕－說書人音樂劇場</span><br>
