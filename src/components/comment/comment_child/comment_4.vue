<h3><strong>陸、2021年跨界音樂現象</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過上述2021年跨界音樂節目的統整中，可以發現受到疫情之因素影響，跨界音樂演出數量不若去年2020年的節目多，其中各節目製作跨表演藝術類的比例（56.3％）最高，跨非表演藝術領域（21.9％）其次，兩者兼有（21.9％）的與跨非表演藝術領域之比例相同，跨表演藝術節目之比例高達所有節目的一半，顯示多數製作仍以跨表演藝術領域為出發，畢竟相較於花較多心力去嘗試新領域，由表演藝術間的「內跨」是較為保險而容易的作法；此外，兩者兼有的節目製作將近所有節目的四分之一，顯示跨界音樂能夠嘗試結合表演藝術與非表演藝術領域的製作頗多，並且佔有一定的比例，足見當代節目製作跨域他領域的思維普遍，也能夠為觀眾所買單。筆者觀察2021年的跨界音樂之發展現象，發現本年度的跨界作品因疫情的影響，在作品編創與製作上趨於保守，也因疫情的關係造成許多節目必須被迫離開劇場、音樂廳等平常熟悉的表演場域，開發其他更多與觀眾連結的形式或管道。綜觀2021年的跨界音樂現象，筆者歸納為以下數點進行探討：</p>
<p><strong>一、跨界音樂製作趨於保守</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由2021年的跨界音樂演出節目中觀察，許多知名團隊不僅減少創新作品的產出外，在固有的演出場次中也急遽的減少。其中，在為數不多的作品中可以發現幾個耳熟能詳作品的身影：如朱宗慶打擊樂團的《泥巴》和《木蘭》、采風樂坊的《東方傳奇．搖滾國樂》，甚至是極受年輕人喜愛的跨界音樂團隊A Root同根生所帶來該團的主題曲《鬧三仙》等，這些極具獨特性的經典作品除了體現團隊的理念與精神外，也是團隊在追求藝術美學中不斷內化、精益求精的展現。回歸筆者於2019年跨界音樂的觀察中所提出「多方雜揉的重新定義」－各藝術團隊打破現存的表演形式，並重新定義屬於自我全新的表演形態，如朱團所提出的「擊樂劇場」、采風樂坊的「東方器樂劇場」等，由這些概念所打造的作品在當時具備創新與自我團隊的獨特性，也在歷年演出中不斷調整與檢討，實踐自我的美學追求、確立獨創的表演型態；時至2021年，可以發現這些經典作品更臻成熟與穩健，每每演出都是累積某團代表作的典範性質，並以此培養進而吸引觀眾，如同現任衛武營文化藝術中心戲劇顧問耿一偉（2021）便認為：「藝術是時代的產物，表演的純正與否、是原真還是改編等判斷，並非依賴什麼客觀的假定，而是以既有常規為基礎。但「常規」也是因人而異、因地制宜的，沒有絕對的標準答案，「<span class="bkt">這也是當前劇院或補助機制的主流趨勢，就是用更『好』、更『藝術性』、更『精美』的製作角度在規畫節目</span>」。藝術界中能夠跳脫凡俗、再創新猷者實占少數，能夠成就經典、形成風格更是極為不易，故采風、朱團等團隊創造經典，以及作品中不斷深化的美學追求相當值得肯定；但由另一層面來看，創作新作品需要投注更多的資源與心力，並承擔票房、環境等極大風險，反觀複製成功經驗、掌握原本客群與觀眾口味相對來說則較為容易，卻也使得跨界音樂製作上趨於保守，缺乏令人耳目一新的全新體驗，這也許是源自2019年底以來疫情衝擊所帶來的影響，在大環境較為不佳的情況下，保留實力並在原有基礎上更為精益求精，亦不失為永續經營之道。</p>
<p><strong>二、表演的解構與重組</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情影響了人類的活動，自然也影響了人類追求娛樂的方式。我國2021年自1月開始加強了國外入境的管制，5月中開始疫情警戒升至二級（停辦室外500人以上、室內100人以上之集會活動）。隨著疫情的逐漸擴散，於5月底開始升至三級警戒（停止室外10人以上、室內5人以上之聚會，僅保留維生、秩序維持、必要性服務、醫療及工務所需之外，其餘營業及公共場域關閉），至7月底因疫情初步獲得控制降回二級警戒，直至2022年依舊維持二級的警戒狀態。因此，2021年5月底開始至7月底，所有節目被迫取消或延期，罕見地創下表演藝術數年以來不曾見過的全面空窗期，而本年度其他時段則為了符合疫情二級警戒的規範，也開啟了我國表演藝術數位化的全新試煉。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在二級警戒的規範下，除了室內必須保持100人以下外，觀眾席保持梅花座並全程配戴口罩，如同強制斷絕了人與人的連結，也象徵著人與人之間保持著戒備與不信任的態度。因應以上的防疫措施，使得許多中大型的場館僅能開放少數座位，小型場館也在梅花座的規劃下使原本能夠容納的觀眾人數銳減，即便不論染疫的風險，就以票房和贊助為營生的表演藝術團隊來說，是絕對難以符合成本的。因此，在如此艱困的環境之中，唯有另尋其他方法才有機會生存下去，利用科技之便似乎是一條可行的道路。受疫情的影響，表演藝術界在2020年已經有數位化的基礎，如我國國家表演藝術中心，國立臺灣博物館，國家電影及視聽文化中心等國家級表演、影視機構早於2020年即提出《如何推動後疫情時代博物館、美術館、表演藝術館舍及各文化館舍之數位策展、線上藝文、數位行銷與服務專題報告》針對疫情提出相關建議，如表演的傳播上除了實體外，可以增加直播、預錄以及製播等方式，擴充觀眾欣賞節目的管道，技術層面建議可運用360度攝影機、4K多視角攝影機等設備，並透過VR頭顯穿戴裝置與平板顯示器的運用，另外搭配5G高速率的網際網路系統，將劇場由場域的限制打開並延伸至虛擬領域，利用科技來觸發未來表演更多的可能，但也不諱言「<span class="bkt">就表演藝術的特性來看，表演藝術『即時共感』、『臨場感』的特性依舊非常重要，『螢幕』絕對無法完全取代『劇場』</span>」（國家表演藝術中心、國立臺灣博物館、國家電影及視聽文化中心，2020），普遍認同表演藝術的數位化僅是疫情下的一種替代，表演藝術的現場性絕對無法取代。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如上所述，疫情下肆虐下的音樂廳或劇場場域受到嚴格的限制，表演藝術團體被迫強制跟上當代科技的腳步，將原本實境的表演場域移至虛擬的雲端世界，絕不只是傳播媒介的轉變這麼簡單。回歸藝術的歷史源流，表演藝術在舞臺上經歷數千年來實踐，自已形成表演者與觀眾間情感與藝術交流的連結管道；因此，相較於實地演出中觀眾可以自由選擇觀賞的角度與聚焦點，而觀賞演出錄影或直播影像，卻只能從掌鏡者、剪輯師所預設的脈絡進入。此外，在表演層面上，也因影音錄製的影響勢必更動原舞臺表演的設計，如同春河劇團團長郎祖筠認為以科技傳播表演不是不可能，而是完全可行的，「<span class="bkt">只是這是跟舞台劇截然不同的表演模式，即使是同樣的劇本，都需要做大幅的轉化。</span>」（李愛斐，2020）而且認為網路串流的優勢仍舊是在行銷方面，透過網路的影音平臺將部分作品內容呈現可以讓觀看者提高興趣，進而進劇場體驗現場演出的魅力。因此，傳播形式改變了原本表演的內容，不論是曲目、動作、劇情、舞臺效果等表演元素勢必在解構後重新設計與編排，找到表演與影像間的平衡。而跨界音樂發展至今已非傳統音樂演出所可以比擬，除了普遍性的與表演藝術領域的舞蹈、戲劇、戲曲與雜技結合外，多媒體、美術、文學等他領域亦是經常合作的對象，表演過程中往往為了追求更好的效果，搭配燈光、音響、多媒體甚至是裝置藝術等劇場元素呈現，在多維度的表演時空下絕非單一取鏡所能捕捉，因此受限於影像錄製，創作者勢必解構原有的表演原素，在視覺上尋求最好的效果。反之，從另一層面科技的運用上來觀察，相較於傳統表演場域的舞臺，透過雲端與無線的傳播打破了原本地域性的限制，跨界音樂創作者反而可以在傳統思維外，重新思索運用新元素並重組原有表演原素的可能。科技的運用限制了原本跨界音樂舞臺的表演型態，但卻開拓了雲端的虛擬舞臺世界。因此，對2021年的跨界音樂來說，是疫情肆虐下的停滯期，同時也是一個解構與重組的過程。</p>
<p><strong>三、表演藝術 v.s. 媒體藝術與電影</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自疫情肆虐的2020年開始，表演藝術開始嘗試進行線上直播與影像錄製的考量，而回溯我國表演藝術製作成影片形式，以電視或電影撥放的範例，從我國紅極一時的黃梅調電影、歌仔戲電視劇，<sup>14</sup>到2012年造成全球轟動、票房長紅的《悲慘世界》<sup>15</sup>電影，都是相當知名的且傳播力驚人的作品。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回歸表演藝術之本質在於即時性與非複製性，並且重視與現場觀眾的互動、情感交流，雖然常錄製成影音檔案用以保存，卻並非為了表演給攝影機直播，更不是為了錄製影像而表演。因此，表演藝術與傳播影視或電影的出發點與本質本就存在很大的差異，但在近年疫情的影響下，表演藝術為了生存與永續經營，開始嘗試了運用科技的直播與影像的錄製，使得現場表演逐漸趨向於數位化傳播的現象。關於表演與傳播之議題，果陀劇場藝術總監梁志民認為：「<span class="bkt">藝術一定要透過某種媒介跟觀眾產生關係，表演藝術的媒介是人，電視、電影則是鏡頭跟機器，未來可能是更快速的網路，作為藝術創作者，未來對科技要更敏銳一點。</span>」（數位時代報導，2021）道出兩者的本質與差異性，也提出對於現場的表演轉變為影像的擔憂。回歸劇場表演者的養成，表演者與電視、電影演員不同，本來就不是訓練來為了面對鏡頭，表演場域中與觀眾的互動都是情感與藝術的交流，如同相聲演員沒有觀眾的笑聲與反應，單純面對冷冰冰的鏡頭，儘管演出順暢、演員技巧高明，但明顯缺乏觀眾熱情的感受；此外，以網路科技透過直播讓觀眾即時參與，但透過受限制的影像與改變後的音質，即使能夠無時差的即時參與，卻也難以還原表演現場的情緒感染力，如同不可無料劇場行政總監鍾欣怡認為：「<span class="bkt">戲劇、舞蹈、音樂都是表演藝術，呈現形式、藝術方式、和節目內容，都會影響到它適不適用線上轉播。是不是在線上轉播的形式之後，還能保留它的藝術性或娛樂性，而不會只是遷就大眾性。</span>」（黃詠潔、黃莉庭，2020）因此，不論傳播媒介為何，表演藝術應回歸以「人」為本的本質，而非流失於科技的浪潮之中。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;關於表演藝術科技化與數位化的發展，國立臺北藝術大學音樂學院院長亦是知名小提琴家的蘇顯達認為，表演藝術順應數位發展成為必然的趨勢，但「<span class="bkt">因應疫情下的數位應用，終究是一種不得已的彌補或暫代</span>」（財團法人臺灣經濟研究院，2021，頁22），形容數位發展像是一把雙面刃，在追求最新科技與速度之餘很容易迷失自我，因此「<span class="bkt">面對各式各樣的發展都要回歸音樂的本質，便不會撼動藝術本身的價值</span>」（頁24），表演藝術透過表演為媒介展現情感與藝術性，如同藝術評論者白斐嵐（2021）認為：「<span class="bkt">與其在媒介與科技上焦慮，試圖藉由不停地產出，以證明自身存在與價值，真正的關鍵還得回歸作品本身，回歸與觀眾的連結。</span>」，也因為在疫情環境下，表演場域的強制限制，迫使藝術家們尋求新的傳播媒介，形成表演藝術趨向傳播藝術靠攏的現況。因此，無論傳播媒介為何，表演藝術應回歸表演的本質，確保表演之獨特性，在疫情的現況下藝術家們選擇折衷而讓表演成為影像，這也許是表演藝術創作端、表演端以及觀眾端們極力維持生態平衡的方式，而疫情過後呢？是否也養大了觀眾不願進入劇場與表演場域的藉口？藉由疫情的因素，藝術家與創作者們游移（猶疑）在實體與影像間，亦是對表演藝術「本質」能夠有再次探索的機會。</p>
<p><strong>四、跨界音樂團隊型態的逐漸形塑與去代工化</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀我國近年來之跨界音樂發展，國內如國家交響樂團、臺灣國樂團、臺北市立交響樂團、臺北市立國樂團、朱宗慶打擊樂團等公私立大型樂團，往往擁有較多資源，在一般的節目規畫中增加跨界音樂作品創作的嘗試，而這些作品亦包裝在國家級藝術節的策畫與行銷下，有著極高的知名度。然而，上述團體中不論其跨界作品的多寡、成功與否，跨界作品僅占其所有節目製作的一部分，往往亦是藝術節或專題中特別企劃的成果，但卻對於我國整體音樂生態的發展起到「領頭羊」的角色。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跨界音樂相較於傳統演奏的作品，無論是在創作端的創作、規劃與節目設計，或是表演端演奏者、表演者以及演出場域素材的搭配與整合，除了需要創作的巧思與縝密的策畫外，演出場域亦往往跳脫傳統的音樂廳，在更為多元的劇場與環境下演出，如與他領域的藝術團隊、藝術家合作更是增加演出的複雜程度，這些都是超出固有樂團純以音樂演奏為主要的範疇。因此，上述如國家交響樂團等我國知名樂團，雖擁有較多資源可策畫跨界節目，但因各樂團本有其發展淵源、形成固有之樂種型態外，其他非關藝術的思維如國家政策、社會責任等等因素，使得大型公私立樂團仍以原有的樂種為主軸，部分心力方進行跨界的嘗試。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於上述傳統樂團的限制與多因素的考量外，許多新創團隊則更可較無顧忌地致力於跨界音樂的追求。關於跨界音樂團隊的崛起與發展，筆者早於2018年對跨界音樂新創團隊進行觀察，發現這些新興的音樂團隊難以現今的音樂範疇來歸類，也因為樂隊規模和資源的限制，多以獨立音樂（Independent Music）的形式運作，從創作、錄音到出版也多由團內團員所自行操刀，因此這些「<span class="bkt">樂團也如同流行樂界獨立音樂樂團的特質般：不喜歡被歸類的「作自己」，保持獨立而原創的核心價值；也因為其不拘一格的特質，更能開啟更多方的嘗試，觸發更多跨界的可能。</span>」（陳慧珊，2016）在當代普遍推廣「斜槓」的思維下，不論是大環境或個人自我的要求裡，對於音樂家或表演者的要求與期待已有所改變；在極端競爭的社會中，藝術家們的除了要擔當趁職的創作者、表演者外，也應成為優秀的推廣、策畫與行銷者，以及首屈一指的管理者、企業家。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在跨界音樂的創作層面，團隊作品多為該團成員依自我團隊之特質所創作，富有原創性質與自我樂團的獨特性，此與傳統大型樂團的特質有很大的不同。由跨界音樂團隊的外部觀察，可發現跨界音樂團隊擁有：藝術作品的原創性、現有範疇的突破性、表演形式的獨特性、跨域美學的激盪與探索等數項特質，其中更展現出「去代工化」之色彩－團隊產出皆由成員自行創作，並有很高的原創性、代表性與非複製性。這些跨界音樂團隊由於規模較為精緻，並跳脫現有的音樂範疇，往往採用特殊的編制，加上不願拾人牙慧、超凡脫俗的性格，使得他們極少直接演出他人的作品，由某種層面來說寧願自我生產而不進行「代工」，而此「去代工化」的精神不僅僅在作品產出上，更實踐於跨域美學的追求－藉由不斷創作、表演與嘗試中，逐漸形塑跨界音樂團隊的獨特性與核心價值。</p>
<h3>結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在全球化與科技化時代下，遭受疫情重創的音樂藝術發展已「回不去了」。音樂不再侷限於創作本身，其觸角將隨著人類生活型態的轉變，延伸到各個角落與面向，包括跨界、跨域、跨國、跨文化等，後疫情時代，表演藝術活動遭受前所未有的衝擊，無論在形式或內容上，在在考驗著藝術家、策展人、以及藝術教育者的專業。包括音樂工作者在內，藝術工作者面臨的，不再只是如何吸引觀眾或如何發展藝術風格的問題而已，更嚴峻的是，必須面臨永續經營、甚至不得不轉行或停止藝術工作的選擇。2021年的音樂活動，每一場呈現出來的成果，不僅僅只是經年累月的努力成果，更有著一種「靠天吃飯」的感恩；畢竟一旦疫情加重，演出便嘎然而止，所有的努力與排練都將化為烏有。在此心情下，2021年的跨界相關演出，每一場皆有一種得來不易的感覺，表演者抱持著兢兢業業的心情，而觀眾們也是以一種「惜福」的態度來欣賞現場的演出。整體而言，跨界音樂的發展趨向保守，藝術家們被迫離開熟悉的表演場域，轉而挑戰數位化與傳播媒介的陌生領域，除了需額外學習新技能外，在無可奈何中還必須解構、重組原本的作品，以更符合現有的傳播方式，在現場表演與數位影像間游移（猶疑）。另一層面來說，在此不可抗拒的自然因素挑戰下，也使創作者們對音樂本質有再次思考的機會。音樂演出被迫轉向科技領域發展，可能是暫時的折衷方式，但也或許是開啟音樂藝術與科技跨域共製的重要契機。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情雖然對我國表演藝術造成重創，但危機也是轉機，跨界音樂本就具備多元的「有機」潛能，較無傳統的束縛與限制，而能以開拓的實驗精神面對時代的考驗。觀察2021年度的跨界音樂演出發現，跨界音樂團隊型態已逐漸形塑，這些團隊重視作品的原創性、現有範疇的突破性、表演形式的獨特性以及對跨域美學的激盪與追求，並呈現出「去代工化」的特質；相較於老牌或大型樂團，這些小而精緻的團隊形象鮮明，更能跳脫傳統的包袱與限制，創造自我團隊的價值。此外，即便後疫情時代下的跨界音樂呈現出更多的不確定性，對於未來的何去何從有所迷惘，但也正因為這樣猶疑卻勇於嘗試的特質，讓當前的跨界音樂發展，在歷經解構、調和與重建的艱鉅過程後，逐漸邁入一個蛻變與重生的新境界。</p>
