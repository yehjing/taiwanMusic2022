<p><strong>九、10月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10月份在劇場的變化上觀察到一場名為《紀實音樂劇場－歸來去》，該演出由新古典室內樂團製作，以文學家、詩人曾貴海的16首詩為創作藍本，以聲音、影像及文學的韻味，在現實與抽象的空間交錯中，建構出臺灣詩歌與當代劇場的憧憬。作品中隱約感受到在地的人為特質，與細膩的人文觸感，透過詩人的眼光以音樂傳達出對客家文化的使命和土地認同的懷抱，是一場結合音樂、多媒體與文學的跨界音樂演出。新古典室內樂團近期有不少跨界的嘗試與策劃，在其藝術總監陳欣宜的帶領下，每每有令人印象深刻的嘗試，創作出一齣齣交融音樂與他者對話的跨界作品。此外，跨界音樂也受到學界和教育界的重視，2021年10月2日在國立故宮博物院南部院區的集賢廳上演的《音樂劇場：爪哇伯公》，是由國立臺南藝術大學的昇華之音樂團與日央人兒世界樂團，和達朗跨文化偶戲班共同演出，該作品從印尼爪哇甘美朗的聲響出發，透過和世界音樂及傳統偶戲皮影的交流與碰撞，衝撞出一場視覺與聽覺跨域融合的多重藝術饗宴。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體而言，跨界音樂的現象在全國各地皆有之，中部的藝術家亦不缺席，例如2021年10月2日在臺中市大墩文化中心城市小舞臺上演的2021臺中兒童藝術節－台中室內合唱團《音樂精靈的芭蕾奇緣》便是一場結合戲劇、音樂及互動概念的親子共樂音樂劇場，此類作品的著力點往往以戲劇和互動為主軸，在歌聲中傳達寓教於樂與美感教育的信念。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;戲曲與國樂的關聯總是息息相關的，近年來國樂的跨界創作亦有透過戲曲的曲牌、節奏甚至劇情來加以發揮。2021年10月3日假國家音樂廳臺北市立國樂團與薪傳歌仔戲劇團在指揮瞿春泉的帶領之下，由作曲家柯銘峰所譜寫的七字與都馬調融合平埔族音樂詠唱出清朝番漢相戀故事，又如在戲曲作品中江湖正邪鬥陣、異族戀情等都是過去在國樂中非國樂傳統曲目。這一類的新素材成為國樂曲目跨界發展的另外一種新面向。此外，包括傳統戲曲團隊亦有跨界的發展趨勢，例如心心南管樂坊在2021年10月23日假雲門舞集所演出的「王心心吟唱－長恨歌」便是由雲門舞集創始人林懷民與南管藝術家王心心互動合作後，從人性的解讀以南管獨有的特色，吟唱詮釋出《長恨歌》中帝王與愛妃之間的情仇愛恨，在人生的唸、唱、吟、詠與琵琶的彈、撥、挑、勾之間，重現歷史上的舊事情懷。配合時序與節慶的演出，往往也能吸引觀眾的目光。例如狂美交響管樂團在10月16日於中壢藝術館的音樂廳配合西洋萬聖節的到來，推出了嶄新的親子音樂劇場《萬聖夜驚魂》在萬聖節的前夕上演，該樂團與如果兒童劇團的演員們及知名的「水果奶奶－趙自強」合作，透過通俗動聽的音樂、詼諧幽默的戲劇演出，以及老少咸宜的對話，讓現場觀眾隨著劇情進入了歐洲文化中的鬼節氛圍與大小朋友一起探討生命的議題。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;配合時事與社會趨勢也是當代跨界音樂創作的一門顯學，而科技的話題始終縈繞在你我的生活中，2021年10月24日臺北市立國樂團與知名作曲家亦是該團的前團長鍾耀光合作，演出了一場「AI創世紀－鍾耀光&amp;TCO」，該演出的最大特色是每一首作品的創作皆透過AI人工智慧的選擇與重組，無論來自民謠、傳統歌曲、部落音樂或經典名著，皆由作曲家和AI人工智慧的重新編排來製作。換言之，除了人腦的思考辯證之外，電腦的運算和選擇也成為當代跨界作品的一個不可多得的便捷工具。除了人腦與電腦的激盪之外，該作品的另一大特色是邀請了國內知名的獨奏家，如鄭安良打擊樂家、田佳玉揚琴演奏家及陳慧君二胡演奏家共同協奏，凸顯了國樂界強大而精湛的演奏實力。</p>
