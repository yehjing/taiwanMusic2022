    <h3>前言</h3>

    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術源自生活，是文化的結晶，社會的縮影。在人為與自然因素的雙重影響下，當代社會急遽變遷且奇異，若從負面的角度看，現代生活其實處處充斥著衝突、矛盾、不安與危機。從核武戰爭、恐怖主義、種族歧視、金融海嘯、通貨膨漲、貧富懸殊、難民流動等社會問題，到氣候變遷、地球暖化、輻射破壞、噪音光害、水源與空氣汙染等環保議題，現代人有太多複雜而不可預測的壓力與挑戰。筆者長期研究臺灣跨界音樂的發展，認為若從歷時性的角度觀之，全面顛覆了民生常態、社會秩序、經濟運行、甚至國家治理的「嚴重特殊傳染性肺炎」<sup>1</sup>（Coronavirus disease, COVID-19），可謂影響我國近年跨界音樂發展甚鉅的一個關鍵性分水嶺；觀察臺灣2021年的音樂生態與跨界音樂活動後發現，歷經反反覆覆疫情所帶來的恐慌與停滯，跨界音樂在走走停停之間已元氣大傷，如今正面臨著後疫情時代下解構的危機、調和的過程、與重建的挑戰。</p>
<h3><strong>壹、疫前的跨藝沃土</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;處於全球化與科技化的地球村時代下，臺灣當代社會蘊藏有多元化、資訊化、少子化、高齡化與休閒化等各種傾向，此恰為音樂藝術犁出一方沃土，並於其上耕耘出一片生氣蓬勃的域外發展氛圍。而此走出框架、以開放包容的胸襟與他者合作的跨界傾向，在進入21世紀後益發明顯。學界也觀察到跨界的特徵與意涵，例如，學者洪世謙在黃涵榆所著的《跨界思考》（2017）一書中的推薦序〈「例外」的戰鬥姿態〉一文中，將跨界視為是有別於常規以外、當事者以外的「例外」，乃一種有力的「戰鬥」姿態：</p>
<p class="bui">這種姿態非但不是無力或邊緣，不是被排除的受迫者，而是一種戰鬥。這種置身例外的戰鬥，既是書寫，意識哲學行動，戰鬥意味著作者以其自身及文本，作為一種充滿差異又具有張力的空間，劃出與既定事務的距離與界線，在此間性之中，差異對話在此展開，並挪移了各種界線。<span>（頁9）</span></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此種視跨界為一種積極的行動，用於自身與他者之間的擴張和對話關係，也瀰漫在當代音樂的發展上。原本主要以西方音樂傳統為基礎、音樂現代化為「範本」的音樂發展，開始在形式和內容上都有明顯的突破；而此種變化，吸引了包括筆者在內的許多作者、學者、評論者等的關注。<sup>2</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以筆者為例，早於2007年時，筆者便針對彼時始萌芽的臺灣跨界現象，發表了〈1+1≧2？淺談跨界（領域）藝術創作之理念與實踐〉<sup>3</sup>一文，對於當時尚屬於發展初階、多半以本我為中心的諸多跨界創演嘗試，做出討論與提問。譬如，雖然「跨界」似乎如顯學般地活躍在本地的音樂發展中，甚至有被無限上綱的傾向（合作對象琳瑯滿目，從音樂之間、藝術之間、到藝術以外皆有之），但若深思所謂的「傳統」與「現代」、「全球化」和「本土化」，實為相輔相成、一體兩面的說詞，那麼當代音樂的跨界合作重點，便不應只侷限在於「一加一是否等於或大於二」這樣的籠統思維，而是雙方（或多方）互動期間，所發酵出的「異中求同」或「同中求異」的交流態度。初期的跨界音樂因經驗不足或認知不深等種種因素，多以型態上或素材上的「嘗鮮」為主，跨界合作的雙方對彼此的了解往往尚未到位，合作動機也多以自身的立場出發，所以磨合程度較劇烈。無論如何，音樂界這股朝域外發展的轉變風氣是相當明顯的，也引起國際學界的注意，譬如，筆者當時便受知名國際學術期刊《Fontes Artis Musicae》邀約，發表了〈Changing Tendency of Western Music Tradition in Taiwan〉<sup>4</sup>（2009）一文，揭露根植於臺灣音樂文化中的西方音樂傳統之近期在地變遷，反映了本地的音樂逐漸朝跨界、跨域、以及跨文化方向發展的趨勢。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;多元族群的共存共榮，是臺灣社會的特徵，這也造就了臺灣音樂與生俱來在語言、曲調、節奏、風格上的跨文化特質，而這種特質一方面可以是很大眾、很普世性，另一方面也可以是很小眾、很個人化的。筆者曾在〈跨文化美學的音樂詮釋－以臺灣當代作曲家之藝術觀為例〉（陳慧珊，2007，頁211-226）一文中，記錄了臺灣當代不同類型的音樂創作者（包括潘皇龍、鍾耀光、劉學軒、黃正銘、謝韻雅等）在創作理念上的強大包容力特質，及其作品所展現的既多元又專屬的跨文化美學。而恰恰是這樣的多元文化特質與共存共榮社會氛圍，滋養了跨界音樂的生長基因。雖然觀望的成分很高，但整體而言，進入21世紀後，對於向外拓展的新興嘗試，樂界普遍感到好奇、甚至躍躍欲試。尤其在幾個指標性樂團和代表性作曲家的影響下，跨界風的吹起造就了許多令人耳目一新的創演，給音樂界帶來欣欣向榮的景象。筆者的〈臺灣公立樂團跨界展演初探－以臺北市立國樂團、國家交響樂團為例〉（陳慧珊，2011，頁135-166）與〈臺灣私立樂團跨界展演初探－以采風樂坊、朱宗慶打擊樂團為例〉（陳慧珊，2013，頁9-50）兩篇文章，便是針對彼時4個重點樂團演出的跨界節目所進行的考察。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跨界音樂的蓬勃發展自然引來廣泛的論述，其中不乏分岐的觀點－從能否被納入某單一樂種的範疇為標準、樂種之間的融合程度、音樂體現的跨文化特質、到音樂的實驗性素材或手法等－各類立場皆有之，但基本上皆能立基於音樂的「形」與「質」。其中較例外的是，亦有單純以消費市場「分類」的角度，將「跨界音樂」歸類在「古典音樂」和「流行音樂」之外的第三種類型的論述<sup>5</sup>。對於將跨界音樂「標籤化」以方便識別的類似主張，筆者認為恐怕是屬於較表面的他律性判斷，恐無法真實反映臺灣跨界音樂的發展實貌，因此特以〈反思「跨界音樂」－從音樂多元本體觀論當代音樂之跨界〉（陳慧珊，2014，頁23-52）一文，提出從音樂的多元本質、多元面向的其他角度，來窺探跨界音樂的發展脈絡，思考其與他者之間的互文性、因果關係與文化意義。雖然「跨界音樂」的定義至今仍未取得普世性的共識，但從幾年下來不斷有推陳出新的跨界音樂製作的事實來看，跨界音樂的概念的確已漸被接受，跨界音樂的演出型態的確漸成常態，且不僅僅與自家音樂人跨界合作而已，與其他領域的藝術家、甚至非藝術領域的合作，也益發常見。筆者於2016年出版的《傾聽弦外之音：音樂藝術跨界展演研究》一書中，便根據跨界參與者（含媒材）彼此之間的合作過程、涉入程度或互動關係，提出包括主客體置位易辨的「cross」、多重主體並置的「inter」、自體變形並與他者交融的「multi」、拼貼混雜的「mix」、文化複合性的「hybrid」、以及越域新創的「trans」等6種類型，並以此觀察了許多涉及劇場、戲曲、舞蹈、多媒等他領域的跨界音樂運用和製作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情爆發的前兩年（2018-2019），臺灣跨界音樂的演出數量和密集度來到相對高點。根據筆者在〈時空交織的絢爛迸發：2018年度跨界音樂活動觀察與評介〉（2019）一文的觀察，無論是在樂風上的融合、樂種上的混編、使用工具上的科技化、或與非藝術領域（所謂「外跨」）的合作等，在2018年度的音樂活動中都已相當常見。雖然在定義上仍有分歧、甚至模糊的空間，但跨界音樂卻已呈現出音樂會劇場化、傳統曲風現代化、科技多媒體常態化、及親民在地化等之發展特徵。<sup>6</sup>隨著觀演關係中微妙的主客角色變化，觀眾的需求與期待，成為音樂實踐者更加重視的考量，音樂活動的實用性與功能性漸成為選擇跨界對象、媒材與合作方式的重要因素。在這股轉變的氛圍－從保守觀望到樂觀其成－驅動下，跨界音樂的發展散落在各樂種之間所進行的內部融合、與非藝術領域的外向交流、各文化之間的趨同逐異、以及科技傳播媒體的運用等。一時之間，各類的跨界嘗試或音樂型態如雨後春筍般地浮出，根據筆者在〈以藝術之名的眾聲喧嘩：2019年臺灣跨界音樂的現象與發展〉（2020）一文中的分析，跨界音樂發展到2019年底疫情爆發前，至少包含七大值得關注的面向：（一）跨界思維普及化；（二）親民路線的推廣；（三）勇於實驗的精神；（四）多方雜揉的重新定義；（五）科技的運用與效應；（六）藝術節的推波助瀾；（七）重視兒童與親子節目。<sup>7</sup>在產、官、學等各界的全面支持下，更自由、開放的創新合作態度，洋溢在一片看似欣欣向榮的臺灣音樂生態中，跨界風帶起的發展衝勁與願景，令各界好不憧憬。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;值得一提的是，隨著跨界音樂接受度的提高，創演需求增多，跨界人才缺乏的問題終浮出檯面。長期以來，表演藝術（音樂、舞蹈、戲劇、戲曲、民俗技藝等）各領域在人才培育上，泰半是「井水不犯河水」地各自發展，音樂界內部各不同樂種的音樂專才栽培也多是「各自為政」，彼此之間的交流有限。單領域的培養人才，優點是能在專業的深度上獲得較有精進的成效，但缺點則是在橫向的廣度上，或是在與他領域合作能力的栽培上，較難有可塑性與團隊績效。無論從經濟上的務實角度，或從藝術、學術上的專業角度，跨界人才的培育都是當前與近期的未來，攸關我國音樂健全發展的關鍵因素。<sup>8</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過跨域共製或融匯不同樂種互動創演而成的跨界音樂，除了呼應本世紀以來，結合「全球化」（globalization）概念與「在地化」（localization）觀點所形成的「全球在地化」（glocalization）思潮後，產出既能與世界對話又能反映本土人文風貌的作品之外，在先進科技的推波助瀾下，也開創出與科技之間的跨域共融新氣象。疫情爆發之前，跨界音樂的創演發展不但有逐漸劇場化、現代化、科技化和親民化等的傾向，在跨界的型態與手法上也日趨精進成熟；而著重多元素材的結合，跨領域合作與跨文化交流的跨界音樂，更因具有民主化與社會化的特色，頗能貼切地呼應網路串聯時代的異業結盟特徵與多元文創精神。整體而言，疫情前的音樂生態，是一個跨界音樂大有可為的環境。<sup>9</sup></p>
<h3><strong>貳、疫後的藝情轉向</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2019年歲末延燒至今的嚴重特殊傳染性肺炎其病毒不斷變種，包括Alpha，Delta，Omicron等，截至2022年的1月29日，已導致全球累計近3.7億例確診個案，逾563萬人死亡，<sup>10</sup>無疑是人類史上規模最大、影響最劇烈的全球性流行傳染病之一。嚴重的疫情造成世界性的經濟與社會混亂，全球經濟陷入自1930年代「經濟大蕭條」（Great Depression）以來最大的經濟衰退，全面性地重創社會上各行各業的發展與生計，從金融、航空、交通、觀光、旅遊、餐飲，到教育、體育、藝術、文化、休閒、娛樂等，無一倖免，輕者減薪裁員，重者熄燈歇業。其中，執行過程必須依賴人與人之間的現場排練、表演及觀賞的表演藝術，可謂此次疫情的「重災區」。眾所皆知，舞臺上光鮮亮麗、技藝超群的表演藝術家，實為消磨多少青春歲月後勤勉苦練的成果，正所謂「臺上一分鐘、臺下十年功」，而今卻因無情的瘟疫，現場演出被迫喊停，合作計畫戛然而止。失去了舞臺的表演藝術家，其創作成果與演出呈現，面臨無處發表、乏人問津、甚至付諸流水的困境。雖然臺灣有目共睹的防疫成就，讓本地表演藝術團隊的實體演出，仍能在疫情緩和時配合相關措施有條件的滾動式舉辦，但隨著現場演出場次、大小藝術節、在地或國際交流合作計畫等，一波又一波的延期，一檔又一檔的取消，整個表演藝術鏈結中參與的工作者們，在在受到嚴峻疫情的全面性衝擊－即使公部門的藝術和文化等相關單位試圖履行其責任義務，盡可能地支持或補助本地藝術家，推出了諸多利多藝術家的政策。<sup>11</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，正所謂「山不轉，路轉；路不轉，人轉」，在病毒不斷變種、疫情終結日遙遙無期的情形下，「隔離」、「封城」、「社交距離」等詞彙已成為生活中的常態，音樂展演的方式也被迫轉型。有的向戶外發展，有的縮小編制，但為了降低人與人的接觸以防傳染病擴大蔓延的疑慮，與數位科技結合、改採線上直播的方式，是最多人的選擇－而這些種種演出方式的改變，在疫情最失控的2020年尤其明顯。<sup>12</sup>筆者曾在〈極度衝擊下的逆勢崛起：2020年臺灣跨界音樂文化與生態評析〉（2021）提出，在越是極端的世界（例如嚴重特殊傳染性肺炎這隻「黑天鵝」）裡，要堅持「經典」越是不易，要傳承「傳統」越是困難。雖然沒人可以真正高枕無憂，但「百年老店」或大型樂團永續經營的壓力遠勝過新興組織或個體戶。跨界團隊多採小編制（甚至獨奏）或創新組合，成員普遍年輕化，無論在科技的運用彈性或網路世界中的串聯能力上，常比傳統或大型團隊更來得靈活，反而較能在混沌不清、充滿變數的極端世界中逆勢竄起。此外，當原本擬由外國音樂家來臺擔綱演出的節目，因疫情而大幅銳減甚至取消後，間接拓寬了本地作曲家的創作機會與演奏家的演出空間，意外活絡了跨界音樂的在地化發展。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情後的藝情，首當其衝的是針對固有的解構，接著與新興元素或媒材的調和，再來是重建適合新氛圍的音樂類型。其中最明顯的便是科技的運用與雲端世界的透入。只是，當各界紛紛把演出「線上展演」、「線上直播」、「表演數位化」等方式視為疫情下維持表演藝術生命的「救命仙丹」時，回歸到表演藝術是在觀者面前展演的一門藝術來看，現場演出所產生的「臨場感」、「即時共感」都是線上播放和數位化所無法取代的。</p>
<h3><strong>參、逐漸回溫的現場演出</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在各界的努力下，疫情在2021年有趨緩的現象，曾一度冷卻幾近「冰點」停滯的表演藝術活動，陸續有「回溫」的情勢。整體而言，2021年的跨界音樂節目，在類型上，並未打破過去幾年來跨界音樂的發展類型，在運用的素材與合作的夥伴方面，亦未有再令人驚艷或創新的題材，或許可以說在疫情的影響下，無論從創作或演出在在都受到了相當大的限制，而在經費縮編甚至刪減的情況下，要從事跨領域的多元合作，想必也是相當不易、處處碰壁的。回顧2021年，嚴重特殊傳染性肺炎疫情趨緩，音樂的現場演出有逐漸回溫的現象。相較2020年，我們觀察到表演活動與合作有漸漸回溫的情勢，雖然數量不多，總類也不若過去那樣的繁多，還是有持續在進行緩步地實踐。</p>
<h3><strong>肆、2021跨界音樂演出</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;主題性的音樂會，或有所定位的演出題材，是近年跨界音樂的製作導向。鎖定題目或類型的作法，一方面固然是對外行銷上的考量，另一方面對內而言，也是合作夥伴之間在工作時，彼此溝通的依據。以下根據歷時性順序，整理出2021年度一整年主要的跨界音樂演出。<sup>13</sup></p>
<p><strong>一、1月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1月份的演出有若干場跨界的內容。包括采風樂坊的「花雨．花語」、2020嘉義市國際管樂節的《他拉喇叭團：蕭士塔高維奇與我－低音號謀殺案》、拉縴人男聲合唱團的「筆下聲韻」、由桃園市國樂團、楊斯琪爵士五重奏及鍾耀光等藝術家合作的「跨界．跨年」2021新年音樂會、狂美交響管樂團與如果兒童劇團合作的狂美親子音樂劇場《聽見．宮崎駿》、以及朱宗慶打擊樂團35週年的團慶演出《泥巴》擊樂劇場等。</p>
<p><strong>（一）</strong></p>
<span>節目名稱：《他拉喇叭團：蕭士塔高維奇與我－低音號謀殺案》</span><br>
<span>跨界元素：喜劇、器樂劇場、銅管樂</span><br>
<span>演出單位：他拉喇叭團</span><br>
