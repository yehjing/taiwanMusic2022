<p><strong>八、9月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;許多經典的重新搬演也是2021年跨界音樂的另一種現象。例如9月份上演的「東方傳奇．搖滾國樂」便是采風樂坊所帶來的舊作新演的演出，《東方傳奇》是以流行文化為訴求，運用搖滾樂的曲風，加上傳統樂器的特有韻味，體現出中西合璧傳統與現代交融的迷人特色，該作品雖不是新創演出，卻能去蕪存菁的保有其原汁原味的特色，對於當代的觀眾而言，這種活潑的表演方式，更是一種別具特色的體驗。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如前所述，「音樂劇場」的確是一種最能體現音樂與它領域元素交流、互動，展現包容力的最佳演出形式之一，觀察2021年的諸多跨界音樂演出類型，可以發現劇場型態的運用是相對普遍的。例如9月份的朱宗慶打擊樂團二團在高雄市政府文化局至德堂所上演的《Bean Bon果實敲敲樂》就是一種結合兒童劇場與音樂會的親子跨界音樂演出，透過活潑生動與趣味的演奏方式，將戲劇性與寓教於樂的題材帶入由打擊樂呈現的視、聽覺雙重享受。融合劇場的演出，在國樂的當代跨界創演上，已有越來越活絡的現象。例如9月5日在臺灣戲曲中心大表演廳所上演的《蓬瀛詠弄人間戲》便是戲曲與國樂結合的戲曲國樂劇場，該演出由臺灣國樂團與瞿春泉合作的《蓬瀛詠弄人間戲》，該作品是一場以國樂為本融合當代劇場創新手法的跨文化作品，此作品呈現中研院院士曾永義的經典名作，以國樂和戲曲融合的精隨，唱出音樂與文學之美，可謂當代國樂的另一種跨界嘗試。</p>
