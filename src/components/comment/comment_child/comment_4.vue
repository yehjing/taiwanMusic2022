<span>節目名稱：「筆下聲韻」</span><br>
<span>跨界元素：文學、新詩、文本、男聲合唱</span><br>
<span>演出單位：拉縴人男聲合唱團、濤韻男聲合唱團（臺中場）、微行星男聲合唱團（高雄場）</span><br>
