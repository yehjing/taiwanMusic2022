<span>演出地點：臺中市大墩文化中心/城市小舞臺</span><br><br>
<p>演出內容：顛覆合唱團只唱歌的刻板印象，透過戲劇和舞蹈元素，以親子劇的方式，唱演出故事與小朋友、爸爸媽媽同樂。</p>
<p><strong>（四）</strong></p>
<span>節目名稱：《點染戲畫－瞿春泉與柯銘峰的歌仔話境》</span><br>
<span>跨界元素：國樂、戲曲、文本、歌仔戲音樂、原住民音樂</span><br>
<span>演出單位：瞿春泉、臺北市立國樂團、薪傳歌仔戲劇團</span><br>
<span>演出時間：2021/10/3</span><br>
<span>演出地點：國家兩廳院音樂廳</span><br><br>
<p>演出內容：把歌仔戲音樂（七字調與都馬調等）與原住民（平埔族等）的曲調融合，以跨文化的新創風格，透過國樂團與歌仔戲團的合作，演繹出清朝「唐山過臺灣」的番漢相戀的傳奇故事。</p>
<p><strong>（五）</strong></p>
<span>節目名稱：狂美親子音樂劇場《萬聖夜驚魂》</span><br>
<span>跨界元素：戲劇、親子劇場、音樂劇場、交響管樂、教育</span><br>
<span>演出單位：狂美交響管樂團、如果兒童劇團</span><br>
<span>演出時間：2021/10/16</span><br>
<span>演出地點：中壢藝術館/音樂廳</span><br><br>
<p>演出內容：《萬聖夜驚魂》是狂美交響管樂團（桃園市傑出演藝團隊）與如果兒童劇團（Taiwan Top演藝團隊）共同合作的親子音樂劇場，配合萬聖節的到來，以萬聖節相關主題作為故事題材，現場演奏出動聽的交響管樂搭配詼諧幽默的兒童戲劇演出，傳達關懷生命的態度。</p>
<p><strong>（六）</strong></p>
<span>節目名稱：「王心心吟唱－長恨歌」</span><br>
<span>跨界元素：文學、南管</span><br>
<span>演出單位：心心南管樂坊</span><br>
<span>演出時間：2021/10/23</span><br>
<span>演出地點：雲門劇場</span><br><br>
<p>演出內容：白居易的《長恨歌》，在雲門舞集創辦人林懷民的藝術指導，作曲家賴德和教授的音樂指導下，「心心南管樂坊」的南管藝術家王心心加以編曲並獨角演出。從唸、吟、唱，到琵琶的彈、撥、挑，王心心用獨樹一幟表演美學，詮釋出古代時詞文學與傳統南管藝術融合之美。</p>
<p><strong>（七）</strong></p>
<span>節目名稱：「AI創世紀－鍾耀光＆TCO」</span><br>
<span>跨界元素：電子音樂、科技、現代音樂、世界音樂等</span><br>
<span>演出單位：鍾耀光、臺北市立國樂團</span><br>
<span>演出時間：2021/10/24</span><br>
<span>演出地點：臺北市中山堂</span><br><br>
<p>演出內容：鍾耀光是國內著名的跨界音樂作曲家，無論風格、類型、乃至於樂種，鍾耀光從不自我設限，是當代跨界音樂發展的重要推手之一。他的《AI創世紀》是一首高實驗性的作品，所包含的8個樂章的素材來自世界各地的民謠、名曲、或是作曲家本人的舊作，但透過AI人工智慧去編排出各種可能性，再由作曲家加以選擇並進行組合。跨科技的音樂創作方式，早已鋪天蓋地的席捲流行音樂、配樂或功能性音樂，而今隨著跨界音樂開放的態度，也一步步地走進藝術音樂的天地。</p>
<p><strong>十、11月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在合唱音樂方面亦有跨界創演的產出，例如采苑室內樂團於2021年11月11日在中山堂所上演的《創藝－引領》藉由說書、戲劇展演、樂曲演唱等，為內容加上多媒體的科技、光點和情境式的燈光設計，該節目以合唱戲劇的視角詮釋、回顧了以「合唱戲劇」為主軸的跨界展演。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;勇於嘗試跨界製作的新古典室內樂團，在2021年11月25日於衛武營推出了一場以吳三連文學獎得主江自得的詩集為主題發想，配合旅荷作曲家方斯由和知名建築師趙建銘，製作出結合文學、互動科技、樂團演奏、舞者演員及電子聲響等等的《聲景音樂劇場－給NK的十行詩》，該作以豐富的視覺、聽覺和動覺的融合，展現出當代跨域表演藝術的交互對應，企圖嘗試以聲景為新美學的一種音樂劇場。</p>
<p><strong>（一）</strong></p>
<span>節目名稱：《創藝－引領》采苑合唱戲劇回顧與展望</span><br>
<span>跨界元素：合唱、戲劇、多媒體、說書、合唱劇場</span><br>
<span>演出單位：采苑室內樂團</span><br>
<span>演出時間：2021/11/11</span><br>
<span>演出地點：臺北市中山堂中正廳</span><br><br>
<p>演出內容：以「合唱戲劇」的概念，在多媒體科技、情境式燈光所營造的舞臺效果中，藉以說書、戲劇展演、合唱的形態，呈現出有別於傳統合唱藝術的表演。</p>
<p><strong>（二）</strong></p>
<span>節目名稱：《聲景音樂劇場－給NK的十行詩》</span><br>
<span>跨界元素：多媒體、劇場、詩詞文學、電子聲響、舞蹈、科技</span><br>
<span>演出單位：新古典室內樂團</span><br>
<span>演出時間：2021/11/25-11/26</span><br>
<span>演出地點：衛武營國家藝術文化中心歌劇院</span><br><br>
<p>演出內容：新古典室內樂團近年在跨界製作方面的耕耘有目共睹。以吳三連文學奬得主江自得探討「美」為主題的詩集創意發想，請來旅居荷蘭的新秀作曲家方斯由，另邀知名建築師趙建銘擔任舞臺設計，《聲景音樂劇場－給NK的十行詩》是一個從樂團演奏出發，結合互動科技、舞者演員、並加入電子聲響和臺灣聲景素材，創造出視覺與聽覺，聲景意境與文學詩意互文並存的音樂劇場新興美學。</p>
<p><strong>十一、12月</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著疫情的逐漸趨緩，2021年歲末不少的演出得以呈現，無論是與流行音樂、戲曲音樂、抑或世界音樂的結合，音樂的跨樂種與跨文化合作益發頻繁，例如12月12日在臺北流行音樂中心，臺北市立國樂團與流行樂、綜藝界的藝人合作（如：白冰冰、鄭怡、京劇名伶魏海敏、歌仔戲名伶郭春美、陳勝在、黃宇琳等）「TCO閃亮亮系列：誰比我流行」。這一類親民而通俗的跨界音樂會，吸引了各界不同背景與年齡的觀眾齊來共享。而臺北市立國樂團作為一個公立的國樂團，在跨域結合和勇於創新上，始終不落人後甚至以前瞻性的眼光佔有重要的一席之地，在2021年12月18日於中山堂的「TCO首席演奏系列：擊擊風」演出中，北市國邀請了朱宗慶打擊樂團的資深創團團員吳珮菁，與北市國的活躍於揚琴與擊樂首席謝從馨，兩位卓越的擊樂家各自有不同的學歷養成背景和職業角色，在這場協奏、獨奏與競奏的過程中，展現了質感與力度之美的多重體驗。無論在視覺、聽覺與動覺上都能敲打出前所未有的新興擊樂美學。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;談到擊樂，事實上近年來有不少的小型團隊以其精湛的技術和跨界的製作引人注目。例如由簡任佑、簡任廷雙胞胎兄弟所組成的Twincussion Duo雙子二重奏在臺中國家歌劇院小劇場所帶來的跨界演出，該演出橫跨古典、現代、電音、民謠、極簡主義等風格，兩位兄弟透過各類擊樂樂器，以兩人絕佳的默契帶領聽眾進入另一個不同凡響的擊樂新天地。而同樣以擊樂見稱的臺灣老字號朱宗慶打擊樂團亦在2021年末至2022年初重演了舊作《木蘭》擊樂劇場，該劇雖為舊作但有多處情節與表演方式做了多番的改革，無論在音樂、身段、唱腔或肢體方面，抑或是服裝、道具、燈光、布景等皆有重大的推陳出新，讓打擊樂與京劇的對話，做到更深層次的交流、淬鍊出更精緻的新興美學，可謂跨界擊樂劇場的一個經典之作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;值得一提的是，2021年的跨界音樂發展，亦有涉及與舞蹈領域的跨界合作，例如2021年12月31日在國家音樂廳所上演的由NSO國家交響樂團和「舞蹈空間」舞團共製演出的「舞夜狂歡」，便是在肢體律動與音樂聲響的共創共享中，以不同的藝術素材一起探索藝術生命的力量，音樂界與舞蹈界這樣的合作較為罕見，尤其以音樂為主體、舞蹈為客體更是不常見，對於廣大的愛樂者而言能透過舞者的肢體與律動，重新看到音樂、聽到舞蹈也的確另有一番滋味。</p>
<p><strong>（一）</strong></p>
<span>節目名稱：【TCO】閃亮亮系列：誰比我流行－經典流行天后傳唱</span><br>
<span>跨界元素：國樂、流行音樂、戲曲音樂</span><br>
<span>演出單位：臺北市立國樂團</span><br>
<span>演出時間：2021/12/12</span><br>
<span>演出地點：臺北流行音樂中心</span><br><br>
<p>演出內容：在北市國的主辦策劃與協演伴奏下，不同樂種、風格與形態的音樂得以跨界串聯，透過綜藝名人白冰冰的主持，流行樂手、民歌手、戲曲藝術家（如京劇名伶魏海敏，歌仔戲名伶郭春美、資深民歌手鄭怡等）齊聚一堂跨界演唱。</p>
<p><strong>（二）</strong></p>
<span>節目名稱：【TCO】首席指揮系列：擊擊風</span><br>
<span>跨界元素：打擊樂、跨文化</span><br>
<span>演出單位：臺北市立國樂團</span><br>
<span>演出時間：2021/12/18</span><br>
<span>演出地點：臺北市中山堂中正廳</span><br><br>
<p>演出內容：朱宗慶打擊樂團創團團員吳珮菁與臺北市立國樂團擊樂首席謝從馨聯手，代表中西擊樂的跨文化交流，給擊樂迷態帶來雙倍的震撼。</p>
<p><strong>（三）</strong></p>
<span>節目名稱：2021 Twincussion雙子二重奏打擊樂音樂會</span><br>
<span>跨界元素：擊樂、數位音樂、古典、現代、電音、民謠、極簡</span><br>
<span>演出單位：雙子打擊樂團</span><br>
<span>演出時間：2021/12/23</span><br>
<span>演出地點：臺中國家歌劇院小劇場</span><br><br>
<p>演出內容：擊樂因其獨特的開展性和可塑性，在跨界、跨文化與劇場的應用上都非常討喜。擊樂雙胞胎簡氏兄弟所組成的「Twincussion雙子二重奏」，便秉持對擊樂的熱愛與開放的態度，演奏範圍跨樂古典、現代、電音、民謠、極簡主義等各種風格、形態，為擊樂迷敲出豐富而多樣貌的擊樂旅程。</p>
<p><strong>（四）</strong></p>
<span>節目名稱：NSO&amp;舞蹈空間 聖桑斯「舞夜狂歡」</span><br>
<span>跨界元素：交響樂、歌劇、舞蹈、芭蕾、服裝設計</span><br>
<span>演出單位：國家表演藝術中心國家交響樂團、舞蹈空間</span><br>
<span>演出時間：2021/12/31</span><br>
<span>演出地點：國家兩廳院音樂廳</span><br><br>
<p>演出內容：由「國家交響樂團」與「舞蹈空間舞蹈團」共同合作，演出8首選自法國作曲家聖桑斯的芭蕾及歌劇作品，器樂與舞蹈地生動融合，再加上服裝設計上的精心搭配，呈現出「看到音樂．聽到舞蹈」的新興跨界美感。</p>
<p><strong>（五）</strong></p>
<span>節目名稱：朱宗慶打擊樂團2022擊樂劇場《木蘭》</span><br>
<span>跨界元素：京劇、多媒體、服裝設計、擊樂劇場</span><br>
<span>演出單位：朱宗慶打擊樂團</span><br>
<span>演出時間：2021/12/30</span><br>
<span>演出地點：國家兩廳院戲劇廳</span><br><br>
<span>演出內容：最初源自擊樂與京劇合體的《披荊斬棘》（2009），擊樂劇場《木蘭》在2010年首演起，便以打擊樂與京劇兩種截然不同的表演藝術之間的跨界對話，將「音樂、身段、唱腔、肢體」共融其中，樹立一種獨特的跨界劇場美學，展現出令人印象深刻的中西跨文化美學。此新版《木蘭》，不但全面昇華了視覺與聽覺的感受層次，從劇情、對白到服裝設計、舞臺美學等，皆更臻精練，而表演者的跨界演出能力也有顯著的大幅度提升，可謂寫下擊樂劇場史上輝煌經典的一頁。</span><br><br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以下根據2021年跨界音樂節目與表演藝術領域、與非表演藝術領域、以及兩者兼具的合作關係，做出圓餅圖與列表呈現。其中，跨表演藝術類，再依據跨音樂、跨戲劇、跨舞蹈、及兩者以上（其他）作整理比較。</p>
