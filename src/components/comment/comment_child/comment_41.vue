    <p><strong>一、跨界</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2020年裡，各種音樂類型或樂種之間的合作互動，不再僅以個人或單一樂團演奏曲目上的擴大變化為主－例如3月1日由張昌傑爵士樂四重奏所演出的「向經典．跨界致敬Tribute
      to:《Clazzic》」便是古典音樂和爵士樂的跨界融合。樂團與樂團、樂團與個人之間的合作互動，也是另一種跨界的方式。在2020年中山堂廣場音樂節中，由聲動樂團和絲竹空爵士樂團擔綱演出的「傳藝跨界」（4月4日）便是由不同的樂團來詮釋跨界。值得一提的是，在疫情期間，戶外的音樂會比室內音樂會更讓民眾安心。而6月28日由谷方當代箏樂和嘉義市國樂團合作的「谷方當代箏樂
      藝起玩音樂」，則是兩團結合傳統與流行的跨界演出。另一個「疫情概念」的例子是「聲東擊西－JPG&amp;TCO網路直播音樂會」，兩團多位擊樂名家以音樂「互相競擊」線上直播演出，融合傳統與現代、中西合璧各種擊樂的跨界節目。類似融合中西配器的跨界演出，還有10月17日「PEF沛－跨界三重奏：出境/入境」，音樂會裡將中西樂器並置，包括二胡、長笛與鋼琴，並將原創改編，呈現不同的風情。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;打擊樂器配合度高、開放性強，相當「百搭」，故經常是跨界合作上的常客。除了一線樂團，地方性的民間擊樂團，亦有不少跨界的嘗試。8月1日的「雲集打擊樂團YUN-PERCUSSION」，便是雲林在地的雲集打擊樂團，以爵士、民謠、古典等各種曲風，運用傳統或非傳統的打擊樂器，以互動的類劇場方式來演出。來自臺南的鄉韻民族樂團，是另一嘗試跨界演出的地方性樂團，在8月8日名為「夏日的跨界音符－鄉韻vs.風動
      父親節感恩音樂會」裡，該團與維克樂團組合了國樂與爵士的元素跨界演出。8月17日迴響國樂團製作的「咱的音樂．咱的歌」，邀請民謠歌手與民族音樂演唱家，合力跨界演繹韻味十足的臺灣風情。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;古典與流行過去存在的鴻溝，近來透過兩造的跨界合作逐漸消弭。12月5日的「振翅Flap
      Wings」便是佳例，高雄市管樂團與獨立樂團大象體操的聯演，讓管樂與數字搖滾樂有了邂逅與火花，跨界合作的原創及改編樂曲都能擦出亮眼的火花。
    </p>
    <p><strong>二、跨領域</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂與他領域的結合，往往因媒材的異質性與表現的差異性，能擦撞出火花、展現出新意，突破音樂做為「聽覺藝術」的局限，同時亦能吸引更多不同族群的觀眾。從戲劇、舞蹈、多媒、新媒，到文學、茶道&hellip;&hellip;，音樂結合的他領域範疇有愈來愈寬廣的趨勢。2020年「跨領域」屬性的跨界音樂節目，在數量上有多於其他兩類（跨界、跨文化）的現象。
    </p>
    <strong>（一）內跨</strong>
    <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;內跨係指與藝術相關領域的跨領域合作，<sup>10</sup>此型態佔跨領域節目之大宗。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;動畫加上現場的配樂，視、聽覺感官的雙重震撼，總能吸引普羅大眾。2月3日、4日，樂興之時管絃樂團搭配鼠年的到來，在農曆春節時演出老少咸宜的「《料理鼠王》動畫交響音樂會」，與迪士尼唯妙唯肖的動畫影像結合，讓現場觀眾猶如置身色香味俱全的美味佳餚面前，聽覺藝術與視覺藝術融合效果，讓觀眾的唾液似乎都不自覺的「分泌」起來，屬於內跨的跨領域。這類型的內跨動畫的音樂會，還有11月15日、11月22日由國立臺灣交響樂團、長榮交響樂團聯合演出的「小美人魚動畫交響音樂會」－以優美的樂聲詮釋經典童話《美人魚》，以及12月5日的「迪士尼《獅子王》電影交響音樂會」－邀請來自國外的歌唱家、演奏家搭配電影畫面現場表演等。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;兒童音樂劇向來擁有明確的觀眾市場－即兒童與家長，如何讓大朋友、小朋友都「目不轉睛」、「豎耳傾聽」，則是這類節目的重點之一。2月22日、23日，由臺灣藝術家交響樂團、沙丁龐克劇團共同合作的「音樂開門－遇見莫札特」，以「音樂神童」莫札特的勵志故事為腳本，搭配現場說書人的變聲說故事，小丑們逗趣詼諧的戲劇演繹，以及樂團的音樂詮釋，屬於內跨的跨領域。另一場親子劇，7月26日由弦響音樂工場演出的《飛吧！小豬比格！》也有類似的結合音樂、戲劇、繪畫與現場互動的作法，也是屬於內跨的跨領域。音樂會加上舞蹈和戲劇元素後，的確比較能吸引小朋友的關注。8月29日愛樂劇工廠、苗栗縣立國樂團、苗栗市客家兒童合唱團、惠風舞蹈工作室共同跨界演出的「老鼠娶親之公主不想嫁」，就是這樣一場結合音樂結合舞蹈、戲劇，屬於內跨的跨領域演出。結合戲劇的音樂演出，亦受大眾的青睞。9月2日的「一起笑哈哈《完美鋼琴老師》」是由網路竄紅的兩位音樂家用幽默的方式，結合戲劇性的語言、橋段，呈現包括古典、流行、電子、跨界等各種音樂，屬於內跨的跨界節目。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂會近來也有不少與文學進行的跨界合作。臺中市政府文化局與臺中文學館於9月19日合辦了一場「2020臺中文學季文字的力量：文學跨界音樂會」，透過朗誦、演唱的方式讓文學的力量得以傳達出來，屬於內跨文學的跨領域呈現。而當跨領域的合作對象增多時，複雜度與挑戰度必然也提高。8月14日新古典室內樂團主辦、主演的「聲景音樂劇場《給NK的十行詩》Ten
      Lines of Poetry to
      NK」，將音樂結合了藝術科技、電子聲響、舞蹈、劇場、文學、視覺藝術等多種領域的媒材與技術，以吳三連文學奬得主江自得探討「美」為主題的詩集作為創意發想，將其內容論述轉化為表演藝術的六幕聯篇樂章，游走在古典、現代與劇場之間，並融進互動藝術科技、舞蹈、電子聲響和視覺藝術等多層次的素材，展現當代表演藝術跨域交互對應的存在形式，對於臺灣特有的劇場美學有嶄新的解讀，其藝術實驗性具前瞻視野，屬於內跨的跨領域。同樣由新古典室內樂團主辦的音樂舞臺劇《阿嬤的歌本》於9月5日上演，該劇在音樂、戲劇、語言、民謠與舞臺美學上都有所嘗試，也相當受到觀眾的歡迎，是內跨戲劇的跨領域。類似的內跨戲劇領域的演出，還有臺北市立交響樂團與果陀劇場在11月28日跨域合作的《綠島小夜曲》－以曲曲曼妙的經典情歌串起一齣充滿回憶與溫暖的原創中文音樂劇，以及臺北市立國樂團、全民大劇團與臺北市立中正高級中學舞蹈班跨領域合作的《我的旁白人生》（12月5日）－除了戲劇亦跨舞蹈領域。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月15日、16日的《豆莢寶寶兒童音樂會：Bling
      Bling大冒險》則是朱宗慶打擊樂團專為兒童打造的兒童擊樂劇場，擊樂、劇場、互動都是必備的元素，屬於內跨劇場的跨領域。同樣是財團法人擊樂文教基金會與朱宗慶打擊樂團主辦的「沉浸式劇場《潘朵拉幻象》」，於9月24日至9月27日由謝賢德等人演出，節目內容以打擊樂結合現場電子音樂與新媒體藝術，創造虛實聆聽之沉浸式體；觀眾被三面環場的影像所包圍，身歷其境於充滿錯置與幻覺的表演場域，也是屬於在內跨電子音樂與新媒藝術的跨領域。另一場內跨擊樂劇場的跨領域演出是9月26日的《罪大惡極》，該節目是DRUMILY樂團主辦並擔綱演出，以類戲劇的方式，結合影像製作與音樂劇場的表演。10月16日的《食相》則是薪傳打擊樂團主辦、演出的又一場擊樂劇場，也同樣是屬於內跨劇場的跨領域。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月30日的「2020
      TSO音樂劇場《燈．影》」是由臺北市立交響樂團和丞舞製作團隊，演出作曲家潘皇龍專為臺北市立交響樂團50週年慶所創的作品《大燈對》。該作編織北管意象，並穿插舞者揉合民族舞、武術、爵士、芭蕾等穿越古今中外的流動表象，讓聽覺、視覺、甚至動覺皆有所刺激，屬於內跨舞蹈的跨領域製作。另一場內跨舞蹈的跨域演出是10月31日、11月1日由臺南市政府主辦，十鼓擊樂團、台南愛樂管絃樂團、滯留島舞蹈聯合演出的《島嶼傳說》，除了舞蹈，還有鼓樂、流行、古典的素材，同樣屬於內跨的跨領域演出。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;結合科技藝術的跨領域製作亦頗為豐富。10月10日演出的「後花園：科技與音樂跨域實驗劇場」為國立臺北藝術大學新媒體藝術學系與音樂學系共同策畫，法國里昂音樂院參與音樂創作的跨域原創作品。該作品結合電子音樂與新媒藝術的跨域合作，透過自製樂器、虛擬電聲、真實器樂與影像裝置的交錯運用，創造聽覺與視覺上的新感受，屬於內跨的跨領域合作。另外，11月20日、11月21日，演出的《共鳴体》乃是動見体、自由擊與裝置藝術家王仲堃三方的跨域合作，作品內容以「空間」與「觀眾參與」為發展軸線，透過機械裝置、燈光、打擊肢體等多元媒材，將實驗劇場量身訂做一個由聽覺、視覺、觀演關係三種不同的層面形塑的大型共鳴箱，屬於內跨聲音裝置藝術、肢體藝術、擊樂藝術等領域的跨領域製作。又如，11月27日薪傳打擊樂團在一系列的「2020高雄國際當代擊樂藝術節」中所安排的「A.I.與擊樂之對話」，即為一場擊樂與科技藝術的內跨型跨領域演出。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，亦有與戲曲的跨領域合作，如長青欣頌合唱團與明珠女子歌仔戲團在11月14日合作的「跨界學習之旅音樂饗宴」，便屬於內跨的跨領域呈現。
    </p>
    <strong>（二）外跨</strong>
    <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;外跨（與非藝術領域的合作）性質的跨領域合作，常因其範疇的擴大或媒材的與眾不同而能吸引更多不限於藝術愛好者的觀眾，這類節目接地氣、親民性高，較能雅俗共賞。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4月30日由采風樂坊舉辦、演出的「花語．花雨」，即結合音樂、花卉與文學，屬於外跨的跨領域。采風樂坊於9月17日的另一場演出「茶禪樂舞」，以傳統社會中一年的「二十四節氣」為主題內容，除了傳統器樂的演奏，音樂家們也即興唱奏，另有自由旋轉舞蹈與東方京劇之動態展現，以及融入茶的飲用與禪的冥想，亦屬於外跨的跨領域。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，7月17日至19日的「明池水劇場：優人神鼓－聽海之心」，是優人神鼓與宜蘭當地的觀光飯店的跨界合作，帶領現場觀眾在依山傍水的大自然中體驗藝術之美，屬於外跨的跨領域製作。8月30日雲林縣政府文化觀光處主辦的「繆斯X古蹟－Muse
      Trio繆斯三重奏音樂會」，演出地點在有百年歷史的行啟記念館，讓室內樂與古蹟對話，欣賞音樂的觀眾同時能感受濃濃的歷史風情與視覺效果，屬於外跨的跨領域。9月5日在臺南水交社園區舉行的《乘光而行》，也是類似這樣與環境對話的外跨型的跨領域演出。在優人神鼓表演者「優人」的鼓聲與肢體移動（神聖舞蹈）的帶領下，觀眾緩慢自在地於眷村樣態的園區裡步行。這類另類的表演與互動（或帶動）方式，能透過擊鼓節奏的力與速度，在變換的空間中一動一靜間地轉換知覺，與在傳統音樂廳中的靜態欣賞方式相當不同。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;與非藝術領域合作，雖易天馬行空但也能創意十足，常帶給觀眾耳目一新的感受。10月31日、11月1日由聲子樂集擊樂室內樂團和魔術師李昂軒、黃柏翰合作的《魔擊：預言家》，便是一場聽、視覺豐富，而又充滿想像與驚奇的體驗，屬於外跨的跨領域演出。
    </p>
    <p><strong>三、跨文化</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;天涯若比鄰的地球村年代，即便在疫情籠罩的高壓下，音樂總能超越國界，帶領我們神遊海內外，飽覽世界各地的人文風情。2020年體驗了不少以異文化觀點出發或融合世界音樂元素的音樂演出，讓樂迷欣賞音樂的同時，猶如參加一場文化的洗禮。跨文化類別的演出，大致可從配器、曲種與曲風等層面的文化差異性來體現。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1月5日由臺北市藝文推廣處所主辦的「無界之境－Wooonta問他跨界音樂會」便是這樣的節目，透過不同的跨界組合模式，將各類異文化的音樂元素組合、融合在一起。本場演出以二胡、低音大提琴及西塔琴三種弦樂器組成，曲目包含樂團原創音樂及改編曲，演出曲風同時融合世界音樂、探戈、爵士、即興及現代等音樂元素，屬於內跨的跨文化。1月17日由國立傳統藝術中心主辦的「臺灣國樂團『戲采』戲曲音樂會」，在國樂團的詮釋下，包括以歌仔戲、京劇、南管戲、北管戲、布袋戲、客家戲及豫劇等七種戲曲為創作素材的音樂作品，在一場音樂會中一次展現，讓中華文化傳統戲劇之美的深層文化底蘊表露無遺，屬於內跨的跨文化。4月18日的「無國擊－關於國樂與世界擊樂」，集結了國內打擊樂藝術家與作曲家，以「跨界創作思維」詮釋傳統音樂與世界打擊樂之結合，對於跨文化的融和與創新展現鏗鏘有力之美，屬於內跨的跨文化。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7月12日的「旅人．出走」是海馬樂團以嗩吶、琵琶、胡琴，搭配蒙古音樂常用的馬頭琴、呼麥，以及世界各地的打擊樂器，詮釋出融合了臺灣本地人文精神與大漠豪爽風情的音樂結晶，屬於內跨的跨文化。7月19日的「竹塹轉運站」，是來自宜蘭的A
      Root同根生樂團，透過音樂呈現來自客家、泰雅族原住民、北管曲牌等不同文化素材的交融，藉以搜索原始記憶、體驗臺灣文化的多樣性，屬於內跨的跨文化。11月3日在花蓮縣政府文化局演藝廳，舉辦了一場「太魯閣族四季交響樂章」音樂會，將太魯閣族傳統人聲古調、傳統樂器聲響轉譯成交響、室內樂曲，由豎琴家李哲音、太魯閣族傳唱家彼得洛．烏嘎、太魯閣族舞蹈家洪琳忠、景美國民小學、聲子樂集共同聯演，屬於內跨的跨文化演出。
    </p>
    <p><strong>小結</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜合以上2020年跨界音樂實況的觀察可知，在跨界音樂的三種類別（跨界、跨領域與跨文化）裡，跨領域（音樂與他領域）的演出最多，而其中又以內跨（音樂與藝術相關領域）型態的跨領域佔最大宗，雖然外跨（音樂與非藝術相關領域）的跨領域演出也有增多的趨勢。此外，跨文化的團隊或演出也有升溫的現象，而且與跨界類似，不再僅以個人或單一樂團演奏曲目上的變化而已，個人與個人、個人與樂團、樂團與樂團之間的合作互動等亦見成長。在疫情嚴峻的全球抗疫時期，2020年跟往年明顯不同的是，外國藝術家和樂團來臺演出的數量大幅度銳減甚至取消，許多演出改由本地音樂家擔任。在一定程度上，疫情的衝擊上反而刺激了本地音樂家的延展性，活絡了跨界音樂的在地化，改變了臺灣的音樂生態。
    </p>
    <h3>肆、跨界音樂生態的形變</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《製造音樂》（<em>How Music Works</em>）的作者大衛．拜恩（David
      Byrne，2015，194），本身也是一位音樂工作者，對於音樂人在現代社會中的生存困境，他有入微的觀察：
    </p>
    <p class="bui">大環境的改變對於音樂未必是壞事，對音樂人來說也未必不好。如今音樂人有太多管道可以找到觀眾&hellip;&hellip;傳遞音樂的裝置日新月異。現階段有太多途徑可以讓觀眾注意到你，這些途徑在我初闖進音樂界時幾乎都不存在。雖然目前的大環境充斥各種可能性，音樂產業本身資金卻已經不再雄厚，所以你得學會步步為營。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時代的氛圍與社會的動盪皆能深刻影響音樂生態的形塑。2020年臺灣的跨界音樂發展，除了延續前兩年的現象與趨勢外，因疫情的衝擊，迫使演出場所從實體場地轉換到虛擬平臺；演出載體的驟變，直接造成介於供給端與接收端之間循環鏈結上的變化，間接影響了音樂生態的模式，而此結構性的重大改變令習慣慣性思維的多數人措手不及。
    </p>
    <strong>一、演出場地的擴大（實體場景到虛擬平臺）</strong>
    <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我們常誤解音樂的認知是一種基於心理或精神層次的超然、絕對客觀的聲波經驗，並將音樂的藝術性建立在這樣一個簡化的假說上。但事實並非如此單純。根據拜恩（2015，253）的觀點，人們對於音樂的觀感、所認知的音樂意義，其實涉及包括演出場所等多種複雜的因素：
    </p>
    <p class="bui">音樂是由那些我們在特定時間與地點接觸到的聲波組成。它出現了，我們意識到它，之後它就消失了。音樂的「經驗」指的不光是那些聲波，還包括它出現的場景。
    </p>
    <p>拜恩點出我們對於藝術的迷思：</p>
    <p class="bui">很多人相信偉大的藝術總隱藏著某些神秘物質，也認為藝術作品之所以能深深打動人，都是因為這些隱形物質。這個不可捉摸的東西還沒有被解析出來，但我們都知道，人際、歷史、經濟與心理等因素也跟作品本身一樣，都會左右我們的感受。藝術不會獨立存在，而各種藝術之中，音樂基於它稍縱即逝的特質，更像是一種經驗，而不是某項物品。它會跟你在哪裡聽見它、支付多少費用、在場有些什麼人等事實緊密聯結。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在疫情爆發之前，跨界音樂已面臨演出場地轉型的問題。先進的科技讓音樂變得唾手可得，串聯平臺上既然有免費或很便宜的音樂可以下載保存，為什麼還要買票聽現場呢？以音樂串流平臺的龍頭Spotify為例，截至2020年，Spotify已有3億2千萬訂閱者（其中付費訂閱的有1億4千4百萬），而且客戶還在持續增長中。在它眾多的成功因素中，最明顯的莫過於可以因人而異的客製化歌單設計。單曲勝過專輯，聽者擁有主導權，不必接受唱片公司「強迫推銷」的每首歌曲。音樂串流平臺的歌單文化讓不同樂種、風格的音樂可以同時並列，聽者可依個人的品味與當下的心情選擇出適合的音樂來聽。而拜高科技的人工智慧所賜，平臺背後的數據分析能精準地追蹤出聽者的聆聽習慣，<sup>11</sup>推測出在不同時刻的當下，聽者可能會想要聽到的音樂，然後再主動將這種可能會受到青睞的音樂推波到聽者的面前供其選擇。換言之，聽者的選擇其實是根據串流平臺所提供的選擇而做出的選擇；以商業的角度來看，最大獲利者是幕後操控串流平臺的人－雖然其他音樂工作者（作曲者、作詞者、演奏者等）也能從中獲利。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂串流平臺的普及化至少說明幾件事實：第一、人對音樂的品味是多元而易變的，「客製化」讓聽者更方便、更有選擇權；第二、人們願意為音樂付出的金額越來越低，一個金額能同時擁有越多越好的「吃到飽」音樂選項，音樂甚至可能被像贈品或額外服務那樣被包裝在其他商品裡；第三、「把餅做大」，集結眾人之力以鼓勵更多聽眾的加入是經營音樂產業的良策－尤其中小型的組織更是如此。<sup
        >12</sup
      >
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;無論如何，網路平臺已成為到音樂廳聽音樂以外的選擇。既然聽音樂變得如此方便，買音樂專輯變成是一種收藏的概念，那麼看現場演出自然是要體驗線上聆賞無法獲得的「其他」部分；而此「其他」部分，正是跨界音樂得以茁壯發展的主因。不論與自家音樂人（古典、傳統、流行、世界等）的「跨界」、與其他領域的「跨領域」（與藝術相關領域的「內跨」、與非藝術領域的「外跨」）、抑或融合異文化素材的「跨文化」，跨界音樂有百變的「玩法」，從聽覺、視覺、到觸覺，嗅覺甚至味覺，跨界音樂讓觀眾有「身歷其境」的臨場感，它與觀眾互動，甚至邀請觀眾一起參與音樂的實踐。音樂是靜態的抽象聽覺藝術的傳統觀念，並不適用於跨界音樂，演出場地在跨界音樂的創、演關係中，扮演關鍵性的角色。音樂的演出場地不侷限於音樂廳、演奏廳等傳統音樂演出場所，包括劇場、劇院、體育館、戶外廣場、公園、古蹟、沙灘、沙龍、咖啡廳、酒館&hellip;&hellip;，都是實踐跨界音樂的所在。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;場地成為創作音樂時的重要考量，音樂與場景、環境、空間的關係，空間與演者、觀者之間的關係，都左右音樂的呈現方式。生物有適應環境的特質，疫情爆發後，密閉空間成為病毒傳染的溫床，「百搭」、「善變」的跨界音樂，比任何他類音樂更務實地順應環境、配合場景，其演出場地有轉往虛擬線上平臺或戶外、非密閉空間的現象。雖然疫情促進了表演藝術數位化的進程，用網路或影片製播的方式來呈現是國內外表演藝術團體的因應疫情延燒的策略，但因臺灣的疫情相對趨緩，表演團體恢復正常表演，展演場館也重新營運，導致數位化的程度相對較慢（財團法人臺灣經濟研究院，2020，24）。
    </p>
    <p><strong>二、參與者的增多（供給端到接收端）</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當我們把音樂視為藝術時，思考的範疇總是圍繞在內涵、意義方面的主題，社會普遍相信藝術是神聖的、是精神性的或超乎自然的。藝術神奇的力量非唾手可及，而是像天賦異稟的藝術家或受到上帝眷顧的神童之類的非凡者才會擁有。如此類似宗教崇拜的藝術觀點，無疑將音樂拱在一個高高在上的位置，音樂的珍貴來自於它獨一無二的作曲家所創造出的原真性（originality），而此原真性的再現（representation）自然非一般「常人」能夠駕馭，必須是技術非凡、悟性甚高的「藝術家」方能將作品再現、詮釋得絲絲入扣。如此假說顯然已將音樂的參與者不均衡地限定在音樂家（作曲家與演奏家）身上，至於聽眾怎麼感受、怎麼認為，似乎是其次、甚至不相干的問題。但怎樣的音樂演出，才能算是忠於「原汁原味」的原真演出呢﹖《為什麼藝術家那麼窮﹖－打破經濟規則的藝術產業》（<em
        >Why Are Artists Poor?: the Exceptional Economy of the Arts</em
      >）一書的作者漢斯．艾賓（Hans
      Abbing，2008），本身具備藝術家、經濟學家與社會學家等多重身分，對於藝術的原真性提出質疑：
    </p>
    <p class="bui">在傳統表演藝術，盡可能準確接近藝術家原本意圖的真正或原真表演，是一種近乎宗教式熱情來追求的目標，這對古典音樂尤其正確（但作曲家的意圖並不是那麼明顯，還是會有品質或詮釋上的些微差異，並為某個特定表演是否忠實詮釋原作，而點燃無盡的辯論）。但什麼樣的表演才算忠實把原作呈現出來﹖（頁176）
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;窄義而言，藝術的定義只是藝術家口中的藝術，是社會階級制度下某一階級的人優越感的加持。對於藝術受到來自不同社會位階的釋義有一番見解：
    </p>
    <p class="bui">只要社會階級存在，只要藝術品還被用來標記一個人的社會地位，對藝術品不均衡的評價就會存在。社會階級較高的人看不起低階藝術，<sup>13</sup>可是後者反而尊崇前者。確實比地位較低講話更大聲、更有力。無論他們是察覺，地位較高佔用藝術釋義權（頁23）。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣社會在歷經日治、光復、戒嚴、解嚴、股市狂飆、政黨輪替等各個時期的動盪與重整後，社會階級的劃分不若以往明確，觀眾的口味與背景趨於多元；音樂生態中雅、俗兩端以往涇渭分明的態勢也逐漸模糊，古典不再高不可攀，流行也未必是平民或底層觀眾的選擇。在現代的臺灣社會中，一般而言，大眾與藝術界似乎失去對於獨創與原真性藝術的興趣。跨界音樂沒有過往藝術音樂與日常生活需求脫鉤的趨向，也不強調以往供給端（作曲家與演奏家）所堅持的音樂原真性，在與各樂種、他領域、異文化之間的合作過程中，跨界音樂敞開心胸廣納不同夥伴的聲音，且樂於邀請更多來自接收端的各種不同背景的觀眾的互動與反饋。跨界音樂音樂詮釋權的釋出與自由融合，反映了跨界音樂與生活、環境的接軌；跨界音樂的定義和其參與者的實踐過程有密切的關係。誠如拜恩（David
      Byrne，2015，9）指出：
    </p>
    <p class="bui">音樂的動不動聽，不單單取決於它本身獨立存在（假使音樂有所謂「獨立存在」的話）時的本質，更大的決定因素在於它周遭有些什麼、在於你聆賞的場合與時間。演奏方式、銷售與發行途徑、錄製過程、誰來表演、你跟誰一起聽，還有，聲響品質如何，以上種種都不只決定音樂能不能打動人－是不是順利達成創作時的目標－同時也決定了音樂的本質。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧2020年各類的跨界音樂演出中，屬與音樂外的跨領域合作之類別佔最大宗，從戲劇、戲曲、舞蹈、裝置藝術、科技藝術、魔術、文學&hellip;&hellip;等，光是供給端裡的創作者與表演者就有顯著的擴大和增多的現象，若再加上不同領域所吸引來的不同背景的觀眾，整個音樂生態因各式參與者的流動涉入而氣氛活潑熱絡。此外，越來越多的演出單位不再嚴格限制攝影或拍照，常常在謝幕或安可曲時，允許觀眾拿出手機來拍照或錄影。有些團隊甚在結束前跟觀眾來個大合照，藉著現場觀眾的人氣趁勝追擊，一方面替自己打知名度，另一方面拉攏觀眾促進他們的參與感。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;值得深思的是，根據艾賓表演藝術的成本理論，成本上漲時，刪減開銷的其一方法是增加觀眾、為更多觀眾而演，此為一種開源的概念；另一是減少表演者，就是節流的思考；若兩者並行則可將生產力大幅度的提高（Hans
      Abbing，2008，175）。但從2020年觀察到的跨界音樂製作發現，參與的觀眾雖然因透過網路平臺的觸及與場地的擴大等各種因素而增多了，但實際購票的實體演出往往只演出一、兩場就結束，在開源方面效果似乎仍有限，而參與的創作者和表演者卻也因跨界、跨領域的合作，在人次上增加了不少，在節流方面恐怕成本不減反增。如此一來一往，跨界音樂製作的成本實際上比單純音樂演出要高，這將是跨界音樂永續發展的另一挑戰。
    </p>
    <p><strong>伍、</strong><strong>跨界音樂文化的質變</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖然「變化」是藝術永恆不變的真理，以多元見稱的跨界音樂，發展至今，其多元性已不再侷限於外在型態、運用素材、媒介的多樣變化而已；內部質地的交融、思想見地的磨合、文化美感的共享等，都成為發展跨界音樂的重要考量。跨界音樂的實踐者已漸意識到，五花八門的嘗試、形形色色的組合，若缺乏明確的中心思想、凝聚力與認同感，跨界音樂將難以扎根永續，更遑論締造美學新境界。綜觀近年來臺灣蓬勃發展的跨界音樂現象探討，<sup>14</sup>我們發現：跳脫常規、風格多元、建立在「非典型」創演模式上的跨界音樂，實乃民主社會下的一種文化產物，而跨界音樂在此民主化與社會化傾向的引領、捍衛下，在2020年嚴峻疫情的極度衝擊下反而逆勢崛起。
    </p>
    <p><strong>一、音樂民主化</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術是時代的產物，表演的純正與否、是原真還是改編等判斷，並非依賴什麼客觀的假定，而是以既有常規為基礎。但「常規」也是因人而異、因地制宜的，沒有絕對的標準答案，「<span class="bkt">品質變化是藝術界常見的現象，而且藝術界向來會、也繼續會自技術進步中獲利</span>」（Hans
      Abbing，2008，179）。在現實的音樂生態中，市場的快速流動性與消費行為的多樣化，使得消費者對於音樂很少有絕對的「忠誠度」，他們昨天到歌劇院看歌劇，今天可以到小酒館聽爵士樂，明天可以去戲曲中心欣賞歌仔戲，這樣在不同領域中的參與行為並不抵觸。對於現代消費者而言，就像換菜單吃、換衣服穿一樣，只要有興趣且經濟許可，他們普遍願意嘗試。當某一類音樂的消費者逐漸流失時，理論上，可由新的或較年輕的觀眾來遞補。但什麼樣的音樂演出能引起「善變」的消費者的興趣？什麼樣的內容能讓消費者獲得最大的滿足而感到收費「合理」呢？要回答這個問題，讓我們先認清，音樂活動始於人、終於人，要理解音樂，就要去裡解音樂在文化中的位置。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《藝術．文化經濟學》的作者詹姆斯．海布倫（James Heilbrum）、查爾斯．蓋瑞（Charles M.
      Gray）（2008），在討論藝術挹注資金的議題時，曾分析菁英主義與民粹主義之間鮮明的立場差異：
    </p>
    <p class="bui">簡單的說，菁英主義者就是所謂高階藝術的愛好者－亦即戲劇、歌劇、舞蹈、「嚴謹」或
      「古典」音樂、精緻藝術與美術館－而民粹主義則是對民間藝術、工藝、民俗與少數民族藝術，還有非傳統藝術有興趣，或至少是對這些藝術也有興趣。菁英主義自認是「卓越」與「品質」的守護者。而民粹派則擁護藝術的「多元」以及「大眾」。（頁416）
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，介於音樂中的菁英主義（古典音樂）和民粹派（流行音樂）之間的高牆，正因環境改變與世代交替等各種因素，近年來正逐漸瓦解中。
    </p>
    <p class="bui">最近這幾十年裡，「嚴肅」音樂在作曲方面的藝術創新不夠吸引人，也不令人興奮，無法增進及維持聽眾群&hellip;&hellip;近年來古典音樂在建立聽眾群上遭遇困難，主要是無法吸引青年樂迷&hellip;&hellip;這是因為陪伴他們長大的音樂讓他們習慣於一種（與政治與社會等相關）「新美學」，它「向古典的西洋美術音樂美學挑戰」&hellip;&hellip;有一種由爵士樂、搖滾樂、民俗音樂與傳統古典音樂構成的「世界音樂」，並適當加入這種新美學，便成為二十一世紀最重要的「藝術音樂」。既然那樣，「嚴肅音樂」產業與音樂會景象將以我們難以想像的方式轉化；觀眾的成長可能是非常有趣的結果。（頁416）
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;或許正是這樣市場導向、民意至上的驅動力，讓音樂能漸漸放下歷史包袱、走出刻版印象，消除對他者的歧異，開始傾聽民意，關注消費者的需求。而孕育於臺灣這樣一個多元文化並列、科技高度發展、思想自由經濟繁榮的先進社會中的臺灣跨界音樂，因能同時擁抱「卓越」、「品質」與「多元」、「大眾」，能滿足各界（包括所謂的菁英主義或民粹派）不同的需求，能化表演藝術跟大眾流行文化的干戈競爭為互助合作，而展現出高度的音樂民主風範，成為雅俗共賞的最佳選擇。如同所有的「民主化」過程，跨界音樂挑戰固有威權式的音樂詮釋權，其重點非鼓吹某些公認大師的藝術崇高性或塑造某種高不可攀的價值，而是刺激源源不絕、來自各界、各領域的創造力整合。
    </p>
    <p><strong>二、</strong><strong>音樂社會化</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我們對於音樂創作的動機常常流於一種刻板印象，認為它是發自某種內心的情感或精神上的召喚，創作提供了一個抒發的窗口，而演奏被視為一種二度詮釋，一種感受的再現。這種後溯過程（backward
      process）常被用在不可重複、驗證的歷史研究上。從跨界音樂的發展，我們觀察到：場景不僅影響創作、甚至主導了表演的呈現方式；這裡所謂的場景包括表演的場所、場合、環境、氛圍、型態、元素、器材、功能與目的等，創作者與演出者的前見會影響他對作品形塑的方向，進而造就了音樂的類型與風格，此即前向過程（forward
      process）的可預測性會左右音樂的發展。例如，創作者知道要為一場在戶外的劇場式舞臺製作一齣親子音樂劇時，他有意無意地會從與「戶外的」、「劇場式舞臺」、「親子音樂劇」相關的先備知識著手，讓他的作品能配合、或者融入該場景的特定氛圍，以達到最高效果。「<span class="bkt">人們如何看待一場表演或任何藝術型式，他們帶著何種期待前來，其實都取決於場合</span>」（David
      Byrne，2015，67）。這就是為何當一票難求的美國小提琴家貝爾（Joshua
      Bell）化身街頭藝人在地鐵站演出時，卻鮮少有人駐足欣賞，更無人認出他來的原因。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由此可知，人們對於音樂的參與，常常是取決於音樂在社會中的姿態、地位，以及音樂與我們之間功能性的關係。如果接受某一類型的音樂，代表認同該類音樂的品味、思維、乃至於意識形態，那麼視音樂為一張進入某一特定會員專區的會員證的看法，也不足以因其可能對音樂有所貶抑而遭受撻伐。臺灣音樂教育普及化的結果，<sup>15</sup>讓音樂在臺灣社會的階級性未如西方社會那樣明顯，古典音樂在西方社會中如貴族般的鑲嵌性（embeddedness），<sup>16</sup>歷經在臺灣長期的社會化過程後，今日已與本地的文化融合轉化，雖然在用詞上仍有「高階」與「低階」等辭彙的出現，但已經沒有之前那種象徵性的意涵，指出某些領域或作品類型。音樂的實踐同時也是社會的實踐；臺灣多元豐富的音樂型態便能反映出我們社會中既有族群的差異性，而能橫向連結、擁抱各界的跨界音樂，本身即為當代臺灣社會的縮影。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跨界音樂的崛起，反映出音樂從聽覺文化拓寬加入視覺文化的趨向；聽覺對於萬物的認知來自四面八方，在辨識上需要較專注的注意力，對於聲音是屬於本質的認識；視覺則是平面的線性思考，畫面的呈現有時間上的先後順序，搭配上敘事性的戲劇或文字，就能有定格的感覺，除此之外，各種形狀、顏色、造型、素材的物品不斷被製造出來，視覺效果很快能接收到刺激，可以一眼看到諸多類型與等級。在3C產品充斥、人人手不離機的年代，吸引、滿足視覺的需求是現代消費市場的生存之道。跨界音樂對於視覺藝術的跨界融合，也可算是種社會化的結果。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;約翰．謝潑德（John Shepherd）與彼得．維克（Peter
      Wicke）（2017，3）合著的《音樂和文化理論》（<em>Music and Cultural Theory</em
      >）中，針對音樂學理的論述模式有「反主流」的主張：「<span class="bkt">音樂是以聲音為基礎的意指實踐，而不是一篇篇音樂作品的總和。</span>」事實上，上一世紀下半葉，西方樂界對於反主流音樂的思想已經落實到創作上。戰後的動盪年代，既有音樂教條被徹底挑戰，出現了不少非典型音樂的自然界聲響等具象音樂等都含括為音樂的範疇；又如艾瑞克．薩替（Erik
      Satie）提出一種音樂可轉化於環境中的家具音樂概念，那是一種可融進環境中，可被聽見但不被刻意聆聽的音樂－這些都是一種反向的音樂思維，都是寧可重視個體之間、或與環境之間相互作用而逐步內化、產生新創作的過程，而不願繼續墨守成規，向不符合潮流的所謂主流音樂靠攏的作法。朝向多元發展的表演藝術，不但擴大了其發展領域，也轉變了型態。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不畏疫情肆虐，2020年的臺灣跨界音樂演出在範疇方面，從文學到科技、魔術到茶道等，在場地上更不設限，除了室內也走向戶外、登上網路平臺。戶外展演能夠結合節慶、攤販、野餐等多種活動，網路平臺演出能結合線上直播、教學講座、工作坊等諸多實用的相關活動，在在都體現了跨界音樂具體社會化的特質。
    </p>
    <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現實從來不是一成不變的太平世界。延燒超過一年的武漢肺炎疫情，使我們意識到現實其實是無數難以預測的「黑天鵝」串聯起來的極端世界。在極端世界裡，過去堅定不移的價值觀很難繼續被信賴，因為事實證明這些「倚老賣老」的經驗與知識已不足以精準預測未來。在極端世界中，想要屹立不搖、永續經營，「百年老店」的壓力更勝個體戶或新興組織。但沒人可以真正高枕無憂，因為難以預料的環境變遷（無論來自人為或大自然）是大家的「公敵」；諷刺的是，極端世界對於「大人物」的巨大殺傷力，有很大部分是來自「小傢伙」的逆勢竄起。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年臺灣跨界音樂有從跨界轉向跨領域發展的現象，這意味著臺灣的跨界音樂正逐漸走出音樂圈內的同溫層，朝向與音樂以外的他領域（目前以藝術領域為主）來交流；這說明了跨界的風氣不但受到音樂界的認同，也已瀰漫在藝術圈中，音樂家得以與其他領域的藝術家、專家等交換心得、相互啟發。此傾向驗證了驅動臺灣當前社會的一股社會創新力－其特性包含創新翻轉、巧用資源、合作共創等（陳俊文，2020，29-30）－正激勵著我們的音樂生態，影響著我們的音樂文化。透過不同載體的結合，跨界音樂的觀眾群得以擴大，其中，網路成為音樂衍生應用的方向，透過實體與虛擬兩個載體的互相結合，融合聽覺與視覺的跨界音樂在新的載體上有更多開發潛能。增加跨界知識、技能的交流與學習，則是面對包括跨界音樂在內的表演藝術因應疫情而加速進入數位化時代的應對之道。<sup>17</sup>此外，如何在跨界音樂的實踐過程中，找出能與參與者（包括供給端與接收端的各類參與者）溝通的語言、元素，讓作品從有限的領域擴大到以外的範疇，甚至跨足到更大的市場以滿足觀眾、消費者更多元的需求，<sup>18</sup>也是值得深思的課題。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;反思臺灣跨界音樂的發展軌跡，我們發現，儘管多元豐富、創意無限、常能達到海納百川、雅俗共賞的效果，不少演出對於藝術性的深度與精緻度的考究，的確也有力不從心的現象。此外，「圖文不符」的誇大節目文宣，及不專業的現場導聆、解說，常令人有「乘興而來，敗興而歸」的遺憾。跨界音樂幫助我們認識、參與了我們所屬年代下的文化，那是一種充分體現民主化與社會化的多元精神。2020年的因疫情風暴加速了跨界音樂數位化的進程，釐清了我們對於跨界音樂場地與參與者選擇上的迷思，但在飛快成長茁壯的同時，跨界音樂仍有艱難的課題必須面對：
    </p>
    <p class="bui">「跨界音樂」最大的挑戰並不在於定義的確認或範疇的劃分（畢竟開放的定義與空間，其實亦是它的特色之一），而是在於它缺乏深刻的自主意識，即所謂的自主權（autonomy）。<sup>19</sup>（陳慧珊，2016，61）
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唯有透過一步一腳印的實踐與反思過程，跨界音樂才能在社會環境的變動性、表演方式的開放性及參與者的自主性之間找到平衡點，循序漸進地確認出專屬於它的自主意識。以此推論，透過科技媒體、網路平臺、翻轉學習（flipped
      learning）、<sup>20</sup>行動參與、移地教學、建教合作等非傳統教學取徑來培育專業的跨域人才，應是目前跨界音樂發展刻不容緩的重要議題。
    </p>

