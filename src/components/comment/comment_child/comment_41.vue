    <h3>前言</h3>

    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;武漢肺炎<sup>1</sup>糾纏2020年一整年，至今<sup>2</sup>已奪走超過2百萬人的性命，這隻顛覆常理與經驗的「黑天鵝」<sup>3</sup>，在世界各地掀起巨大的風暴，重挫士、農、工、商等各行各業的生計，對全球經濟與藝文發展造成極度大的衝擊。臺灣身為地球村的一份子亦無倖免於這場風暴之外，包括表演藝術展演在內的群聚活動紛紛被迫取消或延期，國際性的交流更急速銳減甚至凍結。雖然臺灣有目共睹的防疫成就，讓本地表演藝術團隊的實體演出，仍能在疫情緩和時配合相關措施持續舉辦，這場無預警的風暴的確打亂了既有的音樂生態，音樂演出被迫轉往網路平臺呈現，音樂發展在疫情的推波助瀾下飛快進入數位化時代。於此動盪不安的氛圍下，善於互動、合作、最具「民主化」、「社會化」特色的跨界音樂，以其多元、開放、親民等特質，在逆勢中崛起，蛻變成為臺灣音樂發展現況中，最具潛力與競爭力的佼佼者。
    </p>
    <p><strong>壹、跨界音樂的廣義論述</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今日，跨界已是發展的趨勢、進步的顯學；與其定義何謂「跨界」，思考「什麼不是跨界」或許比較接近現實。放眼日常生活中的大小事，任何食衣住行中的方方面面，在不同程度上，都會涉及跨界的行為。以音樂為例，廣義而言，音樂本就是跨界的活動，古今中外，「樂」在形式上至少包括音樂和舞蹈的行為，其他亦有涵蓋戲劇與詩學等的看法；在內涵上，「樂」則是情感的抒發、心境的映照或故事的描述，並常與崇尚道德倫理的「禮」密不可分。如此觀之，跨界音樂的重點不在於揭露既有已被認知的音樂之內部跨界情形，而是透過各類異質音樂（如不同樂種的音樂）或音樂與異質領域（如非音樂類藝術、非藝術類領域）之間的交涉跨越、互動融合、重組再現的過程，以異質思想（heterogeneous
      thinking）或異質知識（heterogeneous
      knowledge）來融會貫通其複合性；換言之，跨界音樂是經過某種跨界的手段、運用或方式所產生的非典型音樂創作模式或成果，它既不屬於某種固定類型亦非某種特定美學。<sup
        >4</sup
      >
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「跨界音樂」做為一個專有名詞或用語，近來已大幅滲透臺灣的音樂文化，無論是供給端的創作者、演奏者，或是接收端的欣賞者、評論者（包括學者），<sup
        >5</sup
      >
      它屢屢被書寫、論述、研究，<sup>6</sup>進而從10幾年前的陌生一路走到今天的熟悉。「跨界音樂」做為一種創演活動或實踐，這幾年有普及化的趨勢，無論在國家級的音樂廳抑或是戶外臨時搭建的野臺，融合豐富素材且表演方式多樣化的跨界音樂能打破城鄉差距受到觀眾的青睞，吸引更多音樂實踐者的參與，進而影響了臺灣音樂生態的形塑。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，「跨界音樂」做為一種音樂的定義或理解，無論從其形態或內容上而言，卻至今尚未有普世性的共識，在分歧的各方觀點中，有的根據分類的概念，主張跨界音樂為「<span class="bkt">古典音樂和流行音樂外的第三類音樂，非古典也非流行&hellip;&hellip;為古典和流行的複合體，是一個必須獨立出來探討的領域，無法再以傳統的音樂分類和審美標準來檢視</span>」（宋英維，2011，25）；有的兼具音樂的內涵與表現，強調跨界音樂是「<span class="bkt">指風格與表現方式介於藝術音樂、流行音樂、傳統音樂或現代音樂之間，融合科技音樂等不同音樂之素材與技法進行創作或改編之音樂</span>」；<sup>7</sup>有的依照是否能歸屬到單一專業領域的原則來歸類跨界音樂：「<span class="bkt">今日這些不同樂種、風格、素材、語彙、中西樂器等的融合，或將音樂結合視覺藝術、文學等不同領域的藝術表現成為普遍現象，要判定某一演出為單一領域，經常較為困難。在這樣的情況下，就歸類於跨界</span>」（陳惠湄，2019，1731）、「<span class="bkt">無法歸類或是實驗性質的跨界音樂活動，則專立『跨界音樂』篇幅收錄</span>」（張儷瓊主編，2019，5）；有的則更重視各類音樂之間的交流融合，認為跨界音樂「<span class="bkt">係指介於傳統音樂、藝術（當代創作）音樂、流行音樂之間，屬於分項領域之間交越跨涉的表演創作案例</span>」（張儷瓊主編，2019，13）。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者長期觀察臺灣跨界音樂的發展，根據跨界過程中參與者或媒材元素之間合作互動的主客關係、涉入的深淺度、呈現的表徵美學及蘊藏的文化思維等因素，逐步扎根進而提出6種跨界的面向，分別是：主客體置位易辨的cross、多重主體性並置的inter、自體變形並與他者交融的multi、拼貼混雜的mix、文化複合性的
      hybrid、越域新創的trans（陳慧珊，2016，99-106）。與其說這是一套跨界音樂的分類標準，不如將它視為辨識跨界音樂實踐過程裡，各方涉入程度、互動關係與體現方式的切入點；畢竟，跨界音樂的多元異變性實難以固定，更難以單一的標準來將之歸類或標籤化。值得一提的是，跨界音樂因參與者、運用媒材及演出載體的豐富多樣，其立論觀點往往能因立基角度之別而有所差異，故筆者在提出上述6項跨界型態之際，同時也建議將表演藝術循環過程中之供給端（含創作者與演奏者）與接收端（含欣賞者與評論者）的變異性納入考量，如此便能更客觀地掌握跨界音樂的有機異變，理解蘊藏其內的複合性與顯露於外的多樣性（陳慧珊，2016，80-81）。此外，音樂學者蔡宗德（2020，1762）在去年的年鑑〈2019年度跨界音樂與其他之活動觀察與評介－世界、跨界、新住民：臺灣社會環境的改變與新音樂形態的發展〉一文中，則從跨界音樂為多重元素組成的複合體的看法出發，他依據元素之間的結構模式提出跨界的「組合、磨合、聯合、融合」四部曲之說，藉以解釋跨界的最終觀照應能形成一種自身獨有的藝術美學而不至於收編進單獨一項元素之下。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;對於一個透過融合與再生，不斷有機滾動展開且正在快速蓬勃發展的新興藝術型態而言，各界對於「跨界音樂」的定義仍有分歧、甚至褒貶不一的情形似乎再合理不過，畢竟實務先行於理論，音樂定義是發生於音樂實踐之後的一種「事後諸葛」。無論如何，跨界意識瀰漫於當前社會，跨界合作流動於各行各業，臺灣各類音樂都有朝向跨界發展的趨勢；有跨越族群、結合領域、承襲傳統、運用流行等「百搭」、「百變」特質的跨界音樂，正快速崛起引領樂界風雲，並逐步形塑出其獨樹一幟的特色。&nbsp;
    </p>
    <h3>貳、跨界音樂之發展背景</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術與人文密不可分，其可貴之處在於能抒發情感、關照社會以及反映現實（陳慧珊，2016，13、45）。是以，欲探討跨界音樂在當代迅速崛起與傳播流動的現象，宜先釐清隱身其後的人文發展脈絡。
    </p>
    <p><strong>一</strong><strong>、全球在地化</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者於2016年出版的《傾聽弦外之音：音樂藝術跨界展演研究》一書中，透過檢視音樂的全球化（globalization）趨向和觀察西方音樂文化的全球化歷史基礎，嘗試勾勒出臺灣跨界音樂的發展背景：
    </p>
    <p class="bui">新世紀以來，先進的科技加速了全球化概念的擴展，促使了音樂跨界發展的速度與規模。本地音樂藝術在「全球在地化」思潮的影響下，趨向將本土文化的認同與全球化的精神作一結合，其跨界行為一方面呼應了全球化的趨同邏輯，另一方面卻又抵制了全球化的逐異手段，捍衛了在地的多元特色與專有風格。（頁45）
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;何謂「全球化」﹖英國社會學者季登斯（Anthony
      Giddens，1989，520）曾解釋：「<span class="bkt">世界社會增長中的相互依賴性之一般名稱就是全球化</span>」。當這種「相互依賴」的概念發生在各類音樂之間，或音樂與他領域之間的合作過程時，音樂既有的形式被突破、規則被挑戰，界與界之間的藩籬被推倒，領域和領域之間的鴻溝被瓦解。當全球化的邏輯與在地化（localization）的觀念結合形成「全球在地化」（glocalization），而被運用在音樂活動的跨界合作時，其產出（即跨界音樂等）一方面能呼應世界觀、在全球市場架構下找到立足點，另一方面亦能反映本土性、體現在地人文風情或文化意涵。這樣的跨界音樂創作方式與展演活動，在當代有明顯普及化的趨勢，人們傾向於相信「1+1&gt;2」，團結力量大，打群體戰比孤軍奮戰具優勢。
    </p>
    <p><strong>二、科技掛帥的網路時代</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;倘若當代欣欣向榮的跨界現象是全球在地化趨勢下的一種必然結果，推波助瀾此趨勢的先進科技自然也參與了跨界音樂的形塑。不設限的跨界音樂，不但不視科技為洪水猛獸，反而積極與之合作，於內：將科技做出來的音樂、聲響納入其中，於外：善用科技來製作、剪輯、播放、甚至演奏。跨界音樂與科技的融合至少凸顯三點新意：第一、音樂的演奏不再侷限於人為的現場演奏（live
      performance），過去被鄙視為「作弊」或「廉價」的罐頭音樂、電子音樂和輔助特效，而今被廣泛採納甚至重用；第二、由科技儀器製造、剪接或加工出來的音樂，也可以富於情感與人性，音樂的藝術性並不取決於科技的使用與否；第三、科技能突破樂器、編制、技巧等方面的限制，執行人為演奏無法達成的速度、音域或音色等，演奏家的基本「奏樂」功能能輕易被取代。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;進入5G互聯網時代的今天，網路已經成為現代人生活中不可或缺的「必需品」。當食衣住行樣樣都離不開網路時，音樂的欣賞自然也可以透過網路達成。任何時間、任何地點，在網路的世界裡輕輕一點、一滑，全球各地的演出就能「近距離」的在我們面前上演。除了方便性外，網路提供樂迷視聽感官上更寬闊的向度及節目內容上更多元的選擇，滿足現代觀眾「重口味」與「善變」的需求，且「經濟實惠」的優點更能在經濟不景氣年代，吸走大量樂迷――雖說現場音樂會的臨場感仍然難以取代。以「親民」、「接地氣」取勝的跨界音樂，常遊走於古典與流行之間而能雅俗共賞，再加上劇場化的呈現或與視覺藝術的跨界合作，跨界音樂輕易搭上網路列車，在無遠弗屆雲端世界裡「吸眼球」、「吸耳朵」，遇到知音時還能被下載儲存，反覆觀看。
    </p>
    <p><strong>三、疫情陰霾下的動盪環境</strong></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂藝術之美跟現場感有極大的關係，在演奏家的現場詮釋下，作品能栩栩如生地再現於觀眾面前，與之現場交流、互動。2019年11月在中國武漢爆發不明原因肺炎後，前所未見的重大疫情急速傳播全球，襲捲整個2020年並持續延燒至今。隨著疫情的嚴峻度，各國政府祭出保持社交距離、實名制、禁止公共聚會、甚至封城、鎖國等各種限令，包括演奏廳、藝文場在內的大小密閉空間被休館，而演出場地多為密閉空間的音樂會也被迫取消或延期。此種動盪不安的環境給以現場演出為本體的音樂藝術造成巨大的衝擊，同時也重挫了音樂工作者與相關產業的生計。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年3月起，為了防疫與社會觀感等各種緣故，國內音樂會開始被大幅度的取消或延後，國外的表演藝術團隊及個人藝術家被限縮或禁止入臺。原訂的音樂會在延期時因疫情與經濟等因素，改由臺灣在地樂團、音樂家來擔綱演出。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;爾後，觀眾對於買票進音樂廳、劇場的意願不高，表演團隊轉向錄影演出、線上直播或戶外演出等方式。此外，因疫情衍生的線上教學、線上訪談、線上工作坊等相關製播，也如雨後春筍般陸續湧現。而本就與科技、網路有密切連結的跨界音樂，在疫情陰霾下意外激發更大的跨界潛能，更多不同種類、領域的藝術家們透過影片、網路或戶外的方式跨界合作，在疫情的極度衝擊下逆勢崛起。
    </p>
      <h3>參、2020年跨界音樂實況</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者曾於〈時空交織的絢爛迸發：2018年度跨界音樂活動觀察與評介〉一文中，提出2018年最常見的跨界方式計有：風格上的融合、樂種上的混編、非音樂類的跨界、及科技上的運用等，該年度跨界音樂的發展則有音樂會劇場化、定義界定模糊化、傳統音樂現代化、科技多媒體常態化、及親民在地化等傾向（陳慧珊，2019，1660-1697）；另外，在〈以藝術之名的眾聲喧嘩：2019年臺灣跨界音樂的現象與發展〉一文中，筆者則觀察到2019年的跨界音樂特徵與發展傾向主要有：實用性與功能性的結合、藝術領域間的內部融合與非藝術領域的外向交流、文化的趨同逐異、及科技傳播媒體的興起等，該年度跨界音樂的現象則有跨界思維普及化、大眾親民的推廣與自我實驗的取向、多方雜揉的重新定義、科技的運用與效應、藝術節的推波助瀾、及兒童與親子節目漸受重視等情形（陳慧珊，2020，1910-1928）。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧2020年，雖然因武漢肺炎疫情的肆虐，音樂演出大幅度地被取消、延期或縮編，所幸政府策略得宜、全民抗疫有成，讓不少演出仍能執行。除了延續前兩年的特徵與趨勢外，筆者發現，去年所觀察到的「<span class="bkt">與藝術領域間的內部融合</span>」及「<span class="bkt">與非藝術領域的外向交流</span>」傾向（陳慧珊，2020，1921-1923），在2020年有增加的現象；為方便論述，以下簡稱前者－以藝術內部為對象的融合為「內跨」，後者－以藝術以外、非藝術領域的合作為「外跨」。此外，《臺灣音樂年鑑》針對各類音樂（傳統、藝術、流行等）之間的交越跨涉，將之定義為「跨界」；與多媒體、新媒體等媒材，或戲劇、舞蹈等他領域的結合稱為「跨領域」；結合不同文化、以不同的層面和意義來跨涉者，則歸為「跨文化」（張儷瓊主編，2019，1921-1923）。以下2020年跨界音樂的綜合性觀察，便納入此歸類依據一併參考，<sup>8</sup>亦另整理出表1使探討更為聚焦。<sup
        >9</sup
