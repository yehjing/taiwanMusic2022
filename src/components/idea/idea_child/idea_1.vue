      <h4>三、流行音樂</h4>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;流行音樂有其自成一體的產業結構背景，透過出版包裝、仰賴大眾傳播媒體平臺為其展演媒介。其產生與傳播的過程迥異於上述各種自然族群音樂、藝術音樂創作的發展模式，其內質與音樂語彙亦與其他音樂類型大相逕庭。《2021臺灣音樂年鑑》藉由多角度的觀察，記錄流行音樂相關政策、國內外獲獎情形、民間團體作為、人文生態及出版等面向。
      </p>
      <h4>四、跨界音樂與其他</h4>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統音樂、藝術音樂、流行音樂之外，臺灣社會尚有運用不同素材和技法產生的跨界音樂。諸如：客家歌謠結合流行音樂、南管音樂結合劇場或舞蹈概念、國樂結合爵士音樂、甚或原住民音樂結合世界音樂素材…等，以及隨著資訊和人口流動而產生的新住民音樂。這些都是臺灣音樂的實況脈動，值得加以書寫與記錄。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;關於「跨界音樂」的定義，本年鑑援用「傳藝金曲獎獎勵要點」中的相關條文：「跨界音樂：指風格與表現方式介於藝術音樂、流行音樂、傳統音樂或現代音樂之間，融合科技音樂等不同音樂之素材與技法進行創作或改編之音樂。」<sup>1</sup>除了上述樂種的跨越，跨界音樂實質上更有跨領域、跨文化等交相疊合的音樂現象。意指音樂不僅做為單一項類別之存見，經常是以多元異變的樣貌出現。《2021臺灣音樂年鑑》對跨界音樂的觀察，實際上包含跨界樂團、新住民音樂、世界音樂等範疇，討論這種音樂型態及流動現象，翔實呈現臺灣音樂的時代特質和地域性表徵，突出臺灣音樂與世界音樂的交流對話內容及音樂人文場域流動的趨勢脈絡。
      </p>

      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;事實上，跨界音樂的產生與流布樣態變異之大、內容涵蓋範圍之廣，其分類基準和型態內質目前實難定論。然因跨界現象是當代持續發生、進行中的趨勢，在年鑑中必然要加以記錄。至於因應跨界所產生的音樂內容質變與量變，其表演形式與表演內容的變異程度，則因個案而異。因此本年鑑中將音樂跨界的現象分為二部分加以記錄：其一，在每一子領域的蹲點紀錄表中呈現跨界的標註，提醒讀者各子領域的主體性以及跨界現象持續發生的趨勢。其二，對於那些呈現跨界、跨領域、跨文化的音樂活動，另設專章記錄與觀察，即時地呈現音樂跨界的實況。
      </p>
