      <h3>捌、編輯凡例</h3>
      <p>
        一、本文鑑的文獻引用方式，參考美國心理協會（American Psychological Association）所發行的 APA 格式（APA style）。
      </p>
      <p>
        二、若於論述中需引用音樂的採譜紀錄，在實際具有節拍理論的音樂類型中，採用西方音樂理論以小節線（實線）來表現音樂節拍（meter）的概念，至於並不具有節拍理論的樂種，則以虛線標示出樂句的分割方式，而每個音的節奏（rhythm）與樂曲整體速度（tempo）的記譜方式，則與西方記譜法相同。
      </p>
      <p>
        三、有關音高標示的補充說明<br>
        （一）在音樂譜例中，欲表現樂曲的實際音高時，採用西方音樂文獻通用的標示方法「CC&ndash; C &ndash; c &ndash; c' &ndash; c'' &ndash;  c'''」（great/smallmethod），其相應的音域如下列說明：
      </p>
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c' = 中央C</span><br />
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a' = 在中央C 之上的A</span><br />
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c'' = 較中央C 高一個八度的 C</span><br />
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c = 較中央C 低一個八度的 C</span><br />
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;C = 較中央C 低兩個八度的C</span><br /><br />
      <p>
        （二）在文字敘述中，一律以唱名來表示相對的音程關係，而不強調固定音高，比如以「la-mi-sol-mi」代表「完全四度下行－小三度上行－小三度下行」的音程關係；如有必要，則附加括號，顯示絕對音高。
      </p>
      <span>四、音樂譜例在樂譜開頭處的各項標示，說明如下: </span><br />
      <span>（一）正中央為曲目名稱，此名稱乃是演唱（奏）者所告知的曲名或是歷史錄音的出版名稱。</span><br />
      <span>（二）左上方的標示，包括首音的實際音高、樂曲的類型、以及曲調的類型。後兩者乃是依據本論文提出的分類原則而獲致的分類結果。</span><br />
      <span>（三）右上方的標示，是有關參與人員以及時間、地點的資料。 </span><br />
      <span>（四）速度的標示，放置於第一行樂譜的譜號上方，而每當速度改變時，會於該樂段起始處重新標示速度。</span><br /><br />
      
      <span>五、符號之指定用法：</span><br />
      <span>（一）文字部分</span><br />
      <div class="pa4">
        <span
          >書籍、期刊、學位論文名稱、影片、唱片專輯，以《》標示。</span
        ><br />
        <span
          >單篇文章、期刊論文、研討會論文以〈〉標示。英文書寫方式則依據APA格式。</span
        ><br />
        <span
          >短引文、計畫案名稱、團體、活動、主題等，以「」表示，然年鑑的書寫會大量提到團體名稱，因此除行文上需強調團體名稱外，一律不加「」。</span>
      </div>
      <br />
      <span>（二）音樂部分</span><br />
      <div class="pa4">
        <span
          >專輯、劇目、套曲、藝術（當代創作）音樂作品名稱，以《》標記。</span
        ><br />
        <span>單曲、樂章以〈〉標記。</span><br />
        <span>曲牌、唱腔、門頭、素材以【】表示。</span>
      </div>
      <br />
      <span>六、時間相關格式</span><br />
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在文章中提及世紀、年代、年月日採用西元紀年，以阿拉伯數字表示。然需標示出年號者，以西元後方的括號補充說明並以國字表示，如西元1811年（嘉慶十六年）。若必須表示民國時，以西元後方的括號補充說明，如1982年（民71）。在表格中的活動日期表示格式為2011/01/01（/ 為半型）；連續日的表示格式為01/01-12/31（-為半型）。時間表示格式為14:00-19:30。
      </p>
      <p>
        七、有關原住民族語的拼音，皆統一採用教育部與行政院原住民族委員會於2005年（民94）12月15日頒佈之「原住民族語言書寫系統」（台語字第0940163297號、原民教字第09400355912號）中，所運用的拼音方式。
      </p>
