      <h3>肆、資料蒐集方法與來源</h3>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑計畫以田野調查以及文獻資料參考兩種研究方法為主，另輔以文獻分析法、問卷調查法等。田野調查方法主要用於活動觀察紀錄、年度活動影像。文獻資料類型與來源參考，則主要用於活動月曆、重要音樂活動一覽表之內容資料蒐集。
      </p>
