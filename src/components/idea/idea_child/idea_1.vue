      <h3>壹、編輯理念與目的</h3>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由國家號召開始進行的臺灣音樂年鑑編纂，始於2009年。當時的年鑑編纂，從2009延續至2012年，名為《臺灣傳統音樂年鑑》，以傳統音樂為範圍。年鑑中所指的「傳統」，為臺灣原生且非殿堂，即非音樂廳、戲劇院內專業展演的民間音樂形式。所包含的音樂類型，粗分為漢族音樂、原住民音樂、世界音樂三大類。在漢族音樂之下，又再分為福佬音樂（含括南管系統音樂、北管系統音樂、歌子系統音樂）、客家音樂（含括北部客家音樂系統、南部客家音樂系統）、宗教音樂（含括佛教音樂、道教音樂、法教音樂）；原住民音樂為14個當時被認定的原住民族群，和西拉雅族、巴宰族等兩個平埔族群的音樂；世界音樂部分，則視當年度國際交流展演情形而編纂。蒐集內容包括該年所發生的音樂事件，例如表演、傳習、出版、研究等。但文化場部分，也就是專業場所的展演，則並非當時年鑑所觀察的對象。至於成果部分，乃由各領域之專家撰寫觀察評論文章，出版紙本與電子書，紙本年鑑並附有田野調查影音紀錄DVD。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;年鑑紀錄調查，曾因認為每年內容變化不大而中斷了5年，但在文化部2018年提出「重建臺灣藝術史」的政策推動下，第二期年鑑再次於2018年，以前瞻基礎建設經費啟動，並延續至2021年。此次的調查範圍，擴及至當年所有民間與殿堂之音樂活動，因此在範圍上，除原有的傳統音樂類別以外，新增了「藝術（當代創作）音樂」（含括「國樂」與「西樂及混合編制」），以及「流行音樂」、「跨界音樂與其他」三大類別。即使傳統音樂，也於2020年新增釋奠樂的內容，主要針對各地的祭孔音樂，亦包涵民間宮廟系統的儀式音樂。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民音樂的部分，第二期年鑑則新增了2014年才正名的拉阿魯哇族與卡那卡那富族，平埔族音樂的調查範圍也被擴大。整體設計上，增加了音樂活動月曆的書寫，提供全臺不同地區的音樂活動狀況一個全盤的視野，在音樂內容與信息量上因此有較大的擴張，名稱也改為《臺灣音樂年鑑》。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第三期年鑑從2022年度開始，除延續第二期年鑑的分類與內容，並擴充展演團體的基本資料調查：本年度主要進行了南管音樂與北管音樂的調查，以及規範專文的書寫體例，並注重資料範疇、取得方式與研究方法的陳述，進行了個別領域中的年度數據統計與觀察。
      </p>
