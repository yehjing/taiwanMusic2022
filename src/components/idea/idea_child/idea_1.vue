      <br>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第一個面向為傳統音樂，指的是第二次世界大戰結束的1945年以前，產生於臺灣，或已在臺灣盛行很長時間的音樂型態，包括漢族音樂與原住民音樂。漢族傳統音樂因其不同的語言、表演形式、音樂型態等，又可分為南管音樂、北管音樂、福佬音樂以及客家音樂。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於原住民音樂，則因每個族群語言與風俗各具差異，而形成特有之音樂風格。而除了國家認定的16個原住民族群，2022年度年鑑，還包括平埔族群的音樂，本年度的平埔族群調查，包括馬卡道族、大武壠族、西拉雅族、噶哈巫族、拍瀑拉族、巴宰族等6個平埔族群。由於能統領多領域研究範疇的學者專家有限，因此本年鑑依照各個學者研究的專長與屬性，將16個族群與平埔族歸納入3位負責學者手中，由各負責人再與在地觀察員相結合，以獲取第一手的資料。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外傳統音樂中，還包括宗教與祀典音樂。此二者因為表現場所與形式的特殊性，在本年鑑中也以其特殊類型，被歸屬為傳統音樂面向之下的一個專有類別。在此細分為佛教、釋教、道教、法教、釋奠樂等加以敘述。其中釋奠樂部分，每年在臺舉辦時間固定且短暫，在此謹以專文論述，不另行進行其他子領域所有的分項調查與月曆製作等部分。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了傳統音樂領域，藝術（當代創作）音樂所屬的國樂與西樂，佔據了音樂廳演出的一大部分。此領域本年鑑除了報導當年度的演出、比賽、傳習推廣、研討會、出版、政策等訊息，並對本年度首演的國人作品，進行了首演日期、作曲者、曲名、首演單位及個人、補助及委託單位列表，以利後人的查詢與參考。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外流行音樂屬於擁有許多歌者與觀眾互動的音樂類型，雖然近年疫情，使其展演型態轉變，但其音樂生產、表現與傳播形式，也與其他音樂面向多有差別，因此與藝術（當代創作）音樂相同，都被單獨分出。在這個篇章，我們可以看到喧嘩的演唱會、大量的專輯與單曲出版，以及政府在相關政策上的推動。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於當今各種音樂型態，多所融合與交互應用，因此跨界音樂類型也單獨歸成一個面向，不但呈現了不同樂種以及音樂與其他領域的相互交越，進而同時觀察世界音樂在臺灣的發展情形。其所收錄範圍，包括不同樂種、不同表演藝術領域、不同人文領域、與科技媒材共製，或創演形式上的跨界。內容豐富多元，雖與其他領域有可能重疊交錯，但藉由此領域專家依照跨界比例進行選擇，讓我們能見到本土音樂的多樣變化。
      </p>
