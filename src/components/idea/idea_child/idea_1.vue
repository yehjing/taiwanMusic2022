      <h4>二、藝術（當代創作）音樂</h4>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術（當代創作）音樂發生場域包括了精緻的舞臺表演場所、或是藝術節、小眾表演場地…等非傳統樂種的活動場域。本領域著重觀察當代藝術音樂創作的活動，從局內、局外、遵循傳統、發揚創新等多角度、多層次的涵蓋，希能完整記錄一整年臺灣音樂創作的脈動，並提出相關的評述及觀察。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術（當代創作）音樂以藝術原創性為訴求，分為國樂、西樂及混合編制兩項目（如表2），此分類係依產生和傳佈的人文背景範疇作為區分，亦依所使用的樂器和音樂語彙做區分。分別以國樂器（含人聲）以及西洋樂器或混合編制（含人聲）做為音樂表現的載體，演奏（唱）具有原創性的當代作品，特別著重觀察國人作品以及臺灣傳統音樂素材在此類藝術創作領域中的應用、改寫與再製情況。
