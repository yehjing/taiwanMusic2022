      <p>
        二、若於論述中需引用音樂的採譜紀錄，在實際具有節拍理論的音樂類型中，採用西方音樂理論以小節線（實線）來表現音樂節拍（meter）的概念，至於並不具有節拍理論的樂種，則以虛線標示出拍子的分割方式，而每個音的節奏（rhythm）與樂曲整體速度（tempo）的記譜方式，則與西方記譜法相同。
