      <p><strong>二、主體內容</strong></p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑主體內容部分，各子領域將依照以下7個主題進行觀察。</p>
      <p>
        <strong>展演活動</strong
        >：包括音樂會、遶境、藝術節、音樂季等該領域的慣常展演模式與場域之活動。
      </p>
      <p>
        <strong>祭儀活動</strong
        >：包括儀式、祭典等具有祭祀、儀式、信仰性質的活動，以及館閣的慶祝活動。
      </p>
      <p>
        <strong>推廣教育</strong
        >：包括講座、展覽、電視節目、音樂比賽、檢定考試以及具推廣性質的音樂會（如社區音樂會）。其目的是給非原本此領域的聽眾，在非原本慣常的演出場合中所進行的音樂會。
      </p>
      <p>
        <strong>傳習活動</strong
        >：包括工作坊、大師班、研習班、寒暑期傳習營等，以及館閣的例行練習、拜會活動等。主要指該領域的耆老或專家，以一日、短期或長期的形式，將自身在該領域所承襲之音樂技藝，傳習予後進，具有世代相傳意義的活動。
      </p>
      <p>
        <strong>國際與兩岸交流</strong
        >：指臺灣音樂人才前往國外，或外國人前來臺灣進行的音樂交流，都屬此類。
      </p>
      <p>
        <strong>研究與出版</strong
        >：指具公開宣讀、出版之專門研究成果或作品，包含期刊、學位論文、會議論文、網路文章、專書、專輯等。網路平臺以公家/財團法人/藝術基金會等具公信力之官方網站，經編輯審閱之文章為主。
      </p>
      <p><strong>文化政策</strong>：指政府政策以及與行政有關之音樂事務。</p>
