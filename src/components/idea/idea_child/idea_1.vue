      <h3>伍、限制</h3>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次年度音樂年鑑編輯計畫，由於起案時間較晚，開始於當年度6月底，為了取得年鑑的整年度內容之平衡性，因此前半年的活動，除了各子領域團隊在文獻資料（包含網站訊息）中尋覓爬梳，各領域專家更以其長期在此領域上的研究參與，進行回溯式的記錄。雖因疫情的關係，上半年度的表演活動也較為有限，但起案較晚，仍錯失了許多活動觀察的可能性。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本研究的另一項限制，在於經費與人力的有限性，致使許多活動，觀察者本身無法親臨現場。尤其當同一活動在不同地區同時舉行，更因每個領域的人力分配有限，無法全部參與。加以有些活動訊息，並無公布於網路上，尋找起來更為困難。只得借助學者專家與助理在各自領域長期耕耘的人脈關係，獲得相關訊息。但訊息浩瀚，仍不免有所闕漏。因此本年鑑中各項的統計數據，無法達到完全精準的程度。不過仍希望透過一個概略的輪廓，看到當年活動或事件的約略梗概。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另2022年度開始的南管音樂與北管音樂團體基礎概況調查分析，為在歷來臺灣音樂年鑑中首次的嘗試。但這些歷史較為悠久的傳統音樂團體，長期以來有著其自我運作的模式。今日一些統計數據的既有調查結果，其多針對經常演出的藝文團體。但南管與北管音樂團體，在人力結構與經費運作部分，以及接受調查的意願，都與傳統既有調查團隊上，明顯有很大的差別。因此整體上工作進行並不容易。除了因南管與北管本身音樂性質所造就的團體差異性，調查者如何獲得傳統音樂團隊的信任，如何讓團隊願意告知其真正的困難性，以及團隊本身對於政府單位的信任感，都可從調查結果一窺端倪。
      </p>
      <p>&nbsp;</p>
      <h3>陸、工作流程</h3>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年度年鑑的工作流程，可區分為兩個階段，分別為「調查研究」階段及「編輯出版」階段。第一個階段，各子領域專家學者與助手，對其所指定之領域進行全面的調查。2022年底，調查研究階段結束，各子領域專家學者開始進行專文撰寫，進行整年度的資料整理，其中包含對於每日年度活動記載的月曆，針對較具特殊性與代表性的活動與事件之記錄報導，以及針對相關領域的團體與個人建立名錄、相關領域的重要音樂活動一覽，每個子領域呈交4支各3.5分鐘的活動影片等。
      </p>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在經過一個月時間的專文撰寫與資料編輯，經過統整、校對、審查委員審查建議等工作後，正式進入第二階段的「編輯出版」。在此之前，編輯出版工作已開始進行封面照片的挑選與影片的剪輯。在審查委員審查過後，一方面進行相關修改，另一方面則開始進行內容的排版。排版結束後，進行兩次的校稿，之後交予網站製作，排版部分則進入印刷程序，並在7月正式出版發行，網站也將架設完成。
      </p>
      <p>&nbsp;</p>
      <h3>柒、效益</h3>
      <p>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一本年鑑有如一支萬花筒，一年中的一切事物，都透過這萬花筒一覽無遺。臺灣音樂年鑑的編纂，記錄了臺灣音樂的發展樣貌，給予社會各界一個了解臺灣音樂活動的平臺，且可作為國際交流、研究之資料。透過多年持續的調查，我們也可以得到一個歷時性的觀察維度，來考據臺灣音樂的變化軌跡，並看到臺灣音樂文化發展的縮影。為日後臺灣的音樂研究，提供完整的歷史資料，並藉以預測相關趨勢，作為文化部門釐定政策之諮詢與鑒戒。
      </p>
