        （一）在音樂譜例中，欲表現樂曲的實際音高時，採用西方音樂文獻通用的標示方法「CC&ndash; C &ndash; c &ndash; c' &ndash; c'' &ndash;  c'''」（great/smallmethod），其相應的音域如下列說明：
      </p>
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c' = 中央C</span><br />
      <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a' = 在中央C 之上的A</span><br />
