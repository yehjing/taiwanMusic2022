<h4><strong>八、臺灣牽亡歌陣的民間習俗與其宗教氛圍</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的「牽亡陣」，或稱「牽亡歌陣」，多出現在喪葬場合中，一般由孝子、孝媳或孝女、女婿延聘，並由紅頭法師、尪姨（或稱倒退）、老婆、小旦與樂師所組成的一個5人組團隊，進行帶有宗教性、說唱性與歌舞的一種儀式性表演形式。所使用的法器，一般有：龍角、帝鐘、法索、奉旨（拍板）、烏鑼、及木魚；樂器的部分有叩仔、小鼓、三絃（或電子三絃）、四塊（敲仔，由兩個竹木片成一對，左右手各持一對）、月琴、大廣弦來伴奏，近年來也有以電子琴取代。法師與樂師由男性擔任，其餘3位為女性。「牽亡歌陣」依其目的或意義，可以分成5個時機進行：一、出殯前日傍晚，目的在「超渡慰靈」；二、出殯當日遷棺前後，目的在「引魂出山」；三、出殯途中，目的在替亡魂開路關；四、下葬前，帶有「送魂上仙山」的意義；五、返家途中，目的在引魂回家。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於歌陣儀式的流程，團隊之間的執行大同小異，一般包含：開壇－香白（請主名單）、調請五營馬兵護衛、引魂、安慰亡靈、勸亡、啟程、過三十六關（開路關）、入五嶽、遊十殿、遊十二月花園、六角亭（觀音佛祖亭）、拜觀音、奠酒、唱目連經、辭神等。過程中視需要有時會再添加：挑經與過橋。演出分責部分，大多由法師主咒語、主唱，其他3位角色對答、輪唱或齊唱；「開路關」部分通常以一關為一段，每段大多先對答後對唱。唱與說白內容多勸人為善，且以押韻的口語化之「七字歌仔」（即白字歌仔或四句聯仔）為主；再配以伴奏，或歌或舞，或獨白或對答，相當緊湊繁複。音樂表演風格有融合臺灣唸歌和車鼓民間小戲所形成。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於「牽亡歌陣」的緣起，據多位學者與陣頭藝師的說法，「牽亡歌陣」可能於民國初期，至今約100多年前由閩南流傳的「落陰歌」傳入臺灣，之後結合說唱、戲曲或藝陣之表演，以宗教之「薦亡」儀式為載體，而轉化為「牽亡歌陣」喪葬儀式，其中吸收車鼓陣、老歌仔戲之踏謠表演形式。<sup>1</sup></p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀2020年世界性武漢肺炎所帶來的新冠病毒所造成的一場世界性災難的浩劫，影響不能說不深遠。臺灣可以說在這場疫情中，因為防疫有成，是不幸中的大幸。從年初疫情的開始，大家心存觀望、謹慎或僥倖，到疫情嚴峻的惶恐時期，隨著疫情的變化，宗教活動也明顯反映出當時的社會氛圍。我們看到疫情嚴峻的恐慌期，特別是在今年的2-5月期間，眾多廟宇舉辦袪瘟祈福法會，或8月以後，部分的法會或研討會在妥善的防護措施下，小規模或以視訊來舉行；這些都反映了疫情從年初到年底的高低起伏期，影響著臺灣社會信徒的宗教活動行為。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度道法教音樂活動的觀察，記錄了因為疫情的關係，深深影響了廟方原先活動的規劃，不得不從法會內容、規模或屬性等方面進行調整。期待2021年世界性與臺灣的疫情得以受到控制，讓宗教活動回歸到民間生活自然的律動與發展，信徒也不再處於瘟疫的恐慌之中。</p>

