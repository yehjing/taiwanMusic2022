<h4><strong>二、離島的扛乩祭祀文化－馬祖連江縣北竿「擺暝文化祭</strong>」</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;馬祖連江縣的「擺暝文化祭」宗教習俗文化活動，於每年農曆元宵節前後舉行。馬祖各島上的各宮廟分別於農曆正月8日至2月2日（西曆2月1日至2月24日）擇日舉行。今年（2020）的文化祭典於2月6日- 8日在當地的北竿熱鬧舉行。在這個大型宗教活動期間，我們看到了當地以扛乩遊街作為活動主軸的特色，其氣勢有雷同於臺灣宮廟神將出巡的壯觀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;位於連江縣北竿島的塘岐蕭王府廟是本次宗教民俗活動的主要負責宮廟，也是此次各項活動的宗教展示點。蕭王府廟建於1977年，2004年重建竣工落成。該廟源自泉州，當地稱「富美宮」，是閩南人的信仰神祇。至於北竿地區的傳入，來自原籍泉州惠安的漁民周蒙氏，立足於塘岐後，從自家祀奉的家神，成為當地鄉民共同信仰的區域神祇；此外也因蕭王府廟所開創的扛乩信仰文化，擴散至整個北竿島各宮廟，獨樹一格，成為其他馬祖島嶼所未見的閩東信仰文化的變遷。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;馬祖名稱的源由，根據清初《使琉球記》中記載，宋朝福建湄洲的孝女林默娘（人稱媽祖），28歲時，因父兄駕船駛至閩江口海域，突遇巨風大浪，船毀人溺，媽祖得知，飛身入海拯救父兄，因而溺斃罹難。媽祖遺體當年隨海漂流至閩江口附近的小島（即今日馬祖的南竿），被漁民打撈上岸，並將媽祖埋葬在海岸邊。湄洲鄉親不見媽祖下落，認為她羽化昇天成仙，遂建湄洲媽祖廟作為紀念。馬祖人則認為，媽祖葬於現今馬祖南竿馬港天后宮宮內的靈穴石棺中，且興廟供奉，相傳至今。這個島因而被稱為「媽祖島」，爾後又改為「馬祖」。馬祖地名因此而來，媽祖也成為馬祖居民最重要的信仰神祇。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「擺暝」是福州話，白話文念「北漫」，意指晚上在宮廟內擺設好供品祭神、酬神的儀式，即夜間擺宴供品以祭神。擺暝，是馬祖全境的元宵節重要傳統祭儀。這種儀式源自於早期福州一帶的農村，農民於元宵節期間舉行迎神活動，境內各地宮廟的角頭神祇被信徒抬出繞境，巡視，祈求地區境內平安。這是一個比華人過年更隆重的慶典。擺暝的慶祝方式，各鄉、各村也不同，例如北竿最大的特色是扛乩，由4名乩將（或稱乩腳）扛著一頂巨大的神轎遶境宮廟、街道以祈福。在北竿，各村莊大大小小廟宇都有自己的神轎，全鄉數十頂神轎列隊一同遶境，穿梭大小街道，共襄盛舉。很多壯漢信徒也多是宮廟長年來的乩將，從年青到年長不中斷；儘管平時離鄉在臺灣工作，也不忘此時回鄉扛乩，服務神明。慶典席間白天、夜晚，鑼鼓喧天價響鞭炮聲隆隆，長串的煙火沿街施放，場面極其壯觀。馬祖的四鄉五島全境都有擺暝活動，只不過時間點不同，約在農曆正月11至15號，北竿芹壁村龍角峰甚至是農曆正月29號舉行。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北竿島坂里村的擺暝是燒馬糧，主要會場在坂里大宅以及它前方的廣場。當天的坂里大宅，裡頭祖廟會暫時做為白馬尊王進駐之處，北竿的神轎都會到此致意。由4位乩將共同扛著神轎搖擺前進，甚至不停地旋轉，步伐一致、鏗鏘有力，兩神轎相遇時會互相搖擺點頭（打招呼之意）、再貼近一同旋轉，人性化的神祇，顯露無遺；無止境的鞭炮聲不絕於耳，白色煙塵籠罩在神轎之上，彷彿神轎行進於雲霧之中。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在傳統民俗文化活動逐漸被遺忘、消失的年代，2019年8月「擺暝文化祭」被文化部登錄為「重要民俗」活動，並於今年2020年2月元宵節活動期間，由文化部政務次長蕭宗煌於8日赴連江縣北竿鄉塘岐村，親送登錄認定證書；而這也是連江縣第一個獲此榮銜的無形文化資產。馬祖四鄉五個島上的79座宮廟中，有68座辦理「馬祖擺暝」活動，是馬祖人最隆重的傳統宗教祭典。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次北竿島上的宗教文化祭活動，特別以蕭王府廟為中心的祭祀活動，多元而熱鬧；其中特別是當地廟宇信徒所呈現的扛乩活動，令人目不暇給、印象深刻。蕭王府廟除了廟內豐富的祭品與供品的擺設之外，廟外廣場亦呈列了有關擺暝文化祭的文字資料與畫面、迎神繞境的多種神偶、神轎、傳統中藥材的象徵性展示與說明等。此外動態的相關活動，如在繞境的前一天，由保長公手持酒壺與青葉竹枝，腳踏醉步，領數名村丁，扛「清道旗」敲鑼開道，沿路張貼「清潔街衢」黃（紅）紙，巡行村境，進行清潔道路任務，通知孤魂野鬼、閒雜人士等迴避，另一方面在天亮時巡視路線、避開危險路面，以備次日遊神隊伍平安出巡。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;擺暝期間，村內眾多神明起駕出巡繞境，馬祖人稱之為「迎神」。下午時分或入夜之際，眾多神明起駕出巡繞境，陣容相當盛大，民眾沿街祭拜，在鼓板樂的鑼鼓喧天、鞭炮隆隆聲中，各村的元宵酬神活動因此拉開序幕。出巡隊伍，由保長公開道，雙手各持酒壺與青葉竹枝，腳踏醉步，一邊以青葉竹枝拍擊馬路作打掃狀；接著七爺、八爺開路，其身上掛者一串串的戚光餅，沿途讓人摘食；其後接著孩童模樣的神偶；馬奴（為神明的座前騎，鑾轎要起行時，馬奴會在前面躍步引導），緊接著花燈隊、香爐、神轎及鼓板隊、信眾。鼓板隊則沿途敲鑼打鼓，為熱鬧的遊行隊伍助興。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北竿島所呈現的宗教習俗「扛乩」是馬祖地區較為特殊的信仰文化。神明出巡活動隊伍中，每家廟宇的乩將（當地稱為「乩腳」），抬著乩轎隨行，沿途鼓板隊的鑼鼓喧天價響，好不熱鬧。在廟內所供奉的祭品或供品，則於「擺暝」祭之當日或次日，共同委託烹煮，在廟內或廣場由參加的社友或各家各戶的代表食用，稱為「食福」。整個祭典期間，大街小巷，特別是廟口，掛滿了馬祖風燈，因其形體類似類似馬祖酒廠的酒罈，故稱酒罈燈；象徵光明、去除黑暗，指引海上漁民返家之路。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於馬祖「擺暝文化祭」在全程的祭祀或迎神繞境的活動中，主要多由鼓板隊來助興，稱「打鼓板」，這是當地特有的民間樂隊組織，每個宮廟原則上都有自己的鼓板隊及特色，有些傳承已數10年，這幾年來逐漸培養青少年來接班。鼓板隊的編制可達數10人，亦有組成2組鼓板隊，以一前一後的穿插，安置在繞境隊伍中，各自敲打自己的節奏，其隊員可不分年齡、性別，男女老幼皆可參與活動。擺暝期間，從上彩暝掛風燈、排祭，再到迎神遶境，神轎與陣頭巡遊街頭巷尾，最後以送喜、食福圓滿結束，過程中各自宮廟的鼓板樂隊、扛乩隊，以祈福等元素充分顯現敬天酬神、社群整合、祈福求安等文化意涵，展現各庄頭社群一體性及強烈認同感，也突顯在地居民的團結凝聚力與深厚的感情。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，在2月7日主辦單位也安排了一齣音樂劇，其內容呈現與馬祖人世代隔閡家族情懷與當地馬祖人的民俗特色－「相約15暝」，其中由當地人所客串演唱，非職業演員的音樂劇，從對白內容到音樂的唱作時時扣人心弦，反應馬祖人心中所掛念的人、事、物。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;神轎鑾駕是馬祖當地信徒恭請神明降駕為信眾解危難，排疑惑的媒介方式之一種。至於「蕭王爺」之神轎請鑾，可回溯民國初年，適時，「蕭王爺」之信仰普及，最初是用擲筊方式，居民多感不踏實，乃請示「蕭王爺」，是否可找一信徒為乩童，降駕代言，王爺表示不可，因王爺降駕必刀斧加身，人必不能承受，爾後幾經研議，用竹製小靠背椅，兩側綁兩長竹竿，再以2隻小竹竿用繩絞緊，4人似抬轎狀，由1人為桌頭以黃紙錢火化揮動或唸以口訣，請神降臨登鑾駕，竹轎啟動左右進退至靜止，表示神尊已降臨；並請示神明身份，回示問題多畫符號，由桌頭來猜，對、錯，竹轎以進或退為表示。唯竹轎輕而不牢固，每次有請神明問事，必損壞一張竹椅，長期以來，竹椅之毀損數量相當驚人。至民國30年間，有袁忠善公（成為後來的廟內神祇－袁書記），仿竹椅用木材設計出第一張木製神轎（但此轎已不復存在）。現存廟中最早之神轎，是在那之後，由一位聾啞鄉民王恆炳，所加以改進至做出一臺木製神轎（現存於桃園「蕭王府臺灣行宮」王吓顯先生處）。現有之鑾駕均由此衍生而出。從而逐漸發展出現今北竿島各村特有的扛轎文化。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前該廟內所供奉的神祇有：中壇元帥三太子、南京先生、蕭王爺夫人、蕭王爺、金王爺、雷王爺、袁書記、丁將軍、臨水夫人、蓮成佛祖；其中的中壇元帥三太子、臨水夫人，是臺灣法教信仰所熟悉的；其他的神祇則與當地信徒較有關係的特色；如蓮成佛祖、蕭王爺、金王爺則於1909年由開基的信徒周猴彥從福建惠安所帶來的。</p>
