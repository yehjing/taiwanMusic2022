<h4><strong>四：北投代天府「慶祝五府王爺聖誕千秋曁護國祈安植福吉祥法會」</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北投「代天府」於2020年8月4日舉行上述的法會。代天府原為北投侯亭陳姓族人的家廟，後來逐漸發展成為附近當地人的共同信仰中心。其正式建廟於1974年至今已有46年的歷史。據陳氏相公支脈開臺第八代的陳浩泰先生（1949-）與第十代母舅的簡錦祥（1957-）在二人合著的一書中《異地血脈－記述北投陳氏於北投之開發》（2014），指出其開臺祖陳江，於1755年（乾隆20年），時年34歲（66歲時歿），來到北投，受到當時已在北投侯亭陳氏家族之助，在臺逐漸發展事業，並繁衍子孫，至今可能有20代以上的後輩。陳氏家族祖籍為泉州府同安縣馬巷廳十二都翔豐里，封侯保山後亭鄉人。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳家開臺祖，陳江於1755年渡臺至北投後，開始開墾，設立「陳柏記」商號，供奉五府王爺（李府、池府、吳府、朱府、范府）並納平埔族人為妾（取漢名林蘭官，很可能是當時還存在的猴猴族人，從當時文件上的契約所載）。1760年陳江於當時清廷海禁法令稍懈之時，返回馬巷原鄉，接其母、正室（洪寶官）及四子來臺定居。「陳柏記」商號成立初期，以收購平埔族人所獵獲動物之獸皮、臟器再行轉售，另出售酒類、雜貨米糧，客源以平埔族人為主。當時狩獵以猴類、山羌為主，經烹煮後，製膠以入藥，即所稱之猴膠、羌膠。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;據主方陳浩泰訪談時（2020/8/4）言，當時所供奉的是池府王爺（陳與簡一書中，則寫伍府千歲。據陳言，「伍」乃為錯別字，但因沿用許久，故未改之），是陳江從泉州家鄉攜帶來此。此五府千歲則供奉於「陳柏記」商號廳堂。在日後眾多子孫後代的情況下，各有屋堂，逐輪流供奉。日治時期，在皇民化政策下，日警察諭令所有神像必須焚燬，因此輪值之族人，將神像藏於土角厝內壁，並外遮蓑衣，以避日警搜查。1961年五府千歲成為北投地區居民之信仰神祇，每年農曆6月15日五府千歲聖誕，舉辦演戲酬神，宴請賓客，為北投一大盛事。1974年陳氏後代陳永章，有感於五府千歲為陳氏開臺祖奉請渡臺之神祇，亦是陳氏家族之保護神，逐籌資購地建廟於周氏節孝坊之鄰，也因此成為陳氏家族與北投開發史之重要佐證。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於當天負責聖誕及祈福法會的道士團，是由來自桃園市大園區竹圍里的正一「廣盛壇」。比較少見的是，此「廣盛壇」到今年為止已有39年從未中斷地替代天府負責法事科儀。據目前擔綱的年青壇主曾慶峻（1982-）道長言，之前長期由其父親曾和欽（1946-）道長負責。這是一個典型的道教家族傳承。曾慶峻道長從國小三年級開始打鑼鼓，中原大學資訊工程系畢業，退伍後正式學道，吹嗩吶、打鑼鼓；也到過保安宮北管社團學習北管。目前以接班父親高功的工作。曾和欽在未當道士之前為補運師（法師）。「廣盛壇」祖籍為廣東省嘉應州，長樂縣（現改為梅縣），在當地曾氏即為道士家族，其第十七代來臺，曾和欽為第二十六代；換言之即來臺之後的第九代，曾慶峻則為來臺後的第十代。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北投代天府當天的慶祝活動「五府王爺聖誕千秋曁護國祈安植福吉祥法會」是北投代天府幾十年來的例行法會，而且也都是選在同一天舉行，即農曆的6月15日，由同一個道士團負責科儀的進行，尚未間斷過。據陳氏家族的陳浩泰先生言，舉辦法會其實有3個真正的原因：</p>
<span>1.紀念先祖來臺拓墾200多年的事蹟；</span><br>
<span>2.紀念周絹女士（陳先生之太祖母），因早年嫁到陳家後守寡，但未改嫁，反而替陳家撐起「陳柏記」商號的一片天，陳氏家族感恩於她，並在廟旁立了一個牌坊。</span><br>
<span>3.紀念陳家先祖攜帶來臺的保護神池府王爺聖誕千秋（但是我們看到當天道壇的神祇，也是五府千歲並列，但池府王爺置中）。</span><br>
<p></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;據曾道長言，當天的法會，可以稱為一朝三獻，即一朝法會之意，以行內人的術語稱一朝空殼，而不能稱一朝正醮；如果是正醮的話，則必須安排更多的科儀在內，作為一個紮實的儀式內涵。至於後場樂師的部分，共由4個人在支撐整天的法會，鑼鼓部分，由2位年青的樂師專職，偶而會有曾道長及其他的道士來更換；但文場部分則由2位頗有經驗的壯年樂師負責。他們通常自幼開始學北管，打好各種基礎，再熟悉道場儀式轉換及後場音樂的需求，必須能及時反映當下所需的音樂，判斷需使用哪種過場樂及相對應的曲目，經由長期累積經驗及很好的音感判斷力，方能妥善的配合前場道士的唱韻。後場使用的樂器有嗩吶、殼子弦、電子琴、多個鑼鼓、叩子，及前場的鐺子等。當天除了道教科儀法會的舉辦之外，也如往例，請了一班歌仔戲團來演出，有下午場到晚上場，提供神、人共賞的娛興節目。</p>
