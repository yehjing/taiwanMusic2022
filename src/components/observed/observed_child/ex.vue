<h4><strong>三、臺北府城隍廟</strong><strong>2020</strong><strong>年中元普度大法會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北府城隍廟於今年8月24日舉辦的中元普度大法會，正值夏天疫情稍微緩和下來，增加民眾外出參與公共活動的機會大增，故很多之前延期辦理的法會，也紛紛舉行，特別是普度法會部分；但是不少民眾還是戒慎恐懼，主辦單位也遵守官方單位所規定的事項，在大眾場合戴上口罩。所以我們也看到嗩吶樂師，很技巧的戴上挖空口罩，來吹奏。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次中元普度大法會實際負責的團隊是基隆廣遠壇李游坤（1950-）道士團，也是北臺灣一個知名的林厝派道士團。近年來道士團道務的工作，逐漸由其獨子李一興道長挑起大樑。李游坤道長法號玄正，是李松溪道長（1929-2018）的哲嗣。李松溪道長（道號通訊）於1984年在基隆安一路開設的「廣遠壇」外，並又於1997年成立了「丹心宗壇」，二者合稱為「基隆廣遠壇．丹心宗壇」。此廣遠－丹心壇主要的工作，除了服務各地宮廟的一些道教法會、神誕節慶、禮斗、作醮、安太歲、開光安神、祭解、補運、收驚等道法二門的宗教任務外，多年來也一直不忘栽培後進，包括有興趣的社會人士與學者。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;李游坤道長是中國醫藥學院藥學系畢業生，並從事藥劑師工作多年。在父親道長等因素影響下，於三十之年才開始從師爺學習道教相關事務。並於往後的10年，將師爺的工夫一一學了起來，且加以精緻化、道學化，成為中生代的第一人。40歲開始教學，傳授門生，如今已過了20多年，所教之弟子超過千人。力求上進的心，讓李道長考上輔仁大學宗教研究所在職班就讀，並已於2011年6月取得該校的碩士學位，成為第一位具有碩士學位的道長。李道長多年來是臺北府城隍廟、東湖北天宮、丹心宗壇的住持，並且帶領弟子在這3個駐點從事道教法務與教學的工作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當天舉行一朝三獻中元普度法會科儀的程序，在上午先舉行三獻發表、啟請神祇、慶讚中元（拜天公）；中午過後約13:00，犒軍儀式（由該團一名道士身著道袍，進行犒軍科儀）、前場道士接著唸誦中元寶懺；另外巡宴部分，也是該團歷年來所持續進行的一項活動。巡宴地點不是在宮廟前後附近近距離的地點象徵性的走一圈，而是由道士團在府城隍廟外大範圍的3個點（附近2個商業社區群，及松山火車站），以巡察的方式，奏樂吟唱，邀請沿路的好兄弟、松山火車站及捷運站內各角落等的好兄弟，前來享用信徒所準備的供品、食物等。道士們沿途唸誦「太乙就苦天尊」法號，道長並雙手執竹蟠，搖動著帶領道士團及樂師巡宴。道士們回主道壇之後，開始進行普度科儀，中尊道長戴上五佛冠，進行蒙山科儀，施演一連串的手印、化食等儀式。後場也在此時以椰胡拉奏過場曲（【醉月登樓】、【陰陽別】、【紅花蕊】等），以示宴會已結束，陰陽兩途分道揚鑣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;接著以謝壇送聖的儀式請回神尊，所用的曲調皆大同小異，程序如下：神威如在大天尊、香偈（道香、德香）、一點寒泉與六符水、諸天讚、請神（道由心合）、四神咒、悃意、化財、送神（在道壇的神祇：總壇三清、左壇、右壇、三界、天曹、師聖、地頭神（如前來共襄盛舉之他廟神）等。</p>
