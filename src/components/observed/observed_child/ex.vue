<h4><strong>六、</strong><strong>2020</strong><strong>庚子科刀梯武考暨文奏授籙陞職醮會</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活躍於中臺灣的「中國武當道教太玄法師協會」名稱之由來，據載，乃因武當山道教是中華民族道教的一支道脈，亦是「玄天上帝」之香火相傳，在道教界有相當的地位，亦是「真武大帝」修真成道精華所在。該協會自創立（2004年）迄今已邁入第十六年。同時也辦理過21屆之「上登108層刀梯法會」，今年，2020年12月6日辦理「第二十二屆上登108層刀梯法師武考醮會」。在此期間並同時舉行「祈安九皇朝真禮斗科醮」法會（2020年12月3日至12月7日）與「第一屆玄門宗教文化學術研討會」（2020年12月5日）；主辦單位稱此3個法會為「庚子年三合一聖會」。&nbsp;</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於此次主辦單位的負責人蕭道賢道長，今年77歲，與宗教也有一段不凡的經歷。他出身於農家，從小學、初中到高職期間，幫忙務農；但在父母親的薰陶之下，與宗教結緣。30歲時拜師於諸法宗壇第二十五代楊朝潤大師門下，學法3年，而後在嗣漢天師府授職。後又與徐芹庭、謝文藝學易經及「易經三元地理學」；成立佛教經研班，學習佛門經典，並成立「神妙通玄壇」、「聖和真武宮」奉祀「北極玄天上帝」。爾後蒙呂仙祖孚佑帝君煅鸞，於49天後玉帝封賜「法筆」為扶鸞、闡教、濟世之乩士，之後又坐乩禁足108天，成為文武雙用之乩士。於35歲時與五術名家鍾進添學習命理。兩岸開放後，於1989年投拜於武當山道教協會會長王光德道長（玄武派第13代掌門）為師，學習武當玄門科儀及道教哲學，透過雙方多次的文化交流，王光德道長囑咐在臺成立玄武派宗壇，賜名太玄宗壇。蕭道長被賜法名玄賢，正式成為玄武派臺灣太玄宗壇第十四代掌門人；也開始引入「太玄宗壇」之道法科儀演揚。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1996年蕭道長到福建武夷山拜師於閭山道脈海清派第三十六代掌門人王浚明道長，演練閭（呂）山道法。1997年在西安八仙庵巧遇閔智亭道長，由吳誠林道長引薦保舉之下，拜閔道長為師。2004年經由武當山道教協會李光富道長之授命，派任為武當山之真武祖師弟子之海外臺灣代表後，蕭道長即召集同道，向內政部申請「中國武當道教太玄法師協會」，並成立「太玄宗壇」之道樂經團，以武當派為主軸，並為各友宮道觀做醮事。2008年協會重新改組，擔任第二屆理事長，期滿後被推舉為創會會長至今。目前會員已有300多名。2018年5月至2020年5月同時也是國民黨宗教事物委員會委員。2016年蕭道長再次接掌「中國武當道教太玄法師協會」第四屆理事長。2017年為國民黨中央黨部所聘任中央宗教諮詢委員；2019年進入新竹玄奘大學宗教文化研究所碩士班就讀（蕭道賢，2019）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在道法界長期存在一個傳統，要成為一個神職人員（法師、乩士、神童），或神職人員要晉升，均需通過文奏、武考等挑戰；其中的武考即是攀爬刀梯。刀梯可分為36層或108層梯，可能是源於「三十六天罡七十二地煞」之說。刀梯由吉數的刀數組和成一座刀塔。刀塔的形式有：全部為刀梯及釘、刀混合，一階釘、一階刀，或上階為刀、下階為釘，但刀鋒均朝天，除了祈求蒼天見證，也有考驗登刀梯者的法力。準法師在攀爬刀梯前，需坐禁齋戒一週，目的是「讓受戒者隔離日常，考驗意志力與宗教體驗」。儀式當天，時辰一到，高功道長將法祭落成的刀塔，同時禀告祈請上天見證後，欲晉升的準法師再進行攀爬。準法師攀上梯頂時需向上天稟告祈請見證：「登刀梯將凡身之力與氣結合，印證外柔內剛與法術修練的結合成果」，並以擲筊請示。擲出聖茭者表示通過諸天聖神的考驗，成為道行具備的高功道長。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於必須經過上刀梯之武考，方能得到諸神的見證與上蒼之封賜，此原因乃刀梯猶如「刀奎」的比喻。「刀奎」指的是內煉中的「神氣合一」法，指身為修道者欲使更上一層樓，更高的功夫，就是藉用登上過刀梯時，將凡身之力與氣結合而成「出氣化力，入氣為奎」。故刀梯是檢驗修道人之內煉功夫與外具忍度，也是印證一個行道者外剛內柔與法術之成果。故凡有志之士，欲得上蒼 玉皇大帝允賜封勅之法師、乩士，在通過108層刀梯戒定後，其身萬邪不侵，道炁增強，靈智開通，耳目皆聰，並可對自己修道的法力作驗證。由於此刀梯之每一層（支）刀，都在塔架完成時經過高功法師以法（制）化，以符安鎮，以咒催淨，才是具有刀戒之要素。經過有道之士修煉之火侯加持，持其道法登躍足踏，所凝聚之法力戒定於刀中，法師之血跡沾於刀口上，所以此刀在圓功後，皆變成具有斬妖除魔鎮宅聚寶的寶刀（蕭道賢，2020）。攀登刀梯的武考法會中，一般民眾可在刀梯下爬行，祈求消災避禍、祈福延壽。同時在刀梯第一層的桌上，備有米堆與鹽巴，法師在上刀梯之前，踏上此米堆，同時可去腳步的潮濕，保持乾燥。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣「中國武當道教太玄法師協會」成立至今15年來，共舉辦過刀梯武考21次，培養奏職或陞職法師將近600人次。今年參加武考的人數約有60多位。本年度的武考，除了有幾位年長者參加，大部分為年輕一輩的準法師，有幾位甚至是大專院校生。武考科儀的程序分2天進行：</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第一天：上午7:20開始，至17:00結束</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;刀梯108層丁臺香案上香禀疏、應考法師團體刀梯前合照、由高功法師蕭道長淨壇、開刀梯路，蕭宗主布達開刀路成功、開香典禮、花籃陣獻瑞、獅團淨壇界助威；9:40開始武考上午場；12:40下午武考開始、通關法師皈依、傳度、18:30授籙儀式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二天上午及下午：通關法師皈依、傳度、授籙儀式、頒發證書，並結束此次的武考上刀梯儀式。</p>
