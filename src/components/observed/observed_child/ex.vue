<h3><strong>前言</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年臺灣宗教活動史上可謂是不尋常、不平靜的一年。不僅經濟活動、旅遊活動或其他各種活動，凡是與人接觸有關的活動，特別是聚集眾多人群的宗教民俗活動也因此戛然而止或大幅限制；主要原因是幾乎全世界各角落都充滿了武漢肺炎新冠病毒感染的高度風險，臺灣也不例外。為了避免人群的接觸感染，很多大小活動被迫取消，如有小型的聚會、活動，也必須遵守防護措施、戴口罩、消毒等規定來辦理。特別在今年的2-5月期間的高風險期，在政府大力的勸導下，很多大型的宗教活動不是取消，就是縮小規模與人數，或透過網絡視訊與信眾分享。我們看到了宮廟祭典除了人潮大為縮減外，祭典活動的信眾、樂師也戴口罩、噴酒精等配合防疫政策的行動。這些都是2020年很多宗教活動因應疫情權宜措施的實況與寫照。當然在疫情稍微緩和的夏天以來，宗教民俗活動或宗教學術研討會又稍微活躍些，但大部分仍然遵行政府的防疫政策，在消毒、戴口罩或實名制等措施下，仍然有一些小範圍的宗教活動年曆在進行或透過視訊來舉辦。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;宮廟部分，首先打頭陣的全臺知名大甲鎮瀾宮在眾多歧見之後，終於在2月27日宣布延後舉行媽祖遶境。同樣的，白沙屯媽亦宣布進香延期、遶境延期；另外不少宗教團體在疫情顯得嚴重的時候，舉辦息災袪瘟法會，特別是2020年2-5月份期間。面對此種不熟悉又來得猛烈的瘟疫，人心惶惶，無所適從，希望祈求眾神保佑免於禍害。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我們看到幾間舉辦了禳瘟祈福法會、送瘟船活動的大型宮廟團體，以祈求武漢肺炎瘟疫早日消除，如「中華道教純陽祖師協會」與指南宮全臺32宮觀舉辦為期3天聯合並個別（2/20-22）「為新冠疫情禳瘟祈福大法會」，為國家及全民祈求福澤平安；或臺北松山慈祐宮也在2月18-20日舉辦「護國禳瘟清醮法會」，全臺六大媽祖為臺祈福；或臺北市大龍峒保安宮原定於3月28日登場的「保生文化祭」，也首次取消俱有200年傳統的家姓戲演出，以及每年重頭戲「放火獅」古禮、民俗藝陣競演，只留下4月5日的小型聖誕祭典活動；或樹林濟安宮於2020年3月7日舉辦「送瘟船儀式」，3月7日當天廟方原本就有安排文昌法會，替考生祈求考運順遂，恰巧武漢肺炎影響，臨時加開送瘟船的科儀；或宜蘭明德道院在今年2月22-24日，以全真及正一道派的科儀同時舉辦「代天巡守天池千歲與境主保生大帝護民和瘟祈福法會」；或臺北松山慈惠堂在5月10日-14日，延期辦理「2020臺北母娘文化季」活動，且分別以禪和與正一道派科儀舉辦「朝天護國和瘟息災安龍祈福大法會」取而代之等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統上，臺灣民間的宗教活動一直有法會遣送瘟神及瘟船的儀式。為了避免瘟疫之發生，除了尋求傳統醫藥學處方外，信徒並向相關神袛，如呂（仙祖）洞賓、保生大帝、神農大帝等醫藥神，或瘟神王爺、千歲爺等，祈求祝福、袪瘟、保佑。其中，如為禳瘟「武漢肺炎」疫情，由全體以供奉呂（祖）洞賓為主神的指南宮舉行聯合及個別法會，持誦經懺（《呂祖無極寶懺》、《孚佑帝君醒心真經》等多部經文），以祈福消災；另外是延請道士舉行「瘟醮」，祈請行瘟使者不要行瘟，更進一步希望將可能的瘟疫帶走。我們常見在南部靈寶道士的主持下，驅趕瘟疫的相關科儀如「和瘟」、「祭船」，或瘟醮之舉行也持續盛行至今日。科儀內涵的原則，先以「諂神媚鬼」之禮，行瘟使者成為收瘟斂毒之神，甚至成為守護之神。而送瘟船之習俗在道教驅瘟傳統與民眾心理需求下，也被一代代的傳承、實踐延續至今。然而「送瘟船」與燒王船雖然形式相近，都使用船，也有驅逐瘟疫的意涵，但是兩者的科儀內容不同，船上的神祇亦有差異。乘坐瘟船的為瘟神，有別於王船上「代天巡狩」的王爺。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情期間，臺灣各地不少廟宇陸續舉辦了祈福法會。今年松山慈惠堂的法會中更特意舉行平常較為罕見的「和瘟遣舟」儀式，希望能夠藉此讓這一波疫病儘速平息。5天的儀式中，除了相關科儀的誦讀外，最後一天道長帶領信眾完成祭拜，將瘟神請至所預置的大型木造船上，信眾緊接著將大量的紙錢、食物等貢品合力搬上甲板。鞭炮聲響起，在瀰漫的煙霧中，隊伍開始行進，船頭前方有人持鋤頭、另一人以水壺向地上澆水，象徵「開水路」，眾人合力把船向前推進，經過一段略為陡斜的山路及階梯後，將其安置在一塊空地上。信眾把大量的紙錢圍繞在瘟船周圍，儀式準備進入下個階段。火化瘟船的儀式，稱為「遊天河」，這座瘟船載著信眾滿滿的祈求，隨著火焰、灰燼駛向無境的天際、天河。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;宗教活動受到牽連的不僅在臺灣島內，甚至包括筆者所觀察到的今年2月1日至2月24日，在馬祖連江縣舉行年度最大的宗教活動馬祖「擺暝文化祭」之宗教習俗文化活動，於每年農曆的元宵節前後舉行。相較於往年，祭典期間會有離島的信眾，從臺灣或馬祖人的故鄉閩東福州等地，帶著民俗陣頭或藝陣前來助興，但今年的慶祝就大幅縮水，小三通不通，只能是馬祖島內的信徒自己慶祝，沒有往年的熱鬧；僅由當地的住民，男女信徒，或在臺求學返家的青年學子，加入宗教慶典活動的行列，成為當地歷年來規模最不盛大的活動紀錄。而在幾天盛會的活動期間，參與的信徒也依規定，把口罩戴好、戴滿。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一方面，也是2月中旬，如禪和道派「中國道教經典研究會」受到淡水「行天武聖宮」的委託，承辦12天的「恭祝濟公活佛師尊千秋聖誕 啟建太上慈悲普度收圓濟幽十二永日大法會」。該宮的信徒與一般的宮廟稍有不同，或許也是場地的關係，是屬於較小圈子的信眾，多為濟公活佛乩身的信徒，故法會期間一般跟拜的信徒較少。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，北投代天府今年8月4日所舉辦的「慶祝五府王爺聖誕千秋曁護國祈安植福吉祥法會」，由桃園大園區正一道士團「廣盛壇」來承辦。因為8月份屬疫情稍微緩和的穩定期，故社區信徒參加較為踴躍，甚至下午與晚上也安排歌仔戲的演出。比較少見的是，此「廣盛壇」道士團到今年（2020）為止已有39年從未中斷地替同一宮廟－北投「代天府」，負責道教法事科儀執行。據目前該團擔綱的年輕壇主曾慶峻（1982-）道長言，之前長期由其父親曾和欽（1946-）道長負責。這是一個典型的道教家族傳承。「廣盛壇」祖籍為廣東省嘉應州，長樂縣（現改為梅縣），在當地曾氏即為道士家族，其先祖輩第十七代來臺，父親曾和欽為家族的第二十六代。曾慶峻則為來臺後的第十代，持續以道士的家族傳承來進行宗教服務。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;主辦單位的北投「代天府」也與一般所知曉的宮廟歷史略有不同。它原為北投侯亭陳姓族人的家廟，後來逐漸發展成為附近當地人的共同信仰中心。「代天府」結合廟方所供奉的神袛五府王爺，舉辦「聖誕暨護國祈安植福吉祥的法會」。北投侯亭陳姓族人其開臺祖陳江，於1755年（乾隆二十年），時年34歲（66歲時歿），來到北投，受到當時已在北投侯亭陳氏家族之助，在臺逐漸發展事業，並繁衍子孫，至今可能有20代以上的後輩。陳氏家族祖籍為泉州府同安縣馬巷廳十二都翔豐里，封侯保山後亭鄉人。陳家開臺祖陳江於1755年渡臺至北投後開始開墾，設立「陳柏記」商號，供奉五府王爺（李府、池府、吳府、朱府、范府）並納平埔族人為妾（取漢名林蘭官，很可能是當時還存在的猴猴族人，從當時文件上的契約所載）。「陳柏記」商號成立初期，以收購平埔族人所獵獲動物之獸皮、臟器再行轉售，另出售酒類、雜貨米糧等，客源以平埔族人為主。這間由漢人與平埔族人聯姻所發展出的一段深具歷史意義的遷移史與宮廟發展史，且由同一個道教家族以將近40年的時間為其服務，卻也少見；因為目前常見的生態是宮廟常會每年更換邀請不同的道士團或不同的宗教屬性團體輪流前來進行法會服務。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，一般北部少見到的大型上刀梯法師的武考，作為授籙陞職的檢驗，也是道法教正規科儀之外的實踐法門。活躍於中臺灣的「中國武當道教太玄法師協會」，今年，2020年12月6日辦理「第二十二屆上登108層刀梯法師武考醮會」。在此期間並同時舉行「祈安九皇朝真禮斗科醮」法會（2020年12月3日至12月7日）與「第一屆玄門宗教文化學術研討會」（2020年12月5日）；主辦單位稱此3個法會為「庚子年三合一聖會」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該協會成立至今15年，共舉辦過刀梯武考21次，培養奏職或陞職法師將近600人次。今年參加武考的人數約有60多位。本年度的武考，除了有幾位年長者參加外，大部分為年輕一輩的準法師，有些還是大專院校的在學生。攀登刀梯的武考法會中，一般民眾可在刀梯架下爬行，祈求消災避禍、祈福延壽。同時在刀梯旁第一層的桌上，備有米堆混和鹽巴，法師在上刀梯之前，踏上此米堆，同時可去腳步的潮濕，保持乾燥兼具消毒功能。至於此刀梯之每一層（支）刀，都在塔架完成時經過高功法師以法（制）化，以符安鎮，以咒催淨，才是具有刀戒之要素。經過有道之士修煉之火侯加持，持其道法登躍足踏，所凝聚之法力戒定於刀中，法師之血跡沾於刀口上，所以此刀在圓功後，皆變成具有斬妖除魔鎮宅聚寶的寶刀（蕭道賢，2020）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年宗教活動整體縮減之外，學術機構的宗教研討會，也紛紛延期或取消，或縮小規模，或閉門會議，並採實名制的參與者來進行。國際型的會議或以視訊來執行。這些都是因應疫情關係所帶來的直接與間接的影響，權宜應變措施的寫照，也是2020年與宗教各種相關活動的一大特色。</p>
<h3><strong>壹、本年度活動的觀察與評介</strong></h3>
<h4><strong>一、宮廟因應武漢肺炎疫情舉辦「禳瘟祈福大法會」</strong></h4>
<p><strong>（一）臺北松山慈惠堂「太上覃恩朝天護國和瘟息災安龍祈福大法會」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年因應世界性災難的武漢肺炎疫情，臺灣也不例外，故有不少宮廟發起「禳瘟祈福大法會」，如本年度所記錄的，臺北松山慈惠堂舉辦的「太上覃恩朝天護國和瘟息災安龍祈福大法會」活動，希望藉此法會以息災祈福安定人心。因為疫情的關係，該宮廟原訂每年舉辦的「臺北母娘文化祭」活動也遭取消，改於國曆5月10日至14日啟建「太上覃恩朝天護國和瘟息災安龍祈福大法會」5天，為臺灣與世界祈福，希望病毒盡快消退。該活動改以透過臉書、網絡，讓信徒得以隔空參拜或追蹤活動的進行，只有少數信徒還是來到了法會現場進行觀禮參拜。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這5天的宗教祈福大法會，分別由2個道教團體，分2個樓層，以他們各自不同道派的儀式來進行。首先是慈惠堂自己的禪和誦經團，在2樓的瑤池金母大殿堂內壇舉行，由將近11位的前場道士及及6位後場樂師擔綱。至於樓下廣場以臨時搭建的外壇，同時進行的另外一場正一道派的法會，邀請來自臺中烏日「指微道壇」負責人林元啓道長所帶領的團隊宣演。林道長的道法是從岳父處學來，至今已是第三代傳人。林道長同時也是少數會作米龍的道長之一，最早主要在臺中進行道務上的事業，多年來在當地也收了不少徒弟；後來到北部發展，近年來在臺中、臺北往返活動。至於後場樂師也多來自臺中，長期跟著林道長，擁有多年的後場經驗，雖然沒有很系統化的學過北管，因為他們也很少把曲牌直接拿過來用，常常是段落式的穿插應用。其任務最主要是配合前場高功的訊息暗示與動作，以及察言觀色，做出即時反應，才能天衣無縫的配合。這次廟方所特地安排的「安龍祈福大法會」，基於「有土斯有財」之概念，召請「五土龍神」，即召請「五方財神」前來。藉母娘文化季祥瑞隆盛之際，行安龍護國祈福大典，以祈求「五方祥龍正炁安定國邦」之意。法會同時廣邀十方善信參與護持並共沐慈暉，祈求事業順利、祿位高昇、財源廣進。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「指微道壇」屬臺中的正一道派。其科儀詠誦，及後場音樂基本上雷同於北部的正一道派，但是其腔調帶著中部自己的特色。後場共有4位樂師，武場2位，負責鼓、叩仔、大小鑼、鈸等節奏樂器；另有文場2位樂師，負責電子琴及二胡、嗩吶等旋律樂器。法會期間，每天從8:00-16:00進行正一科儀的讚誦等。在法會的最後一天，舉行了燒瘟船儀式，藉以送走瘟神。</p>
