<p><strong>（三）</strong><strong>2020</strong><strong>年華裔學志國際會議－基督宗教與道家</strong><strong>/</strong><strong>道教的會遇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年9月23-24日，在輔仁大學國璽樓2樓國際會議廳，由輔仁大學華裔學志漢學研究中心、德國聖奧古斯丁華裔學志研究所、國家圖書館漢學研究中心、輔仁大學宗教系、華沙大學漢學系共同主辦的國際型宗教學術研討會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;輔仁大學華裔學志漢學研究中心的成立，緣起於1935年聖言會的會士，德國漢學家鮑潤生（Franz Xaver Biallas, 1878-1936）決定創辦一個漢學方面的學術性刊物，以取代原有的輔大校刊。自此，以Monumenta Serica（簡稱MS）為名之學術期刊便應運而生，而華裔學志研究所亦以此為名。當時輔仁大學校長陳垣（1880-1971）十分重視這份刊物的出版，並親自為該刊物選定中文刊名之「華裔」二字，意指華夏及裔遠之邦（指受中國文化影響的友鄰國家，而非今日一般所謂之「旅居國外之華人後代」）。2002年8月1日天主教輔仁大學與聖言會St. Augustin（聖奧古斯丁）華裔學志研究院共同成立「華裔學志漢學研究中心」。2002至2011年由聖言會會士魏思齊擔任主任一職。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該中心的成立具歷史性意義，代表著聖言會華裔學志研究所與輔仁大學重新建立親密關係，共同合作推動天主教輔仁大學的目標，並特別著重於推動中西文化交流之宗旨，致力於中國文化與基督信仰之交融。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;輔仁大學華裔學志漢學研究中心在今年舉辦的國際會議，以基督宗教與道家/道教的會遇，作為主題，希望藉由學者們專家的想法與論述，對此議題開啟多面的理解。如此的構思獲得了國家圖書館漢學研究中心、德國聖奧古斯丁華裔學志研究所、國家圖書館漢學研究中心、輔仁大學宗教系、華沙大學漢學系的支持，共同主辦此次研討會；希望對漢學研究盡一份心力，並提供中西年青學者更多學術性交流平臺。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次的國際研討會為期3天。第一天的內容有宗教音樂的對話，葛麗果與古琴；有易經二元圖像，論17、18世紀中西藝術與科學之交流；有老子《道德經》翻譯的讀解興趣之概觀；道教經典文獻翻成波蘭文的探討等。第二天的專題演講議題包括：當前上海道教研究的探討；三官考校－道教的賜福、赦罪及解厄觀（李豐楙）；13至14世紀基督教傳教士、道士與蒙古人的會遇－宣教內容之比較（鄭素春）；或外籍傳教士對於道家/道教的理解與詮釋等。第三天的青年學者論壇以漢學的跨領域交談為主題，聚焦在青年學者的中西文化交流的新視野。外國學者的部分，則透過視訊論文發表的方式進行。</p>
