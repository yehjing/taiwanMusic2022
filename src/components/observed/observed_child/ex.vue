<h4><strong>五、禪和道派「恭祝濟公活佛師尊千秋聖誕</strong><strong>啟建太上慈悲普度收圓濟幽十二永日大法會」</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;位於淡水的「行天武聖宮」肇建於1998年，位於臺北大屯山脈，前堂面向觀音聖山，是一個行家所說的風水寶地。行天武聖宮正殿主恭奉關聖帝君、濟公活佛、中壇元帥，同時奉祀列位聖、佛尊神眾席闡教；該宮以提倡關聖帝君「仁、義、禮、智、信」的五德為主旨，意端正民間宗教信仰以淨化人心，同時以弘揚濟公活佛「慈悲仁慧」濟渡眾生的偉大精神，倡辦社會慈善公益，推展服務社會工作為志向。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;武聖宮住持陳俊廷（1989-），號玄微，也是濟公的乩身。早期玄微法師之天母住家，其父陳勝彥原本就恭奉有家神－關聖帝君，請回的濟公活佛就安座敬奉於側，從此開始濟公活佛就時常降駕附靈於當時八歲的玄微，一年來靈異顯赫不斷地練乩操訓，直到九歲時濟公活佛降駕附靈玄微，就能開金口示諭指點迷津，並承接玉旨開壇濟世，於1998年敕賜宮名為「行天武聖宮」，且頒令陳勝彥為宮主，輔佐玄微共同為行天武聖宮濟世信徒；並示諭行天武聖宮必須經如孟母三遷而定，果然於2004年就從天母遷至陽明醫院附近住處，再於2006年遷至北投住家。濟世多年後信眾驟增，再於2008年機緣聚會進駐淡水八勢路今址，奠定了香火鼎盛的行天武聖宮。該宮信徒不乏多位政商界的知名人物，並受邀於海內外信徒；其中，如馮世寬、郭台銘等多位政壇人士。至於負責執行此次為期12天大法會的「中國道教經典研究會」（禪和道派）的道團，與該宮多年來有長期合作的關係，故常被邀請承辦該宮法會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「中國道教經典研究會」是屬於道教正一禪和派經典門的傳承道派，不是以斗堂或壇的旗幟來傳承道教經典與經讚、經韻團體。該團創辦者為邱玉珠道長與其夫婿陳炳松道長同為金鳳山「臺北慈靈宮」禪和道壇「中國道教經典研究會」的主事者，亦為同道者。邱道長從小學五年級時，年方12歲多的年紀，正巧主供觀音菩薩的「覺善堂」堂內有老師想教誦佛經；當時她父親問說要不要去學誦經，理由是一方面可以多讀一些漢文，再者祖父也是學習閩南音誦經者。邱道長答應了，也因此開啟了她傳誦經典的人生。1984年邱道長開始接觸道教經誦、讚，學習於福州道長趙可畬門下，約有10年的光陰；並與陳道長同時向一位福州高功魏應銘法師學習禪和「發表」、進表（朝真）及步罡踏斗等科儀，約有3年多。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1987年邱道長與夫婿同時參加當時由「臺北總道教會」所開辦的第一期道經團研習班，與128位同好開始學習福州禪和唱誦。當時學習的方式是以福州話唱誦，並不是每位學員都得心應手，但邱道長最後改以閩南語誦唱，意外竟讓多位同好繼續有學習的動力；所以邱道長可說是以臺語誦唱禪和道經的第一人。往後，邱道長也向董德平先生學習經讚的簡譜及後場樂器，如笛子、二胡等。1995年應各方道友響應，邀請了資深學者共同創立「中國道教經典研究會」。2009年因緣匯集，該研究會搬移於新北市石碇區磨石坑31號現址，成立西歧金鳳山無極慈靈宮，做為道教經典研究的道場，繼續服務信徒。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至今30多年來，邱玉珠道長與夫婿陳炳松道長，以禪和道派的科儀與經誦，足跡踏遍全臺與海外（新加坡、泰國、日本等），並去過福州與大陸禪和道友進行交流，在當地做過法會；這幾年他們二位培養了不少禪和後輩，並持續秉持傳統禪和道脈、精神與內涵，活躍於臺灣的道教界。今年，2020年農曆1/20-2/2（西曆2/13-2/24）為濟公活佛聖誕，中國道教經典研究會受行天武聖宮之邀，舉辦為期12天之「啟建太上慈悲普度收圓濟幽十二永日大法會」。參與該法會的信徒與志工，皆為該宮的信徒居多。</p>
