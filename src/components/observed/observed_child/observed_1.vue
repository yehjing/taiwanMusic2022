    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年因為突發嚴重特殊傳染性肺炎疫情且快速地蔓延擴散至全球，不僅攪亂日常生活，也嚴重影響各行各業的生計，藝文活動更是難以舉辦。臺灣面臨如此嚴重傳染病疫情的考驗，一開始雖然有點措手不及，所幸在一切防疫政策施行後，加上國人的配合，疫情得以控制，生活逐漸恢復正常，臺灣也算安然度過2020年，歡慶喜迎2021年到來。原以為2021年疫情依然可以控制得宜，或許正因為臺灣防疫成功，讓部分人過於放鬆、忽略應該遵守的防疫措施，以致2021年4月下旬因為華航機師、萬華茶藝館、蘆洲獅子會等幾起群聚感染，疫情衍生擴散導致北部疫情緊繃。5/11因出現多例本土染疫個案，疫情警戒升至二級，5/15又因新增1百多例本土個案，臺北市、新北市疫情警戒首先升為三級，之後幾日染疫個案人數持續擴大，5/19全國進入防疫警戒第三級，各級學校也暫停實體上課改為遠端線上教學，三級警戒一直到7/27才解除，防疫調降至二級警戒。所有的活動都因而大受影響，南管活動當然無法倖免。</p>
<h3><strong>壹、館閣的活動紀錄</strong></h3>
