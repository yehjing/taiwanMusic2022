    <p><strong>二、遶境</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管館閣參與廟會活動，基本上是不以徒步參與全程遶境，若有應聘參與，大多是安排布置南管車，人員坐在車上演奏，如南聲社於1/1參加「南廠水門宮辛丑年金籙謝恩祈安五朝建醮：恭送天師上帝佛祖回鑾」安座暨遶境大典，即是以坐車演奏方式參與遶境。汾雅齋南管樂團本年度除了參與「北港朝天宮壬寅年北港媽祖遶境賜福」之外，也應臺南市全臺祀典大天后宮聘請參加「2022壬寅年三大天后鳳山平安遶境」、「府城四安境頂太子沙陶宮壬寅年慶成祈安五朝建醮恭送張府天師回鑾遶境大典」，汾雅齋應聘參與外縣市遶境，基於禮貌皆有邀請部分在地館閣絃友共同參與；而雅月齋也應下大道良皇宮聘請，以南管三輪車方式參與「府城四安境頂太子沙陶宮壬寅年慶成祈安五朝建醮恭送張府天師回鑾遶境大典」，沿途吸引不少民眾注目、拍照。12/4「忠福大囍－保安廣澤尊王往土庫鳳山寺迎娶妙應仙妃」是本年度非常特別的廟會活動，臺中豐原忠福堂安排以古禮迎親，迎親隊伍先自土庫圓環踩街進入市區到達鳳山寺，舉行訂親儀式後，再遶境土庫街；合和藝苑應聘參與迎親，隨駕於神轎之後，館棚上也特地縫上紅色彩球增添喜氣，訂親儀式之後，進行30分鐘整絃，最後再跟隨神轎遶境土庫街，整個迎娶過程非常熱鬧。
    </p>
    <p><strong>三、藝術節</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年度南管展演場次，部分團隊分別參與：臺北霞海城隍文化節、舊街神農文化祭、臺灣戲曲藝術節、彰化傳統戲曲節、彰化走讀藝術節、臺北藝穗節、嘉義草草藝術節、雲林國際偶戲節、後山媽祖文化節，此類活動列算入藝術節類型，而鹿東國小參加2022鹿港好食節則列於其他部分。臺東天后宮於今年舉辦「壬寅年七朝祈安清醮暨後山媽祖文化節」，醮典期間除了相關科儀的進行，同時邀請不同表演團隊於後山媽祖文化節演出。欣逢家鄉盛大慶典，前閩南樂府理事葉圭安帶領他門下學生並邀請部分館閣絃友共襄盛舉，組成「葉圭安南管國樂團」參與文化節演出，9/19、9/20、9/21連續三天分別於臺東市南京路市民廣場醮壇、臺東天后宮廟埕、臺東天官堂廟埕等處整絃。參與「葉圭安南管國樂團」演出者包括臺東聚英社、臺北市立國樂團南管小集以及笨港集斌社、中瀛南樂社部分絃友，此外，合和藝苑也應邀前來支援，林珀姬老師與幾位臺北絃友也專程南下參與。演出基本上依指、曲、譜順序安排曲目，只是指、譜幾乎都以大合奏型態演奏，廟方也採網路直播分享文化節演出，南管整絃9/19全程播出、9/20則部分直播。彰化縣文化局本年度亦積極辦理相關活動，如彰化傳統戲曲節、彰化走讀藝術節，提供在地南管團體有展演的場域，也讓鹿港在地人、外來遊客有親近南管的機會。
