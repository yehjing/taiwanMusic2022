    <h3>壹、前言</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管音樂在2022年仍受嚴重特殊傳染性肺炎（COVID-19）疫情波動影響，部分活動規模縮小或停辦取消。原本部分館閣在農曆新年正月初一，會配合所屬宮廟新春活動在廟埕進行整絃，因唯恐返鄉人潮又掀起群聚感染，故而取消初一整絃活動，如海寮普陀寺清和社、茄萣賜福宮振樂社即是取消活動。其他相關的傳習活動或例行展演也受到波及，如和鳴南樂社上半年第一期研習課程多以線上教學方式完成，成果發表也不對外邀請，而華聲社在有記名茶以及臺北孔廟的展演也大受影響，一直到7月中旬才逐漸恢復正常。2022年上半年，北部館閣除了練習或研習上課之外，其他活動相當少，中南部活動力則稍微好一些，整體而言，南管活動大致到了8月，才逐漸復甦。
    </p>
    <p><strong>一、研究範圍界定</strong></p>
