  <h3>玖、結語</h3>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年就在疫情急速波動後走向與病毒共存的步調中劃上句點，傳統南管館閣屬於業餘休閒組織，疫情之前的2019年全年度活動有四百多場，防疫抗疫第3年，全年度還有三百多場活動，除卻心心南管樂坊、江之翠劇場、南管新錦珠劇團是偏向職業演出之外，以業餘為主的南管團體而言，能有這樣的活動力，已屬不易。從防疫面來看，也可見面對疫情已不再有最初的恐慌或焦慮，生活已開始逐漸回歸疫情前的日常，舉辦活動的防疫限制也寬鬆不少。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度活動仍以展演類型居冠，祭儀活動次之。廟會活動雖有「酬神演出防疫管理措施」規範，但只要防疫規劃得好，辦理活動不再是問題，因此祭儀活動逐漸復甦，相信明年南管參與祭儀活動場次會持續增加。攸關南管傳承的推廣與傳習，在各團體努力下，活動各有六十幾場，看得出大家的用心，推廣教育場次如果能再擴增，以利普羅大眾與學子們有更多機會親近南管，特別是校園巡演部分需要多多益善。而南管相關的計畫案、期刊論文、碩博士論文、研討會論文等研究在本年度不多，或許一部分是因相關系所招生困難，以致攻讀碩博士者少，連帶投入南管研究者銳減。前述年度出版品共計四項，儘管數量不多，但各有其特色，提供愛好者與學習者不少參考。本年度的研究與出版部分都有交加戲的身影，對於交加戲的保存是很好的發展。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門縣南管整絃大會2021年因疫情取消未辦，2022年還是沒有舉辦，這是金門館閣一年一度以樂會友的觀摩聯誼聚會，停辦非常可惜。此外，疫情之前金門館閣於每週六晚上在金門傳統音樂館輪值演出的活動因疫情停止，至今也不再舉辦，令人惋惜。而原訂由公親寮清水寺天子門生辦理「歌清音．詠太平－2021太平歌陣大會親」活動，於2021年9月初公告延期舉行，但延至2022年仍未見活動復辦，太平歌陣大會親是近幾年持續辦理的活動，也是太平歌天子門生的年度盛事，未再舉辦，甚是可惜，期待2023年能恢復舉辦。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這三年，由於疫情之故，影響各行各業的發展，政府相關部門採取因應措施，文化部也為藝文團體提供相關紓困補助，南管因而受惠，多項推廣展演、錄音、錄影出版皆是獲得紓困補助才得以順利完成。而相關的紓困補助方案，當疫情趨緩不再影響經濟時，是否依然持續，就有待觀察。此外，因應防疫政策，為使活動如期舉行而頻繁出現的線上教學、線上音樂會、線上研討會，是否戛然而止，或持續發展，也是值得關注。據聞中國華夏文化網規劃在2023年以線上播出方式舉辦「築夢未來．展現南音少年風采－首屆國際青少年南音比賽」，臺灣也在受邀之列，屆時是否有團體參加，值得關注。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儘管南管每年有數百場活動場次，顯示活傳統的脈搏仍在跳動著，但現實殘酷的斷層危機依然存在。南管的傳承發揚與保存維護，需要多管齊下，不僅需要技藝傳承，欣賞人口的培養也不能輕忽。傳承與推廣就像主幹與旁枝，傳承需要向下紮根，根基穩在，主幹才能日益茁壯，而旁枝需要多向延展，才能枝繁茂密展現生命力。如何增進中小學學生對於南管認知與欣賞的機會，以及如何透過多元管道宣傳吸引大眾親近南管，仍然是南管要加強努力的重要課題。
  </p>

