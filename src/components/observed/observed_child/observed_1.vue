    <p><strong>二、年度社會背景</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年嚴重特殊傳染性肺炎（COVID-19）疫情並未消散退去，臺灣仍視疫情浮動而隨時調整防疫政策，坊間甚至出現防疫保單熱賣的情形，各行各業也還無法完全恢復到疫情之前的常態，各類活動的舉辦亦需配合相關防疫規定。年初還處於二級警戒，大家對於防疫依然警惕在心，臺灣防守也還算不錯，但或許是從2020年疫情爆發到2022年年初，民眾因疫情已壓抑許久，所以逐漸鬆懈，以致於3月底又出現好幾起群聚感染事件，4月起單日確診數急遽增加，死亡人數也往上攀升，臺灣從「清零」不得不走向「與病毒共存」，讓生活逐步恢復疫情前的常態，期盼生活經濟回歸正軌。而從清零到與病毒共存的過渡期，面臨的就是單日確診數與死亡病例的暴增，5月底曾出現單日確診數9萬多的情況，6月上旬更出現死亡病例超過200例的紀錄，這樣的情況讓臺灣民眾一度恐慌，出現不少謾罵指責政府的聲浪，甚至影響了年底九合一選舉。疫情極度波動一直到11月開始緩和，12月起也取消室外空間、室外場所全程佩戴口罩規定，文化部也於12月16日公告修正「表演場館防疫管理措施」、廢止「酬神演出防疫管理措施」。2022的臺灣雖然仍受疫情不小影響，但已逐漸走出嚴重特殊傳染性肺炎（COVID-19）疫情的陰霾。
    </p>
    <p><strong>三、資料來源</strong></p>
