      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;出版部分雖然不多，但各有特色，其中《南管新錦珠交加戲《百花台》》為戲劇影像類有聲書。南管新錦珠劇團繼2021年出版《南管新錦珠交加戲《秦香蓮》》後，2022年再接再厲出版《南管新錦珠交加戲《百花台》》有聲書，出版經費獲文化部「受嚴重特殊傳染性肺炎影響積極性藝文紓困計畫案」部分補助以及民間團體與個人贊助，有聲書製作用心、內容詳細，包含劇照、劇團簡介、學者專家導讀、劇本註解以及工尺譜，並附影片USB觀賞，全書文字與影片字幕皆為中英文對照，對於拓展海外行銷很有幫助。《南管音樂：夜遊》獲文化部文資局補助，收錄曲目指《惰梳妝》、《花園外邊》、《八展舞》，演奏以上四管編制為主，但《花園外邊》、《八展舞》是採品管以笛子吹奏，不同於洞管的洞簫定絃，二首樂曲分別以「反管」、「擠管」等提高定絃音高的移調演奏，11/13發行人林麗馨公告專輯訊息，現代動漫風的封面設計引起不少絃友注意。《南管七大枝頭排門頭系列－【倍工】四孤、四對》專輯為林吳素霞老師申請的錄音計畫，獲文化部「受嚴重特殊傳染性肺炎影響積極性藝文紓困計畫案」補助，專輯由林吳素霞老師擔任主持與製作人、執行製作與解說書撰寫：蔡郁琳、樂譜抄寫：吳炳慧。參與錄音團隊：合和藝苑、臺南市南聲社、鹿港聚英社南樂團、臺北市和鳴南樂社團、臺北市華聲南樂團，整套專輯包括解說書以及5片CD光碟，共計收錄了12首樂曲，專輯無販售，而是分贈給館閣與絃友參考。《陳三五娘有聲行動教材（二）》有聲書獲「臺北市文化局110年度補助無形化資產維護計畫」補助錄音出版，收錄34首與陳三五娘故事相關的南管曲，書中除了南管樂語介紹、樂曲解說，並附唱曲錄音的QR
      Code條碼，提供讀者聆賞。</p>
