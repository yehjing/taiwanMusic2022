<p><strong>（三）「西港慶安宮辛丑年香科」中斷</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年辛丑年適逢西港慶安宮進行第八十科「西港刈香」，參與香科的南管、太平歌團體包括：海寮普陀寺清和社、公親寮清水寺天子門生、永吉吉安宮天子門生、港墘太平歌團天子門生陣、後營普護宮天子門生陣、三五甲鎮山宮文武郎君。參與香科的團隊，在香科日期到來之前，必須進行「入館」、「開館」等儀式，香科結束後，還要舉行「謝館」儀式，各團自2月起陸續進行入館與開館儀式。海寮普陀寺清和社3/24入館、5/16開館；公親寮清水寺天子門生2/27入館、5/1開館；永吉吉安宮天子門生3/6入館、4/24開館；港墘太平歌團天子門生陣3/6入館、4/18開館；後營普護宮天子門生陣2/15入館、5/8開館；三五甲鎮山宮文武郎君4/18入館、5/22開館。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不料5月因疫情持續嚴重，中央流行疫情指揮中心公告5/15雙北防疫警戒升至三級以及全國性防疫因應措施，其中規定「全國宗教祭祀場所活動部分，全面停止進香團與遶境相關活動」，因此海寮普陀寺清和社5/16開館取消，香科原訂5/27請媽祖、5/28-5/30三日香遶境活動也延期，等待二級警戒結束後再商議。之後，經與參加香科各宮廟商議，決定恢復辛丑香科遶境活動，時程訂在12/23請媽祖、12/24-12/26三日香。延期之後的香科活動，太平歌部分僅有永吉吉安宮天子門生全程參加，海寮普陀寺清和社未參加遶境，但仍於二日香千歲爺聖駕蒞臨海寮庄時，前來接駕，以及三日香進行宴王大典時，前往西港慶安宮進行祀神儀式。而「謝館」儀式，也只有三五甲鎮山宮文武郎君、後營普護宮天子門生陣分別於7/31、8/4舉行。三年一科的辛丑年西港刈香就在疫情影響之下延期舉辦完成。</p>
