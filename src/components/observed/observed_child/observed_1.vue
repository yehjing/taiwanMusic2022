<h4><strong>二、館閣參與寺廟相關活動</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年南管館閣參與寺廟相關活動，部分是寺廟依年例聘請南管演出，有些則是年度適逢出現的特殊活動，如1/2中和枋寮威遠壇於中和福和宮舉行「渡臺200年壇慶」禳災祈安福醮，聘請南管新錦珠劇團酬神演出，節目內容包含：南管音樂演奏會、扮仙戲、南管戲傳統劇目《高文舉》。南管新錦珠劇團本場扮仙戲內容非南管戲特有，而是一般熟知的《南詞天官》，第一場扮仙為中和枋寮威遠壇敬獻，第二場濃縮版扮仙則為南管新錦珠劇團敬獻。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;農曆新年春節期間，雖然疫情尚且和緩，但海寮普陀寺仍取消正月初一的整絃活動，茄萣賜福宮振樂社、茄萣金鑾宮振南社則照常於初一進行廟前新春整絃排場，而正月初三灣裡萬年殿葉府千歲聖誕，和聲社也僅前往舉行祀神儀式，取消整絃排場。</p>
