<h4><strong>八、福建省第五批省級非物質文化遺產南音代表性傳承人</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年繼卓聖翔被廈門市文旅局認定為第五批市級非物質文化遺產南音代表性傳承人之後，2021年廈門南音樂團也將卓聖翔、林素梅、羅純禎提報為福建省第五批省級非物質文化遺產南音代表性傳承人，該認定並非是臺灣的文化政策，但3人是以臺籍人士身份被認定，所以也列入本年度紀事。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;卓聖翔、林素梅、羅純禎3人，只有羅純禎出生於臺灣，其原從事於護士工作，後來參加松山奉天宮南樂團，師承卓聖翔。林素梅原是馬來西亞華僑，11歲加入巴生永春公所及巴生雪蘭莪同安會館南音社，1989年來臺，隨卓聖翔學習南管，1996年畢業於高雄師範大學國文系，定居臺灣期間，與卓聖翔一同從事南管教學，2006年移居廈門。卓聖翔、林素梅、羅純禎3人在臺灣期間，雖從事南管教學，但與臺灣南管館閣甚少往來，如今3人以臺灣人身份在廈門積極參與南管相關演出與教學工作。</p>
<h4><strong>九、國際展演與交流</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度，南管館閣無國際展演與交流情況，僅有王心心個人於8/5赴北京南池子美術館，參加「唐風宋韻－傳承與當代表現」學術論壇「美術館之夜－非遺南音」演出，洞簫獨奏李白〈靜夜思〉、琵琶彈唱余光中〈小小天問〉與〈鄉愁〉。另外，中國福建省廈門市思明區文化和旅遊局主辦的第五屆「思明杯」南音大會唱活動，以現場或透過雲端的方式進行展演，曾有主辦單位人士詢問臺灣絃友是否有意願提供演出影片參加，但活動以「南音頌黨恩．古韻傳鄉情」為主題，就婉拒參加了。</p>
<h3><strong>結語</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣安然渡過2020嚴重特殊傳染性肺炎疫情發生的第一年，戴口罩勤洗手變成生活中很重要的事情，原以為疫情可以依然控制得宜，卻因某些人忽視防疫規定，造成5月一連串的本土染疫案例，迎來讓生活幾乎靜止的三級警戒。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統南管館閣為業餘休閒團體，比較不會因防疫措施影響、停止活動，而導致收入短少影響生計的問題。防疫期間，影響較大的是平日拍館、團練、傳習以及例行的孟府郎君祭典等活動的進行，即使7/27後轉為二級警戒，大部分館閣還是到9月才逐漸恢復，而秋祭活動，除了合和藝苑之外，基本上都是不對外邀請館閣參加，如南聲社、振聲社、清雅樂府，和聲社恭祝水仙尊王聖誕南管整絃大會也僅自家人參與。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度心心南管樂坊、江之翠劇場皆積極投入藝文演出。心心南管樂坊共計演出：「紫藤40藝術季：幸逢是春－心心南管樂坊VS春日茶席」臺北1場；「人聲很南管」屏東1場；「王心心吟唱－長恨歌」臺北2場、臺南2場、高雄2場、線上1場。江之翠劇場部分有「梨園新聲」臺北2場；「行過洛津」臺中2場、高雄2場。雖然多不是傳統形態南管演出，但至少南管有參與藝文場所演出記錄。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年是臺灣文化協會100週年紀念，各方舉辦不少相關紀念活動，而南管並未有相關的專場演出，僅僅在10/30臺北市立國樂團主辦、線上直播，由琴園國樂團與臺北市立國樂團附設合唱團演出的「文協臺灣－百年的光與影」第二幕「風雲往事」，蔣渭水拜訪林獻堂商討臺灣文化協會成立事宜，雙方茶敘情節中，安排陳筱玟老師自彈自唱一曲〈冬天寒〉，僅此而已。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳習推廣方面，除了臺南市永康國小南管研習因林秋華老師退休而停止，比較可惜之外，其他學校的研習仍正常持續進行。鳳聲閣民俗樂團本年度「鳳聲閣南管文化推廣演出」在嘉義製材所進行4個場次、「再現文化馬賽克－2021嘉義市古蹟日：嘉義舊監獄興建百年系列活動」演出1場，沈怡秀團長也到嘉義市港坪國小進行「南管校園推廣」，對於嘉義市南管的推廣，很是用心。此外，部分傳習的成果發表規模擴大舉辦，如陳嬿朱2021南管音樂傳習班「南管之聲引鳳來」巡迴演出6場；彰化縣文化局馬興國小南管研習班「山與海的游樂嬉戲－南管戲《雪梅教子》」除了彰化2場、還飛往澎湖演出2場；南聲社傳習班「南聲．傳藝－南聲社傳習班南管音樂會」也安排在臺南孔子廟明倫堂演出。這幾場傳習的成果皆安排在研習場所外的場地演出，這樣能讓更多人聽見南管、看到傳習的成果，也間接推廣南管，活動更具意義。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;嚴重特殊傳染性肺炎疫情已有2年，大家的防疫意識越加高漲，同時也越來越習慣戴口罩演奏演唱，雖然學習與愛好南管的熱忱不減，但多少影響參加活動的意願，如有防疫疑慮也會自主不參加，這是疫情之下的無奈。祈願2022年，疫情緩和退散，不再影響大家的日常，相關活動皆能正常進行，而不再受疫情箝制。</p>

