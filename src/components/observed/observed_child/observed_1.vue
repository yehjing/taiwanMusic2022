  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;彰化縣文化局為推動南北管音樂，本年度擬定新政策，預計在9月成立「彰化縣立青少年南北管實驗樂團」，招募12-18歲青少年團員。青少年南管實驗樂團設立在鹿江國際中小學，甄選駐團教師1名、助理駐團教師1名、團員20-25名。駐團教師與助理駐團教師於7/28在南北管音樂戲曲館進行甄選，分為口試、資料審查、教學演示等三部分，甄選結果由王怜瓔擔任駐團教師、助理駐團教師無人報名從缺，王怜瓔畢業於國立臺北藝術大學傳統音樂學系碩士班，同時也是合和藝苑資深館員與彰化縣文化局南管實驗樂團團員。而團員招募報名原是於7/15截止，後延至7/31，考試訂於8/4，考試項目包括：南管樂器指定曲〈風打梨〉、〈綿搭絮〉二擇一，以及自選曲演唱或演奏，錄取11名，團員來自鹿港國中、鹿東國小、馬興國小等南管研習班。8/29在彰化縣武德殿舉行授證儀式，由王美惠縣長授證。青少年南管實驗樂團的成立，相對提供彰化縣轄內各國中小南管研習班在學或畢業學員另一個成長的學習環境，大家也樂見其成。
  </p>
  <p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有關獎補助部分，部分館閣熟知補助資訊與申請程序者，會比較經常提出申請計畫案，如臺北市政府文化局常態藝文補助，台北市和鳴南樂社團、中華絃管研究團、臺北市華聲南樂團等團體，近幾年經常提出有關傳習推廣的經費補助申請。振聲社「『振聲社』南管傳習計畫」、金門樂府傳統樂團「金門傳統音樂研習計畫」、南管新錦珠劇團「南管交加戲經典劇目《秦香蓮》大學巡演推廣計畫」等本年度亦獲國立傳統藝術中心111年度第一期補助。南管新錦珠劇團「南管交加戲經典劇目《秦香蓮》大學巡演推廣計畫」也獲得財團法人國家文化藝術基金會2022年第1期常態補助。這些館閣團體因為有獲得相關補助，所以年度計畫才得以更加順利完成。
