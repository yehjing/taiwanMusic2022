<h3>肆、南管年度重要紀事</h3>
<h4><strong>一、嚴重特殊傳染性肺炎疫情影響南管活動</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前述提及5/11疫情警戒升至二級，5/15臺北市、新北市疫情警戒首先升為三級，之後，5/19全國進入防疫警戒第三級，各級學校也暫停實體上課改為遠端線上教學，藝文場所防疫規範也隨之更動。5月正逢畢業季，5/14國立臺北藝術大學傳統音樂學系南管樂畢業音樂會「古韻新聲」場次直接受到影響，因此自室內演出緊急改為戶外演出。南管館閣也相繼宣布暫停上課、團練。三級警戒一直到7/27才解除，防疫調降至二級警戒。三級警戒期間，大家除了不得不出門工作、採購物資食材，大部分都盡量宅在家，實體的藝文演出、宗教活動基本上是停止舉辦的，南管相關活動當然也受到不小影響。</p>
<p><strong>（一）館閣活動停止</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5/15至7/27這段三級警戒的防疫日子，部分館閣傳習、團練改採線上進行，如和鳴南樂社、合和藝苑、南聲社傳習班、臺南社大台江分校南管班、高雄鳳山社大南管班，雖說線上學習效果沒有實體上課好，但至少學習沒有停止，有些館閣則是完全停止活動。展演活動有些直接取消，部分延期。和鳴南樂社原訂6/5星期六下午14點舉行上半年度成果發表取消。汾雅齋原訂5/28北港鎮安宮祝壽活動與6/8北港朝天宮三媽會過爐演出取消。廣益南樂社原訂5/29舉行的慶祝成立15週年整絃大會取消，延期至2022年辦理。心心南管樂坊「王心心吟唱－長恨歌」，5/29、5/30淡水雲門場延至10/23、10/24，6/12、6/13臺南原生劇場場次延至9/11、9/12演出。右昌光安南樂社原訂11/27、11/28舉行的「慶祝創社93週年整絃大會」，因疫情尚未穩定，唯恐有變化，為求防疫安全，於8/27宣布延期至2023年辦理「慶祝創社95週年整絃大會」。公親寮清水寺天子門生預計舉辦「歌清音．詠太平－2021太平歌陣大會親」也停辦。除了下半年因三級警戒，活動受到影響，上半年也有少許活動因疫情停辦，如3/6振聲社晚上19:30-21:00普濟殿燈會南管演出取消、內門新興庄集聲社預計3月底參加的內門南海紫竹寺觀音佛祖平安繞境也停辦。</p>
<p><strong>（二）「金聲相嶼南聲相隨－南聲社南管音樂金門巡演」中斷</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「金聲相嶼 南聲相隨－南聲社南管音樂金門巡演」為南聲社自主辦理的活動，巡演計畫於5/13出發前往金門烈嶼、5/16返回臺南，活動行程安排拜館行程與演出活動，演出場地由金門縣政府文化局、金門縣政府觀光處、金門國家公園管理處協助提供。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拜館行程：5/13晚上至烈嶼群聲南樂社拜館、5/14晚上至金門樂府傳統樂團、5/15上午至金門浯江南樂研習社拜館。演出活動：5/14（五）上午10:30金門烈嶼遊客中心－九宮坑道、5/15（六）晚上19:00金門金城鎮－總兵署。</p>
