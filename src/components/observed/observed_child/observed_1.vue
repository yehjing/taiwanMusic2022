<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年館閣依慣例舉辦的春祭、秋祭暨整絃活動，上半年沒受到疫情影響，春祭活動正常進行。海寮普陀寺清和社因為今年要參加5月底西港慶安宮辛丑香科，所以將參與香科的「入館」儀式整絃與孟府郎君聖誕祀典合併舉行，「入館」之後即展開密集練習，預計5月中前往慶安宮進行「開館」整絃。鹿港聚英社南樂團的春祭整絃，原本就會廣邀館閣參加，去年受疫情影響無對外邀請，今年適逢200週年館慶，於4/10-4/11擴大舉辦整絃大會，2天4場整絃活動，共邀請26個館閣團體參與盛會。合和藝苑春祭依慣例邀請兄弟館南聲社參加，就讀幼稚園的方秉棟也跟隨阿嬤黃美美前來，演唱〈老爺聽說〉，國立臺北藝術大學傳統音樂學系（北藝大傳音系）主修南管樂同學也到場唱曲。南聲社的春祭，除了應邀前來參加的館閣之外，臺灣師範大學民族音樂研究所呂鈺秀所長帶領研究生前來進行田野調查，以及執行高雄市南管保存維護計畫的施德玉教授也前來拍攝右昌光安南樂社與陳嬿朱老師參與整絃的影像，整絃一路歡唱到18:20左右煞譜結束。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下半年秋祭活動期間，雖然疫情警戒降回二級，然而館閣舉辦祭典還是有所顧慮，清雅樂府、南聲社秋祭以及和聲社恭祝水仙尊王聖誕的南管整絃活動皆取消對外邀請館閣，僅自家人參與。南聲社雖無對外邀請館閣，但南聲社傳習班、陳嬿朱藝師傳習班學員也多人前來參加，林毓汀理事長趁機安排介紹時間，讓資深館員與2個傳習班學員彼此認識，並拍攝大合照留作紀念，場面溫馨感人。</p>
