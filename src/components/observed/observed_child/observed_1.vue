<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南投縣立明潭國中傳統音樂夏令營，是由南投縣立明潭國中與國立臺北藝術大學傳統音樂學系合作舉辦的夏令營，活動時間為8/2-8/5，課程以南管、北管為主，因疫情關係，改成線上授課，由傳統音樂學系校友王依雯、陳咨伊教學。南管部分，透過講解與教唱，認識南管工尺譜、南管樂器，以及學習〈綿搭絮〉唱唸，並於8/5下午15點舉辦線上成果發表，發表內容是同學依照分配的部分事先錄成影片，雖然線上課程無法實際體驗樂器，但以〈綿搭絮〉選段為成果發表樂曲，同學以唱唸加上自創的肢體動作，也表現精彩。</p>
