<h4><strong>二、陳金潭榮獲第六屆「金門文化獎」</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門縣政府設置「金門文化獎」，是為表彰及獎勵對本縣文化有特殊貢獻者，採推薦或自薦方式，每屆評選出1-4名設籍金門、年滿70歲以上，長期從事文化藝術活動，有傑出事蹟且對家鄉有貢獻者。得獎者頒給金門文化獎章及證書以及新臺幣10萬元獎金以茲獎勵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第六屆「金門文化獎」3位得獎者：南管藝師陳金潭、詩書畫三絕許水富、版畫大師黄世團。11/28中午於金湖飯店舉行頒獎典禮，由縣長楊鎮浯頒獎，表揚3位得獎者的成就以及感謝對金門文化的貢獻。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳金潭藝師為金門金沙鎮斗門人，1944年出生。自幼在金門學習南管音樂，精通南管各項樂器，特別擅長噯子吹奏與唱曲，其嗓音清亮高亢，以正管演唱南管曲，行腔婉轉頓挫有力，為後輩學習效仿的男性曲腳代表之一。此外，其父親為職業道士，陳金潭藝師從小耳濡目染，所以也熟悉許多道教樂曲。早年，陳美娥女士創立「漢唐樂府」之初，曾網羅陳金潭以及李朝奈、李成國等3位金門絃友，隨團遠赴美國、歐洲、日、韓等國參與巡迴演出；中華絃管研究團也曾邀請其蒞館指導。目前為金門縣斗門南樂研習社、金沙絃管社負責人。</p>
<h4><strong>三、張栢仲、蔡芬得榮獲登錄臺南市傳統表演藝術「南管」保存者</strong></h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021/12/14臺南市政府文化局公告臺南市傳統表演藝術「南管」新增「張栢仲」、「蔡芬得」為保存者。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南市先前已登錄的有2009年1月公告「南管南聲社」為市定傳統表演藝術團體保存者及張鴻明先生為保存者，臺南縣2009年5月也公告「安定普陀寺南管」與「安定普陀寺南管團清和社」為傳統表演藝術團體保存者，縣市合併之後，臺南市2012年（民101）5月30日公告更正團名「海寮普陀寺南管團清和社」，多年之後，再次審議通過，登錄張栢仲、蔡芬得為「南管」保存者。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;張栢仲藝師，1945年出生於臺南南管世家，外公江吉四為南聲社的創立者、父親張古樹也曾任南聲社館先生。自小在南管樂聲中成長，直到初中畢業後才正式跟父親學南管。張栢仲承襲家族南管技藝，精熟各項樂器與唱，尤其擅長琵琶，其也是南管圈知名的四塊演奏者。2006年張鴻明藝師辭館，張栢仲繼任為南聲社館先生至今，目前也擔任喜樹國小喜聲社以及國立臺南藝術大學中國音樂學系南管指導老師，對於南管薪傳貢獻許多心力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;蔡芬得藝師，1945年出生，中學音樂教師退休，南管技藝師承張鴻明、蘇榮發、蔡清源等名師。蔡芬得不僅精通南管音樂奏唱，也熱衷研究南管音樂理論與教學法。目前為振聲社館先生，曾先後擔任高雄大社廣益南樂社、高雄茄萣賜福宮振樂社、臺南玉皇玉聖宮御音聖樂社、臺南大天后宮以群社等指導老師，對於南管音樂的推廣與傳承，投注不少心力。</p>
