<p><strong>（二）靈寶道壇的登梯奏職</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;登梯奏職是臺灣南部一種以架設36層刀梯，向天上取得職位與群眾認可的考驗儀式。最早起因自兩岸關係的中斷，道士無法親赴江西龍虎山授籙奏職，而採｢登刀梯」的方式，在梯上的頂端遙對祖庭的方向稟告，並筊杯以祈神明允諾和認定，象徵新科籙士通過傳法的考驗和試煉，與一般凡人不同，才能為地方百姓行濟生度亡的法事。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度團隊採集了5場的登梯奏職，分別是高屏地區靈寶道壇的郭博玄與孫溢泰，臺南府城系統的王浤儒，臺南溪北系統的林振賢與廖文福。當中有從法入道的新起之秀，也有傳衍數代的祖傳道士壇，更有背負父親盛名的「道二代」，及從小立志當道長的多年等待。每一支刀梯底下都有著一段故事，也記錄了在登梯奏職與奏職法會中所使用的儀式音樂，藉此探討作法各異的三個區域對於登梯奏職這件事情的道法觀念。</p>
<ol>
<li><strong>臺南安平道壇籙士王浤儒</strong></li>
