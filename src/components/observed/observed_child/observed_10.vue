<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第一天的《打血盆》在小琉球有著與臺灣本島不同的做法，也是小琉球人的喪禮功德最重視的一個環節。從下午5點〈開光〉血盆之後就必須不停止的搖晃血盆，直至半夜倒盆。而每一本經懺的「見靈」（詣靈）也都需要至血盆前再一次的呼叫亡魂，這樣的方式讓小琉球的打盆時間均是安排在深夜子時。道長必須使出渾身解數演揚法事，召請五營兵馬、步罡踏斗、宣讀符命，只為了等待神明的輦轎發動。過程中的〈拜血盆〉儀節，則是道長手持魂幡唱念【懷胎歌】，引領孝眷們伏地扣首120拜，以感念母親10月懷胎生育之恩。哀淒的唱腔配合著後場樂師的椰胡聲，讓許多孝眷在跪拜中紛紛落淚不能自己，現場眾人也跟隨歌聲感受了那份悲傷的情感。最後經過整夜的鑼鼓關輦，輦轎在凌晨1點的時候發動打破血盆，完成了《打血盆》的儀式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在第二天的《過橋》科儀之後，更是依照小琉球傳統做了《挑經》儀式。這樣的方式在臺灣本島已經罕見，主要是沿襲泉州「打城戲」中的〈雙挑〉段子。在過橋之後由由道長辜堃城與道士陳億進分別扮演「羅卜」帶著母親，與「雷有聲」帶著父親的骨骸要往西天過程的故事。首先由羅卜獨自出場，唱了南曲【生地獄】，詞句唱著「<span class="bkt">鬼門關透黃泉，亡魂到此心頭酸&hellip;</span>」接著雷有聲隨後出場，兩人自介身世，並開始以逗趣的對答與孝眷互動。過程中不時將問題丟給孝眷回答，引導孝眷理解深奧的臺語諧音笑話，以達到紓解孝眷喪親之痛的效果。經過一番的笑語言談之後，兩人開始出發前行。走了一圈突然殺出一隻老虎，師兄弟兩人則被沖散，剩下雷有聲一人獨自面對老虎，這段即是俗稱「打虎」的另一個段落。此時老虎佔據著雷有聲父親的骨骸，而雷有聲則開始以言語揶揄老虎，將老虎說成是貓、是豹，引得現場眾人不禁大笑。老虎則三番兩次阻擋雷有聲的去路，最後在雷有聲動用武力與老虎打鬥奪回骨骸的劇情下，結束整個挑經儀式。</p>
