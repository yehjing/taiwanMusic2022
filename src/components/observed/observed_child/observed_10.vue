<h3>參、臺灣離島的道法儀式</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次採集的資料計有澎湖5篇、金門9篇、馬祖4篇，如澎湖的西嶼輪普、金門的道法儀式互相交流產生的變化、馬祖的喜碟、做出幼、補庫等，皆是地方的特色祭儀。</p>
<p><strong>一、澎湖</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澎湖地區的靈寶道壇依照區域，大可畫分為本島、西嶼、二都（望安、七美）等3個脈絡。在演法科儀與唱腔上有些許的不同，雖然經過多年的交流合作，各區之間的相互差異已經逐漸縮小，但3個區域仍都保有自己的獨特作法。在實地調查過程中，採訪到方姓道士渡海至西嶼定居的道法傳衍故事，推論3個區域是因原鄉祖籍所造成的演法差異，這部份的研究仍待更多的調查資料來佐證。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在本年度的計畫以澎湖最西邊的西嶼鄉當地的輪普做儀式觀察，西嶼輪普為農曆7月時，西嶼鄉的數個村庄輪流普度，並由西嶼當地的合界混元壇、合界清靜壇、內垵玄文壇等3個道壇來主行一朝中元醮。團隊分別在竹灣、大池、小池、外垵、內垵等5個聚落觀察澎湖靈寶道壇一朝中元醮的儀式演法，其法事安排為：第一日傍晚拜海墘、放水燈、鬧廳，第二日早《發表》、《請神》、《度人妙經》三品、《三元水懺》三本、《請孤》、《豎旛》、《獻供》、《道場入醮》、《普度》、送大士。其中《發表》、《獻供》、《道場》、《普度》等都是相當能表現出澎湖道壇演法特色的大科儀，也是對於澎湖道壇的後場儀式音樂的初探。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外後場每日鬧廳的曲目分為「內排」與「外排」，這2種牌子分別於不同天輪流演奏，如第一日內排，第二日就外排，如此循環。依頭手吹李光輝先生所提供，內排曲目有：【小排】、【報馬】、【三通】、【點江】、【起軍大排】、【行隊】、【三獻酒】、【上下小樓】、【半空中】、【連環小排】；外排曲目則是：【三通雷禎台】、【七治子】、【安可耳兒】、【拿特將】、【三星為令】、【班子羌】、【二凡】、【帶月披】、【月望曲】、【鵝尾】、【得勝回軍】。由於頭手吹的樂器為大吹，因而在音聲風格上相當不同於臺灣後場樂，且吹奏技法上也有所不同。</p>
