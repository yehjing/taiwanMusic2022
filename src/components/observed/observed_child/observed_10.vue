<p>孫溢泰道長法名三泰，生於1981年（民70），祖籍為泉州府晉江縣塔頭鄉白礁村，先祖渡臺後定居於苓雅寮下寮地區。孫家是高雄市區相當知名的祖傳道壇，清虛道壇即為先祖留下之壇號，全盛時期曾同時存在3位高功道長，從祖父孫清俊至父親孫源寶到叔伯兄長均為道士之業。年少時拜入小港王金福門下學習道法，學成後於大坪頂購屋立壇，並常於行業圈內數個道壇擔任都講之職務。由於孫家已經多年未有高功道長，因此在家族的期盼與眾多道友的支持下，決定於今年的農曆10月於三塊厝三鳳宮廟埕設立起36層的傳統刀梯進行奏職大典。道主拜入相同出自孫家傳承的鳳山李勅豫道長門下，並由師弟吳宏文擔任刀梯通引官之職務。五師則是聘請中洲郭明玉道長擔任東臺教主、紅毛港林文魁道長擔任西臺教主、鳳山張錦敦道長擔任保舉師、屏東黃錦祥道長擔任傳度師。登梯奏職當天更是有著許多同領域內的道友前來協助，讓登梯過程能順利完成，現場是人山人海相當的熱鬧。</p>
<p>值得一提的一項道法傳統，是高屏地區靈寶道壇的「飛閱傳度奏職道場」法會的最後圓滿科儀《百神入醮三獻》，必須留置到新科籙士行香拜廟回壇來親自執行的，也是下刀梯後以高功身分所作的第一齣大科儀。科儀內容在「步虛」之後會有一段【衛靈神咒】的儀式段落，<a href="#_ftn5" name="_ftnref5">[5]</a>高功必須向五個方位取炁入自身體內，經由存思再彈出於壇場幻化，配合著都副講的咒語吟唱舞動旋繞手上的手爐使其爐煙運化。而在孫家所傳承的腳步手路中，此段在彈出之時還必須配合踢腳的動作，是一段相當具有藝術觀賞性，且富含道教深奧內密的精采科儀。</p>
