<p>張琨國道長法名大琨，1942年（民31）生，家族是傳承數代的祖傳道士壇。年少時便跟隨父親張景春學習道法，1968年（民57）拜入道主大社洪悅意道長門下。於社皮國小操場登刀梯奏職成為高功，當時聘請63代張恩溥天師親臨證盟造成地方上的轟動，混元道壇也因此成為張恩溥天師日後前往屏東地區常造訪住宿的地點，是萬丹、新園地區相當具有盛名的靈寶道壇。張道長早已退休多年，將道法與壇務傳承給長子張文豪與次子張朝榮。</p>
<p>張琨國道長不幸於今年8月發生意外離世，豎靈於萬丹社皮自宅，隔日即聘請有著世交關係的屏東黃錦祥道長前來替其主行《繳籙》科儀，出殯前日再由眾道友一同啟建午夜規模的拔度功德。值得一提的是由於張道長的意外身亡與水有關，因此在法事上的安排便有了《打水車藏》科儀，主要是為了拔度受困於幽冥水獄的魂魄所專屬的水亡儀式。</p>
<p>過程從早上的「水車藏開光」開始，接續宣誦三本水懺詣靈時的「牽車藏」，下午則由來自林園的黃鼎壹道長擔任《打水車藏》的主行高功。在科儀起初先行告下一道「水府救苦真符」用以超拔水魂速出水獄，接著在「繞車藏」時，高功以道曲【天尊哀憫】<a href="#_ftn8" name="_ftnref8">[8]</a>作為旋繞水車藏時的伴襯，在悠慢聲線的唱念下使得現場陷入一種哀戚的氣氛，讓家屬中同為道長身分的長子張文豪因思念父親之情而落下淚來。3次的繞車藏，高功藉著將魂身的向上拉引，象徵水魂已經逐漸出離水獄。高功則化身妙行真人進行「探關」，於五方龍王之間追尋魂魄去處，最終在〈拜車藏〉、〈召營〉、〈破車藏〉等儀式段的救贖之下完成整個《打水車藏》科儀。</p>
