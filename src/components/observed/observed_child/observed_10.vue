<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「慈字頭」佛壇勢力範圍影響了整個嘉義縣市的釋教優勢區。如水上、新港、大林、梅山等尚未採集記錄的鄉鎮，還包括了雲林縣的斗南鎮及鄰近的臺南市東山、白河一帶的釋教佛壇。擁有為數眾多的徒子徒孫立壇執業。而此嘉義地區釋教「慈字頭」佛壇的儀式與傳承研究，仍需更多的田野調查理解。</p>
<h3>結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年的計畫除了臺灣本島的道法儀式與音樂外，我們針對道法壇的儀式專家在回應社會和需求時，發展出的「應世之科」進行調查與採集；3座離島的道法儀式音樂；各地的儀式專家對於日漸流逝的儀式，堅持與用心地將之再現，在在都令人感動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;應世之科的出現，是儀式專家在社會面臨惶恐時，因宗教有撫慰人心之責任，每每發生天災人禍或是重大意外傷亡，生為宗教人總是希望為社會盡一份綿薄之力。儀式的核心價值，是如何藉由儀式的完成，能夠安撫和穩定人心，予人希望面對未來的勇氣，不應僅是關注在有效與否。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在疫情肆虐下，人與人面對面的機會大幅縮減，亦影響了面對面的儀式進行。年輕道長及新生代道壇紛紛推動各種網路法會的出現，讓人與人之間的距離更近；因應疫情設計出來的物件，讓生活壓力下有方法得以紓解。因此，疫情爆發期間，以網路聯絡為主要管道交往時，很快的都能被接受，產生一定的共鳴和效應。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3座離島的道法儀式與音樂，都發展出其各自的風格，與臺灣本島迥異，澎湖輕快的異國情調，金門的道法教二者在交流後的相互影響，馬祖的儀式傳承因學習的人少而可能面臨斷層。且離島居民在移民到臺灣後，亦會將原鄉的信仰與儀式帶到寓居地，如高雄地區發展出來的澎湖廟及澎湖道壇風格，這些人甚至是會回到原鄉澎湖演法；寓居中和與永和地區的金門人將原鄉信仰帶來外，亦會延請金門道法壇儀式專家前來設醮祈福；旅居桃園的馬祖人則是依然進行著原鄉的信仰與喪葬風俗。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;感謝本次給予協助的各儀式專家與樂師，願意提供資料，和儀式內容供後人學習、發展或保存研究。文化和傳統技藝的保存除是傳承外，更重要的是在無法抗力的流逝中能留下些什麼，為後續的研究提供資料和線索。</p>
