<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;收魂的主要目的是，將因犯神煞而四散的魂魄歸回復位，如《合竹收魂》、《轉竹收魂》、《打天羅地網》、《收斗魂》等皆是此類齣頭。《合竹收魂》以竹子的開合作為收魂信號；《打天羅地網》中，太保老爺與鬼卒展現趣味的對話式演法。《收斗魂》從為魂魄所備的物件來看：剪刀為怕魂魄受到天羅地網的牽制，故要把他剪斷；尺化作橋，如此魂魄可以過河；鏡子是因怕魂魄在烏天暗日下找不到路回來，所以要化作日月的光照路；龍角可化作船。另魂衣包著白米和銅板，再用12條黑線將其捆住。米是為了養魂讓魂魄可以有體力回來；銅板為魂魄所準備給把橋將軍的過路費，拿錢買路走；12條線是為了擔憂魂魄如掉入井中或山溝，這12條線就化作12條黑龍將他接上來。</p>
