<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澎湖的靈寶道壇在儀式音樂上的表現相較臺灣本島，不論道士唱曲或後場配樂都較為輕快，其嗩吶的音質與後場的節奏感都與臺灣本島風格有著迥異的差別。在儀式演法的節奏也較為快速，身段與動作有著屬於澎湖特殊的韻律感。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1908年起，日本政府為了臺灣南部的國際貿易與經濟開發需求，在高雄創造出大量工作機會。原本就已經常態性旅居屏東、安平等地工作的澎湖人，不再維持季節性往返的工作模式，紛紛把握機會轉進高雄尋求發展及定居，並將原鄉信仰與小法文化帶到高雄，甚至是興建廟宇及開館組訓小法福官。許多高雄的「澎湖廟」神明聖誕亦會聘請澎湖道壇前來酬神建醮，甚至是在高雄設壇執業。也因這層關係，給了高雄的澎湖道壇與高屏道壇有了互相交流的機會，許多澎湖地區的廟宇亦會延請高雄澎湖道壇前去澎湖建醮演法，形成一個「高雄－澎湖」文化圈。</p>
<p><strong>二、金門</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門鄰近廈、漳、泉地區，深受閩南文化影響。1882年林焜熿所著《金門志》〈風俗記〉中提到：</p>
<p class="bui">每年春季；迎天后，曰進香；多歲始舉行。夏仲，迎城隍，曰出巡；間五歲一舉。先期鳴金鼓，喧繞境內。至日窮華極移，閱遊鄉邨，粧飾人物執事，旌斾飛揚，音樂間作，人家置几櫝香楮甚恭。正神端拱輦上餘神馳輦擁進。旋廟，設醮演劇極夥。而王醮之名，尤加敬肅。鳩金延道設壇，塑遊巡神。<span style="font-family:'微軟正黑體'">（林焜熿、林豪，1993）</span></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文中能看出當時的金門人已透過延請道士或法司搭壇設醮，在每年固定或不固定時間，舉辦一次或多次的儀式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度共計採集新乩入禁與出禁、牽車藏拜藥懺、進桶盤、過布橋、登座普度、轉西方功德、拜懺等9篇相關道法儀式紀錄，其中道壇共有6篇，以齋事功德為主，法壇則有3篇。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門的傳統道、法教儀式專家分為道壇與法壇，現今大金門共有道壇3壇及法壇2壇，其中有2壇道壇亦有兼行法壇；小金門共有道壇2壇，並無法壇。金門地區的道士與法司<sup>15</sup>都有學習相關的經懺與科儀，2者皆有獨立執業的能力，並有能與之相配合的後場班底（林建育，2017）。金門的道壇傳承皆為靈寶道壇，以太上老君為祖師，執行的業務包含醮慶儀典與齋壇功德；法壇傳承則以閭山派為主，以許遜真人為祖師，僅能執行醮慶儀典，兩者最大的差異為法壇無執行中元醮與齋壇功德。</p>
