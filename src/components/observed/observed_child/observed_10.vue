<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;施燦道長法名正豐，生於1932年（民21），壇號為混元道壇。祖籍泉州府晉江縣衙口鄉，年少便跟隨父親施金嗓學習道法，後立壇於興化街上，是鹿港施家道壇傳承的其中重要一脈。晚年退休後常見擔任後場司鼓，於今年4月因病逝世，豎靈於自宅。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由家屬替其啟建大午夜規模的拔度功德，聘請鹿港通玄壇的吳桂照、吳陵勳道長父子前來主行科事。在鹿港地區所謂的「大午夜」，即是同時安排《度人對卷》與《頒降赦書》兩種科儀皆施行的功德法事。而在鹿港這樣的古老城市，對於喪葬習俗更是有著許多傳統堅持，像是由侄子替其糊製的大型「三魂七魄旛」就是鹿港的一項特色。出殯過程也是保留了沿街路祭，家屬身穿的白袍都是來自泉州原鄉的方式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法事內容特別安排了「大藥懺」的科儀，必須架起3層49盞明燈來施作，藉著燃燈懺悔的四十九願來替亡者作為解除罪愆。在〈滅燈〉的儀式段時，則是必須使用【將水韻】來唱頌3位天尊身騎神獸出天門的道曲。配合著道士的旋轉舞動手持扇子與布巾將燈火一一煽滅，象徵罪以消除病已無存，是相當精彩可看的一齣動態儀式。而在法事當日下午進行繳庫科儀焚燒庫錢時，吳桂照道長則是坐在椅子上向家屬唱出【別離親魂】<a href="#_ftn10" name="_ftnref10">[10]</a>一曲。全程使用泉腔，並運用字頭、字腹、字尾的咬字方式，搭配後場2位樂師的「大廣弦」樂器伴奏，頓時讓人感受到那份與親人別離的難捨之情。</p>
