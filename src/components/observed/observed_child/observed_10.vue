<p><strong>2.補庫</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「補庫」為馬祖地區特有，屬廟宇例行性的祭祀活動，依儀式安排的內容和目的，相當於臺灣的普度。舉行的時間多安排在在春夏交際的時候，即農曆4月底5月初，由神明擇定日期，或是擇日給神明決定，確定後張貼公告，通知欲參與補庫的信士，及補庫對象－好兄弟。這個時間的馬祖，因地理環境之故吹起南風，天氣悶熱潮濕，特別容易生病，而馬祖先民認為，這是吹南風的同時，南方海上的煞神或船號過境馬祖諸島引起所致。普品包括包子和麵糊，其中麵糊是為小孩亡魂的貼心設想。補庫不一定會請道士去做，也可以請當境的神明，以陳柏儒法師在華光大帝廟所行的補庫為例，早上啟請各方各境（本島）諸神，下午安排補庫。</p>
