<h3>肆、流逝中的道法儀式</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因為現代社會環境改變和工商社會時間不易安排，有些道法的儀式逐漸流逝，甚至是幾未能見，常隨著儀式專家的消逝而失傳，每每看到儀式再現，內心總是因其傳承而感動。本年度記錄了2場流逝中的琉球例齋事功德及1場再現的臺南靈寶道壇《挑經打虎》，希望透過實地的儀式紀錄，將這些消逝中的道法儀式，透過文字書寫及影像，不再只是保存在科儀文本及儀式專家的腦袋裡，讓更多人得以看到這些精采的道法儀式。</p>
<p><strong>一、消逝中的「琉球例」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;身為海洋子民的小琉球人，在拔度功德的作法上發展了一套異於臺灣的方式，這樣的方式在道壇行業圈內被稱之為「琉球例」，必須是熟知小琉球習俗的當地道壇來承辦。本年度小琉球的2個案例正是當地執業多年的林旭屏道士，替位於小琉球的澳仔口陳府與檨仔腳洪府兩處主家所安排的一朝宿啟拔度功德。在小琉球這樣的大型功德並不多見，當地人會以「功德清」來稱呼這樣的規模。由於是超拔九玄七祖的大功德，因此必須從臺灣聘請道長前來主行。與道士林旭屏合作的潮州正一玄妙壇辜堃城道長，家族為屏東潮州傳承4代的百年老道壇，已在小琉球執行法事多年，熟知當地的風俗作法。林旭屏道士也特別將兩場一朝宿啟功德的日期連續安排，方便臺灣道士團前來承辦。</p>
