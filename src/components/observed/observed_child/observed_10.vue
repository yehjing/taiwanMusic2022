<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次採集的《過布橋》儀式為澄清靖混元壇所演行，其為金城鎮埔後之在地傳統老法壇，現今壇主為陳梅濤法司，主要執行法務還有陳法司之徒弟陳金鑫。陳梅濤法司自曾祖父陳振遣、祖父陳朝允、父親陳金錠一脈傳承至今已久，加上授徒陳金鑫及其之授徒周世勳已有6代，自祖上一輩在金門執法醮儀之聚落已占有全島之半數以上。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《過布橋》（《造橋》）在金門的道壇與法壇皆會進行，結構可分為：〈請神〉、〈賣布〉、〈放兵〉、〈造橋〉、〈鎮橋〉、〈過橋〉、〈拆橋〉，並設有紙糊限官、橋頭將軍、橋尾土地各1尊。法壇大多在二日醮的第二天晚上執行；道壇除地方廟宇有要求外，大多在較大型的醮典才會看到。2者在演法的結構上亦有不同，法壇由〈賣布〉段開始進行，道壇則是在〈造橋〉段開始。後場部分，道壇僅有雙扇（北鑼、銅鐘）、鼓，不若法壇演法有完整後場配合。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「布橋」為一長板凳，以花布覆於其上，金門的道法教儀式專家說以前平常要做衣服不易，所以都會透過過布橋的機會，把這塊布拿去做衣服。橋上置壽金7張及少許米粒，橋下有一臉盆，內中盛水並放有蠟燭及銅板，為「長明燈」之意，橋頭與橋尾各安有橋頭將軍與橋尾土地。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儀式先由1位法司主法，到〈放兵〉段再加入另一位法司配合演法，設置1張特製的長板凳作為橋的象徵，並在橋前方設神案供奉紙紮限官、將軍與土地公，由法司〈啟請〉3位神祇，以及主掌各關限的星君降臨，並告知今日之演法目的。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;請神後便開始過布橋中最為精采的儀式段落，由法司手持令旗，模仿優柔的女性姿態於案前繞行，此時後場音樂亦配合法司之儀態，從鑼鼓轉換為細樂。後進入〈賣布〉儀節，法司拿出原先預備好的花布，與扮演師弟的後場配合，透過「賣買布白文」一問一答的方式，以及繼續採用女性的姿態賣布，從東、南、西、北4條大街沿街喊賣，但都沒人願意買，此時兩人覺得可惜了這匹好布，適巧碰上廟內神明千秋欲造金橋，故將此匹布贈與神明，供法司「布造金橋，過關度限」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法司先以淨水及鹽米淨化花布，再吹響牛角，象徵花布成為法器，具有靈性，並以威武的放兵動作來勅花布。此時另一位法司出場，先將花布釘於長板凳上，並將橋頭將軍與橋尾土地請至橋頭、橋尾處安位。2位法司手持令旗，化身天后娘媽，再次以優柔的女性姿態，不斷旋繞布橋四周，透過召橋兵安五方，在椅腳安鎮橋基使其穩固，並會將金紙上的米粒放入臉盆水中。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在〈鎮橋〉後隨即由法司手持帝鐘，並口誦一路會經過的關限，由法司手持王爺印信，帶領信眾手捧金紙走過布橋，後依十二生肖順序逐一過橋。過橋後須於信眾的後背蓋上王爺之寶印，象徵「過了關，度了限，過關限，保平安。」，並得到王爺與諸神的保佑。在所有善信都過完橋後，法司於案前《辭神》，將板凳上的花布取下，並以手將板凳搬離原本的位置，象徵拆橋。</p>
