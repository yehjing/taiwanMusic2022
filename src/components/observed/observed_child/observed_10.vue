<p><strong>三、太保慈德佛壇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;嘉義民雄松子腳黃府為了超度過世的母親，而特別禮聘太保後潭慈德佛壇黃瑞村法師來主行正一朝二天的功德法會。黃瑞村家中是太保後潭著名的釋教慈德佛壇，自國中開始跟隨父親黃登煌學習釋教科儀，先後跟隨臺中南屯洪清風與雲林大埤劉朝雄兩位釋教名師學習《蒙山》和《焰口》科儀。近年來接替過世的恩師主持民雄大士爺廟每年農曆7月的三朝普度法會，因此也接續了位於民雄地區的法事人脈。而這樣的一朝大功德規模在嘉義地區已經相當少見，2天的法事安排了《金剛對卷》、《地府赦誥》等釋教超度科儀。其中的《蒙山施食》更是大型功德才會開出的普度科儀。儀式開始會先於紙糊大士爺前唱誦【南海讚】，是一首以描述觀音菩薩形象為主的佛曲，曲詞內容相當優美。伴襯著嗩吶與胡琴的聲音迴響在黃家偌大的庭院中，著實令人感受嘉義釋教法師過人的音樂涵養。請完大士，法師們帶領孝眷回壇準備登座。在上座後的〈五方結界〉儀式是請來五方五佛藉以護衛壇場，而〈六天母〉則是以6種物品來做供養之意。經過一連串的〈啟請〉與〈召孤〉，科儀來到《蒙山施食》的〈施食〉儀式段，上師開始存想化食，兩旁僧眾則唱誦【刀兵偈】，形容各類孤魂所受之苦難，並且勸化孤魂早日皈依修行的詞句。僧眾們邊唱曲邊丟供品，最後在孝眷撿拾供品，化金銀錢後完成了《蒙山施食》大科儀。</p>
