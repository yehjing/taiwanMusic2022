<p><strong>三、金門的奠安儀式</strong><a href="#_ftn21" name="_ftnref21">[21]</a></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「奠安」為奠土安鎮之意，舉凡宮殿祠宇、宗祠家廟，或是民宅建築興建與重修落成時所做之儀式。聚落因建築與土地大興土木之際，將土地內原安定之土獸、土煞驚醒，再加上土地龍神之靈氣散失，故須進行一連串之慶成儀式來送走大煞，追回聚落龍脈之龍氣，以及其他祈求地方百姓宗族族民平安之祈安醮儀，故稱為「慶成祈安醮」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度因座向關係，僅有下埔下蔡氏家廟及溪邊溪峰鄭氏家廟舉行奠安。下埔下聚落以蔡姓為主要姓氏，並另有陳姓及其他雜姓。蔡氏家廟於2015年（乙未）重修告竣，由境主下埔下代天府擇定2022年（壬寅）農曆10月26日連27日，修設慶成奠安二朝醮會，農曆10月28日修設一朝啟扉醮，由澄清靖混元壇主行法事。溪邊聚落以鄭姓為主要之姓氏組成，另亦有郭姓。溪峰鄭氏家廟於2012年（壬辰）重修告竣落成，並由境主順境宮保生大帝擇定2022年（壬寅年）農曆11月16日連17日修設慶成奠安二朝醮會，農曆11月18日修設一朝啟扉醮，由閭山明法壇主行法事，為本次奠安慶典畫下完美句點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由於二壇的法事內容無太大差異（表2），在地方對於法事安排慣習有其差異外，如金門東半島慣於在下午黃昏時刻進行過布橋儀式，以利後續醮壇的收拾。另也會因奠安時間活動複雜，且多會重疊，故會依照現場狀況調整活動前後順序，溪邊溪峰鄭氏家廟原欲在第三日寅時啟扉，後因開家廟門鄉老身體不適，調整於子時啟扉。而下埔下因代天府剛進行醮慶完，王爺指示奠安時無須再做進桶盤。以下依溪邊溪峰鄭氏家廟慶成祈安醮的日程和法事安排做出說明。</p>
<p><strong>（一）第一日</strong></p>
<ol>
<li><strong>起鼓、掀樑、制煞、法奏</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「起鼓」為法事中所有儀式之開始，在起鼓之前，所有儀式用品如紙糊六獸與山神土地之醮騎、內壇之五尊紙糊神像：教導先民搆木為巢之有巢氏、象徵打石匠先師之九天玄女、土木工匠先師之魯班先師、泥水匠仙師之荷葉先師、觀其風水之楊救貧先師；四祧祖衣、六獸衣、畫像等皆須就定位。儀式專家由該建築之天德方起鼓，本年度下埔下蔡氏家廟及溪邊溪峰鄭氏家廟皆在家廟左後方，將失序的空間重新建構，同時將象徵原家廟空間內的所有煞氣之大煞送出。</p>
<p>起鼓完便是「掀樑」，由代表三祧師傅的大木匠師、打石匠、泥水匠分別進行「掀樑」、「點樑」、「押煞」及「拋送五穀」。大木匠師先掀起原包覆在大樑上之紅布，並以甘草水擦拭太極兩儀圖，再手持寶劍及白雞，口唸咒文，取其血為其太極兩儀圖開光，賦予其神聖性。打石匠手持寶劍及鴨，口唸咒文，取其鴨血來作押煞。最後泥水匠手捧裝有五穀、木炭、銅錢與鐵釘的盤子或米斗，口唸咒文沿八個方位拋撒。</p>
