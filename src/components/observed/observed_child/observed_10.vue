<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;廖文福道長法名羅福，生於1973年（民62），臺南新營人，沿用著父親的清淨道壇壇號。父親廖水來法名大旺，生於1951年（民40），父子兩人為新營地區相當知名的靈寶道壇。據廖文福敘述年幼曾於佳里塭內蚶寮永昌宮觀看林清隆道長的登梯奏職儀式，自此在心中留下深刻的印象，之後在跟隨父親的學道過程裡也都想著，總有一天也要登上刀梯成為道長。在人生幾次重大抉擇時都想過要舉辦登梯陞職，也曾擔任乩童登梯領旨時的先鋒官，直至鹽水護庇宮啟建了渡臺四百週年的七朝祈安禮斗法會，經由聖母筊選獲得主行科事之殊榮，讓他覺得時機已成熟，毅然決然於今年農曆11月在新營南瀛綠都心廣場設立起36層的傳統刀梯進行陞職大典。由所收之高徒嘉義劉益全擔任刀梯先鋒官，五師則由父親廖水來道長擔任傳度師、七股陳金臨道長擔任監度師、大灣李安雄道長擔任證盟師、北港張澤丕道長擔任引進師、佳里林清隆道長擔任保舉師，均是臺灣南部各區域的知名高功道長。為求慎重更是聘請六十五代天師張懿鳳親臨簪花，加上登梯現場的宋江陣、北管落地吹等陣頭，讓當日只有8度的寒流也被圍觀人群的熱情所抵銷。</p>
<p>登梯陞職之前的「登梯啟請傳度道場」，被行業圈內稱之為「刀梯腳」的重要科儀，特別聘請來自高雄湖內的高石柱道長來主行科事，讓此場登梯陞職成為同時擁有府城、溪北、雲嘉三個系統靈寶道壇的道長一同齊聚的光彩現象。科儀中的〈淨刀梯〉儀式段更是有著十幾位道眾一同進行，由引班道士李士弘手持寶劍與淨水領頭邁步向前走去，灑淨的隊伍於刀梯下穿花形成人龍，搭配高道長低沉渾厚的聲音緩慢吟唱【淨天地神咒】<a href="#_ftn6" name="_ftnref6">[6]</a>，使得現場頓時從人聲吵雜的公園成為了神聖的儀式空間，也象徵著36層刀梯已被潔淨成為聖物。</p>
