<li><strong>晉升高功</strong></li>
</ul>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;道士在入行後欲晉升成為高功道長，除科演能力能否被行內認定得以勝任高功一職外，無論道法二門正一道壇或靈寶道壇都需經過公開的儀式證明，並對外宣布以獲得地方上的支持和認同。道法二門正一道壇，師父會為徒弟安排一場開座首普的「大普賑濟」，讓該名門生登座施法，公開證明他已有主持醮儀、承領醮事的能力，臺南和高屏的靈寶道壇則多是以有無通過｢登梯奏職」的儀式來認定。</p>
<p><strong>（一）道法二門正一道壇的開座首普</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;位於臺灣中部以北地區傳承的道法二門正一道壇，有別於臺南和高屏地區靈寶道壇的登梯奏職，以「大普賑濟」來作為開座首普的晉升方法，亦可比擬為行業圈內的一種能力檢定。普度法事為一宣演斛食濟幽的法事，科儀的宣演含有大量的掐訣、咒語、步罡踏斗與存想發真之功夫，當能熟練的執行大普科儀則代表已經能夠獨當一面主行整場法事，便能取得「高功」或「座師」身分認可。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在本年度的採訪過程中團隊有幸參與了3位年輕道士開座首普的儀式過程，分別為臺北三峽廣遠壇的劉瀚澤道長、臺中大雅廣應壇的温淞富道長與臺中廣道壇的賴文彬道長。3位案例都是道界的新起之秀，而臺中部分同時也具有臺中正一道壇傳承系譜的研究價值，希望藉由詳細的記錄能了解「大普賑濟」科儀的音樂運用與開普首座的道法傳承意義。</p>
