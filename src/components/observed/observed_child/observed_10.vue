<p>郭博玄道長法名三玄，生於1994年（民83），家族原居梓官蚵仔寮，從祖父蔡錦蒼至父親郭俊男到胞兄郭博銘，一家三代均是當地相當知名的法師，擅長「觀轎問神」、「安營犒賞」、「進香取火」等閭山性質法術。由於高屏地區靈寶道壇自古便有道、法雙習的觀念存在，其祖父蔡錦蒼就是從法師更進一步晉升道長的良好案例，因此在年少的郭博玄心裡便有著想成為道長與祖父走相同的路的想法，於是選擇離開家鄉來到燕巢地區設立道壇。</p>
<p>從無到有打天下是一件艱辛的事，在經過數年的經營一切因緣也已成熟，才決定於今年的農曆5月於燕巢大勇巷重劃區路旁廣場上的空地設立起36層的傳統刀梯進行奏職大典。道主拜入與郭家合作多年的高雄潘福財道長門下，並由胞弟郭博仁擔任刀梯通引官之職務，同時也廣邀燕巢地區各大廟宇神祇來臨會場共襄盛舉，成為目前紀錄最年輕的道長，在沒有高功道長立壇的燕巢造成了相當大的轟動。在登梯奏職前一日的奏職法會更是有著許多同領域內的道友前來協助。也聘請了東臺保舉教主林園蔡朝祈道長、西臺監度教主潮州辜堃城道長、南臺證盟教主小港吳寶山道長、北臺引進教主大寮張思凱道長等前輩擔任五師職位，亦是近年來高屏地區靈寶道壇傳度奏職的一大盛事。</p>
<p>法會當晚按照慣例必須進行「開大營」<a href="#_ftn4" name="_ftnref4">[4]</a>，是由通引官郭博仁來執行。法事內容則有別於一般的開營召兵，過程中法師會手持金香單膝跪於地上向五方請來各路統兵元帥，此段的唱詞則是使用了【雲飛調】來詮釋。司鼓使用一種稱為「三腳鼓」的打擊模式來伴奏，整體的音樂速度會變得較為緩慢，卻也更顯為「開大營」的慎重神聖。</p>
