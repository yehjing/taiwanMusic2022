<p><strong>參、臺灣的法派儀式</strong><a href="#_ftn12" name="_ftnref12">[12]</a></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在道法儀式中，相對於道教儀式主法的道士，有著另一群或頭戴「額眉」或綁上紅頭巾，腰繫白色龍虎裙，赤腳，手執牛角，頸掛「法索」演法的法師，部分地區亦稱「法官」。主要負責調兵遣將，為人消災解厄、驅邪治病，或協助乩童辦事這類法事，目前暫以「法派」或「法教」稱之。其傳承脈絡複雜，多以傳法的祖師分類，如較常見的閭山法、普庵法、三奶法、徐甲真人等等，或以地方區分如澎湖法、鹿港法、臺南法、金門法等等，均呈現出法派在臺灣發展的多樣性和豐富性。本年度案例團隊將以臺南和勝堂黑頭小法、東甲北極殿福倌（澎湖宮廟小法）的《犒賞》和臺南閭山協安壇紅頭小法的〈調營〉儀式段作為對法派儀式音聲的初探。</p>
<p><strong>一、團體介紹</strong></p>
<p><strong>（一）臺南和勝堂</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;和勝堂草創於日治1931年（民20），分別由王魯、吳添丁、曾丁樹、蔣江淵、朱天妙等先賢共同建立，最初王魯曾參與敬和堂<a href="#_ftn13" name="_ftnref13">[13]</a>的小法團，因故而離開並與其他師兄弟創立金勝堂，歷經數年後，解散金勝堂後由前述五位先賢，取其敬和堂中的「和」字與金勝堂<a href="#_ftn14" name="_ftnref14">[14]</a>中的「勝」字，合名喚稱為「和勝堂」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;和勝堂主祀劉、黃、李三府尊王，配祀玄天上帝、清水祖師及張公法主等神尊。日治時期，疏開<a href="#_ftn15" name="_ftnref15">[15]</a>的年代，玄天上帝透過乩身告誡信徒空襲轟炸時躲避戰禍的方式，故時常擺設陣法並指示眾人帶上家當及眷屬，在臺南府城內外躲避，廟裡先賢亦感念神庥聖德，揹負諸位神聖金身沿路相互照應。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;戰後，由於當時信徒大都從事錏鉛製作等工作，廟體爰施以竹管為骨架，外觀再以錏鉛搭成，因此另有「錏鉛館」之稱呼。1946年（民35），信徒於普濟殿後一隅興建磚造神堂，並於臺南市政府登記為寺廟在案，其後幾經整建，於2015年（民104）舉行舊廟退火儀式，修建前官廳後觀音殿係三進兩落的巍峨瓊閣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;和勝堂小法團是廟內重要的無形文化資產，隨著至今創堂近百年的時光，小法團也經歷迄今三代法師的經營，一同協助王爺濟世，演法儀式包括「王獻大典」、「祈雨法會」、「消災解厄」消災解厄等儀式。</p>
<p><strong>（二）臺南閭山協安壇</strong></p>
<p>協安壇成立於1999年（民88）桂月，為火山觀進堂之小法團，由祖廟南廠保安宮耆老柯興先生傳授指導學成設於臺南仁德，同年成立第一屆小法會。奉祖師聖諭，於2000年（民89）春於祖廟南廠保安宮池府千歲降駕命名為「閭山協安壇」，由指導老師柯興先生書寫徐甲真人王令開光，為最早之開基神相，次年端月遷館於新興路。2003年（民92）擇吉雕塑開光徐甲真人金身，為今協安壇鎮殿神尊。因感祖師昭靈，庇佑萬方，衆小法於2005年（民94）咸議增雕徐甲真人金身，2006年（民95）增雕中壇元帥、池府千歲金身合祀，2009年（民98）遷館於北安路現址。今主祀徐甲真人，分靈自祖廟南廠保安宮，陪祀中壇元帥、池府千歲。</p>
<p><strong>（三）澎湖東甲北極殿福倌</strong><a href="#_ftn16" name="_ftnref16">[16]</a></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澎湖東甲北極殿在地人稱其「東甲宮」，有一說創建於明朝末年，在文獻的記載裡由當時的澎湖協左營守備趙廣於1690年（康熙29）所創建<a href="#_ftn17" name="_ftnref17">[17]</a>，並經歷康熙、乾隆、嘉慶及光緒等多次的重修。在二次大戰時曾遭美軍軍機的轟炸，東面的建築體嚴重的損毀，因為真武大帝的神威顯赫，留下不少關於戰時的神蹟故事，戰後也歷經1949年（民38）、1965年（民54）、1976年（民65）、2005年（民94）及2021年（民110）等大小規模的修護。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;東甲北極殿的事務管理分為頂芸、中芸、海尾芸（又稱下芸）等三芸來分擔，而澎湖著名的福倌文化，也是區分三芸進行法事的主持，其法脈緣自普庵派，頂芸的福倌主要是習自文澳的福倌系統，而中芸及海尾芸的福倌則是林石頭法師所傳授，雖然林石頭法師也來自文澳，但對於福倌的養成卻有不同的發展。東甲北極殿三芸的福宮，按慣例須輪流主法廟內相關法事科儀，但近年來中芸及海尾芸因係出同為東甲所傳法脈，故常聯合共同主事三芸所輪配到的法事，而頂芸的福倌則主要負責伺候真武大帝乩身扶乩的事務。自2019年（民108）後，由於頂芸的福倌較少協助，所以目前廟內包括扶乩與法事都由中芸和海尾芸的福倌一同合作辦理。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;代天巡狩信仰是澎湖的一大特色，「請王」和「送王」對於澎湖福倌來說是佔有重要的一席之地，其中也包括「王船寶艦」，舉凡「安船廠」、「王船出澳」或是「試帆駕駛遶境」等儀式皆由福倌主持。當代天巡狩駐駕後，廟中會建立一套所於代天巡狩相關事務的管理機制，除了人員組織與經費管理外，每月還有犒賞及送菜文化，在犒賞之前在還會在廟內動鼓祭紙，並恭請三十六官將的營座到廟前設壇犒賞。</p>
<p><strong>（四）澄清靖混元壇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澄清靖混元壇為金城鎮埔後之在地傳統老法壇，現今壇主為陳梅濤法司<a href="#_ftn18" name="_ftnref18">[18]</a>，主要執行法務還有陳法司之徒弟陳金鑫。陳梅濤法司自曾祖父陳振遣、祖父陳朝允、父親陳金錠一脈傳承至今已久，加上授徒陳金鑫及其之授徒周世勳已有六代，自祖上一輩在金門執法醮儀之聚落已占有全島之半數以上。陳梅濤法司年歲已大，現今混元壇主要的行法法司漸漸轉移給陳金鑫法司，而他現在也是金門在地相當知名的法司。</p>
<p><strong>（五）閭山明法壇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;閭山明法壇為金寧鄉盤山的傳統老法壇，明法壇壇主為張輝權法司，1960年（民國49年）生，平時以打石為業。1985年（民國74年）進入明法壇跟隨前壇主翁炳南法司學習法事，先從基礎的寫字、跑壇（雜務）做起，待穩定後慢慢學習站壇，後再學習鼓吹及琴弦等後場技藝。現授有其子張晁綱及陳銓等徒。</p>
<p><strong>二、以調營和犒賞為例</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《犒賞》主要是為慰勞五營官將護佑鄰里的辛勞，由於五營官將平時除了會駐守廟內，維持其空間的神聖不可侵犯外，也會受派前往廟外的空間以護衛信徒的平安，如內及外五營的區別，因此在進行《犒賞》前會配合其他小法儀式，即設置壇場以調請各路駐守的兵馬前來接受犒賞，並在結束後放營回歸原本的駐所。而壇場的布置上，廟方與信徒會準備各項供品，同時請出五營座與各營令旗，由法師及相關法務人員代替主神辦理《犒賞》，由於各法派的做法不同，自然就會有其不同的犒賞方式。在《犒賞》的程序上，由於本次案例是兩間廟的聯合演法，首先由和勝堂進行《清壇》，兩廟的法師調營和犒賞，最後由東甲北極殿福倌放營，所以有部分的儀節，由兩廟演法兩次，這也是與一般常態下《犒賞》有所不同。</p>
<p><strong>（一）臺南和勝堂和澎湖東甲北極殿聯合犒賞</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本段係以臺南和勝堂黑頭小法與澎湖東甲北極殿福倌，在不同地方法脈下聯合進行五營犒賞的科儀。《犒賞》對二者來說，主要也是同為北極真武大帝的信仰關係，透過雙方的法師溝通儀式程序，由二地小法進行犒賞交流與合作。和勝堂為尊重地主東甲北極殿，儀式結束後再由東甲北極殿的福倌放營各歸其所。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;和勝堂每12年，適逢龍年，便會舉辦王獻大典，其因緣乃自1938年（民27）堂內先賢前往南鯤鯓參香，回程路經西港慶安宮時，眾人欣逢西港香科辦理期間，與西港慶安宮丁丑香科代天巡狩千歲爺結緣。後遂於1940年（民29）設置行臺並啟建了首科「庚辰年王獻大典」，當時社會物資缺乏及日治時期庶民生活限制下，先賢亦謹慎且窮其一切資源，塑造紙糊千歲聖像及彩船奉祀，李府三尊王透過乩身告知，同於是年將奉旨出巡，為俾利廟務公事的推展，曾巧遇當時神靈雲遊至附近的玄天上帝，並邀請入廟協助且暫交予廟內一切事務委由玄天上帝暫管。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上帝公時常扶乩救世且神威顯赫，因廟鄰近的東北隅有一花圃，遂於當時有「花園仔 上帝公」之稱，當年的這尊玄天上帝也曾透過扶乩說明其經歷，其中曾在澎湖東甲北極殿行醫救世，信徒在了解後遂與東甲北極殿結緣，於1983年（民72）至1985年（民74），連續三年前往澎湖進香，民國90年代東甲北極殿進行重修，和勝堂也前往參與並密切的交流。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自1983年（民72）和勝堂首次前往澎湖東甲北極殿進香開始，迄今，至少前往參與4次的進香活動，而此次2022年（民111）的再度進香主要是中殿鎮殿張公法主前往澎湖觀音亭海域開光，儀式結束後翌日前往北極殿進香，基於每回進香的慣習，會安排犒賞五營官將的儀式。</p>
<p>有關《犒賞》的對象自然是和勝堂與東甲北極殿的五營官將，對於北極殿來說是主人家，而和勝堂則是來訪的交陪友宮，因此雙方的法師長皆有交流與儀式安排的討論。整場儀式的進行，除各自準備犒賞供品外，聯合犒賞的程序分為《清壇》（和勝堂）、《調營》（東甲福倌）、《調營》（和勝堂）、《犒賞》（東甲福倌）、《犒賞》（和勝堂）及《放營》（東甲福倌）等儀式。</p>
<ol>
<li><strong>清壇</strong></li>
</ol>
<p>《清壇》為請神之意，以和勝堂的小法《清壇》來說，音聲風格的表現基本上會有兩個段落，分別是【男咒】調和【女咒】調。前者主要係調請男性神祇，包括合壇將軍、清水祖師、開臺聖王、保生大帝、中壇元帥和勝堂主神、玄壇元帥、黑虎將軍和王孫三相公等，其中中壇元帥和玄壇元帥的節奏較快，主要是為呈現其威猛的神態。而黑虎將軍的唱調則是顛倒於其他男神請神的唱調，一般男神的請神咒分為上下段的唱調，原本下段的尾音會拖長音，在黑虎將軍的唱法上會在上段的尾音拖長音，下段則是維持正常音調，因此在男咒上會有一種音調三種表現方式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於女咒的部分，主要以觀音佛祖、天上聖母、祝生娘娘、陳氏夫人、蔡氏夫人、九天玄女、勤何李紀四位仙姑、娘媽等神祇。其中觀音佛祖唱調較為其他緩慢，而且會在每二句的上段中拉長高音，並加入聲辭，如原咒內容為「謹請觀音大菩薩&hellip;」，在和勝堂小法唱誦中則為「些耶，些宏兵，要咧謹阿請阿咧觀阿音，大菩囉薩阿，薩咧&hellip;」。而紀氏仙姑的唱調則有別於其他女神唱法，在節奏上也較短，可說是每位神明都配有其音聲的表情。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;東甲北極殿福倌文化中有關請神的唱法，依照速度上的區別，可以區分為【慢山寮】<a href="#_ftn19" name="_ftnref19">[19]</a>、【中山寮】和【緊山寮】等，請神多以【慢山寮】和【中山寮】為主，除非有緊急的狀態，福倌會和乩童配合，在特別的情況下採取【緊山寮】因應，調營則多以【中山寮】為主；另外《犒賞》的曲調則獨樹一格專用，在唱法上如犒賞不同營的區間，在上一營的尾音連續不斷音的轉下一營的起音。</p>
<p>在這次的聯合犒賞中，首先由和勝堂的小法進行《清壇》，並唱誦【太乙洞中神咒】，主要目的係恭請駐衛神祇蒞臨，向五方敕鹽米淨化壇場空間，再由北極殿福倌請神調請五營官將，隨後便分別由東甲北極殿和和勝堂小法，開始就東、南、西、北和中營等順序進行調營。</p>
<ol start="2">
<li><strong>調營</strong></li>
</ol>
<p>基於各宮廟有其五營的指揮權，故雙方各自調請自廟的五營官將。東甲北極殿適逢每月的15日會進行代天巡狩送菜、祭紙及《犒賞》等儀式，這是因為北極殿內也有代天巡狩駐守，故擇訂每月15日進行。</p>
<p>和勝堂的進香慣習，會在出發前進行轎前的《清壇》與調請五營官將隨行護駕。小法會由代表多稱以「中尊」的法師開壇，其他組成的人員包括有鼓手，至少2人；鑼手1人和巴鈴1人等人員編制，有些團體還會設置奉旨，故至少5人以上。在《調營》的程序上包括〈敕天皇尺〉、〈敕七星劍〉、〈開鞭〉、〈請神咒〉、〈調營〉、〈喝營〉、〈結界〉等儀節，除了〈請神咒〉、〈調營〉、〈喝營〉和〈結界〉是中尊和鼓腳一同唱誦外，其他的儀節皆由中尊所執行。在澎湖的福倌文化中，主要演法的代表稱為「法師長」，並有其他福倌包括大鼓手、奉旨及鑼手等，至少5人以上，所以北極殿的法師長在《調營》中，會在開壇請神後，分別調請本殿及代天巡狩的五營官將。</p>
<p>&nbsp;</p>
<ol start="3">
<li><strong>犒賞</strong></li>
</ol>
<p>五營官將的犒賞，是體恤日常官將們守護土地及信徒的平安，抑是協助廟內主神行醫救世之苦勞，所以一般臺灣民間信仰的宮廟會有進香或請火等儀式。對於兵馬來說是需要補充或增加靈力的，而《犒賞》則是透過備妥供品，進而供養官將和兵馬，祈求能持續的護佑地方。</p>
<p>澎湖福倌的《犒賞》會一一調請東、南、西、北及中營前往壇前領賞，也會犒賞本壇官將與合壇官將的兵馬，合計會有七次的唱誦；而在唱咒的用詞裡很清楚的表達，如:「吾今犒賞東營軍，東營將，東營軍馬未賞阿到賞場，千軍共一盞，萬眾共一杯，賞官軍，賞兵郎，犒賞東營軍馬賞了齊全，賞啊！賞軍又賞馬，賞馬又賞軍，賞了東營軍馬阿退後去，南營軍馬未賞阿近前來。」犒賞的對象除官兵外，還有軍馬的賞賜，當唱誦到「賞呀，賞軍又賞馬，賞馬又賞軍」會有專人以碗舀上米水，一一填注在椅條上的碗內，代表五營的兵馬受賞。在本次的聯合犒賞中，東甲北極殿小法在犒賞時，也一同在壇上和勝堂的五營酒爵上斟酒，意表這次是聯合犒賞，也代表身為東道主的北極殿犒賞和勝堂五營兵馬。</p>
<p>另外，有別於其他法派《犒賞》會在壇中安置五營令旗和虎爺外，東甲北極殿會自溫王爺殿<a href="#_ftn20" name="_ftnref20">[20]</a>中請出36官將座，而和勝堂除了請出五營旗座外，會再加上3組紙糊旗幟，並插置於由香蕉、紅圓、發糕、麻荖及餅乾等所組成的旗座。其中兩組放於壇中分別書寫東營九夷軍、南營八蠻軍、西營六戎軍、北營五祿軍及中營三秦軍等，與東營張公聖者、南營蕭公聖者、西營劉公聖者、北營連公聖者和中營哪吒太子等。第三組則是置於大菜宴席的供品桌上，上面分別書寫東營九千九萬人、南營八千八萬人、西營六千六萬人、北營五千五萬人及中營三千三萬人。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最後〈放營〉解除犒賞期間所束縛的空間，再經五營官將和兵馬各自領兵馬回本位駐守，也是犒賞儀式中最後的一個儀式。</p>
