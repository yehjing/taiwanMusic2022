<h3>肆、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度因疫情的相關防疫措施和活動管制逐漸鬆綁，許多的宮廟活動和道壇計畫於下半年如雨後春筍般的接力舉辦，使得道法壇的業務相當活絡，尤其在所謂「好日子」的日時吉良下，團隊必須一日趕好幾場，或是同時安排好幾人，只為不要錯過重要且具代表性的儀式紀錄。非常榮幸的團隊能夠參與並採集，極少對外公開的道士養成和道士羽化相關道途生命的儀式。從中團隊初步的探討不同道派如何在各異的做法上，展現其共同和不同的道法觀念。除此，團隊亦拓展道法儀式紀錄的範圍，嘗試理出臺灣法派的傳承系統在相同的地方不同的法派，和不同的地方相同的法脈下，其對儀式音樂運用的特色和風格。最後，為持續對離島道法儀式的紀錄，今年選以結合醮儀、宗親與姻親獻典之超大型慶典的金門奠安為主要觀察。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;面對社會的需求，道法儀式或因時代科技媒體的分享、交流、學習，或因時代環境的變遷將之簡化或作其他的替代。道、法壇在不得不面對生存和競爭下作出生態的轉型，從外觀的排壇精緻化、劇場化，到對經典史籍所載科儀的演法再詮釋等，無不是希望能從中創造和樹立屬於道壇的專業形象和品牌觀。為此，眾多在趨於一同的演法中，如原為北部正一道壇「入火安座」儀式的地方特色「安米龍」，透過媒介的大量宣揚打開市場需求，遂逐漸成為南部道壇演法的新傳統，又或是禪和派（福州正一）部分道壇特例吸收北部正一派《敕水禁壇》打雙鐧等。為此，除了面對這些道壇在演法上的應世變化，如何把握機會紀錄各地的儀式專家對於日漸流逝的儀式，仍堅持與用心地保存、再現，正是我們刻不容緩的紀錄項目，即呈現那原來地方道壇的特色科儀，或在相同科儀的比較中呈現不同地方具代表的特殊性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;感謝本年度予以協助的各地儀式專家與樂師，願意和我們一起對臺灣道法儀式音樂的保存和紀錄做出貢獻，一起為臺灣音樂寫下歷史。</p>
