<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從李翹宏的訪談中能看到，以前法司屬下九流，道士屬上九流，且法司是武的，專作《搶元神》、《起土》、《安胎》、《淨房》、《造橋》、《放兵》等儀式，演法的時候要赤腳甚至散髮；而道士是文的，束髮穿鞋，作醮讀經懺。後來法司教道士《造橋》、《放兵》，道士教法司作醮，所以現在大家都合作，但是法司只作一朝醮與二朝醮，只有道士才會作三朝醮。其中便能看到道士和法司曾經相互交流學習，這表示他們的儀式知識曾經重新分配過（李翹宏，2005，頁14-21）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門的法司不像臺灣與澎湖的小法僅能做小法事或較簡單的小儀式，而且大多和不潔的凶煞有關，如農曆正月敬天公讀經、起土、淨房、淨鄉里、淨船、犯煞、搶元神、收驚、栽花換斗、處理不易養育等。反而能夠接受各地廟宇的聘請前往建醮祈福。其職能已然類似道士，將官儀化的醮典科儀轉化吸收為自己的演法形式，更將原本自己的強項《過布橋》與《放兵》加入醮儀，使其在演法上更加豐富精采。</p>
<p><strong>三、馬祖</strong></p>
<p><strong>（一）馬祖四鄉五島的道法儀式</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前馬祖在四鄉五島中僅存有南竿的3位法師，以梅石分東、西2區，東區為陳高照和其孫陳柏儒法師，西區為馬港四維的劉治國法師。法師，以馬祖話來說，都稱「先生」，老師的意思。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2家法師的法源傳統和脈絡，陳家源於長樂，劉家則是平潭，因原鄉的傳統與習法的師承不同而有所差異。然所傳法本皆有小兒過關（出幼）、送王船、房屋上樑祭魯班、酬神等內容，亦有拔亡的功德法事安排。若遇有做事，一般多只是安排1天的法事，除非是重要慶典（多天以上），才會聘請中國的或臺灣的道壇過來主法，如鐵板天后宮慶成即聘臺南善化道壇鍾昂翰道長前來主行，其主要的原因是傳統中沒有這類或這麼多的科儀。馬祖基本上沒有所謂的立壇，都是家庭式的，為身兼型態，應民眾需求的時候才會出門做事。受到環境的改變和影響，現仍可見的法事僅有春夏交替時，宮廟所做的酬神和補庫，以及陳家與連江縣政府合作，在牛峰境五靈公廟舉辦的出幼&mdash;過限關成年禮儀式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳家從任法師始於清末民初，陳柏儒法師的曾祖父陳聯潮，法名脩由，往中國拜師習法後回馬祖行法；後傳二子陳高明（已仙逝），再傳至三子陳高照（1938年生），法名騰風；現傳至第三代陳柏儒，法名三儒，法職「太上正一三五都功經籙靈寶闡道演教仙官」。陳柏儒法師從國中起與祖父陳高照習法至今，高中時即任前場，大學於臺灣中壢唸書，直到當兵返回馬祖承襲法業，並於福澳華光大帝廟服務。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四維劉治國法師，1936年生，拜陳壽山為師。陳壽山為平潭人，父執輩即到馬祖發展，亦為法師。原劉法師在陳壽山的私塾上課，後因興趣，10、20歲就開始向其習法，由於陳法師的兒子沒有意思承襲，而將正明壇的壇號轉授劉治國法師，為第三代傳人。以前還有位同門師弟吳光國，任公務員，後因故沒作，在師父往生後5、6年，劉治國自己開始出來作事，沒經過奏職和授籙，自取法名「法成」。劉法師現無收徒弟，訪談中無奈表示，年輕人不願意學，也沒有興趣。除了鐵板天后宮和山隴玄天上帝廟的酬神和補庫，以及謝天地、拔亡法事外，劉法師亦曾被邀聘至西莒送王船。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2家的演法形式相近，前場一人手持法鈴，另一人負責後場鑼鼓，最多2人。唱曲沒有特定曲名，多以隨福州語字音的高低唱唸表現，並有區分拜神和功德的唱韻。在人手不足時就會請主家的人幫忙打鑼鼓，並交代他們鑼打幾下，鼓打幾下，有節奏就可以了。演法的法本，主要分喜事和喪事，拜神以閭山法為主，功德則是釋教，屬雙教。陳柏儒法師認為，福州的閭山法算是佛道融合，在請神或淨穢的呪也都有請到佛教（瑜伽）的神和呪語。由於功德法事屬釋教，在扮裝上會著海清或是袈裟，如在做《破地獄》的時候，即象徵化身為地藏王菩薩。對此，劉法師亦有表示，不同科儀的目的，法師的扮裝亦會有所不同。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度在陳柏儒法師的協助下，採集其道壇所行法事：喜碟祈福求子、補庫、做出幼。</p>
<p><strong>1.</strong><strong>喜碟祈福求子</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南竿福澳華光大帝廟的正月十五日元宵擺暝祭祀，與福澳村社區發展協會合作，結合紅斗祈福、求子等活動，為當年有新婚或是有生子的家庭準備喜酒及糖果、瓜子等供品祭祀臨水夫人，意在與神明分享喜酒。陳柏儒法師提取傳統做出幼科儀中之《延壽斗科》為信眾祈福。在臨水夫人座前依信眾所求裝花蛋後放入信眾個人的紅斗中，象徵由臨水夫人處請得的子嗣進入信眾個人的元辰宮中，以收求子之效。</p>
