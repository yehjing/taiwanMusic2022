<p><strong>（一）</strong><strong>竹灣大義宮</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在竹灣村的大義宮由合界混元壇的楊永村道長主行一朝的中元醮，楊氏家族於西嶼鄉合界村從事道業已有4代之歷史，合界楊家在澎湖與高雄都培育出了許多的道士與後場。自家可往上追溯至祖父楊光榮時就已在合界設壇，傳至父親楊中和與叔父楊中滿亦曾前往高雄從道，是目前西嶼鄉數個壇主中最為年少的一位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我們在此採錄了《普度》科儀前置的〈鬧座棚〉唱曲、於大士爺伯前的【三無量】、【三尊聖號】等道曲，以及普度科儀的最核心〈六供養〉。澎湖道壇大普科儀的〈六供養〉分為兩次，第一段的呈現方式是由道眾唱誦「<span class="bkt">吽字湧出香花燈塗果樂...</span>」等6種物品的詞句搭配高功拗指，打出6種手印供養三寶慈尊，此階段的供養是對神的供養。第二段的〈六供養〉是科儀末段以香、花、燈、漿、菓、飯等6種實物的供養，是用以超薦孤魂的供養。所有普度的儀式段落最精采的為【刀兵偈】的唱誦，除了曲子詞句形容各類孤魂的生動之外，此時也正是道士團向現場群眾施灑普品的時候。當都講緩緩唱出「<span class="bkt">修設齋筵，阿難因緣起...</span>」在旁道眾開始丟出零錢、糖果、餅乾給普臺下的人潮，是整場中元醮最歡樂且重要的時刻。</p>
