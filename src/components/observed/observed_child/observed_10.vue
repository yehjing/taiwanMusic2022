<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門的醮典以祈安醮為主，為地方廟宇神明千秋時舉辦，另外亦有天香醮（天公醮）、中元醮、開光醮、慶成奠安醮、王醮、觀乩身切結之謝醮、農曆正月敬天公讀經、新婚娘娘醮、成年祈安醮、刈香等針對不同對象所行不同目的之醮典（後三者較常見於小金門，大金門已少見）。醮典天數多以一朝與二朝為主，亦有三朝者，但多為較大型的慶成奠安醮與安座慶典才會開出三朝規模，最近一次為2020年料羅順濟宮安座所舉辦的祈安三朝醮。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;道壇最常見的一朝醮典法事安排為《發奏》、《祝聖》（請天公）、誦經、《獻供》、《放兵》、送天公、《收軍》；如二日則會增加《拜斗》、《晚朝》、《進表》（小拜表）等重要科儀；三日則是再增加《請南方火》、《捲簾拜闕》及《登檯拜表》（大拜表）等大科儀。法壇的法事安排與道壇大致相同，但法司因壇場所請神祇的關係，無法做《晚朝》、《請南方火》、《捲簾拜闕》及《登檯拜表》（大拜表）等大科儀，故無三日醮之規模。醮典中誦讀的經典則以《太上老君說常清淨妙經》、《太上說十一曜神咒真經》、五斗經（《太上說東斗注算護命妙經》、《太上說西斗記名護身妙經》、《太上南斗六司延壽度人妙經》、《太上玄靈北斗本命延生真經》、《太上說中斗大魁保命真經》）、《三官感應妙經》等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次計畫有許多儀式與脈絡的採集皆是向東門英妙靖普照壇訪談得來，其為金城鎮東門里之在地傳統老道壇，壇主為陳仲苠道長，主要執行道務還有陳道長之胞弟陳仲荊道長。陳家祖籍為泉州南安，自祖上一輩便以道業為生，曾在泉州設壇職業，傳承至今已有5代。陳仲苠、仲荊兄弟之曾祖為陳文禮，道職「正一盟威經籙九天金闕榮祿大夫清微通教使兼盼五雷院府事」，壇名為「清溪壇」，後因戰亂遷居金門，並在邱良功母貞節牌坊下（地名「石坊腳」）設壇。其子陳天乞以鑼鼓為業，未承道業，而傳至第三代陳金鎮與陳新來（原名「伯輝」，為陳仲苠2兄弟之父），陳金鎮習得其祖父之大成，陳新來亦在其祖父之薰陶下，對道業產生興趣，向其兄學習，後又向北門的吳貴士道長學習。陳仲苠、仲荊兄弟師承伯父陳金鎮、父親陳新來2人，現在已是金門在地相當知名的道長。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法壇因無做《晚朝》，故以《拜斗》代替。儀式從〈啟聖〉開始，後再進入《太上玄靈北斗本命延生真經》部分，並將《晚朝》及《獻供》中的「走壇」借至《拜斗》科儀的最後段進行，意為在朝禮南北斗星君後經過重重路程回到本壇。其演法形式與道壇之方式有些許不同，在「大聖北斗七元君」段增加2位法司一同朝禮南、北斗星君，由中尊跟二腳、三腳<sup>16</sup>輪流跪拜，道壇演法形式則皆為1位道士演法；且法司將《北斗經》的上、下部連貫演拜，中間不休息。在音樂詮釋的部分，2者皆為「北鼓起」的形式，後場襯以噯仔為主調的清樂，惟在進入文本後半段的「家有北斗經」段，法壇樂師換以大吹演奏，其音韻較為高亢熱鬧，與用噯仔的道壇樂師相比，別有一番風味。而「大聖北斗七元君」段的拖韻亦有不同之處，展現出金門道法壇演法的多樣性。</p>
