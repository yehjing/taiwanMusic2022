<p><strong>（二）在桃園的馬祖移民</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺灣桃園有著大量的馬祖移民，其不僅為從原鄉帶來的神明建廟，在生活上仍多保有和維持馬祖的俗例。居住桃園八德區霄裡的林府，從馬祖北竿遷居來臺，為超度高齡94歲仙逝的母親，循傳統特別禮聘熟知馬祖喪禮功德的清微宗壇林敏子道長，前來主持科儀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;林敏子道長（法名高文）1968年生，道法傳自父親林勇一道長（法名蘊德，1940-2019），與其丈夫林永淦道長（法名高白）於桃園市共同創立清微宗壇。林道長傳承的是福州一系的清微派道法，祖師王熥占道長（又名元如，1911-1990）。原是居於福建福州地區，戰後於1949，年來臺即隨著其師吳兆東一同渡臺，以致於落地生根，在臺灣發揚清微道法，現今於桃園一帶的清微道壇多與此一脈絡有關。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;功德法事與喪禮過程遵照原鄉馬祖的習俗辦理，並且開出了一朝規模的拔度功德，法事安排有起鼓、開香、《請神》、《發榜》、《召魂》、《燃點三途五苦迴耀輪燈》、《天醫全形科》、《作七祭靈》、《血湖經》、《血湖懺》、《沐浴科儀》、《破十方地獄》、《轉寶樹燈》、《過金橋》、《繳庫送轎》。其中包含了馬祖特色的《五苦輪燈》、《天醫全形》、《清微破獄儀》等法事，這裡以《清微破獄儀》為說明。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《清微破獄儀》科儀意涵即是奉請天尊至十方地獄，以神光破除地獄之幽暗，並且打開地獄之門救贖亡魂，引領亡魂早日昇天。儀式中高功頭戴五老冠，存想太乙救苦天尊，帶領孝眷旋行於各獄之間。同時由另一名道士在壇前述說十方地獄，之後道眾起韻唱【破獄讚】之該獄詞句，高功焚化符章，掐訣點指，在演唱結束的同時，手持法杖擊破瓷碗，象徵打開地獄的城門。如此形式一共反覆10遍，而每擊破一次都能聽見孝眷哭喊的聲音，希望藉著儀式，讓亡魂真能從十方地獄出離苦楚。在破除十方地獄後，高功持法杖快速繞行並擊破獄城，家屬緊隨在後，後場緊密鑼鼓以配合全場旋繞。最終繞回壇內禮謝天尊完成《破獄》科儀。</p>

