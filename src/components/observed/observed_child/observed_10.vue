<p><strong>（二）收煞類</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;收煞類的齣頭有《祭外方煞》、《弄火碗》、《翻土收煞》、《鏟土收煞》、《驅斬五虎》、《追送陰火》，意在藉由屘姑、師爺和五猖猛將的法力，將沖犯到的煞送出外方。《祭外方煞》為先禮後兵的祭煞，包含天狗、白虎、五鬼、空棺等諸類煞神，後將其送出。《翻土收煞》先以火碗象徵五雷火驅除邪穢，以草蓆翻弄並收除五方土地煞氣。《鏟土收煞》為收除五方土地之煞氣，將煞氣收入甕中後送出。《驅斬五虎》為針對事主所犯的外方白虎煞氣，進行驅斬。《追送陰火》中，法師以火筆去除事主身上之病氣，後為事主吞下體內不好的陰火，並送出外方。</p>
