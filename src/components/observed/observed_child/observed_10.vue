<sup>8</sup> 林威呈道長為中和枋寮威遠壇來臺第八代傳人（福建詔安第二十四代），屬臺灣北部道教正一派。家學淵源深厚，道藝精湛，6歲開始學習道法，迄今已超過30年，專擅醮儀中難度最高的《勅水禁壇》和《午朝玄科》。近年主持的醮典有樹林濟安宮東遷百年五朝清醮，及庚子年枋寮威遠壇200周年壇慶福醮。<br>
<sup>9</sup> 曹育齊道長從小對道士一職感到興趣，上大學後拜臺南和玄堂許銘楠法師為師，並向北部正一道壇蘆洲顯妙壇朱堃燦道長學習正一派科儀。2020年出版《潮牌道士》一書，將道法潮流化，打破傳統印象，用新的詮釋帶大家認識他的道法，並傳播儀式的精神是為帶給我們更多的正面力量：利益自己、利益眾生、利益其他的人。<br>
<sup>10</sup> 林睿竹道長，師承臺北普安堂的澎湖普唵派三壇小法，並向玉勅明德壇（原設於三重）蘇至明道長學習道教正一派科儀，後拜三重開基雷晉壇游堉典道長為師，承襲道場正一派科儀和法場三奶派儀式。2017年於蘆洲設壇，辦理的法事小至收驚、補運、祭改，大至宮廟酬神建醮、中元普度等等，為北部正一道壇新生代道長。<br>
<sup>11</sup> 後龍普玄壇祖壇源自先祖吳曹，嘉慶年間從同安渡海來臺，落腳後龍設立道壇。傳至余吳萬成時，開枝散葉，傳衍道脈，再傳吳鎮雄、吳鎮國兄弟2人。現任壇主為吳驊峰，法名羅通，自幼便跟隨父親與叔叔學習家傳道法，掌壇後將普玄壇聲望推向高峰，成為苗栗海線最著名的靈寶道壇。<br>
<sup>12</sup> 守真道房自西元1893年（道光己亥年）家傳道法至今，根據田野調查所得之手抄本及口述道壇資料，追溯至第一代習道始祖乃為陳尚，其學道因緣頗為特別，為由儒轉道的一種典型。目前竹山守真道房由第五代陳東成道長主持，並傳至第六代長男廷佑、次男廷彥，傳承家傳道法、擇日堪輿、陰陽法事與道教科儀禮節。<br>
<sup>13</sup> 吳政憲道長，法名羅錠，為府城祖傳延陵道壇第五代傳人。2003年於嗣漢天師府奏職，授職為「太上三五都功經籙三天輔佐仙官兼金闕御前陰陽通傳事」，2007年在開基玉皇宮閱籙登梯，其父吳永欽道長和祖父吳西庚道長均為府城知名道長。<br>
<sup>14</sup> 董明杰道長原任軍職，19歲開始接觸紅頭法，先師事林崁法師；25歲為提升自己的道法能力，跟著家族叔輩楊宗斌道士學習道務和演法；在28歲之際，楊宗斌道士登梯奏職晉升道長的同時，正式向楊道長拜師，並退出軍旅生活，全心投入道業。直到2017年38歲，董道長在赴宜蘭三清宮向三清道祖表達意願獲得認同後，決定登梯奏職晉升道長，同年年底於歸仁大人廟保西宮登36層刀梯閱籙。<br>
