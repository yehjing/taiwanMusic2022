<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;栗子崙的陳府為其過世的母親所追修的百日功德，開出為期2日的午夜功德，法事內容已接近一朝，是沿海地區少見的大型午夜規模。其中專為女性亡者所進行的《打血汙》科儀（又名「打沙」），是嘉義釋教相當具特色的科儀之一。在釋教的觀念中女性在世時不免會有汙穢褻瀆天地，過世之後就需以此科儀解救墮入血汙地獄的亡者。儀式過程先以細砂布置沙城，作出圍繞魂身的城牆形態，這正也是俗稱「打沙」的由來，並由一名法師坐地盤腿宣誦《大藏正教血盆真經》，帶領孝眷代為懺悔母親罪過。接著由主法法師以目連尊者的裝扮出場打沙，在一出場的自介便表明了身分及事由，所唱誦的南曲【行出靈山寺】更是配合著劇情，形容目連尊者外出遠行的開端。過程中所經之路為「黃泉路」，來到「奈何橋」，前往「望鄉台」，每一地點都是「目連救母」故事中的場景，亦是民間信仰中對於陰間地獄的描述。到此演唱了第二首南曲【告公公】，講出目連尊者來到岔路的心酸感受，詞句動人心弦，曲調緩慢憂傷。最終目連尊者來到了鬼門關前，再唱一首【懺亡魂】曲子，讓儀式現場的氣氛開始熱絡。面對把守關門將軍的索取錢財，演出了一段被質疑真假尊者的過程，只得宣說經文證明自己的真實身分。在儀式演繹的同時也講解給現場的孝眷知曉，是一種藉儀勸善的效果。接著經過紅衣使者的指引開始尋找亡魂，經過東門關牛馬將軍的刁難、西門關金童玉女的放行、南門關福德正神的報路、北門關地藏王菩薩的慈悲，在諸位角色的共同演繹之下讓一場儀式劇更顯生動有趣。最後目連尊者終於尋得亡魂，帶領孝眷一一擊破沙城關門，並且引領孝眷飲下象徵血汙的紅色飲料，讓孝眷報答母親辛苦的生育之恩，是一種傳統孝道於拔度儀式中的美好體現。儀式最終法師以錫杖打破沙碗，象徵結束完成了整個《振破血汙》儀式。</p>
