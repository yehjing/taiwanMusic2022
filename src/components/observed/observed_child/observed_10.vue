<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以善化道壇鍾昂翰道長<sup>2</sup>於4月29日在善化慶安宮所行的祈雨法會為案例說明，善化慶安宮每年媽祖聖誕日都會啟建三朝禮斗法會，今年因應旱災，配合臺南市政府舉辦37區聯合雩祭祈雨大典，特於禮斗法會前一日舉辦祈雨法會，啟奏上蒼，祭天祈雨，祈求上蒼能降雨以解萬民旱苦之災，祈禱旱沛甘霖，消弭一切災疫。本次法會科儀安排如下：發表（《靈寶清醮發表科儀》）、請神（《靈寶禳災水醮祝聖科儀》）、稟告上蒼（《祈雨疏文》）、請水（《請水科儀》）、《太上三元賜福赦罪解厄妙經》、《太上元始天尊說大雨龍王經》、靈寶太乙祈雨道場（《靈寶太一祈雨醮儀》）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一早起鼓後，先進行《發表》及《請神》，呈奏表文，發文諸天神祇及三界高真，請功曹符使速為傳達，稟告此次舉行法會的目的。焚化表文和關文，啟請諸天聖真降臨壇場證盟。特殊的是本次迎請與雨水及降雨相關的神祇，除四海龍王與風、雨、雷、電四神外，更請來上清紫微碧玉宮太一大天帝、太一真官天順君、江瀆廣源王、太一清靈川真人、東嶽天齊仁聖帝、紫微碧玉宮內院右仙龍王、紫微碧玉宮內院左仙龍王、紫微碧玉宮內院直事一二三使者、當所土地正神、日直功曹使者等神祇，讓這次祈雨法會更加殊聖。《發表》、《請神》後，道長於祈雨臺上恭讀祈雨疏文，向上蒼稟明下界蟻民祈雨之願。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;休息片刻，道長引領眾委員先至慶安宮的荷蘭井請水，再將聖水倒入特別設置的「四溟缸」內。缸外貼有象徵人頭龜身、人頭龍身、人頭魚身與人頭蛇身等四神獸。在道眾宣誦《太上三元賜福赦罪解厄妙經》及《太上元始天尊說大雨龍王》後，開始《靈寶太乙祈雨道場》，呈進祈雨表，朝謁玉皇天宮，望上天能開恩赦罪，降下甘霖。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;道眾於御桌前啟請聖真，三獻後「入意」，稟報此次法會事由。再由高功「三啟禮」，以三跪九叩首之大禮參覲玉皇上帝。後進入〈發爐出官〉的儀節，高功手執手爐，點指放文。引班舞劍花帶出高功，由道長五方〈勅水〉、〈召四靈〉，出外呈表遣送功曹，將祈雨表文送至上蒼。在道長帶領委員朝拜諸神〈進茶〉後，道眾做出「三演三舞蹈」的朝禮動作，跪伏於地，三呼萬歲。道長再腳踏「召龍致雨罡」，進入本次祈雨儀式的特殊儀式段。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;道眾將燒紅的磚塊，放入四溟缸中，使蒸發的水氣昇天，委員再以竹竿攪動缸內的水，希望風、雨、雷、電四神能降下甘霖，紓解旱象。焚化表文之後，回壇禮謝師聖，完成祈雨道場。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;所謂的「應世之科」，其是因靈寶古法中所沒有，而根據世俗之需新創設的齋法。在不同時代中，道壇都有其「應世之科」來解決社會所遇到的疑難雜症，尤其在面臨天災人禍時，或是圓滿社會的需求。隨著時間的推進，資訊的傳播與交換也越來越發達，「應世之科」的被運用和需求也越來越高，甚至成為道壇彰顯其自身特質和風格的標的。</p>
<h3>貳、補運法會</h3>
<p><strong>一、傳統大補運法會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的道壇、道派生態蓬勃且多樣，主要分有正一派和靈寶派。從科儀中若干特徵性法事的不同演法與法源，說明臺灣的道教儀式存在正一法與靈寶法兩個系統的道場科儀（呂錘寬，1994，頁321-355）。<sup>3</sup>林振源將臺灣道教概分為南、北二大主流傳統：「<span class="bkt">臺灣北部傳統為『專門吉事』，專做吉祥的紅事（醮儀、驅邪儀式）、不做白事（喪儀），一般稱『正一派』，還有一個獨特的教派名稱『道法二門』</span>。（林振源，2017，頁123-152）」謝聰輝：「<span class="bkt">臺灣南部傳統則紅、白事都做，一般稱『靈寶派』，俗稱『烏搭紅』。</span>（謝聰輝，2018，頁476-478）」由於北部正一道壇兼行道、法二門，部分雙教壇（兼行道教和釋教）外，以正一派自稱，依傳承脈絡可分為林厝和劉厝二派<sup>4</sup>，但因二派長期交往、交流、交融以來，多已「劉骨林皮」或「林骨劉皮」，即在宣演上已較無明顯的差異。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;正一道壇除拜神類道場科儀外，兼行補運類法事，尤以帶有醫療性為個人所做的大補運。在醫療不發達的年代，遇有身體上病痛或任何疑難雜症，民間多會找法師協助和解決問題。換句話說，在現代醫療健全的體制下，要能一睹大補運法會有其困難度，目前多是在神明的旨諭下才有機會辦理。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法場的排壇，空間分為中壇、左壇和右壇。中壇掛有三幅，分別為陳、林、李三奶夫人。左壇由內而外依序掛有閭山法主、長生大帝、盧太保，右壇三幅則是王母娘娘、註生娘娘和盧二娘。另設有三界桌，拜天公、謝三界、《奏斗求壽》等儀式都在此進行。法場早年多在信士家中設壇，現因都市化種種的因素而改在道壇或廟的後殿進行。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法師的扮裝依所行法術的祖師和派別而有不同。三奶法，法師綁上紅頭巾，代陳、林、李三奶夫人之名收「煞」；又或依法事內容如《追送陰火》，法師除了紅頭巾外，還會綁上2串高錢作為頭髮，並著上黑裙，象徵演法者代屘姑娘行法。其他如《奏狀》和拜天公則是身著道袍，以示對神明的尊重。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法場的法事結構安排流程為請神、驅煞、收魂、祈福，除啟請各方神明的《請神》、請水淨灶的《安灶》、向天界投送狀文的《奏狀》、藉由勅給符令為信士保平安的《勅符》外，依法事的目的可分為卜卦類、收煞類、收魂類以及祈福類。</p>
