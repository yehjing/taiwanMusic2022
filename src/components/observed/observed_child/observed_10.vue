<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賴文彬道長法名山秀，臺中北屯人，繼承了恩師的廣道壇壇號。年少時先拜入臺中法天壇吳清忠法師門下學習閭山法術，後再拜入廣道壇許柳土道長學習道法二門建醮相關科儀。作為廣道壇的關門弟子，開座首普這件事一直也是師父的最大心願。此次的開座首普幸逢臺中大雅六寶的廣澤宮啟建重修40週年暨斗姥太歲殿入火安座的三天法會，廟方決定聘請廣道壇賴文彬道長來主行科事。在這樣良好的因緣促成下，決定於今年農曆8月的三天法會末日來進行開座首普。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法會內容包括了《發表》、《祝燈》、《安龍》、《拜天公》等等常見的中部正一道法二門科儀。而在重頭戲開座首普之前的「請師」，賴道長更是慎重的請出書寫有道法二門前傳口教歷代宗師法名的掛軸設壇祭拜，上頭也明確的記載自師父往上追溯的傳承可推至清末的豐原廣應壇曾家祖壇。此次開座的地點大雅六寶的廣澤宮也正好是40年前由師祖臺中名道西屯廣應壇羅阿墩道長主行入火安座三獻醮的廟宇，如此的意義非凡正是在歷史場景寫上新的歷史。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;開普過程由許柳土道長親自牽扶賴文彬上座，臺上扶座則有師兄蔡弼安、師叔温淞富等人。登座的一刻現場眾人歡聲雷動，都共同分享著這份傳承有望的喜悅，雖然是第一次的大普，但臺上所執行的科儀仍絲毫不得馬虎。臺中正一道壇的大普科儀末端有著一段很長的【刀兵偈】，內容是針對各式各樣的孤魂加以形容並召引壇前受度享食，而在科儀本經過數代的傳承重抄後，也加入了新時代的亡魂類型。其中就有著一段召引車禍與空難亡者的新召引文被安插在其中：「車禍撞死、空難墜機，諸等啊孤魂，專等今宵，來受啊甘露味。」而這樣的與時並進其實正也是臺中正一道壇所具有的靈活演法特色之一。</p>
