<p><strong>二、疫情下三級警戒的法事線上直播</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年，臉書社群為傳統行業的道壇帶來跨地域性客源，以及知名度、名聲和人氣，為傳統道業和道教教儀的推廣起了一定的作用，眾多道壇紛紛成立自己道壇的臉書粉絲專頁。2021年進入5月，臺灣在嚴重特殊傳染性肺炎（COVID-19）防疫上因疫情持續嚴峻，中央流行疫情指揮中心在5月15日宣布雙北進入三級警戒，隨後19日全國跟進：「全面禁止進香遶境活動、聚會，其他宗教活動實聯制。」道壇亦不例外。時過兩週，宮廟相繼對活動停辦、順延，在看不到解封的時間表，道壇紛紛開始運用臉書，或線上為個人服務的貼文，或在符合國家的政策下，以最少的人員，在自家的道壇，以直播的方式辦理法會等等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;簡單的說，正一道壇兼行道、法二門，只行拜神類和補運類法事，不做新亡的齋事，靈寶道壇主行紅、白二事，偶有兼行祭煞、補運類的法事。或許正因為道派業務的對象不同，當政府宣布禁止宗教活動和宮廟祭祀時，直接影響了正一道壇的生計，多數道壇一來擔心開壇被人舉發，一來也憂慮是否會被信眾傳染，多採休息和不開放的方式度過此段三級警戒。靈寶道壇則功德法事正常運作，惟規模變小，採低調行事，相對來說受影響的層面較少。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;針對5月19日全國進入疫情三級警戒，到7月26日微解封止，在此三級警戒期間，符合中央疫情指揮中心的條件下所辦理的道壇業務，並於臉書動態活動公開貼文者，計有：李游坤和臺灣丹心弘道會廣遠道壇<sup>5</sup>、蘇至明和明德道院明德壇<sup>6</sup>、林光洲和理丞道長<sup>7</sup>、林威呈和中和枋寮威遠壇<sup>8</sup>、曹育齊和臺北集神道壇<sup>9</sup>、林睿竹和南泉明法壇<sup>10</sup>、吳驊峰和後龍普玄壇祖壇<sup>11</sup>、陳廷彥和竹山守真道房<sup>12</sup>、吳政憲和府城祖傳延陵道壇<sup>13</sup>、董明杰和明杰道壇等<sup>14</sup>。依照對象的發文可將其動態貼文歸為：共修和抄經、線上直播、發放結緣物3類。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;面對疫情的嚴峻，信眾無法到宮廟焚香參拜，然而學校的實體上課，可以用網路社群軟體來完成，宗教活動是否也能夠如此？5月19日到7月26日三級警戒期間，除神明生外，經有6月14日的端午節、6月15日的天赦日，以及7月15日天貺日半年節，民間習於行補運的大日。據與道壇的口訪，日期尚未到，私訊已不斷進來，都希望道壇能為其服務祭改和補運，透過宗教儀式「去壞運轉好運」，讓身心能夠安穩不致恐慌，祈生意能夠恢復正常，使未來的日子可以好過些。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以線上直播中最受矚目的每年農曆6月初六日（亦有做6月初七日者）天貺日為例，臺北地區常見於此日進行消災解厄與補財庫的儀式。這天玉帝會大開南天門，賜下珍貴財寶給予凡間世人，但世人在等不到珍寶之際，更以為上天會降下冰冷的霜雪來懲罰人世間。臺北地區信眾會到廟中設案祭拜天公以及補運，希望玉帝能夠開恩赦罪，原諒世間凡人的罪過，並賜福人間，因此人們認為在這天祈求上天百姓所願時也較為靈驗。信眾在這天，多會準備三牲四果等供品前往廟宇祭拜祈福。許多宮廟也會舉行祈福法會，透過誦經禮懺來替信眾消災補運祈福。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從臉書社群動態得知，該幾日共計有芝山巖惠濟宮、樹林濟安宮、臺北集神道壇、竹山守真道房、仁德牛稠仔保華宮、臺北府城隍廟等單位在儀式進行時以道長個人臉書或粉絲專頁直播；另有林光州道長與南泉明法壇未採直播方式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;芝山巖惠濟宮今年的開天門拜天公慶典，一切祭儀皆遵照防疫規範辦理。為避免群聚，不開放信徒參拜，以線上直播方式辦理，全由廟方代為準備供品。農曆六月初六日晚上10點半，僅由主委代為奉香向上天祈求，並延請中和枋寮威遠壇林威呈道長為民眾祈福。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北集神道壇為遵守防疫原則，以線上報名、遠端直播參加，避免人群之群聚與接觸，儀式照辦、誠心不減半。農曆六月初六日下午3點，由壇主曹育齊道長為參與善信祈福。道長在儀式開始前特別說明天貺日開天門補年中運的由來及相關祈福事情，也說到年中的這天是一個重新整頓，在下半年不好的運勢能夠漸漸好轉，走上好的軌道。這次消災會的會員共有52人，其餘開放線上的朋友計有120人一同線上參與。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北正一道壇的開天門補運儀式一開始皆由道長淨壇、祈請諸神，道長一面敲擊單音，一面以道教莊嚴的音韻誦唱經文，並請來諸天神祇以及廟內諸神。請神畢，隨即一一宣讀寫有參與信眾姓名、住址的疏文，恭望玉皇大帝及天地眾神能保佑境內及各信眾平安順利，也於特殊的疫情下，祈求嚴重特殊傳染性肺炎瘟疫能平息，早日回到平順的生活。最後將符令過香火，請諸神加持神力，分給參與的諸信眾。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;竹山守真道房往年原是受宮廟邀聘而辦理補運法會，這回第一次於道壇舉行，主要仍是受疫情三級警戒的影響，不得有宗教行為，而臉友在宮廟停止辦理後，主動向道壇詢問，經道壇評估，採線上直播辦理，亦呼籲臉友配合政府規定，不要到現場參與隨拜。</p>
