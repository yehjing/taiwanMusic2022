<p>三、<strong>靈寶道壇的繳籙儀式</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有開始就有結束，在本年度的計畫中，團隊針對道士生命的相關儀式作了相當多場的紀錄，其中對於道士羽化的《繳籙》科儀採集了2位屏東資深老道長的案例，分別是屏東市普照道院的黃潘合寶道長與萬丹社皮混元道壇的張琨國道長的拔度儀式。二者皆是行業圈內曾登刀梯獲取奏職認可的高功身分，因此對於高屏地區靈寶道壇所使用的十二道「陽平傳度公牒」有著一套專屬的處理方法，也試圖去釐清屏東地區的道壇傳承系譜。</p>
<ol>
<li><strong>屏東黃合寶道長頭七乙夜功德</strong></li>
</ol>
<p>黃合寶道長法名大裕，1940年（民29）生，家族是傳承數代的祖傳道士壇。年少時便跟隨父親黃潘朝雨學習道法，1966年（民55）拜入道主楠梓洪天財道長門下，於屏東代天府廟埕登刀梯奏職成為高功。40餘年的道士生涯所收高徒為數眾多，遍及高屏地區上下，整理抄集百餘本的科儀叢書，曾擔任高屏教區道紀協會創會理事長，是高屏地區靈寶道壇下淡水道最具代表性的道教領袖人物。晚年退休將普照道院傳承給次子潘錦宏與孫子潘駿霖，而黃道長則在臥病多年後於今年7月離開人世，豎靈於屏東市福居園。頭七之期由徒子徒孫替其舉行的乙夜功德，便包含了「繳籙」的相關儀式，聘請有著師承關係的楠梓洪欽祥、洪智強父子道長前來主行科事，功德起初的《請神》科儀就有高達7位高功下場。</p>
<p>《繳籙》科儀由徒弟歸仁鍾振榮道長執行，過程中道眾吟唱【青華號】<a href="#_ftn7" name="_ftnref7">[7]</a>道曲讚頌太乙救苦天尊之慈光，高功則步「朝天罡」緩緩旋繞於壇場之中。接著外出登上所擺設的三界天臺之上，於臺前宣讀繳籙疏文向上天稟告，在旁的助手鍾道禎道士則協助焚燒十二道的「陽平傳度公牒」，此儀式段正是繳籙的核心動作「將公牒上繳天庭」。在高屏地區靈寶道壇的道法觀念中，奏職高功的離世是前往天庭擔任所屬官職，因此公牒中所述內容則多為委請官將護送正魂以保赴職、官餉配員馬匹糧草數量等等與職位相關的申告文體，這樣來自原鄉的傳承文書也僅見於此一脈絡，是高屏地區靈寶道壇的一大特色。</p>
<p>最終在十二道陽平傳度公牒均焚燒完畢後，家屬以紅色布袋將其灰燼裝起，便於入殮之時放入棺中。再由洪智強道長進行《開通冥路》科儀，依序告下五方開通道路符命，只見高功手持金古紙點燃火光帶領孝眷與魂身繞壇5次，象徵道力冥光已接引亡魂回歸靈堂，完成了今夜的頭七魂路與《繳籙》科儀，靜待出殯前日的一朝宿啟大齋功德。</p>
