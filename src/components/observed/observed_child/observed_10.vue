<p><strong>（二）</strong><strong>大池角治安宮</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大池角治安宮的一朝中元醮，由合界清靜壇翁順福道長主行科事，翁家於西嶼鄉合界村從事道業已有數代之歷史，往上追溯至清朝時期湖西鄉後寮村1位方姓道士。方姓道士時常往返湖西與西嶼之間渡船做法事，深感交通不便，於是選擇在合界定居設壇。後因家族因素改姓翁林，並將道法傳衍2姓後代，綿衍傳至翁道長父親翁克進手上繼承家業，為西嶼地區相當知名的老道長。翁順福後將道法傳授給徒弟楊朝舜、楊志業2人接棒，與合界村的楊氏家族也有相當長年的合作關係。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該日的《發表》科儀由楊朝舜道長主行，由翁順福道長親自以大鼓起鼓。主旨為向三界發出3封方函、3道牒文，告知眾神此日、此處將修設一朝的中元醮。儀式在清澈的【步虛】聲中開始，道眾吟唱「<span class="bkt">寶籙修真範，丹誠奏上穹</span>」的步虛詞，同時也表明了《發表》科儀的意涵，接續的【祝水白】、【中山神咒】都是為了清淨壇場，使其成為神聖之地。儀式當中最為特別的是【金光神咒】的吟唱，由澎湖特色的嗩吶頭手吹作為伴奏，都講、副講一人一句接續唱著咒語。高功捧著表文從頭頂、面前、胸前3個地方做出「拜表」的動作，搭配著澎湖道壇常用的輕快小調起身拜表，再將表文入爐焚化完成《發表》科儀，是一段令人隨曲調律動的儀式段落，亦是展現澎湖道壇音樂特色的科儀。</p>
