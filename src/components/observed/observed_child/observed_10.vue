</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《進表》為醮典中末日清晨之行事，呈表上奉昊天金闕至尊玉皇上帝，述意設醮事由、鄉佬姓名、設醮之功德，藉由表文來傳達至帝前，並表白設醮祈福願景。後由王爺主持送天公，將天公壇及紙糊南、北斗神像與金紙一同燒化。畢隨即進行《請六獸》，召請東方青龍星君、南方朱雀星君、西方白虎星君、北方玄武星君、上方勾陳星君、下方騰蛇星君蒞臨壇場，護衛此建築之神聖空間。並將內壇有巢氏、九天玄女、魯班先師、荷葉先師、楊救貧先師之五尊紙糊神像迎請至六獸案桌一同祭祀。後返內壇進行《安后土》，地方儀式專家又稱其為「請土地公」，誦唸《太上老君說安土妙經》，安鎮本地的山神、土地，使鄉域清平。</p>
