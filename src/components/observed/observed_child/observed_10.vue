<p><strong>3.做出幼</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「做出幼」為福州話音譯而來，源自福州地方風俗。儀式大致分做2種，第一種解開小兒關煞．如幼兒出生即命帶關煞，可能會不好養，或是遇有甚麼急迫性，若不化解恐會危及生命，或發生不幸之事，遂需禮請法師至家中做法化解。另一種為成年禮，多在新人結婚時所做，通常是男方做。在馬祖是以「戶」為單位，傳統認為，男生成家立「戶」，才稱得上為成年。與臺灣婚前的拜天公不同，這裡於結婚後舉行，且祭祀對象為臨水夫人，亦帶有求子意味。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統出幼儀式較為繁瑣及複雜，通常是到主家家宅裡做，法式安排有《發奏》、《啟聖》、《延壽》（禮斗）、《祭關煞》、《調兵》、《過關》（過限關，闖關門）、《請奶》（請臨水夫人和36婆姐）、《謝宴》等過程，要耗費一整天的時間。按口訪，2位法師都提到，以前做出幼很頻繁，現在則沒甚麼人，一是不知道要做，一是年輕人不大相信這個。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2011年（民100）起由連江縣政府文化處與國立馬祖高級中學合作，於新生入學時，在牛峰境五靈公廟辦理「做出幼」，近似臺南的「做十六歲」成年禮，邀請陳高照和陳柏儒法師，透過傳統儀式替新生祈福。在與政府合作辦理與推動下的做出幼，陳法師遂將傳統一整天繁複的儀式改為著重祈福性質的法事安排，但仍保留傳統上較重要之環節，如《延壽》、《過關》等。</p>
