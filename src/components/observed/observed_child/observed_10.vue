<p><strong>（一）卜卦類</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;補運中卜卦方法有：拋法、箸卦、水卦、米卦、碗卦，以及拜天公的擲筊，當次都在卜同一件事情的吉凶，但以不同的方式解不同的方向。拋法為與四季有關，卜短、中、長期病情的吉凶。箸卦判斷依文本所示：「隨手平安好；箸尾長，三朝午日平安過；雙頭平長，有凶之時便有險。」碗掛則為預測患者的病況，以5個碗，取其陰陽，判別其醫治的方向。</p>
