<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「磚契」，又名「陰陽契」。儀式專家在兩塊方形尺磚上記載該建築的座向、四至、吉祥話，由外向內逐行書寫，不論橫、直，每一行一律24字，相當有規律，這和回文詩的排列頗有異曲同工之妙。其上還有見證人（境主）、主行科事之法司、鄉佬等。磚契寫好後，尚須塗上一層桐油，以防脫落。待一切準備就緒後，將兩塊磚契面對面綁在一起，其繩子外圈之格數亦為24格，象徵「二十四山」山神守護，並在儀式專家指引下，適時埋於大廳正中央供桌下方靠牆部位，並填以火鼎炒過之潔淨細沙。最後插上大把的柳枝，招來吉祥平安。</p>
<ul>
<li><strong>送六獸、化龍</strong></li>
</ul>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「化龍」先到家廟後方用油鍋的火將龍神燒化，再將六獸及山神土地等八尊醮騎與金紙一同燒化「送六獸」。</p>
