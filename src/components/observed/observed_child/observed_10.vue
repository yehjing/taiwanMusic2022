<p><strong>（三）</strong><strong>小池關帝廟、外垵溫王宮、內垵內塹宮</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小池關帝廟、外垵溫王宮、內垵內塹宮3廟的中元醮則是都由內垵玄文壇的洪耀幸道長主行科事。玄文壇於西嶼鄉內垵村創立道壇已有數代之歷史，道業自其父親洪媽武手上傳承，是內、外垵兩村大廟的專任道長。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「入醮」是整場中元醮的核心儀式，在澎湖道壇的作法是以《道場》作為中元醮的「入醮」科儀，演法過程相對繁複。高功在道場初啟時會步起「九鳳破穢罡」，默念【三淨咒】，有一段內煉的訣要與精彩的動作用以【變水訣】。爾後道眾旋繞壇場，交班清淨，請神降臨後如同古制的道場一般。道眾諷誦起了【衛靈咒】，高功手持手爐，點指放文，進入〈發爐出官〉的儀節，延續東晉時代古靈寶派的內煉功法。接著是仿效古代皇帝進朝禮儀的〈三進朝〉儀節，澎湖道壇的後場很擅長做「弄吹」的音效，藉由手指與吸、吞氣的方法，讓嗩吶發出抖動的長音，配合澎湖道士特有的韻律感動作，讓所謂「三演三舞蹈」的動作更真正貼切呈現了舞蹈。在整冠、整衣的禮儀後，道眾唱著當地稱呼的【道場歌】，一步一步依序進朝，走入內殿，再用後退的方式一步一步依序退朝，是很傳統的道教禮儀動作。最後在道曲【金字經】悠悠緩慢的唱誦下，圓滿入醮完成《道場》科儀。</p>
