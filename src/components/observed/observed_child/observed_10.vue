<h3><strong>前言</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年的計畫除了臺灣本島的道法儀式與音樂外，我們亦針對因應社會產生的「應世之科」與3座離島，及流逝中的傳統道法儀式進行調查與採集。5月因疫情未獲得完善的控制，政府規定廟會、遶境須配合疫情的相關規定與辦法停辦，進而影響到原有醮典儀式的進行與地方廟宇的年例活動。這幾個月亦發展出網路直播法會的形式，至8月時才逐漸放鬆，各地的廟會活動開始恢復，故本年度上半年以齋事功德為主要的採集對象。本次計畫採計道法教儀式音樂共62篇，其中計有澎湖5篇、金門9篇、馬祖4篇，皆是地方的特色祭儀；另有釋教儀式6篇，為嘉義地區的釋教儀式紀錄；我們亦採集到1篇祈雨法會及2篇網路直播法會，為回應旱災與疫情影響下的社會，道壇所衍生出的法會形式與內容。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過實地調查的廣泛蒐集，盼能對道法儀式和傳統儀式音樂的保存盡一份力量，除是傳承外，更重要的是希望在難以回復的流逝中能留下什麼，為後續的研究提供一些資料和線索。</p>
<h3>壹、應世之科</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時入辛丑年（2021）臺灣面臨56年來最大的旱象，在久旱不雨，各地紛紛水情告急。地方政府除提出限水措施抗旱外，亦有與宮廟合作舉辦祈雨法會，盼能解除旱象，遂相關祈雨法會為本年度的觀察重點之一。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因大甲鎮瀾宮過去曾舉辦祈雨（1963年5月11日），3月7日受行政院農業委員會農田水利署委請辦理祈雨法會，鎮瀾宮請來天上聖母二媽、貞節媽及五穀仙帝等聖真，向玉皇大帝祈求降下甘霖。儀式過程中，信眾穿著白衣，以象徵古禮的「披麻戴孝」，顯現出大旱猶如喪失親人般悲痛，祈求上蒼垂憐，降雨解旱。儀式由臺中廣安壇林瑞東道長主行，並以知雨之鳥「商羊」鼓舞的意涵，象徵大雨將要到來。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自此，各地宮廟或聘請道長舉辦祈雨法會，或將「祈雨」二字加入現行年例的神明祝壽法會中。直至4月29日，由臺南安平開臺天后宮為主壇，臺南市37區同時假各區的大廟，或聘道長，或自行讀宣祈雨疏文，共同舉行「雩祭」祈雨大典。其中有聘道長法師主持的有：主壇安平天后宮王德和道長、歸仁仁壽宮周忠信道長、關廟山西宮賴泓銘道長、善化慶安宮鍾昂翰道長、新化朝天宮許弦丕道長、大內石仔瀨天后宮王秋堂道長、後壁下茄苳泰安宮陳榮振道長、將軍漚汪文衡殿林青雲道長、龍崎下坑清水宮楊朝富道長、學甲慈濟宮黃炳樹道長、楠西北極殿張事民道長、六甲赤山龍湖巖三壇法師黃信賢、玉井北極殿三壇法師（不知名）。而僅由廟方主持的有：西港慶安宮、八份姑媽宮、北門南鯤鯓代天府、七股龍山宮、新市永安宮、鹽水護庇宮、左鎮北極殿、南化天后宮、白河三官寶殿、佳里金唐殿、仁德中洲天后宮、永康鹽行禹帝宮、下營北極殿、柳營代天院、麻豆代天府等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;依祈雨法會安排道法儀式的實地調查，道壇目前作法和法事安排有以下幾種類型：</p>
<p>（一）以祈雨文疏行拜天公三獻為主，合以古禮懸掛「商羊」，象徵雨水的到來。如：3月7日大甲鎮瀾宮及3月19日和美德安宮等由臺中廣安壇主行的祈雨法會。</p>
<p>（二）以安龍神為體，如：4月25日至29日松山慈惠堂鎮興道壇林元啟道長主行的辛丑年母娘文化祭慈惠龍神甘霖護國祈安法會中的安五龍，及4月24日至5月2日三峽區文聖壇主持的玉皇紫微三官九龍會集大法會中的安九龍等祈求甘霖。</p>
<p>（三）以經典史籍中的科書為憑，如善化道壇鍾昂翰道長於4月18日在臺南大灣武龍宮，以及4月29日在善化慶安宮所行，設祈雨臺和四溟水缸，以宣誦《太上元始天尊說大雨龍王經》及演拜《靈寶太一祈雨醮儀》等法事安排，祈求上天降下甘霖。</p>
<p>（四）其他，如：4月29日臺南安平開臺天后宮由王德和、王浤儒道長主行，安排了置天臺向上蒼稟奏<sup>1</sup>，並以商羊和水缸，由主祭者黃偉哲市長澆灌勺水，安平開臺天后宮主委盧友禮搖動商羊，象徵商羊起舞，鳴雨引來雨水。5月8日永康洲仔尾保寧宮禮聘光明街壇陳聰敏道長主行祈雨法會，道長先開光保生大帝、禹王大帝、水府大帝等紙糊神像，並特別安排了保生大帝所派符令加持的聖水，由廟方人員登上鋁梯，由上而下潑灑聖水，象徵上蒼已經開始下雨。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有祈就有謝，臺南市政府在各區祈雨法會後，陸續降下甘霖，水庫逐漸豐盈後，於8月7日進行雩祭圓滿叩謝神恩大典，並進行三獻儀式，再度邀請黃偉哲市長敬呈「雩禳請雨疏文」，代表全體市民向上蒼謝恩，由王浤儒道長迎商羊至鹽水溪出海口，循著溪水回返天庭。大甲鎮瀾宮亦於11月18日的下元年例謝平安時進行祈雨謝天儀式，感謝上天降下甘霖，緩解旱災。</p>
