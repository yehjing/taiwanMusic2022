<p>林振賢道長法名羅賢，生於1979年（民68），將軍西甲人，延用家族的演慶道壇壇號。父親林青雲法名大壽，生於1950年（民39），師承將軍金登富道長，1980年（民69）登梯陞職，為臺南溪北系統靈寶道壇相當知名的高功道長，擅長糊紙工藝亦曾主行多場大型醮典。近年來由於年事已高故有萌生傳承之意，決定讓其子循著40年前的方式舉辦「陞職」。籌備期間卻因疫情而延後了1年，眼見主行的3年1科佳里金唐殿五朝王醮又即將到來，最終擇於今年的農曆2月於當地境主大廟漚汪文衡殿廟埕設立起36層的傳統刀梯進行陞職大典。聘請有著師門關係的義竹蔡聰德擔任刀梯先鋒官，五師則由父親林青雲道長擔任傳度師、七股陳金臨道長擔任保舉師、臺南蔡其財道長擔任監度師、布袋黃火山道長擔任引進師、漚汪王明賢道長擔任證盟師，均是出自同一傳承師門的當地知名高功道長。登梯陞職與陞職法會2天，更是有著許多同領域內的道友前來襄助。</p>
