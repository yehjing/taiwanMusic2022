<p><strong>（二）臺南閭山協安壇的進錢補運法會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;或在宮廟神明聖誕祝壽中安排，或在鄰家酬神、還願所用，或為個人禳災解厄、保運植福，「進錢補運」實為臺灣民間常見且受信眾熱衷的補運儀式。本次案例採11月20日於臺南協安壇所做的一天進錢補運消災法會，由歐進斌（法名羅巽）法師主行。該日法會安排的法事，依疏文寫有：《請神》、《太上老君說五斗金章受生經》、《調營》、《祭星》、《進錢補運》、《探元辰宮》，參與人數近30人，屬協安壇專為壇內會員不定期所舉辦。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;協安壇對法事演法可分為以中尊1人為主，配有後場鑼、鼓，除此亦有以團體的表現方式，即中尊1人，其他鼓腳4到6人或以上，但需雙數配對，分別手執法仔鼓、鑼、合尺共同演法。常用的曲調有：【合壇】調、【本壇】調用於請神，屬慢板表現；《調營》則分「文界」和「武界」一般皆可用，以及針對調請女官將的「香花界」的運用方式，還有犒賞官將《賞兵》時所用的【調營】調。這裡擬以該日《進錢補運》的《調營》說明。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《調營》是為調召東、南、西、北、中的五營兵馬，在兵馬來壇點清俱足後，法師才會接續進行《祭星》法事。由主行法師依序手執五方營旗調請，其程序為〈香白〉、〈開咒〉，最後以「開安位眾神咒」來安鎮五方。以〈香白〉調召東方為例，協安壇該次採用中尊1人搭配後場演法，故採半文半武的賞兵【調營】調，內容如下：</p>
<p>一調東方甲乙寅卯木，行兵諸迅速，點點各軍兵，照諸樣都若，東營軍馬請耒九夷軍，九夷將九夷軍馬，九千九萬將，軍馬九千九萬人，人人頭戴頭帽身戴甲，手執中鎗青炎旗，細龍車，龍車嘈嘈兵馬到，排兵走馬到壇前。神兵火急如律令。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>按歐法師的說明，《調營》的演法可分為數種，為一活用的演法，依憑法師的能力及視當下時間、人員多寡及氛圍作運用。</p>
