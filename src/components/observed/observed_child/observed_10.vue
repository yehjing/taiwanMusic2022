<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;劉瀚澤道長道號「高兌」，1994年（民83）生，由於地緣的關係， 2007年（民96）先向三峽廣遠壇王宏睿道長學習，2014年（民103）經王道長轉介拜三重正統雷晉壇游宗樺道長為師。求道十年，本次登臺首座在師父游道長與臺北木柵忠順廟的九年祈安大慶斗醮主行蘆洲顯妙壇朱坤燦道長商量後，於5月10日在斗醮第三日舉行登座大典。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該日午後首先由其兄劉家齊領著館閣藝陣猶如新科狀元般的遊街，將各單位祝賀的大匾送至忠順廟的會場。待一切準備就緒後，劉瀚澤道士在父母的陪同下於道法二門宗師壇前禮拜宗師斗，分向引進師王宏睿道長、保舉師朱堃燦道長、監壇師三重雷晉壇高景清道長和師父游道長行三跪九叩答師禮，並由其師游宗樺道長向宗師報告今日徒弟開普一事。接下來的法事宣演與一般《普度》科演上沒有不同，均由劉瀚澤道長所主持，惟在三清壇前〈請師臨壇〉【三皈依】的道曲中，由朱堃燦道長帶其向三清道祖祈請證盟，游宗樺道長為其插仰和掛上朝珠，並於〈登座說法〉儀式段前，再由游宗樺道長牽著門生劉瀚澤的手登上普臺坐座。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此二部分為儀式公開過程中最予人感動的地方，除表示一種道法、道脈的傳承外，更有著師父和道眾們的背書，向里域信眾和同業道友宣告和介紹：｢這是我的師仔，今已成為道長，如有需要還望請大家多多支持。」完成《普度》科儀後，隨即宴請賓客，該日與劉道長和三重正統雷晉壇有關係的道友均都到場祝賀參與盛事。</p>
