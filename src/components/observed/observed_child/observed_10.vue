<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現在的道壇生態受到因為大環境的影響，許多的道法儀式被取代或淘汰，每每向道壇提起，得到的回覆總是「會做的道長有，願意學習的道長少。從現實面，新進道士學了沒有機會發揮，不如把時間花在學習能用上的功夫；又或是會做的道長有，能與其搭配演法的道長少。」，就越來越少有機會做，進而越來越被遺忘怎麼做。有人認為，部分儀式的消失與時代的娛樂選擇相關，早年沒甚麼消遣，且在村內家戶做大補運或大齋功德，鄰居都會來幫忙。儀式的戲謔效果不僅為庄頭和鄰家帶來些許的娛樂，同時也能舒緩主家的憂苦和鬱悶，只是種種的原因，讓人以為這些儀式似乎不具有效性，或有其他儀式可以替換，卻疏略在演法中的對話，為我們傳達的揚善和盡孝。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;日漸流逝的儀式再現令人感動，意義上除了儀式的被承繼外，也提醒著儀式的構成和目的，有其必然性和需要。儀式的核心價值，不應僅是關注在有效與否，而是如何藉由儀式的完成，能夠安撫和穩定人心，予人希望面對未來的勇氣。</p>
<h3>伍、釋教<sup>17</sup></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度的釋教儀式音樂計畫中，以嘉義地區釋教佛壇的拔度儀式為主。臺灣的釋教佛壇分布，依區域畫分有所謂「嘉義派」的稱呼，在演法科儀與音樂唱腔上自成一個脈絡，由於與嘉義地區靈寶道壇位處同個場域的關係，在後場樂師相互的交流之下，形成了儀式用曲及部分音樂相似接近或二者互通的情況。今年觀察記錄的3個釋教佛壇分別是嘉義竹崎的「慈集佛壇」、嘉義中埔的「慈證佛壇」以及嘉義太保的「慈德佛壇」，這個派系亦可稱之為釋教派別中的「慈字頭」系統，從壇號的命名即可看出屬於同一脈絡的傳承。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「慈字頭」系統的來源主要指向嘉義民雄「慈陞佛壇」的郭氏家族，此壇在釋教佛壇行業圈內頗具盛名，因此從科儀文本的傳抄到分壇授徒的佛法衍續，可以得知嘉義地區「慈字頭」釋教佛壇都與「慈陞佛壇」有著直接或間接的關係。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年共採集和記錄了4場嘉義釋教佛壇的拔度功德儀式，主要為嘉義常見的「午夜功德」規模，其中包含了山區獨有的《轉七盞》儀式與鄉間才會施作的〈抓馬〉儀式段，也記錄了罕見的《打沙》科儀，更有大齋規模的一朝功德案例，都是我們對於嘉義地區釋教佛壇儀式音樂研究的一個初步開啟。</p>
<p><strong>一、竹崎慈集佛壇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;竹崎慈集佛壇壇主為劉義生法師，家族自祖父輩就是釋教法師，接手家中壇務後，於竹崎街上設立慈集佛壇及義生禮儀社，活動範圍正是嘉義深山的竹崎、番路一帶。在嘉義縣竹崎鄉的深山地區位於頂笨仔的劉府，因高齡92歲的老父親過世，特別聘請竹崎街上著名的慈集佛壇劉義生法師來做喪葬禮儀以及午夜功德，排出一整天從早到晚滿滿的法事。劉法師特別安排的是嘉義山區特有的儀式《轉七盞》，以7盞燈光搭配佛號及線香、鮮花、水果等供品，於出殯之前以4位法師唱誦佛號旋繞棺材，每唱誦一遍佛號就將一盞燈火放於棺前，全部旋繞7次將7盞燈火放置，故名為「轉七盞」。儀式意涵希望藉由7盞燈光替亡者在渺渺冥路上能照見一道光明，引領亡者出殯的過程能隨佛接引，作法與《轉西方》儀式些微類似。此儀式極為罕見，僅在嘉義深山的幾個村莊會有家屬特別指定。在與法師的訪談過程聽聞似乎與嘉義地區的齋教有關，還仍需做進一步的調查得以釐清。</p>
