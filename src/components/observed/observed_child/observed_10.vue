<li><strong>讀改年</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「讀改年」即《解連》科儀，地方俗稱「小改」，解開前世今生連接在一起的冤結，並消除過去對人的詛咒。</p>
<ol start="5">
<li><strong>進桶盤</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;進桶盤儀式的過程由乩身透過法師誦唸《冥陽路關通行咒》（簡稱「路關」），乩身揮舞令旗走路關，時而拋撒象徵買路錢的金紙。法師路關咒的內容則從大路起走，一路上經過草埔、烏土路、赤土路、石板大路、六角亭、冥陽三江口等地，直到十二街頭後準備上天曹。由法師繼續誦唸路關咒，後場伴以【路關】調，乩身踏上象徵雲梯的板凳，直往天曹而上朝謁玉皇大帝，抵達天曹後乩身手持桶盤，桶盤內綁有敬獻給玉皇大帝的金銀財帛、象徵祈求元辰光彩的斗傘等物件，由法師奏進、讀疏文祈求鄉里平安，最後交繳給玉皇大帝，無事相辭出五門轉至南、北斗。最後交繳給南、北斗星君，若無事便會相辭下雲梯急收急回到本壇。</p>
