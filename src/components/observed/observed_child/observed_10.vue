<p>吳乙麟道長生於1940年（民29），祖籍為泉州府安溪縣。年少師承自四湖李春茂道長，學成後於安溪寮自宅立壇，並將道法傳承給長子吳政昌與次子吳武戎兩位兒子。今年2月因病逝世，由家屬替其啟建午夜規模的拔度功德。由於吳家兄弟兩人於道界交友廣闊，特別聘請吳政昌所拜之師，宜梧保護道壇的李東芳道長前來主行功德《發表》，其子李承錄負責出殯發引，孫子李崇緯擔任拔度普施高功。李家一家三代同壇下場演法，可見對於此場午夜功德的重視程度。此外仍有下崙王清宜、水林吳謙賢、北港吳武鴻、四湖吳永發等道友前來襄助功德。</p>
<p>當晚的《放赦》科儀由太保集應道壇的陳建嘉道長擔任主行，而在這樣行內事的功德氛圍之下，現場的道友無不使出渾身解數來展現出自身最好的功夫，陳建嘉道長則是選擇了【金葫蘆】來作為宣讀赦書穿插曲目。【金葫蘆】是北管舊路曲牌，總共分為4段，正好搭配赦書於符命與告文間的空檔。首先於登上將臺未開赦書前即唱第一段的「身居朝綱，為官清如水，理庶民，心無私，不怕那官多巧辯，從日月，光天化日，那怕蕭何法律條。」接著宣告赦書內容及家屬姓名後再接第二段「前三皇後五帝&hellip;」，第三段則是安排在右符告下的時段唱出「周文王訪賢士&hellip;」，最終封好赦書持於手上再唱「五霸強七雄出&hellip;」，唱畢下臺接續熱鬧的〈跑赦馬〉儀式段。這樣的方式充分表現出了陳建嘉道長對於此曲的熟練程度，也是北管曲牌音樂與道教拔度科儀的又一次完美融合。</p>
