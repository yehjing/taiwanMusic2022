<p><strong>四、</strong><strong>靈寶道壇的行內事功德</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了高屏地區靈寶道壇之外，團隊也於高雄、臺南、雲嘉、彰化地區等區域採集了多場的「行內事功德」。顧名思義即是過世亡者本身便帶有道士的身分，其中包含了高雄茄萣的白沙崙郭家、臺南新市的鍾家、臺南麻豆的下街林家、嘉義布袋過溝蕭三連道長、嘉義朴子混元道壇廖健男道長、雲林四湖安溪寮的吳乙麟道長，以及彰化鹿港歷史久遠的混元壇施家等，均是在地相當知名的靈寶道壇。這裡針對道士過世所啟建的拔度功德相關特色與區域作法差異作了相當詳細的紀錄，試圖呈現「行內事功德」與一般常人功德的氛圍不同。</p>
