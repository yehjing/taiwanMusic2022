<p><strong>二、中埔慈證佛壇</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澐水溪內聚落的曾府，因高齡84歲的老父親過世，特別聘請中埔街上的慈證佛壇李鴻昌法師來做喪葬禮儀以及午夜功德。李法師家族是中埔地區老字號的祖傳釋教佛壇，幼時就從叔伯學習釋教法事，接手家中壇務後，於中埔街上延續祖傳的慈證佛壇壇號並設立中埔禮儀社，活動範圍為嘉義縣山區的中埔、大埔、阿里山地區。此次李法師所安排為嘉義地區傳統規模的午夜功德法事，其中特別的是嘉義釋教在放赦儀式時之後特有的〈抓馬〉。由法師2人進行展演，其中一名法師持馬扮演馬匹，另一位法師分別以空手與毛巾臉盆等物，分為3次進行捕捉馬匹的儀式劇，上演一齣人追馬的戲碼。過程中後場樂師會伴襯熱鬧的過場音樂，也會以嗩吶作出馬兒的嘶吼聲，現場音效相當逼真。法師也會讓家屬加入追逐的行列，或以諧音的笑話逗笑現場所有人，讓功德壇場頓時變得不再哀戚。最後再以弄鐃、轉碗、火流星、變魔術等雜耍小技作為收尾，娛樂亡靈也娛樂了家屬們。此一作法是嘉義釋教佛壇的傳統特色，但是時代因素此一作法於市區與海線地區已經少見，只有在山區的傳統村莊才會演行此法，是在莊嚴肅穆的誦經拜懺之外展現另一種面貌的拔度儀式。</p>
