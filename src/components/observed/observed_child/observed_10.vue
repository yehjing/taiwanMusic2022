<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;王浤儒道長法名羅全，臺南安平人，自15歲起隨父親於道場見習，為安平道壇第三代傳人。安平道壇係安平地區主要行法道壇之一，約1925年（民14）由薛慶元道長肇創於臺南安平，此次登梯亦是繼40年前王德和道長登梯晉陞道長後，再次有安平子弟高登36層刀梯。王浤儒道長的父親王德和道長（法名大典）為第二代傳人。王浤儒道長自2011年（民100）初奏「太上三五都功經籙」品秩任高功道長以來，經十餘年之潛心修道研法，為擔起弘揚道法重責及承接安平道壇，決定循古禮舉辦登梯閱籙傳度陞職，奏陞「太上正一盟威經籙」品秩。</p>
<p>是日，在道眾完成《啟聖安籙》，先由先鋒官王宸緯於四周結界防外邪，後登上刀梯通知祖天師及眾神道；而先鋒官之父王秋堂道長正是40年前王德和道長登梯之先鋒官，兩代父子同牽傳承緣分，著為道門佳話。在先鋒官完成傳令任務後，便輪到籙士王浤儒登場，於登梯前，先於梯前冥想，感應靈感後便動身攀爬刀梯。登頂後朝龍虎山方向敬拜祖天師、燒化疏文，再從空中拋下筊杯，一杯見聖，霎時歡聲雷動、炮聲大響。接著從象徵36重天上撒下紅包、平安符給群眾，下刀梯後，即在眾人協助下換上絳衣後走向觀禮臺，由傳度師王德和道長為其插仰，掛上朝珠，在眾人注目下成為新科道長。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次監督登梯之師長，除了五師外傳度師王德和道長、監度師林德義道長、保舉師林和發道長、證盟師王秋堂道長及通引師鍾易道道長外，尚設有正一學師及閭山學師為其見證，亦表示王浤儒道長之多方涉獵、融會應用，讓道法演繹更為多元，讓眾人能夠接觸及了解。</p>
