<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郭獻常道長法名高典，1944年（民33）生，壇號為神妙通玄壇。祖籍漳州府龍溪縣廿九都圳塘社，家族自原鄉便有道法傳統，是傳承數代的祖傳道士壇。年少時便跟隨父親郭燦雲學習道法，1973年（民62）於白沙崙萬福宮廟埕登刀梯奏職成為高功，先鋒官是之後也成為高功的胞弟郭獻義。後於臺南仁德地區設立道壇，祖壇雖立壇於高雄茄萣的白沙崙，但整體的道法脈絡仍屬於臺南府城系統靈寶道壇，是府城系統一脈於高雄地區的傳承延伸。郭道長年邁時即將仁德地區的人際脈絡與道壇事務傳承給姪兒郭子聖道長接棒，自身便常年退居幕後直至今年農曆年間離開人世，豎靈於臺南殯儀館，便由郭家後輩與眾多道友替其啟建了一朝宿啟的十迴大齋功德。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;兩天的功德法事更是有著許多同領域內的道友前來襄助，高功部分就計有大灣李安雄、灣裡林仲軒、路竹蘇正崑等道長。其中在第二天中午的《獻供》和《薦祖》更是特別禮請李安雄道長來執行，除了於道壇內的「七獻」將香、花、燈、茶、果、食、水等寶物獻於壇中諸天聖真之外，精彩的段落便是於靈前進行的《薦祖》。過程中高功會以唱唸的方式唱唸【薦祖曲】：「開基祖、唐山祖、渡臺祖，郭家歷代祖先諸位親魂啊，一世祖、二世祖、三世祖、四世祖、五世祖，相邀來迫迌。&hellip;&hellip;」請來郭家歷代祖先，並且藉著與司鼓的一搭一唱，以諧音笑話製造出現場幽默詼諧的氣氛，讓家屬們在面對親人過世的哀傷中能暫為鬆緩心情。李安雄道長也在請到郭家近代的幾位道長時講起與前輩同壇的年少往事，讓道界後輩有共鳴者都以一種敬弔的思古情懷來向郭家歷代祖先獻上酒禮。</p>
