<li><strong>拜斗</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《拜斗》透過法司設置一七星燈，引領鄉佬與頭家朝拜南、北斗星君，替全聚落祈福。</p>
<p><strong>（二）第二日</strong></p>
