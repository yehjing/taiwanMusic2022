<ol start="3">
<li><strong>造符、鎮五方</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由儀式專家先行《放兵》，召請兵馬前來鎮守，在熱鬧的【放兵】調陪襯下，法司主行透過其自身之血賦予「鏡符」、「竹符」、「水符」、「鐵符」、「磚符」及「石符」神聖性，再請工作人員將鏡符貼在建築前後落內牆上之五方，請泥水匠將竹、水、鐵、磚、石五符以水泥夯在家廟外五個角落，象徵內外皆有神兵鎮守，使邪祟不敢入侵。</p>
<ol start="4">
<li><strong>過限（過布橋）</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「過限」為《造橋過限》，須準備有紙糊的限官、橋頭將軍、橋尾土地，在〈放橋兵〉、〈安橋〉後，十二生肖依序走過由木板搭成的橋，除初經尚未來之女性幼童外，成年女性不可上橋只能用手象徵性從橋上過。過橋時法師會誦唸過了哪些「關」哪些「厄」，替參與的人消災解厄。</p>
<p>&nbsp;</p>
<p><strong>5 </strong><strong>獻五穀、淨油、安磚契、插柳</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法司再次將裝有五穀、木炭、銅錢與鐵釘的金盤一方為拋撒，讓參與的族人撿起象徵豐收的五穀、宗族生湠的木炭、出男丁的鐵釘及財富的銅錢。再透過《淨油》儀式，由法師〈請火德星君〉、〈放火兵〉、起油鼎後，口含高粱酒噴向油鼎生成蒸騰的歘氣，法師及王爺以手沾熱油輕拍門板，在後場響亮的牛頭<a href="#_ftn25" name="_ftnref25">[25]</a>吹奏及熱鬧的【將軍令】調伴奏下，一行人持油鼎於家廟空間由內而外、由中央而四方，於內殿、拜石、點金柱、石吟、廟門、窗下、五方等重要位置歘火，亦將磚契與柳枝過油鼎，使其潔淨。藉由淨油儀式除了達到潔淨的效果，亦重新整頓了廟宇空間的神聖秩序。</p>
