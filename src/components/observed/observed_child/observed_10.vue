<p><strong>二、再現的臺南靈寶道壇「雷有聲打虎」</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年善化慶安宮奉主神媽祖指示，舉辦3天的黃籙玉籙拔濟報恩三朝大齋功德法會，聘請善化道壇主行科事，依照靈寶派所擅長的拔度法事排出了《轉血狀》、《轉水狀》、《打車關》、《打盆城》等科儀。科儀中，最為特別的是與鍾昂翰道長與其父鍾易道道長重現了臺南地區消失數十年的《挑經打虎》。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在第三天的《過橋》科儀之前，先由鍾昂翰道長扮演「羅卜」，出場時演唱南曲【山嶺路】表達思念母親之情。在表明身分後，便在五里亭留下詩句隨即出發前行。此段為羅卜個人戲份的「單挑」，接著由鍾易道道長扮演的「雷有聲」出場，以逗趣的對話與後場鼓師互動，其中包含了「關乩童」、「派藥方」等橋段，此段是臺南地區《挑經》的特色。雷有聲必須背誦近30種的中藥名稱，並利用諧音的語誤造成效果，如「<span class="bkt">青瞑跑相追－熟地</span>」、「<span class="bkt">跑到撞電線桿－黨篸</span>」、「<span class="bkt">頭殼巴一下－巴參</span>」，現場圍觀的人群也都被逗得放聲大笑。接著桌邊出現了一隻老虎，也就是最後的「打虎」橋段。雷有聲在故事中擊退了老虎，轉換回作科儀的道士身分，持魂幡帶領亡魂過橋，在《過橋》科儀後圓滿了3天的功德法會。</p>
