      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其次，在民營樂團方面，4年的委創樂曲達到107首，除了部分獲得政府補助外，其餘多數仍須自行尋覓財源，以配合音樂會的需要。2021年的數量激增，主要來源係隨心所欲樂團單場演出創作的11首樂曲，若是扣除後，每年的委創樂曲大概在25首的平均水準，長年累積形成記錄當代臺灣國樂聲響的珍貴寶藏。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;社會團體因著國樂學會每年舉辦的創作發表會，穩定維持每年至少10首樂曲的數量；專業系所則因臺藝大國樂系年度系展的傳統，每年至少提供2首委創協奏曲給參與甄選成績最優異的2位同學，為他們量身訂做樂曲，也是年度委創樂曲的固定來源。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2018年至2021年，4年的期間，國樂的委創樂曲無論是獨奏、協奏、合奏、絲竹樂等形式，共計有363首的樂曲。期盼這些樂曲能夠持續被演出，形成一股活水，建立當代臺灣在地的音樂風格，用音樂來述說臺灣美好的故事。
    </p>
    <p><strong>（二）委創的內容</strong></p>
    <p><strong>1.民間取材，融入新聲</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以民間音樂作為素材是作曲家常用的手法之一，有的會以旋律素材成為樂曲的主題，以提升聽眾對樂曲的理解與辨識度；有的會選取片段成為動機發展樂曲；有的會經過內化後，再行創作樂曲。無論使用何種方式，其目的都是為了讓樂曲有更明確的標示，期盼能匯聚成臺灣當代的聲響，為日後留下美好的紀錄。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《堂．樂》為作曲家張宛俞的新創樂曲，她將南管音樂清麗雅緻的感受譜寫成樂曲，以二胡、大提琴、鋼琴3種樂器的組合呈現。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨心所欲樂團以中西混編方式組成的重奏團體，樂團創團的理念就是以臺灣土地為創作靈感取材來源。因此，在2021年5月8日的演出，全場皆為全新的創作，創作的來源係採擷臺灣民間音樂素材，如澎湖褒歌、排灣族古調、阿美族古調《驅邪歌》、邵族的杵音及歌謠《日月潭是個好地方》、客家八音《百家春》、宜蘭民謠、嘉義民謠等等，運用音樂的技巧、即興能力、詮釋情感與感受，展開流行與傳統音樂的對話。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;鄭光智創作的《排灣之歌》，他在樂曲簡介中寫下：「<span class="bkt">使用排灣族收穫祭為概念，使用『收穫歌』與『來甦』兩首排灣族歌謠貫穿全曲，從中吸取排灣族特色四音組織、鼻笛等特殊結構，並用民族配器的方式重新詮釋，拓展民族器樂新語彙與新聲響。</span>」（國樂學會，2021，頁16）
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;徐瑋廷創作的《藍染》運用了客家音樂中的「山歌子」、「天公落水」、「思戀歌」及「老山歌」作為素材。作曲家以位於臺灣的三峽與美濃一帶，客家文化之一的「染坊」產業為發想，而創作的合奏曲。作曲家親自為這首樂曲寫下他的創作理念與手法：「<span class="bkt">此曲便是以客家曲調作為根基，藉由曲調的呈現同時也喚醒這臺灣美好的客家文化。全曲可分為四大段，第一段以「山歌子」為素材，以「殼仔弦」領奏，悠揚的旋律、反覆的樂句，再將旋律逐漸的與樂團融合，開啟這故事的序幕。第二段以「天公落水」並結合「思戀歌」的旋律作為素材，將旋律結合輕快的節奏，表述藍染工業在當時蓬勃的盛況。第三段以「老山歌」作為主要素材，開始先以老山歌的旋律變體作為主題，使用較多的7度和聲產生不穩定的聲響，述說著藍染事業的生存受到考驗。隨後刻意放上「老山歌」的原曲調，透過笛子來演奏，描述在此不斷改變的環境中，能夠好好欣賞這傳統的技藝。第四段除了「天公落水」的曲調再現，最後放上了主要旋律，提醒著我們所以要時刻存著感恩的心，珍惜我們所擁有的自然環境。</span>」（徐瑋廷提供）
    </p>
    <p><strong>2.影像為本，創作樂曲</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來，越來越多的樂團會在演出時結合多媒體播放，使得音樂的演出增加了視覺感官的刺激，更加豐富了單純的音樂欣賞。一般的做法，是先有音樂，再配合樂曲的脈動配搭影像。桃市國在2021年11月7日「韡韡～桃園行－桃園市國樂團五週年慶音樂會」活動中，委託新加坡作曲家羅偉倫、羅堅創作的一首樂曲交響音畫《桃園行》。這首樂曲原先希望能邀請作曲家羅偉倫來桃園采風，依他親身的體驗與感受來譜成樂曲，然而受到疫情影響，無法如人所願，因而改由樂團先行製作影片，再進行樂曲創作。首先，從桃園市文化局及觀光局等單位收集影像，再邀請導演進行編輯的動作，最後，交由作曲家依照影片創作樂曲。這首樂曲發表時，現場播放影片，樂團依照樂譜指示演奏，時間拿捏必須非常精準，才能將作曲家為影片所設計的音樂情趣貼切地呈現。樂曲分為3個樂章，第一樂章〈影動．天地〉以「交通」為發想，畫面將飛機劃過天際作為開端，帶出整座城市的風貌。作曲家以管樂為主，底部用頑固音型的節奏作為伴奏，展現亮麗具光彩性的開場。第二樂章〈漫步．昔日〉以「時間」為概念，呈現出桃園的古蹟、寺廟、眷村等古老建築。作曲家以慢板的速度，來對應畫面的情境。第三樂章〈回味．留香〉以「美食」為主軸，作曲家以輕快的旋律道出在地豐富的美食小吃，進而展現出桃園多元文化的風采。羅偉倫的作品旋律性強，經常使用描繪性手法創作，是音樂會常見的作曲家。桃市國此次的邀稿創作方式，也為後疫情時代找到新的途徑。
    </p>
    <p><strong>二、國人作品專場發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021國人作品專場發表會，高市國、國樂學會、臺藝大國樂系及隨心所欲樂團等4場；以下就各場發表會提出觀察報告：
    </p>
    <p><strong>1.高雄市國樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年4月17日高市國舉辦的「寶島搜神記－親子音樂繪」音樂會，原定於2020年4月18日演出，因疫情而延期至2021年，以角斯的繪本書《寶島搜神》為主軸，另輔以影像、說書人等元素，創造一場極為熱鬧活潑，且適合親子一同欣賞的音樂會。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全場音樂除了序曲外，有8首新創樂曲，其中5首為協奏曲，其他弦樂、彈撥及打擊合奏各1首，用這8首樂曲來述說仙界發生的事情。故事的由來，因掌管人間燈火的燈猴向玉皇大帝稟告凡人都不尊重神仙，也逐漸淡忘仙界對人類的照顧，所以燈猴希望玉帝沉島以懲罰人類。聽聞此消息的各個尊神紛紛來阻止此事，於是當每一位尊神現身時，就會聽見屬於祂的樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由笙主奏的《玄武澤被》，描述玄天上帝替天行道、斬妖除魔而最後修練成仙的故事，旋律緊湊，動畫亦呈現玄天上帝大戰龜蛇的場景，讓觀眾彷彿置身其中，感受玄天上帝的威武氣魄。第二位出現勸說燈猴的是觀世音菩薩，由弦樂合奏演奏《觀自在》，曲調優雅溫柔，體現觀世音的慈悲形象。《土地公在哪裡》為中胡協奏，樂曲分兩段，第一段的行板，讓人感受到土地公的草根性，以及親民的性格；第二段的快板彷彿描述土地公不分日夜的巡視著人間，樂曲以北管音樂的素材為題，呈現熱鬧的氣氛。因燈猴對土地公不敬，年幼就成仙的哪吒看不下去於是出面教訓燈猴，而作曲家黃振南用打擊合奏《哪吒鬧海》形容哪吒淘氣的性格，以及當時和龍王打鬥的畫面。因燈猴與哪吒打架後雙雙受傷，醫神保生大帝出手救治，所以《弦．動》一曲是以彈撥合奏描繪脈搏跳動的聲音，樂曲以客家採茶歌為素材，以不同的手法來呈現彈撥樂各種聲響的組合樣貌。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《天妃好神》是琵琶協奏曲，樂曲分3段，講述海上女神媽祖守護海洋，並降妖伏魔的故事。旋律明確，清晰動聽，令人印象深刻。即使是媽祖也沒辦法讓燈猴回心轉意，這時候武聖關公登場，燈猴立刻向武聖告狀，希望武聖站在自己這邊。以嗩吶為主奏的《武聖》，樂曲氣勢磅礡，描繪武聖馳騁戰場的英勇身姿。故事的結局，玉帝發現人類並沒有燈猴說的那麼壞，所以取消降大水刑罰人類，同時給予燈猴必要的懲罰。最後一首《燈．猴》為笛子協奏曲，透過笛子輕快明亮的聲音，勾勒「燈」的一明一暗，也為整場演出劃下一個活潑愉快的句點（蔡佩娟整理）。這場演出的每首樂曲，都是以描繪性的手法來創作樂曲，配合片段式的影像與旁白，使得音樂從抽象的形體轉成類語言式較能直接理解的載體。
    </p>
    <p><strong>2.國樂學會「國樂創作聯合發表會」</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂學會從2007年開始，每年舉辦國人的創作發表會，至今總計舉辦了14場（2013年停辦），總計創作了143首樂曲。每年邀請的作曲家都是經過創作委員會的討論而定，邀請了郭靖沐、盧亮輝、朱雲嵩、楊杰儒、任重、徐瑋廷、林岑陵、曾翊玹、黎俊平、鄭光智等國內老中青三代的作曲家10人，其中僅保留3人曾於2020年創作的作曲家，好讓國樂的創作能有更多不同的新血加入。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年仍以5首絲竹及5首合奏樂曲為邀稿的原則，上半場的曲目為絲竹樂，下半場的曲目為合奏曲。音樂會的第一首樂曲為郭靖沐的《䳤付鳥》，編制以新笛、揚琴、琵琶、古箏及二胡5樣樂器組成。作曲家以山海經的神獸系列裡，描述「䳤付鳥」的形象為靈感所作。第二首樂曲為盧亮輝的《喜樂情意》，樂曲中富有優美的旋律線條，結尾以活潑歡快的快板結束，描述作曲家以開朗樂觀的心境迎接未來。第三首樂曲為朱雲嵩的《燕山亭》，作曲家以宋徽宗趙佶被金兵擄往北方為背景，描繪身為帝王卻深受命運的摧折，既悲苦又感慨的心境。第四首樂曲為楊杰儒的《落羽松下》，樂曲開頭慢板中安排大阮獨奏段，展現彈撥樂器的滑音技巧與音色，快板中加入木箱鼓，增添聲響豐富與節奏律動感。樂曲描繪秋冬之際，落羽松由綠轉紅，作曲家被眼前這帶有一絲憂鬱氣息的美景所感動。上半場最後一首為任重的《鳳陽？鳳陽！》，樂曲運用傳統民歌《鳳陽花鼓》簡短的旋律素材及切分音、連續後半拍的節奏律動等特點來創作，彷彿能聽見些微鳳陽花鼓的影子，卻又極為不同，是一首富有變化、趣味的樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下半場第一首合奏樂曲為徐瑋廷的《藍染》，「藍染」指的是一種染色技術，這種染布產業曾蓬勃發展於臺灣三峽與美濃一帶，為客家文化的一種象徵。樂曲運用4首客家曲調為創作素材－「山歌子」、「天公落水」、「思戀歌」、「老山歌」，是一首蘊含客家文化的樂曲。第二首為林岑陵的《風中的故鄉》，樂曲中運用臺灣民謠的旋律素材，傳達作曲家對於過往歲月生活在這片土地上的追憶，帶有旅人漂泊、孤單漫遊著的心境。第三首為曾翊玹的《洋流》，此曲是繼《時光下的福爾摩沙》後，第二首描寫臺灣環境生態相關的大型編制作品。樂曲共有3個樂章，本場演出第二、三樂章。第二樂章－東岸，以「戀愛」為主軸，描述在臺灣東部海岸遇見的人物景象。第三樂章－碧海，以「青春」為主軸，描述北太平洋傳說中的文明之都「亞特蘭提斯」，旋律中帶有浪漫、神秘的色彩。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第四首為黎俊平的《22.53Ｎ121.11Ｅ都蘭山》，曲名的地標為臺灣東南海岸山脈，自古以來為阿美族與卑南族的聖山。樂曲以傳說、巨石、山澗、狩獵4個樂段，述說阿美族與卑南族關於聖山的信仰與文化。第五首為鄭光智的《排灣之歌》，使用排灣族歌謠《收穫歌》、《來甦》為素材貫穿全曲，並吸取排灣族的四音組織、鼻笛特色，用民族配器的方式重新詮釋對於排灣族的印象，亦拓展民族器樂新語彙與聲響。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀察這10首創作作品，有以描述臺灣地景為創作題材，也有運用民間、客家、原住民傳統歌謠為素材，或是以描述作曲家心境為主軸等，創作背景與音樂內容可謂十分的多元。「國樂創作聯合發表會」旨在推動、重視國樂作品創作之重要性，邀請臺灣老、中、青三代的作曲家們，每年度為臺灣國樂譜出豐富、多元的新曲目，累積屬於這個時代的「臺灣國樂」作品（胡雅茹整理）。
    </p>
    <p><strong>3.臺藝國樂50「臥虎藏龍」音樂會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂系長期以來作為臺灣國樂人才培育的搖籃，對於臺灣國樂的發展有著重大貢獻。今年適逢創系50週年，本場音樂會的8首樂曲中，除了揚琴樂團演奏的《三拍子》以外，全部都是委創樂曲。並以教師、校友與在校生大手牽小手的演出形式，體現傳承、創新的目標與成果。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;開場樂曲為李慧的《歡慶》。這首樂曲揉合了臺灣北管以及中國各地吹打樂的元素。樂曲當中設計了許多隊形變化與動作，並有鼓棒拋接的表演，充分展現了吹打樂的特性。作曲家本人並擔任腰鼓領奏，帶著傑出校友與在校生們一同完成這首熱鬧的作品。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郭靖沐的《展葉》是為臺藝箏樂團量身打造的樂曲。這首樂曲比較沒有明顯的旋律線，而是以和聲堆疊的方式表達古箏的演奏特色，就如同樂曲解說所述，讓聽眾對「<span class="bkt">表現葉芯至展葉的過程。</span>」（臺藝國樂，2021，頁20）有相當程度的想像。作曲家本身是古箏主修出身，因此對於古箏的特性把握得相當好。此外，在演出的服裝及隊形上也有相應的安排，是一首精心設計的樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第三首樂曲為楊易修創作的《你與我的此時此刻》，由劉寶琇、鄭聞欣、陳崇青3位老師帶領臺藝彈撥樂團演出。樂曲一開始由樂隊模擬時鐘的滴噠聲開始，隨後由主奏中阮演奏出樂曲動機，其他的聲部則逐漸加入頑固音型的演奏。主奏琵琶與柳琴漸次加入，有時互相呼應，有時則一同唱和。樂曲中並加入不同的節奏律動，彷彿象徵擁有不同時間軸的個人，在某一個時間區塊中接納了彼此。最後所有聲部一起演奏同樣的頑固音型動機，則又像是彼此時間的共有。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;林心蘋的《三拍子》雖非首演，作曲者也特別為本次演出調整了編制，由多位傑出校友與在校生一同演出。這首樂曲描寫一位女孩獨自在巴黎旅行的心情，旋律帶有濃濃的異國風味。這首樂曲同時邀請時任臺藝大國樂系主任黃新財一同上臺表演，相信對演出人員以及觀眾而言都是很特殊的體驗。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下半場4首為全新委託創作的協奏曲，對樂團、主奏及指揮都是很大的挑戰。首先登場的是朱雲嵩的36簧高音笙協奏曲《隨．想．曲》，作曲家當年以蘇文慶創作的《隨想曲》考入國立藝專國樂科（臺藝大國樂系前身），這首樂曲即是以蘇文慶的樂曲做為動機，以作曲家自己的手法進行發展。樂曲可視為包含引子與尾聲的三段體，開頭非常有朱雲嵩的個人風格，隨後卻開展出較以往有所不同的表現手法。除了對恩師的致敬，同時也表達對自我歷程的反思以及呈現。這首樂曲原應於2020年發表，因疫情影響而延至2021年演出。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;劉至軒的二胡協奏曲《綠》則是以臺藝大國樂系的奠基者董榕森教授為題，並以董氏的經典作品《巨人頌》、《七夕吟》、《碧潭夜曲》作為樂曲的動機，加入作曲家自己的作曲手法，完成的一首曲風浪漫的作品。這首樂曲沒有明顯的旋律，樂曲動機亦均是一閃而逝；卻呈現出遼闊的空間感。快板樂段則如同枝繁葉茂的大樹偶遇風雨，卻迎難而上的情景。風雨過後，則更加成長茁壯。最後再現《碧潭夜曲》及《巨人頌》的動機，在安靜的氣氛中結束全曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《玉》這首二胡協奏曲包含了作曲家林心蘋個人以身為臺藝大國樂系畢業校友的身份，對學弟妹們的殷切期許。慢板樂段旋律優美，快板則極富挑戰性。主奏雖有小小失誤，卻非常鎮定地在華彩快板中展現了卓越的技術，值得稱許。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;任重的二胡協奏曲《風華》本應為2020年首演，受到疫情影響延宕至今。樂曲可分為4段，曲風一如作曲家近期的作品，帶有濃厚的電玩音樂風格。小快板的旋律清新可愛，快板的技巧也有一定的挑戰性。主奏林怡君臺風極佳，表現相當出色（林青儀整理）。
    </p>
    <p><strong>4.隨心所欲樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨心所欲樂團創立於2013年，以創作與即興演奏為主軸的樂團，團員皆來自國立臺灣藝術大學中國音樂學系。樂團的理念為樂團曲風多元，擅於創作，靈感來源取材自臺灣土地，旨在傳達「音樂即生活、生活有音樂」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「雜談故事舖」是隨心所欲樂團繼「島國樂」及「諸舞花」的第三場全新音樂創作製作，「島國樂」顧名思義就是「臺灣島國的音樂」，以臺灣民歌為創作素材，「諸舞花」則以花為主題，描述臺灣的風景及文化特色，這次製作的「雜談故事舖」則挖掘臺灣4百年來的傳奇神鬼談作為創作的主軸，用音樂述說流傳臺灣的民間故事。「雜談故事舖」由5位音樂家帶來11首創作樂曲，搭配說書人徐灝翔導聆式的生動舖陳，引導觀眾欣賞隨後而來的音樂，邊欣賞邊咀嚼回味故事。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣雖然不大，但是社會多元種族的融合，讓不同地區，不同族群都有好多的傳奇神鬼談，例如北部的鶯歌石與鳶山流傳妖鳥的傳說、新竹頂埔以雞母克制蜈蚣作亂；中部日月潭邵族的祖靈棲息在巨大的白茄苳樹上；南部嘉義東石建廟祭祀母豬－黑皮夫人、屏東高樹鄉的鎮鄉之寶是3隻石獅雕像及墾丁供奉外國公主－八寶公主；東部的宜蘭及花蓮美崙山有山貓妖怪及巨人阿里嘎蓋；離島的蘭嶼及七美島有竹竿變成的巨岩及井邊殉節保貞的7株美麗花束&hellip;&hellip;。這些守護地方的神靈是民間的信仰，也是人民心靈的寄託，更深植為臺灣文化的一部分。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨心所欲樂團不分中西樂器，5位音樂家一同用樂聲述說故事，以音樂營造情緒、氛圍，在此次全新譜曲的創作過程中，音樂家採擷臺灣民間音樂素材，如澎湖的〈褒歌〉、排灣族古調、阿美族古調〈驅邪歌〉、邵族的杵音及歌謠〈日月潭是個好地方〉、客家八音【百家春】、宜蘭民謠、嘉義民謠等等，運用音樂的技巧、即興能力、詮釋情感與感受，展開流行與傳統音樂的對話（萬智懿整理）。
    </p>
    <h3>參、值得討論的趨勢與議題</h3>
    <p><strong>一、2020年趨勢的持續追蹤觀察</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要掌握趨勢的脈動，需要藉由長期持續觀察。本文回顧2020年提出「國樂樂譜借用平台」服務與「疫情影響的國樂生態」兩個議題追蹤觀察。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國樂樂譜借用平台」持續穩健在發展中，是否能突破現有在國樂合奏及絲竹樂合奏樂譜的借用範圍，擴展到各項獨奏樂器的樂譜借用，是需要持續努力的方向。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年持續受到疫情的衝擊，雖然藉由網路線上直播或經典演出曲目重播，可以擴大聽眾的來源與數量，但網路音樂會無法讓人感受到其他人在觀賞時對音樂的情緒反應，更無法沉浸在現場氛圍中，享受彼此互動時樂聲帶來的撼動。
    </p>
    <p><strong>二、2021觀察的議題</strong></p>
    <p><strong>1.「國樂樂譜借用平台」服務</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2016年成立了「國樂樂譜借用平台」，已經邁入了第六年。觀察6年來的執行成效，雖然在執行面上已經能夠達成起初服務學生音樂比賽使用版權及正確樂譜的目標，作曲家授權的樂曲也逐漸增加，但是執行所付出的成本過高，微薄的收費無法長期擔負必要支出。因此，預計2022年將進行檢討分配比例與收費標準，期盼能達到永續服務樂界的宗旨。2021年借譜平台一共借出21位作曲家的樂曲，其中16位是國人，5位非國人；總計借出的樂曲有273首，其中，國人的作品有213首；非國人的作品有60首。借譜平台的產生，起初的目的是為了要解決每年學生音樂比賽指定曲目作曲家的授權問題，並且提供正確的版本。經過與學生音樂比賽的主辦單位國立臺灣藝術教育館的討論，獲得信任並開始提供服務，長年來已經累積不少數量，因此，逐漸擴展到其他的使用範疇，讓作曲家的作品能夠推廣，也能獲得必要的尊重。
    </p>
    <p><strong>2.疫情持續影響的國樂生態</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年受到疫情影響比2020年有過之而無不及，在5月17日雙北升級三級警戒之時，所有表演活動都被迫停止或延期。全國學生音樂比賽更是早在1月28日為了考量學生們的安全，再次被迫取消，對於長期訓練準備參與比賽的學子們受到嚴重的衝擊，也造成不可逆的遺憾。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2021年5月15日至2021年7月29日疫情三級警戒期間，職業樂團為了提供愛樂者有欣賞國樂的機會，紛紛提出了不同形式的表演活動，如：桃市國在疫苗接種站安排4位團員演奏音樂，藉以緩解大家在施打疫苗時的緊張氣氛。臺灣國樂團以線上影音的方式，推出「NCO
      ON
      AIR雲端舞台」，除了分享音樂資源，也提供給團內好手一個展現技藝的機會。臺北市立國樂團延續2020年的形式，以「Friday，聽國樂」及「TCO國樂小學堂」2種方式，分享過往的精彩演出以及推廣國樂樂器知識。臺南市民族管絃樂團透過臺南市文化局「臺南藝文百寶袋」平臺，分享了團員們居家遠距合奏影片，以及個別的演奏分享。高雄市國樂團與高雄市交響樂團攜手合作，配合高雄春天藝術節，推出「午餐伴Fun」網路直播活動，以及分享過往精彩演出的「KCO線上音樂廳」。這些活動對疫情期間生活在充滿不確定性的人們而言，有如久旱逢甘霖，撫慰了人們乾渴的心靈，也為疫情期間留下美好的回憶！
    </p>
    <h3>結語</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年國樂的演出活動在疫情籠罩中度過，全年的創作數量有101首國人作品及18首編配的樂曲。有許多的創作係2020年延至2021年發表，以至於數量能夠維持百首以上之水準。4年產生363首的新創樂曲，要感謝臺灣音樂館提供的臺灣音樂年鑑計畫，讓學界能夠詳實記錄每年的新創樂曲及國樂的生態發展，成為樂界珍貴的資料。謝謝作曲家們願意付出心血，共同為建構臺灣當代音樂而努力，用音樂來關注在我們所生活這塊土地上的多元樣貌，以不同的表現形式與聲響，來述說臺灣美麗動人的故事。
    </p>
    <p><strong class="bkt">後記：</strong></p>
    <p class="bkt">
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年至今，年鑑國樂活動的觀察紀錄已屆4年，個人於2019年接續張儷瓊教授之手，至今已有3年。受到疫情持續影響，不確定性越來越高，三級的警戒改變了我們的生活模式。為了降低染疫風險必須減少與外界的接觸，但是對於國樂活動的觀察卻不能鬆懈。我們的團隊透過各種方式來收集資料，最終才能順利完成年鑑國樂類的觀察，如有疏漏，尚祈見諒！因為疫情，我們大幅降低人與人之間的互動；因為疫情，我們無法盡情歡唱與餐聚。疫情改變了我們生活的習慣，看到的人臉也只剩下靈魂之窗，當脫下口罩的當下，彷彿出現的是一個陌生的臉龐。在疫情中，我們感受到人情的溫暖，在此要感謝各單位提供的寶貴資料，也要謝謝徐瑋廷願意無私的分享創作的理念與手法。當然最重要的還是要感謝我們堅強的團隊：林青儀、萬智懿、吳沛諭及蔡佩娟4位助理的努力。沒有妳們全年全心投入與詳實記錄蹲點場次，這篇報告是無法完成的。在此，我一定要再次向妳們致上誠摯的謝意，因為，能夠順利完成蹲點的榮耀與專文的撰寫，全都歸功於妳們！我們的小組完成了階段性任務，相信這些一點一滴所累積下來的資料，能為當代留下寶貴的紀錄。
