    <h3>前言</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;嚴重特殊傳染性肺炎（Covid-19）自2019年12月在中國武漢市被發現後，快速地在全球蔓延，嚴重影響了人類正常的生活與經濟的發展。臺灣雖然在2020年時疫情管控得宜，但在2021年的5月因為疫情持續升溫，首先在15日將雙北市警戒提升至第三級，繼而在19日將警戒範圍擴大至全國，展演活動完全停止，堪稱為表演藝術界有史以來最寒冷的時期！在全配合政府配戴口罩、勤洗手、施打疫苗、減少社交活動的政策下，終於在7月27日調降為二級警戒，國樂也逐漸開始以線上和實體活動呈現。雖然受到疫情的影響，但是2021年的國樂創作數量仍有101首新創與17首改編作品，總計118首。以下將從國樂領域的「生態與發展」及「國人創作與發表」兩面向來分析與評述，茲分述如下：
    </p>
    <h3>壹、國樂領域的生態與發展</h3>
    <p><strong>一、疫情對國樂生態影響之觀察</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情自2020年一直延燒至2021年，在年初時，一直看不到趨緩的現象，因此，教育部在1月28日宣布109學年度全國學生音樂比賽國樂合奏與絲竹室內樂合奏等項目暫停辦理。連續兩年的停賽，不僅讓參與比賽學生們投入的心血付諸流水，也再度讓國樂基層教師失去主要的收入來源，影響層面非常廣！自5月14日衛福部公告雙北於15日起疫情警戒提升至第三級後，造成所有的演出團隊措手不及，首當其衝的就是國立戲曲學院與國立臺南藝術大學於15日聯合舉辦的「鼓樂昇輝」演出，即刻停止辦理。全年度演出活動取消達到47場，內含1場臺灣國樂團與韓國國立國樂院的交流演出活動；推廣活動取消有29場，20場為中華民國國樂學會（以下簡稱國樂學會）所舉辦的考級檢定活動；2場傳習活動。延期舉辦的演出活動有29場、推廣活動延期的有47場，其中包含臺灣國樂團舉辦的2021古箏器樂大賽的複賽與決賽；演出活動改為線上播放的有2場；線上國際交流的有2場。另外，國樂學會因應疫情的不確定性，將2021年器樂大賽改為繳交影片方式比賽；舉辦多年的「桃園市國樂節」也因疫情影響而停辦，新竹市的「竹塹國樂節」則延期辦理，並改為線上直播。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為了彌補疫情為表演藝術界所帶來的衝擊，文化部於5月14日公告了「藝文事業營運損失補助專案」，提供藝文團體申請，接著各地方政府文化局也陸續推出藝文紓困補助方案，期能彌補應疫情所產生的部分營運缺口。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在教育體系方面，教育部於5月18日公告，自5月19日起各級學校停止到校上課。各級學校為了避免教學的中斷，運用網路教學、線上上課的方式，讓學生的受教權不受影響，但是評量器樂演奏教學的成效，受制於器材設備及網速，成效不如預期。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂學會面對疫情而接續取消了20場考級檢定，僅舉辦8場；網路檢定舉辦5場，線上器樂大賽1場，由以上數字，相較2020年考級檢定的25場及2場的器樂大賽，場次大幅下滑，可見疫情影響程度之深。
    </p>
    <p><strong>二、展演活動</strong></p>
    <p><strong>（一）公部門樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣以公部門經費成立的國樂團有職業與非職業兩類，職業樂團有以公務機關及基金會兩種不同營運的模式；非職業樂團則由客委會、市公所、文化中心等不同機關所支持成立。
    </p>
    <p><strong>1.職業樂團</strong></p>
    <p>（1）臺北市立國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團（以下簡稱TCO）成立於1979年，至今已有43年的歷史，是臺灣最早設立的公設職業國樂團。鄭立彬團長因任期屆滿，返回文化大學任教，2021年5月1日由陳鄭港博士接任TCO第六任的團長。陳鄭港在77期的《新絲路》，談及他的藝文淑世藍圖，以「藝文淑世
      疫情陪伴 藝起抗疫」落實執行「藝術實踐」與「社會參與」，作為厚實TCO全民樂團的體質。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO為了思考音樂家的社會責任，運用網路直播的策略，快速轉型為線上活動，以音樂來撫慰不安的人心，領頭走出表演藝術的困境。陸續推出了「Friday，聽國樂」、「TCO國樂小學堂」、「北市國防疫主題系列MV」、「TCO藝起抗疫系列～國樂新星實境秀等活動」、「聚敘Online－TCO
      2021寰宇音樂嘉年華」、「國樂同樂會」、「好戲鬧連連」、「TCO廣場藝術節」、「藝起玩樂－TCO親子嘉年華」等不同主題的節目。有的是線上播出，也有實體的演出，對受到疫情影響的民眾提供了舒緩心情的良藥，也是演出單位在面對疫情所找到的出口。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖然受到疫情的影響，TCO在2021年還是推出了18場的演出，包含跨界節目有1項，演出4場。另外，附設團隊有13場演出，個人獨奏會有3場，合計31場的音樂會（臺北市立國樂團，2021）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO
      2021年的委託創作的樂曲高達21首及3首的編曲，展現TCO兼顧傳統與創新的努力成果。前團長鄭立彬認為國樂團的發展關鍵仍在曲目，因此在TCO任內進行為期3年的「跨樂季協奏曲委創計畫」，3年來TCO廣邀作曲家為各聲部樂器創造專屬的協奏曲，包括嗩吶、柳琴、打擊、中阮、低音提琴、梆笛等，受疫情影響，作曲家鍾耀光的第二二胡協奏曲《孟小冬》延至10月24日演出後，此計畫圓滿結束。
    </p>
    <p>（2）臺灣國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團（以下簡稱NCO）隸屬於文化部國立傳統藝術中心轄下的單位，也是唯一隸屬於中央的國樂團。2021年受到疫情影響，演出活動明顯減少，在國內的展演活動有5套曲目，演出8場次。近年來樂團的演出偏向與傳統戲曲結合的類型，其中2套曲目《凍水牡丹II～灼灼其華》及《蓬瀛詠弄人間戲》都屬於這類型。《凍水牡丹II～灼灼其華》節目以廖瓊枝為主，在高雄衛武營國家藝術文化中心戲劇院演出2場（3月20-21日），在臺灣戲曲中心大表演廳演出2場（4月30日至5月1日），是2021上半年度的主要活動。9月25日在臺灣戲曲中心大表演廳舉辦《蓬瀛詠弄人間戲》音樂會，由名指揮家瞿春泉擔任指揮，結合了崑曲、京劇、歌仔戲等名角參與演出。11月19日在臺灣戲曲中心大表演廳舉辦「箏綺逗艷－2021
      NCO器樂大賽古箏新秀選拔」決賽1場，由黃光佑指揮5位參與決賽的年輕古箏好手，現場演奏進行比賽；推廣音樂會以「國樂頻道」為名，在4-5月間，一共演出25場次（臺灣國樂團，2021）。
