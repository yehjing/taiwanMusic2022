<p><strong>（三）推廣面向</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;社會教育的推廣，較學校教育更加寬廣，有學會主辦之考級、比賽，有公部門職業樂團及非職業樂團舉辦的器樂大賽、大師班及講座，對於國樂人才的培育，亦有其十分重要且不可或缺的地位，將歸類於推廣教育面向探討。</p>
<p><strong>二、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2020年嚴重特殊傳染性肺炎（COVID-19）疫情蔓延以來，藝文產業受到嚴重影響，雖然文化部在今年5月已提出「藝文紓困4.0」方案，但根據臺北市藝術創作者職業工會（簡稱藝創工會）調查指出，仍有將近1/4的從業人員，在疫情期間離開藝文產業（菱傳媒，2022）。繼臺灣第一波本土疫情爆發滿週年，Omicron變異株於2022年上半年對臺灣造成了衝擊及影響，導致4-6月份的藝文表演活動延期或取消，令臺灣藝文界再度陷入低迷狀態。但隨著疫情逐漸趨緩，6月中以後藝文活動陸續恢復，全國學生音樂比賽照常舉行等，讓藝文產業復甦有望。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著疫情影響，國樂表演形式從傳統、保守的型態轉換為科技發達型態，從大家印象中的實體表演漸漸轉向以網路媒體、影音作品等方式作為傳播的媒介。從文化部（2022）裡的文化參與及消費調查報告可發現，透過網路欣賞音樂的比例逐年攀升，以此可推測民眾對於實體表演參與的下降，可能是由於生活型態的改變，大家越來越傾向使用網路媒介來進行音樂文化的觀賞。另一項統計顯示，疫情爆發前「從小至目前曾經學習過『傳統樂器演奏』」的數據是逐年下降，卻在去年小幅提升，由此可見周遭誘惑變少之後，會讓大家有更多的時間可以專心投入在學習音樂上，這亦有利於國樂往後的發展趨勢，詳見表4。</p>
