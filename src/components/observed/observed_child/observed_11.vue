<h3>捌、國人創作與發表</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年度共創作151首與國樂相關的首演曲子，比去年足足多了33首。於上半年有27首，下半年度則有124首，多數首演都集中於下半年度。今年度的首演以大合奏的曲子居多，共有86首；協奏曲的部分以胡琴與琵琶協奏曲各8首為最多，其次則是揚琴7首。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另有多首雙樂器的協奏曲，其中有簫與笛子協奏曲《絕世神功組曲》；琵琶與簫協奏曲《風》；琵琶與古箏協奏曲《︱一》；二胡、揚琴與大阮協奏曲《江南印象》；琵琶與中阮協奏曲《跡》；笛子與打擊協奏曲《一笑莫愁》等多首不同樂器所擦出精彩的火花。不同屬性的樂器合作，會讓彼此的優缺點互補，整體的聲線會呈現更多層次及更圓潤。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現代國樂自上世紀50年代起，開始有了各種跨文化與跨領域的嘗試，今年度的創造曲子亦不例外，有著多首國樂與西樂跨領域的合作，如琵琶/林慧寬與小提琴/張家倫所演奏的《寂山依稀》；柳琴/孫聿瑩、打擊/任祐嫺、沈振翔、鋼琴/洪芷璇演奏的《魘》；古箏/謝欣怡、打擊/譚楷勳、鋼琴/林上傑演奏的《舞鶴賦》；大墩國樂團、電吉他/李彥平演奏的《湍流》等曲子。此外，還有與聲樂和演唱的合作，分別有3首及9首的部分，這足以證明國樂隨著時代的演變，漸漸有了更多元的詮釋方式，也蘊藏著無限的可能性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次首演創作的曲風多數以臺灣本土風俗民情詮釋，由風景、季節、節日、動物、歌仔戲、廟會等主題來呈現。唯有幾首曲子呈現較特別的異國曲風，如《El Fuego de Las Fallas》37簧笙協奏曲所詮釋的是西班牙曲風。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團一共委託多位作曲家創作出32首全新作品，是今年度最多首演的樂團。緊接著是臺灣國樂團共委託創作26首，王乙聿及林心蘋於今年度分別創作出9首作品，而李哲藝及何立仁則各別創作出5首作品。</p>

<h3>玖、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年對整個社會來說是後疫情時代之瞬息萬變的一年，藝文產業於今年度亦逐漸復甦，當然國樂的活躍度亦比去年更蓬勃。雖然大家已習慣藉由網路線上直播或經典演出曲目重播，但網路音樂會無法讓人感受到其他人在觀賞時對音樂的情緒反應，更無法沉浸在現場氛圍中，享受彼此互動時樂聲帶來的撼動。故今年度恢復多場實體的展演活動，讓大家重溫舊夢，逐漸恢復疫情前的模式。另外前幾年因疫情而停辦的全國音樂比賽，今年終能如期舉行，讓學生及師長們更珍惜這個比賽。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，今年度創作首演較去年的118首，足足增加了33首。觀察今年上半年度為27首，至下半年度則有124首，比上半年度多出了數倍。國樂的當代創作在短短5年，即有514首的新創樂曲取量，足見國樂在臺灣音樂領域日新月異的進步，呈現蓬勃盎然的朝氣。感謝臺灣音樂館提供的臺灣音樂年鑑計畫，讓學界能夠詳實記錄每年的新創樂曲及國樂的生態發展，成為音樂界珍貴的資料。</p>

