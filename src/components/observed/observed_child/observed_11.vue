    <p><strong>（三）個別樂器族群的表演生態</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺灣除了以國樂團的形制營運外，近年來以族群樂器成立的團體，逐年增加中。以古箏組成的團體有：大觀箏樂團、台北正心箏樂團、風雅頌古箏樂團、谷方當代箏界等約22個單位；以彈撥樂器為主體的有：台北柳琴室內樂團、琵琶雅集、和琵琶樂團、台中琵琶樂團等；以揚琴為主體的有：台灣揚琴樂團、蝴蝶琴室揚琴樂團；以打擊樂為主體的有：天鼓擊樂團、紘采樂團等；還有以笙、胡琴或者不同樂器組合的形式，形制多元且有優質的演出表現，為臺灣的展演活動增添色彩。
    </p>
    <p><strong>台灣揚琴樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣揚琴樂團是臺中市的傑出演藝團隊，由揚琴演奏家鐘泉波成立。2021年總計22場演出，其中5項6場次的專題音樂會，16場推廣性質演出。受到疫情的影響，樂團仍舊繳出了22場演出活動的佳績，可見樂團的企圖心非常強烈。2021年樂團榮獲「臺中市第十屆表演藝術金藝獎」的殊榮，此獎項為臺中市政府對表演藝術領域之保存、傳承、發展及創新，具重大貢獻者所設置。樂團能獲得此獎項，是樂團長年在地耕耘的結果。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年的委託創作有5首，編曲4首；這4首編曲有部分是從現有樂曲中改編為揚琴樂團版本。4首新創樂曲及1首編曲在8月29日「精湛之夜－笙聲不息」的音樂會中發表；洪千惠創作的揚琴合奏曲《郊遊趣》及改編的揚琴樂團協奏版《彼時的我在遐等你》則在11月20日「印象臺中－山與海的樂章」音樂會中發表；吳宗憲的《北風》及徐瑋廷的《海岸邊的舊軌道》兩首樂曲，這兩首樂曲原來都是為國樂團的編制所創作的，經作曲家以原曲為基底，重新編配給揚琴樂團演奏，為樂團曲目的來源開闢了新的一條道路（台灣揚琴樂團，2021）。
