<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣嚴重特殊傳染性肺炎（COVID-19）曾於2021年重創藝文產業。2022年在第三劑疫苗覆蓋率已達七成的情況下，疫情得到控制並逐漸趨緩，防疫政策也逐步放寬，全國音樂比賽終能如期舉行。表演藝術界在艱困的環境下逐漸復甦，各大職業樂團、傑出演藝團體、業餘國樂團體也在配合政府防疫措施的政策下，如雨後春筍般以線上和實體呈現。本文將從國樂的展演、出版、學術研究等多維度分析與評述2022年的國樂發展概況。</p>
<p><strong>一、研究範圍：國樂活動團體之類型</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度國樂活動團體之類型分為「演藝團體」、「學校」及「推廣」三個面向。</p>
<p><strong>（一）演藝團體</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「演藝團體」依其屬性又再細分為公部門職業國樂團及業餘國樂團，業餘國樂團依層級分為TAIWAN TOP演藝團隊、傑出演藝團隊及一般演藝團體。若以立案與否分析2022年之演藝團體，在156個演藝團體中，扣除13個公部門職業樂團及附設團、5個縣市政府或行政院客家委員會轄下的國樂團隊、7個學校團體、21個推廣協會，未立案團隊僅有8個團隊，其中2個演藝團體是由經紀公司處理演出事宜，未立案之比例極低，內容詳見附錄一「團體及個人名錄」。</p>
