      TOP演藝團隊，2021年國內演出共7場，包含兩岸薪傳系列音樂會1場，絲竹室內樂系列1場，獨奏家音樂會1場，青年演奏家系列4場；取消改延至2022年演出的有5場。國際交流網路音樂會1場，延期1場。線上音樂會8場，其中包含文化部《藝FUN劇場－精采上線》小巨人兩岸薪傳系列「緣」2場。
