    </p>
    <p>（3）桃園市國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年是桃園市國樂團（以下簡稱桃市國）豐收的一年，成團只有短短的5年，在5大職業國樂團中最為年輕，卻能在第三十二屆傳藝金曲獎中大放異彩！2020年出版的2張專輯《絲竹雅韻》及《桃園四季》，獲得6項入圍的殊榮，最終《絲竹雅韻》獲得「最佳傳統音樂專輯獎」；鋼琴家范姜毅詮釋的《桃園四季》獲得「最佳演奏獎」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2021年演出共舉辦8場，其中專題音樂會5場、1場城市巡演音樂會、1場與臺南市民族管絃樂團聯合演出、1場為二胡及大提琴兩項樂器的器樂大賽音樂會。推廣音樂會中，校園巡迴演出有20場，對於樂團在地生根及國樂推廣，有實質的效益。受到疫情影響，桃市國取消了行之多年的桃園國樂節（桃市國，2021）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國今年的委託創作共有3首，其中2首是國人創作，1首是非國人創作另有編曲6首，除了羅偉倫的作品外，其餘都在「跨界．跨年」元旦音樂會中發表。何立仁《桃園Take
      off》與羅偉倫的交響音畫《桃園行》都是以桃園為題的作品，值得一提的是羅偉倫所創作的《桃園行》係以影像為主體，再依影像創作樂曲，因此，現場演出時，樂團必須精準配合影像演奏，才能具體展現音樂的實質內容。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國2021年出版《世外桃園》專輯，收錄了近年來樂團委創的作品：《夜炫》雙二胡協奏曲、《行酒》、《客風弦韻》二胡協奏曲、《世外桃園》低音笙協奏曲、《桃園Take
      Off》及《TAOYUAN：Capriccio桃園暢想曲》，為樂團增添了新的保留曲目，並能以城市之名行銷樂團，建立樂團的自有特色。
    </p>
    <p>（4）臺南市民族管絃樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南市民族管絃樂團（以下簡稱南市國）成立於2000年，樂團以中小型編制、多樣演奏形式及關懷本土的演出，為臺灣的國樂開闢另一類演出的形式。2021年9月1日原團長劉文祥功成身退，改由黃光佑擔任樂團團長及指揮職務。2021年的大型音樂會共有3場，其中2場是與新竹青年國樂團（以下簡稱竹青）及桃市國合作演出；菁英系列音樂會有5場；推廣音樂會中，臺南市在地社區的巡演活動有7場，配合公務的演出活動有8場，合計15場；受疫情影響取消的演出活動有6場；2021年共計23場活動，參與人數約6,870人（南市國，2021）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年南市國的主力放在春、秋兩季與竹青及桃市國合作的音樂會上，這兩場的觀眾人數不少，尤其是5月1日與竹青合作的「大江南北走一回」，觀眾人數有900人，幾乎座無虛席。另外，南市國的「菁音系列」，今年共演出5場，3場為器樂專場，另外兩場則是分部（打擊、拉弦），演出票房表現亮麗。
    </p>
    <p>（5）高雄市國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄市國樂團（以下簡稱高市國）今年所舉辦的音樂會共計11場，取消2場屬於春天藝術節的演出。11場音樂會又分為高市國演出的有8套節目9場的音樂會；1場由附設親少年樂團擔任演出；1場為推廣音樂會。全年有委創10首作品及1首新編樂曲。主要因應4月17日「寶島搜神記」音樂會中描繪各神明的樂曲需求，一共委託8位作曲家創作全新作品，是年度最多首演樂曲的場次。這場音樂會結合說書、影像和臺灣各路的神明，讓觀眾可以滿足聽覺與視覺的雙重享受，相當受到歡迎（高市國，2021）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年臺灣五大職業樂團持續面對只增不減的疫情，在最嚴峻的5月至7月期間警戒升至三級，導致所有活動被迫取消或延期。在三級警戒期間，連線上演出也無法舉行，樂團的排練更受到嚴格限制，堪稱多年來國樂界所面臨的最大的困境。有了2020年的經驗，在面對突如其來的警戒升級，各樂團較能知道該用何種策略來面對。藉由網路播放的平臺，將歷年來的演出精彩片段，透過專人的解說，讓愛樂們能夠在線上欣賞美好的音樂，在疫情下具有撫慰人心的果效。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年的新創作品，共有35首，其中國人創作有34首，非國人創作1首，國人創作比例達97.14%。相較2020年的國人創作18首新創作品，增加了17首，在無比嚴峻的疫情下，臺灣的職業國樂團能有此亮麗的成績，值得欣慰！其次，為了演出的需要有10首新編樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為了防止疫情的傳播，所有入場聽眾都必須填妥姓名和電話，並量測體溫，且全程配帶口罩。舞臺上除了管樂組外，其餘演奏員都必須配戴口罩，並且在管樂組的前端，設置隔板以減少口沫的傳遞，已成為音樂會舞臺上的常態。疫情雖然減少了實體音樂會演出的交流模式，但它卻無法阻攔樂團透過各種不同管道傳遞樂聲的企圖心。
    </p>
    <p><strong>2.非職業樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由公部門支持成立的非職業樂團有，臺中市政府客家事務委員會成立的臺中市政府客家事務委員會國樂團（以下簡稱臺中市客委會國樂團）；基隆市文化中心成立的基隆市國樂團；彰化市公所成立的彰化市立國樂團；苗栗縣文化觀光局成立的苗栗縣立國樂團等，每年都有定期的演出。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;彰化市立國樂團於4月24日假彰化演藝廳演出「熱情奏響」音樂會。苗栗縣立國樂團2021年演出2場，第一場於9月25日假苗栗縣通霄鎮綜合活動中心演藝廳演出「水月
      秋波」音樂會；10月24日於苗北藝文中心演藝廳舉辦「幻響」音樂會，由黎俊平擔任指揮。臺中市客委會國樂團受到疫情影響，取消原定3月6日於臺中市中山堂的音樂會，僅保留12月5日臺中客家樂活園區樂活運動館演奏廳舉行的「客家國樂音樂會」音樂會，由蔡玟玲擔任指揮。苗栗縣立國樂團與臺中市客委會國樂團都是以推廣客家文化為主體的團隊，透過音樂來展現客家的精神與文化風貌。
    </p>
    <p><strong>（二）民營樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的民營樂團為數不少，也是國樂活動在民間活躍的主要動力來源。以下以小巨人絲竹樂團、九歌民族管絃樂團、新竹青年國樂團、台南樂集、聚樂坊及金門愛樂民族管弦樂團等6個團隊，在2021年的展演活動提出觀察報導。
