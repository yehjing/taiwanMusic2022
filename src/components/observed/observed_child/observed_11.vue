<p><strong>（二）學校方面</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂人才之培育，主要來源分為學校及社會兩方面，由教育部主辦的全國音樂比賽，是孕育國樂人才的搖籃，而與國樂相關之高等學校教育，則是培育國樂專業人才的重要推手。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7所學校團體中，今年度國樂與年鑑相關之學校共有6所，分別為國立臺灣藝術大學、中國文化大學、國立臺北藝術大學、國立臺南藝術大學、南華大學及國立臺灣師範大學，這些學校皆有學士班和碩士班，唯國立臺灣師範大學只有碩士班。國立臺灣藝術大學學士班分為日間部和進修部，系所人數分別為139人及125人。中國文化大學、國立臺北藝術大學、南華大學學士班系所人數分別為137人、102人及94人。國立臺南藝術大學學士班則有分為七年一貫制88人及一般學士班98人，共186人。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;各大院所的碩士班人數北部較高於南部，國立臺灣藝術大學碩士班分為日間部與在職班，今年度在學人數分別為41人及27人。中國文化大學、國立臺北藝術大學、國立臺灣師範大學系所，在學人數分別為40人、32人及53人。南部的國立臺南藝術大學碩士班則有中國音樂學系及民族音樂學研究所，前者在學人數為19人，後者又分日間部及在職班，在學人數分別為28人及32人，南華大學碩士班為30人。北部總計193人，南部109人。</p>
