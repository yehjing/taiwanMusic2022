      林承毅．胡琴&amp;指揮音樂會」、4月21日「一一Ｏ．邀邀您」音樂會、12月19日「三個人X聚」由「三個人」及「聚樂坊竹樂團」聯合演出。另有2場推廣活動音樂會，4月25日「木、竹材料在樂器上的應用及呈現」及5月2日「2021宜蘭綠色博覽會《希望城堡》」（聚樂坊，2021）。
