<li><strong>一般政策</strong>：在臺灣表演藝術文化政策中，常態有「傑出演藝團隊徵選及獎勵計畫」，在各級縣市文化局當中皆可申請，在2022年中，臺北、新北、桃園、新竹、苗栗、臺中、南投、嘉義、臺南、高雄、屏東等都看到有國樂團體補助。在國立傳統藝術中心也有「第三十三屆傳藝金曲獎」的甄選，新北市政府文化局「2022新北市樂壇新星」甄選活動公告。因為疫情影響，文化部特別有「文化部辦理受嚴重特殊傳染性肺炎影響表演藝術事業營運補助」來作為支持表演事業的補助。</li>
<li><strong>政府獎補助</strong>：2022年文化政策裡的政府獎補助共有14件，而發布機構分別為臺北市政府文化局4件；臺中市政府文化局3件；屏東縣政府文化處2件；國藝會2件；新北市政府文化局、台新銀行文化藝術基金會及桃園市政府文化局各1件。政府補助獎是各地方政府舉辦藝文活動時，為演出的團隊進行補助。因此各縣市及各地樂團都能藉由此補助的申請，來提升全臺各地樂團互相交流的機會。</li>
</ol>
