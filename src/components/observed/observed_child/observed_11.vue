    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門愛樂民族管弦樂團（以下簡稱金門愛樂）成立於2017年9月，為金門在地專業國樂人才所組成的國樂團。2017年10月於「金門國樂祭」首次正式登臺亮相，初試啼聲即備受聽眾的喜愛與肯定。成軍4年多以來，於金門各地活動中獻聲，累計場次超過100餘場。未來朝向「保存、發揚國樂文化」、「專業人才培訓」、「專業國樂體驗」三大面向，繼續為在地提供優質且專業的國樂服務。2021年共計演出10場，在2020年及2021年獲選為金門縣傑出演藝團體（金門愛樂，2021）。
