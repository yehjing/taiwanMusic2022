      ON
      AIR雲端舞台」兩部分。「NCO雲端音樂盒」精心挑選了歷年演出的17首樂曲，讓愛樂的朋友們能夠在線上觀看。「NCO
      ON AIR
      雲端舞台」則改變2020年以樂團新進團員為主體的演出形式，改由樂團主要演奏家擔綱演出獨奏曲的全曲或精選片段，提供樂友們觀賞。
