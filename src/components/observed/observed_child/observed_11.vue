    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年因嚴重特殊傳染性肺炎疫情影響，臺灣面臨有史以來最嚴峻的本土疫情，衝擊全國百業，自5月19日全國進入三級警戒至7月27日始降至二級，國樂展演活動在有條件開放下，從8月起才逐步復甦，但因2021年疫情的不確定性，因此學術活動較之2020年明顯減少許多。以下為國樂領域的出版，可分為文字與有聲兩大類。文字部分包括專書、樂譜、樂訊、音樂活動回顧與評論；有聲出版以樂團和個人發行的專輯為主。分述如下：
