    <p>（3）民間器樂大賽</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;受到疫情影響，國樂學會在7、8月間舉辦的器樂大賽，捨棄傳統的實體比賽，改由繳交影帶的方式辦理，以避免群聚造成染疫風險。整體人數較2020年南北兩區總人數1,003人大幅下降，只有397人，其中吹管擦弦類為151人，彈撥類242人，揚琴重奏2組4人。觀察人數大幅下降的原因，除了大家不習慣以錄影方式比賽外，疫情也造成教學無法正常進行，導致降低了大家參與比賽的意願。疫情不僅影響人們的生活，也阻礙學琴人士彼此實體觀摩、競技的機會，對國樂發展的生態影響至鉅。
    </p>
    <p><strong>2.檢定</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;考級檢定提供一般學習國樂的人士，在學習過程中一個檢核的平臺，讓他們能夠藉由外部機制檢核的結果，了解自己學習的成效，以此循序漸進學習，達到自我訂定的目標。2021年國樂學會在全臺實體舉辦8場，受疫情影響停辦20場，網路檢定舉辦5場，總計報名人數708人，其中含304人透過網路檢定，缺考人數4人，延考人數19人，不合格人數63人，合格人數623人。延考的原因，主要都是因著疫情影響所致。相較2020年首次透過網路檢定的人數110人，2021年網路檢定人數大幅提升了194人，呈倍數成長，已逐漸被大家接受，成為常態檢定模式。
    </p>
