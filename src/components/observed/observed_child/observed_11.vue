<li><strong>國樂節</strong>：2022年共有新竹、桃園、嘉義三地舉辦了國樂節。其中以新竹青年國樂團執行辦理的竹塹國樂節資歷最為悠久，今年為第十二年舉辦，此次主題為「樂動青春」，自7/9起展開為期四週13場系列活動至7/31止。有20個團體參與，包括10個學校團體、8個樂團、1個協會及1個劇團，共推出12場演出，包括10場室內音樂會，2場戶外音樂會及1場嗩吶專題講座，為市民帶來多樣化的音樂體驗。桃園國樂節至今邁入第九年舉辦，此次主題為「世外桃園」，由桃園市政府文化局主辦，桃園市國樂團策辦，自8/6-8/21舉辦5場室內音樂會、1場戶外音樂會，另於8/8-8/12舉辦胡琴大師班由歐光勳主講。由嘉義民族管弦樂團主辦的嘉義國樂節則為首次舉辦，自8/6-9/4舉行，推出7場室內音樂會、2場戶外音樂會、1場國樂大師班由鄭立彬主講、2場小舞臺自由演出及以「嘉義」及「國樂」作為意象主題之攝影展。</li>
<li><strong>文化節</strong>：2022年有3個與國樂相關之文化節活動，共5場活動。2022年文化節分別由彰化縣文化局於彰化縣花壇鄉文德宮廣場舉辦的白沙坑迎燈排文化節共進行2場次；獅頭山雅樂坊國樂團於苗栗縣造橋鄉慈聖宮停車場舉辦之客庄打當晝文化節及合和藝苑於桃園市政府文化局大廳所舉辦的桃園閩南文化節2場次。</li>
<li><strong>各項藝術節</strong>（含藝穗節、音樂節）：2022年共有13項與國樂相關之藝術節活動，帶來32場的國樂演出，包括：高雄春天藝術節（5場）、2022柚花藝術節（1場）、2022TIFA 臺灣國際藝術節（2場）、2022夏至藝術節（4場）、2022貓裏表演藝術節（2場）、2022新北市兒童藝術節（1場）、TCO廣場藝術節（1場）、2022臺中兒童藝術節（1場）、2022大臺北藝術節－大觀國際表演藝術節（1場）、2022苗栗店仔藝穗節（4場）、羅東藝穗節（1場）、2022臺南國際音樂節（1場）、2022世界音樂節@臺灣－WMFTaiwan（1場）、2022金門坑道音樂節（7場）等。</li>
</ol>
