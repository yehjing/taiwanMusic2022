    <p><strong>（五）文協臺灣－百年的光與影</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年適逢臺灣文化協會百週年紀念，臺北市立國樂團特舉辦「文協臺灣－百年的光與影」活動，邀請琴園國樂團林谷珍擔任製作。臺灣文化協會成立至今100週年，為發揚100年前林獻堂、蔣渭水等人創立協會，推動「新文化運動」的精神，林谷珍特別製作此劇，由琴園國樂團與多位跨界的藝術工作者，聯袂獻上此齣音樂劇，內容以臺灣史結合書法、說書、戲劇、音樂、舞蹈、詩歌、舞踊等表演藝術，呈現「臺灣文化協會」啟蒙人民思想的精采故事。因嚴重特殊傳染性肺炎疫情的影響，首播改於線上進行，透過臺北市立國樂團YouTube平臺，讓全國甚至世界都能直接觀賞（萬智懿整理）。
    </p>
    <p><strong>三、推廣</strong></p>
    <p><strong>（一）推廣音樂會</strong></p>
    <p><strong>1.</strong><strong>TCO推廣音樂會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO因應疫情的影響，提供了多元形式的推廣音樂會，如：「Friday，聽國樂及TCO國樂小學堂」、「北市國防疫主題系列MV」、「TCO藝起抗疫系列～國樂新星實境秀」、「聚敘Online－TCO
      2021寰宇音樂嘉年華」、「國樂同樂會」、「好戲鬧連連」、「TCO廣場藝術節」、「藝起玩樂－TCO親子嘉年華」等活動，為疫情期間與後疫情時代帶來了國樂演出的復甦景象。以「國樂同樂會」為例，TCO邀請了13個不同型制的團隊，於11/13（六）、11/14（日）13:00-18:00在臺北市二二八和平公園音樂台規劃了大型戶外演出，古典、傳統、經典、流行，應有盡有，頗有國樂嘉年華會的氣氛（TCO，2021）。
    </p>
    <p><strong>2.</strong><strong>文化就在巷子裡及育藝深遠</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團文化就在巷子裡音樂會，每年3-6月及9-12月由7個附設團擔任巡迴全市12個行政區演出36場。但因受到疫情影響，時間上較難掌控。2021年在4月份演出3場，另有1場因天氣因素取消，5月份演出11場，另有2場延期。下半年9月以後，雖然疫情趨穩，但是為顧及里民的健康，避免群聚，因此延至12月份才繼續進行3場社區巡禮活動，2021年共計演出17場（TCO，2021）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每年10月的「育藝深遠」－啟蒙方案，對許多臺北市國小六年級的學童而言，可能是對國樂的初次體驗，因此自2005年來，TCO極為重視「育藝深遠」教育體驗活動，在製作節目上費盡巧思，加上臺北市政府藝術輔導團協助製作教案、課程設計及學習單等，希望帶給六年級學童一個難忘的經驗。110學年之「育藝深遠」主題為「戰鬥吧！英雄：不想說再見大作戰」，結合了戲劇與國樂的創意表演，融入與孩子有關的議題，如電玩、霸凌、毒品、團結、勇氣等等貼近孩子生活的議題。今年由於疫情的關係，改為預先錄製成影片，後製以後在學校播放（TCO，2021）。
    </p>
    <p><strong>3.國樂頻道－藝術教育普及化</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NCO以「國樂頻道」為題，設計了《樂動客庄》、《印象南洋》等2套曲目，在樂團的排練場所，以演出、導聆及互動方式呈現。從4月開始一直到5月8日止，總計演出25場，由於場地的因素，每場人數從30至90人不等，總計觀眾人數1,233人。受疫情影響，下半年度停止舉辦此活動（NCO，2021）。
    </p>
    <p><strong>4.音樂沙龍與文化就在巷子裡－社區藝術巡禮</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市政府文化局所屬藝文推廣處為打造國內藝文展演平臺，鼓勵表演藝術愛好者及專業團體舉辦音樂演出，進而提升民眾藝術素養，於週日下午舉辦「音樂沙龍」活動。2021年國樂類的演出活動有14場，如期演出的有7場，因疫情影響而取消的有7場。文化就在巷子裡－社區藝術巡禮是該處所承辦的社區藝文表演活動，以戶外演出為主，室內演出為輔。2021年國樂類的社區演出共有7場，慶幸沒有受到疫情影響而取消的場次（藝文推廣處，2021）。
    </p>
    <p><strong>5.校園巡迴－推廣國樂，服務偏鄉</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國今年的推廣音樂會仍是採用樂團進入校園演出的方式辦理，由於受到疫情影響，上半年無法進行校園巡迴的演出，因此，一直延到9月中旬才開始進行，一共演出20場（桃市國，2021）。桃市國為了服務在地的國小，尤其是偏鄉、資源較不足的學校，讓學子們能夠有機會近距離接觸國樂樂器，並欣賞優美的樂音。依舊將樂團分為A、B兩組，以通俗、趣味性高的曲目為主，帶給學子們一個知性與感性之旅。此校園巡演不僅可以教育學生認識國樂，更可以藉此埋下國樂的種子，讓這些學子們成為樂團的忠誠支持者。
    </p>
    <p><strong>（二）比賽與檢定</strong></p>
    <p><strong>1.比賽：</strong></p>
    <p>（1）全國學生音樂比賽</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全國學生音樂比賽共分為團體、個人兩大類，參賽人員須為在籍之學生。共分為國小、國中、高中職以及大專組，其次再分為A、B兩組：A組為就讀音樂班、科、系、所者，B組則為就讀非音樂班、科、系、所者。除團體項目B組不得跨A組參賽外，個人項目並不禁止B組的學生跨A組比賽。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;109學年度全國學生音樂比賽團體項目中，屬於國樂類別的有：國樂合奏、絲竹室內樂合奏；個人項目中，屬於國樂類別的則有：柳葉琴、琵琶、阮咸、箏及揚琴等五大類別。因受疫情影響，教育部於1月28日宣布停辦國樂合奏與絲竹決賽部分，並於2月8日頒發「109學年度全國學生表演藝術類競賽決賽參賽資格證明書」。個人項目則如期舉行，個人項目如期舉辦，但取消場內觀摩及頒獎典禮。評審委員、參賽者、工作人員均須佩戴口罩。獎狀及評語，一律等到3月30日所有組別比賽完成之後統一製作寄發。
