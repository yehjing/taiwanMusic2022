<p><strong>（A）音樂的傳統與未來&mdash;2022臺灣音樂學會暨臺灣音樂學論壇年度研討會</strong>：由臺灣音樂學會、國立臺灣藝術大學中國音樂學系主辦，於11/19、11/20兩日假國立臺灣藝術大學國際會議廳演講廳舉行。今年研討會共有81篇投稿，共28篇論文通過審查，稿件審查通過率34.5%。其中包括18篇單篇論文、3組小組（共10篇論文）。本次研討會為期兩天，分為2個場地，共8場論文發表、1場論壇專題演講、1場圓桌論壇與1場土家族打溜子工作坊。與國樂相關論文有7篇，從回顧國樂歷史至國際的連結，乃至跨領域的研究，將國樂議題擴展得更為廣泛。</p>
<p><strong>（B）2022中華民國民族音樂學會青年學者學術研討會－傳統音樂的當代挑戰</strong>：由中華民國民族音樂學會主辦，於12/10假國立臺灣師範大學綜509國際會議廳舉行。會中除了有9篇青年學者的論文發表，還將舉辦已故中央研究院院士、戲曲學者、劇作家曾永義教授（1941-2022）的紀念論壇。</p>
<p><strong>（C）樂飛揚~2022TCO臺灣國樂發展論壇</strong>：由臺北市立國樂團主辦，於12/24、12/25日兩天假張榮發基金會國際會議中心603室舉辦。會中安排7場論壇，邀請專家學者從不同視角及區域探討臺灣國樂發展。此次研討會是從一個明年即將屆滿70年的國樂社團－中華民國國樂學會（以下簡稱國樂學會）談起，正如協同計畫主持人陳如祁所言：「業餘為專業之母、社團乃國樂之本」，點出了本次活動的主軸核心，臺灣國樂發展的基礎正是源自於此。國樂學會近70年，對推廣國樂所做的一切有目共睹，不言而喻。因此共有38位學者專家參與，包括4位退而不休，仍在國樂崗位奉獻的教授；2位專業作曲家；8位現任國樂專業高等教育學者及專家；10位國樂團團長；3位現任專業樂團指揮；12位業餘樂團指揮；耕耘在地基層中、小學國樂社團的教師15位；主要討論臺灣和離島地區的國樂生態，其中以臺灣北、中、南、離島作為分組，邀請長年在地耕耘的國樂推廣者，來做現今國樂在各地區的發展現況做討論，較為特別的是也邀請了部分海外專家分享國際的國樂發展狀況，藉由這樣兩天的會議深入淺出的討論國樂現階段在各地區所面臨到的問題，少子化的衝擊，各地區成立業餘樂團的狀況，對於各地學習國樂人的發展來做研討，藉此了解全臺國樂發展全貌，並與他者交流達到互相學習的效益。</p>
<p><strong>（D）2022音樂藝術與教學學術研討會</strong>：今年國立屏東大學音樂學系於5/3-5/4假屏東大學屏師校區圓廳舉行研討會，研討會聚焦在音樂與教學、音樂與健康、傳統與創新以及音樂與跨域四個主軸，皆為當前音樂領域中的核心發展領域。與國樂相關論文共發表1篇，蘇筠涵〈琵琶泛音技法於當代作品中的探索與實踐〉。</p>
<p><strong>（E）演奏與詮釋－2022研究生學術研討會</strong>：國立臺灣藝術大學中國音樂學系於11/12假國立臺灣藝術大學教學研究大樓10樓國際會議廳舉行「演奏與詮釋－2022研究生學術研討會」，共計發表13篇論文。論題包括創作手法、分析與詮釋、發展現象、樂器形制等不同面向。因多為演奏碩士，所以在論文題目的挑選與選擇以作品的探討為主，其內容有嚴謹的要求，論述其研究作品作曲家的音樂生平，作曲家對於創作時的手法，每篇論文皆有確實的田野調查，新作品皆與作曲家親自做訪談，從樂器上來看是非常多種類，這無形當中也是將各種樂器做深入作品的探討。在論文中更著重於青年演奏家於文獻與訪談研究後，對作品在演奏的詮釋與看法，引領出各自的音樂風格，培育出新的青年演奏家。</p>
