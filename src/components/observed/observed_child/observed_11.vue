<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022臺灣戲曲藝術節（4/1-6/5）鼓勵參展團隊以「妖」為主題，帶給觀眾活靈活現的感官刺激。在11個演出團隊中，臺灣國樂團的《魔幻時空大稻埕》無疑是小巫見大巫，不但沒有「妖」也沒有「魔」。百年來國樂以傳統、創新、跨界、融合等不同面貌訴說著臺灣的歷史故事，《魔幻時空大稻埕》是臺灣國樂團首度嘗試與吊環、大環、單輪車、高空疊椅、雜技、綢吊等新馬戲特技結合，三位作曲家以音樂敘事，安理實以敏銳的觀察文字訴說，分別記錄下大稻埕的前世記憶。因應「嚴重特殊傳染性肺炎COVID-19」疫情嚴峻，原訂5/14-5/15、5/21-5/22分別在臺灣戲曲中心大表演廳及衛武營國家藝術文化中心戲劇院辦理之《魔幻時空大稻埕》音樂會，延期至2023年舉行。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂對於戲曲的重要性不言可喻，《傳藝Online》第141期有一篇闡述戲曲與音樂關係的創見《論戲曲音樂之重要性 從構成元素談起》由鄭榮興教授執筆。戲曲中的「後場」指的就是音樂，後場樂師跟隨演員行腔轉運，在情節適當處幫襯，都要靠音樂，舊調如何新編腔而不失戲曲風格，成為當代戲曲創作的難題。而文武場的配合及戲曲音樂人才的養成也是專業訓練的一環。鄭榮興教授點出了文武場領奏者尚未被傳藝金曲獎納入評選，但慶幸國立傳統藝術中心近年推動傳統藝術接班人計畫以及駐團演訓計畫，使樂師也能經濟無虞，在傳統戲曲領域散放光芒。</p>
