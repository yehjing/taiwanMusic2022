    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了演出外，2021年樂團也舉辦了2場的講座，一場是「臺南傑出演藝團隊藝術交流分享之夜」，一場是「藝術薪傳講堂－來聽國樂543」。委創樂曲有2首，都是在「星空下的香草密斯朵」音樂會中發表，由謝惠如創作的《雙魚座－漢娜星球》及吳家榆創作的《星空下的香草密斯朵》（台南樂集，2021）。
