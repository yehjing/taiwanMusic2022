    <p><strong>1.文字出版</strong></p>
    <p>（1）書籍與樂譜</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;專書、樂譜共7冊，其中專書有6本，USB型式樂譜一份（見表15）。專書中研討會論文集有3冊，分別為顏綠芬（2021）主編《2020重建臺灣音樂史：臺灣音樂史研究回顧與發展願景》、樊慰慈（2021）主編《貳零貳壹傳統箏樂論壇論文集》及蔡秉衡（2021）主編《2021《演奏與詮釋》研究生學術研討會論文集》，皆為學術研討會之論文集出版。林昱廷（2021）〈2020年度國樂活動觀察與評介〉專文，是林昱廷以2020年度國樂領域的生態、國人創作及整體發展的趨勢與議題等面向所提出的觀察紀錄與評介報告，彙整收錄於張儷瓊主編的《2020臺灣音樂年鑑》；廖秋蓁（2021）梳理琴曲〈普庵咒〉與梵語〈普庵咒〉的淵源，〈悉曇章〉、〈釋談章〉與〈普庵咒〉的關係。指揮家顧寶文（2021）以跳脫西方思維，講述民族管絃樂指揮理論與實踐的論著。張儷瓊（2021）認為閩客潮箏樂正符合音樂學論述中的離散與混同，充滿著音樂混雜和流動的現象，因此展開閩客潮箏樂小曲的版本比較，探討南方箏樂因果流變的軌跡與脈絡。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團（2021）以USB型式出版《笙－作品精選》的樂譜，收錄22位臺灣作曲家創作、改編的30首作品，包含11首傳統笙，以及19首鍵笙曲目。從1960年代國樂耆老董榕森雋永的《五福臨門》，到臺灣專業教育培植出第一、二代的蘇文慶、朱雲嵩，以至今日專業教育開枝散葉的李俐錦、楊心瑜等人。《笙－作品精選》的出版，不僅見證臺灣笙音樂的發展脈絡，並留下樂譜供愛樂者參考使用。
