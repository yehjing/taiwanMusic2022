    <p><strong>2.九歌民族管絃樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;九歌民族管絃樂團（以下簡稱九歌）自2011年起，獲桃園市政府文化局同意與協議進駐中壢藝術館（該館自2012年起改隸桃園市政府藝文設施管理中心），駐館期間定期推出「樂活音樂講座」、「國樂工作坊」、「藝術在街角」、「國樂親子營」、「大型音樂會」、「九歌Young系列」、「九歌綻放系列」等活動。10年來，雙方媒合成功創造許多有意義的活動，資源截長補短，互惠互利，形成館方、樂團、觀眾三贏的局面，將藝文活動推展及落實到桃園每一角落，共同為藝術文化貢獻心力。更在5年前催生了桃園第一個專業職業樂團－桃園市國樂團。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年因疫情影響及中壢藝術館演講廳整修，除了空間不足，委員亦認為九歌階段性任務完成！故撤出中壢藝術館結束10年的駐館任務，回到團本址繼續為傳統文化教育與傳承扎根！回歸初心以學校國樂教育為主軸－輔導忠貞國小、福豐國中2所藝才班及跨校性質的桃園市青少年國樂團，並持續推出二胡、笛子&hellip;等工作坊。在疫情嚴峻下轉往線上音樂會發展，錄製了7集線上「九歌音樂學堂」，更結合桃園市國樂團、東風箏樂團及台中琵琶樂團於12月5日一同舉辦「藝起團聚」線上音樂會，藉此感恩醫護們的辛勞，並向醫護們致敬！音樂會中演出1首委創樂曲及2首新編樂曲；另外年度有1首新創樂曲尚未發表（九歌民族管絃樂團，2021）。
