    <p>（4）臺北市立國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TCO於2021年發行3張專輯，分別為《和諧．鳴響》、《彩戲繽紛》及《國樂飛魔力》。《和諧．鳴響》為雙CD，收錄2017-2021年間音樂會的首演作品，包括瞿春泉的《排灣神話》笙協奏曲、陳麒米的《阿朗壹》二胡協奏曲、陸橒的《雲．響》給擊樂、笙與國樂團的雙協奏曲、盧亮輝的《星座掠影》等作品，集結臺灣、大陸、新加坡等地優秀創作。《國樂飛魔力》及《彩戲繽紛》為DVD專輯，《國樂飛魔力》精選7首包含新創與經典的國樂曲，分由少年、青少年、青年、學院、市民與教師6個國樂團擔任，年齡層涵蓋小學3年級到長青族等不同階層的音樂會演出實況；《彩戲繽紛》為TCO於2018年10月10日在臺北市國家音樂廳所舉辦的「彩戲繽紛－四大名家之夜」演出實況，邀請廖瓊枝、王海玲、魏海敏及溫宇航4位名家同臺獻藝，演唱歌仔戲、豫劇、京劇及崑曲的精彩唱段，藉著專輯的問世留下經典，品味國寶藝師至臻的藝術風華。
    </p>
    <p>（5）其他專輯</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《秦語琴音－二胡詩人新美學》是林正欣首張二胡專輯，內容包括詩作朗讀+演奏專輯CD一片，攝影+詩作小冊一冊。結合演奏、創作、音樂、攝影、文學與思想的一張美學專輯。林正欣除了擔任二胡演奏之外，每首樂曲更是親自編創伴奏或重奏，除了音樂之外還加入了她創作的詩篇、攝像、朗讀詩作，此多重元素匯集在專輯中，顯示出林正欣個人的哲學思想與美學堅持。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;廖秋蓁所著的《悉曇普庵咒
      廖秋蓁古琴獨奏與琴歌彈唱》CD光碟即附錄於《悉曇普庵咒》書內。專輯中有廖秋蓁古琴獨奏《流水》、《洞庭秋思》、《普庵咒》、河洛音彈唱《陽關三疊》、梵音彈唱《釋談章》等曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《白蛇幻想－吳奇峯胡琴映象》取材自中國古典名著《白蛇傳》這個家喻戶曉的愛情故事，由現代作曲家劉聖賢與胡琴演奏家吳奇峯攜手合作，共同演繹這張全新創作。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳家崑的《風》二胡演奏專輯，整張專輯音樂風格多元，收錄多首經典傳統音樂和地方音樂改編、移植之作品，另由作曲家唐瑋廷先生為專輯創作新作品《霧影》，樂風突破以往傳統二胡作品的框架，挑戰音樂詮釋與演奏功力。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《嬗》是三個人發行的新專輯。此張專輯由三個人所處之世界觀出發，嬗，音似「三」，有轉化、變更的意味。此張專輯收錄多首古曲新編、民謠再創之作品，三個人融合傳統與現代，將生活中觀察到的體悟，捻進旋律，音樂風格遊走於傳統與當代間。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"On Fire"是陳依芳於2020疫情爆發後，與編曲與製作人藤井俊充開始這張拉丁二胡四重奏的製作，二胡的4個聲部及錄音皆由陳依芳一人擔任演奏。專輯中有傳統也有拉丁、爵士風格，用極具特色的二胡來表現音樂的另一種美。有聲專輯彙整如表19。
