    <h3>貳、國人創作與發表</h3>
    <p><strong>一、全年度的國樂創作</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;創作是國樂持續發展動力的來源，也為日後留下記錄當代聲響的資料，更成為大家共同的回憶與研究的史料。多年來，國樂界為了建立臺灣我們生活這塊土地的作品，持續不斷鼓勵作曲家們創作。職業樂團也為了建立自己樂團的特色，有計劃地逐年邀請不同作曲家來為樂團量身定作樂曲，藉以建立與其他樂團的差異化，此舉可從桃市國看到成效。2021年雖然疫情更加嚴峻，演出場次被迫取消和延期演出的就高達76場之多，部分新創作品因而未能如期發表。在如此艱困的環境下，國人在國樂創作方面仍然交出亮麗的成績，委創樂曲有101首，編曲有17首，共計118首（詳如附錄）。
    </p>
    <p><strong>（一）委創的來源</strong></p>
    <p><strong>1.委創來源的主體－職業樂團、民營樂團</strong></p>
    <p>（1）職業樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;職業樂團每年度都有大量的演出計畫，為了配合演出節目的需要，再加上樂團有較寬裕的經費，因此，都會有大量的委創樂曲，也成為國樂創作來源的主體。2021年持續受到疫情影響，有過之而無不及，甚至近2個半月的期間，臺灣的疫情警戒升為三級，導致所有活動都被迫停止，影響層面非常廣。慶幸臺灣人民的睿智與同理心，才能在短時間內將燃燒的疫情止住，也才有今日創作發表的機會與優異的成績表現。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年五大職業國樂團除了南市國以外都有委創樂曲，其中TCO全年共有21首國人委創樂曲，還有3首編曲；NCO全年共有2首委創樂曲；桃市國全年共有3首委創樂曲，6首編曲，委創樂曲中2首為國人創作，1首非國人創作；高市國全年有10首委託國人創作的樂曲，1首編曲；與2019年相同的是，有整場新創作樂曲音樂會，係2020年延期的場次。總計2021年職業樂團共委託創作了36首樂曲，其中國人創作35首，約佔97.22%，是這幾年的新高。今年職業樂團委託國人創作的樂曲35首，佔2021年101首樂曲中的34.65%，雖不如2019年46首，佔整體的44.66%，但比起2020年同樣受到疫情影響的年份，算是交出不錯的成績了。再從編曲的數量來比較，職業樂團的10首編曲，佔了整體的66.67%，是年度編曲的主要來源。
    </p>
    <p>（2）民營樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年民營樂團委創的單位有：隨心所欲樂團、台灣揚琴樂團、琵琶雅集、三個人、和琵琶、台南樂集等15個團體，一共委創樂曲37首，約占整體的36.63%。其中隨心所欲樂團有11首、台灣揚琴樂團有5首（另外有4首編曲）、琵琶雅集有4首在黃立騰的獨奏會中發表、三個人有3首、和琵琶有3首、台南樂集有2首、Drumily有2首，其餘8個團隊如：竹青、九歌等都有1首委創樂曲。由於民營樂團的經費較不充裕，在委創方面，相對需要公部門的補助。在這37首新創樂曲中，有7首是由國藝會補助。2021年的委創樂曲在數量上，相較2020年國人創作25首，增加了12首樂曲，雖然公部門的補助數量不多，但是對於提高國人創作是有其必要性。其次，隨心所欲樂團應TCO的邀請策劃全場新作品的發表，才會有11首的量體產生，也大幅增加全年的委創數量。
    </p>
    <p><strong>2.委創來源的枝幹－社會團體、專業系所</strong></p>
    <p>（1）社會團體</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年在社會團體的部分，只有國樂學會維持自2007年起關注國人創作的推展面向，同樣委約了10首國人創作，其中5首絲竹樂、5首大型合奏，約佔整體的9.9％。累計國樂學會委創樂曲至今已達143首，其中部分樂曲已成為音樂會選用之曲目，也有樂曲成為年度音樂比賽曲目，是建構臺灣在地作品重要來源之一。
    </p>
    <p>（2）專業系所</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在大專校院委創樂曲方面，2021年有7首，約佔整體的6.93%，都是臺藝大國樂系所委創的樂曲。由於2020年受到疫情影響，原本已經委創完成的2首樂曲，延至2021年演出外，2021年4月12日在國家音樂廳的演出，是臺藝大國樂系設系50週年紀念，因此，整場音樂會除了揚琴樂團外，其餘都是委創樂曲，整場發表7首委創樂曲。其中，4首協奏曲、1首打擊樂合奏、1首箏樂合奏及1首彈撥樂合奏。
    </p>
    <p><strong>3.委創來源的綠葉－個人邀約與創作</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年個人邀約、創作的作品有12首，佔總體的11.88%。青年二胡演奏家張瑟涵在個人音樂會中，發表3首委創樂曲，其中1首自己創作，另外委約2首；陳麗晶在個人琵琶音樂會中委約2首樂曲；李卿碧在個人琵琶獨奏會中委約2首樂曲；林雅雪、洪寬倫、郭虹希等5人在個人音樂會中都委約了1首樂曲。從上得知，在個人音樂會中發表委創樂曲已成常態，期盼這些樂曲能夠流通，讓臺灣的獨奏曲目能夠更加豐富。
    </p>
