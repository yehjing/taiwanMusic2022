<p><strong>三、研究方法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文主要採用歷史文獻分析法及田野調查法來蒐集資料與整理文獻。</p>
<p><strong>（一）文獻分析法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文獻分析法是指研究者根據研究目的廣泛蒐集與研究主題有關之文獻，例：期刊雜誌、書籍、論文、專書、研究報告、政府出版品、報章雜誌、蹲點紀錄、網路資料等，提取所需資料，進行整理、探討、歸納、統整與分析，具有客觀、輔助的特質。因網路發達，先從網路上搜尋有關研究對象及研究場域之資料，後續結合田野調查，加以探究分析佐證。</p>
<p><strong>（二）田野調查法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;田野調查法又稱為實地調查，指透過直接實地考察得到第一手資料之研究方法。若單靠文獻分析法，仍有不足之處，故多增此研究方法來加強。田野調查除了可取得寶貴資料，可檢驗文獻資料之可信度，將實地勘察所獲資料與文獻及訪談內容交叉分析比對，可避免真實與文獻的差異產生。</p>
