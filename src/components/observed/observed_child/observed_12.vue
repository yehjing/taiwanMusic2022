<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度國際交流活動較具特色者為2022年10月23日假國家演奏廳舉行的「2022音樂瑞士之夜─流動的幻境」音樂會。「音樂瑞士之夜」（Swiss Music Night）為旅居瑞士的大提琴家簡碧青與其夫婿穆勒（Fabian M&uuml;ller），為促進瑞士與臺灣音樂交流共同創辦的系列音樂會，每年均於臺灣和瑞士兩地舉辦，以推動臺瑞文化交流。音樂會主題以「水」的意象形塑文化交流的意象，演出曲目除拉威爾《水之嬉戲》、《夜之加斯巴》等鋼琴作品，另有穆勒寫給兩把大提琴的《茶之樂》。《茶之樂》為穆勒以臺灣茶葉和茶藝美學為主題寫作的組曲，靈感來自作曲家與大提琴家夫人於蘇黎世古城茶藝館經由主人周孟霖認識了臺灣與中國各類茶種，並於品茶與茶藝氛圍的薰陶下產生了創作構思。作品多用臺灣原住民音樂素材和山林想像，讓音樂和茶香、茶韻產生連結，如首曲〈肉桂紅茶〉中的阿美族五聲音階曲調，〈正凍頂烏龍茶〉和〈阿里山佛手烏龍〉的布農音樂與文化想像；〈老曼峨古樹普洱茶〉和〈正欉木柵鐵觀音〉轉為悠遠的如歌行板。作品以調性語法為基礎，融入臺灣曲調與山林文化想像。作品之素材運用雖未經嚴密的田野考證，然而茶的味覺聯想與美感刺激，確為音樂創作提供了趣味橫生的多樣可能。整體曲風溫暖和悅，反映了作曲家與大提琴家伉儷質樸的性情、深厚的情感、優雅的品味與溫馨的生活。</p>
