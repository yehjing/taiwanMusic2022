<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度推廣教育包含講座和音樂展演。講座部分較具代表者為國立臺灣交響樂團主辦的NTSO 講座音樂會系列：你所不知道的女性作曲家－芬妮．孟德爾頌、克拉拉．舒曼、麗貝卡．克拉克、比奇夫人，以講座結合音樂會的方式介紹音樂史上四位重要女性作曲家。臺南市許石音樂圖書館黑盒子劇場由劉岠渭、李宸逵、林亮志帶來多場音樂講座，該圖書館亦於10月1日至10月30日舉辦大提琴家馬友友特展「站在世界樂壇的頂峰」；臺南涴莎藝術展演中心藝術館亦有呂岱衛、李韋翰、湯發凱、焦元溥主講的知安讀書會音樂講座。此外，苗北藝文中心視聽中心亦有多場林仁斌主講之音樂講座，由林仁斌主講；而臺中國家歌劇院小劇場2022年的歌劇院沙龍則委由知名音樂評論家焦元溥主講多場歌劇講座。音樂展演方面較具代表者為屏東演藝廳的沙龍音樂會，國立臺灣交響樂團「2022 NTSO遍撒音樂種子在社區室內樂經典音樂會」，臺北市立交響樂團TSO青年管弦樂團「111年文化就在巷子裡系列音樂會」，以及福爾摩沙合唱團「2022藝文推廣系列音樂會」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年8月23日起，由音樂學者徐玫玲策劃、製作的「上帝與家園的浪漫－陳泗治逝世三十周年紀念特展」以及相關講座和音樂會，為本年度極具歷史意義的音樂推廣製作。特展分別於臺北臺灣音樂館和高雄衛武營展覽廳展出，講座和紀念音樂會也分別於臺北、高雄兩地舉行。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;特展分3大主軸：八角塔的浪漫琴韻、創作與演出的紀實和上帝的羔羊。從標題可看出「上帝」、「家園」、「浪漫」等關鍵字，對映著特展主視覺的「聖杯」、「花」與「白鴿」。展覽以大事記敘述陳泗治生平，再以3大主軸呈現陳泗治畢生經歷。「八角塔的浪漫琴韻」代表以「八角塔」為建築標誌的淡江中學，展出陳泗治1955年至1981年擔任淡江中學校長的點滴，乃至於馬偕、吳威廉牧師娘、陳清忠、德明利等關鍵人物的照片與介紹，呈現了西方音樂與宗教於淡水的傳播與發展。「創作與演出的紀實」進一步呈現了陳泗治的音樂歷程：學習鋼琴，接觸西洋音樂，和日本留學期間的作曲嘗試。陳泗治作品集中於鋼琴與合唱曲，數量雖少，卻融合了西洋技法與臺灣鄉土素材。陳泗治並曾於1934年擔任「鄉土訪問音樂演奏會」的鋼琴伴奏。此展區展出了陳泗治樂譜手稿、出版樂譜、CD與錄音帶和相關演出報導、音樂會節目冊與演出照片。「上帝的羔羊」披露了陳泗治1929年的受洗，終其一生為神的僕人，展出陳泗治使用過的聖經、1964年參與編輯的《聖詩》和晚年使用的講道稿、雜記、日記與筆記本。展場另有一音樂專區播放陳泗治作品錄音，包含兩首聖歌（救主我來就爾、至好朋友就是耶穌）與六首鋼琴曲（幻想曲－淡水、臺灣素描、回憶、降D大調練習曲、龍舞、幽谷－阿美狂想曲）。影片播放區則播放了此次展覽相關的紀錄片，如陳氏家族發跡於三角埔的歷史、陳泗治與淡江中學、陳泗治校長與兩位學生（陳冠州和卓甫見）的故事，以及長老教會許承道牧師談陳泗治牧師，溫暖而別具意涵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月17日和10月1日分別於臺北國家音樂廳和衛武營表演廳舉行的「上帝家園的浪漫&mdash;陳泗治逝世三十周年紀念音樂會」，上半場演奏陳泗治的鋼琴三重奏《G調嬉遊曲》和《龍舞》、《臺灣素描》組曲、《回憶》、《幻想曲－淡水》、《幽谷－阿美狂想曲》等鋼琴作品；下半場則由福爾摩沙合唱團演唱《如鹿欣慕溪水》、《上帝愛世人》、《西面頌》、《在主內無分東西》、《讚美頌》、《咱人生命無定著》、《讚美主極大恩賜》、《逝去的悲傷歲月》、《上帝的羔羊》等宗教合唱作品。透過完整的創作軌跡呈現出作曲家畢生不改其志的本土關懷與宗教虔敬精神。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年12月12、18兩日，由東吳大學音樂系校友組成的「雙溪樂集」假東吳大學音樂系舉辦了系列活動。其中由該樂集首度舉辦的「盧炎作曲比賽得獎作品音樂會」深具音樂教育推廣意義。此次比賽僅徵求鋼琴獨奏作品，得獎者有國中組藝術類的第一名《Recollection》王宥紘、第二名《Impromptus I》曾佑翔、第三名《摩天輪迴旋曲》干心菱，高中組藝術類的第一名《蝶夢》顏予、第二名《歧途》趙儀安、第三名《夜幕之下》林子群，大專組藝術類的第一名《沉吟，青橙的》饒雋、第二名《沐浴於憂鬱之鏡》高紹恩、第三名《Intermezzo》歐陽羽泰，高中組應用類的第二名《幽林》劉廷軒（一、三名從缺），大專組應用類的第三名《幻想》曾梓航（一、二名從缺）等作品。得獎者多為大學音樂系和中學音樂班學生，如大專藝術組第一名饒雋就讀於師大音樂系，其作品《沉吟，青橙的》為作曲者追憶夢中所見之「一種略帶酸澀的香氣，散逸在空氣中。有薄霧，有時青綠、有時橙黃，籠罩在我身邊&hellip;&hellip;」。樂曲始於高音域和弦，營造懸盪氛圍，隨後逐漸往中低音域探索新的可能，積累能量並釋放張力，交織著明澈、沉渾的音色。第二名高紹恩就讀於北藝大音樂系，作品《沐浴於憂鬱之鏡》由三個短小樂章：抑鬱、摩訶曼珠沙華、沐浴於憂鬱之鏡構成，以表現人類處於憂鬱徘迴之境，歷經掙扎而逐漸習之，乃沐浴於憂鬱黑暗，不再排斥、懼怕。高中組藝術類第一名顏予就讀於師大附中音樂班，作品《蝶夢》先以不斷重複的音型表達夢境前的眩暈感，再以快速音群與和弦象徵腦海中的記憶碎片與美麗幻想；後段則以頑固音型象徵現實生活中沉重的步伐，時而夾雜著世人紛擾的毀譽。樂曲結尾再次出現曲首音型，彷彿歷劫後步履蹣跚走出夢境的作曲家。第二名趙儀安亦就讀於師大附中音樂班，作品《歧途》間雜著波蘭舞曲的浪漫情調，呈現出流動氛圍和變化難測的聲響，卻勇於探索著不可測的歧途，大步向前。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年12月12日至18日，台北室內合唱團以「台北室內・三十而已」為題推出創團三十週年特別企劃&mdash;合唱無設限第12號作品《和諧的多重定義》。此活動與作曲家林桂如、跨領域藝術團隊「複象公場」、空間設計黃申全、文本創作沈琬婷合作，以跨領域展演形式，於板橋新光音樂廳共同展出。其中演出挑選代表台北室內合唱團不同階段的合唱曲，在新光音樂廳的展演空間內，進行視覺與聽覺多重並列的展演方式。包含合唱團與社會歷史足跡的故事文本、空間陳設與影像燈光等，觀眾只要戴上耳機，就能化身為台北室內合唱團的團員，在後臺空間裡體驗合唱團的過去。展覽最後則是走進音樂廳，欣賞作曲家林桂如為台北室內合唱團設計的無伴奏合唱劇場作品《Are You Ready？》，她與複象公場藝術總監王珩規劃場面設計暨動作指導，把作品設計比擬為數字倒數的過程，從十到一，就像演出者後臺到上臺的過程，也像合唱團要展開新的生命，以數字的形式串連起自由聯想的聲景。</p>
