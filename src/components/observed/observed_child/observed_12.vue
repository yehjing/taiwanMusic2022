<p><strong>一、上半年重要展演與朱宗慶打擊樂團</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年上半年因疫情演出稀少，較具質量之展演首先是3月上旬和4月上旬由新象文創製作，假北、中、南三地舉辦的「力晶2022藝文饗宴－許博允創作生涯六十週年」音樂會。此場音樂會演出許博允早、中期重要作品如《琵琶隨筆》、《寒食》、《境》。完成於1975年的《琵琶隨筆》由新生代琵琶演奏家黃立騰完美呈現了此部具指標意義的當代琵琶作品在傳統演奏手法基礎上的各種演奏可能與變化。同樣完成於1975年的《寒食》則再現了聲樂家張清郎的歷史錄音，說書人、晉文公和介之推皆由一人以西方美聲法和京劇唱腔呈現，吳興國的舞蹈渲染了春秋賢臣介之推的孤忠與悲情。《境》展現了許博允作品典型的堆疊手法，融合意境、心境、情境、環境，以追求作曲家心目中的「境界」，擊樂的豐富音色和西塔琴、蒙古呼麥、馬頭琴、蒙古長調、泛音詠唱等多樣音樂素材，貼切有力地呈現出創作者廣闊的亞洲文化關懷。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年上半年另一個質量俱佳的製作是朱宗慶打擊樂團於5月27、28兩天假臺北國家音樂廳舉辦的兩場「臺灣新聲力」系列音樂會：「擊刻風景」和「名家聚擊」。前者演出黃思瑜、楊祖垚、賴德和等人作品，後者則演出賴德和、洪千惠、李慧、林京美、劉韋志、蔡昀恬、蔡文綺、陳廷銓等八位作曲家風格迥異的作品。其中黃思瑜的《女媧》以擊樂表現上古傳說中的女媧揑土造人、彩石補天，使大地恢復生氣，曲風輕快活潑。賴德和的《不聞鐘聲，唯有風雨》結合了董陽孜的書法藝術，銅鑼的鐘聲象徵祥和，風雨則是揮之不去的疫情帶來的人心與社經動盪。李慧的《木蘭飄香》發想自作曲者閱讀母親編撰之《木蘭飄香》，全曲分風雲起、巾幗英雄、輕舟已過萬重山、燦爛的彩虹等段落，更引用了黃自《抗敵歌》旋律，以彰顯大時代的歷史氛圍。林京美的《心浪》源起於作曲者在臺中高美濕地的行旅經歷，身踏溫暖的土壤，落日餘暉和候鳥起落給予作曲者深深的感動。樂曲始於木琴獨奏，並以節奏堆疊出競奏抗衡的聲響。木琴的柔和音群象徵靜謐的濕地，擊樂合奏則表達了作曲者翻騰的內心。蔡文綺的〈鼓之華〉則嘗試以鼓聲和重複節奏，藉由聲部的堆疊與繁衍表達樹木由根部往上延伸的意象。音樂會尤其令人動容的是朱團子弟對創團者朱宗慶先生以匯演形式表達的感謝和敬意。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;財團法人擊樂文教基金會和蔣渭水文化基金會於2022年10月29日假國家音樂廳舉辦的擊樂音樂劇《俠飛春光&mdash;晨鐘暮鼓蔣渭水》堪為朱團下半年重要演出。音樂劇藉希臘神話依卡洛斯與其父建築師代達羅斯為逃出被幽禁的城堡，於飛行中忘記父親警告，高飛至蠟翼被陽光照射而融化墜海，以隱喻蔣渭水青少時於日本殖民統治下為爭取臺灣人民自決而入獄、驟逝的事蹟。四位作曲家在各個段落以差異頗大的寫作風格推展劇情，櫻井弘二與洪千惠書寫的段落較具旋律可聆性，楊祖垚的〈小夜曲〉和蔡昀恬的〈何罪之有〉則藉由音響性的摩擦與敲擊渲染了禁錮的黑夜與文字獄的意象。音樂劇至最後的〈俠飛春光〉段落，由童聲唱出「欲飛，想欲飛」的曲調，使觀眾情緒終於得到感性的紓解。</p>
<p><strong>二、管弦樂音樂會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;管弦樂演出部分，2022年首先登場的是1月22日假國立臺灣交響樂團舉辦的【新樂‧星躍】2022 NTSO青年音樂創作競賽決賽音樂會，本次比賽進入決賽的作曲家有李若瑜、洪郁閎、韓濟安、方雅襄四人，得獎作品依次為第一名洪郁閎的《杳》、第二名韓濟安的《空中歷險》、第三名李若瑜的《月光雲豹》、佳作方雅襄的《在光與影之間》，李若瑜《月光雲豹》同時獲得團員票選和現場觀眾票選獎。8月7日由臺北市立交響樂團假劍潭臺北表演藝術中心大劇院舉辦的「交響臺北2022－錢南章第五號交響曲」音樂會，作曲家以四樂章述說心目中的臺北意象：牧歌般的第一樂章描述淡水河右岸山林景觀和林中迴盪的鳥鳴聲；第二樂章描繪臺北大稻埕市街，管樂此起彼落的對話，頗能呈現老社區熱鬧的街面和市集，幽默有趣；第三樂章〈划龍舟〉以傳統音樂素材表現端午龍舟競賽的歡愉氣氛；第四樂章以自由手法呈現〈101大樓&mdash;臺北光點〉等城市文明的進展與豐沛能量。悅耳的旋律和貼切的配器描繪出臺北的傳統與現代，懷舊與前衛。搭配作品演出的影像，如第一樂章舞者自在地優遊於山林間和第二樂章臺北老街的熱鬧煩囂頗能對比出臺北的不同面貌。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國家交響樂團7月16日假國家音樂廳的「準．馬寇爾與NSO－理查．史特勞斯之夜」演出蔡承哲的《大自在天》和洪郁閎的《濫觴》。《大自在天》頗具儀式性與神秘色彩。作品以點狀與面狀音響象徵大自在天所代表之破壞與創造性格，作品音響飽滿，可說是「一分鐘作曲計畫」少見，於短時間內呈現完整創作理念的作品。洪郁閎的《濫觴》發想自「濫觴」詞語所指之江河發源細泉乃至於泛濫。作品以管弦樂營造的音響意象堆疊出細小水源乃至於江河泛濫的過程。10月16日由呂紹嘉指揮的「傳奇與傳承－紀念江文也」音樂會演出江文也《臺灣舞曲》、《北京點點》、《孔廟大成樂章》以及客家委員會委託創作顏名秀創作的《孤塵》等作品。呂紹嘉在每首樂曲演出前均詳細分析樂曲，頗能增加聽眾對江文也生平和作品的理解。顏名秀的《孤塵》，曲名取自於江文也《北京銘》中詩句，象徵江文也如同漂泊的砂粒，窮其一生找尋安身立命的位置。作品以五樂章呈現江文也的生命轉折，並藉由《臺灣舞曲》旋律素材的轉化與堆疊象徵江文也的歸鄉意識。</p>
<p><strong>三、室內樂音樂會</strong></p>
<p><strong>（一）</strong><strong>中華民國現代音樂協會與亞洲作曲家聯盟台灣總會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年國人創作之室內樂音樂會仍以亞洲作曲家聯盟台灣總會和中華民國現代音樂協會舉辦之年度作品發表會最具質量。中華民國現代音樂協會於10月28、30兩日假十方樂集音樂劇場舉辦了「中西合璧」與「臺灣當代獨奏作品集」兩場音樂會。首場「中西合璧」：英國作曲家盧長劍的《月》嘗試以「深思地」、「抒情地」和「精力充沛地」三主題的交替出現展現月色映照在水面上的詩意想像。周久渝的《突現深心》則以細碎片段的「小實體」交互作用後逐漸形成嶄新之更大實體的過程契悟人類心智活動之理解、頓悟等概念，作品以細緻的聲響呈現了深刻的哲思。呂文慈的《書七絕》啟發自作曲者觀畫的想像，將視覺所見的飄逸與昂揚呈現於樂音。潘家琳的《對招》則啟發自朱銘太極系列，嘗試透過音樂表現太極的虛實、動靜與剛柔並濟。李雅婷的《星塵》始於微弱之盛水玻璃杯緣的滑動聲響，以表現宇宙間粒子逐漸累積為微塵的緩慢過程，音響纖細。潘皇龍的《東南西北XI》則以預先設計的音列為音高素材，透過密集接應創造不斷變易、蛻變的形式邏輯與陌生化的音響意境。獨奏作品場：鄭建文的《雨荷》透過古箏的彈撥與揉弦，描繪出雨滴掉落荷葉的聲響，優雅傳神。熊仁岳的《呢喃之海》則以低音薩克斯風吹奏出遠方海洋依稀傳來的低語，藉由樂器的獨白呈現作曲家內心的沉思。洪郁閎的《終》藉由吉他虛實交替的聲響描繪《紅樓夢》〈賈寶玉夢遊太虛幻境〉中「落了片白茫茫大地真乾淨」之萬境歸空的曲意。張婉俞的《笙．聲幔》則以雲南民歌《小河淌水》和笙曲《晉調》的片段旋律和鑼鼓經為素材，運用笙的口舌技巧與指斷奏來變化音色，使笙的音質呈現出如同布幔細紋的纖細色彩。陳以軒的《連雲霞》透過鋼琴的音色與技巧變化、動靜聲響的交替與融合，表達芭蕉俳句中青蛙與古池互動的瞬時意象。馬定一的《謐》則以長笛、短笛與中音長笛的交互吹奏，呈現〈牧歌〉、〈希利克〉、〈客居隱士〉和〈來自田園的活力〉等四樂章的不同曲趣，四部小曲組成的整體作品試圖表現作曲家希冀之喧嘩世界中的純真與靜謐。最後，蔡淩蕙的《琴想XI&ndash;神將出巡》嘗試以北管素材，透過琵琶來描繪民間祭儀所見之神將出巡的熱烈氛圍。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;亞洲作曲家聯盟台灣總會2022年的「音樂臺灣作曲聯展」始於11月12日假國立臺中教育大學寶成演藝廳舉辦的音樂會，演出許常惠、馬水龍、張炫文三位作曲家和六位曲盟會員作品；其次是12月3日於國立臺北藝術大學舉行的兩場室內樂音樂會：下午場於馬水龍廳演出馬水龍寫給大提琴與鋼琴的《隨想曲》和其他六部作品，晚上場則於北藝大音樂廳演出賴德和、潘皇龍的舊作與新曲，以及五首該會會員作品。首場於臺中由曲盟委託林進祐創作混聲四部合唱《情牽大墩》，以及謝宗仁創作豎琴與弦樂團《柳綠》，以表現作曲家的本土與文化關懷。臺中場另演出張炫文多首合唱作品和南投作曲家陳慶章描繪墾丁龍磬公園景觀的《龍磐放風吹》。連憲升的銅管五重奏《大武山的呼喚》則採用排灣族音樂素材，透過小號的音色變化和銅管五重奏的壯闊聲響，呈現世代居於大武山區排灣族豐富的音樂資產。許常惠的長笛獨奏曲《盲》藉由臺灣市鎮盲人吹笛聲的旋律，透過單一樂器做各種音調變形和曲式的發展，以五聲性主題和各種調性與無調性的變奏描述生命的迷失、呼喊與掙扎。謝宗仁寫給豎琴與弦樂團的《柳綠》則嘗試表現作曲者漫步於臺中市區的柳川與綠川間，緬懷舊城風華並感嘆新舊遞變。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北藝大作曲聯展的下午場以馬水龍《隨想曲》開場，作品為作曲者童年生活的回憶，音樂始於慢板沉思，活潑的快板彷彿童年的歡笑，並夾雜著莫名的憧憬和淡淡傷感。王之筠的《閾限狀態》從最簡單的肢體動作觸發出微小的聲音，逐漸發展並使感知重組，樂器的互動細膩描繪了生命不歇止的變動。桑磊栢的《光年》，靈感來自由望遠鏡探測之遙遠星系發出的電波。長大的高音代表遙遠的電波，閃爍的音型則象徵星星的循環與運行，下落的音型描述從螺旋星系延伸出的銀河。范立平的《雙生》嘗試以兩把中提琴探討肉身與靈魂複雜的雙生與互動。李政蔚的《形孤影單》啟發自劉禹錫〈陋室銘〉，嘗試以單一樂器的細緻音色表達「山不在高，有仙則名，水不在深，有龍則靈」的寓意。陳立立的《鏡外/鏡內》以飽和學派曲風表達不同視角所觀看的景象和視覺體驗，作品試圖透過各種泛音變化和點狀聲響將「鏡外/鏡內」的景象轉化為失焦/微（defocused/microscopic）的聲景。羅芳偉的《幻夢》則以長笛的氣聲與大提琴泛音為主體，讓音色交疊，建構出模糊而有色彩感的織度，以描寫作曲者平靜卻模糊的夢和作曲者留學期間初履海外那充滿著色彩與未知的生命體驗。晚上場始於蔡承哲的《摩利支天》，作曲者以纖細的音響織體表現其對於古印度黎明女神摩利支天之形象與護身、得勝等神力的想像。林梅芳作品《交織的記憶》則以作曲家創作生涯為出發點，回顧自身於不同作品所營造的音樂姿態和聲響，以為自我的音樂回顧與記錄。賴德和的《夢》為作曲者1998年客居紐約時期所作，作品簡約而節制的音樂呈現給予聽者夢的想像空間。蕭慶瑜的《迷霧》則以七件樂器相互之依存、消融、對立、聚合等多重聲響傳達作曲者霧中漫步於宜蘭太平山古道所體驗之林間古道的幽渺景象。音樂會壓軸為潘皇龍展現其色澤音響理念的《花甲禮讚》。此曲原為日本作曲家協議會60週年慶委託創作，曾於2022年3月31日首演於東京川崎音樂廳。作品由六首小品組成，完整演繹了作曲者的音樂思維和音響理念。</p>
<p><strong>（二）</strong><strong>專題製作，作曲家個展與聯展</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年7月26日，詠樂集假國家演奏廳舉辦「講予全世界聽－路寒袖詩選歌樂創作」音樂會，演出了游昌發、賴德和、潘皇龍、錢善華、李和甫、陳瓊瑜、陳欣蕾等七位作曲家作品。其中李和莆、游昌發、賴德和、潘皇龍的作品以生動的音樂語彙渲染了路寒袖詩作之本土、幽默的詩性，音樂材料多用平易可親的調性與五聲音階。六首委創新作中，陳瓊瑜的《思念的歌》以單音旋律由鋼琴前導，女高音相對較為沉靜的詠唱出詩意的孤寂感。陳欣蕾的作品皆與母性有關，《三月嗩吶》的鋼琴以不斷纏繞之三音動機象徵媽祖繞境的曲牌，人聲唱出媽祖繞境時信眾的虔敬與媽祖庇佑人間的溫暖；《針》則唱出女性的堅韌和慈愛。錢善華的《臺灣》以簡約的質樸音型支撐歌者唱出的原民世界；《布袋戲組曲》則以銅鑼熱鬧開場，三段歌詞〈金光篇〉、〈渡臺篇〉、〈勸世篇〉唱出先人渡臺的艱辛和布袋戲的熱鬧與華彩。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10月1日由音樂學者吳榮順製作的「臺灣音樂憶像系列&mdash;客籍前輩音樂家：徐松榮與曾興魁舊作新曲音樂會」假臺灣戲曲中心小表演廳舉行。音樂會難得聽到兩位作曲家的舊作如徐松榮鋼琴組曲的《人物素描》，從中可窺見作曲家年輕時學習調性音樂的痕跡；〈病子歌〉、〈月光華華〉等合唱作品以及客家素材鮮明的《弦樂四重奏》則充分彰顯了作曲家畢生致力於基礎教育與客家文化傳承的貢獻。從曾興魁的合唱作品〈春天的腳印〉和改編的〈江文也第一彌撒曲〉可看出作曲家扎實的調性音樂功力，《陽關三疊變奏曲》和《五行生尅》木管五重奏則呈現了作曲家悉心現代技法並以之表達東方哲思的努力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10月22日於國立臺北藝術大學音樂廳舉辦的「禮讚40&mdash;北藝大作曲名譽教授音樂會」演出錢南章《大提琴與鋼琴二重奏》，馬水龍《水龍吟》和《弦樂四重奏》，潘皇龍《迴旋曲》和《跨越時間與空間的藩籬》，以及賴德和的《鄉音II：南管音樂的聯想》，呈現出四位作曲家獨特的風格。錢南章《大提琴與鋼琴二重奏》四樂章形式猶如古典奏鳴曲，第二樂章慢板融入民謠曲調，令人印象深刻。馬水龍為琵琶獨奏的《水龍吟》豪放而悲切，表現力不遜於許博允的名作《琵琶隨筆》；《弦樂四重奏》於堅實的曲式結構中融入鑼鼓節奏和吟詠樂念，可看出作曲家融傳統元素於西方技法的努力。潘皇龍《迴旋曲》為作曲家早年於德國留學時運用十二音技法與色澤音響的作品，頗具當代音樂典型之陌生化特質；《跨越時間與空間的藩籬》為整場音樂會較新的作品，單簧管演奏者由遠而近、由近而遠的聲響變化和走位，增添了時間與空間之「跨越」的想像。賴德和《鄉音II：南管音樂的聯想》則以長大的單樂章融入南管散曲〈秀才先行〉曲調和〈梅花操〉的急板，以現代手法呈現出南管古樂的美感。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;對位室內樂團於11月27日假高雄市文化中心至善廳舉行的「聽・聲&mdash;彩虹的聲音們」音樂會演出六部國人作品：應廣儀《四聖諦初探》以簡素的音響探討苦諦、集諦、滅諦與道諦之佛教教法，呈現作曲者體會之顯現與空性雙融合一的「顯空不二」感受。陳宜貞的《漫步日暮時》和《營火旁》分別以美國詩人惠特曼和佛洛斯特的詩歌為素材，唱出詩人於大自然中恬靜、澄澈的生命體驗。林佳瑩的《Isolarion》和楊祖垚的《藍III》，前者嘗試探索某種非固定、去中心的游移音響，後者則以纖細的聲響呈現畫家米羅藍色夢境的觸感與波動。旅奧作曲家高愷怡的《聲存》試圖透過三位演奏者的互動，呈現大自然物種的生命力與存在特質。趙菁文的《在影子上，彩虹的聲音們》音響豐富而多彩，舞臺上靈動的擊樂者以活潑俏皮的造型把玩著象徵童年的音樂盒、風鈴、沙鈴和紙張，生動渲染了藉由音階與和聲、節奏與時間之組合所營造出的「彩虹的聲音」。2022年12月3日，作曲家楊聰賢假十方樂集音樂劇場舉辦其個人作品展，演出七部室內樂作品。其中《斷夢殘景》啟發自日本導演黑澤明電影《夢》，以抒發作曲者任教於交通大學數年間的心境。《家居樂誌》則以作曲家日常生活為靈感，由〈講古〉、〈思前念後〉、〈觀球賽〉和〈家貓搖籃歌〉等小品組成。《含悲調》係寫給一位雙簧管演奏者之現場演奏與預製錄音，為作曲家悼念亡父的安魂曲。為大提琴獨奏的《流水七絃》則為慶賀馬水龍70歲生日而作，靈感來自於李白「聽蜀僧濬彈琴」：「為我一揮手，如聽萬壑松。客心洗流水，餘響入霜鍾」的詩句。</p>
<p><strong>（三）跨域展演</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;室內樂演出較具實驗性且有跨域嘗試的音樂會，首先有2022年末北藝大音樂學系與新媒體藝術學系12月17、18兩日假臺灣戲曲中心小表演廳演出的「後花園 III－情感漫遊，科技與音樂跨域實驗劇場」，音樂部分由作曲家陳立立帶領學生合力完成。主題看似浪漫，實際上卻是以神經失調之「慮病」與「多夢」等恐懼、曖昧情感為主軸的作品。分段副題如「漫遊癖與幽閉、空曠恐懼」，「渴慕與快感恐慌」，「狂怒與狂喜」均環繞此主軸而繁衍、開展。陳立立將其電子音樂原作《病2》拆解為五個段落，以為整場演出之序曲、插曲與尾聲，為作品提供了音響風格與表達內涵的統一基礎，讓作品整體彷彿一人完成。此類主題倒也頗適合委諸由尖銳的電子音響、噪音和激烈的樂器音響組合而成的「飽和」聲響。幽暗的燈光與空間布局呼應了作品主題。生命唯有置身極限的精神療癒，才得以回復平靜，嚮往著溫柔和喜悅、光明。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;時間藝術工作室於12月25日假國家兩廳院實驗劇場舉辦的《我們的二〇年代》委託創作專場也結合了若干預製影像。楊祖垚的《奧塔維亞素描》靈感來自卡維諾科幻小說《看不見的城市》中懸盪於兩山間的城市，以噪音為主的音響營造頗能表現此座城市的危殆狀態。徐儀的《侘寂》嘗試表現日本禪宗美學的「侘」（簡素的優雅之美）與「寂」（時間易逝與萬物無常）。作品於幽靜的基調中偶或以巨大的銅鑼擦擊使人更加眷戀寂靜，作品首尾以「磬」滑奏出的細微聲響迴盪於幽靜的空間，展現出徐儀作品一貫的冥想氛圍。許德彰的《漏斷II》以「漣漪」的意象和聲響營造為核心，暗喻時空中人、物的層層關聯與互動。潘皇龍的《東南西北X》由10首小品組成，作品以古箏與笙結合西樂為編制。10首小品分布於〈虛實相依，古今相生〉、〈動中有靜，靜中有動〉和〈陰陽和諧，東西合璧〉三個音響「聚落」，相較於中青代作曲家以電聲和噪音為主的音響呈現，潘皇龍此曲則實音多於虛音，樂音多於噪音，於當代音樂系譜似已悄悄從開展新局的「前鋒」移轉至堅守堡壘的「後衛」。</p>
<p><strong>四、合唱音樂會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度合唱音樂會較具代表者有二：台大合唱團7月26日假國家音樂廳舉辦的「千縷微風&mdash;2022夏季音樂會」以3首本土作品《濁水溪溪水濁》、《我記得》、《風平浪靜》揭開序幕，呈現出臺灣音樂的多樣風情；由簡上仁採集、整理、譜曲的《十二生肖歌》和展現臺灣民間信仰的《收驚歌》皆為合唱音樂會少見的曲目。上半場最後則以劉新誠的《當我們年輕時》、《新雨》、《渡口》、《一棵開花的樹》、《如此愛你》五闕作品結束，傳達了人間溫柔堅毅的力量。10月19日台北室內合唱團假中山堂中正廳舉行的30週年團慶音樂會「台北室內．三十而已」演出了郭子究的《回憶》、瞿希賢編曲的蒙古民歌《牧歌》、Christopher Borela編曲的《茉莉花》、冉天豪編曲的洪一峰《舊情綿綿》、周鑫泉編曲的李叔同《城南送別》、戴金泉編曲的古調《陽關曲》、曾興魁編曲的臺灣民謠《一隻鳥仔》、錢善華編曲的《丟丟銅仔》、呂泉生編曲的《快樂的聚會》以及潘皇龍的《夢遊女》，呈現出合唱團音樂視野和表達能力的茁壯與成長。</p>
