<p>2022年年鑑紀錄95首國人作品首演，較特別的有：衛武營首屆國際音樂節開幕音樂會「跨越兩世紀的音樂」，由國家交響樂團演出林佳瑩的管絃樂作品《糖話故事》，此曲利用鋼片琴與豎琴的音色加總其他廣闊音色，創造音樂本身靈動的感覺；龍潭愛樂管弦樂團「向大師致敬」為鍾肇政先生紀念音樂會，演出陳廷銓的管絃樂作品《攀一座山》，以音樂詮釋鍾肇政文學作品，同時融合了客家山歌與民謠的元素；朱宗慶打擊樂團臺灣新聲力+系列音樂會「擊刻風景」演出黃思瑜《女媧》以及賴德和《不聞鐘聲，唯有風雨》，《女媧》展現出從天地混沌至女媧造人、洪水氾濫與彩石補天等精彩神話，《不聞鐘聲，唯有風雨》搭配藝術家董陽孜書法作品〈無聲的樂章〉以及多媒體藝術家周東彥設計視覺，樂曲表達期望世界能回歸純粹，「名家聚擊」則是首演劉韋志《RESISTANCE III：卡珊德拉與光》以及陳廷銓《敲》；李元貞《美濃之道》由客家委員會委託創作，國家交響樂團演出，作曲家深入美濃探訪、體驗在地日常，採集美濃人文、客家歌謠而創作的交響曲；國家交響樂團「一分鐘交響曲作曲計畫」2022年首演蔡承哲《大自在天》、洪郁閎《濫觴》、鄭雅慈《西濱印象》、陳思安《濛濛》、洪郁閎《擬態》、蔡倍萱《豐神》、陳彥興《熒熒》，「一分鐘交響曲作曲計畫」為國家交響樂團準．馬寇爾為培育臺灣年輕作曲家所規劃，以60秒的樂曲創作為嘗試，藉此讓更多青年作曲家創作獲得實際被演奏的機會；臺北市立交響樂團「交響臺北2022－錢南章第五號交響曲」首演錢南章第五號交響曲《臺北》作為臺北表演藝術中心開幕節目，以四個樂章述說臺北意象。</p>
<p>李志純《台灣歌詠》為樂亮管絃樂團委託，以客家山歌、臺灣民謠為發想元素創作；金希文《恁著相疼》首演於幕聲合唱團15週年音樂會「愛的誓言」；林松樺《夜的思想》首演於迎曦女聲十五週年音樂會；玎酉擊樂年度音樂會「薈II」演出四首作品，陳鎧岳《鏡．双生》、《春雨》、鍾耀光《疫情．愛情》、陳廷銓《眩月》；長榮交響樂團20週年慶祝音樂會「感恩與祝福」首演鍾耀光的交響詩《鄭和下西洋》，此交響詩的取材對應長榮集團的本業航運，以打擊展現出大海的波濤洶湧；高炳坤大提琴獨奏會「Ciao Paris．再見巴黎」首演楊聰賢以李白古詩為靈感的《流水七絃》，展現大提琴的迷人音色與豐富技巧變化；國家交響樂團「傳奇與傳承－紀念江文也」演出由客家委員會委託創作的顏名秀《孤塵》，取自於江文也《北京銘》詩集中〈沉默〉一詩的文句，作品試圖將江文也大起大落的傳奇人生，依照時間順序，轉化為五個樂段做生命巡禮；中華民國現代音樂協會2022年臺北國際現代音樂節「中西合璧」，於十方樂集音樂劇場首演呂文慈《書七絕》、李雅婷《星塵》、潘皇龍《東南西北XI》，三首曲目皆是由中西樂器共同合作，反映「中西合璧」；中華民國現代音樂協會「臺灣當代獨奏作品集」則首演馬定一《謐》、陳以軒《連雲霞》、熊仁岳《呢喃之海》，三首曲目分別為長笛獨奏、鋼琴獨奏、上低音薩克斯風獨奏；張雅晴2022小提琴音樂會「海翁繪続」首演劉聖賢《福爾摩沙》組曲以及蔡淩蕙《海翁樂繪》小提琴協奏曲。</p>
<p>基隆表演藝術中心開幕季「雞籠狂想－中元渡殤」由基隆交響管樂團首演陳昶安《雞籠狂想》，為推廣在地文化，結合雞籠中元祭歷史，委託作曲家創作《序》、《陣》、《鬥》三首系列作品，內容描述雞籠中元祭的由來，同時慶祝基隆表演藝術中心開幕；亞洲作曲家聯盟台灣總會「音樂臺灣2022作曲聯展－臺中場」於國立臺中教育大學寶成演藝廳首演林進祐《情牽大墩》、謝宗仁《柳綠》，從樂曲中可以感受到臺中的土地與風景；北藝大新音樂工作坊「RE：時代」首演高愷怡《雨水》，為大提琴獨奏，創作概念來自中國二十四節氣「雨水」，藉由大提琴特有渾厚豐滿且溫暖帶質感的音色，加上不同演奏技巧的運用，描繪此時節萬物春回大地的景象；朱宗慶打擊樂團與現代音樂協會「現打．擊音II－擊現無極限」首演黃祈諺《折射》；十方樂集「2聆22－諦聽」首演楊聰賢《清音擊響聆餘輝》、《亂之迷》、《家居樂誌》；曲盟台灣總會「音樂臺灣2022作曲聯展III」首演林梅芳《交織的記憶》、蕭慶瑜《迷霧》，中國文化大學60週年校慶音樂會「振衣千仞岡」演出李哲藝《華岡萬里流》；台北愛樂合唱團50週年音樂會演出錢南章第九號交響曲《紅樓夢》，此曲為錢南章老師的封筆之作，以紅樓夢作為主題，描述人生的起落。《紅樓夢》全曲分為六個樂章，依序為序曲、石頭記、葬花吟、大出殯、劉姥姥、紅樓夢，並在結尾以傳統戲曲「跳加官」結束，祈求太平盛世的到來，交響曲除了序曲為樂團，其餘皆加入合唱，致敬貝多芬第九號交響曲。</p>
<h3>玖、結語</h3>
<p>因疫情故，2022年上半年各類音樂展演均受壓抑與限縮，至夏天以後始逐漸恢復正常，尤其從秋季至年末的演出趨於頻繁和密集。2022年管弦樂演出之國人作品相較往年減少甚多，但室內樂演出則維持如同往年的質量，其中尤以亞洲作曲家聯盟台灣總會和中華民國現代音樂協會，無論是委託創作首演作品或既有作品之發表均維持一定質量，從中並明顯可見年輕世代作曲家有別於前輩作曲家的嶄新作曲思維與音響理念。表演團體方面，朱宗慶打擊樂團可說是最能於疫情稍緩之際，積極推出舊作新曲的團隊。資深作曲家中有許博允、楊聰賢自辦之個展，亦有臺灣音樂館製作之徐松榮與曾興魁舊作新曲專場。潘皇龍先生本年度雖無個人專場音樂會，然其新舊作品仍於諸多室內樂型態之國人作品發表會頻繁演出，且均維持其一貫的創作語彙和優異品質，展現出作曲家精益求精的藝術精神，甚值景仰、學習。研討會方面，2022年「重建臺灣音樂史」學術研討會以「被遺忘的音樂‧人物」為題，探討呂炳川、李哲洋、林氏好、鄭有忠，乃至於李健、徐松榮、包克多、柯尼希等人作品與事跡，補足了臺灣音樂史多年來的缺漏。出版方面，兩位前輩作曲家賴德和、温隆信陸續出版自傳，提供國人更豐富的作曲家生命史料；而臺北市立交響樂團出版的《錢南章第5號交響曲》和辛幸純的《素描．臺灣：馬水龍鋼琴獨奏作品全集》CD專輯更為國內音樂愛好者增添了從音樂親近本土，體會作曲家透過其創作美學實踐文化關懷的多樣可能。</p>
