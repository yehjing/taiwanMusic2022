<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在流行音樂研究方面，不論是期刊論文、碩博士論文、研討會論文或是國科會研究案，涵蓋領域從音樂、傳播、文學、科技、社會、語言等，顯示流行音樂已然成為一個跨域多元的研究對象。國科會專題研究計畫，例如國立中正大學傳播學系簡妙如「串流中的聆聽：音樂科技與串流平台、音樂人、聽眾的相互形塑」、國立政治大學廣播電視學系黃俊銘「玩團、社交與藝術世界：玩音樂的專家與業餘實踐」、嶺東科技大學資訊科技系林冠妤「聽見科技的聲音：探討人工智慧環境下音樂串流平台使用者體驗行為」等；期刊論文，例如陳培豐於《臺灣文學研究集刊》發表〈雙重解殖與「臺灣味音樂」的形塑（Ⅰ）－臺語流行音樂之路（劉清池篇）〉等；碩博士論文，例如國立臺北藝術大學藝術行政與管理研究所焦惠芬〈流行音樂演出的5G科技應用實作：以2021桃園鐵玫瑰音樂節為例〉、國立臺灣師範大學音樂學系流行音樂產學應用碩士在職專班鄧人傑〈流行音樂演奏者使用個人混音器之體驗研究〉、國立臺北大學社會學系林佳欣〈女聲書寫：台灣女性獨立創作歌手的主體意識展現〉等；研討會論文，例如許世融於2022重建臺灣音樂史學術研討會中發表〈戰後臺語禁歌類型與歌詞語料分析－以原臺中「中聲電臺」黑膠唱片為探討對象〉等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在音樂專輯方面，本年鑑共收錄了148張，包括華語專輯93張，其中含3張中國歌手專輯、24張團體/樂團專輯，例如臺南搖滾樂團甜約翰《In Mind》、曾入圍美國獨立音樂獎Fann芳怡《桃樂市》、曾獲2019金曲獎最佳演唱組合的椅子樂團《Cheers!Land》都相當有特色。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在數位發行方面，2010年參加選秀節目《超級偶像5》拿到亞軍，並擁有六千萬點閱率的〈踮起腳尖愛〉，沉寂十多年後，歌手洪佩瑜2022年《明室》專輯正式推出前，以單曲〈不在一起就不會分開〉試水溫，好流量的成績給了團隊很大的鼓勵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五月天在2020年底發布單曲〈因為你所以我〉之後，一直都沒有新作，粉絲苦等670天後，終於在11月1日凌晨盼到新歌〈你的神曲〉，一上架就即刻成為臺灣榜單的冠軍。年底之時，更發行〈為你寫下這首情歌〉，撫慰歌迷，象徵告別2022，期盼迎接嶄新的2023年。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀察iTunes、Apple Music、Spotify、KKBOX、Youtube、MyMusic、Friday音樂平臺之數位點播量，華語男歌手以周興哲、周杰倫、周湯豪為2022年最大贏家，周興哲以〈想知道你在想什麼〉獲金馬獎最佳原創電影歌曲、周杰倫睽違6年再度發行完整專輯、周湯豪擺脫母親比莉的羽翼，展現驚人的創作與製作實力，金曲歌王蕭煌奇發行華語專輯《說故事的歌》也不容小覷。華語女歌手則以香港歌手鄧紫棋、阿美族歌手A-Lin網路聲量較高。團體部分則是告五人、選秀節目出道的男子團體原子少年出線。饒舌歌手在較為沉寂的2021年之後，於2022年捲土重來，以瘦子E.SO、熱狗、熊仔、高爾宣等人為代表，他們的歌詞寫出疫情期間小市民的心情、唱出饒舌歌手不被重視的過往，慢慢奠定此類歌手在流行音樂界的地位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在數位時代，具實力的歌手亦可運用數位頻道創下話題，例如日本人氣音樂YouTube頻道「THE FIRST TAKE」擁有近700萬的訂閱人數，11月7日邀請韋禮安為節目獻唱〈如果可以〉，一鏡到底呈現全新的R&amp;B編曲，創下臺灣歌手的首登場。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺語專輯在今年熱鬧非凡，眾多資深男女歌手紛紛發行專輯，演唱連續劇歌曲，衝高能見度，例如龍千玉《九月玫瑰》專輯中的〈九月玫瑰〉，搭民視「黃金歲月」第141-210集片尾曲；郭婷筠《花開花謝》專輯中的〈花開花謝〉，搭民視「黃金歲月」第223-282集片頭曲；白冰冰《體會》專輯中的〈體會〉，搭民視八點檔「市井豪門」第1-60集片頭曲，與臺視「美麗人生」第4-5季片尾曲；黃妃《回想》專輯中的〈回想〉，搭三立假日八點檔「一家團圓」第1-17集的片尾曲；洪榮宏《寒冬過了是春天》專輯中的〈寒冬過了是春天〉，搭三立假日八點檔「一家團圓」第37-78集片尾曲。以上專輯曲風都依循臺語歌曲的風格，固守音樂基本盤的意味濃厚。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵大倫8年磨出一張《出一支喙》專輯，音樂風格融合情歌、搖滾、Funk、Bossa nova，還加入北管、殼仔弦、相褒歌、臺語八音等文化元素，更難能可貴的是邀請傑出臺語作詞者武雄擔任專輯企劃統籌，特別是〈八音之歌〉是將臺語的八個音調唱成一首歌，對於以音樂推展臺語相當用心。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的臺語歌曲除了實體專輯外，也於數位平臺大量上架。但有關點播量的排行榜僅KKBOX與MyMusic有做年度排行，其餘平臺則無。綜觀上述兩平臺與Youtube上的MV點播量，臺語歌曲以八點檔電視劇片頭曲、片尾曲、主題曲攻佔排行榜，這些電視劇邀請實力唱將如張秀卿、龍千玉、翁立友等人演唱，他們經典的唱腔百聽不膩。而臺語樂團也不可忽視，除了玖壹壹、旺福，年輕團體草屯囝仔、2022年金曲獎最佳新人獎得主珂拉琪也都受到廣大聽眾的喜愛。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在客語專輯方面，2022年除了有生祥與瓦窯坑3《臨暗》重新混音版，與</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;生祥樂隊《菊花夜行軍15週年紀念演唱會》現場錄音精選輯外，亦有不少精彩的專輯。國立臺北藝術大學戲劇系畢業的邱舒，既是斜槓，也是跨域，2016年獲得臺灣原創流行音樂大獎客語組參獎的〈定定仔行〉開啟8首歌曲的專輯《等路Waiting a present for》，各有不同吸引人的風韻。2010年前開始投入母語創作的黃鎮炘，自組阿比百樂團，2017年發行《哈客家Hot Kakka》，以大埔腔凸顯專輯的特質，2022年的《轉轉轉》專輯中探討不同的人生歷練，有愛情、異國戀情、環保議題與底層的農民等，多元而真摯。由顏志文領軍、成軍25年的山狗大後生樂團，延續2014年出版傳統民謠改編的專輯《簷頭下》，《簷風謠》除有部分是收自在簷頭下唱的歌，亦有新的創作，將兩者交織於一，並擴大弦樂團的編制並增添一支雙簧管，更豐富集體的聲響。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有好久不見的音樂人，亦有得到獎項的年輕歌手，2022年的原住民語專輯不是新舊交替，而是世代相互爭輝。睽違22年，Biung王宏恩的全布農語創作專輯《Muskun kata 一起我們》，從當初2000年驚豔金曲獎的《獵人》，到沉澱心情回到部落的母語情懷，訴說對於父親的思念，令人感受到他的音樂誠意。2008年首張個人泰雅族語同名專輯《依拜維吉》，獲得第十九屆金曲獎最佳原住民歌手獎與最佳原住民專輯獎，2022年出身於宜蘭大同鄉四季部落的依拜．維吉再發行《四季的四季》，結合攝影大師阮義忠《四季攝影集》，不論是混搭節奏藍調、放客曲風的〈1234〉，或是R&amp;B抒情風格的〈爸爸說〉，都是獨一無二的精彩樂音。泰雅族歌手曾宇辰的《新心 pkgiqas》以自己的生命故事為主體進行創作，特別是〈kinbetunux 美麗〉這首歌，身為唇顎裂患者寫出自己臉上疤痕的歌曲，深刻而感人。她於2017年與I-Want星勢力團員以〈擁抱世界擁抱你〉奪得臺北世大運主題曲徵選冠軍，2019年與歌手贊薇共同創作〈kinqoyat 平安豬〉，獲得臺灣原創流行音樂大獎亞軍，在族語創作路上可以看她這幾年來的努力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;爵士演奏專輯方面，洪信傑《The One》、楊曉恩《For Papa Shawna Yang Quartet Live at Yilan》與魯千千、里奇・古瓷《繫connected》都是相當精緻的出版。</p>
