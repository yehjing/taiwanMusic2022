<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北流與教育部國民及學前教育署於2021年合作，推出第一期流行音樂培育課程，深受教師的好評。2022年再推出第二期「種子教師培育課程」，於4月23日正式展開。除了全國性3場線上教學與線下實體課程，更有二天的「實戰創作營」，透過老師學員們的實際參與一首歌曲從創作到成品，以實際的體驗化為日後教學的動力，也呼應了108課綱的多元內容。高流從2020年開始辦理「達人導聆」講座，2022年接續從2-11月進行整年度的高流系：「超營養學分VOL.3－達人導聆」，邀請到StreetVoice街聲頻道陳弘樹總監、袁永興、9m88、YELLOW黃宣等，還有魏廣皓的靈魂搖擺；另一系列結合聲音與影像的「樂影共振」，內容豐富，有漫威英雄的音樂宇宙，遊戲配樂、音樂劇等，甚至有關演唱會現場的燈光與音響等，都納入今年講座的重點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在人才培訓方面，各地政府亦會針對在地特色進行流行音樂人的培育，例如臺東縣政府「南島流行音樂中心評估案」、新竹市文化局「111年新竹市『風livehouse』流行音樂人才培育計畫」、桃園市政府客家事務局「2022客家流行音樂&mdash;人才培育暨音樂比賽」、雲林縣政府「雲林縣流行音樂人才培訓計畫」等，顯示流行音樂較往年深受各地政府的重視。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市文化局以「資深音樂人口述歷史影音紀錄」計畫為基礎，舉辦了「臺灣流行音樂備忘錄－《新台語歌繼續向前走》紀錄片放映及座談」，與「臺灣流行音樂備忘錄－《百萬銷售水很深》紀錄片放映及座談」，由導演侯季然拍攝微電影，並在12月規劃6場系列推廣活動，除了辦理2場座談會，在大安森林公園規劃4場露天電影播放活動，與市民共享。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在展覽方面，臺灣近年來因為北流與高流，甚至地方政府都積極規劃，使得流行音樂不論特定音樂人或是針對流行音樂中某個特質去做策展，例如高流「POP是流行！更是靈感爆發的聲音！POP! POP! POP! 流行音樂互動展！」3月1日起在常設展區；北流於2022年11月12日至2023年2月底於文化館展出「張雨生特展 FLY ME TO THE MOON &amp; BACK」，以紀念這位短促生命的閃耀音樂人之逝世25週年；4月26日過世的長青歌手文夏，享耆壽94歲，11月25日至12月10日於北流文化館大廳展出「文夏唱遊人生特展」，回顧他燦爛的一生。甚至臺東縣政府鐵花村附近的臺東縣原住民文化創意產業聚落（TTICC）的Lima展區，於9月19日至11月19日舉辦「風起原舞系列活動&mdash;原住民黑膠音樂展演活動」，透過唱片的展示，讓參觀者更了解原住民的音樂。</p>
