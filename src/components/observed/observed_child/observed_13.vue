<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;展演活動部分，大型演唱會指約3,000人以上的會場，包括臺北小巨蛋、臺北流行音樂中心、臺北國際會議中心、桃園國際棒球場、臺中洲際小巨蛋、高雄巨蛋和高流海音館，本年鑑收錄共有代表性的62場。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北部音樂活動以臺北小巨蛋為重要指標，1月1日由校園歌手施孝榮帶領了15位歌手開啟新年新氣象的「民歌高峰會2022台北小巨蛋」，已經改變了視校園歌曲為「音樂懷舊式」的表演模式，現場如同一般的演唱會特重視覺效果，成功轉型。現場將近9成的觀眾，票房收入約2484萬，更促使「2022夏日民歌嘉年華」，於8月14、28日在高雄巨蛋與臺北小巨蛋分別舉辦。從1995年開始的「民歌20演唱會」，定期的歌手與歌迷的音樂相聚，仍是一定的票房保證。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這種集多位歌手的演唱會就如同集氣一般，都能有不錯的賣座，例如臺灣電視公司從2010年開始的「超級巨星紅白藝能大賞」，今年為慶祝60週年臺慶，於1月8日在臺北小巨蛋盛大錄製「2022超級巨星紅白藝能大賞」。此外亦有其他「異類結合」，例如1月22日「全明星運動會巨蛋熱血演唱會」、1月28日「凹嗚狼人殺總冠軍臺北小巨蛋演唱會」，讓實競比賽與五堅情、瘦子E.SO、羅志祥歌手結合，音樂與體育相得益彰。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而重量級歌手或是樂團更是一個接一個登上小巨蛋，更充分顯示這個場地如同重量級歌手的人氣試金石，重要性不可言喻。以疫情5月嚴峻為分界，在這之前，例如魚丁糸相隔5年，於2月19、20日舉行「池堂影夜」演唱會；動力火車3月5日以2021年所發行專輯之名「都是因為愛」舉行世界巡迴演唱會－臺北旗艦場；張惠妹4月的12場「aMEI ASMR世界巡迴演唱會」，創下紀錄，不僅是華語歌手連續場次之最，現場硬體規格造價非常高，讓歌迷親臨聲響的享宴，為上半年畫下華麗的休止符。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7月疫情稍微趨緩，偶像歌手、饒舌歌手、天后唱將與搖滾樂團等紛紛來襲，讓歌迷目不暇給：睽違6年，7月9日「羅志祥」演唱會；7月16-17日的A-Lin 2022演唱會「A-Link with Passengers」；7月30日陶晶瑩「MOMAN」臺北小巨蛋演唱會；8月6、7日五堅情「Moon Landing月面著陸」臺北小巨蛋演唱會；8月12-14日瘦子「E.SO 2022地囚人臺北小巨蛋演唱會」；8月20-21日八三夭「顛倒世界」巡迴演唱會臺北首站；9月3日徐若瑄「VIVILAND」巡迴演唱會；9月10-11日韋禮安「如果可以，我想和你明天再見」小巨蛋演唱會；9月24-25日「伍佰&amp; China Blue Rock Star演唱會－臺北站」；10月1日彭佳慧2022「怎樣」巡迴演唱會；10月8日劉若英「飛行日2022巡迴演唱會」；10月21-23日周興哲「Odyssey～旅程臺北小巨蛋演唱會」；11月12日「萬芳30你的30，____你今天有沒有來」；11月19-20日周湯豪「REALIVE臺北小巨蛋演唱會」；12月3日盧廣仲「勵志的早晨勵志的夜晚」14週年臺北小巨蛋演唱會；12月10日臺語紅星洪榮宏「2022阿宏的心聲」臺北小巨蛋演唱會；12月24、25日安溥兩天的「2022 to ebb潮水箴言演唱會」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2022年最後一天，蔡依林以陪伴跨年的暖心，舉行「Ugly Beauty」FINALE in TAIPEI臺北最終場，從12月31日唱到2023年1月1、2、6、7、8日，6.6萬張票6分鐘完售，舞臺、服裝造型與舞蹈等華麗炫目，為2022年標下完美的句點。唯一能與之分庭抗禮的是，在桃園國際棒球場舉行的五月天「諾亞方舟十週年進化復刻限定版演唱會」，從平安前夜的12月23、24、25、30、31，一直唱到2023年1月1、7與8日。在年終之時，粉絲的社交距離已經被拋在腦後，民眾以歡欣鼓舞的心情迎接新的一年！</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，在臺北流行音樂中心有兩場極具深度的演唱會，12月17日齊豫、潘越雲「三個女人的壯闊人生&mdash;三毛齊豫潘越雲回聲演唱會」，繼4年前「三個女人的壯闊人生」之「回聲」在小巨蛋演唱會的主軸再做擴展，此次以18人編制的弦樂團，加上溫金龍的二胡以及太古踏舞團舞者，多元詮釋30首歌曲，令聽眾陷入無限的音樂回憶中。另一場為12月30日，「三大吟遊詩人音樂會&mdash;胡德夫、陳永淘、陳明章」，以「唱北投、唱關西、唱臺東。唱我們的地、唱我們的歌&hellip;&hellip;」為訴求，原住民、客語與臺語的吟唱大師們，結合土地的聲音與樂器的鏗鏘聲，緩緩道出悠揚的旋律。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北國際會議中心10月29日的Leo王「音樂戰艦Leo王」，特別與新銳藝術家張騰遠合作，以末日考古為題，展開超現實怪誕音樂戰艦航旅程；11月12日郭靜「《So Lucky！》2022演唱會」，15年來第一場大型演唱會，合體競技啦啦隊Monster，場面熱鬧非凡；12月24、25日，蕭煌奇「《朋友你現在好嗎？》2022出道20年演唱會」，36首歌，邀請視障者樂團全方位，與年少時一起組團玩音樂的好友接力演唱，念舊珍惜友誼，首首溫馨。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄流行音樂中心作為南方重要音樂基地，擁有容納10,000名觀眾的高雄巨蛋、5,000名觀眾的高雄流行音樂中心海音館、1,200位觀眾的LIVEWAREHOUSE等，音樂空間上中下游完備，為南臺灣打造全方位的流行音樂空間，南霸天的角色名符其實。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的高雄巨蛋星光閃閃，例如7月23日魏如萱「《HAVE A NICE：DAY》巡迴演唱會」，邀林宥嘉為嘉賓，引發轟動；詹雅雯選在腦部手術前，勇敢地於8月13日舉行「2022詹雅雯高雄巨蛋大跨越演唱會」，還特別邀請弱勢族群、貧童和獨居老人到場同樂；8月20日謝金燕「TURN口罩」演唱會，承繼2018跨年演唱會上她首次演唱〈TURN口罩〉新歌，標榜「戴上口罩自我防護而得到安全感」，2022年第三次站上高雄巨蛋，另有一番氣象；「田馥甄一一巡迴演唱會」雖因重感冒，演唱會延期又改期，最終於9月16、17日舉行，其餘取消，但是田馥甄非常敬業，值得讚許；10月22日，5566「2056二十週年演唱會」，為孫協志、許孟哲、王仁甫3人以老妝姿態模擬真正2056年演唱會的情境為發想，贏得歌迷熱情的掌聲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再者，高流海音館更是歌手挑戰高雄巨蛋的跳板，館方更以「大場帶小場、小場挑戰中場」的開放心態，歡迎音樂團隊和表演者來高雄推出各式節目。所以2022年下半年高流海音館使用率達62.5%，例如7月30日集林慧萍、黃妃、龍千玉、蔡小虎、王彩樺、曾心梅、邵大倫、張秀卿歌星的「真愛秀・藍寶石大歌廳」；9月17日陳珊妮「調教X教條」與11月28日周蕙「漫步月光下」等，都相當具有代表性，可以預計2023年上半年大型演唱會將更活絡，更能振興產業復甦動能。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂節/祭，聚焦於多為戶外舉辦、強調某種理念、非單場的音樂活動，尤其是結合臺灣各地文化特色的音樂節/祭，近幾年來蔚為風潮，本年鑑收錄共有代表性的19個音樂節，25個音樂祭。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年對於音樂祭/節活動而言，好像乘坐雲霄飛車一般，上下起伏牽動樂迷的心情，既精彩又刺激。「新北市貢寮國際海洋音樂祭」從2020-2022年皆因為疫情持續而接連停辦，心態過於保守，但是其他的音樂節/祭抱持與疫情共存的態度，在遵守防疫規範前提下，樂迷們得以重回音樂節/祭滿檔的日子。從1、2月的新北市「爛泥發芽」、臺中鰲峰山運動公園「浮現祭」，開啟了2022年豐富且精彩的音樂活動後，3月邁入第十三屆的「大港開唱」，更是港都最具代表性的音樂盛宴之一，邀請年輕一代演出者及前輩歌手，例如楊烈、舒米恩、滅火器、大支、炎亞綸、盧廣仲、美秀樂團與春麵樂隊等共百組，兩天熱烈開唱，在高雄駁二特區碰撞出世代間的音樂火花；4月分別於墾丁及屏東舉辦的「臺灣祭」與「屏東三大日音樂節」，吸引了來自各地的樂迷南下，享受國境之南的熱情陽光與音樂。9月24、25日於臺南漁光島舉行「春浪音樂節」，已邁入第17年，年年有新構想，今年結合近來興起的露營方案，與居民結合，創造極大的經濟效益。9月底至10月的「桃園鐵玫瑰音樂節」於2021年完工的桃園陽光劇場舉行，不論是「前夜祭」、「魔性搖擺」、「狂熱躍動」，壞特?te、BOXING、P!SCO、韋禮安等陪歌迷熱情度過。秋天來臨，爵士音樂隨著秋日微風放鬆搖擺，10月分別有「搖滾臺中」、「臺北爵士音樂節」，15年來都是這兩大城市的音樂招牌，形塑都會獨特氛圍，成功創立城市音樂品牌。10月29、30日的「Takao Rock打狗祭」，睽違4年再次上場，結合高流海音館、海風廣場、音浪堤岸、LIVE WAREHOUSE等四大場域。2022年進入尾聲，也迎來音樂節/祭完美的Ending，除了每年都邀請海內外音樂人與產業人士，與樂迷齊聚臺南，也積極促成音樂人與國際產業鏈結合的「貴人音樂節」，還有全臺最大的戶外音樂祭「漂遊者森林音樂祭」，在臺中麗寶樂園東區落羽松林萬坪草原架設巨型雙舞臺搭配、120米高的摩天輪，打造出情境式大型野區戶外活動音樂祭。隨著疫情趨緩，做好防疫政策之下，許多停辦的音樂節/祭逐漸恢復成疫情之前的榮景。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了本島有豐富的音樂節/祭之外，離島各政府推動音樂觀光也不遺餘力，7月有金門「夏日音樂祭」、8月有澎湖「追風音樂節」，除了超強卡司輪番演唱，加上夏日風情市集，結合在地的海洋、音樂、藝術，成為最受矚目的離島夏日音樂時光。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂空間的演出，特指小型、以獨立音樂為主的音樂演出，以臺北市文化局流行音樂資料庫之統計全臺約有63個，2022年於這些音樂空間演出之活動以統計自活動月曆共超過1,900場，有的音樂空間更舉辦超過100場之活動，可以看到這種雖小卻是孕育臺灣流行音樂能量的演出場地，值得繼續觀察，例如China Pa中國父有233場、河岸留言音樂藝文咖啡（臺北公館）有217場、女巫店有133場，臺東縣的鐵花村音樂聚落亦有136場，民間音樂生命力等不及疫情散去，已經熱烈開唱了！</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在爵士音樂演出結合展覽方面，兩廳院夏日爵士20週年，由策展人楊曉恩規劃為期一個月的「不只是爵士」，於國家音樂廳一樓穿廊展出，以臺灣自身視角探討爵士樂，從「戰後台灣」、「輝煌的大飯店時期」、「台灣本土爵士樂唱片」與三支紀錄短片展示臺灣二十年來的爵士樂發展，並由臺北爵士大樂隊於現場，以動態展的概念與高齡八十餘歲的前輩薩克斯風手周高俊共同演出，帶領觀眾回到輝煌的大飯店時期。此外，古典音樂重鎮的臺中歌劇院於年底推出一連兩天的「超復古派對」，可見得爵士樂節目在民眾心中的份量越趨受歡迎。</p>
