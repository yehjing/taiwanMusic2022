<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧去年（2021年），藝文紓困4.0為文化部獲行政院同意編列特別預算45.49億元，對藝文界進行補助以提振被影響甚鉅的流行音樂界。曾獲藝文紓困1.0及2.0補助的自然人，採取主動審查合於規定者，並與其他部會勾稽排除重複領取等因素後，3萬元直接入帳，嘉惠音樂工作者，政策如乾旱後的春雨，深受業界肯定；而文化部更加碼「積極性藝文紓困」，補助展演活動，使「藝文人才不流失、創作能量不中斷、提升數位運用及傳播」，讓藝文界可以撐過疫情之下的考驗。但2022年疫情再起，表演藝術產業已歷經兩年多的波折停演，且又面臨與先前防疫管制不同的考驗，包括演出人員確診居家隔離，以致演出需要延期或被迫取消的窘境。因此，文化部推出「受疫情影響表演藝術事業營運補助」，補助項目包含國內表演藝術節目已支出的演出製作費、行銷費、場地及設備租借、排練費等，補助額度為最高250萬元。此外，行政院也推出紓困振興特別條例及特別預算，實行期間延長至112年6月30日。為了協助受影響的民眾及企業，政府推動了以下策略（此針對藝文產業論述）：一、幫助藝文產業活動營運補助；二、提供演藝團體紓困貸款利息補貼；三、針對承租國有非公用不動產、國有公用及各部會所屬國營事業經營不動產的個人或企業採取減收租金等措施，支持藝文團體邁向正常模式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了上述的疫情補助之外，文化部所屬的影視及流行音樂產業局按往例進行了諸多的補助案，例如流行音樂製作發行、流行音樂人才培訓、流行音樂產業行銷推廣等；客家委員會「第一屆全球客家流行音樂大賽」與桃園市政府客家事務局「2022客家流行音樂節&mdash;客家流行音樂比賽」，亦有針對客語流行歌曲比賽的文化政策，讓更多的客家創作歌手得以透過此管道，獲得培植。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部主辦的金曲獎、金音獎、臺灣原創流行音樂大獎為流行音樂的三大年度盛事，2022年依序舉辦，給予流行音樂界極大的鼓勵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第三十三屆金曲獎共有20,721件，其中數位高達1103件，較2021多出176件。頒獎典禮不似2020與2021年延後舉辦，7月2日於高雄巨蛋舉行，這是睽違17年再度於港都舉辦，對於平衡南北有決定性的意義。珂拉琪拿下最佳新人獎，蔡健雅以專輯《DEPART》奪得4項大獎，成為最大贏家，這個到手的第四座最佳華語女歌手獎，也創下金曲華語歌后的紀錄。最佳華語男歌手獎則是由中國搖滾歌手崔健，以專輯《飛狗》拿下，頗具意義。客家創作歌手黃連煜以《滅人山》專輯入圍年度歌曲獎、最佳年度專輯獎、最佳客語專輯獎、最佳客語歌手獎、最佳專輯製作人獎、最佳編曲人獎以及最佳裝幀設計獎共7項大獎，再登個人音樂紀錄的高峰。特別貢獻獎為邱晨與陳復明共同獲得，前者為丘丘合唱團團長，雖已於今年3月24日逝世，但是在1980年代丘丘合唱團〈就在今夜〉所颳起的搖滾旋風，與他後續受鄒族青年湯英伸事件所激發的《特富野》專輯，在臺灣流行音樂史上有絕對性的歷史性地位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第十三屆金音獎第一次全部以線上報名方式進行，共計3,725件作品參賽，創下歷年來最高件數，包括835張專輯/EP、49件現場演出影片，顯見數位時代在疫情之下，毋庸置疑已成潮流。頒獎典禮11月5日於臺北流行音樂中心舉行，共有111件作品入圍，角逐21個獎項，其中朱約信（豬頭皮）的《防疫清冰箱》專輯，呼應這3年來的疫情，相當符合時事；評審團獎頒給珂拉琪的首張專輯《Memento・MORI》，繼金曲獎後又獲獎，讓人眼睛為之一亮；特別貢獻獎頒給阿通伯樂器行創辦人謝明通，以表彰對其經營三十餘年歷史的阿通伯樂器行之肯定。受獎人雖然已於2006年過世，但是他引進許多國外器材，讓臺灣跟上世界的腳步，凸顯其歷史地位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由文化部、原住民族委員會、客家委員會共同主辦的臺灣原創流行音樂大獎，今年邁入19年，共計240件報名，包含河洛語組146件、客語組43件、原住民族語組51件，各組首獎為30萬元，不僅獎金相當高，更會製作得獎者的MV與協助其行銷，入圍者三年內亦可列為文化部影視及流行音樂產業局「本土語言流行音樂專輯製作發行補助」之優先補助對象，上述誘因可說是政府主辦獎項中最為優渥。對於母語日漸不被年輕人所使用，透過歌曲的創作以利延續母語，都能吸引到對於母語創作有高度興趣的音樂人參與，尤其是原住民語，這次首獎由漂流出口的阿美族歌手布妲菈・碧海拿以〈Maracecay合而為一〉拿下。8月27日的決賽暨頒獎典禮於新北市的Zepp New Taipei，選擇在2020年才成立、可容納2245人的Live House音樂空間，顯示政府單位也跟上了時代腳步，具有新思維。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於政府主導的獎項，民間的流行音樂獎項基本上是以收聽次數來衡量，例如世界超過4.56億名聽眾的Spotify於12月揭曉2022年度回顧回顧排行榜，韋禮安的〈如果可以〉成為今年臺灣最多串流收聽次數歌曲冠軍；周杰倫睽違6年、備受期待的全新專輯《最偉大的作品》推出不到半年，就進入臺灣最多串流收聽次數專輯，更連續第四年問鼎臺灣最多串流收聽次數歌手。</p>
<h3>柒、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「2022-2026臺灣娛樂暨媒體業展望」（PwC Global Entertainment &amp; Media Outlook 2022-2026）報告中指出，2021年全球E&amp;M產業則呈現強勁成長。這樣的上行趨勢，於2022、乃至往後的幾年亦將延續。與全球展望相仿，臺灣E&amp;M產業的前景雖充滿挑戰，但整體尚屬樂觀。疫情過後，臺灣成長動能可望回溫；尤其閱聽眾的數位使用行為益發明顯，在驅動E&amp;M產業面貌變遷的同時，也將成為臺灣E&amp;M產業成長的助力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年雖有疫情於5月大爆發，但是許多歌手仍是挑戰北高兩場次的演唱會，例如動力火車、羅志祥與瘦子E.SO等，亦有如張惠妹、蔡依林與五月天，同場地以多場次去經營，皆成功吸引爆滿歌迷。在疫情有可能於2023年被視為流感化，流行音樂於各方面的發展有機會完全回歸正常的運作，這意味著，文化部將不再有針對疫情的特殊補助。綜觀流行音樂於2022年的各項表現，皆已讓音樂之火於困境中仍繼續延續，不僅沒有因而熄滅，反而有更多驚奇的嘗試，例如5月20日雅維茉芮「雅維的夢」全球線上演唱會，結合泰雅服裝時尚走秀，讓音樂與服裝的現代演藝展現出令人驚豔的文化融合；大象體操在7月16日挑戰舉辦連續12小時、串連多國平臺、跨越時區的雲端世界巡迴「12小時的夢」；珂拉琪視「拾回母語」為一個自我認同的方式，他們的〈這該死的拘執佮愛〉、〈葬予規路火烌猶在〉與〈萬千花蕊慈母悲哀〉等歌，把臺語歌帶到一個嶄新的境界，一個年輕人也聽得懂的音樂。上述三個列舉，或許是實驗，或許是未來趨勢，2023年應該都可以給我們一個明確的答案。</p>
