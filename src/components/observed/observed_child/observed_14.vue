<p><strong>一、異國風情與在地人文的交織</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2022年的幾個大型的跨國或跨文化節慶中，亦可看見跨界音樂活躍的表現，例如：由屏東政府主辦的半島歌謠祭為每年度重要的文化推廣活動之一，發揮屏東本身就富含多元族群的特徵，結合半島歌謠、在地人文、世界音樂及自然地景等。該活動以「我的半島生活PATTERN」為主題，邀請跨域藝術家合作，包括：知名設計師Daniel Wong、跨界樂團「同根生」，及來自不同背景的跨國樂手、歌手（夏川里美、范逸臣）等加入，在10月12日至10月16日為期5天的活動中，藝術家們齊聚恆春的北門廣場，以百年古城為環境劇場，演出別開生面的「民謠時尚秀」，除了歌、樂、舞之外，本活動的另一大特色是服裝與視覺造型和民謠之間的跨領域融合。而在歌謠部分，更體現了來自本土（如：〈雨夜花〉、〈四季春〉等）與日本沖繩文化的歌謠祭曲風，展現出濃濃的跨文化世界風格。此外，超過五十個品牌的集體跨域設計，舉辦了霓虹市集，當地民眾的熱情參與，吸引來自世界各地的藝術同好與觀光客，同享南島生活的特有異國風情。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由文化部影視及流行音樂產業局所舉辦的「世界音樂節」，自2016年至2022年已舉辦第六年（2020年因疫情停辦），今年2022年10月14日至16日位於大佳河濱公園舉行，總共邀請了23組國內外樂團，舉辦一場媒合展演、四場國際講座、兩場樂舞工作坊，以及異國市集和街頭藝術展演等。多年來始終以豐富的文化體驗音樂、舞蹈、工作坊，來自臺灣與國際各地的民俗或世界音樂、藝術家們，而漸漸成為亞太地區薈萃音樂家們的重要盛會，2022年為期三天的藝術節，涵括民族樂舞、爵士樂、打擊樂舞、原住民、電音及鼓樂體驗營、瑜伽、多場工作坊和創藝市集等，為疫情解封後帶來煥然一新的世界音樂能量。<sup>4</sup></p>
<p><strong>二、走向國際的臺灣音樂文化</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在國際交流的部分，2022年亦有跨界音樂團隊出國，將臺灣的特色以音樂傳播出去。來自宜蘭以跨界音樂的製作和演唱見稱，並於2022年8月20日「第一屆全球客家流行音樂大賽」獲得創作組首獎的同根生，應墨西哥塞萬提斯藝術節（Festival International Convantino）之邀於10月22日進行交流演出，該團以臺灣民間的鬼怪傳奇故事為發想，創作《邊緣轉生術》專輯，突破音樂團隊展演的慣性思維，將歌、樂、舞結合，以另類方式呈現臺灣特有的音樂文化，並大受墨西哥民眾青睞，獲得臺上臺下其樂融融的熱烈互動，並獲得墨西哥當地媒體的廣大報導。爾後，同根生又於10月25日巡迴至智利，在La Pintana大劇院、智利燒燙傷基金會等地進行交流演出。將來自臺灣的音樂魅力，充分渲染於所到之處，使參與觀眾深深為臺灣的藝術能量有所共鳴。</p>
<p><strong>三、新興團隊的漸趨成熟</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;於2013年成立的「三個人」樂團，在九年多的發展過程中，已逐漸摸索出該團的特色，建立起獨樹一幟的形象，以「人」為創演核心的三個人，因三位好朋友的志同道合，自然而然的成團，由於編制的特殊性（笛、古箏及中阮），該團往往必須自創音樂才能傳達他們想要表現或抒發的意涵，正因如此，也間接造就了團員們的創作、編曲能力，共享的理念也成為該團能確保自我特色的重要秘訣。無需一味地去討好觀眾或迎合市場，亦不會因利益而隨意接商演，該團音樂總監任重表示：「我們是因夢想而結合，我們認為不論用何種素材或媒介，以人為主的方式、以人為主體的美學，正是我們才是我們熱情的所在，亦是我們至今能持續走下去的初衷」。<sup>5</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2015年創團至今的A Root同根生，是以「歌、樂、舞」合一的理念來發展的跨界音樂團隊，以生活化的表演方式，講述市井小民的日常生活，不做作、不矯情，「你越『Real』的時候，其實大家會更認同你！」<sup>6</sup>―同根生的藝術總監楊智博如此說道。創團以來，不論創演或行銷，總是親力親為的楊智博表示，小團營運的優點是可以輕鬆而自然地訴說自己的故事，分享自己的情感。不用刻意去強調他是國樂或哪一類音樂，而是以自然而然、親民生活化的表達方式，和周遭的朋友和現場的觀眾自然互動，樂團不需被貼標籤，來聽音樂、看表演的觀眾，因為「想看你的團隊的表演而來，而不是因為你被歸類在哪一類的音樂類別」。<sup>7</sup>此外，小型新興團隊，也因為靈活性和可塑性高，常可以緊密地與趨勢結合，即便在疫情嚴峻的時期，也可以改變策略，轉以線上教學、展演、配樂的形式，來繼續團隊的營運。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於成團相對年輕，成立於2018年的普琈門，團長劉佳瑩亦有如此的認同，她認為在當代做小型樂團，並不是為了商業利益，而是以夢想為初衷居多，小型團隊的好處是預算較低、活動性較高，在形式與內容上也比較不拘小節，無論在咖啡廳或休閒場合，很容易就能夠與觀眾面對面的接觸、交流。<sup>8</sup></p>
