<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;親子劇場、音樂會一向是推廣音樂教育的重要形式，2022年也不例外，除了7場講座、3場展覽之外，仍以展演為最大宗，本年度的親子跨界音樂製作，除了仍以兒童觀點出發的視角，去打造敘事內容外，另一特色在於結合在地風情或傳奇故事，透過文本的敘述與戲劇的演繹，讓親子音樂劇此類型的跨界音樂演出，不但深具趣味與啟發性，更體現了寓教於樂、關懷歷史與尋找自我認同的深遠命題。</p>
<p><strong>一、親子音樂會</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;來自南投縣仁愛鄉親愛村的「親愛愛樂」弦樂團，主要是一群偏鄉原民孩童跨出學校界線與城鄉差距，以體制外的方式自主學習古典音樂，並以他們自然純粹的聲音征服世界，站上國際舞臺，歌詠出純真無邪的天籟之聲。《Lawa的彩虹橋》由多位同學以原民神話故事為主軸，透過音樂的編織，劇情講述一位叫Lawa的女孩和祖母相依為命，在祖母生病後，進行了一場愛與勇氣的冒險，試圖用音樂來陳述故事的走向，串起動人的詩篇與戲劇性的畫面，也讓孩童能夠透過音樂直接的感受和想像。演出過程中，每位樂手皆著原民傳統服飾，演出期間並適時與臺下觀眾互動，以音樂傳達良善意念，建立正向品德、樂觀態度以面對人生，而音樂無國界的強大力量，也在他們的演奏中展露無遺。</p>
<p><strong>二、親子劇場</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團於5月正值桐花盛開的花季，推出應景的《桐花護衛隊》兒童音樂劇（原訂演出時間為2022/04/30後延至2022/10/22），將孩童於各式公園遊玩作為劇目之構思，發展成為兒童音樂劇。除了新創劇本外，音樂到燈光、服裝及插畫等設計，融入了視覺和聽覺的搭配，讓樂器聲響與演員角色互相搭配，呈現會走動、會說話的樂器，一方面增加了趣味性，二方面也突破了音樂家與演員的協演模式，無論對於演奏家或演員來說，都是一種新的挑戰。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;擁有龐大演出、教學與人才培育的朱宗慶打擊樂團，長期耕耘親子劇場，已有豐碩成果，「豆莢寶寶」已成為推廣音樂的重要代名詞之一。在今年，朱宗慶打擊樂團2團演出的2022豆莢寶寶兒童音樂會《擊樂星球》橫跨臺灣十個縣市，分別為：臺北、新北、基隆、桃園、新竹、臺中、彰化、嘉義、臺南、高雄等，為今年度巡迴最多縣市的跨界音樂演出。而豆莢寶寶兒童音樂會系列早在2014就已開始巡演，每年皆以不同的主題呈現，例如：「淘氣旅行箱」（2019年）、「BlingBling大冒險」（2020年）和「BeanBon果實敲敲樂」（2020年）等，讓兒童透過不同的劇本、音樂不斷體驗、學習。</p>
