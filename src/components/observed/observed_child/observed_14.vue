<p><strong>一、主題式的專業跨界製作</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在266場活動中，除了歷年來始終備受青睞的親子劇場／音樂會外，在2022亦出現一些以主題式特別企劃的跨界音樂會，且備受矚目，成為焦點。例如：6月在屏東藝術館劇場演出的新古典室內樂團之《阿嬤的歌本2.0》，就是一齣結合音樂、戲劇、舞蹈與文學的跨界製作。該團藝術總監陳欣宜以在地人文的關懷、民主價值的推廣、社會運動的促成及環保議題的倡議等概念出發，視音樂為主要元素，與其他素材跨界合作，在近年來的跨界音樂表現上可謂多有輸出、甚至大放異彩，該團更於2022年獲選為111年度國藝會TAIWAN TOP音樂類年度團隊。從鋼琴家至鋼琴教師至音樂室內樂到歌劇的領導者，乃至於跨界音樂製作、策展人，陳欣宜藝術總監分享她的跨界理念來自於對「臺灣價值」的追求與發揚，當她發現鋼琴甚至室內樂的單純音樂演奏模式，已不足以表達她想傳達的內容和情緒訴求時，其製作自然而然地朝向了跨領域或跨界製作的發展；歸咎其因，她認為跨界音樂的包容性更強，在處理他領域的意象時也更能夠容易地呈現。在《阿嬤的歌本2.0》中，除了有陳總監本身對其家中長輩的思念外，亦有來自該劇之原創編劇暨首演導演許逸聖在家中偶然發現在阿嬤家中遺留下來的「歌謠筆記本」，在濃濃的懷舊素材與希臘神話寓意下，此作品同時集結了跨領域藝術團隊，成就了一齣叫好又叫座的音樂舞臺劇。<sup>2</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，2022年的主題式音樂會，屬全新創的，另有來自桃園市國樂團的《千年渡・白蛇》和臺北市立國樂團的「吟詠性情．歌樂相得 詩詞曲歌唱跨界音樂會」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;繼2020年《不完美的愛》音樂會後，桃園市國樂團再次與唐美雲歌仔戲團於本年度8月假中壢藝術館音樂廳攜手合作新創《千年渡・白蛇》，透過本地指揮家林天吉的指揮，結合現代劇場的聲光舞美設計，對「法海」角色的全新塑造，和文本上的改編，此場結合傳統戲曲與國樂的跨界製作，觸動觀眾纖細的情緒與深度視、聽覺感受。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂學者施德玉應臺北市立國樂團團長陳鄭港邀請協助策展的「吟詠性情．歌樂相得：詩詞曲歌唱跨界音樂會」，原訂為5月演出，但因疫情延至10月7日於臺北市中山堂中正廳演出。本場音樂會以中央研究院院士曾永義之詩詞為主軸，在諸多作曲家的編曲或作曲合作下，產出曼妙而動聽的「歌樂」。此外，更在視覺上著以名書法家薛平南教授所提供的揮毫，在導演劉建幗、多媒設計王逸聖，以及臺大中文系教授洪淑苓導聆等各界專家的跨域操刀下，本場音樂會呈現詩、詞之跨界，劇種之跨界，亦包含有中西樂、戲曲（京劇、崑曲、歌仔戲、採茶等）不同風格的曲風跨界，帶領觀眾進入詩意、詞情的相互並茂，聽覺、視覺的相互輝映。</p>
<p><strong>二、大型跨界製作的推出</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有別於過去以中、小型製作為主的演出型態，2022年有數個大型製作的產出，例如：新古典室內樂團的《笠山之歌》、臺北市立國樂團的《蔥仔開花》、臺灣國樂團的《馬偕情書》、優人神鼓的《轉山》與朱宗慶打擊樂團的《俠飛春光》、《木蘭》和奔放樂集的布瑞頓歌劇《碧廬冤孽》等，而其中更有許多今年度的全新製作，由此可一窺2022年出現了表演藝術界許久未見的盛事榮景，畢竟大型製作所涉及的人力與資源皆相當龐大，若沒有一定程度的演出效益，實難以有效地執行團隊運作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;對於演奏家而言，演出大型的跨界製作常意味著較高的挑戰。例如，在《馬偕情書》中，這些傳統國樂的演奏者需身兼演員或舞者，演出音樂的同時，亦須考量面部表情、走位、演技等諸多戲劇要件；又如在《轉山》之中，表演者除了鼓藝的詮釋外，亦須考量與互動裝置的緊密配合，以呈現虛實交融的臨場感等。綜觀而言，大型跨界製作對於演奏家而言有更多、更大的跨界挑戰必須克服，譬如有更長的臺詞需背誦，更多的肢體動作需練習等，著實為深度與長度的考驗。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年裡，兩個公立國樂團―臺灣國樂團與臺北市立國樂團―不約而同以加拿大籍馬偕醫生為主題製作，或以女性主義為觀點出發，亦或是以殖民歷史為視野的音樂詮釋。臺北市立國樂團的《蔥仔開花》，嘗試以國樂團的編制來詮釋所謂臺語歌劇及臺語文學，以國樂來演繹歌劇或以臺語演唱宣敘調、詠嘆調等雖非首見，但如此全面、徹底地運用，並佐以具歷史意義的劇情，實屬罕見；臺灣國樂團的《馬偕情書》亦有異曲同工之妙，在這個似音樂劇場的演出中，樂團樂手除了演奏外，更兼具演員身分，且同時需參與戲劇和歌唱的演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;朱宗慶打擊樂團的《俠飛春光》從原本只是一首曲子開始發想，後來衍生成為一場音樂會，熟稔擊樂劇場製作模式的朱宗慶總監，提議邀請長期合作的李小平導演來做串連全場的協調，在李小平導演的引薦下找來施如芳教授擔任編劇，和四位跨國作曲家（洪千惠、櫻井弘二、楊祖垚、蔡昀恬）。為了配合劇情，本場音樂會的演奏家有諸多演奏上的挑戰，例如：為了詮釋獄中人急欲掙脫枷鎖和打破世俗藩籬渴望自由的心聲，所有演奏家必須戴著手拷演奏，並且如同行為藝術般，沉浸於整個環境氛圍中，搬演出聲響世界以外的肢體詮釋。本作品的另外一個特色，全劇以臺語演唱的《俠飛春光》，在於以既有的擊樂劇場形式，另加入一組小規模的合唱團，造成擊樂與聲樂的對話，以達到更飽滿而穩重的音樂性。值得一提的是，該製作融入男童歌聲扮演兒少時期的蔣渭水，透過打擊樂的媒介，純真童聲與合唱團的連結，觀眾得以追尋歷史的痕跡，了解上一個世紀，民族傳奇人物的奮鬥人生。<sup>3</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新古典室內樂團於衛武營國家藝術文化中心戲劇院所推出的「文學音樂劇場」《笠山之歌》，是該團繼《給NK的十行詩》後之另一大型製作，從聲景音樂劇場到文學音樂劇場，強調了不同領域的交融與薈萃。《笠山之歌》乃是以臺灣鄉土文學之父鍾理和，及農民書寫作家鍾鐵民的文選作為發想，以傳記式的史詩為藍本，透過道地客語文學的詮釋，及建築領域跨域背景的舞臺設計，完成此全新大型製作。「語言不是問題。」對於不是客家人的陳總監，卻積極推動客語文學，她認為文化之間的尊重來自於各族群彼此間的認識。而如此大型的跨域製作，更將客家之美推到另一層次的新美學境界。優人神鼓藉由當代先進科技的載體，此次與黑川互動媒體藝術共同創作，以5G科技異地共演的方式，在國家劇院演出《轉山》，除了優人神鼓既有的鼓藝與禪意之外，《轉山》更充分地體現了現代高科技技術的成熟度，現場觀眾猶如置身群山與天地之間，不斷輪轉與迴旋，感受到虛實融合之多次元空間感。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來的跨界音樂發展，某種程度而言，已「養出」一群願意、甚至樂意特別去欣賞跨界音樂的觀眾，基於這類觀眾所建構出的音樂市場，對於這些新創的大型製作往往能反映出高度的興趣，且一般而言，大型製作多半能獲得不錯的觀眾反饋。無論從素材或結構來看，2022年的大型跨界音樂製作已不侷限在不同樂種或藝術類別之間的「內跨」，甚至加進了非藝術屬性的跨界合作共製者，於創演過程中扮演重要的角色；對於新的題材和跨界方式，呈現出躍躍欲試、勇於挑戰的傾向。</p>
