<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著嚴重特殊傳染性肺炎（COVID-19）疫情的趨緩，我國政府自2022年中旬起，逐步鬆綁了諸多不利於現場演出的限制，<sup>1</sup>因而促進了跨界音樂的各類實體活動，復甦了沉寂已久的國際交流；除了過去既有的演出類型外，本年度亦出現了久違的大型製作與全新創作，這是音樂工作者和表演藝術團隊，在過去漫長的疫情陰霾下，韜光養晦持續創發與排練後大放異彩的成果。整體而言，2022年的跨界音樂活動要比前一年更加活躍，尤其進入下半年之後，頗有春回大地、萬物復甦之新氣象。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀2022年的跨界音樂發展，大致有六種面向，分別為：持續升溫的跨界趨勢、主題式的專業跨界製作、大型跨界製作的推出、親子劇場／音樂會、異國風情與在地人文的交織，以及新興團隊的漸趨成熟等。以下分別從本年度的社會背景與跨界音樂的實例、個案等方向加以論述。</p>
<p><strong>一、研究範圍界定</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域所收錄之「跨界音樂」範圍界定如下：第一，不同樂種之間的交越跨涉；第二，音樂與其他表演藝術（如舞蹈、戲劇、戲曲、馬戲、民俗技藝等）的結合；第三，音樂與他領域（如文學、繪畫、書法、電影、時尚等）的跨領域合作展演；第四，音樂搭配多媒體、新媒體等科技媒材的互動表現；第五，結合不同文化的跨文化音樂交融或與異國團隊的編創共製；第六，創演形式上的跨界交涉、變化（如擊樂劇場、音樂舞臺劇、兒童音樂劇、聲景音樂劇場、音樂馬戲劇場等）。本子領域2022年度收錄跨界音樂研究對象來源涵括：公立樂團、TAIWAN TOP演藝團隊、縣市政府傑出團隊、立案演藝團體、非立案演藝團體、推廣協會、學術教育單位等，各類型團體活動收錄跨界音樂相關之總數量為347。</p>
<p><strong>二、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年10月，國立傳統藝術中心舉辦的第三十三屆傳藝金曲獎，假臺灣戲曲中心大表演廳舉行。其中，出版類專輯獎項裡的「最佳跨界音樂專輯獎」，入圍者計有：李欣芸音樂創作有限公司的《白日放歌》和《拾光・電影故事》、巴洛克獨奏家樂團的《印象・臺灣》、財團法人臺灣基督長老教會豐山教會的《東西顰笑2－李志純近代東西交會室內作品語錄二》，以及三個人的《嬗》，最終，以三個人的《嬗》獲得最佳跨界音樂專輯獎。由上述活躍的參賽現象可窺見：跨界音樂正朝向新興發展的榮景邁進，越來越多的音樂人參與跨域製作，且不少成果受到產、官、學各界的矚目與好評。</p>
<p><strong>三、資料來源</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域主要資料來源為：全國藝文活動資訊系統網，與各縣市之演出場館，例如：衛武營國家藝術文化中心、臺北國家兩廳院、臺中國家歌劇院、桃園市政府藝文設施管理中心、新竹市文化局和苗北藝文中心等；另也涵蓋跨界團隊之相關網站或平臺。</p>
<p><strong>四、研究方法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域之研究方法，採取田野調查法之訪談與觀察兩種作法，以及個案研究和內容分析法等方式加以執行。</p>
<p><strong>（一）訪談法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域之訪談對象以跨界音樂團隊的領導人或音樂家為主，分別為：劉佳瑩（普琈門）、楊智博（同根生）、任重（三個人）和陳欣宜（新古典室內樂團）。採實體訪談的方式進行，輸出為逐字稿後，加以整理其摘要，並於專文撰寫時適時引用和參考。</p>
<p><strong>（二）觀察法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2022音樂年鑑跨界音樂與其他篇的活動紀錄表專欄中，12個收錄的節目皆有蹲點進行現場觀察，提升活動紀錄之信度及效度。</p>
<p><strong>（三）個案研究</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;針對2022年度較活躍的跨界音樂團隊或音樂家進行研究，例如：入圍第三十三屆傳藝金曲獎最佳跨界音樂專輯獎之團隊、榮獲2022國家文化藝術基金會TAIWAN TOP演藝團隊，以及參與國內外大型藝術節或藝術季之演藝團隊等，從中以個案研究方式探討其在2022年之發展現況和作品的推出情形等。</p>
