<h3>捌、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀2022年整年度的跨界音樂創演推廣與傳習等各類活動，可歸納出：持續升溫的跨界趨勢、主題式的專業跨界製作、大型跨界製作、新興團隊的漸趨成熟、親子劇場／音樂會，以及異國風情與在地人文的交織等面向等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以數量觀之，跨界趨勢有明顯的持續升溫現象，不少過去沒有或極少製作跨界節目的團隊，亦紛紛開啟跨界的嘗試。在眾多跨界演出中，2022年的跨界製作又以主題式的類型居多，其次才是廣受民眾喜愛的親子劇場或親子音樂會，及異國風情等，可見跨界音樂已逐漸邁入較成熟的階段，除了表面的異質素材，和視、聽、覺上的刺激與嚐鮮之外，具有意識型態、甚至思想內涵的主題式跨界製作，更能吸引目光。而在本年度的主題式跨界音樂節目中，出現了不少新興的表演模式，衍生出與眾不同的觀演美學，激發出前衛的實驗精神。此外，逐漸走出疫情陰霾的2022年，在經濟回溫與消費市場擴大等各種因素影響下，較耗費金錢、時間與人力的大型跨界音樂製作陸續有所產出，並可推估將持續在2023年佔有一席之地。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從表演型態的不斷推陳出新，大型製作（不少獲得官方補助或企業贊助）在全國各地、各類演出場域上演的情形可知，相較於其他類型的音樂種類，跨界音樂與表演的場域有更加密切的連結。換言之，製作一齣跨界音樂節目，所需考慮的因素，將不再只是來自供給端（製作人、作曲家、編劇家、導演等）的音樂藝術性或敘事性，或接收端（觀者、聽者等受眾）的認同與定位而已，同時也需務實地考量場域的配合度和演出上的可行性，從傳統的音樂廳、室內舞臺到戶外的公園、兒童樂園等環境劇場，抑或是各類大型演出場域，如：小巨蛋、大巨蛋或體育館等，皆會產生不同的挑戰與連動關係。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;透過產、官、學各界的密切交流與同理合作方能促成藝術健全發展，跨界音樂也不例外，近一、二十年來，跨界音樂的崛起、發展至今，一路走來跌跌撞撞、挑戰不斷，從最初的沒沒無聞，甚至不被認同，至今天可看見頗有百花齊放之勢，其轉變可從幾個面向來一窺究竟。首先，在產業界，民間個人或團隊以靈活而勇於嘗試的方式，打開跨界之門，之後，公立團隊亦加入此行列，推波了跨界的創演新世界；其次，學界開始關注此領域，無論在辦學或研究上，皆有所建樹與產出，與跨界相關的系所、研討會或研究議題，紛紛有所輸出。進而深化了跨界音樂發展的質地與潛能，拓廣了其宏觀視野與在地連結；再者，公部門與政府相關單位，亦以行動呼應了這波趨勢―許多以跨域為議題的補助、競賽或計畫，在近期頒訂提供跨界音樂實踐者、參與者或研究者實質的支持。綜合上述可知，集結了產、官、學三方的「跨域合作」共識，使得跨界音樂在2022年的發展頗有士氣如虹的氣勢，在數量上明顯上升，在質感上亦更加精緻與成熟。然而，「跨界音樂」是否算是一種樂種或一種音樂的創演方式等核心議題，仍需各界從不同角度更客觀地去探討，唯有以穩固的共識為基石，方能促成跨界音樂的穩健發展。此外，作為一門專業的學科，跨界音樂需要更多具備斜槓（如：兼具創作、演奏、行政與推廣等）能力的多元音樂人才，故如何培育跨界音樂人才，則是當今需迫切面對的問題。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;值得一提的是，在新興的跨界團隊逐漸成熟、新創作品逐漸增多之際，智財權、著作權的歸屬與定位，也將成為實踐跨界音樂不得不面對的重要議題。畢竟，跨界製作的過程中常常涉及跨域共製或集體構作的情形；換言之，一首跨界作品或一齣跨界製作，並非個人而是多人共同完成的，其中各方參與的份量、時段與輸出的展現等，都成為作品在歸屬上釐清的難題。</p>
