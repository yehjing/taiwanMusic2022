<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北孔廟釋奠樂是由莊師本立（1924-2001）根據明代樂譜所譯，主要來自於李之藻的《頖宮禮樂疏》，這是在1968年（民57）至1970年（民59）間的「祭孔禮樂工作委員會」所修訂的，當時分年設立了禮儀研究組、服裝研究組、樂舞研究組、祭器研究組等四組，釋奠樂主要依據為明代的釋奠樂規制，採用1373（明代洪武六年）間定祀先師孔子廟的樂章。莊本立師對於釋奠樂的研古思考是：「集鏗鏘明亮和氣勢宏偉的周制鐘磬，古雅調和的宋明服裝，和平博大的明代樂舞，及莊嚴肅穆的清代鼓樂，融合了歷代祭孔樂舞的優點於一堂。」<a href="#_ftn2" name="_ftnref2">[2]</a>此一變革的釋奠樂，成為目前全臺各地孔廟的範本，影響最為深遠。今年的釋奠樂仍由臺北孔廟樂長孫瑞金帶領並訓練，學生並非音樂專業的科班生，因此是由基礎訓練而成，著實不易。樂生主要在丹墀的東西兩側，以及大成殿下丹墀前方，這些年都如此排班，陣容莊嚴，樂聲悠揚平和。</p>
<h3>參、桃園市祭孔典禮</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市孔廟今年的祭孔也是訂在9月28日早晨，前兩年因為疫情原因而縮小規模，今年算是回歸到正常辦理的狀態，但是所有參禮的人員仍須全程戴口罩，樂生擔任吹管樂器者不在此限。大成殿的釋奠典禮前仍是舉行崇聖祠的祭典，於上午六時開始，七時整開始舉行大成殿釋奠禮，正獻官由桃園市長鄭文燦擔任，分獻官分別由教育局長林明裕、文化局專委魏淑真、市議員林政賢、陳美梅、黃婉如及中華孔子後裔儒學促進會等代表擔任，糾儀官由民政局長陳靜航擔任。禮生除了桃園高中的學生外，另有許多國中小校長、主任、教師等組成，樂生有多種身分組成，學生有桃園高中、聯合大學、文化大學、中原大學、臺灣大學等，現任或退休的國中小學校長、主任、教師以及許多社會人士共同組成，在地方孔廟釋奠樂生的組織上也算相當特別，所使用的樂譜與臺北市孔廟相同，皆是明制釋奠樂。參禮者有許多國中國小現任或退休校長參與，也包括許多主任、老師們共襄盛舉，禮生、佾生、樂生及相關執事人員約260人參與，規模宏大。釋奠典禮的儀程總計有32項，今紀錄如表2。與臺北市孔廟相較，桃園市孔廟的釋奠禮儀節無瘗毛血、總統（代表）上香、恭讀總統祝文、全體行三鞠躬禮、奉祀官上香等5項儀節，由此也可看出臺北市孔廟的特殊地位。</p>
