<h3>玖、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從今年各地孔廟逐漸恢復釋奠典禮的情形來看，似乎有逐漸脫離疫情因素的影響，各地孔廟釋奠典禮的觀禮民眾也逐漸增多，佾生樂生多由學生擔任，也多能正常習舞習樂，在今年的典禮中多有良好展現。從音樂的角度觀之，多使用莊本立師的明代樂譜，釋奠典禮的服裝祭器等，也多是遵循1968年-1970年（民國57至59）間的「祭孔禮樂工作委員會」所修訂的，可見當時的修訂對日後的影響。音樂的演奏仍有許多規模略小的孔廟，無法完全達到八音雅樂器的使用，觀察下來，多有樂器不完備之問題，各縣市如能提撥經費加以補充相關樂器，對於提升釋奠典禮的嚴謹當更加莊嚴，亦是對於孔子崇敬孺慕情懷的體現。</p>

