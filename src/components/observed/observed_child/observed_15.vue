<h3>肆、新竹市祭孔典禮</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新竹市孔廟始建於1817年（清嘉慶二十二年），直到1824年（道光四年）完工，爾後幾經遷移重修，於2014年（民103）9月28日開始復辦釋奠典禮，成為新竹市重要的祭典活動之一。去年的釋奠典禮仍籠罩於疫情中，因而縮小規模，今年則開始恢復往昔的釋奠規模，9月28日釋奠禮的正獻官由新竹市代理市長陳章賢擔任，樂生由新竹市立三民國中國樂社學生擔任，佾生由新竹市立民富國小的舞蹈班擔任，禮生則由財團法人宏宗聖堂道學院眾道親擔任。釋奠的儀節為三獻禮，約莫可概分為29項，參見表4。樂生使用的樂譜也是莊本立師所譯的明代樂譜，樂器則是使用國樂器，並無完備的祭孔樂器編制，例如因為沒有傳統笙，而使用36簧笙，樂生人數不多，立於大成殿屋簷下，面向丹墀，佾舞則以六佾為數。典禮結束後，贈送餐禮者智慧筆、麵線（象徵智慧毛）、芹菜與蔥（代表勤學聰明）作紀念，整體釋奠活動仍屬莊嚴典雅。</p>
