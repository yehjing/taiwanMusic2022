<h3>貳、北部地區的釋奠禮</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年臺北孔廟的秋季祭孔仍如期在9月28日舉行，惟仍然受疫情的影響，主辦的臺北市政府民政局精簡了釋奠的執事人員，同時也取消各界陪祭官的邀請與觀禮人員，僅維持所有參與典禮者300人的限制，這也是符合疫情指揮中心「二級強化警戒措施」與臺北市府集會活動規範，室外活動須在300人以下的規定。為了滿足民眾與海外友人對釋奠禮的關注，臺北孔廟採取現場直播以饗大眾，去年的釋奠禮也是採取直播方式，此亦是疫情後新型態的釋奠觀禮。今年釋奠的樂生由重慶國中學生擔任，佾生由大龍國小學生擔任，饌生由開平餐飲學校學生擔任，釋奠樂樂長仍由孫瑞金老師擔任，所有禮生、樂生、佾生以及所有工作人員均全程戴口罩，惟樂生的吹管樂器不在此限。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北孔廟祭孔的釋奠典禮依序為：「鼓初嚴」、「鼓再嚴」、「鼓三嚴」，以上皆是敲擊大成門內兩側之晉鼓與鏞鐘；「執事者各司其事」，主要是禮生就位；「糾儀官就位」，按例多由民政局長官擔任，今年由副局長吳坤宏擔任；「陪祭官就位」、「分獻官就位」、「正獻官就位」，今年正獻官是由臺北市副市長黃珊珊擔任；「啟扉」、「瘞毛血」、「迎神」，此時樂奏《咸和之曲》；「行三鞠躬禮」、「進饌」，此時樂奏《咸和之曲》；「上香」，此時樂奏《寧和之曲》、「行三鞠躬禮」；「行初獻禮」、「行初分獻禮」，此時樂奏《寧和之曲》、「行三鞠躬禮」；「恭讀祝文」、「全體行三鞠躬禮」；「行亞獻禮」、「行亞分獻禮」，此時樂奏《安和之曲》、「行三鞠躬禮」；「行終獻禮」、「行終分獻禮」，此時樂奏《景和之曲》、「行三鞠躬禮」；「總統代表上香」，由內政部長徐國勇代表上香，此也是只有臺北孔廟才有的儀節，此時樂奏《寧和之曲》，「行三鞠躬禮」；「恭讀總統祝文」，此只有臺北孔廟才有的儀節；「全體行三鞠躬禮」、「奉祀官上香」，由奉祀官孔垂長上香，此時樂奏《寧和之曲》，「行三鞠躬禮」；「飲福受胙」、「撤饌」，此時樂奏《咸和之曲》；「送神」，此時樂奏《咸和之曲》；「捧祝帛詣燎所」、「望燎」，由正獻官望燎，此時樂奏《咸和之曲》；正獻官「復位」、「闔扉」、「撤班」、「禮成」，總計36項禮儀，樂生使用的樂章屬於明代的大成樂章，當年由莊本立老師譯訂。</p>
