<h3><strong>前言</strong></h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年是孔子誕辰2571週年，全臺各地尚未脫離疫情管控，因此，各地的釋奠禮，繁簡各異，主要還是為了配合疾管局的疫情規定。今年度未持續進行釋奠樂的田野蹲點，謹以此文作為全年度的觀察概說，因為釋奠樂每年分別在全臺的孔廟或是有奉祀孔子的宮廟，均會進行相關的釋奠儀式，尤其是前者多會舉辦大型祭孔典禮，正因為釋奠樂有常規的穩定性，因此，本年度就不再單獨進行田野蹲點記錄。釋奠典禮大多在每年的9月28日清晨舉行，少數奉祀孔子的宮廟會另擇吉日舉行，如果進行田野記錄，需要在同一天有多組人員前往，這也是釋奠樂田野的特色與不易之處，專文的觀察能適度地解決上述的問題，但也無法全面細緻的觀照，以下就今年度相關的釋奠樂進行敘述。</p>
<h3>壹、全臺主要孔子廟的釋奠</h3>
