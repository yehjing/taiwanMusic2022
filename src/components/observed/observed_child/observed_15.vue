<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年是孔子誕辰2572周年，三年來的疫情也逐漸緩和了一些，過去停辦祭孔或是限制民眾參與的祭孔釋奠儀式，在今年多有大幅度的改善，然參與釋奠禮的所有人員仍須戴上口罩，避免疫情的傳播，吹管樂器的樂生則免戴。全臺各地公部門管理的孔廟以及民間宮廟奉祀的孔子，今年大多有舉行釋奠禮或祭孔相關的活動，本年度並未進行蹲點的活動紀錄，且多數釋奠禮舉行的時間仍在9月28日早晨，並無太多人力能顧及全臺的活動，因此，以觀察專文的方式來進行評介，尚能管窺臺灣釋奠禮的特色，以下分述之。</p>
<h3>貳、臺北市祭孔典禮</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作為首都孔廟的臺北市孔子廟，仍然是全臺具指標性的孔廟，每年的釋奠禮均受到重要關注，也是觀察的重點。臺北孔廟在9月28日清晨五點先舉行崇聖祠的祭典，結束後所有與祭人員即轉至大成殿準備，六時釋奠典禮正式開始。今年「正獻官」是由臺北市政府祕書長陳志銘擔任，「奉祀官」是孔子第七十九代嫡長孫孔垂長先生擔任，孔廟捐地者後代辜弘毅、陳枝秀以及教育局長曾燦金、文獻館館長詹素貞、南非聯絡辦事處代表安德漢、臺北歐洲學校總校長尼克森、臺北美國學校高階教育者何德青、孔廟委員王湘瑾、臺北市大同區區長方越琦等人分別擔任「分獻官」。釋奠禮的禮生由大龍峒仕紳和明倫高中學生組成，樂生長期以來都由樂長孫瑞金老師訓練臺北市立重慶國民中學學生來擔任，佾生的佾舞則由蔡富存老師指導的大龍國小佾舞隊擔任，由此構成龐大的禮樂人員。釋奠典禮的儀程總計有37項，今紀錄如表1。</p>
