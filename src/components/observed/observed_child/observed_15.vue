<h3>捌、離島地區祭孔典禮</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;離島地區的釋奠典禮主要以澎湖為代表，澎湖孔廟的前身為「文石書院」，始建於1766年（清乾隆三十一年），距今二百五十餘年。今年澎湖孔廟於9月28日上午七時三十分舉行釋奠典禮，由縣長賴峰偉擔任正獻官，糾儀官由民政處長陳韻如擔任，分獻官由馬公國小校長王文瑞、中正國小校長林宗彥、中興國小校長陳智賢、文光國小校長洪宏賢、文澳國小校長方南芳、中山國小校長張素紋等人分別擔任，陪祭官則分別由澎湖當地的機關、學校、部隊、團體等首長與各界代表擔任，佾生由中正國小學生擔任，樂生由馬公國中國樂社與龍門國小學生擔任，禮生由中正國中和湖西國中學生擔任。釋奠典禮儀程總計24道，參見表5，今年丹墀上的佾舞由24位學生展演，採六行四列，過去有使用六佾36人的情況，今年或許有其他因素，樂生演奏的樂器皆為國樂器，並非祭孔的雅樂器，使用的樂譜也是屬於明代樂譜，典禮結束後分贈智慧糕與智慧筆給參禮民眾及相關人員。</p>
