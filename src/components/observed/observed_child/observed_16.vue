<h3>捌、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統的南管原本就是以業餘休閒的為主的音樂團體，填寫問卷的51個團隊中，也僅僅只有心心南管樂坊、江之翠劇場、南管新錦珠劇團等3個單位是以職業演出為主。南管平時的活動基本上是以館內傳習、拍館、孟府郎君祭典暨整絃等活動為主，其他活動就如參加他館整絃的同好切磋、公部門邀演以及對外教學與推廣研習等，偶而也應聘進行藝文演出或廟會慶典相關祀神儀式，附屬於宮廟、獲宮廟贊助經費或提供場地的團體，則有更多機會參加民間傳統廟會及祭典等相關活動，問卷結果也正好相對應。既是業餘休閒性質，組織成員也不複雜，主要有負責人（理事長、社長、團長）、教師（館先生）、館員、學員，行政庶務大多是館員分工協助，較難專聘行政人員處理相關事務。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;收入支出情況，除了3個職業演出團最少都在76萬元以上之外，也有少部分團體因本年度獲計畫案補助，收入支出金額突然高於往年很多屬例外情形，一般團體的收入與支出都是在25萬以下。收入來源主要是自籌經費以及政府補助，無來自教學活動的相關收入，但用於教學與推廣研習的費用卻佔了支出第二名，可見南管教學與推廣研習基本上都是免收費。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人力來源部分，業餘或兼職者高於專職者，女性多於男性，年齡集中在50-79歲者，依問卷統計，參與南管的人力有852人（專職男1＋女4人、業餘或兼職男354＋女493人），總計不足1千人，佔2千3百多萬臺灣人口比例實在微乎其微，當中因推廣研習活動參與者而來的有276人，推廣研習活動之效益統計前三名為技藝傳承與藝術推廣、提升民眾參與意願、吸引人才投入，也正好呼應此狀況，足見辦理推廣研習仍有其成效，若經費充足，應積極辦理相關推廣研習活動。傳播方式主要是靠社群媒體、口耳相傳以及節慶活動主辦單位宣傳，大部分團體並未有專職專責行政人力，就較難有時間去執行運用多元的傳播方式，讓更多民眾獲悉相關資訊，這是較為可惜之處。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;面臨困境的本項統計前五名依序為：人才斷層、國內民眾參與意願低、傳統藝術價值傳承困難、資金取得不易、專業技藝不足與民間推廣教育不足。南管面臨困境並非本年度才發生，長期以來，人才斷層、民眾參與意願低、傳統藝術價值傳承困難等皆重重影響南管的傳承保存，解決困境不能採頭痛醫頭、腳痛醫腳的治標方式，而是應該完善規劃執行一套治本方案，讓南管在講求多元聲光刺激的現代社會中有其立足深耕之地，如此才能永續保存、傳承發展！</p>

