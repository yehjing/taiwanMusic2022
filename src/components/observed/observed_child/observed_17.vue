<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為協助日後此類問卷之優化，筆者將問卷聯繫過程中，北管團體對於本次調查問卷之反饋意見，一併收錄於此，供中心及各界參考，盼能促進各方之理解與溝通。在聯繫過程中，婉拒填寫之團體，其原因大致係因時間精力有限，無法協助填寫，或是此類型調查先前已做過，無意願再次回應，並希望公部門間能橫向整合及流通資訊，以避免團體重複受訪。在完成問卷填寫之團體反饋意見中，部分團體不約而同向筆者反應此問卷與北管子弟生態有較大差距，許多問題無從下筆，尤其對於調查團體收支與人力結構二部分有所遲疑；其餘還有提及日後或可簡化及數位化問卷，以提昇問卷回收情況等建議，謹陳於此。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最後，由衷感謝本次問卷調查分析之契機，筆者得以在「2022臺灣音樂年鑑編輯計畫」的執行觀察分析之餘，從量化研究角度管窺北管團體之現狀發展情況。然而，北管團體眾多，填寫者僅其中一部分，因此本文需再次重申，此次分析結果僅能反應2022年度協助填寫問卷團體之部分情況，並無法以此涵蓋整個北管音樂大環境，惟仍希冀問卷之完成，能作為傳藝中心及相關部門日後施政、補助之參考。</p>
