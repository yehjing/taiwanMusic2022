<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;應國立傳統藝術中心之委託，「2022臺灣音樂年鑑編輯計畫」北管音樂子領域今年另執行北管團體之問卷調查，研究團隊根據2021年之團體名錄為基礎，進行問卷填寫意願之探詢。今年度觀察之北管團體共可分為幾類：具文資身分之團體（中央及地方）及不具文資身分之子弟團、職業團體等。其中，隨著時代之演進與順應社會之潮流/生存模式，不具文資身分之子弟團，又因其活動目的或經費補助，而有1.維持日常自主練習的子弟曲館、2.僅在所屬宮廟活動才聚集排場或請職業團以該團名號出門者，3.會兼當職業團接Case出陣的半職業/子弟曲館，以及4.由政府資助、協助已散館或瀕臨散館之曲館開設北管研習班招收新生，惟因此類新生已與早期所認知之「子弟」相去甚遠，更多的是一種參與社區推廣班的概念，因此筆者將他獨立一類。至於劇團部分，則多為職業劇團，因此也歸屬於前述職業團體一類。</p>
<h3>貳、團體數量</h3>
