<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;農曆正月初一一直到元宵節之間，為傳統過年之熱鬧喜慶之際，子弟曲館慣例在除夕夜或是大年初一進行新春賀正排場。今年度此類活動可見有基隆得意堂十組於基隆市奠濟宮廟埕排場（1/31）及雲林螺城園「壬寅年新春賀正」皆為除夕夜排場。正月初一（2/1）則可見有三重清樂音、桃園同芯社、臺中明梨園、基隆聚樂社一組、南屯景樂軒、三坑得意堂、六股和樂社、汐止樂清軒、彰化鳳儀園、北威聲奏樂團、中華民國傳統民俗文化協會、台北並歡樂社、新店十二張平和社、士林舊街團&hellip;等團體於當日進行「壬寅年新春賀正」排場。過年期間之賀正排場，尚有臺中明梨園、灣裡北管彩雲軒等於初二（2/2）排場，板橋潮和社、社團法人金聲順古樂協會於初四（2/4）進行排場，淡水和義軒則於初五排場。正月初九天公生，本年度見有彰化南瑤宮勵英社、淡水和義軒等團進行「玉皇大帝聖誕千秋」排場（2/9），而台北靈安社「壬寅新春鬧廳團拜」（2/13）則安排在元宵節前夕進行排場。正月十五元宵節（上元節）部分，本文初已提及，可見如南屯景樂軒臺中南屯萬和宮「上元天官大帝聖誕千秋暨犁頭店元宵」踩街及排場（2/15）。農曆7月15日中元節部分，亦可見如百福聚樂社「百福宮中元普度」排場（7/30）、淡水和義軒「中元普度」排場（8/13）等。農曆10月15日下元節前後，傳統往昔慣例會舉辦謝平安儀式，以答謝神明一年來的照顧，北管子弟亦不例外，如金山六股和樂社「壬寅年社慶暨謝平安祭典」排場（10/8-10/10）、基隆聚樂社一組「壬寅年謝平安祭典」排場（11/11）、彰化阿夷庄榮樂軒「阿夷庄福成宮謝平安」排場（12/10）、彰化南瑤宮勵英社「南瑤宮謝平安」排場（12/16）等。</p>
