<h3>參、祭儀活動</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;祭儀活動一類，可根據年鑑總計畫辦公室之細項分類指引，分為儀式、祭典與其他三類，三類中以儀式活動佔68%居冠、祭典31%次之，另有少數無法歸類之零星活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文所指「儀式」一類，實則包含與漢人傳統文化習習相關之歲時祭儀、節氣等民俗活動（如新春賀正、神明誕辰、普度、謝平安等）中宮廟所屬駕前子弟之排場，兼含少數子弟曲館神尊之開光紀念或曲館前輩喪禮上之排場祭悼。「祭典」一類，在本文之分類所指為較大型的複合型活動，例如子弟曲館所屬之王爺祭、館慶等，鎮日活動中包含許多小型儀式環節；另外還有現代大型廟會遶境出陣活動（如大甲鎮瀾宮遶境進香等），因其複合多元形式，也包含在本文所指之宗教祭典活動當中。「其他」一類則包含前述二類無法包含之活動，在本年度中可見有集雅軒北管樂團「揭匾、開館暨入館活動」排場（3/20）、基隆聚樂社一組送禮帖至三重自強金獅團、板橋合安社、淡水南北軒、台北共樂軒、台北靈安社、八斗子福清宮神將會、艋舺修境堂、艋舺露店金龍團（6/19）。</p>
