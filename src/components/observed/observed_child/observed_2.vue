<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在第二十二屆國家文藝獎頒獎典禮（10/7）上，邱火榮藝師發表得獎感言時曾如此說道：「拜託媒體先生小姐大力推薦、報導北管音樂快要沒落，不能讓他消失掉，請大力宣傳，謝謝！」<sup>16</sup>一位高齡近九十歲的國寶藝師，對於北管音樂傳承危機的擔憂，令人聯想到今年初中部某高中音樂課，學生與老師因對於南北管認知不同而爆發的言語衝突，後續擴散到媒體報導，成為學界、樂界一時的熱門話題，南北管一夕之間成為熱門關鍵字；而由此小小事件也顯示一般民眾對於臺灣珍貴的傳統音樂南北管仍存在許多成見與誤解，亟待翻轉。回顧2022年北管音樂大大小小一千多筆活動紀錄，都不意外地見到地方團體與官方之間之關係越趨緊密，單從推廣與傳習部分著眼，即可見到大部分講座、展覽、節目，或是工作坊、研習班等，多為公部門提供獎補助後的執行成果，且多為政府機關推動無形文化資產保存之相關活動。不具文資身分的北管團體對於人才流失與老化，顯得急迫與焦慮；反觀具文資身分之團體，有相對多的曝光度與演出機會，然而若能表演的曲目不多，則容易流於一曲走天下的形式，長此以往，北管音樂之流通曲目將會逐漸減少，對北管技藝之傳承或是一大隱憂。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從另一個角度來看，今年度雖見有數位老一輩藝師遠去的同時，筆者也看到有許多新踏入北管音樂領域的生力軍（不論是學校社團或是曲館成立之北管研習班），還有新一代曲館子弟的創意發想與跨域合作，諸如淡水南北軒邀請壞鞋子舞蹈劇場、雞屎藤舞蹈劇團一起參與今年度「淡水清水巖清水祖師聖誕遶境」出陣（6/3-6/4）；或如百合花樂團以《不是路》獲得第三十三屆金曲獎之最佳臺語專輯獎（7/2），無疑是評審團對此張專輯結合傳統音樂與搖滾音樂的一番肯定；其他還有梨春園子弟斜槓歌手曾立馨之創作，或如臺北木偶劇團改編莎士比亞名劇《奧賽羅》並請到國光劇團京劇老生鄒慈愛合作&hellip;等，這些都是新一代子弟表現其對北管音樂文化的認同與轉譯。綜上所述，對藝師的照顧與技藝保存、對新生代團體的提攜與扶持、對一般民眾的推廣，都仍是需要大家共同集思廣益，以拓展屬於現世的保存、傳習與推廣方式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最後，筆者認為2022年的北管音樂活動，已隨著疫情趨緩，正在緩步回歸日常，實際上經過了三年來與防疫之拉扯，所謂的回歸日常相信也不完全是往日的日常，更多的是順應後疫情時代而產生的適應與改變（例如今年度各類型活動多採線上、實體並行方式進行），而這些改變對於北管音樂之傳承與推廣是正向抑或相反之，則有待持續觀察。</p>

