<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;實際上，根據筆者第一線觀察所見，大部分北管音樂活動因其屬性、目的、經費來源等諸多因素，皆可同時包含不同類別之活動性質，惟未避免一則活動多個分類所造成之數字重複計算，筆者僅根據個別內容之類別重要程度，進行單一類別歸納。例如，受官方補助之北管傳習班課程，於本文中歸類於「傳習」一類，而非「文化政策」；或如於國外發生之相關活動，實則為「展演」或「推廣」性質，於本文中筆者將之歸屬於「國際與兩岸交流」一類，以凸顯其海外交流之重要性。</p>
<strong>二、活動取消紀錄</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北管團體活動與歲時節氣、地方民俗活動息息相關，2022年疫情大環境雖較之前二年有趨緩，仍有不少活動受疫情升溫或樂手臨時確診等因素影響而延期或取消。今年度取消活動見有彰化南瑤宮勵英社「福德正神聖誕千秋」排場（1/17）、基隆得意堂十組「開漳聖王聖誕遊行遶境」 出陣（3/17）、彰化過溝仔彩鳳庵清明靖安巡境活動（4/5）、林口樂林園「贊天宮天上聖母聖誕南巡進香」活動（4/16-17）、汐止樂清軒「汐止濟德宮媽祖聖誕遶境」活動（4/23）、國立臺北藝術大學傳音系 「大三北管傳統樂展&mdash;我們都有醉」音樂會（5/6）、竹塹北管藝術團「金門迎城隍－全國城隍大會師」北管戲演出（5/11-5/12）、科技部人文社會科學研究中心、國立臺灣大學、國立臺灣大學藝文中心「動態的聲音、身體與知識－場次一舞蹈主題工作坊」（6/2）、延樂軒北管劇團「接班人好戲台－111年傳統藝術接班人駐團演出計畫」（6/30）、游源鏗「北管戲唱腔與調門」講座（9/24）等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;少部分場次採延期並得已復辦者，則見有國立臺北藝術大學傳音系 「相會&mdash;高湘荃/林雅惠 北管樂畢業音樂會」（5/6延期至6/19）、臺北木偶劇團《三仙台尋龍記》（10/29-10/30延期至12/30單日兩場）、梨春園北管樂團赴大溪永福同義社拜館交流排場（10/22延期至11/13）。</p>
<h3>貳、展演活動</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;展演活動一類，可根據年鑑總計畫辦公室之細項分類指引，分為音樂會、遶境、藝術節與其他四類，四類中以遶境86%居冠、音樂會12%次之，藝術節與其他類則為零星活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文所指「音樂會」一類，實則包含北管音樂以文化場為主要目的之排場、戲曲等登臺演出（包含布袋戲）。「遶境」一類，則多指職業、半職業陣頭所承接之廟會出陣遶境（喪事陣頭亦然）及排場，亦即活動現場雖可見有神像神轎等民俗相關活動，但其出陣本質並非身為該宮廟之駕前樂團而進行之活動。「藝術節」一類，指以北管音樂為主題之相關藝術節或戲曲節，可能是單日、多日或系列節目。「其他」一類可見諸如活動錄影、或選舉等暖場活動之排場。</p>
