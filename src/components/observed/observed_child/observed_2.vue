<h3>伍、傳習活動</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳習活動一類，可根據年鑑總計畫之細項分類，分為工作坊、研習班、寒暑期營隊與其他四類，四類中以其他類37%最多，研習班30%次之，工作坊與寒暑期營隊較少。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文所指「工作坊」一類，包含以北管相關主題所進行之工作坊及短期體驗課程；「研習班」一類，指官方機構主辦或子弟曲館研習、日常練習。「寒暑期營隊」指非日常固定練習、主要舉辦在寒暑假期間之短期課程；至於友好子弟曲館間的拜館活動，實為子弟曲館與其他曲館間重要的交誼往來、彼此切磋技藝的場合，為早期北管子弟曲館日常練習、神誕慶典之外的重要交際活動之一；此類活動循往例歸類在民間傳習活動類別之下，惟因不屬於前三類，因而歸類在其他一類。</p>
