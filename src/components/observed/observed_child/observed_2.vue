<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著嚴重特殊傳染性肺炎（COVID-19）疫情的持續，2022年北管音樂圈仍受到疫情的起伏而多有影響。延續自2021年中的二級警戒，農曆過年前CDC公告宗教團體停止辦理遶境、遊行、搶頭香等活動，因此本年度新春期間之宮廟排場賀正活動相對較少，往昔元宵節能見到之北管排場、踩街、遶境等活動幾乎不見蹤跡。<sup>1</sup> 元宵節當日（2/15），CDC公告2/16起開放遶境、遊行及500人以上宗教活動，凡提報防疫計畫後即可辦理，因此農曆新年過後至3月進香期間的大型宗教活動，紛紛恢復辦理。然而，自4月中旬開始，因Omicron病毒變異株引起之2022年度的第二波疫情持續升溫，此時病毒走向為大規模、流感化時期，4-6月間每日數萬名的確診人數，即便輕症，也對傳統音樂活動造成影響，廟會相關活動縮減規模，官方主辦或民間自辦之北管研習班紛紛比照當時高中以下暫停實體課程之原則宣布暫停上課。<sup>2</sup> 隨後，文化部推出「受疫情影響表演藝術事業營運補助」方案，鼓勵4/1-6/30年度第二波疫情間受影響之表演藝術團體申請補助。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有別於2021年5月的疫情嚴峻、由政府主導升至三級警戒後停止相關活動，3月份隨著官方政策的適度放寬防疫模式，筆者觀察今年度政府普遍採取與病毒共存模式，在持續二級警戒的範圍之內，由團體自行決定是否舉行活動，每一次活動的辦與不辦，成了考驗團隊智慧的兩難選擇題，辦活動有擔心成為助長疫情擴散之緊張壓力、不辦活動又顯得有些失去動力，只能在拉鋸間尋求平衡。過往的大型遶境活動如淡水清水祖師得道紀念日遶境、大溪關聖帝君聖誕遶境、蘆洲湧蓮寺恭迎觀音佛祖遶境等活動紛紛在停辦兩年後，於今年恢復辦理。又如彰化南瑤宮老大媽駕前曲館彰化梨春園北管樂團，5/4於南瑤宮老大媽往第五角過爐時，因疫情影響沒有遶境，而是直接到值年角頭（臺中烏日爐主宅）排場扮仙，以此祈求疫情平穩、平安。下半年，隨著口罩政令之逐步鬆綁，生活逐漸走向開放，北管音樂相關活動亦逐步恢復。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022臺灣音樂年鑑之北管研究團隊<sup>3</sup>採用線上、線下資料蒐集加上實際田野調查探訪，針對北管音樂活動進行全面搜羅。資料蒐集部分，研究團隊會透過臉書、Instagram等平臺蒐集北管團體活動資訊，同時，亦透過政府公開數據查找各級資料庫、資訊系統進行有關研究出版、政策補助方面之資訊，而蒐集到之北管活動資訊，皆會再透過實際田野調查或其他平臺、新聞報導進行核實，以力求資訊之正確。本年度北管音樂研究團隊以上述方法進行研究工作，共計觀察120餘個子弟及職業北管相關團體，其中亦包含文化部文化資產局指定與縣市文化局登錄之各級文資團體，以及國家文化藝術基金會TAIWAN TOP演藝團隊。除了子弟及職業團體之外，目前全臺灣亦有國立臺北藝術大學傳統音樂學系開設北管樂主修，以此培育北管音樂專業人才。根據該校招生簡章，該系大學部每年所收北管樂新生7人，研究所則不分組，每年招生名額總計9人（含推甄與一般入學考試）<sup>4</sup>，該系所之北管音樂活動，也納於本次觀察範圍。</p>
<strong>一、全年度活動統計</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年北管音樂活動分為展演活動、祭儀活動、推廣教育、傳習活動、國際與兩岸交流、研究與出版及文化政策等七大類，全年度共計收錄1342筆活動。從各月份活動總數來看，2022年度以3-4月、7月、9-12月為活動密集期。從活動類別來看，全年度活動以展演活動1044筆為大宗，佔年度活動之78%，祭儀活動次之，佔比12%，其餘依序為文化政策及推廣教育各3%、傳習活動及研究出版各2%，國際與兩岸交流則僅遠遠低於1％。從圓餅圖來看，更可直觀看到全年度活動以展演居多、祭儀次之，其餘類別皆為少量、零星之見。</p>
