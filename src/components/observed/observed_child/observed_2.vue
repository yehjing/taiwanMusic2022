<h3>肆、推廣教育</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;推廣教育一類，可根據年鑑總計畫之細項分類指引，分為講座、展覽、節目、比賽/檢定與其他五類，五類中以節目44%居冠、比賽/檢定、講座分佔20%、18%次之，展覽與其他類則為零星活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文所指「講座」一類，指各種場合之演講座談。「展覽」一類，以北管音樂、文物相關之展覽皆含括在內。「節目」一類，除指傳統媒介形式之電視、廣播之外，亦關注新興Podcast、YouTube等平臺之相關活動。「比賽/檢定」一類，以全國學生音樂比賽為關注對象，以此了解各級院校中，以北管音樂參加絲竹室內樂合奏比賽的學校概況。「其他」一類，則收錄無法歸類於前述四類之零星活動，例如地方走讀導覽活動等。</p>
