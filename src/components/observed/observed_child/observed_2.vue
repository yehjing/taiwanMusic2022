<strong>三、藝術節<sup>9</sup></strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以傳統音樂為主題之藝術節活動今年共有五個：彰化傳統戲曲節、臺灣北管戲曲音樂節、彰化走讀藝術節、林口子弟戲文化節以及大雞籠北管亂彈戲曲藝術節。</p>
<strong>（一）彰化傳統戲曲節（6/4-8/7）</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此為彰化縣政府文化局連續舉辦多年之活動，每年皆會邀請縣內傳統表演藝術保存團體以及南北管音樂戲曲館北管各班學員登臺演出。其中三場為「南北管排場大匯演」，三場次受邀演之北管團體共計有梨芳園北管樂團、東山鳳梨園、文化局北管甲班、文化局北管丁班、文化局北管實驗樂團（6/4）、彰化阿夷庄榮樂軒、文化局北管甲班、文化局北管乙班、彰化集樂軒（7/17）以及梨春園北管樂團、玉琴軒北管樂團、文化局北管丙班、文化局北管戊班（8/6）。有別於往年，今年度文化局將活動安排在彰化縣內不同行政區進行演出（溪州后天宮、大村大庄慈雲寺及和美道東書院），或可藉此推廣與促進傳統音樂的閱聽人口。</p>
