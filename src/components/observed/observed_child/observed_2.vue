<strong>一、一般政策</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年與臺灣北管音樂相關之文化政策，筆者分為以下四類：</p>
<strong>（一）中央及地方無形文化資產保存者/團體動態</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年末，國藝會公告第二十二屆國家文藝獎得主，資深北管藝師邱火榮獲得此殊榮，該獎項之頒獎典禮於今年秋天舉行，從前文屢屢提及，可知國藝會為了彰顯與宣傳國家文藝獎得主之貢獻，舉辦許多演講、紀錄片錄製等推廣活動。例如前文提及之《藝術薪傳北管榮光：重要傳統表演藝術北管音樂保存者－邱火榮》紀錄片出版、《國藝會線上誌》人物報導，或如交通大學國家文藝獎系列講座（11/23）由邱火榮之女邱婷主講，其亦受邀到中央廣播電臺「美麗的台灣」節目中分享邱火榮的藝術人生（12/17）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;縣市文化局部分，宜蘭縣政府於5月公告新增認定漢陽北管劇團資深藝師李阿質、陳李美娘二人為宜蘭縣傳統表演藝術「北管戲曲」保存者，後於 10/27舉辦授證典禮，頒發保存者認定證書。當日漢陽北管劇團團長陳玉環率團員出席，場面溫馨。嘉義縣政府於6/9公告嘉義縣布袋鎮內田慶和軒受文化資產保存法第111條規定重新登錄類別名稱「傳統表演藝術」。年末則有台北靈安社霞海城隍老祖、西秦王爺大王神尊、謝、范二將軍老祖神將獲指定為「臺北市一般古物」（生活及儀禮器物）。</p>
<strong>（二）傳習計畫藝生甄選與考核</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文資藝生傳習計畫部分，因2021年中三級警戒影響當年度傳習計畫課程停擺導致2021年之藝生期末考試延至2022年4月進行之故，今年度之藝生甄選及傳習計畫相關訪視與考核皆隨之順延。今年度北管相關藝生名額，僅漢陽北管劇團與彭繡靜藝師透過公開甄選，分別新收1名及3名藝生。其中，彭繡靜藝師為2021年甫授證為重要傳統表演藝術亂彈戲保存者之新科國寶，此為她首次招收傳習藝生，該計畫委由板橋潮和社執行。該計畫連同梨春園北管樂團、漢陽北管劇團、臺北木偶劇團執行邱火榮傳習計畫，以及金滿圓戲劇團執行王慶芳傳習計畫皆順延至5/1起始新一年度之課程。緣此，本年度文資局針對傳習計畫執行團體之例行訪視與期中審查，皆因此推遲至7-8月及9-10月間舉行。</p>
<strong>（三）彰化縣立青少年北管實驗樂團成立</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年夏天，為了使縣內的南北管音樂傳承能有更好的途徑，擁有最多南北管資深館閣的彰化縣宣布成立青少年南北管實驗樂團，希望能培育愛好南北管音樂之青年。其中，北管部分招考20-25人，考生需有北管基礎演奏能力方能報名。該團邀請臺中十甲振梨園江志宗、彰化梨春園陳柔樺二位年輕藝師擔任北管駐團教師，並於8/29辦理「彰化縣立青少年南北管實驗樂團及傳統工藝保存者」授證儀式，正式對外公告成團。未來在年輕藝師的帶領之下、大手牽小手，相信對北管音樂教育之推動，能有不一樣的面貌。</p>
<strong>（四）第三屆世界記憶國家名錄揭曉</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部自2017年以來，持續推動「世界記憶國家名錄」計畫，2022年為該計畫第三屆，全臺灣共計有14案入選。而位於雲林縣北港鎮的集雅軒北管樂團以其豐富且保存良好之歷史文物收藏與緊密的地緣脈絡，獲得審議委員青睞。該計畫於3/3公告，後於9/2於文化部文化資產園區舉行頒獎典禮。</p>
