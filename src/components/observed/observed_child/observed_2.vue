<strong>一、講座</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;講座部分，今年度共有七場，前述「臺灣北管戲曲音樂節」系列活動之一的「論藝說戲－專家論壇對話」（7/16），以及邱婷於交通大學「國家文藝獎系列活動－2022浩然講座暨經典通識教育講座」中主講「弦吹人生－北管音樂領航者邱火榮」（11/23）這兩場係由專家學者主講，其他尚有新港舞鳳軒團員於「新港文教基金會親子共讀－繪本班」（3/10）進行的藝文教育類推廣活動以及謝宏國、鍾兆生主講「從基隆北管與美濃八音，談傳統音樂在廟宇慶典中的作用與發展」（11/27），共同討論同為廟會慶典中的重要樂隊，基隆北管與美濃八音各自如何發展。另外還有黃苡哲「時光片羽－文化資產生活進行式特展主題講座『這些年，傳統藝術對我說』」（3/19）、陳柏儒「道士家族在時代演進中的傳承與開創」（4/16）、黃偉強「『古物廟』寺廟古物特展系列講座－組團瘋媽祖，百年在地軒社」（5/7）等從個人角度討論傳統藝術與地方生活之關係，雖不是專涉北管音樂的講座，但因三位講者皆具北管相關背景，在講綱中仍會兼及討論。</p>
<strong>二、展覽</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;展覽部分，今年共有三場與北管相關特展：其一為新北市政府文化局舉辦之「新北有藝思－傳統藝師展演」（4/16-5/19），該展中介紹110年度受文化局登錄為新北市傳統表演藝術「北管音樂」之藝師詹文贊等五位藝師之珍貴文物與作品，並邀請詹文贊藝師與汐止樂英社及其指導學生於板橋放送所排場演出《蟠桃會》（5/14）。其二為文化部文化資產園區主辦之「古物廟－寺廟古物特展」，其中第三展區「聽見神音」之第二檔期展出集雅軒北管樂團於媽祖神轎出巡時演奏的樂器等文物（4/27-6/30）。其三為國立臺灣藝術大學古蹟藝術修護學系「失落的匠藝－有形無形潛力文化資產推廣展演」，於臺藝大展出宜蘭暨集堂花籃鼓架、各式繡旗、樂器等許多難得展出的珍貴文物（11/29-12/8），吸引許多北管樂友前往觀展。此外，國立中央大學崑曲博物館於YouTube頻道發布跨領域文物知識交流系列影片四支（12/12）等，其中《曲本的祕密》邀請中央大學孫致文教授介紹新竹地區振樂軒、新樂軒等子弟曲館之抄本以及該館如何利用歷史錄音、影片與抄本結合展覽，使觀眾能更直觀地了解抄本演唱的實際呈現情況。</p>
<strong>三、節目</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;節目部分，年中，延樂軒北管劇團將2021年錄製之「北管小學堂」二支影片，於YouTube頻道上架英文字幕版（8/25），有助於該團之國際交流與推廣。年末，漢陽北管劇團在宜蘭縣政府文化局「提升數位營運能力補助計畫」經費補助之下，於YouTube頻道推出北管樂器介紹及北管戲演出影片共計5支（11/14、11/16上架）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來，部分北管子弟曲館除了用Facebook、IG等社群軟體經營與推廣之外，也擴及Podcast節目之製播與訪談。例如台北共樂軒於年初製作「北管蹦蹦蹦之子弟請這講」Podcast節目，目前已上架9集節目（3/11-7/8），內容由館內年輕子弟透過輕鬆對談的形式，向社會大眾介紹北管音樂與該館之活動概況。同樣為年輕人的投入，公視《青春！咱的夢》節目－「北管偶像團」一輯（3/26）特別前往拍攝記錄鹿港新聲閣的一群年輕人，如何重拾與復興當地的北管文化。彰化梨春園北管子弟曾立馨於Music South YouTube頻道發布〈古早的歌聲〉一曲（7/15），將其個人與梨春園之相遇、三年學習歷程的深厚情感，以歌唱方式唱出，曲中引子穿插細曲唱段與伴奏，顯得相當別緻。</p>
<strong>四、比賽/檢定</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;教育部全國學生音樂比賽之絲竹室內樂合奏組別中開放南管樂北管樂及客家八音參賽，於每年的音樂比賽中，皆可零星看到北管社團參與其中。例如新北市丹鳳高中國中部北管團自2018年成立後，已多次參加新北市市賽與全國賽。其餘還可見有基隆市立中興國小、基隆市立和平國小、基隆市立安樂高中國中部、新北市瑞芳區濂洞國小、新北市板橋區溪州國小、新北市欽賢國中、新北市丹鳳高中國中部北管團、臺中市立大安國中、嘉義縣布袋國中、臺南市私立南光高中、宜蘭縣頭城國中等參賽隊伍，以北管牌子或絃譜之曲目參賽。這些學校以北管參賽，可見長期來自地方子弟曲館之深耕地方之餘，亦多有北藝大傳音系校友之耕耘有成。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年底，由北藝大傳音系潘汝端教授所策劃之第一屆「北管青少年音樂會」（12/17-12/19）中，也邀請到基隆市中興國小、基隆市安樂高中國中部、新北市丹鳳高中國中部、臺中市大安國中、嘉義縣布袋國中、林口樂林園等中小學北管社團與民間曲館子弟，與該校北管組學生共同線上匯演、交流切磋，以此促進北管幼苗之發芽茁壯。</p>
