<strong>二、政府獎補助</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;政府機關獎補助活動，筆者分為中央機關補助以及縣市文化局補助二類：</p>
<strong>（一）中央機關補助</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年度由文化部文化資產局補助之傳統表演藝術C類補助，以子弟曲館申請傳習計畫為多，如內田慶和軒「北管傳承研習發展計畫」、「梨芳園北管樂團人才培育傳習計畫」、「榮樂軒北管劇團後場傳習計畫」；以推廣為主要目標而進行之申請，則有淡水南北軒「咚嘻南北：北管音樂學校計畫」、板橋潮和社「風『潮』再起－北管藝術傳承、推廣計畫」、竹塹北管藝術團「2022新竹市北管音樂戲曲推廣活動計畫」、梨春園北管樂團「111年度重要傳統藝術保存團體梨春園北管樂團拜館聯誼暨巡迴演出推廣計畫」等。其他還有臺北木偶劇團獲文化部「111年媒合演藝團隊進駐演藝場所合作計畫」補助（1/7），於臺北市紀州庵文學森林安排2日展演，分別演出《女巡按》（9/24）、《定三秦》（9/25）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國藝會部分，長年提供一年二次之戲劇（曲）類補助，2022年度見有臺北木偶劇團「自我提升計畫II」、「樹上的朋友Treetop Friends」、「花東偏鄉巡演計畫」、延樂軒北管劇團「北管扎根計畫」、翠蘭戲曲工作坊「北管廟口系列－北管戲【走三關、回窯】演出計畫」、漂鳥演劇社「臺灣亂彈戲語言藝術調查與研究：以彭繡靜藝師為例」等，皆為今年度第一期、第二期之國藝會獎補助受獎者。另外，庄尾文化聲音工作室「北管深度學習與再轉譯：跨域藝術青年培力暨藝文展演計畫」獲得國藝會2022年第二期文化資產類補助。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前章提及之三個研究計畫案：「110-112年宜蘭縣北管軒社文物普查（二）」、「110-111年花蓮縣列冊追蹤文物『花蓮福蘭社軒社繡綵旗幟』、『豐田碧蓮寺石雕藥師佛（老佛祖）佛像、木雕不動明王神像』調查研究計畫」及「北管藝師莊進才生命史研究調查暨編印出版計畫案」，實際上，亦為中央及地方機關所補助執行之計畫，惟在本章計算時，並未列入。</p>
<strong>（二）縣市文化局補助</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;地方文化局補助部分，今年度見有翠蘭戲曲工作坊、臺北木偶劇團、延樂軒北管劇團、新莊新樂園等團體獲得補助，補助內容亦多為北管戲曲之傳承與推廣。其中，新莊新樂園過往較少見於獎補助名單，今年度隨著新莊、蘆洲等地之子弟開始投入復甦，「新樂園之『大家學子弟戲』傳承及推廣計畫」獲新北市文化局「111年度新北市社區營造一般性補助計畫第一梯次補助」，補助經費得以用來延聘師資進行北管研習班之開課。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上種種，可見今年度之獎補助內容，仍以傳習、推廣、展演為主，但凡遇到文物/屋舍年久失修時，亦有相關舉措進行專案推動。</p>
<h3>玖、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;通過本文的梳理與評介，我們可以觀察北管音樂各類別之活動型態與比重皆各有側重。若再從歷時性角度來看，比對2021年與2022年之北管音樂活動統計概算圖，可以觀察到以下幾點特性：其一，這二年之活動，除了受疫情影響之明顯減少之外，二年之全年度活動比重基本相同－意即北管音樂活動之整體趨勢基本跟隨展演活動起伏、祭儀活動次之，其餘活動相較之下都極為零散；其二，2022年全年度活動量能明顯高於2021年（見下方二圖之Y座標跨度）；其三，2021年末受疫情影響之活動數量降低，2022年初則接續此一活動量能並逐漸提高。</p>
