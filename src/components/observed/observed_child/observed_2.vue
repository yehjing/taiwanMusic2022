<strong>一、儀式</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如同前文所及，北管音樂活動與歲時祭儀、神誕慶典息息相關，是臺灣傳統社會中重要的傳統文化之一。根據筆者研究，北管子弟所參與之儀式排場一類，大致又可分為全國性及地方性歲時行事二類。全國性歲時行事，可見如新春開正、元宵節（上元節）、保生大帝聖誕、媽祖聖誕、觀音佛祖、中元節、中秋節、下元節謝平安、尾牙&hellip;等；地方型歲時行事，則可見如霞海城隍爺、開漳聖王、神農大帝、清水祖師&hellip;等神明聖誕。在這些活動中，子弟曲館會在其所屬廟宇、館閣或相關場域進行排場祭拜或小型遶境。</p>
