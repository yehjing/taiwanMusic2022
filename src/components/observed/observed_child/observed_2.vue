<strong>一、工作坊</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度以工作坊形式進行的北管活動，主要有三：其一為鍾繼儀「Soundpainting發展暨演出計畫工作坊－你曾聽過，但不一定理解的聲音－北管音樂」（7/9），鍾氏從聽覺與視覺角度帶領課程學員進行北管鑼鼓「跳台」之實作，以促進理解戲曲演出中，後場鑼鼓與前場身段、台步之間的關係。其二，國立清華大學環境教育中心榮芳杰教授帶領計畫團隊開發一系列「文化資產現地教學系列」之課程，其中包含「北管搖滾雙面人」單元，7/15於新竹市金長和媽祖水仙文物館進行學習實作，以此推動學生對於無形文化資產之認識與理解。其三，彰化縣南北管音樂戲曲館主辦「半線憨子弟．北管小學堂」（8/20），邀請縣內梨春園北管樂團開課，活動招收國小三年級以上學生，且歡迎大人小孩一同報名，共同體驗北管藝術文化，梨春園北管樂團透過一日的課程，帶領學員了解並學習北管經典戲齣《三仙白》。</p>
<strong>二、研習班</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;研習班部分，大致又可分為地方文化局或私人機構主辦以及民間子弟曲館自辦活動。前者如桃園市政府文化局「桃園閩南書院北管培訓班」、元太道堂「北管入門班」、儒門柑仔店「北管初體驗」等，多為4-8節課之短期課程。或如彰化縣文化局「傳統音樂戲曲傳習計畫」於南北管音樂戲曲館固定開班，又如前述「彰化南北藝文扎根教育計畫」所設點之學校、曲館所進行的課程，因為有經費的挹注，而有相對長期且穩定的練習與上課。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;民間子弟曲館自辦活動，則如台北會安樂社、新莊新樂園、基隆三坑得意堂、基隆聚樂社一組、淡水和義軒、淡水南北軒、慈願北管團、北威聲奏樂團&hellip;等，有持續對外宣傳與招募新生，因此記錄在本年鑑北管活動月曆中。然而，北管音樂之日常文化中，本就隨時歡迎有興趣的各界人士中途加入，因此其餘曲館雖無特別招募新生，其日常學習仍為持續精進技藝的方式。是故，此部分之數字統計，僅記錄今年度有在官網或其他網路平臺上公開招募之曲館，並不代表所有有例行練習之曲館數量。</p>
<strong>三、寒/暑期營隊</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;寒/暑期營隊在北管音樂活動中相對少見，今年度主要為國立臺北藝術大學夏日學校「傳音學校復古音樂趴」的中學生與教師營隊，前者招攬14-20歲學生，後者「傳統音樂教師營」則招攬傳統音樂教師及18歲以上民眾參加。課程包含南北管、古琴、琵琶、甘美朗&hellip;等多元文化與音樂之認識與演練，二個營隊最後皆以發表會形式進行成果展演（7/10教師營、7/16學生營）。</p>
<strong>四、其他</strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拜館交流為曲館間維繫情感、相互切磋技藝的一大活動，2022年可見到梨春園北管樂團在「文化部藝文振興計畫」經費補助之下，先後前往新店太平宮太平社（1/8）、嘉義縣內田慶和軒（9/4）、學甲慈濟宮漢聲軒（9/18）、大溪三層三社子弟班（10/15）、大溪永福同義社（11/13）等進行五場次之拜館交流；此為該團自2016年起的年例活動，以此促進團體發展並開拓更多的交陪館。其他還有彰化集樂軒赴集雅軒北管樂團拜館（1/16）、板橋潮和社赴彰化集樂軒拜館（2/22），以及台北並歡樂社赴月眉厝昭樂軒拜館（3/20）等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外還有彰化縣南北戲曲教育扎根計畫之北管教學觀課與交流活動，邀請彰化花壇御夆閣亂彈劇團、彰化集樂軒、梨芳園北管樂團、彰化阿夷庄榮樂軒、彰化鈺淨園、東山鳳梨園等在地曲館共同聚集交流（12/1）、切磋教學心得，也極具傳習意義，因此歸於此類。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在官方與民間皆積極保存與傳習北管音樂的同時，藝師之凋零亦為令人唏噓之事，本年鑑今年亦見到幾位資深北管藝師之遠去，如板橋福安社張天培、梨春園北管樂團陳景星、林口店仔永興軒張國雄、共樂軒賴陳隆、彰化南瑤宮勵英社黃氏、聖震聲樂長陳仲仁&hellip;等，筆者酌予記錄於此，以茲紀念。</p>
<h3>陸、國際與兩岸交流</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際與兩岸交流一類，指在臺灣以外之世界各地，涉及北管相關之音樂活動，又根據地區別，以「國際」及「兩岸」二個細項分開計算。此類活動近年因疫情之故，活動量能極低。2022年僅見有2筆國際交流活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其一為旅德作曲家、國立臺北藝術大學音樂學系校友陳政文以北管音樂素材結合電聲進行之創作作品《北管賦流》（2020首演），於德國廣播電臺「新音樂工作室」節目中播出（德國時間5/7）。<sup>13</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其二為臺北木偶劇團受疫情延宕多年之文化部「2020臺日文化交流專案」計畫，前往日本小金井結城座排練場與當地懸絲傀儡藝師進行合作交流，後於日本東京都杉並區座高圓寺劇場進行成果展演。是次演出以臺灣傳統布袋戲結合日本傳統懸絲傀儡，共同演出北管布袋戲經典劇目《劈山救母》（9/3-9/4）。特別值得一提，該團赴日期間，除了展演之外，亦辦理多場次工作坊，如鄭湘蓁之報導紀錄：<span class="bkt">「該活動除了與日本傳統懸絲傀儡劇團『結城座』合作《劈山救母》外，也舉辦日本福島縣白河市工作坊、《大鬧水晶宮》布袋劇人形展、指尖上的生命－臺灣布袋戲源流特展等，以展覽、工作坊等方式向日本民眾推廣臺灣布袋戲的源流」</span><sup>14</sup>。是次活動受到當地平面媒體廣為報導，堪為一次成功的文化外交。</p>
