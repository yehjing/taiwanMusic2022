<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當今，唸歌、恆春民謠、褒歌、歌舞藝陣等皆未如南管、北管，列入學校的正式課程之中，多屬社團性質，或是以「本土語言課程」之名安排的課程內容，稱不上「人才培育」。反而學校以外，在各地社區大學、縣市藝文推廣部門、文化部重要傳統表演藝術保存者傳習計畫，甚至私人開設的研習班等，培訓了更多人才。</p>
<p><strong>二、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2019年底開始延燒全球的嚴重特殊傳染性肺炎（COVID-19）疫情，仍然繼續延燒，臺灣的各類表演藝術的演出也大受影響。雖然未如前一年2021年那般嚴重，但是看2022年2月和4月的活動數量仍會發現，不只是展演活動，推廣活動同樣受到影響。</p>
<p><strong>三、資料來源</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活動訊息來源，以計畫人員在網路上搜尋為多，次之為主辦或演出者（團體和個人）發布的訊息（海報或私下轉達）。在本計畫執行期間，皆會與演出者保持聯絡，適時取得相關訊息。接著，從中擇選適合者前往觀察進行記錄，部分活動基於交通、時間等問題，僅能從主辦單位或演出者在網路上發布的訊息，經過整理、比對後，撰寫活動觀察紀錄表。由媒體製作的文字報導和節目（如電視、廣播和Podcast、YouTube、Facebook等網路平臺），也會仔細閱聽後，擇選適合者撰寫活動觀察紀錄表，或只是填寫在月曆中。各個相關出版品、研究論文等，也採取相同方式進行記錄。</p>
<p><strong>四、研究方法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本研究囿於經費、人力，活動記錄（包括月曆和活動觀察紀錄表）主要來自現場觀察、網路訊息（包括網路直播）和演出者提供訊息三者。活動記錄表多數為現場觀察，除非主辦單位不允許，活動現場皆會以攝影（視人力狀況同時錄影）及文字記錄活動內容。</p>
