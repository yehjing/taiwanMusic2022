<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧2022年，全世界依舊未能完全擺脫嚴重特殊傳染性肺炎（COVID-19）的陰影，處處受到侷限，臺灣亦然。從上面圖表9標示出的傳統福佬歌謠每個月活動/資料筆數，可以發現1、2月和4、5、6月的活動筆數最少。1、2月正值舊曆年前後，儘管多數人已接種疫苗，但心中仍有所疑慮，不論主辦方、演出方或觀賞方，多自肅減少活動次數。3月份活動激增，與前後月份相比，就像「一支獨秀」，其實代表當時疫情未如大家先前所擔心的，稍微放心將之前（包括2021年）未能舉辦的活動儘速完成。沒想到進入4月，終究還是陷入疫情的泥淖裡，到了5月更只有少數的展演和推廣活動進行著。6月的狀況依舊如此，然筆數會多於5月，端賴3篇研究論文和2篇學位論文，加上楊秀卿離世後，電子媒體重播過去的專輯、平面媒體也出現數篇紀念專文，才會有較多的筆數。7、8月暑假期間疫情稍緩，加上民眾逐漸走向與病毒共存的生活，活動逐漸恢復。等待數個月，往年每年慣例舉行的幾個大型活動，2022年總算能一如往常在9、10月舉行，包括台灣月琴民謠協會舉辦的「台灣月琴民謠祭」（自籌款項舉辦）、微笑唸歌團主辦的「臺灣唸歌節」（自籌款項），和屏東縣政府主催的「2022半島歌謠祭第十五屆屏東縣恆春民謠全國大賽」皆順利辦理完成。而國立傳統藝術中心自2017年開始規劃的「傳統藝術接班人－駐園演出計畫」，也早在8月底就已恢復舉辦。一如往年，11、12月是傳統福佬歌謠活動舉辦的淡季，但活動次數仍比前半年的1、2月和4、5、6月多。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如前述，2022年的活動中以唸歌佔最多數（參見本文「前言」圖表2），唸歌過去曾經沉寂了一段時間，隨著楊秀卿習藝的藝生們，經過一番努力漸漸走出一條路來。值得注意的是，可以發現「跨界合作」的場次漸多，例如微笑唸歌團分別與「迷火佛拉明哥舞坊」、「神奇樂坊」等的合作。恆春民謠的部分，活動略少於唸歌，展演和推廣兩類活動各佔一半，2021年亦同，可看出恆春民謠近兩年展演之餘，也很注重推廣（參見表2、圖表1）。所有類別中，活動數量最少的，當數褒歌（參見以下圖表10，或前文表1）。比起其他類別，褒歌原本就不是一種對外展演性質的歌謠，恆春民謠原本也是屬於自娛的藝術，但是因為種種機緣，加上陳達受到學者與大眾的重視，進而有較高的能見度，褒歌卻一直未有這種機會，2022年和2021年相同，對外展演幾乎等於零（其因見前文說明），傳習活動也只有澎湖文化局開辦的「褒歌讀書會暨二崁走讀活動」1個。在研究出版上，期刊論文和學位論文闕如，僅有兩本專著出版，秦秀蘭主編的《台灣茶山裡的雲雀再現－閩南山歌傳唱》（臺灣新高齡社區健康發展學會出版），以及林金城、許亮昇編著的《三峽相褒歌》（昊天嶺文史工作室出版），兩書雖然都附有影音資料，卻不是光碟，而是在網路YouTube平臺觀看。這一點某一層面代表現代人閱聽影音資料的習慣，已從過去購買CD光碟，逐漸改變從網路平臺觀看、欣賞的習慣了。</p>
