<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儘管能唱褒歌的人越來越少，能演出的機會也不多，但仍有研究者持續不斷整理出版相關著作，如秦秀蘭主編的《台灣茶山裡的雲雀再現－閩南山歌傳唱》和林金城、許亮昇編著的《三峽相褒歌》，2本的影音資料都可以透過書中QR Code連結，在YouTube平臺免費觀賞。其中值得一書的是，林金城不斷有褒歌相關著作出版，且多自行出資出版，將其一直以來在北臺灣山區田野調查訪查得來的褒歌曲調、曲詞，及影音資料，逐次分地區出版，總計目前包括《金山相褒歌》（昊天嶺文史工作室，2003年）、《平溪相褒歌》（昊天嶺文史工作室，2004年）、《雙溪相褒歌》（昊天嶺文史工作室，2005年）、《坪林相褒歌：註解、釋義、賞析、耆老唸唱原音CD》（昊天嶺文史工作室，2012年）、《石碇相褒歌》（昊天嶺文史工作室，2021年）等。<sup>12</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;恆春民謠的部分，總計有1張黑膠唱片（包括網路串流音樂）、2本曲集、1本教材、1本專論、1套是樂人的生平故事和創作曲集。2022年適逢洪建全基金會成立50週年，基金會復刻再次發行史惟亮製作、陳達演唱的黑膠唱片《陳達和他的歌》，為此洪建全基金會還規劃了系列的講座，作為50週年的慶祝活動。余昕晏編著的4本著作，《反身性實踐：再探恆春半島民謠的異與藝》、《月琴彈唱趣：恆春半島民謠曲集》、《半島序曲：恆春半島民謠詩歌集》和《吟遊半島：恆春半島民謠教材》，其實是前述臺北藝術大學執行教育部USR計畫－「Hi-Five Plus：從北投平埔族群到屏東原住民的國際藝術對話」的成果之一。周定邦編著的「三台山&ecirc;風」套說（包括《網紗記事》、《島嶼戀歌》2書及2張音樂光碟），是記錄其與朱丁順（文化部「重要傳統表演藝術恆春民謠保存者」）的對談和作品。<sup>13</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌的部分則有2本與歌仔冊相關、1本是唸歌有聲書，另1是音樂光碟。這張音樂光碟是由微笑唸歌團製作，將楊秀卿的唸歌錄音結合鋼琴作品創作而成，入圍了2022年第三十三屆傳藝金曲獎最佳演唱獎、最佳作詞獎和最佳傳統音樂專輯獎。唸歌有聲書則是楊秀卿的結業藝生丁秀津編著，附有曲譜的《大舜耕田》。台灣歌仔冊學會自2021年開始刊行《歌仔冊年刊》，2022年發行第二期，該學會同時也出版了蕭藤村的《「韓文公祭十二郎」歌仔冊解析》一書。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;黃文博主編的《藝陣演藝：2021國家重要民俗南鯤鯓代天府五府千歲進香期專輯》，其實是編輯出版2021年臺南市南鯤鯓代天府舉辦的「藝陣演藝：2021國家重要民俗南鯤鯓代天府五府千歲進香期」活動資料。這一年的活動裡，主辦單位邀請多個藝陣團體和學校陣頭社團共襄盛舉，其中包括臺南地區的東竹林保安宮和竹橋七十二份慶善宮2個受臺南市政府登錄的牛犁歌陣團體。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最後3本專書（含曲譜），莊永明（1942-2020）的《台灣歌謠：我聽我唱我寫》曾在2011年出版發行，2022年由臺北市文獻館再版發行。何真真編著的《新世紀鋼琴台灣民謠30選》是一本收錄多首臺灣傳統福佬民謠的鋼琴譜集，修訂再版發行（二版）。由宜蘭縣政府文化局出版發行，鄭英珠編輯的《傾聽舊卷－張月娥樂譜遺稿集》，是將張月娥（約1927-2020）生前蒐集整理的樂曲手稿整理出版。書中除了簡介她的生平事蹟外，還收錄她編寫的【大調】、【七字調】等樂譜。</p>

