<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體而言，展演活動仍是佔所有活動之冠，其次是推廣教育，接著是傳習活動，國際與兩岸交流只有1筆，其因不需贅言，倒是研究與出版一項有31筆，不論唸歌、恆春民謠，或褒歌、歌舞藝陣等，每一類都有研究論文和出版品，這是令人感到欣慰和欣喜的，不論是演出者或研究者，都有同樣的想法，希望多紀錄和研究這幾類傳統福佬歌謠。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;總結來說，2022年上半年活動仍受疫情影響，活動數量約莫減半，下半年生活逐漸回歸正常，不論是展演、推廣、傳習等多項活動，多數照常舉辦。專書、專輯、研究論著、學位論文等，也一如往年持續著。本文論及的幾類傳統福佬歌謠中，褒歌的筆數是最少的，時代的改換、能演唱者的年齡偏高、傳承的困難等等因素，都使得這項傳統表演藝術快速消失，亟需各界更多關注。以上是2022年關於傳統福佬歌謠的觀察與評介，希望能藉此為對傳統福佬歌謠有興趣者，留下一些資料。</p>
