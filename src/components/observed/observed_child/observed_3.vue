<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的講座會較2021年多，多是緣由於洪建全教育文化基金會（以下簡稱「洪建全基金會」）為慶祝基金會成立50週年，重新復刻發行曾在1977年發行的黑膠唱片《陳達和他的歌》，再版發行的同時，洪建全基金會舉行了一系列活動，包括多種講座以及演出。此外國立臺灣交響樂團（以下簡稱「國臺交」）配合文化部的「走讀臺灣」計畫，同樣也以「陳達」為主題，規劃的「尋訪恆春民謠先師陳達」系列活動，活動內容包括多場講座及一場演出。值得一提的是，國臺交策劃的這一系列活動，皆是採線上直播、演出的方式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;校園巡迴的部分，3場都是與唸歌有關，一場其實是跨界合作，鄭美受邀與龍潭愛樂管弦樂團等，共同參與「2022紀念鄧雨賢校園巡迴音樂講座」。該活動雖是講述鄧雨賢的生平故事，卻是藉故事介紹幾類臺灣傳統音樂，如唸歌、南管等。鄭美飾演故事中，鄧雨賢遇到的一位「唸歌先」（唸歌表演者）。這個活動以桃園市各級中小學學生為對象，包括龍潭國中（9/13）、龍潭國小（9/19）、高原國小（9/28）、武漢國中（9/28）、石門國中（9/30）、石門國小（10/4）、龍興國小（10/5）、龍潭高中（10/5）、三和國小（10/6）等。另有一場校園推廣值得一提，那就是微笑唸歌團前往澎湖縣望安鄉望安國小進行的唸歌推廣演出，雖然僅有一場，某種程度來說也算是一種交流，讓該校師生有機會重新認識唸歌。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外需要特別說明的是，表7中「網路影片」一欄中的影片，都是在YouTube平臺的影片。2筆是前述國臺交舉辦的「走讀臺灣計畫－尋訪恆春民謠先師陳達」中的兩支影片，一支是介紹陳達的生平，另一支是預錄的恆春民謠演出。而唸歌的網路影片，則是微笑唸歌團申請文化部文化資產局紓困補助，製作的新編唸歌劇本「文學歌仔仙《送報伕》」，分4天上傳到YouTube平臺，免費提供社會大眾觀看。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於被歸為「其他」的3筆，係因3筆活動內容各不相同，難以歸類，因此皆歸為「其他」類。其中1筆，是台灣月琴民謠協會參加文化部影視及流行音樂產業局主辦的「2022世界音樂節＠臺灣」（在臺北市大佳河濱公園第8、9號水門舉行），在活動中開設「月琴入門體驗工作坊」。另外2筆和唸歌相關，其一是吳栯禔在宜蘭縣礁溪鎮成立「臺灣唸歌館」，另一則是「臺中聲五洲布袋戲團」團長王英峻偕同微笑唸歌團一起合作的「1916工坊掌中世界體驗營」（文化部文化資產局主推的「暑期文資體驗營」之一，07/26-07/29）。這場體驗活動有2項特點，一是對象雖然都是國小學生為主，但來自不同學校，也不在學校內舉辦，因此定義上無法歸類為「校內巡迴」；二是活動內容上結合布袋戲與唸歌，參與的學童雖然分成操偶、口白、唸歌三個組別，但是最後的成果是一起發表的，學童可以同時認識布袋戲和唸歌，是一個很有意義且有趣的活動。</p>
