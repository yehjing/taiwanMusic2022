<h3>壹、前言</h3>
<p><strong>一、研究範圍界定</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本章所界定的「福佬歌謠」，是指福佬人移民至臺灣，從家鄉帶來的民間曲調，有的是曲調固定，由演唱者創作填詞，有的則是曲詞和曲調均固定，然多數詞曲創作者皆不詳。現今世人常把這一類傳統福佬歌謠，與日治時期（1920年代）開始流傳，明確知道作詞、作曲者的近現代歌曲混淆，通稱為「臺灣歌謠」或「臺灣民謠」等詞。儘管兩者都是使用「福佬話」，也就是以「臺灣話」、「臺語」演唱的樂曲，但兩者的風格明顯不同。因此，若要了解臺語演唱的近現代歌曲，也就是臺語流行歌，或慣稱的臺語老歌等，請參閱《2022臺灣音樂年鑑》關於「流行音樂」的專章討論，本文不重複論述。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;使用福佬話，或者說以臺語演唱的傳統歌謠，本文主要以唸歌（或稱唸歌仔）、恆春半島民謠（以下簡稱「恆春民謠」，包括恆春鎮一帶流傳的民謠和流傳在滿州鄉附近的民謠）、褒歌（或稱「相褒歌」）等為主，旁及部分歌舞陣頭，如牛犁歌陣、車鼓歌陣、竹馬陣、牽亡歌陣等。關於「褒歌」有兩種意義，一者為往昔的褒歌普遍流傳在臺灣各地以茶葉種植、採收為主的地區，屬於一種庶民、勞動階級，隨口哼唱的曲調，多是七言一句、四句一葩（段）的歌詞形式，歌詞內容即興，曲調以「歌仔」為多，部分地區也會摻用當地平埔族的曲調。根據文獻及田野調查，臺北市陽明山、新北市坪林和金山一帶、桃園、澎湖等地皆有流傳過褒歌的紀錄。「褒歌」的另一個意義，係指一種演唱形式，演唱者間如對話般，互相嘲弄、調侃，歌詞依據對方的歌詞內容輪流即興編詞演唱，有時甚至變成相互間一種「拚輸贏」的小比賽。這種演唱形式同時也出現在客家採茶戲、歌仔戲等戲曲演出，或是一些民間歌舞小戲中，例如一般熟悉的民間曲調《桃花過渡》，就是「褒歌」的形式。本文所指為前者，流傳在山區，隨口哼唱、不使用樂器伴奏、歌詞即興且押韻的曲調。本文各個表格，皆分別列出唸歌、民謠（恆春半島民謠）、褒歌、歌舞藝陣等類，「其他」一項則指綜合性的系列活動，以台灣月琴民謠協會不定期主辦的「民謠常在我心」、「北投農創市集音樂會」等活動為例，演唱內容多樣，從傳統福佬民謠曲調、臺語老歌、自創曲，甚至結合搖滾、電子樂等，由於演出內容難以劃分，遂歸為此類。本文旁及關注的牛犁歌陣、車鼓歌陣、竹馬陣、牽亡歌陣等歌舞陣頭，演出時使用的歌謠，不論是出現在牛犁歌陣和車鼓歌陣中的〈牛犁歌〉、〈車鼓調〉，〈番婆弄〉、〈桃花過渡〉、〈五更鼓〉、〈病子歌〉、〈採蓮歌〉等，<sup>1</sup> 或出現在牽亡歌陣的〈十月懷胎〉、【將水】、【相思引】等，<sup>2</sup> 又或者是竹馬歌陣使用的〈車鼓調〉等，<sup>3</sup> 都是傳統的福佬歌謠。儘管歌舞陣頭一種伴隨身段，載歌載舞的演出型態，並非單純「演唱」歌謠，然傳統上後場樂曲皆是傳統福佬歌謠。再者，這些歌謠時至今日也會被單獨拿出來演唱，或是出現在如歌仔戲、本地歌仔等戲曲演出中，因此筆者認為有稍作紀錄的必要性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從現況來看，不同類別的傳統福佬歌謠似乎只出現在臺灣少數幾個地方，並沒有出現僅只在臺灣西部平原流傳的歌謠種類。關於這點，必須先回溯臺灣傳統音樂的發展，或者說臺灣傳統表演藝術的發展。不論風山堂的〈俳優と演劇〉（1901年，《臺灣慣習研究記事》第1卷第3號）、武內貞義的《臺灣》（1915年、1927年改訂版，臺北：株式會社臺灣日日新報社）、片岡巖的《臺灣風俗誌》（1921年，臺北：株式會社臺灣日日新報社）、椿本義一的《臺灣大觀》（1923年，東京：大阪屋號書店），或是鈴木清一郎的《臺灣舊慣冠婚葬祭と年中行事》（1934年，臺北：株式會社臺灣日日新報社）、宮川次郎的《趣味の臺灣》（1941年，臺北：日本旅行協會臺灣支部）等文獻資料中，皆可發現素來臺灣民間偏好的表演藝術，戲曲佔絕大多數，因此上述文獻中也多記述與戲曲相關的記載。少數幾篇與「歌謠」相關的著作，如王育霖的〈臺灣歌謠考〉（1938年，《翔風》第18號）和陳保宗的〈臺南の音樂〉（1942年，《民俗臺灣》第5號，臺北：東都書籍臺北支店）等，總算能進一步了解早期福佬歌謠的發展，兩篇文章都把歌謠加以分類，儘管兩篇文章內容不同，大致上都提到了「採茶－撐渡」、「駛梨仔歌」、「歌仔」、「童謠」、「阿祿仔歌」等類歌謠，<sup>4</sup> 未見有只在臺灣西部平原流傳的福佬歌謠。1995年筆者參與許常惠教授主持的「彰化縣音樂發展史調查計畫」和2019年范揚坤助理教授主持的「北港地區開路鼓樂、南北管陣頭調查研究及舊樂譜數位化與出版計畫」時，<sup>5</sup> 也未探得只在西部平原流傳的福佬歌謠。從上述文獻資料，唯一能找出的，僅有流傳全臺灣的一類歌謠，也就是最後依此衍生發展出歌仔戲的「歌仔」。現今雖然各地仍然有許多職業或業餘歌仔戲團體，只是內容範圍上，已超出本文所及，加上筆者能力不足，無法納入討論，還請各界見諒。</p>
