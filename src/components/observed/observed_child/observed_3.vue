<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統福佬歌謠的傳習，多是針對一般社會大眾所開設的研習班，進入學校的，多半只有特殊課程（如臺語課程）或社團活動。從表8的數據，研習班總計有43筆。需要加以說明的是，一般而言，社區大學、臺北市藝文推廣處等單位開設的研習課程，大致上與學校學期時間相同，也就是說一整年的課程分成上、下半年兩期。雖然同一教師在同一地點開設相同名稱的課程，本文仍視為不同筆資料。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳習計畫，是指受文化部指定為「重要傳統表演藝術保存者」（如唸歌的楊秀卿、滿州民謠的張日貴和恆春民謠的陳英等）所執行的「傳習計畫」。參加「傳習計畫」的「藝生」，必須經過公開甄選，才能成為「傳習計畫」藝生的一員。之後，四年內經過期中和期末考核皆通過者，始得參加「結業考核」，經審核通過者成為「結業藝生」。由於楊秀卿已於2022年6月離世，原由其所執行的「傳習計畫」也隨之終止，2022年下半年僅剩滿州民謠張日貴和恆春民謠陳英兩項傳習計畫。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不論是唸歌、恆春民謠、褒歌，或是車鼓、牛犁歌陣等歌舞陣頭，傳統福佬歌謠並未正式納入學校課程，多數是以社團或臺語課程的名義進入學校授課。普遍來說，由於不屬於正式課程，傳習效益反不如各個單位開設的研習課程。事實上，也僅有恆春民謠、車鼓陣和牛犁歌陣有機會進入校園推廣傳習，唸歌、褒歌皆無。以下從授課教師公開發表的訊息，加上演出、比賽等，簡略整理開設恆春民謠、車鼓陣和牛犁歌陣社團的學校成下表：<sup>10</sup></p>
