<h3>四、客家音樂相關研究</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;研究活動、研究論述與出版方面，吳榮順教授於臺音館《音樂年鑑》專書中，亦有專文論述〈六堆文化區的音樂系統與音樂現況觀察〉。至於張二文則在「右堆客家聚落祭河江及敬義塚祭儀調查研究」持續推進研究工作，並有張二文、吳煬和〈六堆文化資產潛力點評估－六堆作福、三獻禮、敬字風俗〉「六堆客家文化資產工作坊：概念與進路」的辦理。至於資深記者何來美報導「何來美專欄」〈江平成、曾雲珍夫婦 開啟客語廣播先河〉<sup>43</sup>，則蘊藏著臺灣客家唱片發展史中有關傳統客家表演藝術與中國廣播公司苗栗廣播電臺的地方發展歷史，可為客家音樂發展史再添一筆。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;期刊論文部分，有鄭秀美〈客家山歌與文化傳承－客家山歌薪傳師謝其國研究〉（《問哲》第12期），鍾繼儀〈民歌採集運動之餘韻：從1967年中國民歌比賽與全省客家民謠比賽談起〉（《民俗曲藝》第215期）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;學術研討會論文則有蘇秀婷〈日治時期 78 轉唱盤中的採茶戱唱詞初探－以一九一四年錄音為討論〉與李文勳（李菄峻）〈台灣客家戲曲製作的創意思維與策路運用－以《送鄉》為例〉（「2022 戲曲國際學術研討會」）。歐陽嶽〈從客家八音來看客家遷徙路徑〉（「新楊平社區大學客家語言與生活文化學術研討會」）。林曉英〈小說的戲曲編創－以鍾肇政《中元的構圖》為例〉（「2022台灣客家文學研討會」）等。</p>
