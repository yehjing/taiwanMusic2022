<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有關新建場館部分，桃園市政府客家事務局「臉書」專頁公布（2022/06/27）「2023世界客家博覽會」以「Travel to tomorrow天光日个客家」為主題，於8月11日至10月15日在桃園舉行。這是第一屆「世界客家博覽會」，由桃園擔任主辦城市。邀請臺北市、新北市、桃園市、新竹市、新竹縣、苗栗縣、臺中市、雲林縣、南投縣、高雄市、屏東縣、臺東縣及花蓮縣等13縣市簽訂合作MOU。而且將使用桃園會展中心作為「臺灣館」會場，以「扎根與共榮」為主題，串連各地的客家文化，展現臺灣作為發揚客家文化的振興基地。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「2023世界客家博覽會」設有10大展館，分別是：桃園機場捷運「A19站」的主展區，分設有「世界館」與「臺灣館」兩大展館。副展區部分則包含：八德區的「桃園北區客家會館」、龍潭區「臺灣客家茶文化館」、平鎮區「1895乙未保台紀念公園」、新屋區「永安海螺體驗園區」，以及觀音區「崙坪文化地景園區」、桃園機場捷運「A17領航站」的「原民文創園區」、青埔高鐵特定區的「兒童美術館」，以及大園區「橫山書法藝術館」。客委會將與桃園市聯手推辦「世界客家博覽會」。意圖呈現多元的客家文化，「客家音樂」將是其中一環。該博覽會規劃兩大主題館，「世界館」用以展現世界客家的在地化與多樣化經驗，「臺灣館」則反映臺灣客家的典範特色。<sup>40</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「2023世界客家博覽會」10大展館中的桃園「永安海螺館」，適合展現海洋客家，榮興《海東奇逢》巡演製作的宣傳片<sup>41</sup>即是在海螺館與永安一帶海邊取景，分別展現「礫岸」與「沙岸」景觀，使劇本中設定的文資地景「石滬」與「海洋客家」與表演藝術有所連結。此外，2021年景勝戲劇團《1895乙未烽火》首演即選定在平鎮區1895乙未保台紀念公園演出。而桃園「桃園展演中心」一地，亦有國立臺灣戲曲學院青年團《三山國王&mdash;韓文公》，以及音樂時代演繹鄧雨賢生平故事的「台灣音樂劇」《四月望雨》之演出。至於「臺灣客家茶文化館」開幕系列活動，邀約有榮興客家採茶劇團、榮英客家戲劇團的戲曲表演，陳家班北管八音團開幕演奏與戲曲後場伴奏。<sup>42</sup></p>
