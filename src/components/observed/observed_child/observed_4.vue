<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂相關活動記錄之「類屬」，有思量斟酌之需。以歌謠為例，民間客家歌謠班、傳統歌謠合唱團之教學活動與競賽交流（如各縣市民謠社團組織、地方縣市政府客家委員會、客家事務局等單位補助者）歸於「推廣」一類，若為正式劇場空間之成果表演、年度公演則納入「展演」，若是尚未舉辦之公部門公告（或預告）則納入「政策」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;計畫採樣、紀錄仍以觀察「展演」為主，包含官方與民間兩種場域平臺，並以「客家八音」、「客家歌謠」、「客家（採茶）戲曲」三者為計畫重點觀察領域，並包括專業從業者以及業餘同好（以前者為重），並延伸部分跨域演出之觀察。是以採取雙重指標為綱目，以上列「客家八音」、「客家歌謠」、「客家（採茶）戲曲」三樂種（含戲曲後場音樂）之展演，輔以計畫各類屬，將年度活動取樣評介如下。</p>
<p><strong>一、客家八音</strong></p>
<p><strong>（一）與儀式、民俗活動結合</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;客家八音強調「禮樂合一」，經常擔任傳統典禮、儀式的伴奏，輔助儀式之進行。至今仍可見到古禮中八音樂團的參與，甚至有網路新平臺的結合。例如農曆正月20的「天穿日」，桃園市政府在「2022桃園天穿日－祭天祈福儀式」，因疫情採取「線上直播」方式將古禮祭天儀式對外展示。<sup>10</sup>儀式當天的伴奏樂團為桃園在地八音團隊的「和成八音樂團」擔任。至於高雄縣六龜區新民庄勸善堂「拜祭河江伯公及送字金紙灰」，照例於農曆3月初7在荖濃溪畔辦理。並有南部在地客家八音樂團隨行伴奏，為活動進行伴奏的是美濃八音團，樂師編制6人，嗩吶主吹為謝宜文。<sup>11</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;又如農曆6月24日的大溪普濟堂關聖帝君聖誕慶典亦持續辦理慶典活動。「大溪普濟堂關聖帝君聖誕慶典」已登錄為桃園市文化資產「民俗」（類別）。屬於北臺灣關公信仰具獨特性的一脈，為大溪區與復興區之信仰，本為鸞堂屬性，後轉為廟宇。當年度（2022）遶境活動有35個社頭參與，八德「和成北管八音團」再次受普濟堂邀請，以「普濟堂八音團」為名義參與其中。<sup>12</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;義民祭是客家族裔的特色祭典，地方各縣市客家聚落重點寺廟已有長期辦理「義民祭」之慣俗，此乃展現在地民間力量匯聚的重要標的，當年度輪值之庄頭（角頭）莫不蓄勢待發。或有私人許願、還願而畜殺豬公，以示敬誠。新竹縣新埔褒忠義民廟在「義魄千秋2022義民祭在新竹縣－義民祭開幕式系列活動」中，結合客委會客家八音相關計畫之執行，將前一年（2021年）分散不同地點辦理之八音表演，調整為規模較大的「全國客家八音大匯演」配合義民祭擴大辦理。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8月13日的八音匯演活動配搭新埔義民廟的義民祭系列活動，增加曝光度。當日計有9個八音團體參與匯演活動，包含：關西祖傳隴西八音團（6人）、田屋北管八音團（11人）、吉聲八音團（6人）、苗栗縣益揚軒八音團（10人）、新埔內立同樂軒（8人）、新竹北埔八音團（8人）、和成八音團（8-10人）、美濃客家八音團（4人）、深井新興軒北管八音團（7-8人）等，北部地區的八音團佔多數。</p>
