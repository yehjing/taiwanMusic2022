<p><strong>三、客委會補助專案資訊以及相關田野觀察特徵表現</strong></p>
<p><strong>（</strong><strong>1</strong><strong>）區域分布與地方特色</strong>：集中於雙北都會（臺北市與新北市），以及客籍人口相對集中、有客庄聚落之縣市（a. 桃園、新竹、苗栗，b. 臺中、南投，c. 高雄、屏東，d. 花蓮、臺東），這部分與客委會2017年公告之「客家文化重點發展區鄉（鎮、市、區）」的縣市區域頗為一致。<sup>5</sup>再者，上列縣市之補助活動同時體現具區域發展特色，例如客家傳統戲曲劇團相對集中於北臺灣的桃園、新竹、苗栗三縣市，印證「四縣山歌，海陸齋」之諺與藝術之間的關係，北臺灣使用四縣客語的重點縣市仍有傳統演劇的市場空間，且「山歌」之傳唱仍以四縣腔客語為主，無論地域南北，且海陸客語佔比較高之縣市亦然；至於雲林縣則為客家布袋戲重點縣市，兼具「（詔安）客家」與「布袋戲」，多以「閩客雙語」方式呈現。</p>
<p><strong>（</strong><strong>2</strong><strong>）樂種界線與音樂表現</strong>：客家歌謠班為數頗多，集中分布於客家聚落與都會，部分團體為合唱團類型，以業餘同好屬性為主，為推廣客語流播和客家歌謠，曲目多兼具傳統與流行，語言更不限於客語。八音團亦集中於客家聚落而以桃竹苗和高屏為多，部分樂師為北管或國樂基底，或是具備演奏樂器之基礎或興趣，因而部分專業的八音團會在團名強調「北管八音」，以示具有彈奏北管與八音兩種不同樂種（風格）的能力，具有「跨界」色彩。</p>
<p><strong>（</strong><strong>3</strong><strong>）專業主業與同好推廣</strong>：表意團體與個人的參與型態多元，其專業/業餘，主業/兼職之別，往往連動所能參與之活動屬性與規模差異，公部門資源之委託、補助經費差異亦往往有所反映。其次，縣市立案之在地團體對所屬縣市活動的參與有一定的曝光度，專業團隊著重在「藝術性」，推廣型社團則側重在「參與」。</p>
<p><strong>（</strong><strong>4</strong><strong>）建構在地品牌新傳統</strong>：直轄市、地方縣市政府所辦活動常見以「文化節」、「藝術祭」命名，持續辦理較易形塑、經營為在地品牌，建構為新的傳統。例如各縣市入選過客委會「客庄十二大節慶」<sup>6</sup>的活動，已有從賽事轉為「文化節」者（竹東客家山歌比賽）。再者，大規模活動之舉辦，往往匯集多層次、多向度的表演藝術參與其中，事前事後的公關行銷（曝光）已然成為推銷活動的重要步驟和手段。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了上述的補助案之外，客委會自成立以來，其招標案件數量與金額統計，除了2016與2017這兩年稍微偏低（約5.6億），2012年相對較高（約14.5億）之外，大致皆在8至10億之間。自2020年疫情開始以來的最近三個年度，或許因為疫情之故，整體年度決標總金額呈現微幅下滑、緊縮的趨勢，從9.39億、8.57億，至2022年降至6.73億之數。</p>
