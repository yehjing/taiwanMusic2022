<h3>壹、前言</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當各型嚴重特殊傳染性肺炎（COVID-19）病毒於世界各國廣為流傳，造成重大災情並逐漸轉向解封開放之際，臺灣是在2022年開始遭遇病毒的降臨肆虐，快速引發在臺數百萬人染疫的是「Omicron」第二型，高風險變異株使得疫情詭譎多變，相關防疫單位與其政策僅能嚴陣以待。然而，不僅世界各國因此重創，嚴守將災害降到一定程度的臺灣，各行各業仍然難以避免受到影響，非每日生活「必需」的表演藝術自然也難以置身事外。隨著疫情幾波高峰，或有決斷停演者，亦有延期演出者，無論團體或個人被動的依著疫情發展與政策走向而隨遇而安。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年3月底、4月初，臺灣疫情爆發流行，染疫人口數大幅成長，5月中下旬更來到通報人數（法定通報與擴大監測）高峰，次高峰為9月中、下旬，至10月15日官方統計數字，臺灣染疫人口累計已破714萬人。<sup>1</sup>至11月27日凌晨的官方統計數字，臺灣染疫人口累計已破825萬人。<sup>2</sup>臺灣整體染疫人口比例雖然短期內迅速攀升，然而也已緩步往「全體免疫」方向前進，全面解封口罩令之日或將不遠，各個產業活動的復甦指日可待。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;資料來源：衛福部疾管署<sup>3</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;連續三年的疫情，使得表演藝術產業與從業人員的表演「轉型」加速，尤其是網路崛起的新型態露出平臺，政府紓困方案亦接受以「線上直播」方式的型態。</p>
<h3>貳、疫情之下的政策支持與民間活動概況</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統表演藝術有其專業性，然在當代社會中卻往往是「非主流」的小眾（分眾）藝術。政府公部門文化政策與經費挹注，對民間演藝團體與社團組織活動幫助頗大，尤其是文化部與客委會，透過不同層級、網絡有一定程度的支撐力道。公部門的經費一般分為委託案與補助案兩種，以中央的客委會公告資訊為例，其補助對象包含縣、市政府文化相關單位以及專業學校、民間團體和個人等，觸及的層面與類型頗寬，有講究藝術性、專業度者，亦有注重參與、屬推廣性質者。從計劃與活動規模觀察，中、大型活動的補助經費大致從數十萬至數百萬元不等，小型活動則多半補助數萬元至十多萬。以下，查詢客委會已公開（截至年底公告）的去年度前三季補助彙總資料，就與「客家傳統音樂」較相關的團體或個人以及其所從事之藝文活動型態或名稱，查詢、篩選較為相關者，<sup>4</sup>摘要如下：</p>
