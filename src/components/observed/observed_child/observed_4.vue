<h3>五、其他</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統表藝在當代呈現小眾與分眾的發展趨勢，資深藝術從業工作者的凋零，警醒創作、表演者急起直追、承繼傳藝的必要。八音樂師的老成凋零，有陳家八音團資深成員謝顯魁（1943-2022，陳慶松之徒）過世，其「挨弦」技藝高超，客家八音結業藝生吳岳庭頗得其真傳。再者，前一年鍾彩祥逝世，2022/01/16其告別式見於客委會新聞。<sup>44</sup>戲曲方面，曾有多部客家戲作品《霸王虞姬》、《天上．人間．桃花源》的編劇院士曾永義教授（1941-2022）過世，其作品音樂會「吟詠性情．歌樂相得－詩詞曲歌唱跨界音樂會」為臺北市立國樂團主辦「精彩TCO系列」節目之一，其中有客家大戲摘段，由江彥瑮、蘇國慶擔綱演唱。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立傳統藝術中心第三十三屆傳藝金曲獎，部份獎項有所調整。除取消「宗教音樂」獎項，調整以「傳統音樂」為標榜，另有「藝術音樂」與「跨界音樂」為獎目。客家傳統音樂部分，榮興客家採茶劇團《天上．人間．桃花源》一劇入圍「最佳傳統表演藝術影音出版獎」，藝術總監鄭榮興亦以《天上．人間．桃花源》與《花囤女》兩劇獲「最佳音樂設計獎」入圍。此外，陳芝后主演榮興劇團《花囤女》一劇，詮釋女主角年輕與年老兩個不同年齡層的表演反差和層次處理，唱腔動聽且富有技巧，和小咪（陳鳳桂）的《天鵝宴》一起榮獲該年度戲曲表演類的「最佳演員獎」殊榮。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於客委會2022年「客家事務專業獎章」與傳統藝術（客家音樂相關）相關的獲獎者，包括「一等」：溫福仁（客家八音樂師）。「二等」：潘玉嬌（文化部「亂彈戲曲」保存者）、王慶芳（文化部「亂彈戲曲」保存者，採茶戲說戲先生）。「三等」：謝顯魁、賴義發、謝旺龍與彭宏男（客家八音樂師）等。<sup>45</sup></p>
<h3>肆、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;連續三年嚴重特殊傳染性肺炎（COVID-19）疫情，使得表演藝術產業與從業人員的表演「轉型」加速，尤其是網路崛起的「自媒體」、「串流平臺」等新型態露出模式，政府相關文化單位的紓困方案亦見接受以「線上直播」方式的型態作為成果。全球性疫情時期帶來的種種衝擊，確實明顯改變當代人原有的生活形態，以往的經驗世界有所鬆動。在此情況之下，傳統表藝產業界能否順勢轉型、提昇，或者能從土地再次出發，因自覺而能從更多方面獲得嶄新的動能力量，這既是時代變數的新挑戰，也是趁勢蛻變的關鍵時刻。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，客家傳統音樂生態圈「眾聲喧嘩」的復甦景象背後確實藏有隱憂。客家傳統音樂有自己的「音樂語彙」，這也是為何過去的專業傳習十分仰賴「口傳心授」、師徒「手把手」傳教學習的緣故。以客家傳統戲音樂為例，各個劇團參與公部門「文化場」之時，多半有意識的使用相當份量「九腔十八調」曲調的抉擇背後（甚至盡刪劇團常見「流行歌」穿插入劇的作法），反映的正是不同音樂系統背景的後場樂師的參與，內裡或有傳統曲調「獨特性」消融之隱憂。正向觀察這樣的現象，多元、各方的「參與」共榮了客家傳統戲曲音樂，使之有更多種的表現形態，但採用「九腔十八調」為唱段主體的外包裝之下，是否仍能在跨劇種、跨樂種的參與過程中穩握劇種音樂之分別？「錦上添花」或許是音樂內涵擴充、延展多元表現的發展契機，然而是否可能致使該種客家音樂的藝術核心特質、獨具特色的某些作韻方式與音樂風格（包含演奏與演唱）消融流失？這方面的發展尚需後續觀察。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，值得持續關注的是，教育體系與官方文化單位聯手創造的新世代專業表演者（專業藝術教育體制與人才培育支援體系，如：臺灣戲曲學院與臺北藝術大學、臺灣藝術大學、臺南藝術大學等相關表演科系，以及文化部、客委會與地方文化局、客家事務局（委員會）等之「專業」傳習和培訓），以及民間客庄聚落在地青年力量的崛起、串聯和相互凝聚支撐（如：新埔聯庄祭典中所見在地客家青年與其新形式之「社造」作為的深耕學習和民俗慶典參與）。儘管疫情無情，但在田野現場中可觀察到，前述兩者宛若兩股旺盛伏流，隱然在與現今時代趨向對抗著，同時也展示他們正以某種新的姿態承續過往、迎向未來。</p>

