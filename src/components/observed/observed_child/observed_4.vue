<p><strong>（二）傳習成果</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部文化資產局的「客家八音」，當年度重點紀錄除了文資傳習與研習成果演出之外，另有國指定保存者鄭榮興的授證儀式。2022年9月24日典禮在臺中文化資產園區舉辦。典禮中由鄭榮興率領苗栗陳家班北管八音團與文資局數屆傳習藝生（含結業藝生與修業藝生）展現傳習成果有成。挑選《糶酒》組曲演奏，設計為「三段式」輪番換位演奏（組曲演奏中，各樂手將置換於不同位置，全曲職司數樣八音樂器），先由結業藝生擔任前兩段嗩吶手主吹位置，行進至最後一段再由保存者鄭榮興擔負嗩吶主吹（前兩段分別擔任「打擊」與「挨弦」位置），展現民間八音樂團長年耕耘的表演實力，同時彰顯與官方文化單位文化資產局接棒傳習的合作成果。</p>

