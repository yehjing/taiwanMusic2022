<p><strong>（三）文化資產保存</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於苗栗「客家山歌」保存者（實踐者）賴仁政2021年登錄，持續推廣客家山歌，其領導之「大襟客客家文化藝術協會」持續辦理山歌徵詞比賽與歌唱大賽，以「營造客庄生活環境」為宗旨推動傳統客家歌謠賽事。2022年該比賽徵詞組「特選」有傅秋英、劉燕玉兩位，「優選」則為傅宏井等十名，佳作三名。歌唱組「平板組」前三名為彭瑞鳳、羅碧珍、陳紹杞，「老山歌組」前三名是許麗芬、劉桂花、陳建居，「山歌子組」前三名為謝銘壽、劉秀蓮、黃寶萱，「小調組」前三名為鄭阿香、賴素芳、尤秀菊。另有「兒少組」與「挑戰組」。<sup>27</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，苗栗縣政府亦於2022年10月舉辦「苗栗縣無形文化資產授證典禮」肯定縣內已登錄的多位保存者，此外，其中與「客家音樂」較相關者除了「客家山歌」保存者賴仁政之外，尚有黃鳳珍（2020年苗栗縣登錄「客家採茶戲」保存者）、王慶芳（2018年苗栗縣登錄「亂彈戲曲」保存者）、彭繡靜（2019年苗栗縣登錄「亂彈戲曲」保存者）、謝顯魁（2018年苗栗縣登錄「客家八音」保存者）、鄭榮興（2019年苗栗縣登錄「客家八音」與「採茶戲後場音樂」兩項保存者，2021年文化部指定「客家八音」保存者）等。<sup>28</sup></p>
<p><strong>三、客家（採茶）戲曲</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現階段臺灣客家戲曲劇團仍集中立案在桃園、新竹、苗栗三縣市，不過亦有部分劇團為了爭取不同縣市公部門經費資源挹注，而有母團、子團（一團、二團）的分團現象（非僅是為了「大月」或特殊節日之多方戲約而有分班需要而生），因而呈現出幾乎可謂是同一核心班底而有多張劇團牌照的現象（分團立案地多在選定母團所在縣市之外的其他縣市，且集中在雙北市與桃園、新竹、苗栗）。相對於前場演員，劇團後場樂師成員組成方面，則呈現「非固定」卻又「相對固定」之合作模式，主因是民間傳統演藝團隊無法聘任專職樂師（除非具家班屬性或有優先配合之合約、默契），且具有一定專業能力的樂師亦慣於遊走數個演藝領域或多個劇團之間，故而常見以「特聘」邀約方式合作。對劇團經營者而言，這種經營模式相對降低人事成本壓力，且相對可以將重心和餘裕放在穩定前場演員上，因而形成跨劇種（劇團）支撐某些動態組成之「後場（樂團）」的情形。從「互惠」的角度來看，民間八音班以嗩吶手領班，而且主力樂師彼此間也有相對固定之邀約、合作關係的情況有異曲同工之妙。</p>
<p><strong>（一）樂師與樂隊</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;民間劇團樂師方面，各團後場依然是民間與學院兩類出身，然而戲曲後場二十至三、四十歲左右的年輕樂師多有進入正規教育體系學習的背景，且不乏國樂系或臺灣戲曲學院戲曲音樂背景者，不同劇團因前場演員的表演基底、後場樂師的養成背景以及排戲者所講劇目的曲調屬性，亦有多種風格、路線的呈現，劇團亦會評估演出性質或戲金多寡而有不同編配（例如區別中大型「（文化場）公演」與日常「民戲」）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者於2022年的現場觀察，當今客家戲曲劇團的後場樂隊樂師（包含外部特聘），榮興劇團主要為鄭榮興培育的後進為主體，文武場領奏包含吳岳庭、蔡晏榕，近年常見鍾繼儀、莊鈞復、古雨玄等擔任領奏，主要是戲曲學院戲曲音樂學系客家戲組背景，此批後場樂師橫跨客家採茶戲、亂彈戲曲與客家八音數個傳統表演藝術，分別參與榮興客家採茶劇團、慶美園亂彈劇團與陳家北管八音團等，具有相當的演奏實力，為傳統文武場編制外加部分西洋樂器，大型製作慣以樂團編制規模配器演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另有一批樂師為臺灣戲曲學院戲曲音樂學系歌仔戲組，如景勝戲劇團、紫玄居或紫園延聘的周煌翔、周毓書，新樂園的郭亦維、郭亦翔兄弟，兼跨歌仔戲與客家戲兩劇種。前者，周煌翔以胡琴主弦，周毓書專攻電子琴，兩人亦擔任音樂設計，除了九腔十八調，兼用亂彈戲曲調與部分歌仔戲曲調，常見新編曲。郭氏兄弟則以文場、武場分別專攻，劇團的打擊樂器兼具傳統與採用爵士鼓。此外，另有民間培育出身的樂師，多為家傳或是在採茶戲之外另具備演奏亂彈、四平、歌仔戲的演奏基底。例如徐先亮、林保木、朱作民、黃建銘等。亦有客家山歌底參與劇團演出者，如謝其國、劉羅照參與松興戲劇團。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現今民間外臺戲演劇，常見樂隊編制多為「一文一武」（常見電子琴加上傳統武場或局部爵士鼓，北管曲牌亦由電子琴演奏），亦有「兩文兩武」者，仍以前者為多，劇團老闆會視戲金多寡調度人手。若是進入正式劇場演出，則往往以「樂團化」編制展現豪華陣容與企圖心。「樂團化」多是傳統樂隊外加樂器，包含西洋樂器或是他種傳統樂器，以增加聽覺整體層次，常見增加低音樂器（如低音胡琴類或洞簫），或是彈撥類樂器（如中阮、三弦、揚琴等），或是增添具有表現力的、不同調高的各式笛子。現階段榮興客家採茶劇團、景勝戲劇團、國立臺灣戲曲學院客家戲學系與學校青年團等，在重要演出製作，常見以10人以上的編制呈現。</p>
