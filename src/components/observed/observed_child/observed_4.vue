<p><strong>（二）社團推廣</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市政府客家事務委員會自2004年（民93）以來，開始補助北市立案的非營利性客屬社團辦理客家歌謠與客家語研習，111年持續辦理推動客語的相關藝文活動補助，其中「客語教育中心客語傳承及文化推廣成果發表觀摩」假大安高工學生活動中心辦理，可見民間團體於社區推廣之成果，111年以「首都客韻」為主題展現。<sup>24</sup>從歌曲擇選觀察，可見傳統客家歌謠的推廣在現代都會城市裡，「客家歌謠」教學者採取相對以往【老山歌】、【山歌子】、【平板】與「小調」類等傳統曲調，教唱歌曲選擇傾向通俗化、流行化與多元交融的推廣策略，新創客家流行歌曲比例增加，亦見其他語言的歌曲在內，表演活動強調「參與」同歡，因而多為合唱形式呈現學習成果。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市客家事務委員會長年輔導、補助民間客家歌謠班社團活動，並擇選團隊參與臺北市客家義民祭表演，使得在地社團活動頗為活躍，以111年補助的客家歌謠班數量即有37團可見一斑。<sup>25</sup>例如星韻創意合唱團參與「2022臺北客家義民嘉年華－義民35『客潮』」，即以客語演唱《拜伯公》等曲為基底，設計以情節帶動曲唱，由不同年紀的表演者輪番登場。再者，申請補助的單位大抵為臺北市來自其他縣市之同鄉會、北市各區里之社區發展協會、客家相關民間社團型之協會、客家會，以及婦女會、長青會及合唱團等，僅少數以山歌團（如台灣客家山歌團）、歌劇團（如全球客家聲韻歌劇團）為申請單位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在都會推廣客語與客家歌謠並進的宗旨之下，臺北市民間歌謠班社團林立。長年參與的學員亦有相當的基本盤，並有互相聯繫的網絡。這些有賴於傳教歌謠的指導老師，因而同一教師教授數班的情況不算少見。依據去年度（2022年）臺北市客語教育中心教授客家歌謠的師資群大抵有：薛能章、陳忠義、溫淑玲、徐韻絜、鍾年修、陳寀玲、黃月玫、廖宇盟、陳美玲、呂錦明、曾梅穀、徐秀菊、梁秋香、張彩美、劉玉芳、蔡梅秋、劉秀蘭、羅思琦、黃珮舒、王羽馨、蕭佩茹、溫貞玲、陳玫玟、曾桂英、羅茵茵、陳昶均等，傳授曲目大抵傳統與當代流行兼具，依師資背景而比重不一，不過流行歌曲的比例有漸增趨勢。<sup>26</sup>當年度37個班級上課地點區域分布與數量：大安區15個班，文山區6個班，松山區4個班，北投區3個班，中正區3個班，信義區2個班，中山區1個班，士林區1個班，南港區1個班，內湖區1個班。教學空間多為當地的客家會館相關空間、社區之區里民活動中心、國中小學教室等。基本上多固定每週上課一次，一次兩小時。</p>
