<h3>壹、前言</h3>
<h4>一、研究範圍界定</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域的涉及對象，包括太魯閣族、卑南族、阿美族、雅美族（又稱達悟族，下文依行政院原住民族委員會用法稱「雅美族」）、撒奇萊雅族、賽夏族，共六個臺灣原住民族族群。除賽夏族居住在新竹縣與及苗栗縣一帶的山區以外，其他五族為臺灣東部的原住民族群。雅美族傳統領域在離島蘭嶼。太魯閣族、卑南族、阿美族及撒奇萊雅族則主要分布於花蓮到臺東之間的區域。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;太魯閣族以花蓮縣秀林鄉、萬榮鄉、卓溪鄉與部分的吉安鄉為居住區。過去曾被劃分在泰雅族、賽德克亞族之內，因此風俗習慣與歌謠舞蹈，可與泰雅族、賽德克族同屬一個大的體系，但操作與形式上的細節則各有表現。太魯閣的音樂，除歌謠外，也以木琴及口簧琴聞名。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;卑南族居住在臺東縣卑南鄉及臺東市一帶，舊有「八社」之稱，然近年已有新的部落成立。卑南族的傳統歌謠以悠遠婉轉見長，亦具有複雜的多聲部組織型態。根植於傳統音樂而新興的創作與演唱活動更是十分發達。因鄰近臺東阿美族的關係，兩個族群傳唱許多共同的歌曲。每年陽曆年底的年祭是卑南族最重要而盛大的傳統祭儀活動，其男子獵祭歸來的盛大迎接儀式以及除喪活動為特色。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;阿美族主要居住在花蓮、臺東的縱谷及海岸地帶，較偏遠的山區也有古老部落。由於分布區域廣，人口眾多，各地方的風俗習慣、語言、服飾甚至歌舞系統都有所差異。如果用音樂型態的角度來看，大體上可以劃分為花蓮地區以及臺東地區兩個部分。臺東地區以過去音樂學界所謂的「自由對位式」複音歌唱聞名，花蓮地區的歌唱形式則很多樣。每年夏季舉行的豐年祭是歌舞活動最為熱絡的傳統祭儀活動，也為外人所熟知。如果用豐年祭的儀式結構來劃分的話，則又可以劃分成北部阿美、中部阿美、南部阿美，各自豐年祭舉行的方式不同，歌舞內容也不同，不過都有統稱為malikuda的大會舞。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雅美族的傳統領域是蘭嶼島以及其周圍的海洋。在島上有東清、紅頭、朗島、野銀、椰油、漁人六個雅美族部落。雅美族雖然是音樂學家所稱沒有使用樂器的族群，然而廣泛運用滑音及大幅度顫音的歌樂卻獨具特色。在飛魚相關的季節、在新房落成、在大船下水、在涼爽的夏夜、在捕魚的船上、在田間的農務中，都有雅美族的歌謠傳誦。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;撒奇萊雅族是花蓮阿美族的鄰居，因百餘年前達固湖灣事件與清軍的流血衝突，使得過去有一段很長的時間裡，撒奇萊雅人藏身在阿美族當中，也因此許多方面有「阿美化」的危機。近年來積極的文化「復振」以重塑自我認同，是撒奇萊雅人最重要文化使命，內容包括祭典儀式以及歌舞。豐年祭、火神祭是其最受人矚目的祭典。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽夏族居住在新竹縣五峰鄉、苗栗縣南庄鄉與獅潭鄉一帶。由於歷史上與鄰近泰雅人、客家人的交流頻繁，使得賽夏人保護自我文化的意識相當強烈。雖然現在少有機會使用一般日常歌謠，但禁忌性高、詞曲內容長達十餘篇章、組織結構嚴謹的矮靈祭歌，是臺灣原住民最珍貴的音樂文化資產之一。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除上述各族群的傳統生活領域外，在經濟生活快速變動的情況下，原住民有大量人口移居西部或都會區而落地生根，形成新的原住民社群，並且舉行自己的祭典活動。這些社群也是本子領域觀察與記錄的對象。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域雖在整體音樂年鑑的領域劃分上屬於「傳統音樂」的部分，但身處二十一世紀的今天，「傳統」實際上難以劃界與定義。過去所認定的傳統音樂，經常與當代音樂活動融合在一起而無法分割。半個多世紀前開始盛行的所謂「流行歌曲」，如今也根植於原住民的生活當中而不可分離。因此本子領域在觀察與記錄上，並不會刻意排除非「傳統」的音樂活動。</p>
