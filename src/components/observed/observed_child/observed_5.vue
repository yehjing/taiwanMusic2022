<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;或許是受到嚴重特殊傳染性肺炎（COVID-19）的影響，本子領域今年度所觀察、記錄的國際與兩岸交流活動，僅有兩件。恰好一件屬國際交流，一件屬兩岸交流。其一是5月份雅美族團體赴歐演出；其二是7月份的「湘臺少數民族文化交流暨『遇見非遺』兩岸非物質文化創意活動」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雅美族在歐洲所參與的活動，全稱是「蛻變：理解及誤解的儀式」（Transformations: Ritual of Mis(Understandings)）。該活動由稱為「國際文化平臺協會」（International Cultural Platform）的單位所主辦，該單位為維也納音樂暨表演藝術大學藝術演奏與研究人員所組成，其中包括長年關注雅美族音樂的臺灣學者林維亞。林維亞也在雅美族的訪歐演出活動中，扮演策畫、接洽與聯繫的重要角色。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活動在5月20及23日，分別於奧地利維也納世界博物館（Weltmuseum）及匈牙利音樂之家（Hungarian House of Music）舉行。參與活動的雅美族人，主要是團體「海洋之聲Sound of the Ocean」。該團體中，有擅長古謠吟唱的七旬耆老，也有對舞蹈抱持濃厚興趣的女青年。他們的演出節目包括：〈Ipapazaka四門房落成長子套藤圈古調〉、〈Anood敘事歌〉、傳統頭髮舞、蚯蚓舞。耆老顏新添所演唱的古謠，以極富傳統特色的大幅度滑音與長顫音，震撼西方聽眾的聽覺經驗與感受，女子的頭髮舞與蚯蚓舞，也獲在場觀眾的好評。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「蛻變：理解及誤解的儀式」並非單純的歌舞展演。活動還包括了：半即興式的音樂會裝置藝術作品、紀錄片、攝影展、多媒體與聲響裝置，以及以蘭嶼田野調查及歷史錄音為基礎的音樂創作發表。綜觀此次活動，乃是一場以雅美歌謠、生活與文化為核心的多元文化展示與對話，嘗試以跨領域、跨形式的方式，探討當代藝術形式的發展或互動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「湘臺少數民族文化交流暨『遇見非遺』兩岸非物質文化創意活動」，則是臺灣原住民族與中國大陸少數民族的交流。活動的參與者，分別是卑南族的南王部落，以及中國侗族。由於當時疫情仍然緊張，兩岸交流的管道並不明朗，因此活動以網路線上轉播的方式進行。南王部落在活動中，呈現除草完工祭婦女敲警訊鈴、男子英雄史詩、宴會舞蹈等傳統與當代的歌舞。</p>
