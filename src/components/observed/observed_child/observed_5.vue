<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據行政院原住民族委員會於2021年10月5日公布的《原住民族委員會111年度推展原住民族長期照顧－文化健康站實施計畫》（下稱實施計畫），為重視原住民的健康權，增進原住民的福利與醫療資源，在原住民社區設置「文化健康站」（下稱文健站），提供原住民長者照顧服務。服務對象主要是55歲以上的原住民獨居或身心障礙等弱勢長者。據臺東縣原住民族行政處111年的統計，該縣各鄉、鎮、市總共設立了103處文健站；花蓮縣109年的統計資料也達90處。上述數據所涉及的雖不止於本子領域的觀察對象族群，但可藉以窺知文健站的設置密度，以及其對目前原住民長者日常生活照護的重要性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文健站的服務項目，載於《實施計畫》第5頁，主要在於健康照顧、提供餐飲、日常關懷。不過有一項特別值得注意：「延緩老化失能活動（活力健康操、肌力與體能訓練、文化藝術課程、心靈課程、文化音樂活化腦力）」。臺灣逐年進入老年化的社會，高齡化的情況下長者失能的風險也提高，政府因而推動「長照2.0」的政策。文健站此一服務項目，並且認為運動、藝術特別是音樂活動，能有助於延緩老化失能。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域的觀察記錄中，就有多筆與文健站的長者歌舞活動有關。2月間，臺東縣政府舉辦「老幼共學．大手牽小手&mdash;部落文化健康站歌唱比賽」。阿美族的都歷部落以精神抖擻的傳統歌舞贏得第二名的佳績。年底則是各文健站的成果發表會。10月19日，原民會依例發布112年度的文健站實施計畫。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了以歌舞活動豐富長者的生活、促進長者的健康外，文健站也身兼文化上的功能。許多原住民部落因長期人口外移而遭遇文化斷層的困境。文健站形同當代新型態的長者組織，老年人得以聚會交流，喚起往日生活的記憶，重拾歌舞活動；而照服員照顧長者的同時，長者也將歌舞傳承給年輕的照服員（Bunkiatr Katatrepan 2022年4月29日報導）。臺東縣成功鎮都歷阿美族的文健站，則規畫以「老幼共學課程」的模式，進行傳統海祭，參與的國小學童皆學習傳統儀式文化而產生高度興趣。</p>
<h3>玖、各族群分析</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;太魯閣族的部分，本年度觀察記錄了不少的活動。主要是有駐地報導人特別注意該族群活動的關係。於太魯閣臺地及大禮部落百合屋不定期舉行的音樂演出，值得持續關注。其中太魯閣臺地的演出有較多傳統的成分，除歌謠外，還包括器樂如口簧琴、木琴等。其他的展演活動，多半以當代流行為主體。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度有關卑南族的記錄較少，很可能並不是本身活動確實少，而是受限於資料來源。而且有許多卑南族樂人參與的活動，在統計上被列入跨族群的部分。像9至11月所舉行一系列與原住民黑膠唱片有關的展覽與演出，卑南族的樂人與音樂就佔有重要的地位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;阿美族的相關統計數據，在各種活動類型上都領先其他族群。一方面是因為阿美族本身部落、人口眾多，一方面又樂於以歌舞展現文化特色，甚至以樂舞建立文化認同。今年有數位阿美族歌手入圍金曲獎。此外，文化健康站的長者照護，阿美族社區也明顯採用更多歌舞活動。阿美族音樂相關研究方面的成果也相當豐碩，並且涵蓋各種研究角度。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雅美族的展演，比例上以當地社區型的活動較多。或許是地處外島，要頻繁到臺灣舉辦演出，並不是那麼方便。同樣地外界的關注與報導也比本島的族群要來得困難。但值得一提的是5月間的赴歐演出，其時疫情正在發展，能在研究者的牽線安排下完成國際交流活動，實屬不易。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;撒奇萊雅族的統計資料，顯示該族群的音樂活動偏向於祭儀與傳習類，展演則較少。在本子領域所觀察的6個族群當中，撒奇萊雅族過去長期被視為阿美族，文化上也與阿美族之間大量交融。但撒奇萊雅族人仍努力尋回屬於自己的文化特色，祭典儀式故成為音樂活動的核心，傳習推廣上也以「復振」為主要訴求。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽夏族的民族性強、族群團結，但表現上相對較為內歛，很少向外界展現歌舞文化。尤其矮靈祭的禁忌很強，其祭歌也絕不輕易示人，只能在祭典活動期間使用。不過今年度完成製作並出版的專輯《守護賽夏天梯》，完整收錄祭歌音樂與歌詞，乃原住民音樂的重要記錄。</p>
<h3>拾、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;總體而言，本子領域所觀察的原住民族群，在今年度的音樂活動大體上已脫離過去兩年疫情帶來的影響，恢復往年熱絡的盛況，僅國際與兩岸的樂舞交流活動數量較少。運用國家文化資產的資源來維繫古調的傳承、歌舞活動在長者日常照顧當中扮演的角色，是未來可以持續關注的焦點。融入傳統意味而又兼具當代意涵的原住民樂舞創作已十分熱絡，成為二十一世紀原住民樂舞活動的常態。音樂年鑑的子領域類別劃分實可擺脫「傳統」、「現代」的框架束縛。再者，樂舞的製作與策展，或是學術研究，樂見越來越多原住民的投入，展現原住民的主體性。未來音樂年鑑有關原住民類別的觀察、記錄與評論，也應思考由原住民來主導。</p>
