<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不意外地，阿美族音樂仍是研究者最關注的對象。值得一提的是，藍天壕（Raway Huiciang）藉由研究臺東縣關山鎮新福阿美族部落的pakarongay訓練營，研究當代原住民的文化復振，依循「玩」的論述脈絡，有細膩而精采的觀察與討論。阿布伊．布達兒本身是臺東杵音文化藝術團的成員，以馬蘭阿美族macacadaay（即過去所稱的南部阿美族複音歌謠）為對象，討論馬蘭部落的音樂聲景。淑奈．谷拉斯以阿美族音樂為材料，設計一場畢業音樂會，論文中並將之與羅馬尼亞與俄羅斯的音樂案例進行比較，將原住民音樂置於民俗文化意識的視野之中。越來越多原住民青年學者投入自身族群的研究，真是令人欣喜的趨勢。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外林婉真與郭淑禎的論文，探討的是過去少見討論的撒奇萊雅族與太魯閣族。林婉真記錄了撒奇萊雅族撒固兒部落的豐年祭，除呈現祭歌的樂譜外，也說明了視覺（影像）記錄的手法。郭淑禎的博士論文則是透過傳統歌謠的採集、翻譯、解釋，探討太魯閣族的農業、狩獵、自然生態、動植物、天氣等在地知識，以及太魯閣族人的宇宙觀、土地觀、時間觀、權威觀與族群觀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這些學位論文當中，還有3篇並非直接以歌舞為題材，而是論述內容上涉及原住民的傳統歌舞。這3篇分別是吳秀蘭的〈阿美文化與祭儀內涵之探討〉、林芳誠的〈護衛文化的都蘭阿美人〉以及魯金子的〈部落文化特色文創產業行銷策略之研究&mdash;以撒奇萊雅部落為例〉。後者的主題是從地方文化創意產業看部落文化特色扮演的角色，文中記錄了撒奇萊雅族火神祭、播祭、豐年祭、收藏祭的儀式內容與歌舞，可供後續相研究參考。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了11篇學位論文外，另有數篇研討會論文。3月「2021/2022 重建臺灣音樂史&mdash;被遺忘的音樂．人物」研討會上，洪嘉吟發表〈1967夏季採集的留聲與白冷會神父在長光部落的遺緒〉；9月在臺東舉行的「阿美族學學術研討會暨MACACADAAY A KAPOT傳唱分享」，會上黃天來發表〈從阿美族傳統歌謠詞看到阿美族傳統社會與其語言之美〉、黃東秋發表〈阿美族傳統歌謠的價值與活力〉；11月「音樂的傳統與未來－2022臺灣音樂學會暨臺灣音樂學論壇年度研討會」有兩篇相關口頭發表的論文，分別是施永德的〈歌謠為歷史行動者：阿美族語跑船歌與都市原住民研究實踐〉以及戴文嫺的〈教會瘋原民歌謠&mdash;以兩個阿美族教會「感恩節/祭」為例〉。其中洪嘉吟的論文從民歌採集運用的調查錄音出發，探討天主教傳教活動對於原住民早期音樂記錄的貢獻，對應目前臺灣音樂學界對於歷史錄音的重視，該文已收錄於7月正式出版的研討會論文集中。</p>
