<h4>二、年度社會背景</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年由於嚴重特殊傳染性肺炎（COVID-19）新型變種的傳播力越來越強而難以防堵，相對地染疫的症狀也變得輕微，因此在上半年採取共存的政策。這使得上半年度的整體音樂活動，不如下半年來得活躍。</p>
<h4>三、資料來源</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由於本子領域自2022年7月開始執行，1至6月的音樂活動訊息，主要是來自於媒體報導。其訊息來源有三：一、原視新聞網；二、各縣市政府網站；三、Facebook的社團或粉絲專頁。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「原視新聞網」乃是「原住民族電視臺」的網路平臺，包括官方網站以及其YouTube頻道。原住民族電視臺，是根據《原住民基本法》，為保障原住民族權益而設置的公共媒體平臺，也是臺灣公共廣播電視集團的成員之一，因此其消息來源具有可靠度與公信力。原視新聞網的報導，與本音樂年鑑相關者，多屬原鄉部落祭典、地方社區公眾事務、音樂團體近期活動等。本子領域工作團隊不定期透過原視新聞網官方網站的「最新新聞」，蒐集可能相關訊息。由於新聞報導多以事件為對象，報導多半不會詳明活動的起迄時間，因此獲知音樂相關事件的消息後，需再從網路搜尋進一步的消息，交叉確定時間、地點等資訊。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;各縣市政府（主要是花蓮縣、臺東縣，亦包括新北市、桃園縣等）的原住民族行政或觀光相關部門的網站，亦不定期公布原住民祭典或音樂展演方面的消息，也是本子領域的主要訊息來源。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;各部落附屬組織或團體（如：年齡階層組織、青年會、文化健康站、公共事務平臺）的Facebook社團或粉絲專頁，由於多屬部落或團體內部訊息公布的管道。因此像祭典這類活動的日程、地點等，會比原視新聞網以及縣市政府網站來得更加細緻而精確。音樂展演場所、音樂團體的Facebook專頁，更是歌舞表演消息不可或缺的重要來源管道。</p>
<h4>四、研究方法</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域亦聘請地方人士，以駐點方式記錄音樂事件與活動。本子領域約用之人力，其駐點所在以及觀察記錄之對象族群，計：花蓮縣1人負責太魯閣族及撒奇萊雅族、臺東縣2人負責阿美族及卑南族、蘭嶼1人負責雅美族、苗栗縣1人負責賽夏族，其中3位具有原住民族身分（阿美族、雅美族、賽夏族）。這些駐點報導人，平素即關注地方音樂活動及公眾事務，有神職人員，有母語專業人士，有歌舞團體成員，也有原住民文化或民族音樂學相關的碩士或研究生。駐點報導人的主要工作，在於蒐集地方性的音樂活動，以彌補原視新聞網、縣市政府等消息來源的不足之處。更重要的是，駐點報導人針對具有代表性的音樂活動或事件，進行詳細過程的觀察與記錄，並撰寫成活動觀察紀錄表。</p>
<h4>五、統計數據表</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據音樂活動月曆，依「展演活動」、「祭儀活動」、「推廣教育」、「傳習活動」、「國際與兩岸交流」、「研究與出版」、「文化政策」8個項目，統計各族群（含跨族群）之全年次數。部分活動之屬性涵括兩種項目，例如同屬展演活動與推廣教育者，則兩項均予以計入。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體數據上，阿美族的活動總次數最高，應該是族群人數最多，而且在一般的認識上也最熱衷於歌舞活動的關係。阿美族的祭儀活動次數極高，是因為部落個數眾多，本計畫分別計入各部落豐年祭的結果。研究與出版方面，也是阿美族音樂受到最多的關注。賽夏族的音樂活動次數少，乃是因為族群人口規模小，音樂活動又相當集中於矮靈祭的關係。展演活動方面，以跨族群類型演出的統計次數最高，顯示當代原住民歌舞演出的型態，多半是一個展演活動（像是音樂節之類）當中，邀請各族群的不同團體輪流演出。數據呈現上太魯閣族的展演活動次數相當高，僅次於阿美族，有點令人意外。這應歸因於駐點報導人特別關注於地方上專供太魯閣族人使用的音樂展演場地的緣故。卑南族也是歌舞十分活躍的族群，其展演活動可能多計入跨族群，以至於數據上難以顯現。上半年（1月份至6月份）的活動次數少，一方面可能是因為嚴重特殊傳染性肺炎（COVID-19）疫情影響各類活動，一方面也是因為本計畫自7月方始執行，在此之前的活動，就僅能憑媒體報導來記錄。</p>
