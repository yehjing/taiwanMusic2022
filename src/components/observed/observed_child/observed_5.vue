<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統祭儀活動，可謂是當代社會中原住民與祖靈/族群主體精神之間維繫的橋梁，也是傳統歌舞最豐富的部落活動。隨著防疫政策的逐步放寬，嚴重特殊傳染性肺炎（COVID-19）對臺灣民眾生活造成的影響降低，各族群的傳統祭儀所受到的衝擊，主要是部分儀式段落的刪減。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民各族群的傳統祭儀中，屬傳統新年相關祭儀最為人關注，如阿美族的豐年祭、卑南族的年祭、撒奇萊雅族的豐年祭等等。賽夏族的矮靈祭因禁忌性高，儀式與祭歌複雜而嚴謹，也是原住民族的重要傳統文化資產。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;阿美族的年節祭典，外界雖通稱豐年祭，實際上各地區的族語稱法不一，儀式的內容結構也不盡相同。南部的豐年祭稱kiluma&rsquo;an。臺東市馬蘭部落的豐年祭以七天的規模來進行，祭儀的重心在海祭以及海祭前的引領祖靈。今年因顧慮疫情，海祭前一夜的年輕人團體大會舞取消。但海祭之後，凝聚全部落向心力的歌舞競賽如常舉行，並由年齡階層lamahengheng奪冠。中部的豐年祭稱ilisin。以臺東縣成功鎮的宜灣部落為例，已停辦兩年的豐年祭今年恢復舉行，但原本需要五天的時間進行完整儀式，今年也基於防疫而縮減成三天，三日分別舉行迎靈、宴靈、送靈的儀式。北部的豐年祭則稱為malalikit。花蓮縣壽豐鄉光榮部落今年恰逢成年禮，由aladiwas入階。入階的男孩必須通過marengreng的長跑考驗。四天的舉行時間當中，第二、三晚年輕人的大會舞都從晚上8點左右持續到隔天早上的6點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽夏族的矮靈祭（paSta&rsquo;ay），今年自10月1日開始進入準備階段。當天南祭團到北群祭場與北祭團共同商議約定祭儀的正式舉行日期，並且開始祭歌的練唱。南群的正式開始日是11月4日，北群則晚一天開始。南群從3日傍晚就開始跳舞演唱祭歌直到正式開始日的凌晨。在進行完3天的儀式之後，7日下午派代表赴北群祭場參加北群的第三晚儀式。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;花蓮縣吉安鄉的幾個阿美族部落，至今仍保有巫師（sikawasay，或稱祭師）的組織與活動。巫師的成巫有特殊的條件限制，必須在生病的情況下，被巫師判斷為惟成巫方可治癒的情況下，才能加入巫師組織，學習巫師祭儀，並且終身遵守嚴格的禁忌。巫師依其資歷，以及對儀式、祭歌等各方面的熟悉程度而有位階高低之分。巫師的工作是在部落的各種大型儀式中負責祈福、祝禱與除穢，也負責族人的個人醫療與喪葬儀禮。由於具有一般人沒有的能力，又為部落提供服務，因此在部落中受人崇敬，享有很高的社會地位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每年10月，是吉安鄉東昌村里漏部落舉行巫師祭（mirecok）的時間。每位巫師身負自己的守護神靈（這是成巫的原因），巫師祭就是他們各自祭祀守護神靈的重要祭儀。而每兩年一次，在巫師祭完成之後，部落舉行盛大的祖靈祭（talatu&rsquo;as），以宴饗祖靈，祈求祖靈保佑。今年的里漏祖靈祭以及前夜的窺探祭（milasung），在颱風外圍帶來的風雨中進行。雖然惡劣天候造成諸多不便，但巫師及部落族人無一喊苦。在祖靈祭最高潮的宴靈儀式中，族人激動落淚，旁觀者無不動容。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;里漏的巫師團目前僅有6名成員，其中年紀最長者已96歲。欠缺部落年輕人的加入，是巫師團面臨的最大危機。雖然有財團法人原住民音樂文教基金會（由巫師團成員，現任教國立東華大學的Panay Mulu創立於1997年）經常性舉辦巫師文化的推廣講座，但巫師的延續，並不像歌謠的傳習那樣只要學會即可，而是需要新血的實踐參與。</p>
