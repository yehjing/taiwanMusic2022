<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;研習過程主要分成三個部分。首先由本族的講師，介紹該族群的傳統文化，幫助與會學員建構歌舞知識的文化基礎。第二是講師的歌舞教學，包括解釋形式上的特點，並進行學員的實際練唱，教學時每位講師都搭配一位助理。阿美族場次邀請的講師高淑娟，介紹的是臺東阿美族的macacadaay複音歌謠，因此單一一位講師無法進行歌曲的示範，還必須助理一同演唱才行。研習的第三個部分，則由講師與學者進行對談，為學員梳理出潛藏在歌舞運作原則背後的文化思維。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;研習營的每個場次都吸引了許多國中、小教師甚至一般音樂愛好者報名參與。受限於舉辦場地歐樂思廳的空間大小，無法容納太多聽眾，是比較可惜的地方。因此依過往經驗，最受歡迎的阿美族場次特別開放網路直播，以加惠廣大有興趣卻不能實際到場的報名人士。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上述的本土音樂教育培力工作坊研習營，屬於動態的歌舞學習；5月中到8月底，由花蓮縣政府與花蓮縣文化局主辦的「悠揚樂聲話邦查－吉安阿美族部落工作歌採集特展」，則偏向於靜態陳列展示的性質。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這項特展的展出的地點，在花蓮縣吉安鄉的阿美族文物館。現場除了以看板文字的方式介紹歌謠的文化意義與歌詞外，還設置影音播放器材，觀眾可以聆聽實際的音樂，並了解策展過程當中族人蒐集歌謠的過程。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;特展的實際規畫、製作者，都是花蓮縣吉安鄉本地的阿美族人，特別是年輕人。他們藉由田野調查的方式，蒐集長輩所演唱的歌謠，整理後成為本次特展的內容。原住民族掌握文化展示的主導權與話語權的情況，希望未來能夠越來越普遍。</p>
