<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺東阿美族向來以追求自由發揮又講究協調合作的集體群唱歌謠聞名於世。十幾二十年前，這類受到音樂學者關注與讚譽的「自由對位式」複音歌謠，並不受到阿美族年輕人太多的青睞，只有年長者能夠自在熟練地演唱。不過幾年來局勢已然轉變，越來越多的阿美青年肩負起傳承的使命，投入歌謠的學習。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以臺東市馬蘭部落為主要據地的杵音文化藝術團在阿美古調的復興浪潮中居功厥偉。2021年，馬蘭阿美族的複音歌謠受文化部文化資產局登錄為「重要傳統表演藝術」，並正式以阿美語定名為macacadaay，杵音文化藝術團也被指定為保存者。111年，杵音文化藝術團持續在馬蘭部落進行歌謠傳習工作，由部落中具精湛歌藝的年長大歌手，指導青壯世代習唱古調。首先3月份時，舉行了110年度傳習計畫的期末成果展演，地點在屏東縣演武場。5月份在部落的馬蘭文化廣場舉行111年度傳習計畫的開訓式。10月份則在部落聚會所舉行期中審查。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;講究實地考查、忠實再現部落文化的老字號原住民歌舞表演團體「原舞者」，在今年舉辦了多場傳習活動，包括4月的「原舞者樂舞人才培育工作坊」，7月份「大專（學）院校原住民樂舞海洋文化研習營」以及「小小原舞者－2022暑期兒童原住民樂舞文化體驗營」。花蓮縣秀林鄉公所，於暑期舉辦「太魯閣族樂舞人才培訓」，今年的活動以樂器為主軸，教授學員太魯閣木琴、口簧琴的製作與演奏，並與古調整合，甚至進一步嘗試進行創作。蘭嶼的雅美族於年度舉行一場「學生音樂暨鄉土歌謠成果發表會」，年底還有「達悟族拍手歌會復振計畫練習」。Tabang文化藝術團，舉辦「111年傳統歌謠培力種子計畫」，領帶臺北市的原、漢青年到花蓮、臺東阿美族部落學習歌舞，11月時在臺北市凱達格蘭文化館舉行成果發表演出。</p>
