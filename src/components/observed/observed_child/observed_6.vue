<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域的文化政策影響較少，與文化政策相關僅有在國家推動語言政策下，為使國中小以下孩童有感語言沉浸的重要性，於是2019年開始挹注經費至各學校舉辦「夏日族語學習營隊」，並舉辦相關語推活動，如結合歌謠語詞等推動，本計畫於上述傳習、推廣教育分項說明，此不再贅敘。其次則是「長照2.0&mdash;原住民文化健康站」的活動推動，除了每日對於耆老進行相關活動、樂舞教唱之外，文化健康站的樂舞比賽，亦是耆老傳承與永續的發展重心。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;政府的獎補助方面，則以原住民村落文化發展計畫為主，計畫在於落實多元平權，促進文化多樣發展，賦權臺灣各原住民族群（含平埔族）因應族群文化發展之在地議題，結合青年來體現自發行動，掌握社區營造的趨勢，以此推動都市及原鄉文化傳承發展計畫，111年獲得補助單位與本子領域相關計有：南投縣魚池鄉邵族文化發展協會的「水社海的賣鹽嫂－關於文化詮釋權系統架構與推廣」，小米影像工作室 「《Sqoyaw部落誌的影像書寫＆舊部落Srasit》的考古調查計畫」、獵人居文化創意工坊的「陶醉吧！」、國立宜蘭大學的「噶瑪蘭（Kavalan）文化薪傳暨環境永續發展計畫」、財團法人原舞者文化藝術基金會的「文‧舞雙」、財團法人親愛音樂文化基金會的「親愛傳琴平台計畫」。</p>
<h3>玖、各族群分析</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;縱覽上述各族群分析，本子領域的6個族群，每個族群偏重的發展重點皆有所不同：</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;泰雅族的音樂活動以傳統的祭典活動為主，例如泰雅族在7、8月的祖靈祭或小米收穫祭，文化藝術展演團體從過去1990年代的「原舞者」、「高山舞集」與「原緣」等發展，2022年則以新一代的泰雅族人成立的「喜裂克文化藝術團」、「泰雅原舞工坊」、「傳源文化藝術團」、「巴達思文化藝術團」、「山風文化藝術團」、「Maralas多元文化藝術團」以及「Saicel文化藝術團」，以單一族群或多元族群的融合展現新生代泰雅族群樂舞的能量，樂舞展演則以主題性，結合樂舞與劇場、傳統與當代或電子音樂，例如傳源文化藝術團的「彈撥之間」舞劇，探討博物館展示與賦權的意義，一改過往只單就傳統儀式以及文化傳承，新一代泰雅族人從樂舞展演思索自身與掌握論述話語，帶動新世代泰雅音樂的發展可能，傳統與當代發展皆為本計畫觀察與紀錄的重點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年泰雅族的一項重大音樂活動則是宜蘭縣史館與臺灣師範大學民族音樂研究所、長老教會寒溪教會，於大同鄉寒溪村舉辦「聽見1967、宜蘭縣大同鄉寒溪村泰雅族歌謠發表會」，起因於史惟亮、許常惠兩位臺灣音樂研究者，於1967年7、8月，成員分為東西兩隊於臺灣各地，包含原鄉地區收錄民間音樂，當時部落耆老參與共錄下12首泰雅族歌謠，此次發表會由當年錄音的耆老後代與部落族人，重新演繹與複復刻當時流傳的歌謠，從「音樂載體的有聲錄音」為出發、為中介，連結傳統與當代的不同時序，以樂舞展演體現不同世代的音樂有聲「共感」，是2022年泰雅音樂最獨特的篇章。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;鄒族部分，2022年傳統的祭儀，如homeyaya小米祭，是傳統鄒族人在小米穗收時舉辦的祭儀，其次傳統祭儀的轉型，如阿里山鄉鄒族生命豆季鼓勵鄒族青年結婚、生育，並結合阿里山鄉各部落之在地文化、產業、觀光等，共同慶祝一年一度的生命豆季，自2002年開始它跨越傳統的鄒族祭儀，而是一個以鄒族集體婚禮為主軸，並結合阿里山部落人文、觀光、產業等資源，由地方政府輔助協辦的新興節慶。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵族部分，2022年的音樂活動，除了傳統的祭典儀式、先生媽的mulalu活動外，部落常態性樂舞展演仍是音樂的重點，由邵族文化發展協會經營管理的邵族逐鹿市集歌舞，是邵族樂舞文化展演、杵樂教學傳承的場域，是部落以樂舞對外展示、文化體驗的最佳窗口，在伊達邵部落每週常態性在潭畔演出，內容以傳統的邵族杵音與杵歌為主，藉以理解邵族的文化與活動，其次對外的推廣活動亦常以逐鹿市集舞團作為商演對象，樂舞活動由逐鹿市集成員以杵音、杵樂，配上新編舞蹈呈現，其次，則以語文推廣為核心，因著「語言主宰思考模式」的理念，藉此成為文化外拓與延伸的機制，例如從中延展至歌謠、部落記憶、從歌謠承載的傳統智慧等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;布農族人經常與官方合作，由各地縣市聯合舉辦的射耳祭儀活動最為盛大，然而因疫情緣故，大型聯合祭儀於零星部落舉行，射耳祭多半與體育競賽一同舉辦，如南投縣仁愛鄉中正、法治、萬豐部落舉辦全國布農族射耳祭暨傳統技藝競賽，南投縣信義鄉歲時祭儀及傳統技能競賽，高雄市那瑪夏區舉行區運暨射耳文化活動，臺東縣海端鄉布農族人舉行的射耳儀式，仍維持傳統於胡姓與王姓祭司家族祭場舉行，結束家祭之後回到部落為族人進行吹耳祈福。其次，與小米種植有關的是農業祭儀，祭儀日期因各社群居住地略有不同，在音樂上，農業祭儀中的〈祈禱小米豐收歌〉、〈獵前祭槍歌〉等膾炙人口的曲目仍在部落傳唱。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而本年鑑的觀察除了各地區布農聚落之外，傳統文化資產局所扶持的團隊南投縣信義鄉布農文化協會Lileh合唱團，由馬彼得校長擔任指導原聲學校，計有原聲阿卡樂團、原聲打擊樂團、台灣原聲童聲合唱團、原聲國際學校合唱團、原聲國際學院與原聲音樂學院等，2022年各地區的布農語推，以音樂素材為中介輔以語言的推廣，新生代布農族古謠新唱，以及跨界音樂交流如Kiwa Takihusungan在Ethno Sweden瑞典音樂從事布農工作坊，有聲唱片出版與研究等都是有一定成就。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪蘭族人音樂活動迥異於以往的年代，2022年噶瑪蘭族除了每年參與花蓮聯合祭儀之外，臺灣音樂館的世代之聲「噶瑪蘭重返榮耀之夜」，以及pateRungan新社噶瑪蘭傳唱隊在東元科技舉辦的驚嘆號活動，復刻傳統治病儀式的再現，皆是2022年的樂舞表徵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽德克族部分，2022年的音樂活動除了祭典儀式之外，新一代的賽德克青年以有聲唱片、音樂展演、樂舞的傳統與當代創作、區域性的年祭儀式、樂舞傳習等為主要活動，其次樂舞與觀光產業與地方農作結合。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除上述各族群的傳統生活領域、祭典活動與音樂傳承之外，現代社會的變遷，都市原住民及各縣市鄉鎮的聯合祭儀、展演機構的表演性活動劇增，綜觀2022年音樂活動以各原住民部落的地方組織佔有多數樂舞展演的活動，其次則是以推廣協會為發展，少有公立樂團、TAIWAN TOP演藝團隊、縣市政府傑出團隊等，登錄文資團體僅有以南投縣信義鄉布農文化協會Lileh合唱團等，立案演藝團體如傳源文化藝術團等。原住民大量人口移居西部，或於都會地區形成新一代的離散原住民社群，除了傳統祭儀活動、音樂傳承之外，都會地區或縣市政府原住民行政單位，以打破區域性的族群界線，舉行的聯合祭儀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民族群的部落教室、文化自覺，才是原住民樂舞人才培育的發展重點，族群的自我認同、文化復根與相關復育計畫，樂舞從本我的血脈、耆老的智慧永續，思索當代的各種傳習可能，例如結合語言、孩童夏令營、部落青年會等，是一種無法以量化論述與計算的文化永續，其次結合產業觀光的相關傳習，例如部落旅遊人員、解說人員的相關工作坊亦是人才培育的發展重心，其次結合著產業觀光、經濟鏈，部落樂舞無疑是最佳的產業代言。文化資產局的相關扶植團隊，從相關專業人員的訪視、部落樂舞的短中長程樂舞學習，以及期中、期末樂舞考核等，皆是原住民樂舞人才培育的主力所在，原汁原味，從傳統的價值核心入手，再者，文化部或民間企業的相關演出，例如臺灣音樂館世代之聲演出，從老、中、青三代的共同展演，有形與無形凝聚部落核心力，透過對外演出，重新挖掘與呈現過往消逝音樂的可能再現；部落相關立案的演出團體，亦透過展演補助，重新反思傳統樂舞的當代意義，多元跨界的嘗試，筆者認為，未來是一種以原住民樂舞傳統，展現人才培育、融合當代跨界與傳承的新勢力。</p>
<h3>拾、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜合上述的分析以及數據顯示，邵族部分以祭典儀式、語推等相關補助、研究論文與計畫為主；布農族則以文化資產局的計畫團隊扶植、儀式活動為主體，其次則是語言推廣的相關活動，新一世代的音樂推廣、工作坊與音樂競賽等，多有顯著佳績；賽德克族則偏重各部落儀式發展，其次青年世代的音樂以街聲與串流平臺，為有聲發送的新趨勢；噶瑪蘭族除了歲時祭儀之外，2022年以多項展演的能見度，如臺灣音樂館世代之聲、國立傳統藝術中心亞太傳統藝術節、東元科技驚嘆號等演出，都有噶瑪蘭族的音樂表演，實屬該族群最獨特的一年；鄒族族群則偏向儀式活動，其次新世代音樂、演奏人才，亦逐漸嶄露頭角，成為原住民音樂的新主力；泰雅族方面除了歲時儀式之外，樂舞演出團體以一種反思性之姿，經由樂舞展演敘述族人的內心反思，重新討論傳統與現代之間的關聯，並對傳統文化消失與再現的反身性進行多方探討。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的祭典活動、音樂會、藝術節、音樂祭等，已逐漸脫離嚴重特殊傳染性肺炎（COVID-19）的縮編或者停辦影響，逐漸恢復以往，目前在兩岸樂舞交流以及國際間的跨域合作較為少見。2022年的原住民樂舞活動，以新生代與新一世代的創作與敘事最為亮眼，從古謠與傳統音樂素材出發，不論從傳承、跨界合作、藝術創作等，都展現新一個世代呈現的音樂語彙，期待未來能持續關注世代更迭的音樂發展、古調傳承與樂舞展演等活動，因此不論從樂舞製作、編整、對外行銷推廣、策展、學術研究與工作坊等，2022年逐漸由原住民族群身份人士投入，掌握原住民音樂敘事的話語權及自身主體性表述，建議未來臺灣音樂年鑑原住民的觀察與記錄，應思索由原住民來表述、彙整的可能。其次，2022年的國家文化政策，例如語言推廣結合古調新唱的音樂活動，或者樂舞比賽、戲劇比賽等，皆為從部落底層出發的新興發展，也因嚴重特殊傳染性肺炎（COVID-19）的影響，部落耆老也重新思索及學習，如運用線上、手機等媒體與app來聯繫與傳續樂舞的新可能，傳統古謠也以多媒體方式，結合語推、教材開發、雲端鏈結、開放博物館等平臺的開發，展現2022年迥異於以往的新發展，另外，原住民的樂舞活動時常難以單一切割局面觀之，例如音樂展演結合祭儀、語言、工作坊或者有聲事業出版等，音樂年鑑未來能擺脫各項分層的束縛框架，思考各項分點研究間的橫向鏈結與發展可能。</p>
