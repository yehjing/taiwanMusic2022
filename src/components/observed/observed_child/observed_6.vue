<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年傳源文化藝術團團長弗奧伊&middot;舒樣Bawxin&middot;Suyan與副團長陳光明<strong>Yumin</strong><strong>．Lakis</strong><strong>，兩位溫潤的部落知青以新作</strong>《彈撥之間》，批判當權「靜物」知識，解放、反身，敘述歷史、土地、血脈牽引之間的臍帶關係與滋養，作品《彈撥之間》能動文化的「圓」、回溯血脈的「源」、反身存在的「原」，五幕脈絡明晰，層次鞭辟、絲絲入扣，《彈撥之間》肢體情境化、器樂歌樂化&hellip;的多重視域，是「自身」又是「他者」，在在表述自他邊界的消融，試圖發展出跨界合作與實驗創作的可能，展現新一世代的作品敘事。</p>
