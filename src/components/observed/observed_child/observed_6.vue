<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年從國中小學更擴展到耆老等技藝文化的傳承，花蓮縣馬遠部落等數位耆老選定於此主辦「布農族丹社群音樂文化族語學習營」，合作夥伴有前年南投Islua青年合作辦，以及馬遠部落族語老師等周邊學校，活動舉辦形式採四天三夜的方式進行，學生依照低、中、高年級分成三組，每個小隊老師皆是馬遠部落族語老師，會在協同一位馬遠部落青年協助老師授課教學，課程安排以語言學習：認識自己氏族與聚落、馬遠部落文化資產：布農族傳統家屋及日治時期空間、布農族丹社群樂器文化：杵音、口簧琴、弓琴樂器製作與敲打、布農族丹社群生活歌謠：數夢歌與杵音歌、山林知識等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南投縣信義鄉布農文化協會所屬的明德部落，因為文化傳承的使命感，期許能夠將布農的音樂代代傳承，部落中幾位耆老自發性地組成音樂展演團體，定期練唱，1999年正式成立南投縣信義鄉文化協會，並組成「Lileh之聲合唱團」，為文化部指定之傳統表演藝術文化保存團體。「南投縣信義鄉文化協會Lileh合唱團」為臺灣少數獲頒為「國家級重要傳承藝術保存團體」之一，該團由南投縣信義鄉明德社區布農族人所組成，每位團員具有豐富的演唱經驗與絕佳的音樂默契，為了維持傳統的古謠傳承，每週耆老們固定時間相聚聯繫，縱使在疫情期間部落同時思索如何在現代科技時代，以線上方式進行音樂的傳承與永續。</p>
