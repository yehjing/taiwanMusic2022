<h3>壹、前言</h3>
<p><strong>一、研究範圍界定</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子項計畫的研究範圍與涉及對象，包括泰雅族、鄒族、邵族、布農族、噶瑪蘭族、賽德克族，一共六個臺灣原住民族族群。除了族群的傳統音樂傳習與祭儀活動之外，樂舞展演、推廣教育、音樂文化傳承與教育、國際與兩岸交流、研究與出版、文化政策下的音樂活動，都是本子領域的觀察與研究範圍，各族群音樂發展從展演性、傳承與傳習、研究論述多元面向從事研究，族群概述如下：</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;泰雅族群分布地理位置居於臺灣新北市、桃園市、新竹縣、苗栗縣、臺中市、南投縣、宜蘭縣等山區個鄉鎮，是臺灣原住民族中，領域分布廣闊之族群。泰雅族群分成賽考列克（Seqoleq）以及澤敖列（Tseole）兩個語群，賽考列克泰雅族群位居於新北市的烏來、新竹尖石鄉、臺中和平區以及桃園復興等地，澤敖列泰雅族人主要的活動範圍在於新竹縣尖石鄉、五峰鄉，苗栗的泰安鄉，宜蘭縣的南澳與大同一帶。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;鄒族主要居地在玉山西南方，以嘉義的阿里山鄉為中心，少部分族人則以南投縣信義鄉的久美村為聚地，一路南行則遠達高雄市那瑪夏一帶，因此鄒族人的分布沿著嘉義縣阿里山曾文溪以及濁水溪流域，範圍以阿里山達邦、特富野，以及南投信義的魯夫都群為主。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵族依著南投日月潭湖光山色而居，此地區舊稱「水沙連」之地，相傳邵族祖先追逐一隻白鹿來到日月潭，資源肥美，主要族人聚集於南投縣魚池鄉伊達邵部落，少部分則居於南投縣水里鄉邵族雨社山聚落，屬於過去的頭社系統。邵族的活動於當代，以聯合辦公室方式，組織於南投縣魚池鄉邵族文化發展協會、邵族逐鹿市集、邵族文化故事館、邵族傳統智慧創作共同基金管理委員會、邵族語言推動委員會、南投縣魚池鄉伊達邵文化健康站等，從伊達邵部落成立南投縣魚池鄉邵族文化發展協會之後，接連邵族語言發展中心、邵族杵音班等組織陸續成立，形成部落組織推動一連串文化復振以及族群意識的核心機制，使族人透由在地文化、歷史音樂、文獻史料、祭儀等相關資源，建立屬於全體族人與共享的事業，部落的族語、杵音、歌謠與相關資料持續爬梳，展現邵族於當代的文化意義，因此邵族的樂舞音樂以推廣協會發展為主。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;布農族部落依靠中央山脈而居，根據遷徙史，原先居住於玉山與北側的布農族逐漸因環境擴張至西部平原，再陸續遷往臺東、花蓮與高雄，因此族群的主要發展，則位於南投縣仁愛鄉、信義鄉，花蓮縣卓溪鄉、萬榮鄉，臺東縣延平鄉、海端鄉，高雄市桃源區、那瑪夏區等地，皆留有布農族人的足跡。就語言與文化差異，布農族群分成六個亞群，分別是卓社群、卡社群、巒社群、丹社群、郡社群、蘭社群。布農族傳統宗教以dihanin（天）與hanito（精靈）為信仰核心，相信個人能力、疾病、善與惡等都與精靈hanito產生連結，自然界同時具有靈的「泛靈信仰」也深深影響布農人的宇宙觀，樂舞活動因此開衍，布農族的祭儀分成生命儀禮、歲時祭儀、臨時祭典，對於族人來說，射耳祭典最為重要，舉行的日期約於4與5月期間進行，儀式的意義，於部落智慧傳遞、競技、團結等文化內涵。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪蘭族過去以蘭陽溪為界，以北的稱為「西勢番」、以南的稱為「東勢番」，重要聚落有打馬煙社、抵美簡社、奇立丹社、抵美福社、流流社、武暖社、歪仔歪社、新仔羅罕社、利澤簡社、加禮宛社、奇武荖社等，主要分布於宜蘭、羅東、蘇澳一帶，以及花蓮市、東海岸之豐濱鄉與臺東縣長濱鄉等地，以新社、立德、大峰峰、樟原、佳里宛等部落，人口與社群集中，因此文化保存最完整。原居於蘭陽平原的噶瑪蘭族，因漢人爭地逐漸向南遷徙，落腳於花蓮、臺東，與阿美族等族群比臨，未南遷的族人則在宜蘭地區與漢人一同生活，近年來，宜蘭壯圍地區奇立板社感嘆流逝的文化，於是積極進行語言、古謠與祭儀等文化復振，不斷向花蓮新社與其他部落噶瑪蘭族人學習，近年具顯著成果。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;分布在南投縣仁愛鄉，以濁水溪上游為腹地的賽德克聚落，南起中原部落北至靜觀部落；語言的差異，使賽德克族分為3種語系，SEDIQ、SEEDIQ、SEEJIQ，族人經常以「3S3T」來形容語系的差異。以濁水溪及其支流建立群落：太魯閣（Truku）群賽德克族人以濁水溪Truku地帶為部落聚點，因族人規模的擴充原有獵場領地不足，部分族人穿越奇萊北峰遷徙花蓮縣境，形成東賽德克族，居於花蓮縣秀林鄉、萬榮鄉與吉安鄉一帶。都達社（Toda）的賽德克族人，日人稱「道澤」或「韜侘」群，清代舊志稱「斗截」，居住地位於德克搭雅群（Tgdaya）東北方，太魯閣群（Truku）西南方，為於濁水溪最大的河谷山臺地上，部分族人遷徙至今花蓮縣境，形成東都達群的賽德克族人，即現今的萬榮鄉立山村山里部落，而留在原居住地的族人在濁水溪畔形成部落。德克搭雅群（Tgdaya）的賽德克族人居住地位於都達群的賽德克族人之西南方霧社附近，主要居住於眉溪、濁水溪上游及馬赫坡溪等河谷臺地；德克搭雅群（Tgdaya）在1930年霧社事件前是賽德克族勢力最大的族群，事件翌年後參與抗日之部落未戰亡族人，被強制遷移，居於北港溪中游河岸臺地，日人稱之為「川中島社」，現為仁愛鄉互助村清流社區，目前賽德克族，特別是南投境內，以清流部落為主要文化的傳承地。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人才培育方面分成學界、地方耆老傳習、地方青年會、語言推廣結合歌謠、文化部及教育部等公部門的相關培育。首先是學界領域，少有以單一原住民族群的科系，而是以多元族群的學習為主，例如東華大學原住民民族學院、國立暨南大學原住民文化產業與社會工作學士學位學程原住民族專班、布農族原聲學校等，各級學校經常安排計畫與部落耆老相互學習，其次則是各大學的原資中心、原住民社團的部落鏈結，例如為慶祝國立臺北藝術大學校慶40週年，原住民族社團-Dalan社賡續參加2022年第二十七屆北區大專學生原住民族族語歌唱大賽榮獲團體組第一名佳績，北藝大原資中心利用課後，凝聚社團原住民學生投入研究賽德克族的歷史文化，由賽德克族青年Teymu一字一句教導歌詞內涵，並進入部落進行深度踏查，從族人口中了解到賽德克族歌舞、生活型態及身體感等，透過親身製作傳統樂器、傳統弓箭及織布體驗，學習古謠傳唱，從中揣摩與學習耆老的生活智慧，學校與部落的雙向鏈結與合作，是2022年的人才培育發展趨勢。教育部國高中的師資培育領域亦是人才培育的發展重心，由師大呂所長鈺秀教授主持「2022 本土音樂教育與文化培力工作坊研習營」計畫，工作坊以原住民的樂舞培育為課程設計，近距離向部落族人學習，親身接觸文化，並精心安排專家學者們對談，從知性與樂舞實作的師資培育的樹人入手。</p>
<p><strong>二、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的臺灣社會背景，歷經了幾年高強度的防疫生活，許多原住民族的傳統祭儀對外取消或者改為內部不對外進行，而到了2022年末臺灣邊境逐漸開放，指揮中心放寬了口罩禁令，臺灣演藝活動與原住民音樂會、慶典、傳習、工作坊、比賽等，逐一開放，從年鑑的月曆與蹲點活動檢視與觀察，原住民樂舞活動逐漸回歸正常，甚至於2022年下半年數量日增的趨勢。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的社會背景另有九合一選舉，地方性的造勢活動或者候選人的競選拜票與造勢等，時常出現於臺灣原住民族的樂舞表演、祭儀，各地縣市鎮原住民族行政單位，紛紛推出聯合祭儀，以展現縣市政績，各地區的原住民族群以打破單一族群的祭儀活動，融合行政單位的儀式慶典，甚至一改過往舉行儀式的時間以配合聯合祭典，這樣的活動亦打破同一族群的不同區域差異，以異地開花形式呈顯，例如桃園縣舉行的噶瑪蘭除瘟祭，即以治病除瘟儀式，在桃園串連花蓮新社噶瑪蘭族裔、宜蘭奇立板、旅北噶瑪蘭同鄉、平鎮噶瑪蘭人等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年臺灣原住民音樂的發展，仰賴臺灣原住民委員會的政策發展，例如「原住民族語言發展法」、「原住民族教育法」等法律，文化健康站的預算編列，原住民政策原住民族基本法等，以尊重與保存原住民族的族語、文化藝術、生活習慣、傳統習俗等。在政策制定與預算編列增加下，逐一落實於各單位的原住民族層次的文化推動，每一城鄉與族群各有其顯著的發展，除了傳統樂舞的保存傳習規劃之外，以音樂領域而言發展新一世代的創作動能，不論政府或者民間企業出資規劃，亦是2022年的整體社會背景環境下的另一趨勢，例如原住民族群委員會政策性的推動「2022 Taiwan PASIWALI 音樂創作培訓營」，鼓勵新一代的原住民族人以音樂內容產業為生涯培訓的可能，提供專業技術加值課程、並提升音樂文化涵養與拓展音樂產業的實務經驗，試圖以此開創與鏈結國際視野。</p>
<p><strong>三、資料來源</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年鑑計畫資料來源可分為田野蹲點資料與文獻資料搜集與彙整，田野蹲點資料由魏心怡教師擔任子計畫主持，帶領國立臺北藝術大學團隊，計有李盈青、簡志霖、劉懿霖等進行泰雅族、布農族、邵族、鄒族、賽德克族、噶瑪蘭族六個族群的蹲點紀錄，其次則從相關原住民青年、地方文史工作人員、語推人員的田野協力，從親身的經驗與經歷出發，鉅細靡遺紀錄原住民族的傳統與當代音樂活動，藉由駐點報導人、原住民文化相關的文史工作者、民族音樂學相關碩士與研究生的協力，部落地區音樂活動、祭典及公眾事務，或者原住民跨國展演等，皆提供最直接鮮明的報導。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一方面以地毯式、逐月建構資料蒐集等文獻彙整，亦是本計畫的研究重點，從月曆的書寫進行量化研究，相關資料彙整來源有四：一、各縣市中心、音樂場館的展演資訊；二、各級縣市原住民部落、原住民音樂團體官網及自媒體；三、原視新聞網等相關媒體報導露出；四、自媒體社團或者粉絲專頁，如Facebook、Instagram等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;資料來源也包括各級演出單位的相關平臺與展演資訊，兩廳院、臺中歌劇院、衛武營等演出單位、國立傳統藝術中心、臺灣音樂館、文化資產局等，各地方政府如苗北藝文中心等相關單位平臺，是本研究重要的參考依據，其次則是原住民文化館，如順益博物館、臺東縣成功鎮原住民文化館、海端鄉布農族文化館等相關樂舞活動推廣、展示、展演等，皆為本計畫的資料來源，各縣市政府例如南投縣政府、桃園縣市政府、花蓮縣、臺東縣、臺北市與新北市等原住民族行政或觀光相關部門的網站，時常公布的原住民祭典或音樂展演方面的消息，是本子領域的主要參考資料依據。目前學校的原住民資源中心、原住民推廣中心、原住民音樂團體官網或者文資局扶持團隊等，皆設立相關官網或者自媒體如Facebook等，所發布的儀式、祭典活動、樂舞推廣、工作坊等消息，是本計畫資料的參考來源，各部落附屬組織或團體，例如青年會、文化健康站的Facebook社團或粉絲專頁，訊息的公布管道皆從部落內部得知，因此如祭典等的日程與地點，經常描述得較為細緻而精確，部落組織的自媒體也時常將照片與影片公開露出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;公播媒體是本計畫案資料來源主力，訊息仰賴財團法人原住民族文化事業基金會，從原住民族電視臺及原住民族廣播電臺的部落發聲平臺，理解原鄉部落祭典活動、音樂傳承、社區發展、音樂團體等大小事，而原視新聞臺的最新消息，給予最即時、第一手的部落資訊。</p>
<p><strong>四、研究方法說明</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域所使用的研究方法，以質性研究與量化研究為主，根據勞倫斯・紐曼（W. Lawrence Neuman）提出質性研究與量化研究的差異時，提出質性研究強調命題，目的在呈現社會事實與建構意義，關注事件的互動過程，而量化研究則著重於假設，以資料證明測量的事實，關注於變數之間的關係。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域的研究方法則分為兩大方向，一為親身的蹲點觀察，以類民族誌（Ethnography）的個案研究，從田野對象本身著手，在參與觀察的過程中，除捕捉事件、制度、結構、外來力量與當事者的敘述及論述之外，進一步從身體化的實作意識（practical consciousness）、默會知識（tacit knowledge）中，探索行動者的軌跡與認同，並以人、物、地方與歷史互為媒介，分析自我認同、相互認同的形成，進一步捕捉重要的在地範式（indigenous categories）與社會構成原則，最後對田野知識生產進行反身性的還原建構（楊弘任2014：317、333）。透過第一手民族誌與解析他者已撰述的民族誌，「再現」深描（thick description），讓每一個事件的「文本」（text）背後不同行動者的理解範式、文化框架一一現身，進而體顯事件的深層意義。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本子領域由2022年7月開始著手執行，因此7月至12月的音樂活動訊息則以質性研究，親身的田野紀錄為主，1至12月整體原住民音樂活動訊息，則強調地毯式的資料蒐集方法，經由網路、官方文宣以及媒體的報導為主要參照。</p>
