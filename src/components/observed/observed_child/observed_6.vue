<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;阿里山鄉鄒族生命豆季，於2002年開始舉辦，是以鄒族族人集體結婚為主軸的活動，鼓勵鄒族青年結婚、生育，並結合阿里山鄉各部落之在地文化、產業、觀光等，共同慶祝一年一度的生命豆季。自2002年開始舉辦的阿里山鄒族生命豆祭，其實並不是一個傳統的鄒族祭儀，而是一個以鄒族集體婚禮為主軸，並結合阿里山部落人文、觀光、產業等資源，由地方政府輔助協辦的新興節慶。homeyaya小米祭是傳統鄒族人在小米穗收時舉辦的祭儀，感謝小米女神對農作物的照護，另一方面則藉此凝聚氏族力量，祭儀前族人會將在收成的小米掛在小米田作儀式後，再帶到各自氏族的小米祭屋擺放，相傳小米是小米女神的化身，不喜歡吵雜，因此在祭儀的過程中都不能大聲喧嘩吟唱。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽德克族在舊年跨越新年期間，舉行smratuc，整個儀式過程包括cmucut年祭以及Tmukuy macu播種祭，行前舉辦火占，2022年賽德克族群則由各部落舉行年祭，南投中原社區發展協會在大巴蘭部落舉行年祭，試圖復刻傳統的祭儀火占模式，透過口述歷史與耆老經歷陳述、生活經驗教導，透過年節轉換來承續新的智慧與Gaya精神。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因嚴重特殊傳染性肺炎（COVID-19）緣故，睽違兩年多停辦的聯合祭儀，2022年由桃園市政府原住民族行政局，恢復舉行各族群歲時祭儀活動，由泰雅族群首先拉開序幕，當天原民局局長、議長、各部落泰雅族人等一同參與活動，文化展演全程以雙語（泰雅語、中文）同步進行，祖靈祭儀式結束後由各部落的傳統歌舞演出，讓族人一同感受祖靈的祝福並歡慶祖靈一整年的保佑，結合桃園原住民族文化會館的文化資源，同步推出桃原好市WAH MITA&rsquo;，再配合文化會館後方「Tayniho桃原好棧」藝廊區、輕食區、舞臺區、DIY區及藝品區等五大空間，提供民眾充實的文化體驗空間，賽德克族、泰雅族等輪番在桃園市舉行儀式活動，今年是噶瑪蘭族在桃園舉辦的第一場歲時儀式，主題是除瘟祭Pakelabi，傳統的Pakelabi是Metiyu專屬的祭祀與醫療行為，選擇在夏末初秋，天上沒有月亮的日子舉行。Metiyu們面向北方迎請噶瑪蘭族眾神靈以及祖靈降臨，替身體欠安的Metiyu治病，同時也為部落祈求平安順利，今年為了呈現Pakelabi除瘟祭，也邀請花蓮豐濱新社族人參與，將古調、舞蹈及祭祀傳統移植至桃園舉行，舉行Pakelabi除瘟祭時複製Metiyu登上屋頂的茅草小屋，現場響起花蓮新社部落的古謠錄音，由桃園市噶瑪蘭文教發展理事長江艾芯女士重新教導，帶領旅北一帶族人演譯展演Pakelabi，參與者計有樹林、板橋、平鎮、宜蘭奇立板部落等噶瑪蘭族，最後由花蓮新社部落龐志豪與龐愛花等擔任領唱，帶領族人圍成圈舞演唱。</p>
