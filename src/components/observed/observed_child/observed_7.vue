<h3>參、族群音樂發展的深入剖析</h3>
<h4>一、振興國際交流的可行途徑</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年的年鑑紀錄，國際交流的紀錄全年為零，沒有任何資料，究其原因，嚴重特殊傳染性肺炎當然是最主要的因素，因為疫情等等因素，各國的表演場館幾乎呈現休止的狀態。面對幾乎消失的國際交流活動，筆者認為有幾個方法，是可以有效的振興國際交流活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;首先，結合網路科技是一個最好的方式。嚴重特殊傳染性肺炎之前，企業界早已頻繁的應用網路線上會議的功能，進行商業上的各項商議、談判、議價、聯誼。把這個模式引導到藝文領域，同樣也可以在線上實施許多的藝文活動。以專訪的活動為例，世界之舞聯盟透過Facebook社團的「藝術家專訪周明傑：排灣族古謠與勇士舞線上直播」，就是一個透過線上專訪音樂家的例子。「音樂客廳（李正秋、張小艾）－線上直播」（屏東B1 music Facebook粉絲專頁），是透過社群軟體所辦理的展演活動。出版活動方面，「排灣族《vuvu的聲音》StreetVoice街聲數位上架」（屏東縣泰武鄉圖書文物館），則是透過數位的方式，實施專輯的出版。以上都是音樂活動利用網路傳播的最好例子，值得參考學習。</p>
<h4>二、嚴重特殊傳染性肺炎重創臺灣藝文活動</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年5月，嚴重特殊傳染性肺炎在臺灣肆虐，所有的藝文活動因此受到影響，藝文活動產生的影響分成幾種結果，第一個是活動取消辦理，包括：「排灣篇～古勒勒的生命史」（原住民族委員會原住民族文化發展中心）場所暫停開放、「拉阿魯哇族－再回到那日昇之地」（原住民族委員會原住民族文化發展中心）場地暫停開放、「蒂摩爾古薪舞集－唱牡丹歌謠《HOW ARE YOU》」（屏東藝術館）取消演出。第二種結果是暫緩或延後辦理，如：「牡丹社事件147週年紀念活動暨牡丹故事館開館 系列活動」（牡丹社事件紀念館）暫緩辦理、「南和青年會會長交接暨部落音樂分享會」（南和集會所）延期辦理、「《我們小時候的歌》音樂專輯發表會」（屏東青創聚落）延期辦理、「排灣族口、鼻笛－〈勇士歌piyuma歌謠〉」（國立傳統藝術中心宜蘭傳藝園區曲藝館）延期辦理、「2021驚嘆樂舞－台灣原住民樂舞饗宴」（國父紀念館大會堂）延期辦理、「排灣族口、鼻笛〈lalinedan情歌〉」（國立傳統藝術中心宜蘭傳藝園區曲藝館）延期辦理、「豐中有祢－基督教中華循理會排灣區教會豐年感恩祭」（屏東縣獅子鄉楓林國小）延期辦理、「排灣族口、鼻笛〈斜坡子民的禮物〉」（國立傳統藝術中心宜蘭傳藝園區曲藝館）延期辦理、「2021 kapanan音樂節」（屏東縣牡丹鄉石門國小）暫緩辦理。第三種則改為線上直播，例如：「2021臺灣樂舞祭 世界之舞－草埔青年會表演」（世界之舞聯盟Facebook社團）以線上直播的方式辦理。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;疫情造成的影響非常大，直到2022年，疫情似乎並沒有消退的跡象，因此，現今藝文活動的辦理，除了要有暫緩或取消的心理準備，實體音樂會需要將相關的防護措施備齊，例如實聯制掃描、音樂廳裡配戴口罩、溫度測量、溫度過高觀眾的處理等等，每一個步驟都不能省略。</p>
<h4>三、藝文活動如何克服疫情的衝擊</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;嚴重特殊傳染性肺炎的傳播影響了全世界人民的生活，未來，疫情可能還要持續很長一段時間，面對如此的情況，藝文單位要有更多元的方式，辦理各種藝文活動，以因應疫情所帶來的衝擊。筆者從2021年的活動當中，擷取幾個可行的例子，讓我們能找出更多的策略，辦理以後的活動。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;首先，是活動的線上直播音樂會，這些例子如：「2021臺灣樂舞祭 世界之舞－草埔青年會表演」（世界之舞聯盟Facebook社團）、「2021收穫那麼多－高士古謠隊」（屏東市千禧公園）、「排灣女聲 音樂客廳－線上直播」（屏東B1 music Facebook粉絲專頁）、「One Song週末音樂廳－2020、2021臺灣的聲音．新年音樂會」（灣聲樂團OneSongOrchestra YouTube頻道）、「《嘻哈、搖滾、原勢力》專輯發表會網路直播」（無虞音樂工作室）、「線上療音樂－尚諺&amp;徹摩」（秘密基地）、「World Village Festival線上演出－「小島大歌」計畫」（World Village Festival音樂節官網）、「2021世界音樂節在臺灣－泰武古謠傳唱再見查馬克」（臺北大佳河濱公園）、「kapanan麥雅巴札基各電台－第一集線上播出」（kapanan音樂節主辦單位）、「2021台北愛樂雲端音樂會－原鄉新聲《瘋原祭》」（台北愛樂YouTube頻道）。另一種音樂會的演出方式，是線上與實體一起呈現的，如：「細雨灑落群山－查馬克．法拉屋樂 追思音樂會」（屏東演藝廳音樂廳）。音樂會以外，尚有其他的藝文活動，例如音樂活動、音樂演講、音樂課程。如：「臺灣原創流行音樂大獎」（臺灣原創流行音樂大獎直播平台）、「《zalan見識南島》臺灣原住民音樂航向全世界」（原住民族電視臺）、「原住民族音樂研究專題－講題：林班歌謠研究：現況概述、疑問與展望」（國立屏東大學原住民專班）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由於實體課程、實體音樂會群聚的關係，許多觀眾因為恐懼染疫，欣賞音樂的方向反而轉向家中的電視與廣播，許多節目反而因此有了回流的觀眾觀看音樂活動。這些節目如：「《Semenay我們的歌》第六集 排灣族、魯凱族」（原住民族電視臺）、「《Semenay我們的歌》第七集 拉阿魯哇族」（原住民族電視臺）、「《Semenay我們的歌》第十集 排灣族」（原住民族電視臺）。收聽廣播也是欣賞音樂節目很好的方式，這些節目如：「《Paicelen出力》節目談《小島大歌》計畫」（Alian96.3原住民族廣播電臺）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最後要提到的，是線上的功能除了可以聽音樂會以及欣賞各種音樂相關活動，一種最特殊的線上活動在宜蘭展開。這個是「第十九屆台灣之舞台灣民俗舞蹈營」，主辦單位國立宜蘭大學海韻國際民俗舞蹈社竟然以線上營隊的方式辦理音樂活動，這種形態吸引了很多年輕學子參加，值得注意。</p>
<h4>四、平埔族度過驚濤駭浪的一年</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2020年，平埔族總紀錄量為28筆，相關的族群為馬卡道族、大武壠族、西拉雅族、噶哈巫族、道卡斯族、拍瀑拉族。2021年，疫情影響了辦活動的意願，總紀錄量降為24筆，相關族群為馬卡道族、大武壠族、西拉雅族、噶哈巫族、道卡斯族。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於去年，平埔族某些族群的祭儀活動已經成為常態，不見得會受到疫情影響，這些族群為馬卡道族、大武壠族、西拉雅族、噶哈巫族、道卡斯族，只是辦理祭儀的過程中會增加一些規定，比如說戴口罩、實聯制等，比較謹慎的部落很早就公告祭典活動不開放外賓參加，僅開放該族族人參與，例如「110年吉貝耍夜祭與孝海祭」。平埔族群當中，展演活動的表現最積極的族群，首推大武壠族，該族群的大滿舞團演出紀錄非常多，如「Hi o hui一起給力－環境劇場」（高雄市杉林區日光小林社區公廨廣場）、「線上藝文宅配通－經典劇目再現《回家跳舞》」（高雄春天藝術節YouTube頻道）、「國慶焰火晚會－夜祭演出」（高雄港蓬萊商港區）等，顯示大滿舞團在藝文界展現出的旺盛活動力。</p>
<h3>結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年，嚴重特殊傳染性肺炎疫情持續傳播，全世界籠罩在疫情的威脅，嚴重特殊傳染性肺炎的傳播幾乎影響了全世界人民的生活。2021年中，原本被視為防疫模範生的臺灣，也開始受到疫情的威脅，以至於所有的藝文活動，全部都受到影響。藝文單位要如何面對這樣的衝擊，我們雖然無法一下子找到最好的方法，但是透過2021年的活動紀錄，有很多值得我們學習的地方，從這裡來找出策略，或許是最好的方法。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂發展深入剖析的章節，我們探討了幾個主題，第一個是「振興國際交流的可行途徑」。國際交流是2021年最弱的項目，我們可以找到最佳的改善途徑為－結合網路科技，在線上實施許多的藝文活動，紀錄當中如世界之舞聯盟透過Facebook社團的「藝術家專訪周明傑：排灣族古謠與勇士舞線上直播」、屏東B1 music Facebook粉絲專頁辦理的「音樂客廳（李正秋、張小艾）－線上直播」，以及屏東縣泰武鄉圖書文物館所辦理的「排灣族《vuvu的聲音》StreetVoice街聲數位上架」出版活動等都是極佳的例子。第二個探討的主題是「嚴重特殊傳染性肺炎重創臺灣藝文活動」。自從2021年5月，嚴重特殊傳染性肺炎在臺灣肆虐，所有的藝文活動因此受到影響，藝文活動產生的影響分成幾種結果，第一個是活動取消辦理，第二種結果是暫緩或延後辦理，第三種則改為線上直播。無論哪一種活動改變方式，都對臺灣藝文界產生了重大的影響。2022年，疫情似乎並沒有消退的跡象，因此，我們要有更多的因應方式面對未來藝文環境的改變。第三個探討的主題是「藝文活動如何克服疫情的衝擊」。藝文活動要如何克服疫情的衝擊的話題上，我們參考了2021年的活動紀錄，找出了幾個可行的方式，第一，是將音樂會實體活動改成線上的模式。第二，是將過去傳統音樂會模式，改成線上與實體一起呈現。第三，音樂會以外的藝文活動，例如音樂活動、音樂演講、音樂課程，也都以線上方式呈現。除了以上的方式，家中的無線臺以及有線臺，其實也提供了很多好的音樂節目，值得我們觀賞。最後是收聽廣播，也是欣賞音樂節目很好的方式。第四個探討的主題為「平埔族度過驚濤駭浪的一年」。儘管，平埔族在2021年，疫情影響了辦活動的意願，但總紀錄量只些微少了去年3筆，我們發現平埔族某些族群的祭儀活動幾乎已經成為常態，不見得會受到疫情影響，這些族群為馬卡道族、大武壠族、西拉雅族、噶哈巫族、道卡斯族等。另外，平埔族群當中，展演活動的表現最積極的族群，首推大武壠族，該族群的大滿舞團演出紀錄非常多，令人欣喜。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年是一個人與人之間的關係最疏離的一年，因為疫情因素，人人自危，大家害怕群聚，於是造成所有的集體活動都受到影響，我們期待來年，疫情漸漸趨緩，藝文活動再次蓬勃，讓國人的生活增添更多的藝文氣息。</p>
