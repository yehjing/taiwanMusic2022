<h4>五、國際與兩岸交流</h4>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年，全世界籠罩在疫情的威脅，各國為了穩定疫情，紛紛關閉國門，於是，原本預定的國際交流，都因為疫情的因素取消，國內藝文團體也因為這個因素，國際活動全數取消，2021年沒有任何國際與兩岸交流的紀錄。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際交流活動因為疫情因素更動，國內藝文團體大致上有幾種處理方法。第一，活動取消不再辦理。第二，改成直播的方式辦理，如戴曉君「《Our Island》數位發行」（南島音樂串連計畫「小島大歌」）、「西拉雅族Onini竹音樂團－2021國際傳統音樂學會東南亞表演藝術研究小組第六屆學術研討會－表演節目」（Zoom平臺）。第三，直播與實體活動一起辦理。</p>
