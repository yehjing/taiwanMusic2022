<h3>貳、展演活動</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀察表1所列數字，排灣族的展演活動數量最多，達到97次，其次為魯凱族的17次，這兩族展演活動相當蓬勃。展演活動類有活動紀錄的族群計有排灣族、魯凱族、拉阿魯哇族、卡那卡那富族、馬卡道族、大武壠族、西拉雅族等。展演活動紀錄當中，比較重要的有：大武壠族「第八屆大武壠歌舞文化節－祈求雨水」（日光小林社區）、排灣族「藝起來我家 聽我唱歌吧」（金峰鄉撒布優部落）、排灣族「二峰圳百年紀念活動《環境劇場》」（二峰圳戲水區）、排灣族「一起去澎湖，那裡需要我們」（宜蘭演藝廳）、排灣族「2022臺東藝術節 胡德夫《山谷的呼喚》音樂會」（臺東藝文中心演藝廳）、排灣族「2022驚嘆樂舞 臺灣原住民樂舞饗宴」（國立國父紀念館大會堂）、排灣族「2022解憂祭 下班音樂節」（臺東縣太麻里金針山金山埡口露營區）、排灣族獅子鄉觀光發展協會「內獅村2023 Happy New Year」（內獅村大門口）、排灣族屏東縣三地門鄉公所「愛在星空閃亮」（地磨兒國小運動場）、排灣族少妮瑤．久分勒分「說故事之旅－佳佐溪之戀的謎之旅」（屏東縣萬巒鄉五溝村和興伙房）、魯凱族曉劇場「戰士，乾杯！To the Warriors!」（臺北萬座曉劇場、臺中國家歌劇院、屏東藝術館）、魯凱族青葉部落「AUBA的吶喊 音樂野餐派對」（Auba魯凱神話藝術村）、拉阿魯哇族中興國小合唱團「成功高中校友合唱團2022年度音樂會樂聲揚」（衛武營國家藝術文化中心音樂廳）等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;排灣族2021年的展演數量，僅52次，2022年數量幾乎增至兩倍，拉阿魯哇族的展演數量，相較前一年亦增至兩倍之多，大武壠族展演數量由1件到5件。可見，經過兩年的疫情阻礙，族人對於「想恢復以往榮景」的想法非常濃烈，也因此，展演活動在2022年的數量急遽升高。</p>
<h3>參、祭儀活動</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;祭儀活動的活動數量為今年7大類型當中，增加幅度最大的，排灣族祭儀活動甚至高達105筆，比展演活動的數量還多。祭儀活動比較重要的活動有：魯凱族「達魯瑪克小米收穫祭暨鞦韆祭」（臺東縣達魯瑪克部落）、拉阿魯哇族「聖貝祭」（雁爾社祭儀場、美瓏社祭儀場）、排灣族「回嘉祭敘&mdash;賽嘉收穫祭」（賽嘉部落集會場）、魯凱族「2022年部落聯合年祭暨文化自治元年慶典活動」（嘉蘭村部落）、排灣族屏東縣瑪家鄉公所「111年度瑪家鄉收穫祭暨全鄉球類競賽活動」（瑪家鄉北葉國小及轄區室內場地）、魯凱族「霧臺村村運暨豐年祭」（霧臺國小）、卡那卡那富族高雄市那瑪夏區公所「111年度那瑪夏區射耳祭暨傳統技能競賽」（瑪雅里風雨球場）、排灣族南世部落「南世收穫祭暨傳統競技系列活動」（南世部落）、排灣族高雄市原住民族委員會「都會區排灣族Masalut收穫祭」（高雄市舊左營國中旁草地）、排灣族牡丹鄉牡丹村社區發展協會「111年牡丹村masalut農事感恩祭」（新保將神明會宗祠）、馬卡道族屏東縣高樹鄉加蚋埔馬卡道文化協會「高樹鄉加蚋埔馬卡道慶典」（加蚋埔阿姆寮）、噶哈巫族「噶哈巫族牛睏山社Azem晚會」（南投縣埔里鎮牛眠社區）、排灣族南投縣噶哈巫文教協會「梅溪四庄噶哈巫過新年」（南投縣埔里鎮守城社區文化館）、卡那卡那富族卡那卡那富族青年會kanakanavu nanmaruan 「pasika&rsquo;arai（河祭）」（楠梓仙溪達卡努瓦段）、排灣族南田部落收穫祭（南田部落）、排灣族地磨兒青年會 「2022 masalut a qinaljan i timur 111年三地村收穫祭暨運動大會」（中山公園廣場）、排灣族達瓦達旺部落收穫祭暨全村運動會系列活動（達瓦達旺部落）、排灣族收穫祭暨全村運動會系列活動（達瓦達旺部落）等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;平埔族的祭儀活動也非常多，值得觀察，這些祭儀活動如：馬卡道族「加匏朗平埔族夜祭文化活動」（屏東縣萬巒鄉加匏朗部落仙姑祖廟）、西拉雅族「夜祭」（蔴荳聚落）、拍瀑拉族沙轆社文化促進會「拍瀑拉過番年」（沙轆社普善寺）、西拉雅族「吉貝耍Ta-ai夜祭」（臺南市東山區東河里吉貝耍大公廨）、大武壠族「六重溪部落夜祭」（六重溪部落）等。以上的平埔族群活動，與2021年差異不大，比較不一樣的是，2022以前道卡斯族曾有祭儀活動，但是2022年卻沒有記錄到此類活動，拍瀑拉族以往並沒有祭儀的活動，2022年卻出現了一筆祭儀活動紀錄。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比較排灣、魯凱、拉阿魯哇族、卡那卡那富族，以及平埔族之間的祭儀活動差異，前者（排灣等四族）的祭儀活動容易受到疫情、天候、交通、地理位置的影響，而會有數量上高低的落差，但後者（平埔族）卻相對穩定，平埔族雖然祭儀數量不多，但是2021和2022的差異不大，顯見，平埔族的祭儀活動，因為居住地點氣候穩定、交通便利、族人居住型態集中，因此每年的祭儀活動均已成為族人生活日常，不會有臨時變動的現象。</p>
<h3>肆、推廣教育</h3>
