<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;巴宰族今年的活動僅1件，屬於推廣教育，活動紀錄為：巴宰族臺中市政府原住民族事業委員會「看見臺中原來的路－111年度臺中市原住民族傳統文化節活動」（臺中市原住民族文化館）。</p>
<h3>拾、族群音樂發展的深入剖析</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本章節，筆者舉出四個值得分析的要點，細部探討其中的發生因素及其可行的發展方向。</p>
<p><strong>一、疫情第三年的活動復甦現象</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年的年鑑紀錄，國際與兩岸交流的紀錄與2021年一樣仍然為零，其原因是各國的表演場館避免人群聚集，人群越密集之處，就越容易染疫。而各國的邊境雖然已經慢慢解封，但是2022年對於出入境的旅客仍然設下多項限制，以至於在出入境各項措施尚未完全開放的情形下，表演團體就沒有意願出國表演。另外，線上直播的方式越來越被民眾接受，因此很多國家乾脆把音樂演出活動改成線上音樂會，聽眾只要在家裡連接網路，就可以欣賞到國外團體的演出。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際與兩岸交流以外，其他類型的音樂活動則迅速回復以往的活力，以2021年到2022年的數量相較，展演活動從94件增加到136件，祭儀活動從39件增加到135件，推廣教育從85件到75件，數量持平，傳習活動從13件到48件，研究與出版從7件到8件，文化政策從5件到1件。以上的活動以祭儀活動增加幅度最大，足足增加了兩倍之多，而祭儀活動最貼近人民的信仰生活，因此從祭儀活動的數量的增加，可以理解到一般百姓對於疫情的威脅已經慢慢卸下防衛之心，到了2022年8月以後，很多祭儀的現場，不再設下「不開放外來賓客」的警語，歡迎每一個想要參與的外賓。疫情尚未完全消失，但是我們卻已看到人民生活漸漸回復往日的活力。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一個疫情期間常見的「線上直播」操作方式，或是以數位方式出版的方式，到了2022年後期卻成為很多活動繼續使用的活動傳播形式，幾乎已成常態並沒有因為疫情減緩而消失。例如：排灣族舒適圈有限公司 「臺灣巫師祭 以巫寫巫，排灣族的神靈觀線上講座」（書適圈有限公司線上系統）、排灣族來義高中 「來義高中藝術成果展&mdash;舞動原藝（線上直播）」（來義高中活動中心）、排灣族《分水嶺 Padiyudr》專輯發行數位上架（那屋瓦公司）、排灣族《cinevuan 7鄰86號》數位專輯上架（ Matzka）、排灣族魯凱族2.0樂團 「音JOY 鐵花尋寶祭《原音重現》－2.0樂團live直播」（臺東市區鐵花商圈）等。可見，當今很多活動，為了要擴大其廣告效益或是增加收視聽眾，實體音樂會以及「直播」音樂會常一起進行，不會在海報當中特別強調。</p>
<p><strong>二、屏東縣原住民歌謠專輯亮眼出版</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年9月，由屏東縣政府出版，潘孟安發行，並由周明傑撰文的四本新書出版，這四本新書為《歌頌山林 吟唱歷史 屏東縣原住民族傳統歌謠－兒歌》、《歌頌山林 吟唱歷史 屏東縣原住民族傳統歌謠－情歌》、《歌頌山林 吟唱歷史 屏東縣原住民族傳統歌謠－勇士之歌》、《歌頌山林 吟唱歷史 屏東縣原住民族傳統歌謠－結婚歌》。這四本專輯皆以有聲書的形式呈現，每本書都附上兩張CD以及一張DVD。本次的出版計畫為第一期，2023年開始進行第二期計畫，預計再出版四本專輯，依序為工作歌、祭儀之歌、生活歌、近代歌謠。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在《歌頌山林 吟唱歷史 屏東縣原住民族傳統歌謠－兒歌》的序言上，屏東縣潘孟安縣長表示：「今天，音樂文獻的夢想成真，四本有聲書閃亮現身－《歌頌山林吟唱歷史－屏東縣原住民族傳統歌謠》(兒歌、情歌、勇士之歌、結婚歌)，本人在此要點出書籍的幾個亮點。第一，四本書的撰寫風格結合了民族學與音樂學，無論是專文書寫、文字編纂或是有聲資料呈現，都符合了學術層級嚴謹的標準，作為文獻資料或是教學材料，都可以發揮其最大的效益。第二，本書的採錄對象都是部落長者，長者的生命經驗從舊部落跨越到新環境，唯有他們能精確的傳達傳統部落文化藝術精華，唱出歌謠最真實最完整的古老面貌。第三，本著作採錄之歌謠北起霧臺佳暮，南到牡丹高士，研讀本著作可以瀏覽全縣多采多姿的族群特色，感受到北中南原住民絢麗璀璨的文化內涵。第四，聲音稍縱即逝，無法捉摸，還好，本書『攔截』耆老的原音，並以現代科技製作成CD和DVD，後代子孫從此不再因為『耆老凋零』而懊惱，歌謠生命長長久久，永不止息。」<sup>3</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;無論從專輯規模、錄音對象、調查範圍、製作品質等各方面來看，這本專輯都是空前的，其編排方式不是以各鄉一本專輯的編排，而是以一本書一個主題的方式來呈現，四本書具有極高的參考價值。</p>
<p><strong>三、音樂活動展演型態的多元化</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂活動型態日新月異，規模、方式、類型、表演地點，時時創新。在此，筆者是以「深入族人生活」這個方向來談論，有兩種音樂活動需提及。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2022年11月的活動紀錄，出現了三個有趣的音樂展演活動如下：排灣族「春日鄉鄉長候選人鄭志偉選前之夜團結音樂會」（春日村集會所）、排灣族「獅子鄉鄉長候選人『英』樂饗宴」（屏東縣獅子鄉內獅村口）、排灣族「春日鄉鄉長候選人鄭志偉競選總部成立音樂會」（屏東縣春日鄉七佳村自強一路10號）。這三個活動是2022年選舉日前一天的晚上，以及候選人總部成立大會上所舉辦的，選舉人為了吸引人氣，邀請知名的流行歌者到選舉總部演唱，製造「團結」、「熱鬧」的氣氛，藉以拉抬士氣，增加更多的支持者。本次雖然僅記錄三件選舉時的演唱會，但是實際上，選舉之夜辦理演唱會的場合相當多，因此，選舉過程中的演唱會儼然已成為選戰當中很重要的行事重點。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一個音樂深入百姓生活的例子，是越來越多的結婚場合，會辦得如演唱會一般。舉一個我們所記錄的場合，是排灣族「郭享然與吳艾妮結婚儀式音樂會」（牡丹鄉石門集會所），這一場婚禮，邀請許多知名的歌者以及舞蹈團體演出，如蒂摩爾古薪舞集、戴曉君、卓秋琴、游啟輝等，整場婚宴儼然成為音樂會一般，非常熱鬧。</p>
<p><strong>四、平埔族祭儀回歸往日榮景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年，平埔族總紀錄量為31筆，與前一年相較，數量上增加許多，要說活動上有什麼部分是可以探討的，那就是平埔族的祭儀活動了。2022年平埔族的祭儀活動，馬卡道族3件、大武壠族5件、西拉雅族4件、噶哈巫族1件、拍瀑拉族1件。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於排灣、魯凱、拉阿魯哇、卡那卡那富族，平埔族群的祭儀活動數量，受到疫情等因素影響雖也有變動，但是穩定度始終比上述四個族群好很多，何以平埔族在祭儀方面數量不多，但卻反而會比上列四個族群穩定呢？其因素如下：第一，排灣等四個族群居住地區均在山上，而平埔族群居住地區都在平地，山上的地理環境易受到天候影響，發生水災、土石流、強降雨的現象，而導致祭儀活動取消的情形，因此天候因素是影響祭儀活動因素最先要提及的。第二，排灣族許多鄉鎮，並非每年都舉辦收穫祭，以獅子鄉來說，是採行一年辦收穫祭，一年辦理全鄉活動這樣的方式，因此各部落的收穫祭就會是兩年舉辦一次。而牡丹鄉也非每年舉辦收穫祭，有的部落每年一定舉辦，如牡丹村，但有的部落幾年才辦一次，各部落舉辦節奏不一樣。第三，平埔族的祭典，祭典上一定會祭拜神明，並透過靈媒與神祖溝通，這樣的祭拜節奏就一定採行每年一次，不會有取消辦理或是兩年合併辦理的情形。而排灣等四個族群，因為諸多部落大部分的族人已經改信西方宗教，因此所謂的祭儀，很多部落有祭之名，卻無祭之實，如此的祭儀型態，幾乎就等同於「活動」，穩定度相對不高。</p>
<h3>拾壹、結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年上半年，疫情似無減緩的跡象，但是民眾「想恢復往日生活」的想法已經衝到最高點，於是，諸多音樂活動戰戰兢兢的，遵循以往的模式開辦。到了2022年後期，音樂活動開始解禁，雖然音樂活動仍舊採行防疫最嚴厲的規定，但是展演活動迅速增加，祭儀活動也開始回復到以往的狀態，因此，2022年是一個想放手一搏，但是卻又害怕疫情肆虐的一個極其矛盾的一年。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在族群音樂發展深入剖析的篇章上，我們首先探討疫情第三年的活動復甦現象，除了國際與兩岸交流以外，其他類型的音樂活動則迅速恢復往日榮景，特別是祭儀活動數量增加幅度最大，足足增加了兩倍之多，我們似乎已看到人民生活漸漸回復往日的活力。另一個疫情期間常見的「線上直播」操作方式，或是以數位方式出版的方式，到了2022年後期卻成為很多活動繼續使用的活動傳播形式，幾乎已成常態並沒有因為疫情減緩而消失。第二個探討的是屏東縣原住民歌謠專輯出版的大事，由屏東縣政府出版，潘孟安發行，並由周明傑撰文的四本新書出版，這四本新書無論從專輯規模、錄音對象、調查範圍、製作品質、影響程度等各方面來看，都能顯示其極高的參考價值。第三個探討的是音樂活動展演型態的多元化，音樂活動型態日新月異，選舉期間邀請歌手演唱，將選舉活動結合音樂會，竟成為選舉過程當中必要的手法。而在結婚典禮上，越來越多年輕人將典禮儀式刻意打造成音樂會形式，邀請藝人演唱。第四個要提到的是2022年平埔族祭儀回歸往日榮景。2022年，平埔族總紀錄量為31筆，與前一年相較，數量上增加許多。相較於排灣、魯凱、拉阿魯哇、卡那卡那富族等族，平埔族群的祭儀活動數量雖然不多，但是整體的祭儀的穩定度較高。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年是一個疫情持續威脅的一年，2022年也是疫情稍稍緩解的的一年，從活動數字上，我們似乎看到了一些曙光，期待，2023年人類的生活可以重回往日的光景，讓藝文活動再次蓬勃，民眾生活平安順遂。</p>
