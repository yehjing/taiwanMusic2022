<h3>壹、前言</h3>
<p><strong>一、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年，嚴重特殊傳染性肺炎（COVID-19）邁入第三年，疫情持續發揮其威力，確診人數上上下下，根據「新頭殼newtalk」報導當中引述臺大公衛學院教授詹長權之言論稱：</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022是臺灣新冠肺炎的災難年，在臺灣的疾病史上，從來沒有一個疾病像新冠肺炎一樣，不到半年讓全國30%的人口感染，且超過1萬臺灣人染疫死亡，並且感染新冠後的副作用『長新冠』，可能是未來影響臺灣人健康最重要的致病危險因子之一，政府應要有防疫的新思維、新指揮、新系統、新方法，控制這波疫情。<sup>1</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;可見，2022年嚴重特殊傳染性肺炎（COVID-19）的影響力並沒有停止，政府投入相當多的人力與資源，控制疫情的蔓延。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人民的文化藝術生活，因為嚴重特殊傳染性肺炎（COVID-19）的因素，影響的層面就更大了。以兩廳院表演節目為例，國家兩廳院於2021年年底即公布：「2022TIFA臺灣國際藝術節節目，18檔節目包含數位映演、線上沉浸式劇場等全數位劇場，377場演出中有333場可線上觀看，2022年2月起陸續登場。」針對此活動，兩廳院藝術總監劉怡汝亦表示：「歷經表演者染疫、封館等風暴，原本只是為了生存掙扎不得已端出的替代方案，在疫情兩年的催化之下，成為迎向未來的重要養分。」<sup>2</sup>南部原住民族以及平埔族地區，也因為嚴重特殊傳染性肺炎（COVID-19）的因素，音樂活動有了很大的影響。例如：2022年4月16日的大武壠族「第八屆大武壠歌舞文化節&mdash;祈求雨水」活動，採行實體與線上音樂會呈現。原本於2022年6月3日舉辦之「E起舞動－歌舞劇及成果公演延期公告」活動，因嚴重特殊傳染性肺炎（COVID-19）疫情趨嚴峻而做延期的公告。2022年6月14日，排灣族「來義高中藝術成果展－舞動原藝」活動，採直播的方式處理，且慎重言明，表演活動不開放外人參加。以上的活動皆因嚴重特殊傳染性肺炎（COVID-19）的因素而有了型態上的改變。</p>
<p><strong>二、研究範圍界定</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本文，筆者將活動紀錄的評論焦點集中在南部的原住民族－排灣族、魯凱族、拉阿魯哇族、卡那卡那富族，以及平埔族－馬卡道族、西拉雅族、大武壠族、巴宰族、噶哈巫族、拍瀑拉族以及道卡斯族等等。全年活動分門別類，計有展演活動、祭儀活動、推廣教育、傳習活動、國際與兩岸交流、研究與出版、文化政策等多個不同面向的觀察紀錄，不同類型有其特定指涉場域，我們根據活動的性質進行相應陳述。</p>
<p><strong>三、統計數據</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀察2022的活動數量，展演活動類總計136筆、祭儀活動類135筆、推廣教育類73筆、傳習活動類48筆、國際與兩岸交流類0筆、研究與出版類8筆、文化政策類3筆。整體看來，所有類型的文化活動，相較於去年，數量增加許多，因此，文化藝術活動數量的增加，是我們對於2022年整體表現的印象。以下，筆者將先依照年鑑記錄的7個主題，一一分析解說，並歸納出各個主題在年度紀錄上的表現。接著，筆者依照族群排列，解析每個族群今年的活動表現，並討論某些活動表現不如預期，其值得檢討之處。</p>
