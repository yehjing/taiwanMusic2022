<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜上所述， 2022年正值世界紛亂、人心惶惶的時代，臺灣宗教生態與多年發展之榮景雖然也受到前所未有的衝擊及改變。然而，在僧團及人們的努力下各類宗教音樂活動亦漸重新熱絡起來，受疫情衝擊與宗教祈安之需，2022年臺灣佛教、釋教各類活動以祭儀居多，約占61.2%，其他依次為傳習，約占26.8%；推廣教育，約占5.3%；研究與出版，約占4.4%；展演，約占1.6%；文化政策，約占0. 5%；國際與兩岸交流則最少，僅約占0. 3%（見圖15）。透過上述各項活動，儀式性與供養性佛樂舞兩大傳統的品質，於疫情衝擊下仍得以被有效地發展與維持。而釋教主業是超度儀式，且既因疫情衝擊，又無僧團支援，故今年仍以儀式為主。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;現今佛教與釋教音樂文化在臺灣之發展皆包括承繼傳統與改變。佛教以僧團為單位的各項活動，主要仍朝儀式性與供養性兩大方向發展，二者亦包括承繼傳統與改變。在儀式傳統之承繼方面，仍沿襲中國文化大革命之前的大僧團或小廟傳統，從僧團的體系或規則，乃至音樂、唱腔皆如此；在傳統之改變方面，主要包括臺灣僧團的運作情況、發展方向以及作業方式等，這些改變實際上亦反映在儀式地點、參與方式、人物時空結構、儀式形式、音樂發展方向、音樂訓練之品質、唱腔之改變、音樂之可融性以及面臨傳承危機各處之上，其改變亦發生於不同的宗教派系與不同形式的僧團。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;供養性樂舞相對於傳統而言，似乎更是一種創新，但其本身亦包含繼傳統與改變，所謂繼傳統──即承繼中國<sup>67</sup>以及世界其他佛教國家<sup>68</sup>有供養性樂舞展演之文化<sup>69</sup>，且創作取自佛教相關題材，同時輔以類似的動機、目的、功能及表演形式；所謂改變──即結合新的方法、元素、技巧及科技，呈現另一種屬於他們自己的新傳統。然而，這個新的傳統現今亦面臨新的挑戰與改變，尤其佛樂創作之錄音比表演的衝擊更大，主要的改變包括主導人及推廣團體減少、科技改變、人才有限、市場需求較少、邁向跨領域等，此外，受疫情影響與衝擊較大。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而臺灣民間的釋教，在儀式傳統之承繼方面，乃承繼中國南方民間釋教度亡傳統而來，主要出於佛之「沙門」、「緇門」所傳之葬儀功德佛事，經文化變遷，逐漸內化、涵化、在地化，已成為另一支具多元文化色彩，以佛教為主體、兼容道教與民間信仰之度亡儀式。其傳統的改變主要則包括儀式減少、參與人物減少、時空結構改變、音樂縮短、傳承亦少。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上述臺灣佛教與釋教音樂文化之改變，雖是音樂文化變遷中不可避免之內化與涵化自然現象。但令人擔憂的是，現今二者最主要且迫切之難題是皆面臨傳承危機，傳統佛教、釋教儀式音樂之傳習方式，大多為口傳，即以心印心，以聲印聲，方能領會起、承、轉、合之唱誦技巧，須於宗教環境中長期薰陶，方能對其樂曲神韻與宗教情感有深入體會。然而其傳承皆面臨人才短缺之危機：優質之傳授師資既少、有心學習此道之年輕人不多、可做深入研究之人才亦有限。對此等問題因應之道，筆者認為宜有更多的專論，來引發更多人對此文化資產之關注與了解，讓大家正視其文化價值與其所面臨之問題嚴重性，期能將影音資料設法保存，俾有心鑽研宗教學者進行研究與學習，且讓一般社會大眾有參考或賞鑑之管道。希望藉此可讓政府及僧團等相關單位，對宗教音樂之發展更加關注、支持、保存及推廣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;語末，由衷感恩慈濟、法鼓山、佛光山、靈鷲山、中台山等僧團的協助，凡提供影音、照片及在田調過程中所訪談或接觸的各法師、大德等，在此一併致上最誠摯的感謝！</p>
