<h3>柒、供養儀軌</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛教的「供養」，具有尊崇、虔誠之奉獻，可為財物供養和法供養。尤其法供養關係眾生之法身慧命，故諸供養中，法供養最。如，攝受眾生、勤修善根、不捨菩薩業、不離菩提心等為殊勝之法供養，成佛從此而入。而供養為佛門重要儀軌，亦因其體現報恩思想。佛弟子的報恩對象，除了佛法僧三寶、天地、師長、父母（四重恩），更包含眾生之恩。因而供養對象包含十法界，如每日2時過堂《臨齋儀》，誦曰：「<span class="bkt">三德六味，供佛及僧。法界有情，普同供養。若飯食時，當願眾生，禪悅為食，法喜充滿</span>」；亦如《齋佛儀》，偈云「<span class="bkt">此食色香味，上供十方佛，中奉諸聖賢，下及六道品，等施無差別，隨願皆飽滿，令今施者得，無量波羅蜜</span>」。「供養」為重要修法，佛教多數法會都含供養，更有以供養為主的儀軌，如漢傳佛教《齋天》、《齋佛儀》、《普供》、《獻供》等。除此之外，今年所記錄的藏傳佛教儀軌中，包含薈供供養法，和藥師佛千供法會，皆屬此類法儀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、《齋天儀》，臺灣佛寺多於農曆正月初九設儀供諸天，亦有寺院將《齋天儀》作為定期共修法會，如法雨寺，每月初八舉行此儀。齋天法儀源於金光明經修懺時，設供三寶諸天。《金光明經》載：諸天在金光明會時，於佛前發菩提心，誓願護持佛教、奉行正法、修諸善業、不造諸惡。並秉承佛之囑咐，護持眾生修行佛法，以其神威為人們祛惡化邪，降福消災，提攜修道者圓滿佛果。護法實為重要而艱難的工作，日夜無間、歲歲年年、發心不斷，堅持護持三寶，令正法永住。故道場僧眾也設儀酬報諸天護法恩德。清朝儀潤的《百丈叢林清規證義記》列報恩章，包括國恩、諸天恩、檀越恩等。諸天，有護法之恩，於是叢林設有齋天之規，為報恩也。《齋天儀》表酬答諸天菩薩護法之恩，也祈願祝禱諸天菩薩護佑眾生，護法安僧。齋天之儀攝於清晨，早以清勝為義，因修五戒十善，感受天然清勝之福故。其人間清勝至誠，亦在於早。而諸天喜人清勝至誠，自然受供也。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、《齋佛儀》，佛教僧團平日早齋及午齋過堂，為日行的重要功課，食的修法，設有《二時臨齋儀》。《臨齋儀》開始先行供養，先獻三寶，後施眾生。逢農曆初一、十五兩日，及重要節日，則設《佛前大供》，《禪門日誦》名《齋佛儀》，或寺院通稱《上供》。《佛前大供》舉行時間設在午齋之前，地點通常是在大雄寶殿，或者寺院設有大講堂逢多人誦經共修，亦可於講堂內行《齋佛儀》。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《齋佛儀》包含之儀程為，（一）香讚，先行拈香供佛法儀，課誦本《佛前大供》之香讚為〈爐香讚〉，而各寺施行此儀所用的香讚，則不拘〈爐香讚〉，其他香讚如〈香纔爇〉、〈戒定真香〉、〈心香五分〉、〈心然五分〉、〈珍香乍爇〉等亦可運用。（二）稱念十方常住三寶之聖號。（三）變食及供養儀，含〈變食真言〉、〈甘露水真言〉、〈供養文〉、〈普供養真言〉。（四）供養讚〈天廚妙供〉。《齋佛儀》施供法中，由〈變食真言〉、〈甘露水真言〉、到〈供養偈〉，都作普施供養。觀想此食體，即是法界。能運之心，體同虛空，供養遍法界。故偈云：此食色香味，上供十方佛，中奉諸賢聖，下及六道品。等施無差別，隨感皆飽滿。令諸施主得，無量波羅蜜。接唱「<span class="bkt">三德六味，供佛及僧。法界有情，普同供養。</span>」</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、薈供，在密法之教、理、行、果四大部內，為行之一種修持法門。薈，聚集之義，指行者聚集共修。當代格魯派的通行儀軌為《甚深妙道上師薈供供養法》。儀軌首先祈請歷代傳承上師，及諸佛降臨薈供壇城加持，清淨行者之內、外、密，使之化為勝妙的薈供壇食。外壇食，指陳設之供品；內壇食，行者自身四大；密壇食，行者意之壇食。接續上供。行者以虔誠心供養諸佛菩薩，以平等心祈請上師、本尊、空行、財神、以及六道父母怨親等一起入供席，享用薈供。薈供之末，是將大眾取用後的另一半供品，由事業金剛送出壇外供養山神土地神祗及一切守護神。薈供是具足「息災、增益、懷攝、降伏」四種事業的殊勝修法。薈供可作為行者懺悔的殊勝方便，於薈供日，在薈供壇城中，至誠懺悔與戒律相違的過失，令三業重得清淨。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、藥師佛千供法會，為藏傳佛教格魯派的修行儀軌，道場格西之唱念與修法，係依印度色拉寺僧團之傳承，誦持第五世觀音尊者所著之《藥師七佛供養儀軌－如意自在王》，至誠祈請藥師諸佛放光加持，並祈請諸佛憶持在因地及成就佛果時所發的殊勝大願，饒益參與法會者及一切眾生，令彼等違緣消除，福壽俱增，一切善願得以圓滿。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藥師佛千供修法中的「千供」，包含千盞燈、二千杯水、千支香千朵花（合為一杯）、千杯塗香、千食子，共六千杯供品。此外，念誦亦須為千遍。「壇城」象徵佛之淨土，準備供品和擺設布置的工程不小，令法會場地莊嚴殊勝。此儀軌所修供之佛，包括藥師七佛，及本師釋迦牟尼佛。示小段中譯供養文意涵，「<span class="bkt">導師釋迦頂前我頂禮，慈悲善巧誕生釋迦族，他無能勝魔軍盡摧滅，莊嚴宛如金色須彌身。於彼釋迦王前我頂禮，實設明咒勝解力化現，薈供全供惡墮盡懺悔，隨喜善行禮敬及祈請，此等諸善回向大菩提。</span>」（佛教篇影片3含此段儀式內容）念誦此部儀軌，格西持用之法器包括法鈴、金剛杵、鐃鈸、曲柄鼓。</p>
