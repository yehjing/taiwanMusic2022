<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五、普供，叢林道場於農曆過年皆行普供，於一年之始，先行報恩。學佛用功靠助道因緣，古代叢林一天到晚用功，於除夕、元旦，甚至亞朝，於寺院諸殿堂上供，如大雄寶殿、海島觀音、祖師殿、伽藍殿、韋馱殿、大寮監齋菩薩、彌勒殿、禪堂等處上供。法儀雖短，但結構完整，具有香讚、稱名禮敬、正文念誦、三真言（變食、甘露水、普供養真言）、末讚。因為上供殿堂多，一般除了第一個上供處（大雄寶殿）和最後一處（禪宗道場為禪堂）之起讚（香讚）為完整讚頌，其他殿堂則只唱「南無香雲蓋菩薩摩訶薩」。各殿上供儀軌，除了三真言相同，法儀細節各異，其中末後讚各具清楚的祝禱讚頌主旨。例舉二殿堂上供儀，一、祖師殿上供：南無香雲蓋菩薩摩訶薩（三稱）、南無西天東土歷代祖師菩薩（三稱）、《摩訶般若波羅蜜多心經》、三真言（各三遍）、〈祖師讚〉（<span class="bkt">拈花悟旨 祖道初興 綿延四七演真乘 六代遠傳燈 奕葉相承 正法永昌明 南無度人師菩薩摩訶薩 摩訶般若波羅蜜</span>）。二、彌勒殿上供：南無香雲蓋菩薩摩訶薩（三稱）、南無當來下生彌勒尊佛（三稱）、〈如意寶輪王陀羅尼〉（三遍）、三真言（各三遍）、〈彌勒讚〉（<span class="bkt">位居補處 萬德周圓 龍華樹下說真詮 願生兜率天 長侍佛前 當來授記先 南無阿逸多菩薩摩訶薩 摩訶般若波羅蜜</span>）。</p>
<h3>捌、重要佛教節慶儀軌</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;寺院的重要節慶，如紀念釋迦牟尼佛之降誕、成道、涅盤之儀式，伽藍結夏、解夏之制定，關係於僧團之象徵、歷史、與文化的重要傳承。儀軌簡要而歷史久遠，這些節慶之力量，能令不同地域及時間長流裡，各佛教傳統皆能延續宗教之正統性，維繫佛教僧團精神及傳承。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、佛降誕、成道、涅槃紀念，及浴佛儀。宋朝清規《入眾須知》（約1263），設「啟建滿散浴佛成道涅盤」之儀，曰：「<span class="bkt">第一晚報眾，來早具威儀。次早升堂，下大殿，點茶湯。出班拈香。&hellip;次舉經、回向。散浴佛偈：『我今灌沐諸如來，淨智莊嚴功德聚。五濁眾生令離垢，同證如來淨法身。』</span>」釋迦牟尼佛的降誕、成道、涅槃為各佛教傳統的重要節日，自古漢傳佛教叢林於此節日行浴佛儀式，至今仍保持相同儀軌，只是浴佛時間逐漸固定於4月初八。浴佛儀，源於印度，唐義淨法師《南海寄歸內法傳》卷四〈灌沐尊儀章〉，與其翻譯《浴佛功德經》，記錄了西國諸寺灌沐尊儀的情況。至於浴像儀法，唐三藏法師寶思惟所譯《佛說浴像功德經》載錄甚詳。從製作香湯，沐浴佛像，及浴佛功德具清楚闡明。浴佛時，唱誦〈浴佛偈〉「<span class="bkt">初於像上下水之時，應誦以偈『我今灌沐諸如來，淨智功德莊嚴聚；五濁眾生令離垢，願證如來淨法身』</span>」。<sup>19</sup>佛誕當天浴佛，不僅是宗教象徵，也有修行的法義。漢傳佛教的儀軌都具大乘佛法的根本精神：成佛、度眾生。「浴佛」雖是一個簡單的儀軌，但同樣具有清楚的教旨。行浴佛儀的當下，唱誦〈浴佛偈〉，簡單的偈詞，直指大乘教義。正浴佛時，行人了知諸如來是智慧福德究竟者，而入萬德莊嚴之果海。眾生以色受想行識此五蘊之身為我，想像為塵識情為垢，覆蓋真如佛性，於虛妄無明中起惑造業，枉受輪迴。若能透過五蘊，即超越五濁，妙覺真如圓通之體顯現，親見本來一面，證淨法身。佛與眾生，僅在迷悟之別。因浴佛儀式為佛所說，浴佛之義直指大乘之道，又為僧團的重要傳統，所以歷來佛誕浴佛法會莊嚴隆重，參與者眾多。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;浴佛慶典，可謂今年佛教儀軌線上影片最多的佛事。各寺院嚴飾浴佛台，具是精緻莊嚴。不少道場錄製大眾浴佛情況，上傳網路分享。許多寺院提早浴佛、或於週末時間舉行；有些寺院設計線上的動畫浴佛，讓大眾按照指示操作線上浴佛。佛光山許多分別院近年則有雲水浴佛，即由浴佛車將浴佛台開往鄉鎮村落或市集供眾浴佛，佛光山嘉義圓福寺別出新裁，以傳統人力車「力阿卡」，回收改裝成行動雲水浴佛車，到大街小巷供眾浴佛。5月中疫情轉重，佛誕日當天（5月19日），一些寺院雖然備好莊嚴的浴佛台，但因全臺進入三級警戒，法師們自行於寺內舉行浴佛，沒有對外開放。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年的浴佛儀，除了現場和線上記錄，也對傳統浴佛儀軌作訪問統計。臺灣原來有許多寺院於佛誕當日祝聖普佛，唱誦〈娑婆界〉，是浴佛節的經典佛讚，敘述佛陀在世間降生、修行、成道。此首讚子特殊，唯佛誕日使用，音律曲調獨一不共其他讚子。然現今僧團生活型態改變，學習唱誦各式儀軌讚偈時機變少，對此特殊讚子若無學習，便失傳承。目前熟記能唱〈娑婆界〉的法師漸少，故近10、20年已愈來愈多寺院於佛誕日不再唱誦此讚。今年狀況亦復如是，僅有少數道場仍維持唱誦〈娑婆界〉。佛誕日，寺院早課祝聖普佛，於正文之後，接佛讚，即唱〈娑婆界〉。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了釋迦牟尼佛聖誕，紀念觀世音菩薩之聖誕、出家、成道，也是臺灣佛寺甚為普遍的年度節慶，設立觀音法會，行誦經、禮懺、祝聖等法儀。今年記錄了新北市金山寺的觀音祝聖儀，承續叢林儀軌，合《禪門日誦》之祝聖法儀，含香讚〈寶鼎讚〉、〈齋佛儀〉、〈觀世音讚〉、宣疏、〈天廚妙供讚〉。其中起讚之〈寶鼎讚〉傳統為祝聖儀之用讚；〈觀世音讚〉特為觀世音菩薩聖誕祝讚（含農曆2月19日、6月19日、9月19日），皆合正統叢林祝聖之儀。祝聖之疏文，由住持修熏法師手寫，內容標指法會意旨，發揮文疏提點佛事綱領之功能。這些傳統叢林儀式知識與技術，目前臺灣多數寺院已漸模糊。金山寺之祝聖儀軌承襲叢林規制，莊嚴殊特，值得參考學習。</p>
