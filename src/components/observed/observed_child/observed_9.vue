<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《水陸法會》，全名「法界聖凡水陸勝會修齋儀軌」，為漢傳佛教最大法會。明朝蓮池大師祩宏重訂同名儀軌本，說明「法界聖凡水陸普度大齋勝會」立名之義。<sup>18</sup>法界，諸佛眾生性平等之理；聖凡，指十法界，四聖六凡事相差別；水陸，六凡所依，謂水陸空，又於水陸二處，其苦重故；普度，無不度故；大齋，以食施，若聖若凡，無不供故；勝會，因法施，六凡界中蒙勝益故。法儀主要內涵不出二者，一者約事：上奉四聖，下及六凡，一時等供，無不周徧。二者約理：即真法供養，不緣於心，不住於相，忘情照理。水陸法會啟建約需數十位至百位法師，動員人力、物力頗鉅，分為外壇和內壇佛事。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;內壇是行水陸儀的場域，儀軌從開啟結界法事，至末後送聖法儀，共分15個儀程。內壇儀軌，除了主法和尚開示佛法，並有正表和副表和尚宣唱儀文、疏表、符表等。其次有悅眾法師負責鼓鈸、木魚等法器，香燈師父負責全場香燈供品，引導法師和功德主進行法會的諸種儀節，掌控儀式進行。外壇分為大壇（梁皇壇）、諸經壇、法華壇、淨土壇、華嚴壇、瑜伽壇六大壇場，各壇場擺設及掛像皆有規矩，傳統法會為時7日。外壇部分，於圓滿日之午時前誦經或禮懺完畢，配合內壇法事，於當日下午送聖，圓滿水陸佛事。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年記錄了3場水陸儀軌，因空間運用與儀器技術搭配不同，使同一部儀軌產生不同的運作方式與參與狀況。新營妙法寺屬傳統運作，內壇齋主同在主壇大悲殿內參與儀式。圓光佛學院則因學院大樓殿堂空間充足，內壇與會人數眾多，並考慮年長者與會，故應用多媒體科技輔助，將內壇齋主分3區，主區（華嚴殿，主法法師所在之殿，部分齋主參與）、禮拜區（觀音殿，提供可以進行跪拜的齋主與會）、博愛區（國際會議廳，有座椅，令年長者坐著參與法會），第二、三區，以電視螢幕與主區同步進行。現代音響在寺院的大型法會扮演重要角色。圓光佛學院設有音響室，有專業的音響人員操作。第三場為佛光山的萬緣水陸法會，每年參與人數甚多，近年分為兩期連續舉行。今年11、12月雖然疫情緩和，但如水陸此大型活動，需多項考慮，採取諸多防護措施，調整儀式空間規劃等，以達儀式進行的安全性、效率性，而能圓滿其功德。佛光山2021年的兩期萬緣水陸法會，雖然於內、外壇之儀軌依循規制，如法執行，然而運用的技術、人力、時空場地規劃，實別於往常之操作，也是時勢造就的新水陸佛事歷史。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛光山萬緣水陸法會，內壇佛事從11月下旬於本山開啟，同時於本山大雄寶殿設置「大壇」（「梁皇壇」）實境修法，並以直播方式，開放所有民眾參與線上法會。而內壇佛事，除了部分內壇齋主參與，也提供直播，讓未入內壇壇場的水陸頭可以在佛光山，或各分別院，以線上方式參與法會。外壇，今年則結合各分別院共同修法，結眾寺諸師之力，共成外壇佛事。時間上，部分外壇和內壇分開舉行，甚至外壇的眾多壇儀，於兩期的水陸，以南北區域分別執行，讓全臺佛光山分別院的法師和信徒皆能參與修法，培福培慧，建立功德。除了動員各分別院執行外壇佛事，其中部分分別院的外壇，配合今年的網路直播技術，也設置線上共修，擴大參與的信眾人數，和超越實境法會的時空限制。</p>
