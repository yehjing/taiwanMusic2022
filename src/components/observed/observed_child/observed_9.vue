<li><strong>人物時空結構之改變</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;配合防疫措施，參與人數也有改變，前半年實體儀式參與人數大多減半，儀式線上直播因時空方便又有防疫功效，反而更多人參與。<sup>18</sup>在儀式的舉行日期、舉行時段以及儀式之長短，亦隨各僧團的運作情況而有異，例如：2022年水陸法會，圓光禪寺、金龍禪寺等於清明時節舉行，靈鷲山依舊擇農曆7月間舉行，而法鼓山則維持歲末11月27日至12月4日，各處舉行月份差異甚大。佛事時間表也有差異，例如：靈鷲山依古法、傳統時程進行，啟壇共修、結界、請上堂、告赦等仍維持在午夜進行，而法鼓山則全部於白天進行，晚上則休息。對如此改變，果慨法師表示：「<span class="bkt">基於大環境改變、佛事結構、內心修行效用、身心狀況等考量，因此法鼓山水陸法會全部於白天進行，如此調整，並不影響儀式功效。</span>」<sup>19</sup> 現今各僧團之早晚課時段亦不一，有些則是延後或縮短，其音樂內容因而有所縮減，因防疫之故，也只有少數人輪替進行。又如寶華焰口需6小時，天寧焰口需時4小時，而鼓山焰口僅需2至3小時。</p>
<ol start="3">
<li><strong>儀式形式與音樂發展方向之改變與差異</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;並非所有的僧團皆會例行性舉行中國漢傳佛教儀式，有些僧團係選擇性地舉辦，並有其自身之發展方向，最明顯之例子即是慈濟，他們除了舉行修行性與典禮性之傳統儀式，並無另外發展其他之普濟性儀式。<sup>20</sup>有些即使是修行性與典禮性儀式，亦會發展出一套與其他僧團迥異的形式，例如：慈濟花蓮精舍與慈濟醫院例行性地在固定時段播放新式佛曲，供大眾共修，且其典禮性儀式以全創的儀式結構與音樂形式發展；又如「浴佛」本為漢傳佛教傳統儀式，但自 2007年起，慈濟浴佛大典便透過全新的展現方式進行，以新創的儀式排場與佛曲〈讚佛偈〉莊嚴展開。<sup>21</sup>而今年慈濟之「浴佛」大典，結合傳統梵唄與新創形式進行，花蓮靜思堂之慈濟師父與信眾，與臺北臨濟護國禪寺師父線上同步舉行「2022佛誕祈福｣，今年形式與往年在中正紀念堂的類似，但傳統儀式元素較往年為多。音樂主要包括傳統的〈讚佛偈〉等，以及歌手殷正洋所唱之〈祈禱〉。</p>
<ol start="4">
<li><strong>音樂品質與訓練方式及唱腔之改變</strong><sup>22</sup></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今日臺灣之僧團仍有規律地安排梵唄訓練的時間，然每個僧團之訓練時間與傳授方式不盡相同，傳統口傳亦不再是唯一的傳授方式，由於現代科技之進步，攝影錄音普及帶來方便性，影音播放學習也成為僧團之另一種傳授方式。錄音之真實性可能不及原音，正如印度音樂學者Bhagyalekshmy（1991 : 86）指出：「音樂錄音只可以補充使用，但它不能替代這真實的聲音。」然而，並非所有的僧尼都有機會直接向長老或精於梵唄的僧人學習唱誦與法器之使用，<sup>23</sup>因此錄下品質好的梵唄音聲，對僧團亦不失為一種方便學習的工具，然而用此取代性的傳授方式，展現出來之音樂品質難免有失真之憾。再者，臺灣各僧團梵唄之學習並非很有系統性，譬如僧人原先向A長老學習梵唄，後又請學於另一位B長老，最後該僧人誦唱之梵唄可能又與前述兩位長老所授不盡相同。此外，亦有許多僧團同時學習兩種不同之唱腔，再者因現代影音取得方便，俾使僧團們有機會接觸或學習到更多不同的唱腔，例如法鼓山最初由廣慈法師傳授天寧腔梵唄，但法鼓山之師父亦有到外面學習其他唱腔之機會，歷經傳承，今已發展成法鼓山之獨特唱腔。梵唄傳承有其系統，男、女眾分開，焰口學習以男眾為主，<sup>24</sup>但其骨幹仍屬天寧腔，其他僧團亦有類似情況。另，果慨法師也補述：</p>
<p class="bui">在臺灣比較大的道場佛光山及法鼓山的梵唄老師都是廣慈老法師，屬常州天寧腔，但佛光山與法鼓山所呈現出來的梵唄是完全不一樣的，&hellip;&hellip;同一個老師為什麼會完全不一樣？其實是跟他們的道風有關。&hellip;&hellip;我個人感覺法鼓山的梵唄不屬任何體系，它是屬於法鼓山的梵唄。廣慈老法師曾經跟我們說：「雖然是他教的，可是唱是我們在唱&hellip;&hellip;。」這就跟我們的道風有關&hellip;&hellip;，我們的道場就是禪修為主，所以我們的梵唄轉折很少，天寧寺的梵唄本來就不是那麼的花腔。<sup>25</sup></p>
<ol start="5">
<li><strong>儀軌的修訂與沿用</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛教儀軌在其儀式中舉足輕重，大多由佛教高僧大德所制定，不僅記述儀式程序之軌則，在儀式中，讚、偈、白文、咒（真言）等儀軌內容，亦為儀式音樂之體裁及曲詞，儀軌內容具連結儀式程序與宗教教化等功用，可謂儀式、儀軌、音樂互為一體，因此，儀軌內容的修訂，亦會對儀式的程序與音樂內容產生影響。然而，2022年法鼓山大悲心水陸法會所使用的儀軌版本與臺灣其他僧團不同，法鼓山於2009年曾邀集相關學者參與修改儀軌，沿用至今，據釋常智（2009）表示：</p>
<p class="bui">&hellip;&hellip;此次儀軌所秉持的修訂大原則有三：一、以正信佛法為依歸；二、長短適中的佛事；三、合理的作息安排。<sup>26</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;於〈聖嚴長老序〉《大悲心水陸儀軌》中亦明文：</p>
<p class="bui">在二十一世紀的今天，法鼓山舉辦大悲心水陸法會的作法，具有革新的意義，也是對我們僧俗四眾一種重新的教育。我們的革新之舉，就是把原來懺儀之中，凡是源於中國民間信仰的部分，或是採擷道家、道教的俗儀之處，皆一一捨去。&hellip;&hellip;水陸中一些不合時宜的作法，譬如燒紙馬、燒紙人、燒紙衣、燒種種的牌位，這在現代來講是非常不符合環保的；況且追溯這些內容，皆非源自印度的原始佛法，而是歷代佛教中人，為了接引民間信仰的人士能夠接受正信的佛法，為此向民間信仰模仿學習才有添加之物。因此，法鼓山辦大悲心水陸法會的革新，就是針對這些民間信仰的成分，以及不符合環保理念的做法，全皆去除；去除以後，一般信眾的反應都很能夠接受。這是水陸儀軌第三次大規模的修正改進。因此，我也希望今後的水陸法會，不僅僅是法鼓山這麼做，其他道場也能夠一起嘗試改變。<sup>27</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;據果慨法師表示：「<span class="bkt">儀軌修訂的大方向及內容都是聖嚴師父還在的時候決定的。此修改後的水陸儀軌目前僅沿用於法鼓山&hellip;&hellip;。</span>」<sup>28</sup>法鼓山的法會壇場布置大多採莊嚴簡約，許多活動會結合電腦動畫，連牌位亦數位化，沒有燒牌位、燒紙馬等作法，法鼓山之法師認為只要心存誠意，上述作法不會影響法會效果。<sup>29</sup>此外，法鼓山於水陸及焰口法會的召請也有所改變，改由信眾各自口唸自己想召請之亡靈，而非照傳統儀軌內容召請各類亡靈。<sup>30</sup>果慨法師也表示：「<span class="bkt">時代在變遷，佛教、儀式也要應時宜而調整。</span>」<sup>31</sup>以筆者多次實際參與水陸法會之經驗，於前夕或當天會在佛前召請自己欲邀請之亡靈至法會現場受甘露法味。2022年，許多寺院也在各式法會迴向疏文中，添加祈求疫情趨緩、烏俄戰爭早日結束之祈願。</p>
<ol start="6">
<li><strong>音樂傳統的可融性</strong><sup>32</sup></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022臺灣各寺廟所流通之梵唄唱腔中，接近中國大陸叢林唱誦的主要派別有：南京大寶華山之律腔、常州天寧腔及閩南福州之鼓山調，其中以鼓山調為主流。以焰口音樂而言，目前臺灣所使用之「焰口」儀軌版本，文字內容雖大同小異，但唱法因地而異，俗說「七家焰口八家懺」，顯示焰口之懺法之多元，可見瑜伽焰口在華人社會流傳之廣，中國各行省，應當各有不同腔調的焰口傳播。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上述三種焰口唱腔，已被臺灣的社會所認同，並已發展成一種屬於臺灣的儀式唱腔傳統，可見焰口音樂傳統在臺灣之發展情形相當可觀。承上述臺灣中生代以及新生代的焰口施食法師，亦有混雜唱腔的情況，可見臺灣佛教音樂傳統的可融性。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這些混雜唱腔該歸於何種體系？目前常見之分類方式為依其唱腔比例多寡，指稱該唱腔偏向何種體系。然而混雜唱腔，是否亦能發展成獨具一格之另一新唱腔呢？關於此問題，仍需時間觀察，以上乃音樂文化變遷中之內化與涵化現象。</p>
<ol start="7">
<li><strong>面臨傳承危機</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;雖然焰口、水陸音樂深具藝術價值及宗教意義，且中國文化大革命後，臺灣幾乎取代中國大陸之佛教唱誦傳承地位。但這傳承地位，近來亦產生了一些變化，令人頗有「黃鐘毀棄」的危機感，法會唱誦品質良莠不齊，唱誦品質與發展方向難以把關，故導致傳統之延續面臨危機。果慨法師表示：「<span class="bkt">年輕法師對傳統唱腔不熟悉，乃至中生代以及新生代的法師間也有青黃不接的問題。</span>」<sup>33</sup>其他大小僧團也有此隱憂。</p>
