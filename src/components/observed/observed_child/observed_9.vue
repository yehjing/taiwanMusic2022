<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛光山秉持「文化弘揚佛法」之理念，自1979年起，陸續將佛教樂舞帶至國內與國際之舞臺上，帶動臺灣其他大僧團以及藝術團體供養性樂舞之展演。供養樂舞在臺灣之表演團體包括僧團與非僧團，主要進行展演的僧團包括佛光山、慈濟；非僧團的藝術團體主要包括敦煌古典舞集、臺北民族舞團、雲門舞集、優人神鼓等。<sup>10</sup>根據筆者的分析，僧團的表演方式主要包括：</p>
<p class="bkt">（1）獨唱或獨奏新佛曲（樂），無其他明顯之肢體身段表演。</p>
<p class="bkt">（2）或多或少之肢體動作（身段）表演，結合新音樂。</p>
<p class="bkt">（3）結合梵唄與各式新佛樂舞。</p>
<p class="bkt">（4）結合梵唄與中國音樂與舞蹈。</p>
<p class="bkt">（5）其他。<sup>11</sup></p>
<p>筆者也將臺灣供養性佛舞展演的創作題材，大致歸納，並分為三大類：</p>
<p class="bkt">（1）創作取自儀式之動作，以及僧團日常生活等。</p>
<p class="bkt">（2）創作取自敦煌壁畫。</p>
<p class="bkt">（3）其他：創作取自其他佛教故事、相關哲學與概念等。<sup>12</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而依上述統計，2022年受到疫情影響，各類大型供養性樂舞展演並不多，較具代表性的有夢蓮花戲劇坊舞臺劇《大寶神珠》與慈濟《靜思法髓妙蓮華》，此二作品皆屬上述第三類：創作取自其他佛教故事、相關哲學與概念等。《大寶神珠》屬舞臺劇，改編自佛教經典故事《賢愚因緣經・善事太子入海品》，傳說大海中有諸多珍寶，尤以寶珠為最，王子兄弟入海尋寶，一人欲令百姓富足安樂，另一人則為己私欲，爭奪寶珠。舞臺劇以此為本，加入另一條現代之故事線，兩相比較，發人省思。2022年《大寶神珠》曾於桃園、臺南、臺中、高雄等地演出，至苗栗苗北藝文中心進行兩場最後加演，作為最後的閉幕。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《靜思法髓妙蓮華》經藏演繹是慈濟與優人神鼓、唐美雲歌仔戲團、臺灣京崑劇團等合作，透過結合藝術、音樂、影像方式，演繹佛教經典故事、世間百態及慈濟故事。表現手法有音樂劇、傳統戲曲、手語等，透過有形的聲音影像，讓人們得以體會其中所欲傳遞之佛法內涵，將佛學思想與藝術合而為一。劇團行經日本、菲律賓、印尼、新加坡、馬來西亞等國，7月回到臺灣進行驗收，12月9日至11日於高雄巨蛋現場演出，海外各國亦同步連線一同參與此盛事。此展演結合僧團、信眾、藝術團體、科技、跨領域、跨國界，是2022年最大型且富凝聚力之動態藝術美的供養性展演（見圖1-3）。</p>
