<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;通常較具規模、有制度化之僧團方有文化政策，今年受疫情影響，各僧團推行之政策較少，相關議題主要有：慈濟關注之「環保／心靈環保／以德傳家<strong>」</strong>；法鼓山近年關注之「環保／心靈環保／慈悲／跨宗教／跨領域／藝術化」；佛光山的宗旨「以文化弘揚佛法、以教育培養人才、以慈善福利社會、以共修淨化人心」等<sup>66</sup>；靈鷲山關注之「環保／回佛對談二十週年，各宗教團結守護地球生態<strong>」</strong>；此外尚有慈濟、玄奘大學宗教與文化學系等單位關注之「人間佛教之全球化與在地化<strong>」</strong>。今年各僧團尤為關注環保議題。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;僧團之財政大多來自財團及信眾發心支持，因而大僧團於人力、財力上較有資源於困境中繼續推文化政策。若依往例，常以音樂相關活動推廣文化政策，然而今年之疫情影響表演規劃，故較少搭配推廣的音樂表演活動，而以研討會、論壇等活動輔之，相關代表性活動如：法鼓山財團法人聖嚴教育基金會於7月1日舉辦之「氣候變遷與心靈環保論壇」，希能援引聖嚴法師之「心靈環保」思想，讓民眾調適心靈，適應氣候變遷及其所帶來的社經變化。此一心靈環保思維，亦可促進修行，對於音聲佛事等部分，皆能有所裨益。8月27日至28日於花蓮縣慈濟靜思堂國際會議廳所舉辦之第二十屆印順導師思想之理論與實踐國際學術會議【人間佛教之全球化與在地化】，乃為紀念印順導師一百一十七週年誕辰暨圓寂十七週年，並促進「人間佛教」理念之弘揚與印順學之研究，各界爰舉辦此國際學術會議。另，靈鷲山於11月2日與美國德州達拉斯南方衛理會大學合作舉辦「回佛對談二十週年，各宗教團結守護地球生態論壇」，邀請猶太教、基督教、伊斯蘭教等不同宗教的朋友們，共同討論如何面對全球生態的危機。上述各論壇既是國際交流，亦為文化政策之活動。</p>
