<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以僧團為單位的各項活動，現今主要仍朝儀式性與供養性兩大方向發展，二者皆包括承繼傳統與改變。2022年度配合傳藝中心之規劃，透過文獻整理、田野訪談、活動觀察及月曆數據，具體呈現臺灣僧團常態性舉辦之各類音樂活動成果，計有：展演活動、祭儀活動、推廣教育、傳習活動、國際與兩岸交流、研究與出版、文化政策數類，擬探討人們在疫情等外在衝擊下，如何有效地發展與維持儀式性與供養性佛樂舞兩大傳統的品質；並以宏觀視野對現今之音樂文化變遷提出看法與建議。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在釋教音樂方面，通常華人認為：「<span class="bkt">釋教即佛教，指釋尊（佛祖）所説之教法。</span>」<sup>3</sup>但臺灣之民間釋教<sup>4</sup>，既不同於也不屬於一般人所認定之正信佛教叢林系統，雖然主要出自佛教「沙門」<sup>5</sup>、「緇門」<sup>6</sup>所傳之葬儀功德佛事，但歷經文化變遷，逐漸內化、涵化、在地化，已成為另一支具多元文化色彩，以佛教為主體、兼容道教與民間信仰之度亡儀式。一般人對臺灣之釋教仍不甚了解，常將佛、釋、道混淆，有鑑於此，為讓大眾能更清楚認識臺灣民間釋教與佛教、道教之關係，2022年度釋教部分之重點將透過文獻整理及田野訪談，歸納整理臺灣之民間釋教定義與概要，並闡述疫情衝擊與文化變遷下，儀式之承繼傳統與改變。</p>
