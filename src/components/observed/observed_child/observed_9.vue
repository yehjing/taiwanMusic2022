      <h2>2021年度佛教音樂活動觀察與評介</h2>
      <span>文/陳碧燕</span>
    </div>
    <hr />
    <p class="firstRow"></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年臺灣佛教音樂呈現豐富多元的面向，基於佛教寺院的儀軌法音與宗教音樂不斷地施行，及外在環境劇變衝擊，包括疫情影響、科技與網路演進、直播技術普及等，造就了今年特殊的佛教音樂活動狀況，和突破性的發展。鑒於疫情，年初以來有少數較大的佛教機制一直沒有對外開放，多數寺院則照常活動，除了5月中到8月是全面警戒封閉狀態。10至12月全臺佛寺活動恢復，包括大型佛事。今年的寺院儀式蹲點紀錄，由於許多法師們的支持協助，順利記錄了重要儀典和珍貴傳承，並將儀軌內涵、法義、常住道風、唱念法脈，甚至個人的唱誦特點真實呈現。更因現代科技運用，與後疫時期的僧俗互動維繫等多種因素，建構佛事活動能跨越時空的連結功能。除了傳統儀典之唱誦修法、法器擊奏等儀軌法音，佛教音樂之創作，作為推廣教育和弘法機制；跨界流行文化的音樂專輯、製作、錄音等，今年皆有卓越成果。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年佛教音樂篇的紀錄，顯示臺灣寺院的常住環境、法會類別、共修模式的差異性。臺灣佛教寺院，有些具悠久歷史，而僧團年紀偏大，但數十年如一日的修行功課、儀軌實行，依然遵循古制規範。有些寺院常住住眾不多，卻熱心為信徒及社會大眾宣導善法，弘傳佛教，並大作利益社會之事。有些寺院常住，具優良唱念傳統，對儀軌法音之規範和唱誦如理如法，以悠揚之音聲，攝受大眾，廣行佛事。有些道場，利用現代科技、音響設備、電視牆等設置，讓大型法會的空間運用大異往昔之法會操作。離島寺院，常住法師們傳遞正信佛教法儀並因地制宜共修內容，提供大眾學習佛法之機緣。藏傳佛教寺院，格西們帶領大眾以藏文發音念誦共修，將正統而完整的儀軌修法傳授臺灣信眾，予與會大眾和法界眾生深切的祝福祈禱。有些常住融合藝術多媒體，廣泛並深入地探究佛法、生命、藝術的聯繫，創作非凡的音樂作品長流人間。更有寺院巧思奇想，推陳出新，發明便利親和的力阿卡浴佛車，在大街小巷行走，讓行路人能於戶外隨機浴佛，成就功德。5月中疫情嚴峻，全臺進入三級警戒，隨即便有寺院將共修活動轉為線上參與。農曆7月（8-9月），全臺寺院佛事甚多，但因仍於警戒狀態，諸佛教常住轉而進行線上法會的學習與發展，觀察記錄證實其效果，各處直播版面清晰莊嚴，參與人數多，並可跨越地域、國界、時空，是今年佛教儀式與音樂的特殊景象。</p>
<h3>壹、寺院儀軌</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年所記錄的佛教寺院儀軌，類別眾多，歷史久遠，資料珍貴。長期以來寺院儀軌之制定，對僧伽修行、日用軌則，特別於團體之共修活動中更顯重要，以令行事順暢，合理合法，又能符應法義宗旨，達到共學同修，自利利他之目的。梵唄法器之運用與儀軌功能關係密切。聲唄法器可以攝眾、止斷喧亂，儀軌唱誦之威儀禮節及和雅梵音使人凝心不散、深入經義、誦鎮心神，而經唄唱誦功德多，利益人天。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;事實上，佛教僧伽之生活日用無不在禮儀與規範之中，食衣住行，唱講宣說，皆有規矩，皆為佛事。尤其是各式唱念，各類儀軌施行，所呈現的不外乎「禮」。從禮敬諸佛、聖眾菩薩，及唱念時的威儀具足、唱誦舉腔的規矩合宜、念誦速度與音調的適當合理等，在在說明佛法與禮之關係。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「儀軌」一詞，出於密教之念誦及修行軌則和經典。初為密教部經典所載關於諸佛、菩薩、天部等於祕密壇場之密印、供養、三昧耶、曼荼羅、念誦等一切儀式軌則，逐漸轉為記述儀式軌則之經典的通稱。後來寺院之許多佛事活動，尤其涉及念誦法之佛事，多稱為「儀軌」。現今普遍的佛教寺院儀軌，可概歸8類：一、戒法；二、日常念誦儀軌；三、禮懺儀；四、講經、誦經、上堂、陞座儀式；五、超薦、施食、放生儀軌；六、供養儀軌；七、重要佛教節慶儀軌；八、法門專修儀範。今年佛教篇的蹲點紀錄內容，涵括了以上各類儀軌。</p>
<h3>貳、戒法</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛弟子發菩提心，受戒、剃髮、出家修行，於形式、行為，和宗教及社會意義，經由儀軌之舉行表達，而更加深刻清楚。今年所記錄戒法部分，含三皈五戒菩薩戒法、《幽冥戒》、《八關齋戒》、短期出家等戒會。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、三皈依。學佛者，以三寶為歸依，作為證道與解脫之因。故佛弟子，亦作三寶弟子，而入佛門之初始儀禮，即為「三皈依」。皈依三寶之義，見於諸經論中。如《阿毘達磨藏顯宗論》謂「三皈依」為救濟義：以皈依三寶，能息無邊生死苦輪大怖畏故。<sup>1</sup>《薩婆多論》云：以三寶為所歸境，欲令救護，不得侵凌故。<sup>2</sup>《薩婆多論》及《優婆塞戒經》記載，對於要求戒者，須先受三皈，方得五戒。為何先教三皈，後施與戒？經云：「<span class="bkt">善男子為破諸苦斷除煩惱，受於無上寂滅之樂，以是因緣受三歸依。如汝所問，云何三歸者。謂佛法僧。佛者，能說壞煩惱因得正解脫。法者，即是壞煩惱因真實解脫。僧者，稟受破煩惱因得正解脫。</span>」<sup>3</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、五戒、八戒、菩薩戒。三寶既是三乘七眾之所歸仰，而於三界火宅中拔苦與樂則須崇戒。故戒於佛教初傳漢地時期，將其梵語「尸羅」翻為「性善」，以止過防非為義。《優婆塞戒經》云：戒者名制，能制一切不善法故。《菩薩資糧論》云：尸羅者，清涼義，離心熱惱故；安隱義，能為他世樂因故；安靜義，能建立止觀故；寂滅義，得涅槃樂因故。<sup>4</sup>《遺教經》云：「<span class="bkt">戒是正順解脫之本。故名波羅提木叉。依因此戒得生諸禪定及滅苦智慧。是故比丘，當持淨戒勿令毀犯。若人能持淨戒是則能有善法；若無淨戒諸善功德皆不得生。是以當知，戒為第一安隱功德之所住處。</span>」<sup>5</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「五戒」為佛教徒的基本戒律，謂：不殺、不偷盜、不邪婬、不妄語、不飲酒。《大毘婆沙論》云：此五戒名學處，是近事者所應學故。又名學迹，若有遊此，便升無上智慧殿故。又名學路，此為徑路。一切律儀，妙行善法，皆得轉故。又名學本，諸所應學，此為本故。<sup>6</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「八戒」，含五戒及第六不坐高廣大床，七不著花鬘瓔珞、不用香油塗身熏衣，八不自歌舞、不得輒往觀聽，九不過中食。八戒為俗家人受，以一日一夜不犯戒為要期。所謂八關齋戒者，關為禁閉義，謂禁閉八罪不犯故。又齋者，過中不食，不非時食者名齋。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「大乘菩薩戒」，為三聚淨戒，含攝善法、攝眾生及攝律儀三戒法。此戒法理曠事深，在家出家平等而受。慈雲懺主遵式云：大乘菩薩戒者，菩薩道之根本，正覺果之因基。<sup>7</sup>《菩薩瓔珞本業經》述佛陀為諸菩薩結一切戒根本，所謂三受門：「<span class="bkt">攝善法戒，所謂八萬四千法門。攝眾生戒，所謂慈悲喜捨化及一切眾生皆得安樂。攝律儀戒，所謂十波羅夷。</span>」<sup>8</sup>菩薩戒有受法而無捨法，有犯不失盡未來際。《菩薩瓔珞本業經》云：「<span class="bkt">其受戒者，入諸佛界菩薩數中，超過三劫生死之苦，是故應受。有而犯者勝無不犯，有犯名菩薩，無犯名外道。</span>」<sup>9</sup>故知眾生受佛戒，即入諸佛位。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;菩薩戒的受戒者是以本師釋迦牟尼佛為得戒和尚，文殊師利菩薩為羯磨和尚，彌勒菩薩為教授和尚，十方三世一切諸佛為尊證阿闍黎，十方三世一切菩薩為同學伴侶。菩薩戒會皆設此五支牌位，而戒會的三師和尚，前面會加「秉戒」，意為代替戒子們向諸佛菩薩秉承為大眾授戒，所以菩薩戒之三師為秉戒得戒、秉戒羯摩、秉戒教授和尚。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、幽冥戒，此戒法即為大乘菩薩戒，佛教徒除了自身得受戒法，身心清淨。並可代替過世之親人或幽冥眾生，受持淨戒，以祈遠離幽冥之苦，往生善道。誠如《幽冥戒》儀文云：「<span class="bkt">此則佛慈平等，普濟三界眾生。戒法弘施，均攝六道靈識。</span>」得受此戒，皆名第一清淨者。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、短期出家之剃度正授儀。五戒、菩薩戒，在家人即可受持，不需剃度鬚髮。出家為僧尼，謂披剃，即披僧衣、剃髮。《法苑珠林》〈入道篇〉云：「<span class="bkt">惟夫道俗形乖，淨染殊趣。由善惡不等報應不均。欲觀仁義盛德之風，當尋禮儀玄軌之範。</span>」<sup>10</sup>此禮儀玄軌之範，指的是入道之始，所行之禮節法式，以顯出家意義及功德。由形式儀禮之建立，從入道之始，便令僧俗之別，及與異教，作清楚的分野。在外型上，捨飾好、剃鬚髮。意義與發心上，如悉達太子剃髮時所發願言：「<span class="bkt">今落鬚髮，願與一切，斷除煩惱及以習障。</span>」「<span class="bkt">毀其形好，剃除鬚髮</span>」為成就無上菩提故；亦為「<span class="bkt">即發先業，使異餘出家者。</span>」<sup>11</sup>從形象和精神皆別於異教。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;短期出家欲剃度者應受五戒，次受沙彌、沙彌尼十戒。沙彌、沙彌尼十戒相為：一不殺生，二不盜，三不婬，四不妄語，五不飲酒，六不著香華鬘不香塗身，七不歌舞倡伎不往觀聽，八不坐高廣大牀，九不非時食，十不捉持生像金銀寶物。佛教僧人出家，剃髮、著染衣（壞色衣），乃為離憍慢心。戒子並發四弘誓願：眾生無邊誓願度，煩惱無盡誓願斷，法門無量誓願學，佛道無上誓願成。與會者若沒有剃度，則受《八關齋戒》。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年臺灣北、中、南區，各有舉辦在家五戒菩薩戒會之道場；《八關齋戒》則是今年許多寺院的定期共修戒法，以提供在家居士能於寺院精進用功，齊斷諸惡具修眾善。《幽冥戒》今年舉辦狀況，可分為兩種，一則為單一修法儀軌，二為大法會中結合其他儀軌之施行，前者為數較少，多數道場行此戒法，則與其他戒會（如菩薩戒），或大型法會（如水陸法會）結合，作為其中一項重要佛事。短期出家及其剃度正授儀，由東部寺院所舉辦，戒師們皆是臺灣資深的戒會法師，儀典莊嚴清淨。</p>
