<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;祭儀活動為宗教活動之主軸，尤其在此天災人禍之際，透過祭儀之祈福、祈安，更能發揮安定人心之作用。上述統計數據顯示2022年臺灣之佛教、釋教活動中，祭儀尤多，茲分述其儀式傳統之承繼與改變如下。</p>
<span><strong>一、佛教</strong></span><br>
<p><strong>（一）在承繼傳統方面<sup>13</sup></strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自1990年以後，佛教在臺灣主要包括中國漢傳佛教、藏傳佛教與少數日本佛教，其音樂傳統目前仍隨之發展。<sup>14</sup>但臺灣佛教音樂傳統，目前仍以中國漢傳儀式傳統為主，基本上當代臺灣主要之佛教儀式與音樂傳統，乃沿襲中國文化大革命之前的大僧團或小廟傳統，從僧團的體系或規則，乃至音樂、唱腔皆如此。中國各類漢傳佛教傳統儀式，<sup>15</sup>如早晚課、浴佛、各種懺法、焰口、水陸等，臺灣各大僧團仍不斷地舉行，例如佛光山、法鼓山、靈鷲山、中台山等大僧團，疫情前全年皆有大量的修行性、典禮性或傳習性的儀式音樂操練。其他祈福祛魅與普濟性之儀式與音樂，亦在臺灣各大、小道場例行舉辦。其音樂亦隨儀式遍傳寶島。<sup>16</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但是近年受疫情衝擊，較為保守，許多廟宇與精舍大多僅維持早晚課及重要儀式，其他各類儀式明顯減少，法鼓山、靈鷲山、金龍禪寺等僧團，則盡量於實體或線上正常運作，2022年仍盛大舉辦水陸與焰口法會等普濟性的儀式，希望藉由宗教的儀式力量能令冥陽兩利，亦祈求疫情趨緩、戰爭消弭。</p>
<p><strong>（二）在傳統的改變方面</strong></p>
<ol>
<li><strong>儀式地點及參與方式之改變</strong></li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儀式地點不僅限於寺院，且各僧團或喪家皆有其考量與選擇，因地域、交通、空間之緣故，為使信眾的交通更為便利，且於更寬廣的空間參與法會，僧團也會擇用寺院外的大場地。儀式之參與也不再局限於實體，亦可線上進行，今年上半年疫情仍不穩定，僧團各有其應對方式，例如：2022年「佛誕節、母親節、慈濟日」三節合一浴佛大典暨慈濟五十六週年，因防疫改以花蓮靜思精舍、臺北臨濟護國禪寺為主場地（見圖4），以直播方式進行「雲端線上祈福浴佛」，邀約大家線上浴佛，或請信眾前往鄰近之慈濟道場隨喜浴佛，取代大型實體活動，於注重防疫規範之餘，畢佛誕日慶祝、祈福之功。<sup>17</sup>「2022佛誕祈福」（見影音一）。靈鷲山於本山線上舉行第二十九屆水陸空大法會（見影音二），其中內壇啟壇開示、啟壇共修（寶篋印陀羅尼經）、結界、發符懸幡、請上堂、供上堂、告赦、佛供、請下堂、幽冥戒開示、幽冥戒、供下堂、圓滿供、圓滿香、圓滿開示、圓滿送聖，及外壇焰口、齋天、佛前大供、梁皇、晚課等有直播，其他外壇誦經則無直播（見圖5-8）。法鼓山大悲心水陸法會雖維持實體（見影音三、四），但其餘信眾仍以線上參與為主，現場11座壇場僅開放大壇供信眾隨喜參加，並於全球分支廣設共修處，盡量讓信眾在疫情之衝擊下也能參與盛會（見圖9-12）。水陸法會音樂內容豐富，難度亦僅次於焰口音樂，水陸中之特殊音樂，主要在於內壇；外壇主要則是誦經與念佛。水陸音樂體裁主要包括讚、偈、白文、咒（真言）、佛號等，其演唱形式主要分為誦、白（直白、梵白、書聲白、書梵白、道腔白）、唱。水陸音樂中以唱之形式居多的是讚、偈、真言，例如〈三寶讚〉、〈香讚〉、〈淨三業偈〉、〈回向偈〉、〈灑淨真言〉等皆以唱之形式呈現。法鼓山以禪修為主，故其梵唄轉折少，其水陸音樂也不是那麼的花腔。</p>
