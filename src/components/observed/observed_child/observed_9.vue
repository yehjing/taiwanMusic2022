<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、「結夏」、「解夏」，與《盂蘭盆法儀》。現存最早之清規，宋《禪苑清規》（1103），十卷清規目錄所列節日僅「結夏」與「解夏」，並有念誦儀式，令使分別於其他非安居之寺院時間。漢傳佛教將結夏安居期，定為約梅雨季的農曆4月15日至7月15日。在佛世時，佛弟子經九旬安居用功，於結夏期滿，多證得聖果，因此安居圓滿日也稱「佛歡喜日」。《盂蘭盆法會》，為漢傳佛教據《佛說盂蘭盆經》於結夏安居圓滿當日，舉行的供僧法儀，以拔濟七世父母離惡道之苦。《佛說盂蘭盆經》載，佛陀教示目犍連尊者，若欲解救其母免受餓鬼道之苦，可於結夏安居圓滿僧自恣日，以百味飲食、資具，以平等之心供養安居自恣圓滿僧眾，並將供僧功德回向其母，則可以救度其脫離惡道之苦，更可以此無量供僧功德超拔七世父母。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;同《浴佛儀》為經典所載法儀，《佛說盂蘭盆經》除論法會緣起，更備述如法供僧之儀。自晉至唐，許多佛門大德為此經著疏，也記錄了道俗設盂蘭盆供之景象。不僅在家士庶競修供養，出家僧侶也各出己財，造盆供佛及僧。如宗密《盂蘭盆經疏序》云：「<span class="bkt">年年僧自恣日，四事供養三尊，宗密依之崇修，已歷多載。</span>」到了宋代，這種風俗相沿不改。但是盂蘭盆供的富麗莊嚴和供佛及僧的意義減少，而代之以薦亡的行事。明朝袾宏曾針對此加以辯正。《正訛集》云：「<span class="bkt">世人以七月十五日施鬼神食為盂蘭盆大齋之會，此訛也。蘭盆緣起目連，謂七月十五日，眾僧解夏自恣，九旬參學多得道者，此日修供，其福百倍，非施鬼神食也。施食自緣起阿難，不限七月十五。</span>」確實，行盂蘭盆經儀，才能如法供僧，而於眾僧解夏自恣之日供僧，功德甚大，甚至可令惡道眷屬離苦得樂，此是盂蘭盆法會的最初目的。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年農曆7月15日，臺灣寺院行盂蘭盆會供僧者不多。許多寺院將7月訂為教孝月，非以供僧法拔濟先亡，而是行諸多佛事－禮懺誦經等，祈作功德回向，以資福追薦，故通常7月是佛事最多的時候。今年適逢疫情警戒，許多寺院將法會轉為線上直播，讓大眾配合政府宅在家，自行同步修法。不少機制，自月初便開始啟建整月法會，線上直播。於農曆十五當日，及前1、2日，是直播法會場次的高峰，大約有40個道場的法儀於YouTube直播。今年的直播技術普及，各寺院的壇城布置莊嚴，版面清晰，並配上字幕，方便共修者參與。7月的直播法會，每場線上人數幾百人到幾千人，參與人數甚多。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、藏族燃燈節，為藏人傳統燈火節，在藏曆10月25日舉辦，紀念佛教格魯派始祖宗喀巴．洛桑扎巴的圓寂。其他教派，及一般民眾也過燃燈節。當日待夜幕降臨，家家點燃酥油燈，格魯派大小寺院，於寺內外神壇點酥油燈，晝夜不滅。屏東果碩大慈普利寺屬於藏傳佛教格魯派，是北頂法王，怙主果碩仁波切在臺灣的根本道場。今年的燃燈節，是陽曆12月29日，則由桑珠格西帶領大眾舉行法儀。晚上先修薈供法《甚深妙道上師薈供供養法》，「上師薈供」結束，格西們帶領大眾繞佛，誦宗喀巴大師祈請文－〈密集嘛〉，之後，至寺院玄關五比丘浮雕像前供燈、發願、回向，圓滿燃燈節法儀。</p>
<h3>玖、法門專修儀範</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近代以來佛教僧團亦建立許多非懺法之修行儀範，如禪七或佛七儀軌等，以利專修參禪或念佛者，可於七期內，精進用功，剋期取證。儀軌之建立，經由歷代高僧大德所制，嘉惠行人用功辦道，利益難量。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、佛七，以儀軌唱誦為主，誦經、念佛、禮佛拜願、繞佛行道、回向發願等儀節完整，偈文深刻，念佛音律節奏多有設計安排，念佛儀軌之制定，以助修法者深入念佛三昧、疾證菩提。臺灣寺院長期以佛七為主要行法，有好幾處，其中埔里靈巖山寺，開山住持妙蓮長老承蘇州靈巖山寺規範，並自身長期修法般舟三昧，信解行證淨宗之法，每月所設立之佛七共修佛事，數十年來，皆有許多佛弟子參與，專宗持名六時繫念，影響最大。靈巖山寺之念佛曲韻，採用妙蓮老和尚的六度念佛，從有聲入無聲，內觀次第，進而能夠不透過名相，對彌陀真實的皈依與意境，甚為殊特。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;禪七，雖無唱誦儀軌，然而正統禪宗道場打七，整體活動之運作，包括出入往返、坐香跑香、止靜開靜、催板站板，放腿子、下座、開示等行動，全靠鐘板犍椎法器之號令而運作。禪堂法器包括：鐘、板、小魚子、引磬、香板等，其中鐘板最為關鍵，因其所擊之方式和數目，具表佛法。表法，即表理。眾生迷理，而理由事來表。禪宗之五家鐘板使用，當代以臨濟宗為主，臨濟以三玄三要為宗，每日的鐘板敲擊，即於日用中顯現。三玄指眾生之三因，正因、了因、緣因。正因是眾生本具理體，緣因和了因有功，正因得顯。正法來自正因，無處不在、充塞虛空、徧滿法界。三要為三佛（法身報身化身），是果。玄，當下具足，但看不到。要，從始覺修行至究竟覺。因中有果，果中有因，至玄至要。臨濟鐘板全日所敲擊之數目即表十法界之法。除此之外，禪堂一日坐香，從早上關大靜到晚上開大靜，有四止四開，亦由鐘板表法、引導。一次止靜，鐘板分三階段，即一板一鐘、二板一鐘、三板一鐘，止靜有淺深，一次比一次令心更安定止靜。開靜，揚板，敲「若人欲了知 三世一切佛」（在禪堂揚板第二句，三世一切佛 佛佛佛佛佛佛，煞板要加6下，共11下，從慢至快，從重至輕。）止，從體起用；開，攝用歸體。十法界都在體用中運行。</p>
<h3>拾、佛教音樂創作與弘化推廣</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當代佛教音樂創作、舉辦佛教音樂會等甚為普遍。今年記錄了幾場佛教音樂會，包含實境與線上直播。此外，長期創作佛教音樂多媒體作為弘化推廣的和南寺，於2021年推出大型的作品，限於疫情影響，轉載於YouTube播放，提供閱聽大眾親近佛法，欣賞藝術創作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「2021年佛誕音樂會」是由佛光山圓福寺及南華大學民族音樂學系合作，於5月8日在嘉義市文化局音樂廳舉辦。不過因為疫情的關係，只能開放100人進入欣賞，並以線上直播方式進行。這次演出由民族音樂學系系主任李雅禎教授領軍，樂團指揮周安瑞教授，合唱團指揮蕭呈叡教授、助理教授張誦芬及黃乾育教授改編樂曲。音樂會以合唱團、管弦樂團、國樂團不同的形式演出，音樂會的曲目大多是財團法人佛光山文教基金會將星雲法師的法語、詩、偈輯成詞庫，並公開徵曲、發表及推廣，譜曲創作而成，透過以音樂來弘法、以音樂安定社會為力量。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法鼓山「法青會」屬於法鼓山體系的青年組織，以「提昇人的品質，建設人間淨土」為宗旨。而「心潮悅音」是法青會培養有音樂長才的法青建立的佛曲團隊，自2015年成立始，不定時的聚會團練以及佛曲創作、邀請專業師資授課、每年固定參與法鼓山體系內的各項活動演出。今年「心潮悅音」於5月和11月，各有演出。目前「心潮悅音」佛曲班在詞曲創作的部分，有「日出」一首單曲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;和南寺迂園道一頻道，是和南寺於YouTube的頻道，內容包含佛學課程、禪修課程、多媒體音樂創作影片。迂園道一法師於今年的採訪中提到：透過網路和YouTube他們希望對世界和宇宙提供更多的正能量、更多的正思維，一切人類能過比較幸福的生活。今年自1月至12月於YouTube迂園道一頻道，每月皆有音樂影片上傳，主要為兩大系列：「袍修羅蘭」和「華嚴盛會」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《袍修羅蘭》最初是愚溪先生的小說，以詩和散文融合的語言，借助象徵意義的人物和故事，傳達佛家對大千世界的體悟，曾榮獲1996年文學創作類圖書出版金鼎獎。之後依《袍修羅蘭》書中8位主角，結合現代作曲家，探討躍動於生命之流的「地、水、火、風、空、見、識」七大因子匯歸真理「如來藏」創作樂曲。2021年上傳於迂園道一頻道之袍修羅蘭系列之音樂影片包含；袍修羅蘭系列之（1）地～編鐘，作曲為趙咏山，編鐘套曲之演奏者為湖北省歌劇舞劇院民族樂團。袍修羅蘭系列之（2）水～箜篌，作曲是劉錫津，演奏者崔君芝。袍修羅蘭系列之（3）火～二胡，作曲為劉文金，宋飛演奏。袍修羅蘭系列之（8）如來藏～古琴，成公亮作曲，以唐琴「秋籟」演奏。《袍修羅蘭》之七大，「地水火風空見識」，出於《大佛頂首楞嚴經》。和南寺以多媒體方式介紹深廣微妙的佛法，而音樂創作與演出，精緻講究，對藝術、法理、自然之表達細微，堪為佳作。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《華嚴盛會》為今年最重要的大型音樂創作，因為尚未完成、及疫情因素，延後音樂會演出。但創作成品，配合多媒體之影片內容，則已上傳於迂園道一頻道。目前網路YouTube《華嚴盛會》以一會一樂章的方式發表至第六章。並訂2022 年3月20日，和南寺將設七處九會壇場，禮請81位法師，由每位法師帶領6位善信各誦一卷經，連誦3遍，於一日內誦完3部《大方廣佛華嚴經》。《華嚴盛會》作曲者盧亮輝，將每會之佛法，以中國樂器呈現，包含簫、笛、笙、古箏、胡琴、琵琶和打擊樂，演奏出佛國天人之境。音樂家及製作單位：策畫監製，普音文化；製作人，楊枝梅；音樂監製，蘇文慶；音樂製作，鄭翠蘋；作曲，盧亮輝；指揮，劉至軒；簫，徐曼妮；笛，林克威；胡琴，吳孟珊；笙，朱樂寧；琵琶，梁家寧；古箏，陳妍君；擊樂，林雅雪、徐易達；錄音，江國安；美術繪圖，蔡德盛、魏彩羿；影像合成，陳偉思。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了和南寺運用多媒體製作音樂影片，弘傳佛法。亦有一些唱念甚佳的法師，將傳統儀軌唱誦，取單曲形式獨唱錄音，配合背景影像製成影片上傳YouTube提供大眾學習。其中包含今年記錄活動中之主法法師，他們在臺灣佛教唱誦頗負盛名。拾得草堂住持宗諦法師，戒德長老之弟子，錄製許多傳統寺院梵唱上傳於YouTube，頻道名為麓山無本，涵括各類唱誦讚偈，作為梵唄教學之用。淨土院住持自誠法師，今年於疫情警戒期間，上傳多首佛教唱誦，輯為「淨土梵月」系列。兩位法師皆希望藉由錄製影片保存正統唱誦，也因此令音聲傳達佛法，廣行佛事。</p>
<h3>拾壹、跨界流行佛教音樂專輯</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年第三十二屆傳藝金曲獎，10月9日在臺灣戲曲中心大表演廳舉辦頒獎典禮。佛教音樂專輯入圍了3項，包括「最佳宗教音樂專輯獎」、「最佳錄音獎」、「最佳編曲獎」，其中「最佳宗教音樂專輯獎Religious Album」有3個佛教音樂專輯入圍，分別為《光明真言》、《決戰生死》、《猿聲猿音》，由滾石國際音樂股份有限公司製作發行的《猿聲猿音》獲獎。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《光明真言》由原動力文化發展事業有限公司出版，伊藤佳代主唱。專輯名稱即是佛曲演唱內容，佛教〈光明真言〉，為佛教的重要真言，出於佛經《毗盧遮那佛說金剛頂經光明真言》，是世尊為諸眾生消滅定業而說。專輯《光明真言》以藏系佛教發音，為現代作曲和演唱方式，定位於新世紀音樂New Age類型。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《決戰生死》是由夢蓮花交響樂團、夢蓮花讚頌合唱團演出，夢蓮花文化藝術基金會出版。專輯共有12首曲目，包含〈決戰生死〉、〈清涼月光〉（交響樂演奏曲），〈希望〉、〈正念威武〉（男高音聲樂曲），〈神弓傳奇〉、〈上師請傾聽〉、〈佛子行〉、〈思念的河〉、〈踏浪而歌〉、〈虹彩〉（交響合唱曲），〈知音〉、〈永不分離〉（女高音聲樂曲）等不同類型曲目。專輯以「看我壯志凌雲 傲骨頂天立地 聽我呼嘯三界 頓令煩惱止息」為主旨介紹，欲透過宏偉澎湃交響樂音，展現佛子與生死直面對決的勇悍力量。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《猿聲猿音》由滾石國際音樂股份有限公司出版，主唱者為齊豫和張淑蓉，收錄6首曲子。除了〈送別〉是為大眾耳熟能響的歌曲，其餘5首是依漢傳佛教學佛者較熟悉的5個小咒為詞，由幾位流行音樂人作曲，包括〈七佛滅罪真言〉、〈藥師灌頂真言〉作曲為黃韻玲，〈消災吉祥神咒〉作曲小蟲，〈功德寶山神咒〉作曲項忡為，〈虛空藏菩薩憶持不忘咒〉作曲屠穎。專輯名稱「猿聲猿音」是齊豫定名。此專輯獲得第三十二屆傳藝金曲獎的「最佳宗教音樂專輯獎」。</p>
<h3>結語</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2021年臺灣社會雖然受疫情影響甚大，佛教團體的佛事活動及各類佛曲的音樂創作仍然豐富精采。無論是大小寺院、傳統或新興的道場，整年活動、課程、儀軌修法等，皆為僧團的修行功課或提供社會大眾學佛而充分安排。傳統寺院儀軌，尤其集唐宋以來的梵唄法音等，構成曲目與形式相當豐富龐大的唱誦系統，不同的音樂形式在儀式中發揮個別的功能，聲文並具，運轉多方。今年佛教篇之紀錄，見證佛教各類儀軌於寺院、社會的運作，及其重要功能；觀察了諸方常住之道風、修行法門、常行儀軌；探究了唱念傳承、法脈、傳承者之特點。新舊之概念、技術，也使儀軌唱誦有不同之詮釋。佛曲創作，和商品佛樂的演進，今年呈現的內涵，顯示佛法的表達和佛教經典的親近感，成為重要旨趣。不論是欲導引眾生入佛智慧，或宣揚佛教文化，音聲佛事作用廣大，釋門聲明之學深奧幽微。</p>
