<h3>壹、前言</h3>
<p><strong>一、研究範圍界定</strong></p>
<p>1.2022年度年鑑中，臺灣佛教、釋教各類音樂相關活動數量計有952，大多屬於立案、具代表性之佛、釋相關宗教及藝術團體所舉辦之活動。</p>
<p>2.本計畫2022年度研究對象主要含括：佛、釋教相關之儀式、音樂、宗教與藝術團體等，如：各僧團、僧侶眾、釋教業者、道長、信眾、影音公司、藝術團體、作曲家、製作人、音樂家、表演者、觀眾等。</p>
<p></p>
<p><strong>二、年度社會背景</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近五十年來，由於經濟繁榮及政治自由化之高度發展，臺灣佛教進入了一段急速發展與創新的時期，大量新興佛教團體成立，建立了自己的網路媒體、大學及慈善節目，因而廣為人知。佛教不僅在臺灣社會中扮演重要角色，在國際上亦具有相當之影響力，其中，臺灣當代佛教音樂、舞蹈更是享譽國際。<sup>1</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而在2022年的現今，諸如疫情肆虐及烏俄戰爭等，仍相繼為人類帶來一連串的災難與衝擊，全球社會依然存在許多危機：氣候變遷、環保問題、能源危機、社會解構、經濟通膨、資源重新分配、失業、臺海關係緊張等，在此世界紛亂、人心惶惶的時代，臺灣在眾神佛的護佑下，似危而安，然宗教生態與多年發展之榮景也受到前所未有的衝擊及改變。今年係臺灣正學習與嚴重特殊傳染性肺炎（COVID-19）病毒共存的一年，因疫情趨緩而產生變化之宗教活動亦漸重新熱絡起來。綜觀今年度之宗教活動，大致可分為三大期程：觀察期（1-4月）、保守期（5-8月）、復甦期（9-12月），在此時代之下，各式宗教活動不僅融合傳統與創新、人文與藝術，更藉由科技讓各活動正常運作，甚至更加發揚光大。僧團之儀式直播、線上共修，跨越領域、地域、國界、時空限制，係今年1-8月佛教儀式與音樂之常景。自9月起，民眾生活陸續恢復正常。</p>
<p><strong>三、資料來源</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本計畫所用資料主要來自文獻整理與田野調查，田調方式除了實體訪談外，亦透過視訊、電話、email、line等方式，獲取田野資料。由於科技及疫情之關係，電子資源、網路影音傳播已然是今年，甚至未來之發展趨勢。</p>
<p><strong>四、研究方法</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在佛教音樂活動方面，Turner（1982）指出：研究傳統人類學之儀式演出，如何與當代戲劇產生相關性，他將儀式定義為「<span class="bkt">針對非技術性例行場合展現的預先決定正式行為，與被視為所有影響力最初與最終起因的無形生命或力量的信仰有關</span>」，認為「<span class="bkt">儀式在本質上是表演、法令，而不是以規則或指示為主。規則雖然『框住』儀式流程，但儀式流程可跳脫其框架。</span>」（p.79）在探討劇場表演（舞臺劇）與儀式（社會劇）之間共同性時，Turner 也針對兩者提出區分，即：「<span class="bkt">儀式和劇場不同，它並未區分觀眾與表演者。儀式反而像一場聚會，領導者可能是神職人員、主辦方或其他宗教或俗世儀式專家。然而所有人在形式上及實質上擁有相同的信仰，接受相同信仰體系，並採取相同儀式或儀式行動。</span>」（p.112）上述見解提供筆者研究儀式性與供養性分類之理論參考。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據筆者《<em>Buddhist Music and Dance in Contemporary Taiwan</em>／當代臺灣的佛教音樂與舞蹈》（Ho 2008），佛教音樂與舞蹈可分為 Liturgical music-dance（儀式性樂舞）與 Para-liturgical music-dance（供養性樂舞）兩大類，當代臺灣佛教音樂文化與生態，即朝此二大方向發展，以下闡明其分類與界定：</p>
<ol>
<li><strong>Liturgical music-dance</strong><strong>儀式</strong><strong>性</strong><strong>樂舞</strong></li>
</ol>
