<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如靈鷲山國際青年團於7月15日至17日所舉辦之「第九屆國際哈佛青年營」，其主旨即透過生動活潑之營隊活動與互動學習，吸引青年之興趣，凝聚其向心力，並於營隊活動中讓青年們認識佛教信仰，亦藉由佛曲的薰陶讓學員透過歌詞，體會佛法真諦，進而達到推廣佛學及青年團「三好五德」之核心理念，在艱困疫情下，參與學員們依然展現出蓬勃朝氣，替佛教界注入活水。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一大型推廣教育活動是「慈濟歲末祝福」，此活動是慈濟每年歲末的大型活動，由證嚴法師帶領進行座談、開示，感念眾人一年之辛勞，並祝願新的一年亦能平安順遂，慈濟人同聚一堂一同為世界祈福，活動中穿插〈祈禱〉曲目，並翻譯成英文、西班牙語、葡萄牙語，讓各族群皆能接收曲中傳遞大愛之祝福。場次遍布全臺，2022年首場歲末祝福從臺東靜思堂開始，直至2023年初之最後一場社區歲末祝福。堪稱相當有凝聚力、和諧、深具意義之推廣教育活動。</p>
