<h3>伍、誦經、上堂、陞座儀式</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;講經、誦經、講法為寺院弘法與學習教理的主要活動。自佛法東傳漢地以來，講經說法儀式已莊嚴建立，以利法理之宣揚，眾心之開導。今年於此部分儀軌的紀錄，包含誦經、上堂、方丈陞座。誦經法儀，多含開經儀、誦經、與總結回向。然而漢傳佛教之念誦《大方廣佛華嚴經》，所設之起止儀式，獨立不共於其他誦經儀。且儀式特殊、唱韻講究，並因《大方廣佛華嚴經》為大乘佛法極為重要的經典，故於《禪門日誦》亦載《華嚴儀》，俾常住行其念誦法儀，並利行者讀誦修法。寺院方丈陞座，為重大儀典，每個儀程細節，都有章程規範，今年適逢臺灣佛教之大法脈，月眉山靈泉禪寺方丈陞座，得以記錄此儀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、「諷華嚴經起止儀」。漢地有3種《華嚴經》譯本，即東晉佛馱跋陀羅翻譯之六十卷華嚴，唐代實叉難陀翻譯之八十卷華嚴，和唐代般若譯之四十卷華嚴。三經具是大乘佛教重要經典，長期以來皆有許多行人讀誦修持。諷誦《大方廣佛華嚴經》，尤其是大眾共修，需要多日才能誦完。叢林道場諷誦《大方廣佛華嚴經》，莊嚴慎重，儀軌完整，唱誦腔韻精緻講究，梵唱內容獨特。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;諷誦《大方廣佛華嚴經》，若是連日讀誦，在一定期限內誦畢，亦稱華嚴七、華嚴法會。於法會期間，每日初起經前，先舉香讚，此時主法法師出位、拈香者出班依序至佛前拈香供佛。香讚畢，主經者出班，於佛前供香、禮拜，然後合掌跪誦恭敬起梵腔白〈遮那妙體〉。此為諷《大方廣佛華嚴經》不共其他經典之起經儀式與唱念。叢林宣誦〈遮那妙體〉多融合快、慢梵腔之表白，即先以慢梵開始宣唱，中段接換快梵，末段再唱慢梵，慢、快梵腔交互，使白文之唱念具有抑揚頓挫之張力。梵腔悠然莊嚴，多用於對佛菩薩之表白。〈遮那妙體〉表白既畢，大眾合掌，稱念「南無華嚴教主盧舍那佛」三次，接〈開經偈〉：「<span class="bkt">無上甚深微妙法，百千萬劫難遭遇，我今見聞得受持，願解如來真實義</span>」。〈開經偈〉後，直誦經文。當日其他的卷次，則依一般誦經之起經儀，即三稱佛號、〈開經偈〉、直誦經文。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《大方廣佛華嚴經》每誦完一卷經，誦〈補闕真言〉，再舉讚，此為經末讚，該攝整卷經文主旨。以八十卷版本，依經卷目分為以下諸讚：</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）、「佛讚」〈華嚴海會舍那如來〉（卷一之三）、</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）、「法讚」〈華嚴海會寶藏靈文〉（卷四之五）、</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）、「海會眾讚」〈華嚴海會十聖三賢〉（卷六之八）、</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（四）、「九會讚」，包含〈華嚴海會覺帝揚靈〉（卷九之十一）、〈普光明殿大法宏宣〉（卷十二之十五）、〈菩提樹下不動而升〉（卷十六之十八）、〈波騰行海雲布慈門〉（卷十九之二十一）、〈率陀天上慧日舒光〉（卷二十二之三十二）、〈高升他化自在天宮〉（卷三十四之三十九）、〈普光再會大法重宣〉（卷四十之五十二）、〈普光三會萬行圓修〉（卷五十三之五十九）、〈福城東際童子南詢〉（卷六十之八十）。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般佛經之諷經儀式，至卷末讚，再行回向，便圓滿法儀。而《大方廣佛華嚴經》卷末讚之後接「華嚴字母」之唱誦，為〈華嚴儀〉最特殊、不共於其他的經典誦持之處。字母發音於人類文明發展極為重要，《華嚴經》更強調字母和修行的關係，為解脫智慧。經云：「<span class="bkt">善男子，我得菩薩解脫，名善知眾藝。我恒稱持入此解脫根本之字。&hellip;如是字門。是能悟入法空邊際。&hellip;若菩薩摩訶薩於如是入諸字門，得善巧智。於諸言音所詮所表，皆無罣礙。於一切法平等空性，盡能證持。</span>」<sup>15</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛門如何將此殊勝獨特的字母，於〈華嚴儀〉中展現，作為修行與度眾的關鍵，則是另一門高深的學問。〈華嚴儀〉中字母音韻聲腔運用，展現聲母、字母、音律、聲調、獨唱並眾和的層次與張力，完整呈現卓越的藝術和邏輯性，也造就釋門聲業中極為優美超凡的音韻。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;華嚴字母有42字，可謂主音字母。每個主音字母各再包含12個字母，即從音字母。卷末唱誦字母的儀程，分3部分：字母讚，字母，回向。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）、〈華嚴字母讚〉。每卷經畢、誦真言、舉讚，完成整卷經儀，接續唱〈華嚴字母讚〉，稱揚唱誦字母的功德：「<span class="bkt">華嚴字母 眾藝親宣 善才童子得真傳 秘密義幽玄 功德無邊 唱誦利人天</span>」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）、唱誦「華嚴字母」、回向。每卷經畢，〈華嚴字母讚〉後接唱3個華嚴字母，42個字母十四卷唱完，第十五卷再從頭開始。每唱一個字母單元（一個主音字母，加12個從音字母），接一句小回向「<span class="bkt">普願法界眾生，入般若波羅蜜門</span>」。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）、總回向。3個字母單元唱完，接總回向文：「<span class="bkt">四十二字妙陀羅，字字包含義理多，梵韻滿娑婆，功德大(「大」唱「多」)，法界沐恩波。四生九有，同登華藏玄門。八難三塗，共入毘盧性海。</span>」</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儀軌體現大乘精神，符合經典教旨。而音樂唱誦令行人於字母之發音能清楚念誦受持，於字母之功德利益予以了解，唱誦的同時令字母得以被記誦傳承。華嚴字母揉合獨唱、法器節奏變化、大眾齊和等多層次的音響張力，而悠揚的旋律，振人的音聲，令行人發心無窮。以此功德回向法界，普施眾生，令入大乘一實之道。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;叢林諷誦《華嚴經》前，大眾會特別學習字母的唱念，悅眾法師們也會學習字母和回向文的法器板眼。華嚴字母的念誦，正確發音為其關鍵，故經典中字母皆標示切音，類似現代拼音。唱誦者應依經典所標之發音來唱誦，是最接近原始翻譯的字母發音。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「諷華嚴經起止儀」的讚偈內容，也揭示了佛教儀軌法音的悠久歷史。卷末讚〈華嚴海會〉，及〈字母讚〉，所用的旋律共通，文格相同，為同一曲牌【華嚴會】。此曲牌仍為當代佛門最通用之曲牌；所有的六句讚，都是用「華嚴會」。【華嚴會】曲牌最初用於諷誦《華嚴經》之儀軌，歷史上亦曾被民間戲曲採用，而世俗流行更替不斷，佛門中的儀軌唱念則多延續傳統，【華嚴會】見證釋門儀軌歷史。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、上堂、陞座、陞堂，皆為說法儀式。早期叢林清規，如宋朝宗賾法師所集《禪苑清規》，列有「上堂」一目，若有上堂法式，禪堂首座率大眾上法堂，待住持入法堂，陞座於禪椅，首座大眾轉身正望法座問訊，然後歸位。又述，凡遇陞堂，除寮主直堂外並赴違者山門當有重罰。又「五日陞堂，激揚宗旨」，大眾赴參，可以問話。上堂、陞座皆為叢林上敬下導，互相激揚之禮。叢林中舉凡重要佛事，都要挂牌，而最重要的佛事，不但要挂牌，還要陞座。結夏解夏、起七解七（結制解制）、傳法、上堂齋，都要陞座。陞座時，和尚說法，穿黃海清搭祖衣，平常上殿不搭祖衣。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;成佛之法，至為尊貴，說法儀式，慎重肅穆。常見重要說法儀，如禪宗禪七之「起七儀」和「解七儀」，主七和尚進堂說法語。打七，為生死大事，主七和尚所說之法至關重要。說法前，聽法者當生殷重，肅聽玄旨。故有定心引磬為導，共擊三陣，一陣一陣漸入深定。唯靜心，才能聽法。甚至念誦亦復如是，舉凡誦經，尤其是早晚課誦，亦有三陣定心木魚、定心鼓，定心後念誦。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年靈泉禪寺方丈陞座大典，包含12個儀程：典禮開始鳴鐘12響、迎請鑒交和尚及送位和尚、迎請新任方丈、上香、掛大珠、送位、新任方丈陞座說法、長老咐囑、新任方丈致謝詞、禮成、佛前及祖堂上供。</p>
