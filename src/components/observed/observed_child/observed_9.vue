<h3>陸、超薦、施食儀軌</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛教法門，謂出生死入涅槃，出苦入樂之門。而大乘佛法，菩薩用心，不務專己。學習佛陀於曠劫修習慈悲喜捨，法界眾生無所不度。行道人間，講經勸眾，設供薦親，普利存亡。佛教徒為先亡，設薦親法，資福追善；為三塗惡道眾生，拔濟令出；為人天者，得見諸佛聞法悟解。設諸超薦、施食、放生法儀，俾行人長養慈悲利益眾生，於知見和實際行為能具智行，拔苦與樂。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀察今年的佛教寺院佛事活動，除了道場定期共修課程，常行佛事可屬追資薦拔、施與法食博濟法界之儀軌，包括《三時繫念佛事》、《瑜伽集要施食壇儀》、《水陸法會》、《大蒙山施食》、《甘露施食》等。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、超薦佛事，即專為亡者施作佛事，以期超脫三惡道，甚至拔濟六道眾生，能出離三界，永脫苦輪。《三時繫念佛事》為追善拔薦，離惡向善，至於佛道淨土的法事，目前在臺灣，日行普遍。《三時繫念佛事》作者為元朝中峰明本，為禪宗臨濟宗開悟祖師，洞徹玄旨，為當時學佛者競相求法的大善知識。《三時繫念佛事》，儀軌簡要，而涵容周全。《三時繫念儀範》〈緣起〉載曰：「<span class="bkt">中峯祖師著文繫念，何云繫念，一心繫屬於佛不雜他念之謂也。乘此念心，堅固純熟，決定往生。</span>」<sup>16</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從元代始，《三時繫念》作為念佛儀軌，流傳至今已7百多年。佛事共分三時功課，每一時皆誦《佛說阿彌陀經》、〈拔一切業障往生淨土陀羅尼〉、提綱、〈讚佛偈〉、念佛、〈彌陀讚〉、講演、旋繞念佛、觀文、〈佛寶讚〉（第二、第三時分別為〈法寶讚〉、〈僧寶讚〉）、念佛、懺悔發願、〈西方讚〉。功課內容相似於佛門日課的普佛內容，行人並不陌生。但別於其他儀軌，於課誦之中，有說法部分（提綱與講演），令念佛人了知自性彌陀。整部佛事理事互融，甚為殊勝。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣道場舉行《三時繫念佛事》，主要為以下之情況：接續總結佛七之儀軌，紀念專修念佛之大德的往生佛事，及作為一般超薦之法事。而後者又可分為兩類：以此儀軌作為獨立超薦佛事，或置於大型法會之末作為總結超薦佛事。今年記錄的《三時繫念佛事》含括以上諸類，並適逢新北市靈巖山寺雙溪小築進入第七屆百七護國息災繫念法會，而見證了他們經歷12年，長年不斷日日修法的特殊歷史紀錄。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、施食儀軌，釋門之「施食」二字，法義甚廣。明朝祩宏著《施食補註》釋曰：施食，梵語「檀那」，此云布施。居六度首，為萬行先。有財、法、無畏三種布施。題雖獨標施食，而文則兼有也。蓋緣極苦眾生而起悲濟，故先施其食。食又有四種，所謂「段觸思識」。約事而言，即前三食。約理而言，即後一食。藏識具本覺不覺二義，以此識食，施為開發之義，謂開發眾生本有之覺性，俾其轉識而成智也。《瑜伽集要施食壇儀》、《大蒙山施食》、《甘露施食》等皆為施食法；《水陸法會》全名《法界聖凡水陸勝會修齋儀軌》，作供十法界法。以下概述《瑜伽集要施食壇儀》和《水陸法會》二儀，及今年舉辦情況。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）《瑜伽集要施食壇儀》，緣於《佛說救面然餓鬼陀羅尼神呪經》之施食法。經中敘述阿難尊者於夜中習定，見焰口鬼對他說：「<span class="bkt">汝過三日必當命盡生餓鬼中，汝若能平等普施無量餓鬼飲食，便得增壽。</span>」阿難請求世尊救度，佛告阿難，有一陀羅尼法，名〈無量威德自在光明如來陀羅尼〉，作此陀羅尼法加持7遍，能令一食變成種種甘露飲食，即能充足恒河沙數一切餓鬼、婆羅門仙、異類鬼神上妙飲食，皆得飽滿，皆獲聖果解脫苦身。而受持此法者，能增長福德壽命；餓鬼生天及生淨土受人天身。此陀羅尼法為寺院諸儀軌中的「變食真言」。由此漸增，而匯成諸種施食儀軌，包括瑜伽焰口施食法。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;佛教施食法，啟教於阿難，屬瑜伽部。瑜伽大興於唐之金剛智、大廣智不空二師。不空三藏譯出《瑜伽集要救阿難陀羅尼焰口儀軌經》，為施食法儀軌本，依此歷經祖師大德增訂刪修，成東土佛寺的施食儀軌。明朝祩宏法師對當時流行於坊間的瑜伽施食，有較深入的研究評論。查閱藏經典集，對此部施食壇儀中的梵文、咒、和印，校對研究，並依《瑜伽集要》，而重加參定，以成善本。祩宏所著《修設瑜伽集要施食壇儀》，及其《施食補註》對後世的施食儀影響很大。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《施食補註》釋曰：此瑜伽大本有五部，一佛部，即佛說咒。二蓮華部，諸菩薩咒。三金剛部，金剛神咒。四寶部，諸天神咒。五羯摩部，諸鬼神咒。每部復各有五，即成二十五部。於中皆權實雙彰，顯密並談。因聚集此五部的精奧要意，所以稱「瑜伽集要」。祩宏強調，瑜伽法會之登壇者，知三業相應之謂瑜伽。即手之所結，必端必嚴。口之所誦，必真必正。意之所作，必精必專。毋散心亂想而虧正觀。然後身與口協，口與意符，意與身會。以此度生，亦以此自度，功德不可思議。<sup>17</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年臺灣各地佛寺舉辦《瑜伽集要施食壇儀》甚多，尤其於大型共修佛事之後，大眾因修法精進增長善根，所獲勝大功德之時，不忘愍念救濟極苦眾生，令離諸苦同霑法利。施設《瑜伽集要施食壇儀》，是極圓滿的普濟佛事，而廣被施行，俗稱「焰口法會」，或「瑜伽焰口」。今年所記錄的焰口法會，多為一位金剛上師主法，唯新北竹林禪寺是3位金剛上師，所謂三大士焰口。前幾年，竹林禪寺曾有七大士之焰口法會，在臺灣較為少見。竹林禪寺在臺灣當代佛教儀軌唱念是一個歷史重點。由江蘇常州天寧寺方丈證蓮大和尚，並其弟子戒德與佛聲2位法師於1951年創建，傳承天寧海潮音之正統，唱念敲打卓絕特殊，為臺灣外省派唱腔代表。尤其以戒德長老的水陸內壇、瑜伽施食、大供天、華嚴經儀、及傳寶華山律腔三壇大戒，最負盛名。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;瑜伽法會施食餓鬼，應於戌亥二時施之。如此，餓鬼得食，施主獲福。法會總設三壇：主壇、面燃大士壇、靈壇。主壇為金剛上師主法施食三昧之壇；面燃大士壇，主奉觀世音菩薩；靈壇即設放靈牌之壇。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《瑜伽集要施食壇儀》可分為三個儀程：開啟法事、施食三昧、和功圓解散。「開啟法事」，包含：一警聞法界、二燃香達信、和三結界灌頂三個儀程。「施食三昧」，此為整部施食儀的主軸，全體即為身口意相應之瑜伽施食。而「功圓解散」為圓結法會程序。</p>
