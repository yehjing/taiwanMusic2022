<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該書中之戒德長老（1909-2011）是當代臺灣與中國大陸佛教音樂首要之傳承與倡導者，亦為其中剖析佛教「焰口」的核心對象。「瑜伽焰口」簡稱「放焰口」，係一結合音樂、儀式、文學而成之佛教儀軌，主要目的是祈求平安或超度亡魂，也就是為幽冥界眾生說法，是冥陽兩利之儀式，深受信眾敬重與喜愛；其音樂性之豐富與複雜，更是漢傳佛教儀式之冠。因此，戒德長老所誦唱之焰口音樂，實兼具文化傳承意義及多元價值。此書不僅透過民族音樂學之田野採集及文獻資料的整理，闡述焰口儀式之重要性，以及它在臺灣的發展與變遷；並探析焰口儀式之結構、音樂特點、樂曲型態等內容，同時結合中、西方法學，期望以較宏觀之視野，探討音樂與儀式之關係。此書堪稱是第一本以戒德長老為研究對象之影音專書，故為當代最具古老傳承性、最完整之佛教「焰口音樂」與「儀式結構」紀實與探討。隨文附圖例、譜例，另附戒德長老所唱瑜伽焰口音樂CD6片及儀式實錄（1998年）DVD4片、田野訪談之部分剪影（1999-2010年）DVD2片。此書由呂錘寬教授、曾永義院士、林谷芳教授親撰序文。<sup>58</sup>瑜伽焰口儀式結構與音樂向來皆心傳口授，要逐一整理及深入分析，實非易事，本書實有功於佛教儀式與音樂之研究。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;過去二、三十年臺灣新式佛曲之錄音與創作成果豐碩，根據筆者的分析，臺灣新式佛曲之錄音其創作題材主要包括：</p>
<p class="bkt">（1）音樂題材取自傳統儀式唱誦──改編。</p>
<p class="bkt">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（a）聲唱（或誦）並以樂器（或法器）伴奏。</p>
<p class="bkt">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（b）樂器演奏。</p>
<p class="bkt">（2）音樂創作取自其他題材──全創。</p>
<p class="bkt">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（a）取材自高僧大德的開示、經典名言或教義。</p>
<p class="bkt">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（b）取材自佛教的故事、寓言或氣氛等。<sup>59</sup></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;惟2022年傳藝中心取消傳藝金曲獎最佳宗教音樂專輯獎，<sup>60</sup>並於3月14日傳藝新聞公告中提出其原因及目前之策略：「<span class="bkt">&hellip;&hellip;出版類項目則取消最佳宗教音樂專輯獎，改以『傳統音樂』、『藝術音樂』及『跨界音樂』等音樂類型進行評選。&hellip;&hellip;在出版類項目中，為減少報名團隊於選擇報名項目時針對音樂類型和主題重疊的疑慮，並考量宗教音樂類報名組數近年呈現下降趨勢，也同時希望將出版類作品以更單純的音樂類型進行評選，今年取消最佳宗教音樂專輯獎，但宗教音樂作品仍可依音樂類型進行報名。</span>」<sup>61</sup>今年獎項之取消，實是一憾。為了讓臺灣宗教音樂能永續發展及避免被混淆，建議傳藝中心在適當時機能恢復「傳藝金曲獎最佳宗教音樂專輯獎」，或是再研擬更周延之策略，方為長久之計。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2022年新創作之佛樂作品亦相當罕見，其中具代表性者如《靜觀水月<strong>．</strong>照徹心海》（CD1片／風潮音樂出版），此作品由財團法人夢蓮花文化藝術基金會製作，主要演出者為夢蓮花讚頌合唱團，形式上結合國樂與交響樂，內容為跨心靈與宗教之音樂，音樂創作則屬上述第二類：取自其他題材──係屬全創佛曲。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據田調結果所示，2022年佛曲創作出版減少之主因如次：</p>
<ol>
<li>主導人及推廣團體減少：臺灣佛曲創作曾有輝煌的成果，當代臺灣佛曲創作之首要倡導者為佛光山之星雲大師，但近年主要推廣者們年事已高，佛光山亦表示：「<span class="bkt">近年較少發展這方面的作品。</span>」<sup>62</sup>僧團或唱片公司繼續主導或推廣者似乎也減少，或對未來發展方向未有明確規劃。</li>
<li>科技改變：現代人上網閱聽影音方便，購買CD、DVD的人與日俱減。另據靜思人文表示：「目前因電腦無DVD設備，所以很少出版DVD、CD作品。」<sup>63</sup>其他出版公司也有類似的考量。</li>
<li>人才有限，市場需求較少：據一些相關出版公司，例如：如是我聞、愛華、靜思人文、風潮、源動力等表示，有些優秀的合作作曲家已逝，要找到適合的合作對象不易，且近年市場需求少，故目前僅販售舊作品，今年則很少新作品發行。<sup>64</sup></li>
<li>邁向跨領域：依照現今及未來之發展趨勢，跨領域整合之作品較能滿足現代人的要求，故所需投入之人力與財力亦多，預算亦成為一大難題。</li>
<li>疫情影響：受到疫情影響，社會經濟受到衝擊，對創作及消費亦造成不小之影響。</li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;對於佛曲創作之未來發展，目前訪談的出版公司大多持保守態度，表示需視公司企劃部門如何規劃。雖然創新佛曲之發展，目前暫處於式微階段，但筆者認為近年佛曲創作減少僅是逗號，應不至於畫下句號，待有好的因緣俱足時，仍能再創佳績。正如有夢蓮花文化藝術基金會自信地表示：「<span class="bkt">未來會持續積極提倡佛樂表演與創作，希望每年皆能持續創作，每年至少能有一代表新作。未來也會結合流行音樂等多元形式呈現，希望能吸引更多年輕人喜歡。</span>」<sup>65</sup>希望政府及僧團等相關單位，對宗教音樂之發展更加關注、支持、保存及推廣。</p>
