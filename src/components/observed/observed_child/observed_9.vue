<h3>參、寺院日課</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;寺院僧團制度規範了僧伽日常共通活動，作為修行、鍛鍊僧伽人格培養之基礎。這些例行性佛事含朝暮二課、過堂、普請等。其中，朝暮二課含莊嚴之念誦儀式，內容涵括各類大乘佛法修行之精要，於每日早晚僧伽共同精進用功，端肅身心，淨身口意三業於課誦中，既是個人用功的方法，也是僧團共修和合之途徑。早晚課之重要性，在於其為一菴日用之重點，寺院行道之軌則。寺院若無早晚課儀，叢林精神難以凝聚啟發，僧眾修行缺乏軌章綱紀，僧團之團體性與寺院之神聖性，便難以建立。尤其當代早晚課誦內容，涵納了長期漢傳佛教諸種行門的經驗累積，整合大乘佛法的教理與精神，並能兼顧多方，以令不同根器，不同修行法門者，於大乘佛教之學習能涉入融通。故朝暮二課長期以來為僧團的核心儀軌，每日例行的共修課程。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;朝暮二時課誦內容，可以分為平日課誦、朔望課誦、普佛課誦，後二者皆以平日課誦內容為主體，增加香讚、佛讚、拜願、普佛儀，或禮祖等儀程。平日之朝時課誦和暮時課誦，皆含正文、普結回向、祝禱護神三部分。早課正文為：〈楞嚴咒〉、〈大悲咒〉、〈十小咒〉、《摩訶般若波羅蜜多心經》、稱誦「摩訶般若波羅蜜多」。回向儀文包含：〈普回向偈〉、〈讚佛偈〉、行道繞念佛號、〈觀文〉、〈晨發願偈〉、〈三皈依〉。祝禱護神法儀包含：稱誦「南無護法韋馱尊天菩薩」、〈善女天咒〉、〈韋馱讚〉。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;晚課正文分為兩套：《佛說阿彌陀經》、《蒙山施食儀》，和《八十八佛大懺悔文》、《蒙山施食儀》。前者是農曆單日，後者雙日時念誦。回向儀文包含：〈回向偈〉、〈讚佛偈〉、行道繞念佛號、〈觀文〉、〈普賢菩薩警眾偈〉、〈三皈依〉。祝禱護神法儀包含：稱誦「南無伽藍聖眾菩薩」、〈大悲咒〉、〈伽藍讚〉。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年的紀錄觀察，統計了目前臺灣佛寺執行朝暮二課的概略情況。傳統佛寺多作全堂課誦，且唱誦和法器運用純熟講究；部分新興寺院或佛教機制則於課誦內容多有簡略更動，唱念和法器之學習，比較不足。這是相對的統計狀況，非絕對性的論述。</p>
<h3>肆、禮懺儀</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大乘之義，即眾生心，該攝一切世間出世間法。心佛眾生，是三無差別。然而迷悟殊途，現量不一。覺者了知法界真如平等一味，而不覺者於無生滅中宛見生死浩然。佛教大德為啟悟眾生心，應機設教，演發諸多法門。禮懺法是大乘佛教的重要行法。因眾生不覺，無明覆蔽，而常隨貪嗔癡三毒，積集罪障，迷淪於三界，無有出期。以諸佛菩薩大慈悲力，使蒙覺悟心生慚愧發露懺悔。故古德造懺儀，令眾生能如法禮懺，達到圓滿的修法結果。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;漢傳佛教之諷經持咒修法，欲入三昧，亦須設立法儀，令行法圓滿。經由大德之創立儀範，俾行人欲以誦經得證者，或欲以持咒，或念佛得證者，皆能如法修行，得大利益。自古以來，諷經持咒禮懺法儀，即為佛弟子修行用功，疾證菩提之法。佛教篇蹲點的紀錄統計，諸禮懺法為各寺平日共修，或為年度法會的重要修法。全年之紀錄涵括多部懺儀，如《大悲懺》、《往生淨土懺願儀》、《慈悲三昧水懺》、《慈悲道場懺法》、《金剛寶懺》、《萬佛寶懺》、《三千佛洪名寶懺》等。佛教之懺法，有事懺和理懺。懺法雖殊，不外自悔。一念真淨妙心，立地心華發明。從修懺處可悟明心體。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、《慈悲道場懺法》，又名《金山御製梁皇寶懺》，是最早亦是最大部的禮懺儀，依諸經修懺悔禮敬，並學大乘法義，依此滅罪而開發心地。懺文闡明法理，帶領大眾承慈悲道場力、皈依三寶力、斷疑生信力、懺悔發心力、解怨釋結力、自慶歡喜力、踴躍志心力、發願回向善根力，能令眾生得如所願。並禮佛稱名，承諸佛菩薩慈悲攝受，令除煩惱，得廣大智慧。《慈悲道場懺法》所禮諸佛，有其表法和功德願力，包括七佛大慈心力、三十五佛滅煩惱力、五十三佛降伏魔力、百七十佛度眾生力、千佛攝受眾生力、十二菩薩覆護眾生力、無邊身菩薩及觀世音菩薩流通懺法力等。《慈悲道場懺法》共具十卷，懺文詳細周全，結構完整有序。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣佛寺行《慈悲道場懺法》所用的懺本有兩種，一是古叢林版，一是通行版，兩者皆行之已久。兩版本之根本結構：懺法正文（含懺文與佛號），一致無別。但後者除此之外，於每卷增加了啟讚，末讚，和出、入懺文等。除了版本，諸寺之唱念方式亦有差異，尤其是佛號唱念部分區別明顯。《慈悲道場懺法》十卷共六十五進佛號，叢林版的禮佛稱名，以單一韻唱腔禮拜，而使用通行版的寺院幾乎皆以多種唱韻曲調，於每一進佛號唱誦時輪替運用。目前施行叢林版本和單一唱韻禮佛的道場為臺北臨濟護國禪寺、十普寺，和新北金山寺，此3處皆有蹲點紀錄。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、《慈悲三昧水懺》，為唐朝懿宗皇帝時期悟達國師所作，共為三卷。《慈悲三昧水懺》結構如《慈悲道場懺法》，唯內容較後者簡短。唱誦正文、讚偈、禮佛稱名等，皆同《慈悲道場懺法》。新北瑞芳金山寺於每年農曆正月初四至初六，讓民眾到寺禮拜水懺，轉民間習俗祭太歲，為禮佛懺悔發願進道，轉煩惱為菩提，極為圓融善巧。金山寺的《慈悲三昧水懺》，如同其《慈悲道場懺法》之唱念，所有佛號稱名唱同一韻，且東西兩班同一佛號同時禮拜，而非分為兩班輪流禮拜，於臺灣道場中獨樹一格。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、《大悲懺》、《往生淨土懺願儀》等天台懺儀，皆依隋朝智顗大師天台教觀，正行三昧修法，制有十科行軌，而以理觀為主。天台懺儀結構和其他懺法不同，智顗《法華三昧懺儀》之十法為，一嚴淨道場法；二淨身法；三修三業供養法；四請三寶法；五讚嘆三寶法；六禮佛法；七懺悔勸請隨喜回向發願法（五悔法）；八行道法；九誦經法；十坐禪實相正觀法。《大悲懺》、《往生淨土懺願儀》分別為宋朝天台大德知禮與遵式法師所制，亦以十科修法之結構，禮佛懺悔行道誦經坐禪觀行，發願專精，以令正行三昧，身心清淨。法儀結構相似，禮懺起止儀相同，但懺法所修之經典不同，故於「誦經」法時，各有所持。《大悲懺》，全名《千手眼大悲心咒行法》，以誦持〈大悲咒〉為主要行法。《往生淨土懺願儀》，則以讀誦《佛說阿彌陀經》，及持《拔一切業障根本得生淨土陀羅尼》，行五悔法門（懺悔、勸請、隨喜、回向、發願）等懺願之法，作為淨土法門的禮懺儀。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《大悲懺》於叢林的共修歷史已久，元朝天台宗清規《增修教苑清規》，由自慶法師編述，卷下之〈安居門〉記載寺院修建《大悲懺法》：「<span class="bkt">四月二十日晨朝啟修大悲懺法，前一日堂司行者覆住持兩班諸寮，仍掛修懺牌報眾。&hellip;所修行法次第具如補助儀，凡儀理觀皆悉精熟，於此總持生決定信，起精進心。於相無相盡力修之，現世障惱皆除，淨土往生不忒。廣論利益具載經文。</span>」<sup>12</sup>清《雨山和尚語錄》卷三〈住延令慶雲禪寺語〉載：「<span class="bkt">元旦，禮千佛大悲二懺。</span>」」<sup>13</sup>然而於臺灣早期佛教道場懺法，則多修法《金剛寶懺》或《藥師懺》，直到1940年代後，由大陸叢林來臺法師們將《大悲懺》弘揚推廣，現在已成為許多寺院之定期共修課程。臺灣《大悲懺》的唱誦曲韻，主要為戒德長老、廣慈法師、明常長老等系統。這3個系統，多行於北部，板眼平緩莊嚴，音調有點差異，但基本腔韻相近。中南部的《大悲懺》唱韻則因地區而各有變化。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、《金剛寶懺》，全名《大乘金剛般若寶懺》，異於以上諸懺法，因其儀軌以整部《金剛般若波羅蜜經》為儀文主體，和以懺悔文作為儀文內容，或天台懺法之十科行法，明顯不同。而《金剛寶懺》作為禮懺儀，是因其包含稱誦禮拜佛號的部分，和其他懺儀相似。《金剛寶懺》稱誦的佛號，從過去七佛開頭，接著是賢劫諸佛名號，共有十二進佛號。懺儀分上中下三卷，每卷四進佛號之稱名禮拜。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不同於其他懺法，《金剛寶懺》之上中下三卷之第一進佛號前，各先念誦〈讚佛偈〉，後直接稱佛名號，其他段落之佛號，由經文直接佛號。一般懺儀由懺文接佛號多有過渡的儀文銜接，如「某等志心頂禮求哀懺悔」，或「今當皈命一切諸佛」後接念佛號；或以偈文連接，如「啟運慈悲道場懺法 一心皈命三世諸佛」後接佛號。這樣的儀文或偈文於唱誦上（音韻變換），和行動上（由跪姿到起身）有轉換過渡之功能。《金剛寶懺》念誦結構主要為：經文－佛號－經文－佛號，並無其他儀文或偈詞連接、過渡。主要原因則與此部懺法之正文乃一部完整的經典有關，經典的完整性，也成為另一種儀軌的連續性。此外，《金剛寶懺》懺儀開頭之「旃陀羅引」，也是其獨特之處，道出《金剛般若波羅蜜經》起經時，先啟請八金剛和四菩薩之因緣，也說明誦持《金剛般若波羅蜜經》及禮拜《金剛寶懺》之功德。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五、《萬佛寶懺》，所禮經典為《佛說佛名經》，元魏北天竺沙門菩提留支譯，共十二卷。此經釋迦牟尼佛無問自說過去未來現在諸佛名字。所舉佛名，一萬一千一百佛。經云：「<span class="bkt">若善男子、善女人，受持、讀誦諸佛名者，是人現世安隱，遠離諸難，及消滅諸罪，未來當得阿耨多羅三藐三菩提。</span>」<sup>14</sup>禮萬佛寶號、禮萬佛懺、禮千佛等，自古即為佛門的重要行法，載於諸多祖師語錄中。臺灣禮《萬佛寶懺》最主要、歷史最久的佛寺為臺北臨濟護國禪寺。臨濟禪寺禮拜《萬佛寶懺》起於盛滿法師。盛滿法師於1947年由福州鼓山湧泉寺來臺，欲興臺灣佛教，始於臨濟禪寺禮拜《萬佛寶懺》至今。臨濟禪寺禮拜萬佛寶號之唱念也是獨特的單一韻唱法。此部經典的主要內容，即是佛名。此懺儀的重點，也是敬拜禮佛。於稱名禮拜萬佛時，從始至終皆唱同一韻。一韻到底，在禮萬佛時，能令人專心禮拜，久成三昧，功夫不打失。如此以單一唱韻禮佛稱名，於臺灣諸寺之諸懺禮佛多韻唱誦中獨樹一格。近年，尤其是農曆春節期間，已愈來愈多寺院舉辦禮佛懺儀，其中多數頂禮《三千佛洪名寶懺》，禮拜萬佛的寺院較少，今年禮萬佛懺之寺院包括臨濟禪寺、超峰寺、大仙寺。</p>
