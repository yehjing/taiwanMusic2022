<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;施食三昧可分為方便加持，與正行三昧。各儀程皆具手印、持咒、觀想三淨法。方便加持，即正施食之前方便，先觀淨法界，結界、淨身器界，含〈淨法界真言〉、〈點淨真言〉、〈加持花米真言〉、〈加持鈴杵真言〉、〈降魔偈〉。花者因義，以此米為因。誦真言時，想成智慧光明種，然後加持鈴杵。持杵振鈴誦〈降魔偈〉，最後供散花米。誦真言時手拈花米，將花米散空中，想此所結梵界道場光網莊嚴如因陀羅。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;正行三昧包含兩部分：自成曼荼門，和成他曼荅門。因緣無主，我心自空，是佛說因緣義。今觀諸法因緣皆無自性，是即曼荅妙行之門。從此以起，正明行願相資，建壇設供，莊嚴淨土。先發願皈依三寶，次請三壇主：娑婆教主釋迦如來，甘露教主觀世音菩薩，興權啟教之主阿難陀尊者。伸偈焚香讚請，然後獻供。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;正立曼荼分頌內心曼荼，和立曼荼自體。（1）、頌內心曼荼、含頌成身－〈自性偈〉，頌淨地－〈淨地偈〉，誦持〈音樂咒〉，凡唱此咒，如鈞天普奏，五音和雅，六律交暢，微妙通徹，供養十方三寶也。（2）、立曼荼自體：分香水塗壇、立壇基位、寶壇出生三段。香水塗壇以戒度取白檀香水，三灑曼荼以加持之，誦真言唵斡資囉麻明啞吽。立壇基位：行者自以勝智觀心內外皆無所有三世等同虛空，今將出生金剛界大曼荼羅五佛寶閣。界聖住位：五佛寶閣四方四門，中八金剛柱為嚴飭，安住聖位。寶壇出生：持誦〈音樂咒〉，以微妙音樂殊妙供養；〈寶錯真言〉，想寶錯內出生無盡諸天如意真寶而奉獻之；〈撒花米真言〉，手拈花米撒於寶錯上以加持之，俾用之時令無障礙也）；〈遣魔真言〉，遣境界魔；〈真空呪印〉，由結此印誦真言故，其壇中諸佛菩薩及諸聖眾，量同虗空，徧周法界，成報土佛剎。至此不特唯心變現，而且轉成受用報土。以下復有〈音樂咒〉、〈十二因緣咒〉、〈曼拏囉真言〉等不思議供養。此以報土妙曼拏囉，及妙供具，總獻上師三寶。後唱誦〈三皈依讚〉。成他曼荼門：包含皈依發心，修施法供，回向三寶三部分。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（1）、皈依發心：「<span class="bkt">歸依金剛上師。歸依佛。歸依法。歸依僧。我今發心。不為自求人天福報。聲聞緣覺。乃至權乘諸位菩薩。唯依最上乘發菩提心。願與法界眾生。一時同得阿耨多羅三藐三菩提心。</span>」</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（2）、修施法供，則分上奉三寶，與下施六道。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「上奉三寶」分四部分：（一）入壇請聖，表白舉香花迎請，奉請三寶及金剛密跡，護法神王等，再「印現壇儀」，即雖現壇儀，意兼密請。謂上所建曼怛，在於印上一時顯現。（二）修明智行，默轉心經一遍，此正應轉法輪之意。（三）啟告本事，正敘施食本意，原始要終。（四）奉申供養，包含申供與讚禮，即伸五供養、奉施飲食。結〈運心供養印〉、次結〈獻佛印〉（一舊作遣魔印）、次結〈變空印〉、次結〈奉食印〉）、振鈴念〈奉食偈〉。讚禮：誦〈三寶讚〉「<span class="bkt">世尊大慈妙莊嚴，明解圓滿一切智，能施福慧如大海，於諸如來我讚禮&hellip;。</span>」雖名為「三寶讚」，而文體和音樂是偈頌唱法，即每句字數一樣，每兩句為一個旋律牌，重複唱頌。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「下施六道」，上段誦「三寶讚」後，接續同樣的偈誦音調，由侍者宣唱「普陀落伽常入定」偈，接〈入觀音禪定〉（以前乃是定中境界。今復結印，正請智佛冥熏加持四處。今我父母所生之身，頓同菩薩堅固不壞之金剛身也）。施六道之法，以三昧行化，含破獄請召，鉤壇授法。「破獄」：誦「<span class="bkt">若人欲了知 三世一切佛 應觀法界性 一切惟心造</span>」次結〈破地獄印〉。「請召」：一請幽冥教主，二召法界六道。鉤壇授法，含拔除苦障，加稱聖號，施與法食三部分。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.拔除苦障，誦「<span class="bkt">阿彌陀佛宿有無邊誓&hellip;我以大悲佛神力，召請冥陽諸有情&hellip;</span>」，結〈召請餓鬼印〉（此是密召，先偈次印）。接續結〈召罪印咒〉、〈摧罪印咒〉，〈破定業印咒〉、〈懺悔滅罪印咒〉、〈施甘露印咒〉、〈開咽喉印咒〉。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.加稱聖號，上師稱讚七如來聖號，令眾生永離三途八難之苦，常為如來真淨佛子。七如來聖號為寶勝如來、離怖畏如來、廣博身如來、妙色身如來、多寶如來、阿彌陀如來。復稱世間廣大威德自在光明如來名號，所謂統眾德而大備。次授〈三皈依〉，結〈三寶印〉。次發菩提心，結〈發菩提心印〉。誦真言時。應想心佛眾生三無差別。授〈三昧耶戒〉，結〈三昧耶戒印〉。一攝律儀戒，是斷德因，修成法身。二攝善法戒，是智德因，修成報身。三攝眾生戒，是恩德因，修成化身。至此為無畏施，和法施。施與法食，令破煩惱障。今發大心，智慧從茲而起。一受大戒，禪定因之而生。定慧具足，此名斷惑。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.施食印咒：此部分為施食，結〈無量威德自在光明如來印〉，施冥陽諸有情甘露食，以法力加持使食遍十方，變此一食為無量食。復結前印，誦〈乳海真言〉。（融彼諸食皆成乳海，如會眾流統為一味，普施餓鬼。令彼身心皆悉飽滿受用無乏。）誦〈障施鬼真言〉，恐彼等業障深重卒難解脫，或見飲食仍變猛火，今再誦密言加持前食，盡成法食。普供養印咒「<span class="bkt">神咒加持淨法食 普施河沙眾鬼神 願皆飽滿捨慳貪 速脫幽冥生淨土</span>」，次結〈普供養印〉（上來所建種種供養，普供於六道也。）至此，施食已畢。大眾同念〈尊勝咒〉令圓滿佛事。圓滿有二義：神咒圓滿，及善根圓滿。誦咒時表白取壇上花米，向六道前旋轉俵散。想諸鬼神觸此花光，皆得往生極樂淨土。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「化圓獲益」，施法圓滿，振鈴誦〈法樂偈〉「<span class="bkt">承斯善利。地獄受苦有情者。刀山劍樹變化皆成如意樹。&hellip;</span>」承斯善利者，謂此施食之善利，五趣皆轉苦具為樂緣。回向三寶：〈發願回向偈〉。此處回向具有三義：一回向一切有情，二回向無上菩提，三回向真如實際。若無回向，則被相縛，盡屬有漏之法。若回己功德而向實際，則一一功德皆如法性廣大無涯，無有不成佛也。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;功圓解散，法儀之圓結程序，含奉安囑護、安住三摩、結願回向。「奉安囑護」，囑護天龍，誦〈藥叉偈〉。誦此偈，以成善報圓滿，意在誡勉護法神，唯當精誠護法，并衛國護民。到此，下五佛冠，誦咒。「安住三摩」，誦〈金剛薩埵百字咒〉，此咒為補闕也。若行者手印不次第，觀想不專注，真言誵訛等過，誦此咒已，悉皆清淨。此外，行者誦此咒，金剛薩埵恒作護持，而令安住三昧。若修行佛事，求願補闕，功德無量。「結願回向」：「<span class="bkt">皈依三寶 上來設放瑜伽焰口 平等甘露法食 功德圓滿 無限良因 普沾沙界 和南聖眾。</span>」（此復皈依三寶，請求證明。）金剛上師下座，至佛前唱〈回向偈〉。「<span class="bkt">施食功德殊勝行 無邊勝福皆回向 普願沉溺諸眾生 速往無量光佛剎 十方三世一切佛 一切菩薩摩訶薩 摩訶般若波羅密。</span>」此總回向，正行者所集功德，盡以回施法界有情，同生西方無量佛剎。見佛聞法，入不退地，證無上道。</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《瑜伽施食壇儀》的音樂元素，是漢傳佛教儀軌之中，最複雜多元的。曲牌的運用最多，唱腔豐富，並用戲曲的唱念技巧，尤其在重要偈文的起腔，特別加強其唱韻，而宛轉抑揚風韻綿長，大異於其他的儀軌偏重經文及偈誦的端正莊嚴，故曲調多精簡平整。此外，法器的運用，大大豐富了此施食儀的音樂和節奏內涵。主要法器包含：法鈴、鈴鼓和木魚。如同唱腔的高揚宛轉多變，法器在此法會的運用，不同於其他儀軌的平緩簡要，而是層次多樣，繁複精彩。此段儀程，各法式連結，環環相扣，故主要為偈、咒、和白文，少有讚子。一般儀軌，讚子是相對咒偈，音樂比較豐富的曲牌唱誦。然而在瑜伽施食儀，施食三昧為主要法式內容，偈與咒的音樂、唱念，反而是重點。唱腔悠揚微細深刻，能攝心觀想，又能導俗入道。唱誦同時，配合法器之節奏。除了鼓和木魚，其他悅眾師父，同金剛上師，都是持鈴。於施食儀程中，宣唱偈文、咒語，多同時振鈴。整體而言，不論是唱念、節奏、還是法器音響，處處充滿張力和音樂性。如唐朝僧人道世形容佛教唄讚之聲：「選勝聲以啟軸，宮商唄發，動玉振金，反折四飛，哀悅七眾。」若以此言敘述《瑜伽集要施食壇儀》，亦甚恰當。</p>
