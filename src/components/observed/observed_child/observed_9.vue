<p><strong>（二）儀式傳統的改變</strong></p>
<ol>
<li><strong>儀式少</strong>：近年受疫情之衝擊與影響，釋教傳統儀式也產生了改變。2022年，臺灣社會正學習與病毒共存，為避免被感染之風險及不必要之困擾，許多喪家儀式都一切從簡。據黃瑞村法師表示：「<span class="bkt">除非是家中德高望重的長者，家屬才會辦超度儀式。因此生意有差。</span>」<sup>52</sup></li>
<li><strong>人物少</strong>：參與人數變少，即使是家眷，也僅是代表性的少數人參與儀式。</li>
<li><strong>時空變</strong>：以往私人拔度時間，通常由午齋前至隔天天亮，如今應家屬要求改為早上9點至晚上9點，甚至要求早上8點開始，晚上6點前結束。壇場亦有所改變，過去壇場較小，現今則較大，主要是為了排場及面子。</li>
<li><strong>音樂縮短</strong>：隨著儀式時間縮短、內容簡化，音樂唱腔因而變得更快，缺少了昔日之拉韻功夫。<sup>53</sup></li>
<li><strong>傳承少</strong>：現代社會少子化，年輕人有意從事此業者亦不多，故面臨傳承危機。但據田野調查，業者認為物傳有緣人，雖有意傳承之人較少，但並不擔心此學會失傳。</li>
<li><strong>釋教被混淆</strong>：因有些道壇兼作釋、道超度法會，或有混搭之情況，故常被一般人混淆。釋教與道教之超度法會有異，應不宜混淆，但各宗派若有交互影響之處，也是音樂變遷中不可避免之内化與涵化過程。</li>
</ol>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣民間釋教之繼傳統與改變，目前仍在内化、涵化持續變化中。</p>
